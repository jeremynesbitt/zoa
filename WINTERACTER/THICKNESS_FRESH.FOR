        SUBROUTINE THICKNESS_FRESH(J,TEMPTH)
        USE WINTERACTER
        IMPLICIT NONE
        INTEGER J
        REAL*8 TEMPTH
        INCLUDE 'RESOURCE.INC'
        INCLUDE 'DATLEN.INC'
        INCLUDE 'DATMAC.INC'
        INCLUDE 'DATMAI.INC'
        CALL WDIALOGSELECT(IDD_STHICKNESS)
        CALL WDIALOGPUTINTEGER(IDF_SURFNUM,J)
C
        TEMPTH=ALENS(3,J)
        CALL WDIALOGPUTDOUBLE(IDF_VAL,TEMPTH)
        CALL WDialogShow(-1,-1,0,Modeless)
                        RETURN
                        END
