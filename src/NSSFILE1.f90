SUBROUTINE PLTNSSRY
   USE NSSMOD
!
   use DATLEN
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS ROUTINE DOES THE PLOT RAY COMMAND AT THE CMD LEVEL
!
   real(real64) X,Y,Z,XN,YN,ZN,ROT1X,ROT1Z,ROT2Y,&
   &ROT2Z,AX,AY,AZ,AALF,APHI,YMAXI,YMINI,XMAXI,XMINI &
   &,XNEW,YNEW,LKG,VIEPH,VIEAL,NSSFACTOR,L,M,N
!
   INTEGER CN
!
   LOGICAL GGO,LONG
!
   INTEGER IX,IY,I,II,IPST,COLPAS,FLAG,JK
   INTEGER NSSP1ARRAY,ALLOERR,ICOUNT,IINEXT
   DIMENSION NSSP1ARRAY(:,:)
   ALLOCATABLE :: NSSP1ARRAY
!
!
!     LOOK.VIEW TRANSFORMS

   ROT1X(AX,AZ,APHI)=((AX*DCOS(APHI))-(AZ*DSIN(APHI)))
   ROT1Z(AX,AZ,APHI)=((AX*DSIN(APHI))+(AZ*DCOS(APHI)))
!
   ROT2Z(AZ,AY,AALF)=((AZ*DCOS(AALF))+(AY*DSIN(AALF)))
   ROT2Y(AZ,AY,AALF)=((-AZ*DSIN(AALF))+(AY*DCOS(AALF)))
   IF(.NOT.NEXISTN) THEN
      WRITE(OUTLYNE,*) 'NO NSS DATABASE EXISTS'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*) 'NO NSS RAY CAN BE DRAWN'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
   CALL NSS_VERTEX_SILENT
!
   DEALLOCATE(NSSP1ARRAY,STAT=ALLOERR)
   X=0.0D0
   Y=0.0D0
   VIEPH=(PII/180.0D0)*VIEPHI
   VIEAL=(PII/180.0D0)*VIEALF
!       NOW WE HAVE THE REAL WORLD COORDINATES OF THE RAY TO BE PLOTTED
!       THEY ARE STORED IN THE FILE NSSRHIST.DAT
!
!     HERE IS WHERE REAL WORK BEGINS. FIRST PROJECT THE 3D WORLD COORDINATES
!     OF THE VERTICIES ONTO THE 2D PLANE INDICATED BY THE PLOT LOOK/ PLOT VIEW
!     DATA. THE DIRECTION COSINES OF THE LOOK VECTOR ARE LOOKX, LOOKY AND
!     LOOKZ. THE TRANSFORMATIONS ARE:
!
   CALL NSSROT
!
!     AFTER THIS TRANSFORMATION, THE WORLD X COORDINATE IS PLOTTED IN THE
!     HORIZONTAL X-AXIS OF THE DISPLAY AND THE Y WORLD COORDINATE IS PLOTTED
!     IN THE VERTICAL Y-AXIS OF THE DISPLAY AFTER A CONVERSION TO
!     DEVICE INDEPENDENT COORDINATES ARE MADE.
!
   CALL NSSPLTSC
!
   ICOUNT=0
   OPEN(UNIT=92,FILE='NSSHT.DAT',STATUS='UNKNOWN')
   REWIND(UNIT=92)
5  CONTINUE
   CN=NSSWAVNUM
   READ(UNIT=92,FMT=*,END=6) I,X,Y,Z,L,M,N,CN
   ICOUNT=ICOUNT+1
   GO TO 5
6  CONTINUE
   CALL CLOSE_FILE(92,1)
   IF(ICOUNT.EQ.0) THEN
      WRITE(OUTLYNE,*) 'NO RAYS EXIST TO BE PLOTTED'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   ALLOCATE(NSSP1ARRAY(1:ICOUNT,1:3),STAT=ALLOERR)
   OPEN(UNIT=92,FILE='NSSHT.DAT',STATUS='UNKNOWN')
!     HERE IS THE LOOP WHICH PLOTS THE RAYS USING CALLS TO
!     PENMV2(IX,IY,IPST)
   REWIND(UNIT=92)
   DO II=1,ICOUNT
      READ(UNIT=92,FMT=*) I,X,Y,Z,L,M,N,CN
      IF(I.EQ.0) THEN
         IF(DABS(X).GT.1.0D10.OR.DABS(Y).GT.1.0D10.OR.&
         &DABS(Z).GT.1.0D10) THEN
            IINEXT=2
         ELSE
            IINEXT=1
         END IF
      END IF
      X=X-XROT
      Y=Y-YROT
      Z=Z-ZROT
      XN=ROT1X(X,Z,VIEPH)
      YN=Y
      ZN=ROT1Z(X,Z,VIEPH)
      X=XN
      Y=YN
      Z=ZN
!
      ZN=ROT2Z(Z,Y,VIEAL)
      YN=ROT2Y(Z,Y,VIEAL)
      XN=X
      X=XN
      Y=YN
      Z=ZN
!
!     RIGHT NOW,COORDINATES ARE IN WORLD COORDINATES
!     CONVERT THEM TO DEVICE INDEPENDENT GRAPHICS COORDINATES IN
!     TWO STEPS.
!
!     THE WORLD X PLOTS TO THE PLOTTER X
!     THE WORLD Y PLOTS TO THE PLOTTER Y
!
!     STEP 1: CONVERT USING AN APPROPRIATE SCALE FACTOR
!               CALCULATING AN APPROPRIATE FACTOR IF NECESSARY
!
      Y=(Y/NSSSCFA)*1000.0D0
!
      X=(X/NSSSCFA)*1000.0D0
!
!     STEP 2: APPLY THE SCREEN Y-OFFSET AND SCREEN GAMMA ROTATION
!
!     APPLY THE PLOT LOOK/VIEW Y-OFFSET VALUE
!
      IF(LORIENT) CALL ORSHIFT
      Y=Y+3500.0D0+DBLE(PYSHFT)
      X=X+DBLE(PXSHFT)
!
!     IF THERE ARE X-OFFSETS (JUSTIFICATION) TO APPLY, DO THEM HERE.
!
      IF(RCL.EQ.1.OR.RCL.EQ.-1) THEN
         JUSOFF=500.0D0-((XMINI/NSSSCFA)*1000.0D0)
         RCL=-1
      ELSE
      END IF
      IF(RCL.EQ.2.OR.RCL.EQ.-2) THEN
         RCL=-2
         JUSOFF=5000.0D0
      ELSE
      END IF
      IF(RCL.EQ.3.OR.RCL.EQ.-3) THEN
         JUSOFF=9500.0D0-((XMAXI/NSSSCFA)*1000.0D0)
         RCL=-3
      ELSE
      END IF
!
      X=X+JUSOFF
!
!     IF THERE IS A NON-ZERO GAMMA SPECIFIED IN PLOT LOOK OR PLOT VIEW THEN
!     ROTATE IN GAMMA ON THE SCREEN ABOUT THE CENTER OF THE SCREEN
!     WHICH HAS COORDINATES X=5000.0D0,Y=3500.0D0
!
!     FIRST SHIFT THE COORDINATE ORIGIN TO THE CETER OF THE DISPLAY
!
      X=X-5000.0D0
      Y=Y-3500.0D0
!     THE SCREEN COORDINATE IN real(real64) IN THE SHIFTED COORDINATE
!     FRAME IS NOW X AND Y

      IF(DBLE(PGAMMA).NE.0.0D0) THEN
         LKG=(PII/180.0D0)*DBLE(PGAMMA)
         XNEW=((X*DCOS(LKG))-(Y*DSIN(LKG)))
         YNEW=((X*DSIN(LKG))+(Y*DCOS(LKG)))
         X=XNEW
         Y=YNEW
      ELSE
      END IF

!     THE ROTATION IS DONE, NOW SHIFT THE ORIGIN BACK TO THE BOTTOM
!     LEFT HAND CORNER
      X=X+5000.0D0
      Y=Y+3500.0D0
!
!     PUT INSTRUCTIONS IN NSSP1ARRAY TO DROP PEN AND DRAW
      IPST=1
      LONG=.FALSE.
      IF(DABS(X).GT.2.0D9.OR.DABS(Y).GT.2.0D9) LONG=.TRUE.
      IF(X.LT.-2.0D9) X=-2.0D9
      IF(Y.LT.-2.0D9) Y=-2.0D9
      IF(X.GT.2.0D9) X=2.0D9
      IF(Y.GT.2.0D9) Y=2.0D9
      IX=INT(X)
      IY=INT(Y)
!     MOVE TO NEW POSITION WITH LOWERED PEN
      NSSP1ARRAY(II,1)=IX
      NSSP1ARRAY(II,2)=IY
      IF(I.EQ.0.OR.I.EQ.1.AND.IINEXT.EQ.2) THEN
         IF(IINEXT.EQ.2.AND.I.EQ.1) IINEXT=1
         NSSP1ARRAY(II,3)=0
      ELSE
         NSSP1ARRAY(II,3)=1
      END IF
      IF(.NOT.PLEXIS) PLEXIS=.TRUE.
      CONTINUE
   END DO
   CALL CLOSE_FILE(92,1)
!
!     NOW ISSUE THE PLOTTING COMMANDS STORED IN THE P1ARRAY ARRAY
!
   COLPAS=COLRAY
   CALL MY_COLTYP(COLPAS)
   NSSP1ARRAY(1,3)=0
   FIXUP=.FALSE.
   DO I=1,ICOUNT
      CALL PENMV2(NSSP1ARRAY(I,1),NSSP1ARRAY(I,2),NSSP1ARRAY(I,3))
   END DO
   COLPAS=COLLBL
   CALL MY_COLTYP(COLPAS)
   DEALLOCATE(NSSP1ARRAY,STAT=ALLOERR)
   RETURN
END
SUBROUTINE PLTNSSSPOTS(V1,V2)
   USE NSSMOD
!
   use DATLEN
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS ROUTINE DOES THE PLOT NSSSPOTS COMMAND AT THE CMD LEVEL ON SURFACE V1
!
   real(real64) X,Y,Z,XN,YN,ZN,ROT1X,ROT1Z,ROT2Y,&
   &ROT2Z,AX,AY,AZ,AALF,APHI,YMAXI,YMINI,XMAXI,XMINI &
   &,XNEW,YNEW,LKG,VIEPH,VIEAL,NSSFACTOR,V1,L,M,N,V2
!
   LOGICAL GGO
!
   INTEGER IX,IY,I,II,IPST,COLPAS,FLAG,JK
   INTEGER ICOUNT,BAR,CN
!
!
!     LOOK.VIEW TRANSFORMS

   ROT1X(AX,AZ,APHI)=((AX*DCOS(APHI))-(AZ*DSIN(APHI)))
   ROT1Z(AX,AZ,APHI)=((AX*DSIN(APHI))+(AZ*DCOS(APHI)))
!
   ROT2Z(AZ,AY,AALF)=((AZ*DCOS(AALF))+(AY*DSIN(AALF)))
   ROT2Y(AZ,AY,AALF)=((-AZ*DSIN(AALF))+(AY*DCOS(AALF)))
!
   BAR=INT(DABS(V2))
!
   CALL NSS_VERTEX_SILENT
!
   X=0.0D0
   Y=0.0D0
   VIEPH=(PII/180.0D0)*VIEPHI
   VIEAL=(PII/180.0D0)*VIEALF
!       NOW WE HAVE THE REAL WORLD COORDINATES OF THE RAY TO BE PLOTTED
!       THEY ARE STORED IN THE FILE NSSRHIST.DAT
!
!     HERE IS WHERE REAL WORK BEGINS. FIRST PROJECT THE 3D WORLD COORDINATES
!     OF THE VERTICIES ONTO THE 2D PLANE INDICATED BY THE PLOT LOOK/ PLOT VIEW
!     DATA. THE DIRECTION COSINES OF THE LOOK VECTOR ARE LOOKX, LOOKY AND
!     LOOKZ. THE TRANSFORMATIONS ARE:
!
   CALL NSSROT
!
!     AFTER THIS TRANSFORMATION, THE WORLD X COORDINATE IS PLOTTED IN THE
!     HORIZONTAL X-AXIS OF THE DISPLAY AND THE Y WORLD COORDINATE IS PLOTTED
!     IN THE VERTICAL Y-AXIS OF THE DISPLAY AFTER A CONVERSION TO
!     DEVICE INDEPENDENT COORDINATES ARE MADE.
!
   CALL NSSPLTSC
!
   ICOUNT=0
   OPEN(UNIT=92,FILE='NSSHT.DAT',STATUS='UNKNOWN')
   REWIND(UNIT=92)
5  CONTINUE
   READ(UNIT=92,FMT=*,END=6) I,X,Y,Z,L,M,N,CN
   ICOUNT=ICOUNT+1
   GO TO 5
6  CONTINUE
   CALL CLOSE_FILE(92,1)
   OPEN(UNIT=92,FILE='NSSHT.DAT',STATUS='UNKNOWN')
1  FORMAT(I5,D23.15,D23.15,D23.15,D23.15,D23.15,D23.15)
!     HERE IS THE LOOP WHICH PLOTS THE RAYS USING CALLS TO
!     PENMV2(IX,IY,IPST)
   REWIND(UNIT=92)
   DO II=1,ICOUNT
!     THE RAY IS A RAY AT THE SURFACE OF INTEREST
      READ(UNIT=92,FMT=*) I,X,Y,Z,L,M,N,CN
      IF(I.EQ.INT(V1)) THEN
         X=X-XROT
         Y=Y-YROT
         Z=Z-ZROT
         XN=ROT1X(X,Z,VIEPH)
         YN=Y
         ZN=ROT1Z(X,Z,VIEPH)
         X=XN
         Y=YN
         Z=ZN
!
         ZN=ROT2Z(Z,Y,VIEAL)
         YN=ROT2Y(Z,Y,VIEAL)
         XN=X
         X=XN
         Y=YN
         Z=ZN
!
!     RIGHT NOW,COORDINATES ARE IN WORLD COORDINATES
!     CONVERT THEM TO DEVICE INDEPENDENT GRAPHICS COORDINATES IN
!     TWO STEPS.
!
!     THE WORLD X PLOTS TO THE PLOTTER X
!     THE WORLD Y PLOTS TO THE PLOTTER Y
!
!     STEP 1: CONVERT USING AN APPROPRIATE SCALE FACTOR
!               CALCULATING AN APPROPRIATE FACTOR IF NECESSARY
!
         Y=(Y/NSSSCFA)*1000.0D0
!
         X=(X/NSSSCFA)*1000.0D0
!
!     STEP 2: APPLY THE SCREEN Y-OFFSET AND SCREEN GAMMA ROTATION
!
!     APPLY THE PLOT LOOK/VIEW Y-OFFSET VALUE
!
         IF(LORIENT) CALL ORSHIFT
         Y=Y+3500.0D0+DBLE(PYSHFT)
         X=X+DBLE(PXSHFT)
!
!     IF THERE ARE X-OFFSETS (JUSTIFICATION) TO APPLY, DO THEM HERE.
!
         IF(RCL.EQ.1.OR.RCL.EQ.-1) THEN
            JUSOFF=500.0D0-((XMINI/NSSSCFA)*1000.0D0)
            RCL=-1
         ELSE
         END IF
         IF(RCL.EQ.2.OR.RCL.EQ.-2) THEN
            RCL=-2
            JUSOFF=5000.0D0
         ELSE
         END IF
         IF(RCL.EQ.3.OR.RCL.EQ.-3) THEN
            JUSOFF=9500.0D0-((XMAXI/NSSSCFA)*1000.0D0)
            RCL=-3
         ELSE
         END IF
!
         X=X+JUSOFF
!
!     IF THERE IS A NON-ZERO GAMMA SPECIFIED IN PLOT LOOK OR PLOT VIEW THEN
!     ROTATE IN GAMMA ON THE SCREEN ABOUT THE CENTER OF THE SCREEN
!     WHICH HAS COORDINATES X=5000.0D0,Y=3500.0D0
!
!     FIRST SHIFT THE COORDINATE ORIGIN TO THE CETER OF THE DISPLAY
!
         X=X-5000.0D0
         Y=Y-3500.0D0
!     THE SCREEN COORDINATE IN real(real64) IN THE SHIFTED COORDINATE
!     FRAME IS NOW X AND Y

         IF(DBLE(PGAMMA).NE.0.0D0) THEN
            LKG=(PII/180.0D0)*DBLE(PGAMMA)
            XNEW=((X*DCOS(LKG))-(Y*DSIN(LKG)))
            YNEW=((X*DSIN(LKG))+(Y*DCOS(LKG)))
            X=XNEW
            Y=YNEW
         ELSE
         END IF

!     THE ROTATION IS DONE, NOW SHIFT THE ORIGIN BACK TO THE BOTTOM
!     LEFT HAND CORNER
         X=X+5000.0D0
         Y=Y+3500.0D0
!
!       PLOT THE SPOT
!       X AND Y ARE THE LOCATIONS OF THE RAY IN DEVICE DEPENDENT COORDINATES
         IPST=0
         IX=INT(X)
         IY=INT(Y)
!     MOVE TO NEW POSITION WITH RAISED PEN
!       PLOT SPOT HERE
!     NOW ISSUE THE PLOTTING COMMANDS STORED IN THE P1ARRAY ARRAY
!
         IF(CN.EQ.1)  COLPAS=(COLR1)
         IF(CN.EQ.2)  COLPAS=(COLR2)
         IF(CN.EQ.3)  COLPAS=(COLR3)
         IF(CN.EQ.4)  COLPAS=(COLR4)
         IF(CN.EQ.5)  COLPAS=(COLR5)
         IF(CN.EQ.6)  COLPAS=(COLR6)
         IF(CN.EQ.7)  COLPAS=(COLR7)
         IF(CN.EQ.8)  COLPAS=(COLR8)
         IF(CN.EQ.9)  COLPAS=(COLR9)
         IF(CN.EQ.10) COLPAS=(COLR10)
         CALL MY_COLTYP(COLPAS)
         CALL PENMV2(IX,IY,0)
         CALL PENMV2(IX+BAR,IY,1)
         CALL PENMV2(IX-BAR,IY,1)
         CALL PENMV2(IX,IY,0)
         CALL PENMV2(IX,IY+BAR,1)
         CALL PENMV2(IX,IY-BAR,1)
         CALL PENMV2(IX,IY,0)
         COLPAS=COLLBL
         CALL MY_COLTYP(COLPAS)
         IF(.NOT.PLEXIS) PLEXIS=.TRUE.
      ELSE
!       RAY WAS NOT AT THE SURFACE OF INTEREST
      END IF
   END DO
   CALL CLOSE_FILE(92,1)
!
   RETURN
END
SUBROUTINE NSSCLPTUB(JJ)
   USE NSSMOD
   USE GLOBALS
!
   use DATLEN
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
   real(real64) X,Y,Z,XN,YN,ZN,ROT1X,ROT1Z,ROT2Y,MDX,MDY,GAMGAM &
   &,ROT2Z,AX,AY,AZ,AALF,APHI,YMAXI,YMINI,XMAXI,XMINI &
   &,XNEW,YNEW,LKG,VIEPH,VIEAL,Z1,ANGLE,AN2,ZDELZ &
   &,X00,Y00,Z0,LX0,LY0,LZ0,ZCORR,SFI &
   &,X1,Y1,MX0,MY0,MZ0,NX0,NY0,NZ0,XID,YID
!
   INTEGER JJSTOP,J,IK,III,NO,JJ,ALLOERR,IIRUN,CLPTYPE
!
   INTEGER M1,M2,M3,M4,M5,IX,IY,I,II,IPST,K,L,POINT(1:2,1:3),N
!
   INTEGER SURFACEI
!
   INTEGER COLPAS
!
!
   REAL CLPDAT
   DIMENSION CLPDAT(:,:)
   ALLOCATABLE :: CLPDAT
!
!     LOOK.VIEW TRANSFORMS

   ROT1X(AX,AZ,APHI)=((AX*DCOS(APHI))-(AZ*DSIN(APHI)))
   ROT1Z(AX,AZ,APHI)=((AX*DSIN(APHI))+(AZ*DCOS(APHI)))
!
   ROT2Z(AZ,AY,AALF)=((AZ*DCOS(AALF))+(AY*DSIN(AALF)))
   ROT2Y(AZ,AY,AALF)=((-AZ*DSIN(AALF))+(AY*DCOS(AALF)))
!
!
   DEALLOCATE(CLPDAT,STAT=ALLOERR)
   ALLOCATE(CLPDAT(0:360,3),STAT=ALLOERR)
!
   VIEPH=(PII/180.0D0)*VIEPHI
   VIEAL=(PII/180.0D0)*VIEALF
!
   CLPDAT(0:360,1:3)=0.0
!
   X=0.0D0
   Y=0.0D0
   XID=0.0D0
   YID=0.0D0
!
!       ALL INPUT IS OK, KEEP GOING
!     THE ARRAY CONTAINING SURFACE CLAP DATA IS:
!     CLPDAT(0:360,1:3)
!
!     THE FIRST DIMENSION IS FOR THE DATA POINT NUMBER
!     THE SECOND DIMENSION IS FOR THE X,Y AND Z COORDINATES OR THE POINT
!     THE THIRD IS THE SURFACE NUMBER
!
!     WE NEED TO LOAD THE ARRAY BEFORE PLOTTING
!
!     THE PROCEDURE IS:
!
!     CYCLE THROUGH ALL THE SURFACES
!
!     1. WE WILL CLOCK AROUND THE CLEAR APERTURE FROM THE LOCAL +X
!     TOWARD THE LOCAL +Y AXIS,
!     4.0 DEGREE INCREMENTS AS MEASURED
!     BY AN OBSERVER AT THE SURFACE VERTEX, IN THE LOCAL COORDINATE
!     SYSTEM OF THE SURFACE, WITH THE OBSERVER FACING THE -Z AXIS
!     DIRECTION
   DO N=1,2
!       N=1 IS START OF TUBE
!       N=2 IS END OF TUBE
      DO J=0,360
         ANGLE=(DBLE(J)*PII)/180.0D0
         AN2=(DBLE(J+1)*PII)/180.0D0
!     NOW ALONG THIS ANGLED LINE, WHAT ARE THE X AND Y COORDINATES
!     OF THE CLEAR APERTURE
!
         CALL NSSCAO3(JJ,X,Y,ANGLE,AN2)
!
!     THE RETURNED X AND Y ARE WHERE THE SAG IS TO BE CALCULATED
!
!     2. ASSIGN SAG BASED ON N
         IF(N.EQ.1) Z=0.0D0
         IF(N.EQ.2) Z=DABS(NSSALENS(11,JJ))
!
!
!     ASSIGN ARRAY VALUES BASED ON J VALUE
         CLPDAT(J,1)=X
         CLPDAT(J,2)=Y
         CLPDAT(J,3)=Z
      END DO
!
!     3. THE ARRAYS NOW HAVE LOCAL X,Y AND Z VALUES STORED IN THEM
!     CONVERT THE LOCAL X ANY Y CLAPS TO GLOBAL NUMBERS
!     GLOBAL VERTEX DATA IS
      DO I=0,360
         X00=NSSVERTEX(1,JJ)
         Y00=NSSVERTEX(2,JJ)
         Z0 =NSSVERTEX(3,JJ)
         LX0=NSSVERTEX(4,JJ)
         MX0=NSSVERTEX(5,JJ)
         NX0=NSSVERTEX(6,JJ)
         LY0=NSSVERTEX(7,JJ)
         MY0=NSSVERTEX(8,JJ)
         NY0=NSSVERTEX(9,JJ)
         LZ0=NSSVERTEX(10,JJ)
         MZ0=NSSVERTEX(11,JJ)
         NZ0=NSSVERTEX(12,JJ)
         X=CLPDAT(I,1)
         Y=CLPDAT(I,2)
         Z=CLPDAT(I,3)
!
         X1=X00+((LX0*(X))+(LY0*(Y))&
         &+(LZ0*(Z)))
         Y1=Y00+((MX0*(X))+(MY0*(Y))&
         &+(MZ0*(Z)))
         Z1=Z0+((NX0*(X))+(NY0*(Y))&
         &+(NZ0*(Z)))
         CLPDAT(I,1)=X1
         CLPDAT(I,2)=Y1
         CLPDAT(I,3)=Z1
      END DO
!
!     4. NOW DETERMINE THE BEST PLACE FOR THE ROTATION POINT FOR
!               PLOT LOOK/VIEW
!
      CALL NSSROT
!
!     5.  CONVERT THE GLOBAL X AND Y CLAP VALUES
!               USING THE LOOK/VIEW VALUES
      DO I=0,360
         X=CLPDAT(I,1)
         Y=CLPDAT(I,2)
         Z=CLPDAT(I,3)
         X=X-XROT
         Y=Y-YROT
         Z=Z-ZROT
         XN=ROT1X(X,Z,VIEPH)
         YN=Y
         ZN=ROT1Z(X,Z,VIEPH)
         X=XN
         Y=YN
         Z=ZN
!
         ZN=ROT2Z(Z,Y,VIEAL)
         YN=ROT2Y(Z,Y,VIEAL)
         XN=X
         CLPDAT(I,1)=XN
         CLPDAT(I,2)=YN
         CLPDAT(I,3)=ZN
      END DO
!
!     THE ARRAYS NOW HAVE GLOBAL SURFACE CLAP DATA IN THEM
!
!     6.IF NEEDED, DETERMINE SCALE FACTORS AND PLOT RANGE
!
      CALL NSSPLTSC
!
!     7.CYCLE THROUGH THE THE ARRAYS, APPLY SCALE FACTORS
!
!     RIGHT NOW,COORDINATES ARE IN WORLD COORDINATES
!     CONVERT THEM TO DEVICE INDEPENDENT GRAPHICS COORDINATES IN
!     TWO STEPS.
!
!     THE WORLD X PLOTS TO THE PLOTTER X
!     THE WORLD Y PLOTS TO THE PLOTTER Y
!
!     STEP 1: CONVERT USING AN APPROPRIATE SCALE FACTOR
!               CALCULATING AN APPROPRIATE FACTOR IF NECESSARY
!
      DO I=0,360
         CLPDAT(I,1)=(CLPDAT(I,1)/NSSSCFA)*1000.0D0
         CLPDAT(I,2)=(CLPDAT(I,2)/NSSSCFA)*1000.0D0
      END DO
!
!     8. APPLY THE XSHIFT AND YSHIFT VALUES
      DO I=0,360
         IF(LORIENT) CALL ORSHIFT
         CLPDAT(I,1)=CLPDAT(I,1)+DBLE(PXSHFT)
         CLPDAT(I,2)=CLPDAT(I,2)+3500.0D0+DBLE(PYSHFT)
      END DO
!
!     9. SET THE PLOT JUSTIFICATION IF NEEDED
!     IF THERE ARE X-OFFSETS (JUSTIFICATION) TO APPLY, DO THEM HERE.
!
!     NOW
      IF(RCL.EQ.1.OR.RCL.EQ.-1) THEN
         JUSOFF=500.0D0-((XMINI/NSSSCFA)*1000.0D0)
         RCL=-1
      ELSE
      END IF
      IF(RCL.EQ.2.OR.RCL.EQ.-2) THEN
         RCL=-2
         JUSOFF=5000.0D0
      ELSE
      END IF
      IF(RCL.EQ.3.OR.RCL.EQ.-3) THEN
         JUSOFF=9500.0D0-((XMAXI/NSSSCFA)*1000.0D0)
         RCL=-3
      ELSE
      END IF
!
      DO I=0,360
         CLPDAT(I,1)=CLPDAT(I,1)+JUSOFF
      END DO
!     9. PLOT GAMMA
!     IF THERE IS A NON-ZERO GAMMA SPECIFIED IN PLOT LOOK OR PLOT VIEW THEN
!     ROTATE IN GAMMA ON THE SCREEN ABOUT THE CENTER OF THE SCREEN
!     WHICH HAS COORDINATES X=5000.0D0,Y=3500.0D0
!
!     FIRST SHIFT THE COORDINATE ORIGIN TO THE CETER OF THE DISPLAY
!
      DO I=0,360
         CLPDAT(I,1)=CLPDAT(I,1)-5000.0D0
         CLPDAT(I,2)=CLPDAT(I,2)-3500.0D0
      END DO
!     THE SCREEN COORDINATE IN real(real64) IN THE SHIFTED COORDINATE
!     FRAME IS NOW X AND Y

      IF(DBLE(PGAMMA).NE.0.0D0) THEN
         LKG=(PII/180.0D0)*DBLE(PGAMMA)

         DO I=0,360
            X=CLPDAT(I,1)
            Y=CLPDAT(I,2)
            XNEW=((X*DCOS(LKG))-(Y*DSIN(LKG)))
            YNEW=((X*DSIN(LKG))+(Y*DCOS(LKG)))
            CLPDAT(I,1)=XNEW
            CLPDAT(I,2)=YNEW
         END DO
      ELSE
      END IF
!     THE ROTATION IS DONE, NOW SHIFT THE ORIGIN BACK TO THE BOTTOM
!     LEFT HAND CORNER
      DO I=0,360
         CLPDAT(I,1)=CLPDAT(I,1)+5000.0D0
         CLPDAT(I,2)=CLPDAT(I,2)+3500.0D0
      END DO
!
!     NOW DRAW THE CLAP
!     WITH THE PEN UP, GO TO THE STARTING PLOT POSITION
      IF(.NOT.PLEXIS) PLEXIS=.TRUE.
      DO J=0,360
!     PUT INSTRUCTIONS IN P1ARAY TO DROP PEN AND DRAW
         IF(J.EQ.0) IPST=0
         IF(J.NE.0) IPST=1
         IF(CLPDAT(J,1).GT.1.0D6) CLPDAT(J,1)=1.0D6
         IF(CLPDAT(J,2).GT.1.0D6) CLPDAT(J,2)=1.0D6
         IF(CLPDAT(J,1).LT.-1.0D6) CLPDAT(J,1)=-1.0D6
         IF(CLPDAT(J,2).LT.-1.0D6) CLPDAT(J,2)=-1.0D6
         IX=INT(CLPDAT(J,1))
         IY=INT(CLPDAT(J,2))
         P1ARAY(J,1,1)=IX
         P1ARAY(J,2,1)=IY
         P1ARAY(J,3,1)=IPST
         IF(.NOT.PLEXIS) PLEXIS=.TRUE.
      END DO
!     FINISHED WITH THAT CLAP, LIFT PEN
      IPST=0
!     NOW ISSUE THE PLOTTING COMMANDS STORED IN THE P1ARAY ARRAY
!
!     LINE TYPE SETTING
      COLPAS=COLCLP
      CALL MY_COLTYP(COLPAS)
      OLLNTP=LNTYPE
      LNTYPE=0
      FIXUP=.FALSE.
      DO IK=0,360
         CALL PENMV2(P1ARAY(IK,1,1),P1ARAY(IK,2,1),P1ARAY(IK,3,1))
      END DO
      LNTYPE=OLLNTP
!
   END DO
   DEALLOCATE(CLPDAT,STAT=ALLOERR)
   RETURN
END
