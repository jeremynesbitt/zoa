SUBROUTINE NSSTRACE
   USE NSSMOD
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
   INTEGER NREF,NOBJ,I,J,K,L,M
   real(real64) ODEL,RDEL,OCENX,OCENY,OCENZ,MAG,XSTART,YSTART,ZSTART
   real(real64) OX,OY,OZ,RX,RY,RZ,LSTART,MSTART,NSTART,GOX,GOY,GRX,GRY
   real(real64) OGRID_SPAN,RGRID_SPAN,XANG,YANG,RSHAPE,GRZ,GOZ,SSHAPE
   real(real64) OOX,OOY,ORX,ORY,FRX,FRY,RCENX,RCENY,RCENZ
   LOGICAL TESTSHAPES,TESTSHAPER
!       THIS CONTROLS THE NSS DATABASE RAY TRACE
!       CALCULATE THE STARTING RAY LOCATION AND DIRECTION COSINES
!       IN GLOBAL COORDINATES
!
   RHISTN=0
!       OBJECT GRID SPACING IS ODEL
   ODEL=NSSSYSTEM(37)
!       DIMENSION OF THE OBJECT GRID IS NOBJ
   NOBJ=INT(NSSSYSTEM(32))
!       CENTER OF OBJECT GRID IS LOCATED AT OCENX,OCENY,OCENZ
   OCENX=NSSSYSTEM(29)
   OCENY=NSSSYSTEM(30)
   OCENZ=NSSSYSTEM(31)
!       CENTER OF REFERENCE GRID IS LOCATED AT RCENX,RCENY,RCENZ
   RCENX=NSSSYSTEM(40)
   RCENY=NSSSYSTEM(41)
   RCENZ=NSSSYSTEM(42)
!       THE DIRECTION COSINES OF THE LINE CONNECTING THE CENTER OF THE OBJECT GRID
!       WITH THE CENTER OF THEREFERENCE GRID ARE: OBJL, OBJM AND OBJN
   OMAG=DSQRT(((RCENX-OCENX)**2)+((RCENY-OCENY)**2)+&
   &((RCENZ-OCENZ)**2))
   IF(OMAG.EQ.0.0D0) THEN
      WRITE(OUTLYNE,*) 'OBJECT AND REFERENCE GRIDS ARE CO-LOCATED'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*) 'NO RAYS MAY BE TRACED'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
   OBJL=(RCENX-OCENX)/OMAG
   OBJM=(RCENY-OCENY)/OMAG
   OBJN=(RCENZ-OCENZ)/OMAG
!       DETERMINE THE ALPHA,BETA AND GAMMA ROTATION ANGLES WHICH CAN PRODUCE THESE
!       DIRECTION COSINES.
   CALL GETOANGLES
!       OALPHA,OBETA AND OGAMMA ARE THE EULER ROTATION ANGLES IN CODE-V SIGN CONVENTION
!       WHICH ROTATE THE 0,0,1 INTO OBJL,OBJM AND OBJN. THESE WILL BE USED TO CONVERT OX,OY AND OZ
!       FROM THE LOCAL OBJECT AND REFERENCE GRIDS INTO GLOBAL COORDINATES IN ORDER TO GET GLOBAL
!       RAY STARTING POSITIONS AND DIRECTION COSINES.
!
!       REFERENCE GRID SPACING IS RDEL
   RDEL=NSSSYSTEM(38)
!       DIMENSION OF THE OBJECT GRID IS NOBJ
   NREF=INT(NSSSYSTEM(36))
!       CENTER OF REFERENCE GRID IS LOCATED AT RCENX,RCENY AND RCENZ
!       DO A RAY'S STARTING DATA,IN GLOBAL COORDINATES THEN TRACE THE RAY
!       THE OBJECT GRID POINTS SPAN A SEMI-DISTANCE OF DBLE(NOBJ-1)*ODEL/2
!       IN GLOBAL COORDINATES, THE LOCATION OF THE STARTING POINTS ARE:
   IF(DABS(DBLE(NOBJ/2)-(DBLE(NOBJ)/2.0D0)).EQ.0.0D0) THEN
      OBJODD=.FALSE.
   ELSE
      OBJODD=.TRUE.
   END IF
   IF(DABS(DBLE(NREF/2)-(DBLE(NREF)/2.0D0)).NE.0.0D0) THEN
      REFODD=.FALSE.
   ELSE
      REFODD=.TRUE.
   END IF
!     IN THE LOCAL COORDINATE SYSTEM OF THE OBJECT PLANE WHICH
!     IS PERPENDICULAR TO THE LINE CONNECTING THE CENTERS OF THE OBJECT AND REFERENCE GRID
!     THE X AND Y NEGATIVE LIMITA ARE OOX AND OOY
   IF(OBJODD) THEN
!       ODD GRID
      OGRID_SPAN=DBLE(NOBJ-1)*ODEL/2.0D0
      OOX=-OGRID_SPAN
      OOY=-OGRID_SPAN
      OZ=OCENZ
      GOZ=OCENZ
   ELSE
!       EVEN GRID
      OGRID_SPAN=DBLE(NOBJ-1)*ODEL/2.0D0
      OOX=-OGRID_SPAN
      OOY=-OGRID_SPAN
      OZ=OCENZ
      GOZ=OCENZ
   END IF
   TESTSHAPES=.FALSE.
   IF(NSSSYSTEM(33).EQ.0.0D0) TESTSHAPES=.TRUE.
   IF(TESTSHAPES) THEN
      SSHAPE=DABS(OOX)
      IF(DABS(OOY).GT.DABS(OOX)) SSHAPE=DABS(OOY)
   END IF
!       THE REFERENCE GRID POINTS SPAN A SEMIDISTANCE OF DBLE(NREF-1)*RDEL/2
!     IN THE LOCAL COORDINATE SYSTEM OF THE REFERENCE PLANE WHICH
!     IS PERPENDICULAR TO THE LINE CONNECTING THE CENTERS OF THE OBJECT AND REFERENCE GRID
!     THE X AND Y NEGATIVE LIMITA ARE OOX AND OOY
   IF(REFODD) THEN
!       ODD GRID
      RGRID_SPAN=DBLE(NREF-1)*RDEL/2.0D0
      ORX=-RGRID_SPAN
      ORY=-RGRID_SPAN
      RZ=RCENZ
      GRZ=RCENZ
   ELSE
!       EVEN GRID
      RGRID_SPAN=DBLE(NREF-1)*RDEL/2.0D0
      ORX=-RGRID_SPAN
      ORY=-RGRID_SPAN
      RZ=RCENZ
      GRZ=RCENZ
   END IF
   TESTSHAPER=.FALSE.
   IF(NSSSYSTEM(22).EQ.0.0D0) TESTSHAPER=.TRUE.
   IF(TESTSHAPER) THEN
      RSHAPE=DABS(ORX)
      IF(DABS(ORY).GT.DABS(ORX)) RSHAPE=DABS(ORY)
   END IF
!       INITIALIZE THE RAY HISTORY FILES
   OPEN(UNIT=48,FILE='NSSRHIST.DAT')
   OPEN(UNIT=91,FILE='NSSHIST.DAT')
   OPEN(UNIT=92,FILE='NSSHT.DAT')
   CALL CLOSE_FILE(48,0)
   CALL CLOSE_FILE(91,0)
   CALL CLOSE_FILE(92,0)
   OPEN(UNIT=48,FILE='NSSRHIST.DAT')
   OPEN(UNIT=91,FILE='NSSHIST.DAT')
   OPEN(UNIT=92,FILE='NSSHT.DAT')
!
!       THE OUTER MOST LOOP, LOOPS THROUGH THE ACTIVE WAVELEGTNS
   DO M=1,10
      IF(NSSSYSTEM(M).NE.0.0D0.AND.NSSSYSTEM(M+10).NE.0.0D0) THEN
         NSSWAVL=NSSSYSTEM(M)
         CURWAVELENGTH=NSSWAVL
         NSSWVWT=NSSSYSTEM(M+10)
         NSSWAVNUM=M
!       TRACE THAT WAVELENGTH
         DO I=1,NOBJ
            OX=OOX+(DBLE(I-1)*ODEL)
            OY=OOY
            DO J=1,NOBJ
               OY=OOY+(DBLE(J-1)*ODEL)
               IF(TESTSHAPES) THEN
                  IF(DSQRT((OX**2)+(OY**2)).GT.SSHAPE) THEN
!       SKIP TRACING THIS RAY. IT DOES NOT GO INTO THE RAY HISTORY
                     CYCLE
                  END IF
               END IF
               DO K=1,NREF
                  RX=ORX+(DBLE(K-1)*RDEL)
                  RY=ORY
                  DO L=1,NREF
                     RY=ORY+(DBLE(L-1)*RDEL)
                     IF(TESTSHAPER) THEN
                        IF(DSQRT((RX**2)+(RY**2)).GT.RSHAPE) THEN
!       SKIP TRACING THIS RAY. IT DOES NOT GO INTO THE RAY HISTORY
                           CYCLE
                        END IF
                     END IF
!       STARTING RAY COORDINATES ARE XSTART,YSTART,ZSTART
!
!       NOW CONVERT THE OX, OY AND RX AND RY FROM THE LOCAL COORDINATES OF THEIR
!       RESPECTIVE GRID PLANES INTO GLOBAL COORDINATES
!
                     CALL OXOYRXRY_LOCAL_TO_GLOBAL(OX,OY,RX,RY,GOX,GOY,GRX,GRY)
!
!       NOW ADD THE GLOBAL OBJECT AND REFERENCE GRID CENTERS
                     GOX=GOX+OCENX
                     GOY=GOY+OCENY
                     GRX=GRX+RCENX
                     GRY=GRY+RCENY
!       WE ALREADY HAD OZ AND RZ
!
!       PROCEED WITH THE RAY TRACE
!
                     XSTART=GOX
                     YSTART=GOY
                     ZSTART=GOZ
!       STARTING RAY DIRECTION COSINES ARE:
                     LSTART=(GRX-GOX)
                     MSTART=(GRY-GOY)
                     NSTART=(GRZ-GOZ)
                     MAG=DSQRT((LSTART**2)+(MSTART**2)+(NSTART**2))
                     LSTART=(GRX-GOX)/MAG
                     MSTART=(GRY-GOY)/MAG
                     NSTART=(GRZ-GOZ)/MAG
!       FRACTIONAL REFERENCE GRID COORDINATES ARE FRX,FRY
                     FRX=GRX/ORX
                     FRY=GRY/ORY
                     IF(ORX.EQ.0.0D0) FRX=0.0D0
                     IF(ORY.EQ.0.0D0) FRY=1.0D0
!       WE NOW HAVE THE STARTING RAY COORDINATES AND DIRECTION COSINES
!       IN GLOBAL COORDINATES FOR A RAY.
!       CALCULATE THE PERPENDICULARS TO THIS STARTING RAY BECAUSE
!       THESE MUST BE TRACKED FOR POLARIZATION.
!       THEN CALL NSSRAY INORDER TO ACTUALLY PERFOM
!       THE NSS RAY TRACE FOR THE SPECIFIC RAY.
!       WE START AT THE OBJECT SURFACE WITH THESE STARTING VALUES AND TRACE
!       THE RAY THROUGH THE SYSTEM. tHE STARTING RAY DATA IN THE GLOBAL COORDINATE
!       SYSTEM IS KEPT IN THE START ARRAY, THE CORRESPONDING RAY DATA AT A SURFACE
!       IN THE SURFACE COORDINATE SYSTEM ID KEPT IN STARTP, BOTH PASSED IN NSSMOD MODULE
                     START(1)=XSTART
                     START(2)=YSTART
                     START(3)=ZSTART
                     START(4)=LSTART
                     START(5)=MSTART
                     START(6)=NSTART
                     IF(NSSSYSTEM(34).EQ.-1.0D0) THEN
!       THE OBJECT IS VIRTUAL AND THE RAY REALLY STARTS AT THE REFERENCE GRID LOCATION
!       RX,RY,RZ (ADDED 4/13/2004)
                        START(1)=GRX
                        START(2)=GRY
                        START(3)=GRZ
                        START(4)=LSTART
                        START(5)=MSTART
                        START(6)=NSTART
                     END IF
                     IF(START(6).EQ.0.0D0) THEN
                        YANG=PII/2.0D0
                        XANG=PII/2.0D0
                     ELSE
                        IF(DABS(START(5)).LE.1.0D-15.AND.DABS(START(6)).LE.1.0D-15) THEN
                           YANG=0.0D0
                        ELSE
                           YANG=DATAN2(START(5),START(6))
                        END IF
                        IF(DABS(START(4)).LE.1.0D-15.AND.DABS(START(6)).LE.1.0D-15) THEN
                           XANG=0.0D0
                        ELSE
                           XANG=DATAN2(START(4),START(6))
                        END IF
                     END IF
                     START(7)=(1.0D0*DCOS(XANG))
                     START(8)=0.0D0
                     START(9)=-(1.0D0*DSIN(XANG))
                     START(10)=0.0D0
                     START(11)=(1.0D0*DCOS(XANG))
                     START(12)=-(1.0D0*DSIN(YANG))
                     START(13)=0.0D0
                     START(14)=0.0D0
                     START(15)=1.0D0
!
!       1. INITIALIZE ALL STARTING RAY DAYA
                     CALL INITIALIZE_RAY(FRX,FRY)
                     CURINDEXR=CURINDEX(M)
                     PREVINDEXR=CURINDEX(M)
                     CURINDEXI=CURINDEX(M+10)
                     PREVINDEXI=CURINDEX(M+10)
!       CURINDEX() AND PREVINDEXR AND I AND CURINDEXR AND I
!       WILL BE UPDATED AFTER THE COMPLETION OF EACH SURFACE INTERACTION.
!       TRACE THE NON-SEQUENTIAL RAY
                     RHISTN=RHISTN+1
                     CALL NSSRAY
                  END DO
               END DO
            END DO
         END DO
      ELSE
!       SKIP AND GO TO NEXT WAVELENGTH
      END IF
!       DO THE NEXT WAVELENGTH
   END DO
   CALL CLOSE_FILE(48,1)
   CALL CLOSE_FILE(91,1)
   CALL CLOSE_FILE(92,1)
!                       RETURN
END
SUBROUTINE INITIALIZE_RAY(FRX,FRY)
   USE NSSMOD
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
   INTEGER I
   real(real64) FRX,FRY,APX,APR
   NSSRAYFAIL=.FALSE.
   NSSRAYFAILCODE(1)=0
   NSSRAYFAILCODE(2)=0
!       THE RAY IS AT THE CORRECT LOCATION AT THE REFERENCE GRID LOCATION.
   OLD_LTEST=0.0D0
!       SNUM IS THE SURFACE NUMBER FOR THE NEXT SURFACE INTERACTION
!       AFTER OPTIMAL RAY INTERSECTION
   SNUM=-1
!       RAY ENERGY INCLUDING GAUSSIAN RAY APODIZATION TERM
!       ADDED 4/1/2004
   NSSRAY_ENERGY=1.0D0
   APX=-DLOG(10.0D0**(-DABS(NSSSYSTEM(23))/10.0D0))
   APR=(FRX**2)+(FRY**2)
   NSSRAY_ENERGY=NSSRAY_ENERGY*DEXP(-APX*APR)
   START(16)=NSSRAY_ENERGY

   NSSRAY_HITS=0
!       RAY POLARIZATION DATA
   NSSRAY_RPOL(1:20)=0.0D0
   NSSRAY_IPOL(1:20)=0.0D0
!       RAY EXISTENCE STATUS (ASSUME IT EXISTS)
   NSSRAY_EXIST=.TRUE.
!       CURRENT RAY DATA IS AT SURFACE 0 AT STARTING SOURCE LOCATION
   NSS_GRAY(1:50)=START(1:50)
   NSS_GRAYP(1:50)=START(1:50)
   NSS_LRAY(1:50)=START(1:50)
   NSS_LRAYP(1:50)=START(1:50)
   CURGLASS(1)=NSSOGLASS(1)
   CURGLASS(2)=NSSOGLASS(2)
   CURINDEX(1:20)=OBJINDEX(1:20)
   RETURN
END
