   ROT1X(AX,AZ,APHI)=((AX*DCOS(APHI))-(AZ*DSIN(APHI)))
   ROT1Z(AX,AZ,APHI)=((AX*DSIN(APHI))+(AZ*DCOS(APHI)))
!
   ROT2Z(AZ,AY,AALF)=((AZ*DCOS(AALF))+(AY*DSIN(AALF)))
   ROT2Y(AZ,AY,AALF)=((-AZ*DSIN(AALF))+(AY*DCOS(AALF)))
!
   DEALLOCATE(CLPDAT,STAT=ALLOERR)
   ALLOCATE(CLPDAT(0:360,3),STAT=ALLOERR)
!
   VIEPH=(PII/180.0D0)*VIEPHI
   VIEAL=(PII/180.0D0)*VIEALF
!
   CLPDAT(0:360,1:3)=0.0
!
   X=0.0D0
   Y=0.0D0
   XID=0.0D0
   YID=0.0D0
!
!       ALL INPUT IS OK, KEEP GOING
!     THE ARRAY CONTAINING SURFACE CLAP DATA IS:
!     CLPDAT(0:360,1:3)
!
!     THE FIRST DIMENSION IS FOR THE DATA POINT NUMBER
!     THE SECOND DIMENSION IS FOR THE X,Y AND Z COORDINATES OR THE POINT
!     THE THIRD IS THE SURFACE NUMBER
!
!     WE NEED TO LOAD THE ARRAY BEFORE PLOTTING
!
!     THE PROCEDURE IS:
!
!     CYCLE THROUGH ALL THE SURFACES
!
!     1. WE WILL CLOCK AROUND THE CLEAR APERTURE FROM THE LOCAL +X
!     TOWARD THE LOCAL +Y AXIS,
!     4.0 DEGREE INCREMENTS AS MEASURED
!     BY AN OBSERVER AT THE SURFACE VERTEX, IN THE LOCAL COORDINATE
!     SYSTEM OF THE SURFACE, WITH THE OBSERVER FACING THE -Z AXIS
!     DIRECTION
!
   DO J=0,360
      ANGLE=(DBLE(J)*PII)/180.0D0
      AN2=(DBLE(J+1)*PII)/180.0D0
!     NOW ALONG THIS ANGLED LINE, WHAT ARE THE X AND Y COORDINATES
!     OF THE CLEAR APERTURE
!
      CALL NSSCAO2(JJ,X,Y,ANGLE,AN2)
!
!     THE RETURNED X AND Y ARE WHERE THE SAG IS TO BE CALCULATED
!
!     2. USE APPROPRIATE CALLS TO THE NSSSAG ROUTINE
!
      CALL NSSSAG(JJ,X,Y,Z)
!
!     ASSIGN ARRAY VALUES BASED ON J VALUE
      CLPDAT(J,1)=X
      CLPDAT(J,2)=Y
      CLPDAT(J,3)=Z
   END DO
!
!     3. THE ARRAYS NOW HAVE LOCAL X,Y AND Z VALUES STORED IN THEM
!     CONVERT THE LOCAL X ANY Y CLAPS TO GLOBAL NUMBERS
!     GLOBAL VERTEX DATA IS
   DO I=0,360
      X00=NSSVERTEX(1,JJ)
      Y00=NSSVERTEX(2,JJ)
      Z0 =NSSVERTEX(3,JJ)
      LX0=NSSVERTEX(4,JJ)
      MX0=NSSVERTEX(5,JJ)
      NX0=NSSVERTEX(6,JJ)
      LY0=NSSVERTEX(7,JJ)
      MY0=NSSVERTEX(8,JJ)
      NY0=NSSVERTEX(9,JJ)
      LZ0=NSSVERTEX(10,JJ)
      MZ0=NSSVERTEX(11,JJ)
      NZ0=NSSVERTEX(12,JJ)
      X=CLPDAT(I,1)
      Y=CLPDAT(I,2)
      Z=CLPDAT(I,3)
!
      X1=X00+((LX0*(X))+(LY0*(Y))&
      &+(LZ0*(Z)))
      Y1=Y00+((MX0*(X))+(MY0*(Y))&
      &+(MZ0*(Z)))
      Z1=Z0+((NX0*(X))+(NY0*(Y))&
      &+(NZ0*(Z)))
      CLPDAT(I,1)=X1
      CLPDAT(I,2)=Y1
      CLPDAT(I,3)=Z1
   END DO
!
!     4. NOW DETERMINE THE BEST PLACE FOR THE ROTATION POINT FOR
!               PLOT LOOK/VIEW
!
   CALL NSSROT
!
!     5.  CONVERT THE GLOBAL X AND Y CLAP VALUES
!               USING THE LOOK/VIEW VALUES
   DO I=0,360
      X=CLPDAT(I,1)
      Y=CLPDAT(I,2)
      Z=CLPDAT(I,3)
      X=X-XROT
      Y=Y-YROT
      Z=Z-ZROT
      XN=ROT1X(X,Z,VIEPH)
      YN=Y
      ZN=ROT1Z(X,Z,VIEPH)
      X=XN
      Y=YN
      Z=ZN
!
      ZN=ROT2Z(Z,Y,VIEAL)
      YN=ROT2Y(Z,Y,VIEAL)
      XN=X
      CLPDAT(I,1)=XN
      CLPDAT(I,2)=YN
      CLPDAT(I,3)=ZN
   END DO
!
!     THE ARRAYS NOW HAVE GLOBAL SURFACE CLAP DATA IN THEM
!
!     6.IF NEEDED, DETERMINE SCALE FACTORS AND PLOT RANGE
!
   CALL NSSPLTSC
!
!     7.CYCLE THROUGH THE THE ARRAYS, APPLY SCALE FACTORS
!
!     RIGHT NOW,COORDINATES ARE IN WORLD COORDINATES
!     CONVERT THEM TO DEVICE INDEPENDENT GRAPHICS COORDINATES IN
!     TWO STEPS.
!
!     THE WORLD X PLOTS TO THE PLOTTER X
!     THE WORLD Y PLOTS TO THE PLOTTER Y
!
!     STEP 1: CONVERT USING AN APPROPRIATE SCALE FACTOR
!               CALCULATING AN APPROPRIATE FACTOR IF NECESSARY
!
   DO I=0,360
      CLPDAT(I,1)=(CLPDAT(I,1)/NSSSCFA)*1000.0D0
      CLPDAT(I,2)=(CLPDAT(I,2)/NSSSCFA)*1000.0D0
   END DO
!
!     8. APPLY THE XSHIFT AND YSHIFT VALUES
   DO I=0,360
      IF(LORIENT) CALL ORSHIFT
      CLPDAT(I,1)=CLPDAT(I,1)+DBLE(PXSHFT)
      CLPDAT(I,2)=CLPDAT(I,2)+3500.0D0+DBLE(PYSHFT)
   END DO
!
!     9. SET THE PLOT JUSTIFICATION IF NEEDED
!     IF THERE ARE X-OFFSETS (JUSTIFICATION) TO APPLY, DO THEM HERE.
!
!     NOW
   IF(RCL.EQ.1.OR.RCL.EQ.-1) THEN
      JUSOFF=500.0D0-((XMINI/NSSSCFA)*1000.0D0)
      RCL=-1
   ELSE
   END IF
   IF(RCL.EQ.2.OR.RCL.EQ.-2) THEN
      RCL=-2
      JUSOFF=5000.0D0
   ELSE
   END IF
   IF(RCL.EQ.3.OR.RCL.EQ.-3) THEN
      JUSOFF=9500.0D0-((XMAXI/NSSSCFA)*1000.0D0)
      RCL=-3
   ELSE
   END IF
!
   DO I=0,360
      CLPDAT(I,1)=CLPDAT(I,1)+JUSOFF
   END DO
!     9. PLOT GAMMA
!     IF THERE IS A NON-ZERO GAMMA SPECIFIED IN PLOT LOOK OR PLOT VIEW THEN
!     ROTATE IN GAMMA ON THE SCREEN ABOUT THE CENTER OF THE SCREEN
!     WHICH HAS COORDINATES X=5000.0D0,Y=3500.0D0
!
!     FIRST SHIFT THE COORDINATE ORIGIN TO THE CETER OF THE DISPLAY
!
   DO I=0,360
      CLPDAT(I,1)=CLPDAT(I,1)-5000.0D0
      CLPDAT(I,2)=CLPDAT(I,2)-3500.0D0
   END DO
!     THE SCREEN COORDINATE IN real(real64) IN THE SHIFTED COORDINATE
!     FRAME IS NOW X AND Y

   IF(DBLE(PGAMMA).NE.0.0D0) THEN
      LKG=(PII/180.0D0)*DBLE(PGAMMA)

      DO I=0,360
         X=CLPDAT(I,1)
         Y=CLPDAT(I,2)
         XNEW=((X*DCOS(LKG))-(Y*DSIN(LKG)))
         YNEW=((X*DSIN(LKG))+(Y*DCOS(LKG)))
         CLPDAT(I,1)=XNEW
         CLPDAT(I,2)=YNEW
      END DO
   ELSE
   END IF
!     THE ROTATION IS DONE, NOW SHIFT THE ORIGIN BACK TO THE BOTTOM
!     LEFT HAND CORNER
   DO I=0,360
      CLPDAT(I,1)=CLPDAT(I,1)+5000.0D0
      CLPDAT(I,2)=CLPDAT(I,2)+3500.0D0
   END DO
!
!     NOW DRAW THE CLAP
!     WITH THE PEN UP, GO TO THE STARTING PLOT POSITION
   IF(.NOT.PLEXIS) PLEXIS=.TRUE.
   DO J=0,360
!     PUT INSTRUCTIONS IN P1ARAY TO DROP PEN AND DRAW
      IF(J.EQ.0) IPST=0
      IF(J.NE.0) IPST=1
      IF(CLPDAT(J,1).GT.1.0D6) CLPDAT(J,1)=1.0D6
      IF(CLPDAT(J,2).GT.1.0D6) CLPDAT(J,2)=1.0D6
      IF(CLPDAT(J,1).LT.-1.0D6) CLPDAT(J,1)=-1.0D6
      IF(CLPDAT(J,2).LT.-1.0D6) CLPDAT(J,2)=-1.0D6
      IX=INT(CLPDAT(J,1))
      IY=INT(CLPDAT(J,2))
      P1ARAY(J,1,1)=IX
      P1ARAY(J,2,1)=IY
      P1ARAY(J,3,1)=IPST
      IF(.NOT.PLEXIS) PLEXIS=.TRUE.
   END DO
!     FINISHED WITH THAT CLAP, LIFT PEN
   IPST=0
!     NOW ISSUE THE PLOTTING COMMANDS STORED IN THE P1ARAY ARRAY
!
!     LINE TYPE SETTING
   COLPAS=COLCLP
   CALL MY_COLTYP(COLPAS)
   OLLNTP=LNTYPE
   LNTYPE=0
   FIXUP=.FALSE.
   DO IK=0,360
      CALL PENMV2(P1ARAY(IK,1,1),P1ARAY(IK,2,1),P1ARAY(IK,3,1))
   END DO
   LNTYPE=OLLNTP
!
   DEALLOCATE(CLPDAT,STAT=ALLOERR)
   RETURN
END
SUBROUTINE NSSPRO(JJ,V3)
   USE NSSMOD
   USE GLOBALS
!
   use DATLEN
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS ROUTINE DOES THE PLOTTING OF SURFACE PROFILES FOR THE NSS DATABASE
!
   real(real64) X,Y,Z,XN,YN,ZN,ROT1X,ROT1Z,ROT2Y,XX1,XX2,YY1,V1,V2,V3,&
   &ROT2Z,AX,AY,AZ,AALF,APHI,YMAXI,YMINI,XMAXI,XMINI,RANGE &
   &,XNEW,YNEW,LKG,VIEPH,VIEAL,Z1,YY2,AX1,AX2,AY1,AY2,ZDELZ &
   &,X00,Y00,Z0,LX0,LY0,LZ0,ACALL1,ACALL2,XM,YM,ZCORR,ZDELZ1 &
   &,X1,X2,Y1,Y2,SLOPE,DELXX,DELYY,MX0,MY0,MZ0,NX0,NY0,NZ0
!
   LOGICAL ALT,VERT,INSIT,YESONE1,YESONE2,YESONE3,YESONE4 &
   &,YESONE5,YESONE6,NSSINSIDEIT
!
   COMMON/YESSIR/YESONE1,YESONE2,YESONE3,YESONE4,YESONE5,YESONE6
!
   EXTERNAL NSSINSIDEIT
!
   INTEGER M1,M2,M3,M4,CAFLG,COFLG,J,K,IK,III,NO,ALLOERR &
   &,COLPAS,JJ
!
   real(real64) XMIN,YMIN,XMAX,YMAX,ZA,ZB,ZM,FRACRAD,&
   &XMINO,YMINO,XMAXO,YMAXO,DRAPRO,THETA &
   &,TEMP,YMIN2,XMIN2,YMAX2,XMAX2,CALL1,CALL2
!
   INTEGER IX,IY,I,II,IPST
!
!
   real(real64) PRO
!
   DIMENSION PRO(:,:)
   ALLOCATABLE :: PRO
!     LOOK.VIEW TRANSFORMS

   ROT1X(AX,AZ,APHI)=((AX*DCOS(APHI))-(AZ*DSIN(APHI)))
   ROT1Z(AX,AZ,APHI)=((AX*DSIN(APHI))+(AZ*DCOS(APHI)))
!
   ROT2Z(AZ,AY,AALF)=((AZ*DCOS(AALF))+(AY*DSIN(AALF)))
   ROT2Y(AZ,AY,AALF)=((-AZ*DSIN(AALF))+(AY*DCOS(AALF)))
!
   M1=360
   M2=4
   DEALLOCATE (PRO,STAT=ALLOERR)
   ALLOCATE (PRO(M1,M2),STAT=ALLOERR)
   PRO(1:360,1:4)=0.0D0
!
   X=0.0D0
   Y=0.0D0
!
   VIEPH=(PII/180.0D0)*VIEPHI
   VIEAL=(PII/180.0D0)*VIEALF
!
!       USES GLOBAL NSSVERTEX ARRAY
!
!     CONVERT THETA TO RADIAN MEASURE
   IF(V3.GE.89.95D0.AND.V3.LE.90.05D0) V3=90.001D0
   IF(V3.GE.269.95D0.AND.V3.LE.270.05D0) V3=270.001D0
   THETA=V3*PII/180.0D0
!     THE ARRAY CONTAINING SURFACE PROFILE DATA ARE:
!
!     PRO(1:360,1:3)
!
!     THE FIRST DIMENSION IS FOR THE DATA POINT NUMBER
!     THE SECOND DIMENSION IS FOR THE X,Y AND Z COORDINATES OR THE POINT
!
!     WE NEED TO LOAD THE ARRAY BEFORE PLOTTING
!
!     THE PROCEDURE IS:
!     1. DETERMINE THE MAXIMUM AND MINIMUM LOCAL X AND Y
!               COORDINATES FOR A SURFACE FOR WHICH THE PROFILE
!               IS TO BE CALCULATED.
!
!     CYCLE THROUGH ALL THE SURFACES BUT SKIP SURFACES WITH MULTIPLE
!     APERTURE DEFS ON IT
!
   II=JJ
   YESONE1=.FALSE.
   YESONE2=.FALSE.
   YESONE3=.FALSE.
   YESONE4=.FALSE.
   YESONE5=.FALSE.
   YESONE6=.FALSE.
   X1=0.0D0
   X2=0.0D0
   Y1=0.0D0
   Y2=0.0D0
   AX1=0.0D0
   AX2=0.0D0
   AY1=0.0D0
   AY2=0.0D0
   XX1=0.0D0
   XX2=0.0D0
   YY1=0.0D0
   YY2=0.0D0
   XMIN=0.0D0
   YMIN=0.0D0
   XMAX=0.0D0
   YMAX=0.0D0
   XMIN2=0.0D0
   YMIN2=0.0D0
   XMAX2=0.0D0
   YMAX2=0.0D0
   XMINO=0.0D0
   YMINO=0.0D0
   XMAXO=0.0D0
   YMAXO=0.0D0
   CAFLG=0
   COFLG=0
   III=II
!
!     NOW FOR THE II SURFACE WE CALCULATE ALL THE END POINTS
!
   ZDELZ=0.0D0
   CALL NSSCAOJK(YMIN,XMIN,YMAX,XMAX,&
   &YMINO,XMINO,YMAXO,XMAXO,CAFLG,&
   &COFLG,III,&
   &YMIN2,XMIN2,YMAX2,XMAX2,THETA,ZDELZ)
!
!     NOW WE HAVE THE END POINTS, CYCLE THROUGH ALL FOUR PAIRS
!
!     2. USE APPROPRIATE CALLS TO THE NSSSAG ROUTINE
!               TO CALCULATE THE SAG AND MAKE
!               CERTAIN THE SIGN IS CORRECT FOR A LOCAL Z COORDINATE
!
   X1=XMIN
   Y1=YMIN
   X2=XMAX
   Y2=YMAX
   AX1=XMIN2
   AY1=YMIN2
   AX2=XMAX2
   AY2=YMAX2
   XX1=XMINO
   YY1=YMINO
   XX2=XMAXO
   YY2=YMAXO
!     SET UP THE LINE SLOPE AND THE INITIAL DELXX VALUE
   IF((X2-X1).NE.0.0D0)SLOPE=(Y2-Y1)/(X2-X1)
   VERT=.FALSE.
   IF((X2-X1).EQ.0.0D0) VERT=.TRUE.
!
   DELXX=(X2-X1)/(360.0D0-1.0D0)
   DELYY=(Y2-Y1)/(360.0D0-1.0D0)
!     NOW WE HAVE THE END POINTS, CYCLE THROUGH ALL THE PAIRS AND LOAD
!     ALL THE ARRAYS WITH SURFACE LOCAL X,Y AND Z DATA
!
   DO J=1,360
      IF(J.EQ.1) THEN
         X=X1
      ELSE
         X=X+DELXX
      END IF
      IF(.NOT.VERT) Y=(SLOPE*(X-X1))+Y1
      IF(VERT) THEN
         IF(J.EQ.1) THEN
            Y=Y1
         ELSE
            Y=Y+DELYY
         END IF
      ELSE
      END IF
!
      III=II
      ACALL1=X
      ACALL2=Y
      ALT=.FALSE.
      ALT=.FALSE.
      IF(X.LT.AX1) THEN
         ACALL1=AX1
         ALT=.TRUE.
      END IF
      IF(X.GT.AX2) THEN
         ACALL1=AX2
         ALT=.TRUE.
      END IF
      IF(Y.LT.AY1) THEN
         ACALL2=AY1
         ALT=.TRUE.
      END IF
      IF(Y.GT.AY2) THEN
         ACALL2=AY2
         ALT=.TRUE.
      END IF
      IF(NSSALENS(19,III).EQ.1.0D0) THEN
!     CIRCULAR CLEAR APERTURE
         ALT=.FALSE.
         IF(X.LT.AX1) THEN
            ACALL1=AX1
            ALT=.TRUE.
         END IF
         IF(X.GT.AX2) THEN
            ACALL1=AX2
            ALT=.TRUE.
         END IF
         IF(Y.LT.AY1) THEN
            ACALL2=AY1
            ALT=.TRUE.
         END IF
         IF(Y.GT.AY2) THEN
            ACALL2=AY2
            ALT=.TRUE.
         END IF
      END IF
      CALL NSSSAG(III,ACALL1,ACALL2,Z)
      IF(NSSALENS(19,III).NE.1.0D0.OR.NSSALENS(19,III).EQ.1.0D0 &
      &.AND.NSSALENS(23,III).NE.0.0D0.OR.ALENS(19,III).EQ.1.0D0.AND.&
      &NSSALENS(24,III).NE.0.0D0) THEN
         FRACRAD=0.0D0
      ELSE
         IF(NSSALENS(20,III).NE.NSSALENS(21,III)) THEN
            FRACRAD=((DSQRT((X**2)+(Y**2))-NSSALENS(21,III))/&
            &(NSSALENS(20,III)-NSSALENS(21,III)))
            IF(FRACRAD.LE.0.0D0) FRACRAD=0.0D0
         ELSE
            FRACRAD=0.0D0
         END IF
      END IF
      ZDELZ1=ZDELZ*FRACRAD
      Z=Z+ZDELZ1
!
!     ASSIGN ARRAY VALUES
      PRO(J,1)=X
      PRO(J,2)=Y
      IF(J.GT.1) THEN
         XM=PRO(J-1,1)
         YM=PRO(J-1,2)
      ELSE
         XM=PRO(J,1)
         YM=PRO(J,2)
      END IF
!     DO WE HAVE TO DO A FIX FOR A HOLE
!
! IF XM,YM AND X,Y ARE INSIDE A HOLE DRAPRO=0.0D0
!                  OTHERWISE
!                  DRAPRO=1.0D0

      INSIT=.FALSE.
      INSIT=NSSINSIDEIT(II,X,Y,XM,YM)
      IF(INSIT) DRAPRO=0.0D0
      IF(.NOT.INSIT)DRAPRO=1.0D0
      PRO(J,3)=Z
      PRO(J,4)=DRAPRO
!
!               CYCLE THROUGH THE NEXT DATA PAIR
   END DO
!
!     3. THE ARRAYS NOW HAVE LOCAL X,Y AND Z VALUES STORED IN THEM
!     CONVERT THE LOCAL X ANY Y PROFILES TO GLOBAL NUMBERS
!     GLOBAL VERTEX DATA IS
   II=JJ
   DO I=1,360
      X00=NSSVERTEX(1,II)
      Y00=NSSVERTEX(2,II)
      Z0= NSSVERTEX(3,II)
      LX0=NSSVERTEX(4,II)
      MX0=NSSVERTEX(5,II)
      NX0=NSSVERTEX(6,II)
      LY0=NSSVERTEX(7,II)
      MY0=NSSVERTEX(8,II)
      NY0=NSSVERTEX(9,II)
      LZ0=NSSVERTEX(10,II)
      MZ0=NSSVERTEX(11,II)
      NZ0=NSSVERTEX(12,II)
      X=PRO(I,1)
      Y=PRO(I,2)
      Z=PRO(I,3)
!
      X1=X00+((LX0*(X))+(LY0*(Y))&
      &+(LZ0*(Z)))
      Y1=Y00+((MX0*(X))+(MY0*(Y))&
      &+(MZ0*(Z)))
      Z1=Z0+((NX0*(X))+(NY0*(Y))&
      &+(NZ0*(Z)))
      PRO(I,1)=X1
      PRO(I,2)=Y1
      PRO(I,3)=Z1
   END DO
!
!     4. NOW DETERMINE THE BEST PLACE FOR THE ROTATION POINT FOR
!               PLOT LOOK/VIEW
!
   CALL NSSROT
!
!     5.  CONVERT THE GLOBAL X ANY Y PROFILES
!               USING THE LOOK/VIEW VALUES
   II=JJ
   DO I=1,360
      X=PRO(I,1)
      Y=PRO(I,2)
      Z=PRO(I,3)
      X=X-XROT
      Y=Y-YROT
      Z=Z-ZROT
      XN=ROT1X(X,Z,VIEPH)
      YN=Y
      ZN=ROT1Z(X,Z,VIEPH)
      X=XN
      Y=YN
      Z=ZN
!
      ZN=ROT2Z(Z,Y,VIEAL)
      YN=ROT2Y(Z,Y,VIEAL)
      XN=X
      PRO(I,1)=XN
      PRO(I,2)=YN
      PRO(I,3)=ZN
   END DO
!
!     THE ARRAYS NOW HAVE GLOBAL SURFACE PROFILE DATA IN THEM
!
!     6.IF NEEDED, DETERMINE SCALE FACTORS AND PLOT RANGE
!
   CALL NSSPLTSC
!
!     7.CYCLE THROUGH THE THE ARRAYS, APPLY SCALE FACTORS
!
!     RIGHT NOW,COORDINATES ARE IN WORLD COORDINATES
!     CONVERT THEM TO DEVICE INDEPENDENT GRAPHICS COORDINATES IN
!     TWO STEPS.
!
!     THE WORLD X PLOTS TO THE PLOTTER X
!     THE WORLD Y PLOTS TO THE PLOTTER Y
!
!     STEP 1: CONVERT USING AN APPROPRIATE SCALE FACTOR
!               CALCULATING AN APPROPRIATE FACTOR IF NECESSARY
!
   II=JJ
   DO I=1,360
      PRO(I,1)=(PRO(I,1)/NSSSCFA)*1000.0D0
      PRO(I,2)=(PRO(I,2)/NSSSCFA)*1000.0D0
   END DO
!
!     8. APPLY THE PLOT XSHIFT AND YSHIFT VALUES
   DO I=1,360
      II=JJ
      IF(LORIENT) CALL ORSHIFT
      PRO(I,1)=PRO(I,1)+DBLE(PXSHFT)
      PRO(I,2)=PRO(I,2)+3500.0D0+DBLE(PYSHFT)
   END DO
!
!     9. SET THE PLOT JUSTIFICATION IF NEEDED
!     IF THERE ARE X-OFFSETS (JUSTIFICATION) TO APPLY, DO THEM HERE.
!
!     NOW
   IF(RCL.EQ.1.OR.RCL.EQ.-1) THEN
      JUSOFF=500.0D0-((XMINI/NSSSCFA)*1000.0D0)
      RCL=-1
   ELSE
   END IF
   IF(RCL.EQ.2.OR.RCL.EQ.-2) THEN
      RCL=-2
      JUSOFF=5000.0D0
   ELSE
   END IF
   IF(RCL.EQ.3.OR.RCL.EQ.-3) THEN
      JUSOFF=9500.0D0-((XMAXI/NSSSCFA)*1000.0D0)
      RCL=-3
   ELSE
   END IF
!
   DO I=1,360
      II=JJ
      PRO(I,1)=PRO(I,1)+JUSOFF
   END DO
!     9. PLOT GAMMA
!     IF THERE IS A NON-ZERO GAMMA SPECIFIED IN PLOT LOOK OR PLOT VIEW THEN
!     ROTATE IN GAMMA ON THE SCREEN ABOUT THE CENTER OF THE SCREEN
!     WHICH HAS COORDINATES X=5000.0D0,Y=3500.0D0
!
!     FIRST SHIFT THE COORDINATE ORIGIN TO THE CETER OF THE DISPLAY
!
   DO I=1,360
      II=JJ
      PRO(I,1)=PRO(I,1)-5000.0D0
      PRO(I,2)=PRO(I,2)-3500.0D0
   END DO
!     THE SCREEN COORDINATE IN real(real64) IN THE SHIFTED COORDINATE
!     FRAME IS NOW X AND Y

   IF(DBLE(PGAMMA).NE.0.0D0) THEN
      LKG=(PII/180.0D0)*DBLE(PGAMMA)

      DO I=1,360
         II=JJ
         X=PRO(I,1)
         Y=PRO(I,2)
         XNEW=((X*DCOS(LKG))-(Y*DSIN(LKG)))
         YNEW=((X*DSIN(LKG))+(Y*DCOS(LKG)))
         PRO(I,1)=XNEW
         PRO(I,2)=YNEW
      END DO
   END IF
!     THE ROTATION IS DONE, NOW SHIFT THE ORIGIN BACK TO THE BOTTOM
!     LEFT HAND CORNER
   DO I=1,360
      II=JJ
      PRO(I,1)=PRO(I,1)+5000.0D0
      PRO(I,2)=PRO(I,2)+3500.0D0
   END DO
!
   I=JJ
!     NOW DRAW THE X PROFILE OF THE CLAP AT SURFACE I
!     WITH THE PEN UP, GO TO THE STARTING PLOT POSITION
   IF(.NOT.PLEXIS) PLEXIS=.TRUE.
!     FIRST CLAP PROFILE
   DO J=1,360
!     PUT INSTRUCTIONS IN P1ARAY TO DROP PEN AND DRAW
      DRAPRO=PRO(J,4)
      IF(J.EQ.1) IPST=0
      IF(J.GT.1.AND.DRAPRO.EQ.1.0D0.AND.PRO((J-1),4).EQ.0.0D0) IPST=0
      IF(J.GT.1.AND.DRAPRO.EQ.1.0D0.AND.PRO((J-1),4).EQ.1.0D0) IPST=1
      IF(J.GT.1.AND.DRAPRO.EQ.0.0D0) IPST=0
      IF(PRO(J,1).GT.1.0D6) PRO(J,1)=1.0D6
      IF(PRO(J,2).GT.1.0D6) PRO(J,2)=1.0D6
      IF(PRO(J,1).LT.-1.0D6) PRO(J,1)=-1.0D6
      IF(PRO(J,2).LT.-1.0D6) PRO(J,2)=-1.0D6
      IX=INT(PRO(J,1))
      IY=INT(PRO(J,2))
      P1ARAY(J,1,1)=IX
      P1ARAY(J,2,1)=IY
      P1ARAY(J,3,1)=IPST
      IF(.NOT.PLEXIS) PLEXIS=.TRUE.
   END DO
!
!     LINE TYPE SETTING
   COLPAS=COLPRO
   CALL MY_COLTYP(COLPAS)
   OLLNTP=LNTYPE
   LNTYPE=0
   FIXUP=.FALSE.
   DO IK=1,360
      CALL PENMV2(P1ARAY(IK,1,1),P1ARAY(IK,2,1),P1ARAY(IK,3,1))
   END DO
!     FINISHED WITH THAT PROFILE, LIFT PEN
   IPST=0
   LNTYPE=OLLNTP
   DEALLOCATE (PRO,STAT=ALLOERR)
   RETURN
END
FUNCTION NSSINSIDEIT(I,XONE,YONE,XTWO,YTWO)
   USE NSSMOD
!
   use DATLEN
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
   EXTERNAL INSID1,INSID2
!
   INTEGER I,CAFLG,COFLG,N,ITIIT,III
!
   real(real64) X,Y,XONE,YONE,XTWO,YTWO,ANGLE,&
   &XR,YR,LS,RS,XRD,YRD,X1,X2,X3,X4,Y1,Y2,Y3,Y4,&
   &X5,X6,X7,X8,Y5,Y6,Y7,Y8,XC1,XC2,XC3,XC4,YC1,YC2,&
   &A22,YC3,YC4,RAD2,MAXSID,CS1,CS2,CS3,CS4
!
   LOGICAL NSSINSIDEIT,INS,INSID1,INSID2,INITIT(1:2)
!
   INTEGER CAERAS,COERAS
!
   COMMON/CACO/CAERAS,COERAS,LS
!
!     VARIABLES FOR SPOT TRACING
!
   LOGICAL TCLPRF,SPDTRA
!
   COMMON/SPRA1/SPDTRA
!
!       I IS THE CURRENT SURFACE NUMBER
!
!       THIS ROUTINE SETS THE FLAGS
!       CAERAS AND COERAS ARE
!       FLAGS SET IF THE NEXT CURRENT SURFACE HAS A COBS OR
!       CLAP ERASE.
!
   COFLG=INT(NSSALENS(25,I))
!       THERE IS COBS, PROCEED CHECKING
!
   NSSINSIDEIT=.FALSE.
!
   DO ITIIT=1,2
      INITIT(ITIIT)=.FALSE.
      IF(ITIIT.EQ.1) THEN
         X=XONE
         Y=YONE
      ELSE
         X=XTWO
         Y=YTWO
      END IF
!
!******************************************************************
!       COFLG NON-ZERO, RESOLVE ALL COBS BLOCKAGES NOW
!******************************************************************
!
!       COFLG=1 CIRCULAR COBS, DOES IT STOP THE RAY
      IF(COFLG.EQ.1) THEN
!
         LS=0.0D0
!       CIRCULAR COBS EQUATION IS:
!
!       (X)**2 + (Y)**2 = R**2
!
!       SUB X AND Y IN THE LEFT SIDE. IF THE LEFT SIDE IS
!       LESS THAN OR EQUAL TO THE RIGHT SIDE, THE RAY IS BLOCKED
!       ELSE IT IS NOT BLOCKED.
!
!       IF NON-ZERO COBS DECENTRATIONS EXISTS, THEY MUST BE CONSIDERED
!       IN COBS CHECKING. WE CONVERT THE RAY COORDINATES X AND Y
!       TO XR AND YR IN THE COORDINATE SYSTEM OF THE DECENTERED
!       OBSCURATION. REMEMBER.
!
         XR=X-ALENS(28,I)
         YR=Y-ALENS(29,I)
!
         LS=DSQRT((XR**2)+(YR**2))
!
         RS=DSQRT(ALENS(26,I)**2)-AIMTOL
         IF(REAL(LS).LT.REAL(RS)) THEN
!       RAY IS BLOCKED BY COBS ON SURFACE I
            LS=10.0D0
            INITIT(ITIIT)=.TRUE.
         ELSE
            LS=0.0D0
            INITIT(ITIIT)=.FALSE.
         END IF
!       COFLG NOT 1
      END IF
!
!       COFLG=2 RECTANGULAR OBSCURATION, DOES IT STOP THE RAY
      IF(COFLG.EQ.2) THEN
!
         LS=0.0D0
         INITIT(ITIIT)=.FALSE.
!
!       IN THE COORDINATE SYSTEM OF THE RECTANGLE, THE CORNER
!       COORDINATES ARE:
         X1=-NSSALENS(26,I)-AIMTOL
         Y1=NSSALENS(27,I)+AIMTOL
         X2=-NSSALENS(26,I)-AIMTOL
         Y2=-NSSALENS(27,I)-AIMTOL
         X3=NSSALENS(26,I)+AIMTOL
         Y3=-NSSALENS(27,I)-AIMTOL
         X4=NSSALENS(26,I)+AIMTOL
         Y4=NSSALENS(27,I)+AIMTOL
!
         XRD=X
         YRD=Y

!       IF NON-ZERO COBS DECENTRATIONS EXISTS, THEY MUST BE CONSIDERED
!       IN COBS CHECKING. WE CONVERT THE RAY COORDINATES X AND Y
!       TO XRD AND YRD IN THE COORDINATE SYSTEM OF THE DECENTERED
!       OBSCURATION. REMEMBER.
!
         XRD=XRD-NSSALENS(28,I)
         YRD=YRD-NSSALENS(29,I)
!
!       IF A NON-ZERO COBS TILT EXISTS, IT MUST BE CONSIDERED
!       IN COBS CHECKING. WE CONVERT THE RAY COORDINATES XRD AND YRD
!       TO XR AND YR IN THE COORDINATE SYSTEM OF THE ROTATED
!       OBSCURATION. REMEMBER. GAMMA ROTATION OF A CLAP OR
!       COBS HAS THE SAME SIGN AS IN A SURFACE ROTATION

         A22=NSSALENS(30,I)*PII/180.0D0
         XR=(XRD*DCOS(A22))+(YRD*DSIN(A22))
         YR=(YRD*DCOS(A22))-(XRD*DSIN(A22))
!
!
!
!       ARE THE POINTS XR AND YR ON THE RECTANGLE OR OUTSIDE
!       IF YES, SET LS=10.0D0
!       IF NO SET LS=0.0D0
         XT(1)=X1
         YT(1)=Y1
         XT(2)=X2
         YT(2)=Y2
         XT(3)=X3
         YT(3)=Y3
         XT(4)=X4
         YT(4)=Y4
         NP=4
         X0=XR
         Y0=YR
         INS=INSID2()
         IF(INS) THEN
!       RAY  BLOCKED
            LS=10.0D0
            INITIT(ITIIT)=.TRUE.
         ELSE
            LS=0.0D0
            INITIT(ITIIT)=.FALSE.
!       RAY NOT BLOCKED
         END IF
!
!       COFLG NOT 2
      END IF
!
!       COFLG=3 ELLIPTICAL COBS, DOES IT STOP THE RAY
      IF(COFLG.EQ.3) THEN
!
         LS=0.0D0
!       ELLIPTICAL COBS EQUATION IS:
!
!       (X)**2/A**2  + (Y)**2/B**2 = 1
!
!       SUB X AND Y IN THE LEFT SIDE. IF THE LEFT SIDE IS
!       LESS THAN 1.0D0, THE RAY IS BLOCKED
!       ELSE IT IS NOT BLOCKED.
!
         XRD=X
         YRD=Y

!       IF NON-ZERO COBS DECENTRATIONS EXISTS, THEY MUST BE CONSIDERED
!       IN COBS CHECKING. WE CONVERT THE RAY COORDINATES X AND Y
!       TO XRD AND YRD IN THE COORDINATE SYSTEM OF THE DECENTERED
!       OBSCURATION. REMEMBER.
!
         XRD=XRD-NSSALENS(28,I)
         YRD=YRD-NSSALENS(29,I)
!
!       IF A NON-ZERO COBS TILT EXISTS, IT MUST BE CONSIDERED
!       IN COBS CHECKING. WE CONVERT THE RAY COORDINATES XRD AND YRD
!       TO XR AND YR IN THE COORDINATE SYSTEM OF THE ROTATED
!       OBSCURATION. REMEMBER. GAMMA ROTATION OF A CLAP OR
!       COBS HAS THE SAME SIGN AS IN A SURFACE ROTATION

         A22=NSSALENS(30,I)*PII/180.0D0
         XR=(XRD*DCOS(A22))+(YRD*DSIN(A22))
         YR=(YRD*DCOS(A22))-(XRD*DSIN(A22))
!
!
         LS=((XR**2)/(NSSALENS(26,I)**2))+&
         &((YR**2)/(NSSALENS(27,I)**2))
!
         IF(REAL(LS).LT.(1.0-(AIMTOL**2))) THEN
!       RAY IS BLOCKED BY COBS ON SURFACE I
            LS=10.0D0
            INITIT(ITIIT)=.TRUE.
         ELSE
            LS=0.0D0
            INITIT(ITIIT)=.FALSE.
         END IF
!       COFLG NOT 3
      END IF
   END DO
   NSSINSIDEIT=.FALSE.
   IF(INITIT(1).AND.INITIT(2))NSSINSIDEIT=.TRUE.
!
   RETURN
END
