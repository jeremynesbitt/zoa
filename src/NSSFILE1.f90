!       FIRST COLLECTION OF NSS FILES
!
SUBROUTINE GETOANGLES
   USE NSSMOD
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE

   real(real64) D31,D32,D33,COSB,SINB,ALPHA,BETA
!
!
!     OBJL,OBJM AND OBJN ARE THE L,M AND N-DIRECTION COSINES OF THE
!     ROTATED COORDINATE SYSTEM. 0.0, 0.0 AND 1.0 ARE THE DIRECTION
!     COSINES OF THE UNROTATED SYSTEM. ONLY AN ALPHA AND A BETA ROTATION
!     RELATE THE TWO SYSTEMS. GAMMA IS ALWAYS ZERO.
   D31=OBJL
   D32=OBJM
   D33=OBJN
!
!     NOW CALCULATE ALPHA,BETA
!     CALCULATE BETA
   BETA=DASIN(-D31)
   COSB=DCOS(BETA)
   IF(COSB.NE.0.0D0) THEN
!     COSINE OF BETA IS NOT ZERO
!     COSINE OF BETA IS NOT ZERO
      IF((D32/COSB).EQ.0.0D0.AND.(D33/COSB).NE.0.0D0) ALPHA=0.0D0
      IF((D32/COSB).EQ.0.0D0.AND.(D33/COSB).EQ.0.0D0) ALPHA=0.0D0
      IF((D32/COSB).NE.0.0D0.AND.(D33/COSB).EQ.0.0D0) ALPHA=PII/2.0D0
      IF((D32/COSB).NE.0.0D0.AND.(D33/COSB).NE.0.0D0)&
      &ALPHA=DATAN2((D32/COSB),(D33/COSB))
   END IF
   IF(COSB.EQ.0.0D0) THEN
!     COSINE OF BETA IS ZERO
      IF(D31.EQ.-1.0D0) SINB=1
      IF(D31.EQ.1.0D0) SINB=-1
      IF(SINB.EQ.1) BETA=PII/2.0D0
      IF(SINB.EQ.-1) BETA=-PII/2.0D0
      IF(SINB.EQ.1) THEN
         IF((D32).EQ.0.0D0.AND.(D33).NE.0.0D0) ALPHA=0.0D0
         IF((D32).EQ.0.0D0.AND.(D33).EQ.0.0D0) ALPHA=0.0D0
         IF((D32).NE.0.0D0.AND.(D33).EQ.0.0D0) ALPHA=PII/2.0D0
         IF((D32).NE.0.0D0.AND.(D33).NE.0.0D0)&
         &ALPHA=DATAN2((D32),(D33))
      END IF
      IF(SINB.EQ.-1) THEN
         IF((D32).EQ.0.0D0.AND.(D33).NE.0.0D0) ALPHA=0.0D0
         IF((D32).EQ.0.0D0.AND.(D33).EQ.0.0D0) ALPHA=0.0D0
         IF((D32).NE.0.0D0.AND.(D33).EQ.0.0D0) ALPHA=PII/2.0D0
         IF((D32).NE.0.0D0.AND.(D33).NE.0.0D0)&
         &ALPHA=DATAN2((-D32),(-D33))
      END IF
   END IF
!     CONVERT TO DEGREES
   OALPHA=(180.0D0/PII)*ALPHA
   OBETA=(180.0D0/PII)*BETA
   OGAMMA=0.0D0
   RETURN
END

