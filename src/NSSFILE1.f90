SUBROUTINE NSSRAY
   USE NSSMOD
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
   INTEGER JJ,I

!       THIS IS AN INDIVIDUAL RAY TRACE LOOP WHICH GOES UNTIL THE RAY
!       RUNS OUT OF ENERGY OR HITS THE DETECTOR SURFACE.
!       A RAY RUNS OUT OF ENERGY WHEN IT HAS INTERSECTED
!       NSSSYSTEM(27) SURFACES OR WHEN ITS ENERGY DROPS BELOW OF THE VALUE NSSSYSTEM(26)
!       THE VARIABLE NSSRAY_HITS TRACKS THE NUMBER OF SURFACE HITS, THE VARIABLE NSS_ENERGY TRACKS
!       THE FRACTIONAL RAY ENERGY. DETSNUM TRACKS THE DETECTOR SURFACE AND AT
!       THE DETECTOR SURFACE, NSSSYSTEM(49)
!       OUTPUT RAY HISTORY FOR THE OBJECT SURFACE (SOURCE)
   CALL TAG_RHIST(RHISTN)
   CALL NSSRHIST(0)
   CALL NSSHIST(0)
   CALL NSSHT(0)
!       INITIALIZE OPL AND RAY LENGTHS TO ZERO
   NSS_OPL=0.0D0
   NSS_RAYLENGTH=0.0D0
   GO TO 11
!       FIRST TIME IN, WE DON'T RUN THE FOLLOWING TEST
10 CONTINUE
!       STATEMENT 10 IS THE RETURN POINT FOR THE NEXT ATTEMPTED RAY/SURFACE INTERSECTION
!       AND INTERACTION. CHECK TO SEE THAT THE RAY HAS ENOUGH ENERGY LEFT TO PROCEED.
   IF(NSSRAY_HITS.GE.INT(NSSSYSTEM(27)).OR.&
   &NSSRAY_ENERGY.LE.NSSSYSTEM(26).OR.JJ.EQ.MAXS+1) RETURN
!       RAY HAS DIED, RETURN TO NSSTRACE TO TRACE THE NEXT RAY
11 CONTINUE
!
!       WE HAVE THE STARTING RAY COORDINATES AND DIRECTION
!       COSINES IN GLOBAL AND LOCAL COORDINATES PLUS STARTING ENERGY, POLARIZATION
!       AND EXISTENCE FLAGS HAVE BEEN SET ALL IN NSSTRACE FOR A SPECIFIC RAY. WE KNOW
!       EVERYTHING ABOUT THE RAY AT THE SOURCE.
!
!       1. FIND OUT WHICH SURFACE NUMBER THE RAY STRIKES
!       WITH LEAST POSITIVE, NON ZERO OPTICAL PATH LENGTH.
!       THE RAY MUST FALL INSIDE THE SURFACE
!       BOUNDARY AND INSIDE THE ASSIGNED CLAP AND NOT INSIDE AN ASSIGED HOLE.
!       THAT OBJECT AND SURFACE NUMBER IS RETURNED VIA JJ
!
!       NOTE:
!       IN THIS SEARCH PROCESS, THE PERMANENT RAY DATA IS NOT CHANGED!
!
   I=LASTSUR
   LVECTOR(1:I)=0.0D0
   VECTORL(1:I)=0.0D0
   VECTORM(1:I)=0.0D0
   VECTORN(1:I)=0.0D0
   CALL NSS_FIND_SURFACE(JJ)
!
!       NOW INTERSECT SURFACE JJ
   IF(JJ.EQ.MAXS+1) THEN
!       SAVE PREVIOUS RAYS DATA
!       WE ARE GOING TO INTERSECT TRAVEL THE "TERMINAL RAY DISTANCE"
!       SAVED IN NSSSYSTEM(25)
! LOCAL
      RRX=NSS_LRAY(1)
      RRY=NSS_LRAY(2)
      RRZ=NSS_LRAY(3)
      RRX=RRX+(NSSSYSTEM(25)*RRL)
      RRY=RRY+(NSSSYSTEM(25)*RRM)
      RRZ=RRZ+(NSSSYSTEM(25)*RRN)
      NSS_LRAY(1)=RRX
      NSS_LRAY(2)=RRY
      NSS_LRAY(3)=RRZ
      NSS_LRAY(4)=RRL
      NSS_LRAY(5)=RRM
      NSS_LRAY(6)=RRN
      NSS_LRAY(7)=XRRL
      NSS_LRAY(8)=XRRM
      NSS_LRAY(9)=XRRN
      NSS_LRAY(10)=YRRL
      NSS_LRAY(11)=YRRM
      NSS_LRAY(12)=YRRN
! GLOBAL
      RRX=NSS_GRAY(1)
      RRY=NSS_GRAY(2)
      RRZ=NSS_GRAY(3)
      RRL=NSS_GRAY(4)
      RRM=NSS_GRAY(5)
      RRN=NSS_GRAY(6)
      XRRN=NSS_GRAY(7)
      XRRN=NSS_GRAY(8)
      XRRN=NSS_GRAY(9)
      YRRN=NSS_GRAY(10)
      YRRN=NSS_GRAY(11)
      YRRN=NSS_GRAY(12)
      RRX=RRX+(NSSSYSTEM(25)*RRL)
      RRY=RRY+(NSSSYSTEM(25)*RRM)
      RRZ=RRZ+(NSSSYSTEM(25)*RRN)
      NSS_GRAY(1)=RRX
      NSS_GRAY(2)=RRY
      NSS_GRAY(3)=RRZ
      NSS_GRAY(4)=RRL
      NSS_GRAY(5)=RRM
      NSS_GRAY(6)=RRN
      NSS_GRAY(7)=XRRN
      NSS_GRAY(8)=XRRN
      NSS_GRAY(9)=XRRN
      NSS_GRAY(10)=YRRN
      NSS_GRAY(11)=YRRN
      NSS_GRAY(12)=YRRN
!
      NSS_LRAYP(1:50)=NSS_LRAY(1:50)
      NSS_GRAYP(1:50)=NSS_GRAY(1:50)
   ELSE
!       SAVE PREVIOUS RAYS DATA
!       NOT JJ=0
!
!       CONVERT GLOBAL RAY DATA TO LOCAL RAY DATA
      CALL NSS_TRANSF(JJ)
      CALL NSS_TRANSFP(JJ)
      CALL NSS_HIT_SURFACE(JJ)
!
!       2. WE ARE NOW AT JJ, INTERACT THE RAY WITH THE SURFACE.
!       SET THE NSS ENERGYS FOR THE BEFORE INTERACTION RAYS
      NSS_GRAY(16)=NSSRAY_ENERGY
      NSS_LRAY(16)=NSSRAY_ENERGY
      CALL NSS_SURFACE_INTERACT(JJ)
      NSS_GRAY(17)=NSS_N
      NSS_LRAY(17)=NSS_N
      NSS_GRAYP(17)=NSS_NP
      NSS_LRAYP(17)=NSS_NP
      CALL NSS_TRANSF_BACK(JJ)
      CALL NSS_TRANSFP_BACK(JJ)
   END IF

!                       IF RAY FAILURE HAPPENED OR WE HIT UNIVERSE SPHERE
   IF(NSSRAYFAIL.AND.JJ.NE.MAXS+1) THEN
      NSSRAY_ENERGY=0.0D0
   END IF
!
!       3. WRITE THE RAY DATA IN THE RAY HISTORY FILE AND THEN RETURN TO STATEMENT 10
!       TO PROCEED WITH THE NEXT RAY/SURFACE INTERSECTION/INTERACTION
!
   NSSRAYFAILCODE(1)=JJ
   NSSRAY_HITS=NSSRAY_HITS+1
   CALL TAG_RHIST(RHISTN)
   CALL NSSRHIST(JJ)
   CALL NSSHIST(JJ)
   CALL NSSHT(JJ)
!       NOW THE "P" RAY DATA BECOMES THE UN"P" DATA FOR THE NEXT SURFACE
   NSS_GRAY(1)=NSS_GRAYP(1)
   NSS_GRAY(2)=NSS_GRAYP(2)
   NSS_GRAY(3)=NSS_GRAYP(3)
   NSS_GRAY(4)=NSS_GRAYP(4)
   NSS_GRAY(5)=NSS_GRAYP(5)
   NSS_GRAY(6)=NSS_GRAYP(6)
   NSS_GRAY(7)=NSS_GRAYP(7)
   NSS_GRAY(8)=NSS_GRAYP(8)
   NSS_GRAY(9)=NSS_GRAYP(9)
   NSS_GRAY(10)=NSS_GRAYP(10)
   NSS_GRAY(11)=NSS_GRAYP(11)
   NSS_GRAY(12)=NSS_GRAYP(12)
   NSS_GRAY(13)=NSS_GRAYP(13)
   NSS_GRAY(14)=NSS_GRAYP(14)
   NSS_GRAY(15)=NSS_GRAYP(15)
   NSS_LRAY(1)=NSS_LRAYP(1)
   NSS_LRAY(2)=NSS_LRAYP(2)
   NSS_LRAY(3)=NSS_LRAYP(3)
   NSS_LRAY(4)=NSS_LRAYP(4)
   NSS_LRAY(5)=NSS_LRAYP(5)
   NSS_LRAY(6)=NSS_LRAYP(6)
   NSS_LRAY(7)=NSS_LRAYP(7)
   NSS_LRAY(8)=NSS_LRAYP(8)
   NSS_LRAY(9)=NSS_LRAYP(9)
   NSS_LRAY(10)=NSS_LRAYP(10)
   NSS_LRAY(11)=NSS_LRAYP(11)
   NSS_LRAY(12)=NSS_LRAYP(12)
   NSS_LRAY(13)=NSS_LRAYP(13)
   NSS_LRAY(14)=NSS_LRAYP(14)
   NSS_LRAY(15)=NSS_LRAYP(15)
!
!       SET AFTER INTERACTION ENERGY LEVELS
   NSS_LRAYP(16)=NSSRAY_ENERGY
   NSS_GRAYP(16)=NSSRAY_ENERGY
   GO TO 10
END
SUBROUTINE NSS_HIT_SURFACE(J)
   USE NSSMOD
   use DATMAI
   INTEGER J
   real(real64) URAD
!       WE ARE NOW AT J
!       THE REFRACTIVE INDEX VALUES ARE:
!       WHERE 1 AND 2 REFER TO THE SPACES ON EACH SIDE OF THE SURFACE
   REALINDEX1=NSSALENS(100+NSSWAVNUM,J)
   REALINDEX2=NSSALENS(120+NSSWAVNUM,J)
   IMAGINDEX1=NSSALENS(110+NSSWAVNUM,J)
   IMAGINDEX2=NSSALENS(130+NSSWAVNUM,J)
!       SET SIMPLE LOCAL RAY DATA VALUES
   RRX=NSS_LRAY(1)
   RRY=NSS_LRAY(2)
   RRZ=NSS_LRAY(3)
   RRL=NSS_LRAY(4)
   RRM=NSS_LRAY(5)
   RRN=NSS_LRAY(6)
   XRRL=NSS_LRAY(7)
   XRRM=NSS_LRAY(8)
   XRRN=NSS_LRAY(9)
   YRRL=NSS_LRAY(10)
   YRRM=NSS_LRAY(11)
   YRRN=NSS_LRAY(12)
!       THE SURFACE DIR COSINES AT THE SURFACE VERTEX ARE:
   NSSLN=0.0D0
   NSSMN=0.0D0
   NSSNN=1.0D0
   IF(INT(NSSALENS(1,J)).EQ.1)  CALL NSSFLA(J)
   IF(INT(NSSALENS(1,J)).EQ.2)  CALL NSSASP(J)
   IF(INT(NSSALENS(1,J)).EQ.3)  CALL NSSANA(J)
   IF(INT(NSSALENS(1,J)).EQ.4) CALL NSSUSER(J)
   IF(INT(NSSALENS(1,J)).EQ.5)  CALL NSSMEM(J)
   IF(INT(NSSALENS(1,J)).EQ.6) CALL NSSTUB(J)
!        THE SINGLE INTERSECTION POINT IS, BY DEFINITION, CORRECT
!        NOW LOAD THE RAY DATA INTO THE LOCAL ARRAY
   NSS_LRAY(1)=RRX
   NSS_LRAY(2)=RRY
   NSS_LRAY(3)=RRZ
   NSS_LRAY(4)=RRL
   NSS_LRAY(5)=RRM
   NSS_LRAY(6)=RRN
   NSS_LRAY(7)=XRRL
   NSS_LRAY(8)=XRRM
   NSS_LRAY(9)=XRRN
   NSS_LRAY(10)=YRRL
   NSS_LRAY(11)=YRRM
   NSS_LRAY(12)=YRRN
   NSS_LRAY(13)=NSSLN
   NSS_LRAY(14)=NSSMN
   NSS_LRAY(15)=NSSNN
   RETURN
END
SUBROUTINE NSSCLP(JJ,SFI)
   USE NSSMOD
   USE GLOBALS
!
   use DATLEN
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
   real(real64) X,Y,Z,XN,YN,ZN,ROT1X,ROT1Z,ROT2Y,MDX,MDY,GAMGAM &
   &,ROT2Z,AX,AY,AZ,AALF,APHI,YMAXI,YMINI,XMAXI,XMINI &
   &,XNEW,YNEW,LKG,VIEPH,VIEAL,Z1,ANGLE,AN2,ZDELZ &
   &,X00,Y00,Z0,LX0,LY0,LZ0,ZCORR,SFI &
   &,X1,Y1,MX0,MY0,MZ0,NX0,NY0,NZ0,XID,YID
!
   INTEGER JJSTOP,J,IK,III,NO,JJ,ALLOERR,IIRUN,CLPTYPE
!
   INTEGER M1,M2,M3,M4,M5,IX,IY,I,II,IPST,K,L,POINT(1:2,1:3)
!
   INTEGER SURFACEI
!
   INTEGER COLPAS
!
!
   REAL CLPDAT
   DIMENSION CLPDAT(:,:)
   ALLOCATABLE :: CLPDAT
!
!     LOOK.VIEW TRANSFORMS

   ROT1X(AX,AZ,APHI)=((AX*DCOS(APHI))-(AZ*DSIN(APHI)))
   ROT1Z(AX,AZ,APHI)=((AX*DSIN(APHI))+(AZ*DCOS(APHI)))
!
   ROT2Z(AZ,AY,AALF)=((AZ*DCOS(AALF))+(AY*DSIN(AALF)))
   ROT2Y(AZ,AY,AALF)=((-AZ*DSIN(AALF))+(AY*DCOS(AALF)))
!
   DEALLOCATE(CLPDAT,STAT=ALLOERR)
   ALLOCATE(CLPDAT(0:360,3),STAT=ALLOERR)
!
   VIEPH=(PII/180.0D0)*VIEPHI
   VIEAL=(PII/180.0D0)*VIEALF
!
   CLPDAT(0:360,1:3)=0.0
!
   X=0.0D0
   Y=0.0D0
   XID=0.0D0
   YID=0.0D0
!
!       ALL INPUT IS OK, KEEP GOING
!     THE ARRAY CONTAINING SURFACE CLAP DATA IS:
!     CLPDAT(0:360,1:3)
!
!     THE FIRST DIMENSION IS FOR THE DATA POINT NUMBER
!     THE SECOND DIMENSION IS FOR THE X,Y AND Z COORDINATES OR THE POINT
!     THE THIRD IS THE SURFACE NUMBER
!
!     WE NEED TO LOAD THE ARRAY BEFORE PLOTTING
!
!     THE PROCEDURE IS:
!
!     CYCLE THROUGH ALL THE SURFACES
!
!     1. WE WILL CLOCK AROUND THE CLEAR APERTURE FROM THE LOCAL +X
!     TOWARD THE LOCAL +Y AXIS,
!     4.0 DEGREE INCREMENTS AS MEASURED
!     BY AN OBSERVER AT THE SURFACE VERTEX, IN THE LOCAL COORDINATE
!     SYSTEM OF THE SURFACE, WITH THE OBSERVER FACING THE -Z AXIS
!     DIRECTION
!
   DO J=0,360
      ANGLE=(DBLE(J)*PII)/180.0D0
      AN2=(DBLE(J+1)*PII)/180.0D0
!     NOW ALONG THIS ANGLED LINE, WHAT ARE THE X AND Y COORDINATES
!     OF THE CLEAR APERTURE
!
      CALL NSSCAO1(JJ,X,Y,ANGLE,AN2)
      X=X*SFI
      Y=Y*SFI
!
!     THE RETURNED X AND Y ARE WHERE THE SAG IS TO BE CALCULATED
!
!     2. USE APPROPRIATE CALLS TO THE NSSSAG ROUTINE
!
      CALL NSSSAG(JJ,X,Y,Z)
!
!     ASSIGN ARRAY VALUES BASED ON J VALUE
      CLPDAT(J,1)=X
      CLPDAT(J,2)=Y
      CLPDAT(J,3)=Z
   END DO
!
!     3. THE ARRAYS NOW HAVE LOCAL X,Y AND Z VALUES STORED IN THEM
!     CONVERT THE LOCAL X ANY Y CLAPS TO GLOBAL NUMBERS
!     GLOBAL VERTEX DATA IS
   DO I=0,360
      X00=NSSVERTEX(1,JJ)
      Y00=NSSVERTEX(2,JJ)
      Z0 =NSSVERTEX(3,JJ)
      LX0=NSSVERTEX(4,JJ)
      MX0=NSSVERTEX(5,JJ)
      NX0=NSSVERTEX(6,JJ)
      LY0=NSSVERTEX(7,JJ)
      MY0=NSSVERTEX(8,JJ)
      NY0=NSSVERTEX(9,JJ)
      LZ0=NSSVERTEX(10,JJ)
      MZ0=NSSVERTEX(11,JJ)
      NZ0=NSSVERTEX(12,JJ)
      X=CLPDAT(I,1)
      Y=CLPDAT(I,2)
      Z=CLPDAT(I,3)
!
      X1=X00+((LX0*(X))+(LY0*(Y))&
      &+(LZ0*(Z)))
      Y1=Y00+((MX0*(X))+(MY0*(Y))&
      &+(MZ0*(Z)))
      Z1=Z0+((NX0*(X))+(NY0*(Y))&
      &+(NZ0*(Z)))
      CLPDAT(I,1)=X1
      CLPDAT(I,2)=Y1
      CLPDAT(I,3)=Z1
   END DO
!
!     4. NOW DETERMINE THE BEST PLACE FOR THE ROTATION POINT FOR
!               PLOT LOOK/VIEW
!
   CALL NSSROT
!
!     5.  CONVERT THE GLOBAL X AND Y CLAP VALUES
!               USING THE LOOK/VIEW VALUES
   DO I=0,360
      X=CLPDAT(I,1)
      Y=CLPDAT(I,2)
      Z=CLPDAT(I,3)
      X=X-XROT
      Y=Y-YROT
      Z=Z-ZROT
      XN=ROT1X(X,Z,VIEPH)
      YN=Y
      ZN=ROT1Z(X,Z,VIEPH)
      X=XN
      Y=YN
      Z=ZN
!
      ZN=ROT2Z(Z,Y,VIEAL)
      YN=ROT2Y(Z,Y,VIEAL)
      XN=X
      CLPDAT(I,1)=XN
      CLPDAT(I,2)=YN
      CLPDAT(I,3)=ZN
   END DO
!
!     THE ARRAYS NOW HAVE GLOBAL SURFACE CLAP DATA IN THEM
!
!     6.IF NEEDED, DETERMINE SCALE FACTORS AND PLOT RANGE
!
   CALL NSSPLTSC
!
!     7.CYCLE THROUGH THE THE ARRAYS, APPLY SCALE FACTORS
!
!     RIGHT NOW,COORDINATES ARE IN WORLD COORDINATES
!     CONVERT THEM TO DEVICE INDEPENDENT GRAPHICS COORDINATES IN
!     TWO STEPS.
!
!     THE WORLD X PLOTS TO THE PLOTTER X
!     THE WORLD Y PLOTS TO THE PLOTTER Y
!
!     STEP 1: CONVERT USING AN APPROPRIATE SCALE FACTOR
!               CALCULATING AN APPROPRIATE FACTOR IF NECESSARY
!
   DO I=0,360
      CLPDAT(I,1)=(CLPDAT(I,1)/NSSSCFA)*1000.0D0
      CLPDAT(I,2)=(CLPDAT(I,2)/NSSSCFA)*1000.0D0
   END DO
!
!     8. APPLY THE XSHIFT AND YSHIFT VALUES
   DO I=0,360
      IF(LORIENT) CALL ORSHIFT
      CLPDAT(I,1)=CLPDAT(I,1)+DBLE(PXSHFT)
      CLPDAT(I,2)=CLPDAT(I,2)+3500.0D0+DBLE(PYSHFT)
   END DO
!
!     9. SET THE PLOT JUSTIFICATION IF NEEDED
!     IF THERE ARE X-OFFSETS (JUSTIFICATION) TO APPLY, DO THEM HERE.
!
!     NOW
   IF(RCL.EQ.1.OR.RCL.EQ.-1) THEN
      JUSOFF=500.0D0-((XMINI/NSSSCFA)*1000.0D0)
      RCL=-1
   ELSE
   END IF
   IF(RCL.EQ.2.OR.RCL.EQ.-2) THEN
      RCL=-2
      JUSOFF=5000.0D0
   ELSE
   END IF
   IF(RCL.EQ.3.OR.RCL.EQ.-3) THEN
      JUSOFF=9500.0D0-((XMAXI/NSSSCFA)*1000.0D0)
      RCL=-3
   ELSE
   END IF
!
   DO I=0,360
      CLPDAT(I,1)=CLPDAT(I,1)+JUSOFF
   END DO
!     9. PLOT GAMMA
!     IF THERE IS A NON-ZERO GAMMA SPECIFIED IN PLOT LOOK OR PLOT VIEW THEN
!     ROTATE IN GAMMA ON THE SCREEN ABOUT THE CENTER OF THE SCREEN
!     WHICH HAS COORDINATES X=5000.0D0,Y=3500.0D0
!
!     FIRST SHIFT THE COORDINATE ORIGIN TO THE CETER OF THE DISPLAY
!
   DO I=0,360
      CLPDAT(I,1)=CLPDAT(I,1)-5000.0D0
      CLPDAT(I,2)=CLPDAT(I,2)-3500.0D0
   END DO
!     THE SCREEN COORDINATE IN real(real64) IN THE SHIFTED COORDINATE
!     FRAME IS NOW X AND Y

   IF(DBLE(PGAMMA).NE.0.0D0) THEN
      LKG=(PII/180.0D0)*DBLE(PGAMMA)

      DO I=0,360
         X=CLPDAT(I,1)
         Y=CLPDAT(I,2)
         XNEW=((X*DCOS(LKG))-(Y*DSIN(LKG)))
         YNEW=((X*DSIN(LKG))+(Y*DCOS(LKG)))
         CLPDAT(I,1)=XNEW
         CLPDAT(I,2)=YNEW
      END DO
   ELSE
   END IF
!     THE ROTATION IS DONE, NOW SHIFT THE ORIGIN BACK TO THE BOTTOM
!     LEFT HAND CORNER
   DO I=0,360
      CLPDAT(I,1)=CLPDAT(I,1)+5000.0D0
      CLPDAT(I,2)=CLPDAT(I,2)+3500.0D0
   END DO
!
!     NOW DRAW THE CLAP
!     WITH THE PEN UP, GO TO THE STARTING PLOT POSITION
   IF(.NOT.PLEXIS) PLEXIS=.TRUE.
   DO J=0,360
!     PUT INSTRUCTIONS IN P1ARAY TO DROP PEN AND DRAW
      IF(J.EQ.0) IPST=0
      IF(J.NE.0) IPST=1
      IF(CLPDAT(J,1).GT.1.0D6) CLPDAT(J,1)=1.0D6
      IF(CLPDAT(J,2).GT.1.0D6) CLPDAT(J,2)=1.0D6
      IF(CLPDAT(J,1).LT.-1.0D6) CLPDAT(J,1)=-1.0D6
      IF(CLPDAT(J,2).LT.-1.0D6) CLPDAT(J,2)=-1.0D6
      IX=INT(CLPDAT(J,1))
      IY=INT(CLPDAT(J,2))
      P1ARAY(J,1,1)=IX
      P1ARAY(J,2,1)=IY
      P1ARAY(J,3,1)=IPST
      IF(.NOT.PLEXIS) PLEXIS=.TRUE.
   END DO
!     FINISHED WITH THAT CLAP, LIFT PEN
   IPST=0
!     NOW ISSUE THE PLOTTING COMMANDS STORED IN THE P1ARAY ARRAY
!
!     LINE TYPE SETTING
   COLPAS=COLCLP
   CALL MY_COLTYP(COLPAS)
   OLLNTP=LNTYPE
   LNTYPE=0
   FIXUP=.FALSE.
   DO IK=0,360
      CALL PENMV2(P1ARAY(IK,1,1),P1ARAY(IK,2,1),P1ARAY(IK,3,1))
   END DO
   LNTYPE=OLLNTP
!
   DEALLOCATE(CLPDAT,STAT=ALLOERR)
   RETURN
END
