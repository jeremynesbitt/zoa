      SUBROUTINE RETRES
      IMPLICIT NONE
      INTEGER I,RETSURF,SINB
      REAL*8 D11,D12,D13
      REAL*8 D21,D22,D23
      REAL*8 D31,D32,D33,BETA,ALPHA,GAMMA,COSB
C
      INCLUDE 'DATLEN.INC'
      INCLUDE 'DATMAI.INC'
C
C
                   DO I=0,INT(SYSTEM(20))
      IF(ALENS(25,I).EQ.6.0D0) THEN
C     THERE IS A TILT RETURN TO RESOLVE WITH A RETURN TO SURFACE
      RETSURF=INT(ALENS(70,I))
C     SET ALL THE TILT AND DECENTERS ON THIS SURFACE TO ZERO
      ALENS(114:120,I)=0.0D0
      ALENS(26:31,I)=0.0D0
      ALENS(69,I)=0.0D0
C
C     HERE IS WHERE THE MAGIC GOES.
C     1. SET THE GLOBAL SURFACE TO SURFACE I
C     2. GET THE LOCATION OF SURFACES I AND RETSURF AND THEIR ORIENTATIONS
C        IN THE COORDINATE SYSTEM OF SURFACE I.
C     3. APPLY X,Y,Z DECENTERS TO PLACE SURFACE I'S VERTEX WHERE SURFACE
C        RETSURS'S VERTEX IS LOCATED.
c     4. DETERMINE THE ALPHA, BETA AND GAMMA TO APPLY TO THE ITH SURFACE
C     5. APPLY TILTS OF ALPHA,BETA AND GAMMA TO SURFACE I SO ITS AXES
C        ARE ALLIGNED WITH THE RETSURF'S AXES
C     5. SET ALENS(77,I) TO 1.0D0
      SAVE_KDP(10)=SAVEINPT(10)
      WC='GLOBAL'
      SQ=0
      S1=1
      S2=0
      S3=0
      S4=0
      S5=0
      DF1=0
      DF2=1
      DF3=1
      DF4=1
      DF5=1
      SN=1
      SST=0
      STI=0
      W1=DBLE(I)
      CALL GLOBAL
      REST_KDP(10)=RESTINPT(10)
C     NOW GET ALL THE GOODIES
        D11=VERTEX(4,RETSURF)
        D12=VERTEX(5,RETSURF)
        D13=VERTEX(6,RETSURF)
        D21=VERTEX(7,RETSURF)
        D22=VERTEX(8,RETSURF)
        D23=VERTEX(9,RETSURF)
        D31=VERTEX(10,RETSURF)
        D32=VERTEX(11,RETSURF)
        D33=VERTEX(12,RETSURF)

C     NOW CALCULATE ALPHA,BETA AND GAMMA AND ASSIGN THEM TO SURFACE I
C     CALCULATE BETA
      BETA=DASIN(-D31)
      COSB=DCOS(BETA)
      IF(COSB.NE.0.0D0) THEN
C     COSINE OF BETA IS NOT ZERO
      IF((D32/COSB).EQ.0.0D0.AND.(D33/COSB).NE.0.0D0) ALPHA=0.0D0
      IF((D32/COSB).EQ.0.0D0.AND.(D33/COSB).EQ.0.0D0) ALPHA=0.0D0
      IF((D32/COSB).NE.0.0D0.AND.(D33/COSB).EQ.0.0D0) ALPHA=PII/2.0D0
      IF((D32/COSB).NE.0.0D0.AND.(D33/COSB).NE.0.0D0)
     1ALPHA=DATAN2((D32/COSB),(D33/COSB))
      IF((D21/COSB).EQ.0.0D0.AND.(D11/COSB).NE.0.0D0) GAMMA=0.0D0
      IF((D21/COSB).EQ.0.0D0.AND.(D11/COSB).EQ.0.0D0) GAMMA=0.0D0
      IF((D21/COSB).NE.0.0D0.AND.(D11/COSB).EQ.0.0D0) GAMMA=PII/2.0D0
      IF((D21/COSB).NE.0.0D0.AND.(D11/COSB).NE.0.0D0)
     1GAMMA=DATAN2((-D21/COSB),(D11/COSB))
                        END IF
      IF(COSB.EQ.0.0D0) THEN
C     COSINE OF BETA IS ZERO
      IF(D31.EQ.-1.0D0) SINB=1
      IF(D31.EQ.1.0D0) SINB=-1
      IF(SINB.EQ.1) BETA=PII/2.0D0
      IF(SINB.EQ.-1) BETA=-PII/2.0D0
      GAMMA=0.0D0
      IF(SINB.EQ.1) THEN
      IF(D12.EQ.0.0D0.AND.D13.NE.0.0D0)
     1ALPHA=0.0D0
      IF(D12.EQ.0.0D0.AND.D13.EQ.0.0D0)
     1ALPHA=0.0D0
      IF(D12.NE.0.0D0.AND.D13.EQ.0.0D0)
     1ALPHA=PII/2.0D0
      IF(D12.NE.0.0D0.AND.D13.NE.0.0D0)
     1ALPHA=DATAN2((D12),(D13))
                       END IF
      IF(SINB.EQ.-1) THEN
      IF(D12.EQ.0.0D0.AND.D13.NE.0.0D0)
     1ALPHA=0.0D0
      IF(D12.EQ.0.0D0.AND.D13.EQ.0.0D0)
     1ALPHA=0.0D0
      IF(D12.NE.0.0D0.AND.D13.EQ.0.0D0)
     1ALPHA=PII/2.0D0
      IF(D12.NE.0.0D0.AND.D13.NE.0.0D0)
     1ALPHA=DATAN2((-D12),(-D13))
                       END IF
                        END IF
      ALPHA=ALPHA*(180.0D0/PII)
      BETA=BETA*(180.0D0/PII)
      GAMMA=GAMMA*(180.0D0/PII)
      ALENS(26,I)=ALPHA
      ALENS(27,I)=BETA
      ALENS(28,I)=GAMMA
      ALENS(118,I)=ALPHA
