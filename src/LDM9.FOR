C     SET TILT RET RES FLAG TO ON AND SET THE TILT TYPE TO TILT
      ALENS(77,I)=1.0D0
      ALENS(25,I)=1.0D0
                   END IF
                   END DO
C     SET TILT RET FLAG TO RESOLVED FOR THE LENS
                   SYSTEM(90)=1.0D0
C     TURN OFF GLOBAL MODE
      IF(RAYCLEAR) THEN
                        FOBYES=.FALSE.
                        REFEXT=.FALSE.
