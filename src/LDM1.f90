! SUB ULENNS.FOR
SUBROUTINE ULENNS
!
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE ULENNS. THIS IS THE SUBROUTINE WHICH
!       INITIALLY STARTS THE LENS  UPDATE OR CHANGE PROCEDURE.
!       THE CMD LEVEL IS DISABLED AND FLAG F6 IS SET TO 1.
!
!
   IF(SN.EQ.1.OR.SST.EQ.1) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & '"UPDATE LENS" TAKES NO NUMERIC OR STRING INPUT'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   END IF
!
   F1=0
   F6=1
!       INITIALIZE REFERENCE RAY LOGICAL TO .FALSE.
!       AND NULL TO FALSE
!
   IF(RAYCLEAR) THEN
      REFEXT=.FALSE.
      CPFNEXT=.FALSE.
      SPDEXT=.FALSE.
      GSPDEXT=.FALSE.
      CALL DELPSF
      FOBYES=.FALSE.
      NULL=.FALSE.
      RAYEXT=.FALSE.
      POLEXT=.FALSE.
      FAIL=.TRUE.
   END IF
!
!       SET SURF=0
   SURF=0
   IF(F15.EQ.0.AND.F12.NE.1) THEN
!       NOT AT CONFIGS OR UPDATE CONFIGS, ONLY CHANGE CFG 1
!
!       NOW COPY THE PERMANENT LENS BACK TO THE CURRENT LENS
!       DATA

      CALL PTOC
!
!               FINISHED RETURNING TO CFG 1
!
      F12=1
!
!       F12 MAY BE ANY VALUE FROM 2 TO 100
   END IF
   RETURN
END
