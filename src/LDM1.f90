SUBROUTINE THSOLV
   use type_utils, only: real2str
!
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE THSOLV WHICH IMPLEMENTS THE
!       THICKNESS SOLVES AND DELETIONS
!       COMMAND AT THE LENS INPUT AND UPDATE LENS LEVEL.
!
   INTEGER PIKCNT,I,SF
