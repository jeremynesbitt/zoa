      CALL MACFAL
      RETURN
   END IF
!
   IF(STI.EQ.1) THEN
      WRITE(OUTLYNE,*)'NO ADDITIONAL INFORMATION'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
