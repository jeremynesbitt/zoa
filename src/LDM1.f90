      IF(WC.EQ.'PCY'.OR.WC.EQ.'PCX') THEN
         IF(SURF.LT.1.AND.SYSTEM(26).EQ.-99.0D0.OR.SURF.LT.INT(SYSTEM(26)).AND.SYSTEM(26).NE.-99.0D0)THEN
