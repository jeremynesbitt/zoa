! SUB LENNS.FOR
SUBROUTINE LENNS
   USE GLOBALS
!
   use DATCFG
   use DATLEN
   use mod_surface
   use DATMAI
   use command_utils, only: report_error_and_fail
   IMPLICIT NONE
!
   INTEGER I,K,J
!
   REAL*8 XFOBB0,YFOBB0,ZFOBB0

!
   COMMON/OHFOB/XFOBB0,YFOBB0,ZFOBB0
!
!       THIS IS SUBROUTINE LENNS. THIS IS THE SUBROUTINE WHICH
!       INITIALLY STARTS THE SETUP OF A NEW LENS INTO THE ARRAY
!       ALENS. THE CMD LEVEL IS DISABLED AND FLAG F5 IS SET TO 1.
!
   XFOBB0=0.0D0
   YFOBB0=0.0D0
   ZFOBB0=0.0D0
   NUMHITS(0:MAXSUR)=1
!
   IF(SQ.EQ.1.OR.SN.EQ.1) THEN
      call report_error_and_fail('"LENS" TAKES NO EXPLICIT INPUT\nRE-ENTER COMMAND', 1)
      RETURN
   END IF
   MFG(1:75)='Manufacturer Name'
   CATNUM(1:75)='Lens Catalog Number'
   F1=0
   F5=1
!       SET GLASS CATALOG FLAG TO 1
   F22=1
!       INITIALIZE THE REFERECE RAY LOGICAL TO FALSE
!       AND NULL TO FALSE
!
   CALL AUTOFF
!
   IF(RAYCLEAR) THEN
      REFEXT=.FALSE.
      SPDEXT=.FALSE.
      GSPDEXT=.FALSE.
      CPFNEXT=.FALSE.
      CALL DELPSF
      FOBYES=.FALSE.
      NULL=.FALSE.
      RAYEXT=.FALSE.
      POLEXT=.FALSE.
      FAIL=.TRUE.
   END IF
!
!       ARRAY SYSTEM STORES NUMERIC DATA NOT SPECIFIC TO A
!       PARTICULAR SURFACE.
   LBL(0)(1:80)=' '
   ALENS(1:LSIZ,0)=0.0D0
   SOLVE(1:9,0)=0.0D0
   PIKUP(1:6,0,1:PSIZ)=0.0D0
   MULTCLAP(1:1000,1:3,0)=0.0D0
   MULTCOBS(1:1000,1:3,0)=0.0D0
!
   SYSTEM(1:SSIZ)=0.0D0
!
!       SYSTEM(31) TO SYSTEM(35) REPRESENT THE SPTWT VALUES
!       WHICH DEFAULT TO 1.0 AT LENS START UP.
!
   SYSTEM(31:35)=1.0D0
!
!       THE VALUES OF THE REFRACTIVE INDICES FOR THE 5 POSSIBLE
!       WAVELENGTHS ARE STORED FOR THE JTH SURFACE OF THE LENS
!       IN surf_refractive_index(J, 1),surf_refractive_index(J, 2),surf_refractive_index(J, 3),surf_refractive_index(J, 4) AND
!       surf_refractive_index(J, 5). THESE ARRAY ELEMENTS ALWAYS START WITH VALUES
!     AND 71 TO 75 FOR WAVELENGTHS 6 TO 10
!       OF 1.0 STORED IN THEM REPRESENTING THE INDICES OF THE DEFAULT
!       "AIR" MATERIAL. SO DO ELEMENTS 71 TO 75
!
!       THE ARRAY GLANAM STORES GLASS NAMES ASSOCIATED WITH
!       EACH SURFACE WHEN APPROPRIATE. COLUMN 1 STORES THE
!       MANUFACTURER NAME (CATALOG NAME) AND COLUMN 2 STORES
!       THE SPECIFIC GLASS DESIGNATOR (LIKE BK7).
!
!       INITIALIZE THE LENS TITLE AND ITS CONTINUATION AS WELL.
!
   IF(SST.EQ.1) THEN
      LI=WS
   ELSE
      LI='NEW LENS'
   END IF
   LIC(1)=CNULL
   LIC(2)=CNULL
   LIC(3)=CNULL
   LIC(4)=CNULL
   LICCNT=1
   INNI=CNULL
   LLTYPE=CNULL
!
!       INITIALIZE THE LENS SURFACE POINTER TO 0
!
   SURF=0
!       EACH AND EVERY TIME THE VARIABLE SURF IS CHANGED,
!       A FLOATING POINT REPRESENTATION IS STORED IN SYSTEM(20)
   SYSTEM(20)=DBLE(SURF)
!
!       SET DEFAULT WAVLENGTHS TO:
!
!       INITIALIZE MULTIPLE FIELDS
   CFLDCNT=0
   CFLDTYPE=0
   CFLDS(1:2,1:10)=0.0D0
!
!                       WV1=0.58756
!                       WV2=0.48613
!                       WV3=0.65627
!                       WV4=0.43584
!                       WV5=0.70652
!
!       WV1 THROUGH WV5 ARE STORED IN SYSTEM(1) THROUGH
!       SYSTEM(5) IN THE SYSTEM ARRAY.
!
!       SET THE DEFAULT WAVELENGTH VALUES.
!
   SYSTEM(1)=0.58756D0
   SYSTEM(2)=0.48613D0
   SYSTEM(3)=0.65627D0
   SYSTEM(4)=0.43584D0
   SYSTEM(5)=0.70652D0
!     SET WAVELENGTHS 6 TO 10 TO ZERO AND WEIGHTS TO ZERO
   SYSTEM(71:80)=0.0D0
   SYSTEM(111)=0.58756D0
   SYSTEM(112)=0.48613D0
   SYSTEM(113)=0.65627D0
   SYSTEM(114)=0.43584D0
   SYSTEM(115)=0.70652D0
!     SET WAVELENGTHS 6 TO 10 TO ZERO AND WEIGHTS TO ZERO
   SYSTEM(116:120)=0.0D0
!
!       THE PROGRAM DEFAULT UNITS ARE INCHES. THE UNIT CODE IS:
!
!                       INCHES=SYSTEM(6)=1
!                       CM    =SYSTEM(6)=2
!                       MM    =SYSTEM(6)=3
!
   SYSTEM(6)=1.0D0
!
!       SET THE DEFAULT VALUES FOR THE PRIMARY WAVELENGTH PAIRS
!       STORED IN SYSTEM(7) AND SYSTEM(8).
!
!       THESE PERTAIN TO THE COMMAND PCW.
   SYSTEM(7)=2.0D0
   SYSTEM(8)=3.0D0
!
!       SET THE DEFAULT VALUES FOR THE SECONDARY WAVELENGTH PAIRS
!       STORED IN SYSTEM(9) AND SYSTEM(10).
!
!       THESE PERTAIN TO THE COMMAND SCW.
   SYSTEM(9)=2.0D0
   SYSTEM(10)=1.0D0
!
!
!       SET THE DEFAULT VALUE FOR THE CONTROL WAVELENGTH
!       STORED IN SYSTEM(11).
!
!       THESE PERTAIN TO THE COMMAND SCW.
   SYSTEM(11)=1.0D0
!
!       SET THE DEFAULT VALUE FOR THE SAY AND SAX TO 1.0
!       STORED IN SYSTEM(12) AND SYSTEM(13).
!
!       THESE PERTAIN TO THE COMMANDS SAY AND SAX AND WRX,WRY.
   SYSTEM(64)=0.0D0
   SYSTEM(67)=0.0D0
   SYSTEM(12)=1.0D0
   SYSTEM(13)=1.0D0
   SYSTEM(83)=1.0D0
   SYSTEM(84)=1.0D0
   SYSTEM(85:86)=1.0D0
   SYSTEM(87:88)=0.001D0
!     SET  RAY AIMING ON
   SYSTEM(62)=1.0D0
!     SET TEL OFF
   SYSTEM(63)=0.0D0
!     SET AIMAPL OFF
   SYSTEM(70)=0.0D0
!
!       SET THE DEFAULT VALUES FOR SCY AND SCX.
!       EACH HAS TWO VALUES. SCY HAS Y0 AND Y1
!       SCX HAS X0 AND X1.
!       THE DEFAULT VALUES ARE Y0=1.0,Y1=0.0
!       X0=1.0,X1=0.0. THESE FOUR VALUES ARE STORED IN SYSTEM
!       14,15,16,AND 17
!
   SYSTEM(14)=1.0D0
   SYSTEM(15)=0.0D0
   SYSTEM(16)=1.0D0
   SYSTEM(17)=0.0D0
!       NOTE:
!       THE ALTERNATE FORMS OF SCY AND SCX ARE THE ANGULAR FORMS
!       SCY FANG AND SCX FANG.
!       IF THEY ARE USED FOR INPUT,
!       THEY USE THEIR INPUT VALUES PLUS LENS DATA FOR SURFACES
!       0 (OBJECT) AND 1 INORDER TO CALCULATE SCY,Y0 Y1 AND
!       SCX,X0 X1.
!
!       IF THESE ALTERNATE ANGULAR FORMS ARE USED, SYSTEM(18)
!       FOR SCY FANG AND SYSTEM(19) FOR SCX FANG ARE SET TO 1.0.
!       OTHERWISE THEY REMAIN SET TO 0.0 AS A DEFAULT.
!
   SYSTEM(18:19)=0.0D0
!
!       DEFAULT FOR IMAGE SURFACE NUMBER IS 1.0
   SYSTEM(20)=1.0D0
!
!       THE DEFAULT VALUES FOR SCY FANG AND SCX FANG AUTOMATICALLY
!       ARE ASSIGNED FROM LENS DATA AND SCY AND SCX VALUES AT "EOS".
!       DURING LENS INPUT THE DEFAULT VALUES ARE 0.0 AND ARE STORED
!       SYSTEM(21 THROUGH24)
   SYSTEM(21:24)=0.0D0
!
!       SYSTEM(49) TRACKS IF SCX/SCX FANG OR SAX HAVE BEEN ENTERED
!       EXPLICITLY.
!       IT IS ASSUMED THAT THEY HAVE NOT IN WHICH CASE
!       SYSTEM(49)=0.0, PUPIL AND FIELD ARE CIRCULAR
!
   SYSTEM(49)=0.0D0
!
!       ALL SURFACES ARE ASSUMED TO BE FLAT,SPHERICAL OR CONIC
!               FOR ASPHERICS  -  ALENS(8,SURF)=1.0
!
!       THE DEFAULT REFERENCE SURFACE IS SURFACE 1
!       TRACKER BY THE VALUE IN SYSTEM(25)
!
   SYSTEM(25)=1.0D0
!
!       THE DEFAULT APERTURE STOP CONDITION IS "NO"
!       AND IS INDICATED BY THE VALUE IN SYSTEM(26) OF -99
!       THIS IS THE DEFAULT. WHEN ASTOP NO IS ISSUED, SYSTEM(26)
!       IS ALWAYS SET TO THIS DEFAULT VALUE, OTHERWISE IT IS
!       SET EQUAL TO THE FLOATING POINT REPRESENTATION OF THE SURFACE
!       NUMBER WHICH IS TO BE THE APERTURE STOP
!
   SYSTEM(26)=-99.0D0
!
!       THE ASTOP ADJUSTMENT IS TRACKED IN SYSTEM(27)
!                       DEFAULT IS NO ADJUSTMENT = 0.0
!       EN ADJUST = SYSTEM(27)=1.0
!       EX ADJUSTMENT = SYSTEM(27)=-1.0
!
   SYSTEM(27)=0.0D0
!
!
!       THE LENS EVALUATION MODE (FOCAL,UFOCAL,AFOCAL OR UAFOCAL)
!       STATUS IS STORED IN SYSTEM(30) AS 1.0,2.0,3.0 OR 4.0
!       RESPECTIVELY. FOCAL IS THE DEFAULT MODE SET HERE.
!
   SYSTEM(30)=1.0D0
!
!       SET SYSTEM(50) TO ITS DEFAULT OF 1.00
!
   SYSTEM(50)=1.00D0
!       SET SYSTEM(56) TO ITS DEFAULT OF 1.00
!
   SYSTEM(56)=1.00D0
!
!
!       NOTE:
!       ACTUAL OUTPUT OF THE "CURRENT" LENS TO LENSTEXT.DATA IS
!       HANDELED DURING THE EXI OF EXIT ROUTINE.
!
!       ANY OTHER ACTION TO BE TAKEN DURING LENS INPUT IS HANDLED FROM
!       WITHIN SUBROUTINE LENIN.
!
!       INITIALIZE THE ARRAY THAT TRACKS THE EXISTENCE OF
!       ENTRIES IN THE CONFIG SUBFILE
   CFGCNT(2:MAXCFG)=0
   RETURN
END
! SUB XYGAMA.FOR
SUBROUTINE XYGAMA
!
   use DATLEN
   use mod_surface
   use DATMAI
   use command_utils, only: report_error_and_fail
   IMPLICIT NONE
!
!       THIS ROUTINE DOES THE PLOT XSHIFT,YSHIFT AND GAMMA COMMANDS
!
!
   IF(STI.EQ.0) THEN
!       CHECK SYNTAX
      IF(SST.EQ.1) THEN
         call report_error_and_fail('"PLOT "'//WQ(1:6)//'" TAKES NO STRING INPUT\nRE-ENTER COMMAND', 1)
         RETURN
      END IF
      IF(S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
         call report_error_and_fail(&
         & '"PLOT "'//WQ(1:6)//'" TAKES NO NUMERIC WORD #2 THROUGH #5 INPUT\nRE-ENTER COMMAND', 1)
         RETURN
      END IF
      IF(DF1.EQ.1) THEN
         call report_error_and_fail(&
         & '"PLOT "'//WQ(1:6)//' REQUIRES EXPLICIT NUMERIC WORD #1 INPUT\nRE-ENTER COMMAND', 1)
         RETURN
      END IF
!
      IF(WQ.EQ.'XSHIFT') PXSHFT=INT(W1)
      IF(WQ.EQ.'XSHIFT') LORIENT=.FALSE.
      IF(WQ.EQ.'YSHIFT') PYSHFT=INT(W1)
      IF(WQ.EQ.'YSHIFT') LORIENT=.FALSE.
      IF(WQ.EQ.'GAMMA') PGAMMA=INT(W1)
67    IF(PGAMMA.GE.360) THEN
         PGAMMA=PGAMMA-360
         IF(PGAMMA.GE.360) GO TO 67
      ELSE
!     PROCEED
      END IF
76    IF(PGAMMA.LE.-360) THEN
         PGAMMA=PGAMMA+360
         IF(PGAMMA.LE.360) GO TO 76
      ELSE
!     PROCEED
      END IF
      RETURN
   ELSE
!     STI=1
   END IF
   IF(STI.EQ.1) THEN
      IF(WQ.EQ."XSHIFT") WRITE(OUTLYNE,801) PXSHFT
      IF(WQ.EQ."YSHIFT") WRITE(OUTLYNE,802) PYSHFT
      IF(WQ.EQ."GAMMA") WRITE(OUTLYNE,803) PGAMMA
      CALL SHOWIT(1)
801   FORMAT('THE CURRENT "XSHIFT" VALUE IS ',I6)
802   FORMAT('THE CURRENT "YSHIFT" VALUE IS ',I6)
803   FORMAT('THE CURRENT "GAMMA" VALUE IS ',I4,' DEGREES')
      RETURN
   END IF
   RETURN
END
! SUB ULQUER.FOR
SUBROUTINE ULQUER
   use glass_manager
!
   use DATCFG
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE DISPLAYS THE CURRENT VALUE OF A LENS
!       SYSTEM PARAMETER FROM WITHIN THE UPDATE LENS
!       LEVEL IN RESPONSE TO A COMMAND NAME FOLLOWED BY A ?
!
   REAL*8 VALUE1,VALUE2,VALUE3,VALUE4,VALUE5,VALUE6
!
   INTEGER V1,V2,V3,V4,V5,V6,VA1,VA1WS1,VA1WS2,VA1WS3,VA1WS4 ,MAT,REPEAT,I
!
   CHARACTER VAL*80,VALWS1*80,VALWS2*80,VALWS3*80 ,VALWS4*80
!
!
   VALUE1=0.0D0
   VALUE2=0.0D0
   VALUE3=0.0D0
   VALUE4=0.0D0
   VALUE5=0.0D0
   VALUE6=0.0D0
   VAL=AA//AA//AA//AA
   VALWS1=AA//AA//AA//AA
   VALWS2=AA//AA//AA//AA
   VALWS3=AA//AA//AA//AA
   VALWS4=AA//AA//AA//AA
   V1=0
   V2=0
   V3=0
   V4=0
   V5=0
   V6=0
   VA1=0
   VA1WS1=0
   VA1WS2=0
   VA1WS3=0
   VA1WS4=0
   REPEAT=0
!
   MAT=0
   IF(WC.EQ.'AIR') MAT=1
   IF(WC.EQ.'REFL') MAT=1
   IF(WC.EQ.'REFLTIR') MAT=1
   IF(WC.EQ.'REFLTIRO') MAT=1
   IF(WC.EQ.'PERFECT') MAT=1
   IF(WC.EQ.'IDEAL') MAT=1
   IF(WC.EQ.'GLAK') MAT=1
   IF(WC.EQ.'GLASS') MAT=1
   IF(WC.EQ.'MODEL') MAT=1
   IF(gdb%isNameInCatalog(trim(WC))) MAT=1
   IF(WC.EQ.'USER') MAT=1
   IF(WC.EQ.'GLCAT') MAT=1
   IF(WC.EQ.'MATL') MAT=1

   IF(WC.NE.'IDEAL') THEN
      IF(MAT.EQ.1) THEN
         VA1WS1=1
         VALWS1=GLANAM(SURF,1)//GLANAM(SURF,2)
         VAL='CURRENT SURFACE MATERIAL TYPE IS:'
         VA1=1
         IF(WC.EQ.'MODEL') THEN
            V1=1
            VALUE1=surf_fict_n(SURF)
            V2=1
            VALUE2=surf_fict_v(SURF)
            V3=1
            VALUE3=surf_fict_w(SURF)
         END IF
         GO TO 200
      ELSE
      END IF
      IF(WC.EQ.'N1'.OR.WC.EQ.'N2'.OR.WC.EQ.'N3'.OR.WC.EQ.'N4'.OR.WC.EQ.'N5'.OR.WC.EQ.'N6'.OR.WC.EQ.'N7'.OR.WC.EQ.'N8'.OR.WC.EQ.'N9'.OR.WC.EQ.'N10') THEN
         IF(WC.EQ.'N1') THEN
            VA1=1
            VAL='THE REFRACTIVE INDEX FOR WAVELENGTH #1 IS:'
            V1=1
            VALUE1=surf_refractive_index(SURF, 1)
            GO TO 200
         ELSE
         END IF
         IF(WC.EQ.'N2') THEN
            VA1=1
            VAL='THE REFRACTIVE INDEX FOR WAVELENGTH #2 IS:'
            V1=1
            VALUE1=surf_refractive_index(SURF, 2)
            GO TO 200
         ELSE
         END IF
         IF(WC.EQ.'N3') THEN
            VA1=1
            VAL='THE REFRACTIVE INDEX FOR WAVELENGTH #3 IS:'
            V1=1
            VALUE1=surf_refractive_index(SURF, 3)
            GO TO 200
         ELSE
         END IF
         IF(WC.EQ.'N4') THEN
            VA1=1
            VAL='THE REFRACTIVE INDEX FOR WAVELENGTH #4 IS:'
            V1=1
            VALUE1=surf_refractive_index(SURF, 4)
            GO TO 200
         ELSE
         END IF
         IF(WC.EQ.'N5') THEN
            VA1=1
            VAL='THE REFRACTIVE INDEX FOR WAVELENGTH #5 IS:'
            V1=1
            VALUE1=surf_refractive_index(SURF, 5)
            GO TO 200
         ELSE
         END IF
         IF(WC.EQ.'N6') THEN
            VA1=1
            VAL='THE REFRACTIVE INDEX FOR WAVELENGTH #6 IS:'
            V1=1
            VALUE1=surf_refractive_index(SURF, 6)
            GO TO 200
         ELSE
         END IF
         IF(WC.EQ.'N7') THEN
            VA1=1
            VAL='THE REFRACTIVE INDEX FOR WAVELENGTH #7 IS:'
            V1=1
            VALUE1=surf_refractive_index(SURF, 7)
            GO TO 200
         ELSE
         END IF
         IF(WC.EQ.'N8') THEN
            VA1=1
            VAL='THE REFRACTIVE INDEX FOR WAVELENGTH #8 IS:'
            V1=1
            VALUE1=surf_refractive_index(SURF, 8)
            GO TO 200
         ELSE
         END IF
         IF(WC.EQ.'N9') THEN
            VA1=1
            VAL='THE REFRACTIVE INDEX FOR WAVELENGTH #9 IS:'
            V1=1
            VALUE1=surf_refractive_index(SURF, 9)
            GO TO 200
         ELSE
         END IF
         IF(WC.EQ.'N10') THEN
            VA1=1
            VAL='THE REFRACTIVE INDEX FOR WAVELENGTH #10 IS:'
            V1=1
            VALUE1=surf_refractive_index(SURF, 10)
            GO TO 200
         ELSE
         END IF
      ELSE
      END IF
      IF(WC.EQ.'INDEX'.OR.WC.EQ.'VNUM'.OR.WC.EQ.'DPART') THEN
         IF(GLANAM(SURF,1) .NE.'MODEL') THEN
            OUTLYNE='NO "INDEX", "VNUM" OR "DPART" VALUES EXIST'
            CALL SHOWIT(1)
            OUTLYNE='FOR NON-"MODEL" GLASSES'
            CALL SHOWIT(1)
         ELSE
            IF(WC.EQ.'INDEX') THEN
               VA1=1
               VAL='THE "INDEX" VALUE IS:'
               V1=1
               VALUE1=surf_fict_n(SURF)
               GO TO 200
            END IF
            IF(WC.EQ.'VNUM') THEN
               VA1=1
               VAL='THE "VNUM" VALUE IS:'
               V1=1
               VALUE1=surf_fict_v(SURF)
               GO TO 200
            END IF
            IF(WC.EQ.'DPART') THEN
               VA1=1
               VAL='THE "DPART" VALUE IS:'
               V1=1
               VALUE1=surf_fict_w(SURF)
               GO TO 200
            END IF
         END IF
      END IF
   ELSE
!     WC IS 'IDEAL'
      IF(MAT.EQ.1) THEN
         VA1WS1=1
         V1=1
         VALWS1=GLANAM(SURF,1)//GLANAM(SURF,2)
         VAL='CURRENT SURFACE MATERIAL TYPE IS:'
         VALUE1=surf_ideal_efl(SURF)
         VA1=1
      ELSE
      END IF
   END IF
   IF(WC.EQ.'UNITS') THEN
      IF(SYSTEM(6).EQ.1.0D0) VALWS1='INCHES'
      IF(SYSTEM(6).EQ.2.0D0) VALWS1='CM    '
      IF(SYSTEM(6).EQ.3.0D0) VALWS1='MM    '
      IF(SYSTEM(6).EQ.4.0D0) VALWS1='METERS'
      VA1WS1=1
      VAL='CURRENT SYSTEM UNITS ARE:'
      VA1=1
      GO TO 200
   ELSE
   END IF
   IF(WC.EQ.'REAL'.OR.WC.EQ.'PARAX') THEN
      IF(surf_paraxial_val(SURF).EQ.0.0D0) VALWS1='REAL'
      IF(surf_paraxial_val(SURF).EQ.1.0D0) VALWS1='PARAX'
      VA1WS1=1
      VAL='SURFACE TYPE IS:'
      VA1=1
      GO TO 200
   ELSE
   END IF
   IF(WC.EQ.'LBL'.OR.WC.EQ.'LABEL') THEN
      VA1=1
      VAL=LBL(SURF)
      IF(surf_label_flag(SURF).EQ.0.0D0)VAL='"THERE IS NO LABEL FOR THE CURRENT SURFACE"'
      GO TO 200
   ELSE
   END IF
   IF(WC.EQ.'LI') THEN
      VA1=1
      VAL='THE CURRENT LENS IDENTIFIER IS:'
      VA1WS1=1
      VALWS1=LI
      IF(LI.EQ.AA) VALWS1='"LI IS ALL BLANK"'
      VA1WS1=1
      GO TO 200
   ELSE
   END IF
   IF(WC.EQ.'MFG') THEN
      VA1=1
      VAL='THE CURRENT MFG IS:'
      VA1WS1=1
      VALWS1=MFG
      IF(MFG.EQ.AA) VALWS1='"MFG IS ALL BLANK"'
      VA1WS1=1
      GO TO 200
   ELSE
   END IF
   IF(WC.EQ.'CATNUM') THEN
      VA1=1
      VAL='THE CURRENT CATNUM IS:'
      VA1WS1=1
      VALWS1=CATNUM
      IF(CATNUM.EQ.AA) VALWS1='"CATNUM IS ALL BLANK"'
      VA1WS1=1
      GO TO 200
   ELSE
   END IF
   IF(WC.EQ.'INI') THEN
      VA1=1
      VAL='THE CURRENT DESIGNER IDENTIFIER IS:'
      VA1WS1=1
      VALWS1=INNI
      IF(INNI.EQ.AA) VALWS1='"INI IS ALL BLANK"'
      VA1WS1=1
      GO TO 200
   ELSE
   END IF
   IF(WC.EQ.'LTYPE') THEN
      VA1=1
      VAL='THE CURRENT LENS TYPE IDENTIFIER IS:'
      VA1WS1=1
      VALWS1=LLTYPE(1:5)
      IF(INNI.EQ.AA) VALWS1='"LTYPE IS ALL BLANK"'
      VA1WS1=1
      GO TO 200
   ELSE
   END IF
   IF(WC.EQ.'LIC') THEN
      VAL='THE CURRENT LENS IDENTIFIER CONTINUATION IS:'
      VA1=1
      VALWS1=LIC(1)
      IF(LIC(1).EQ.AA) VALWS1='"LIC (LINE 1) IS ALL BLANK"'
      VA1WS1=1
      VALWS2=LIC(2)
      IF(LIC(2).EQ.AA) VALWS2='"LIC (LINE 2) IS ALL BLANK"'
      VA1WS2=1
      VALWS3=LIC(3)
      IF(LIC(3).EQ.AA) VALWS3='"LIC (LINE 3) IS ALL BLANK"'
      VA1WS3=1
      VALWS4=LIC(4)
      IF(LIC(4).EQ.AA) VALWS4='"LIC (LINE 4) IS ALL BLANK"'
      VA1WS4=1
      GO TO 200
   ELSE
   END IF
   IF(WC.EQ.'REFS') THEN
      VA1=1
      VAL='CURRENT REFERENCE SURFACE NUMBER AND ORIENTATION ANGLE(DEG) ARE:'
      V1=1
      VALUE1=SYSTEM(25)
      V2=1
      VALUE2=SYSTEM(59)
      GO TO 200
   ELSE
   END IF
!
   IF(WC.EQ.'WRY'.OR.WC.EQ.'WRX'.OR.WC.EQ.'BDX'.OR.WC.EQ.'BDY') THEN
      VA1=1
      IF(WC.EQ.'WRY') VAL='CURRENT "WRY" VALUE IS:'
      IF(WC.EQ.'WRX') VAL='CURRENT "WRX" VALUE IS:'
      IF(WC.EQ.'BDY') VAL='CURRENT "BDY" VALUE IS:'
      IF(WC.EQ.'BDX') VAL='CURRENT "BDX" VALUE IS:'
      V1=1
      IF(WC.EQ.'WRX') VALUE1=SYSTEM(85)
      IF(WC.EQ.'WRY') VALUE1=SYSTEM(86)
      IF(WC.EQ.'BDX') VALUE1=SYSTEM(87)
      IF(WC.EQ.'BDY') VALUE1=SYSTEM(88)
      GO TO 200
   ELSE
   END IF
   IF(WC.EQ.'SAY'.OR.WC.EQ.'SAX') THEN
      IF(SYSTEM(64).NE.0.0D0) THEN
         WRITE(OUTLYNE,*)'"'//WC(1:3)//'" HAS NOT BEEN EXPLICITLY SET'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'BUT IS HELD WITH AN "NAO(X OR Y)" ASSIGNMENT'
         CALL SHOWIT(1)
      ELSE
