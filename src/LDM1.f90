   IF(DF1.EQ.1) W1=0.0D0
   IF(DF2.EQ.1) W2=SYSTEM(20)
   IF(WQ.EQ.'GAS') THEN
      IF(DF3.EQ.1.OR.DF4.EQ.1) THEN
         WRITE(OUTLYNE,*)'"THERM GAS" REQUIRES EXPLICIT NUMERIC WORDS #3 AND #4'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
   ELSE
