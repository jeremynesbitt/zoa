   END IF
!
   IF(SURF.EQ.0) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & 'OBJECT SURFACE MAY NOT BE DECENTERED'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   END IF
!
!       WE ARE AT LENS UPDATE LEVEL
!
!               PROCEED
!
!       CHECK FOR PIKUP GDX, GDY OR GDZ
!       AND DELETE THEN RESOLVE surf_special_type(SURF)
!
   IF(WC.EQ.'GDX') CT=37
   IF(WC.EQ.'GDY') CT=38
   IF(WC.EQ.'GDZ') CT=39
!
   IF(PIKUP(1,SURF,CT).EQ.1.0D0) THEN
      PIKUP(1:6,SURF,CT)=0.0D0
      call set_surf_special_type(SURF, surf_special_type(SURF)-1)
      IF(CT.EQ.37)WRITE(OUTLYNE,*)'SURFACE',SURF,' :PIKUP (GDX) DELETED'
      IF(CT.EQ.38)WRITE(OUTLYNE,*)'SURFACE',SURF,' :PIKUP (GDY) DELETED'
      IF(CT.EQ.39)WRITE(OUTLYNE,*)'SURFACE',SURF,' :PIKUP (GDZ) DELETED'
      CALL SHOWIT(1)
   END IF
   PIKCNT=0
   DO 10 I=1,PSIZ
      IF(PIKUP(1,SURF,I).NE.0.0D0) THEN
         PIKCNT=PIKCNT+1
      ELSE
      END IF
10 CONTINUE
   IF(PIKCNT.EQ.0)call set_surf_special_type(SURF, 0)
!
!       PROCEED WITH GDX, GDY OR GDZ ASSIGNMENT
!
!
   IF(surf_tilt_flag(SURF).EQ.6.0D0.OR.surf_tilt_flag(SURF).EQ.1.0D0) THEN
   ELSE
      IF(WC.EQ.'GDX')OUTLYNE='"GDX" REQUIRES A "TILT" OR A "TILT RET" SURFACE'
      IF(WC.EQ.'GDY')OUTLYNE='"GDY" REQUIRES A "TILT" OR A "TILT RET" SURFACE'
      IF(WC.EQ.'GDZ')OUTLYNE='"GDZ" REQUIRES A "TILT" OR A "TILT RET" SURFACE'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('NO-ACTION TAKEN', 1)
      RETURN
   END IF
   IF(surf_tilt_flag(SURF).EQ.6.0D0.OR.surf_tilt_flag(SURF).EQ.1.0D0) THEN
!
      IF(SQ.EQ.0) THEN
         IF(WC.EQ.'GDX')call set_surf_global_dx(SURF, W1)
         IF(WC.EQ.'GDY')call set_surf_global_dy(SURF, W1)
         IF(WC.EQ.'GDZ')call set_surf_global_dz(SURF, W1)
      END IF
      IF(WQ.EQ.'DELT') THEN
         IF(WC.EQ.'GDX')call set_surf_global_dx(SURF, surf_global_dx(SURF)+W1)
         IF(WC.EQ.'GDY')call set_surf_global_dy(SURF, surf_global_dy(SURF)+W1)
         IF(WC.EQ.'GDZ')call set_surf_global_dz(SURF, surf_global_dz(SURF)+W1)
      END IF
      IF(WQ.EQ.'CENT') THEN
         IF(WC.EQ.'GDX')call set_surf_global_dx(SURF, surf_global_dx(SURF)+(W1*0.01D0*surf_global_dx(SURF)))
         IF(WC.EQ.'GDY')call set_surf_global_dy(SURF, surf_global_dy(SURF)+(W1*0.01D0*surf_global_dy(SURF)))
         IF(WC.EQ.'GDZ')call set_surf_global_dz(SURF, surf_global_dz(SURF)+(W1*0.01D0*surf_global_dz(SURF)))
      END IF
   END IF
!
!
   RETURN
END
! SUB SGRAT.FOR
SUBROUTINE SGRAT
!
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE SGRAT WHICH IMPLEMENTS GRT,GRO,GRS,GRX,GRY
!       AND GRZ COMMANDS AT THE LENS UPDATE LEVEL.
!
   INTEGER PIKCNT,CT,I
!
!
!               CHECK FOR ADDITIONAL INPUT AND
!               PRINT ERROR AND RETURN IF DISCOVERED.
!
   IF(SST.EQ.1) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & '"'//WQ(1:3)//'" TAKES NO STRING INPUT'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(WC.NE.'GRT') THEN
      IF(surf_diffraction_flag(SURF).NE.1.0D0) THEN
         CALL REPORT_ERROR_AND_FAIL(&
         & '"'//WQ(1:3)//'" REQUIRES THE SURFACE TO BE A LINEAR GRATING'//'\n'//&
         & 'NO ACTION TAKEN', 1)
         RETURN
      END IF
   END IF
   IF(WC.NE.'GRT') THEN
      IF(S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1 .OR.S5.EQ.1) THEN
         CALL REPORT_ERROR_AND_FAIL(&
         & '"'//WQ(1:3)//'" TAKES NO NUMERIC WORD #2 THROUGH #5 INPUT'//'\n'//&
         & 'RE-ENTER COMMAND', 1)
         RETURN
      END IF
   ELSE
      IF(SN.EQ.1) THEN
         CALL REPORT_ERROR_AND_FAIL(&
         & '"'//WQ(1:3)//'" TAKES NO NUMERIC INPUT'//'\n'//&
         & 'RE-ENTER COMMAND', 1)
         RETURN
      END IF
   END IF
   IF(SQ.EQ.1) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & '"'//WQ(1:3)//'" TAKES NO QUALIFIER WORD INPUT'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(DF1.EQ.1.AND.WC.NE.'GRT') THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & '"'//WQ(1:3)//'" TAKES NO NUMERIC INPUT'//'\n'//&
      & 'REQUIRES EXPLICIT NUMERIC WORD #1 INPUT'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   END IF
!
   IF(SURF.EQ.0) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & 'OBJECT SURFACE MAY NOT BE A DIFFRACTION GRATING'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(F6.EQ.1.AND.SURF.EQ.INT(SYSTEM(20))) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & 'FINAL SURFACE MAY NOT BE A DIFFRACTION GRATING'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   END IF
!
!       WE ARE AT LENS UPDATE LEVEL
!
!               PROCEED
!
!       CHECK FOR PIKUP GDX, GDY OR GDZ
!       AND DELETE THEN RESOLVE surf_special_type(SURF)
!
   CT=43
!
   IF(PIKUP(1,SURF,CT).EQ.1.0D0) THEN
      PIKUP(1:6,SURF,CT)=0.0D0
      call set_surf_special_type(SURF, surf_special_type(SURF)-1)
      WRITE(OUTLYNE,*)'SURFACE',SURF,' :PIKUP (GRT) DELETED'
      CALL SHOWIT(1)
   END IF
   PIKCNT=0
   DO 10 I=1,PSIZ
      IF(PIKUP(1,SURF,I).NE.0.0D0) THEN
         PIKCNT=PIKCNT+1
      ELSE
      END IF
10 CONTINUE
   IF(PIKCNT.EQ.0)call set_surf_special_type(SURF, 0)
   IF(WC.EQ.'GRT') THEN
      call set_surf_diffraction_flag(SURF, 1)
      ALENS(97:99,SURF)=0.0D0
      call set_surf_grating_vy(SURF, 1.0D0)
      call set_surf_grating_vz(SURF, 0.0D0)
   END IF
   IF(WC.EQ.'GRO')call set_surf_grating_order(SURF, W1)
   IF(WC.EQ.'GRS')call set_surf_grating_spacing(SURF, W1)
   IF(WC.EQ.'GRX')call set_surf_grating_vx(SURF, W1)
   IF(WC.EQ.'GRY')call set_surf_grating_vy(SURF, W1)
   IF(WC.EQ.'GRZ')call set_surf_grating_vz(SURF, W1)
!
   RETURN
END
! SUB SGRTD.FOR
SUBROUTINE SGRTD
!
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE SGRTD WHICH IMPLEMENTS GRTD
!
   INTEGER PIKCNT,CT,I
!
!
!               CHECK FOR ADDITIONAL INPUT AND
!               PRINT ERROR AND RETURN IF DISCOVERED.
!
   IF(SST.EQ.1.OR.SQ.EQ.1.OR.SN.EQ.1) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & '"'//WQ(1:4)//'" TAKES NO ADDITIONAL INPUT'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(surf_diffraction_flag(SURF).NE.1.0D0) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & 'NO DIFFRACTION GRATING TO DELETE ON THE CURRENT SURFACE'//'\n'//&
      & 'NO ACTION TAKEN', 1)
      RETURN
   END IF
!
!     DELETE GRATING PIKUPS
!
   CT=43
!
   IF(PIKUP(1,SURF,CT).EQ.1.0D0) THEN
      PIKUP(1:6,SURF,CT)=0.0D0
      call set_surf_special_type(SURF, surf_special_type(SURF)-1)
