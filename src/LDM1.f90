      IF(PIKUP(1,SURF,I).NE.0.0D0) THEN
         PIKCNT=PIKCNT+1
      ELSE
      END IF
   END DO
   IF(PIKCNT.EQ.0)call set_surf_special_type(SURF, 0)
   ALENS(96:101,SURF)=0.0D0
   RETURN
END
