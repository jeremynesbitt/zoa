SUBROUTINE P33_PY(R,W,O,I,E)
   use iso_fortran_env, only: real64
!     EQUATION 3-PY,PAGE 33, ORBITS FOR AMATEURS WITH A MICROCOMPUTER
!     THIS ROUTINE COMPUTES PY AS R
!     W=ARGUMENT OF PERIHELION,I=ANGLE OF INCLINATION,O=LONGITUDE OF ASCENDING NODE
!     E=ANGLE OF THE OBLIQUITY OF THE ECLIPTIC
!     W,O AND I ASSUME ANGLES IN RADIANS
