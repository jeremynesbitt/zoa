SUBROUTINE P33_1(b,a,e)
   use iso_fortran_env, only: real64
!     EQUATION 1, PAGE 33, ORBITS FOR AMATEURS WITH A MICROCOMPUTER
!     THIS ROUTINE COMPUTES THE SEMI-MINOR AXIS OF AN ELLIPSE FROM SEMI-MAJOR
!     AXIS AND THE ECCENTRICITY
   real(real64) A,B,E,B2
   B=DSQRT((A**2)*(1.0D0-(E**2)))
   RETURN
END
