! SUB OPDLOD.FOR
SUBROUTINE OPDLOD
   use zoa_file_handler
   use global_widgets, only: curr_opd
!
   use DATSP1
   use DATLEN
   use DATMAI
   use mod_surface, only: surf_clap_type, surf_clap_dim, surf_array_parity
   use mod_system, only: sys_wl_ref
   IMPLICIT NONE
!
   EXTERNAL FF3
!
   INTEGER SSN,SM,NP2,MP,COUNT,I,J,N,M,II,IIP, OPDCNT
   INTEGER IWIW,NUMTOT,IW
   COMMON/IWIWIW/IWIW,NUMTOT,IW
!
   LOGICAL OPMAP,OPEN64
!
   COMMON/OPOPMP/OPMAP
!
   COMMON/PRSIZE/COUNT
!
!       THIS PASSES THE SOLUTION OF THE LEAST SQUARES PROBLEM
   COMMON/SOLU/X
!
   REAL*8 ACCUM(1:96,1:96),REFHT,&
   &CCOL(1:96),&
   &X(1:96)
!
   COMMON/ACDATA/ACCUM,CCOL
!
   INTEGER III,JJ,KK,ALLOERR
   integer :: rec64
!
   REAL*8 DWW1,DWW2,DWW3,DWW4,&
   &TERM,FF3,RHO,THETA
!
!
   REAL*8 W,V,U,&
   &B,XXX
!
   DIMENSION W(:),V(:,:),U(:,:),&
   &B(:),XXX(:)
!
   ALLOCATABLE :: W,V,U,B,XXX
   ALLOCATE (W(1:96),V(1:96,1:96),U(1:96,1:96),B(1:96),XXX(1:96)&
   &,STAT=ALLOERR)
!
!       DWW1=REF SURF Y-COORDINATE
!       DWW2=REF SURF X-COORDINATE
!       DWW3=OPD VALUE
!       DWW4=INTENSITY VALUE
!
   IF(SQ.EQ.1.OR.SST.EQ.1) THEN
      OUTLYNE=&
      &'"FITZERN" TAKES NO QUALIFIER OR ALPHANUMERIC INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      DEALLOCATE(U,W,V,XXX,B,STAT=ALLOERR)
      RETURN
   END IF
!
!
   IF(S3.EQ.1.OR.S2.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
      OUTLYNE=&
      &'"FITZERN" ONLY TAKES NUMERIC WORD #1 INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      DEALLOCATE(U,W,V,XXX,B,STAT=ALLOERR)
      RETURN
   END IF
!
!     DEFAULT WAVELENGTH IS CONTROL WAVELENGTH
   IF(DF1.EQ.1) W1=sys_wl_ref()
   IF(INT(W1).LT.1.OR.INT(W1).GT.10) THEN
      OUTLYNE=&
      &'NUMERIC WORD #1 MUST BE 1, 2, 3, 4, 5, 6, 7, 8, 9 OR 10'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      DEALLOCATE(U,W,V,XXX,B,STAT=ALLOERR)
      RETURN
   END IF
   IF(.NOT.CPFNEXT) THEN
      OUTLYNE=&
      &'NO CAPFN EXISTS'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('NO ACTION TAKEN', 1)
      DEALLOCATE(U,W,V,XXX,B,STAT=ALLOERR)
      RETURN
   END IF
   OUTLYNE=&
   &'FITTING THE WAVEFRONT TO A 37 TERM FRINGE ZERNIKE'
   CALL SHOWIT(1)
   CCOL(1:96)=0.0D0
   CFTYPE(1:96)=0.0D0
   X(1:96)=0.0D0
   XXX(1:96)=0.0D0
   ACCUM(1:96,1:96)=0.0D0
!
!     DETERMINE THE REFERNCE APERTURE HEIGHT
!     OF THE REF SURFACE COORDINATES
   IF(surf_clap_type(NEWREF) >= 1.AND.surf_clap_type(NEWREF) <= 5.AND.surf_array_parity(NEWREF) == 0) THEN
      IF(surf_clap_type(NEWREF) == 1) THEN
         IF(surf_clap_dim(NEWREF, 1).LE.surf_clap_dim(NEWREF, 2)) THEN
            REFHT=surf_clap_dim(NEWREF, 1)
         ELSE
            REFHT=surf_clap_dim(NEWREF, 2)
         END IF
      END IF
!        RECT CLAP
!
      IF(surf_clap_type(NEWREF) == 2) THEN
         REFHT=DSQRT((surf_clap_dim(NEWREF, 1)**2)+(surf_clap_dim(NEWREF, 2)**2))
      END IF
!        ELIP CLAP
!
      IF(surf_clap_type(NEWREF) == 3) THEN
         IF(surf_clap_dim(NEWREF, 1).GE.surf_clap_dim(NEWREF, 2)) THEN
            REFHT=surf_clap_dim(NEWREF, 1)
         ELSE
            REFHT=surf_clap_dim(NEWREF, 2)
         END IF
      END IF
!        RCTK CLAP
!
      IF(surf_clap_type(NEWREF) == 4) THEN
         REFHT=DSQRT((surf_clap_dim(NEWREF, 1)**2)+(surf_clap_dim(NEWREF, 2)**2))
      END IF
!        POLY CLAP
!
      IF(surf_clap_type(NEWREF) == 5) THEN
         REFHT=DABS(surf_clap_dim(NEWREF, 1))
      END IF
!        IPOLY CLAP
!
      IF(surf_clap_type(NEWREF) == 6) THEN
         REFHT=DABS(surf_clap_dim(NEWREF, 5))
      END IF
!
   ELSE
!       NO CLAP ON REF SURF.
      IF(PXTRAY(1,NEWREF).GE.PXTRAX(1,NEWREF)) THEN
         REFHT=PXTRAY(1,NEWREF)
      ELSE
         REFHT=PXTRAX(1,NEWREF)
      END IF
   END IF
!
   !      OPEN(UNIT=64,ACCESS='DIRECT',FILE=LIBSPO//'OPDDAT.DAT',
   !   1  FORM='UNFORMATTED',RECL=(10*NRECL),STATUS='UNKNOWN')
   !    CALL CLOSE_FILE(64,0)
   call clear_file(trim(LIBSPO)//'OPDDAT.DAT')
   inquire(iolength=rec64) DWW1,DWW2,DWW3,DWW4
!
!     OPEN OPDDAT.DAT
!
   OPEN(UNIT=64,ACCESS='DIRECT',FILE=trim(LIBSPO)//'OPDDAT.DAT',&
   &FORM='UNFORMATTED',RECL=rec64,STATUS='UNKNOWN')
   KK=1
   PRINT *, "ITOT is ", ITOT
   DO IIP=2,ITOT
!     LOAD DSPOT(*) WITH DSPOTT(*,ID)
      ID=IIP-1
      CALL SPOTIT(4)
!
      IF(DSPOT(16).EQ.W1.AND.DSPOT(12).NE.0.0D0.OR.&
      &DSPOT(16).EQ.W1.AND.DSPOT(12).EQ.0.0D0.AND.&
      &DSPOT(7).EQ.7.0D0) KK=KK+1
   END DO
   IF(KK.LT.37.0D0) THEN
!     NOT ENOUGH DATA FOR A FIT
      OUTLYNE=&
      &'NOT ENOUGH DATA POINTS EXIST FOR A RELIABLE FIT'
      CALL SHOWIT(1)
      OUTLYNE=&
      &'OR NO DATA WAS AVAILABLE AT THE SPECIFIED WAVELENGTH'
      CALL SHOWIT(1)
      OUTLYNE=&
      &'TRY TRACING A DENSER GRID OF RAYS'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('NO ACTION TAKEN', 1)
      DEALLOCATE(U,W,V,XXX,B,STAT=ALLOERR)
      RETURN
   END IF
!
   KK=1
   if (allocated(curr_opd%X)) then
      deallocate(curr_opd%X)
      deallocate(curr_opd%Y)
      deallocate(curr_opd%Z)


   end if
   allocate(curr_opd%X(IWIW))
   allocate(curr_opd%Y(IWIW))
   allocate(curr_opd%Z(IWIW))
   curr_opd%numPts = IWIW
   curr_opd%numx = INT(sqrt(real(curr_opd%numPts)))
   curr_opd%numy = INT(sqrt(real(curr_opd%numPts)))

   PRINT *, "OPDLOD numPts is ", curr_opd%numPts
   PRINT *, "IWIW is ", IWIW



   DO IIP=2,ITOT
!     LOAD DSPOT(*) WITH DSPOTT(*,ID)
      ID=IIP-1
      CALL SPOTIT(4)

!


!
      IF(DSPOT(16).EQ.W1) THEN
         !PRINT *, "W1 for ID ", KK
         DWW1=DSPOT(6)/REFHT
         DWW2=DSPOT(5)/REFHT
         DWW3=DSPOT(4)/(TWOPII)
         DWW4=DSPOT(12)

         curr_opd%X(KK) = DWW1
         curr_opd%Y(KK) = DWW2
         curr_opd%Z(KK) = DWW3

!
!     DATA FOR DESIRED WAVELENGTH, PROCEED
!
!       PROCEED WITH FITTING A TYPE 3 TYPE/SURFACE
         RHO=DSQRT((DWW1**2)+(DWW2**2))
         IF(DABS(DWW1).GE.DABS(((1.0D35)*DWW2))) THEN
            IF(DWW1.GE.0.0D0) THETA=PII/2.0D0
            IF(DWW1.LT.0.0D0) THETA=(3.0D0*PII)/2.0D0
         ELSE
            IF(DABS(DWW1).EQ.0.0D0.AND.DABS(DWW2).EQ.0.0D0) THEN
               THETA=0.0D0
            ELSE
               THETA=DATAN2(DWW1,DWW2)
            END IF
            IF(THETA.LT.0.0D0) THETA=THETA+(TWOPII)
         END IF
!
         III=0
         DO I=1,37
!       FILL THE COLLUMN ARRAY
            III=III+1
!       CALCULATE COLUMN VECTOR
!       FUNCTION FF3 RETURNS THE CORRECT VALUE
!       FOR THE ITH ENTRY IN THE COLUMN VECTOR
!       WITHOUT THE GIVEN DEPENDENT VARIABLE VALUE
!       OF THE WEIGHT FUNCTION FACTORED IN. THIS IS
!       FOR THE STANDARD ZERNIKE. I RANGES FROM 1 TO 37
            TERM= FF3(RHO,THETA,I)
            TERM=TERM*DWW3*DWW4
            CCOL(III)=CCOL(III)+TERM
            JJ=0
            DO J=1,37
!       DO THE APPROPRIATE ROW FOR COLUMN I
               JJ=JJ+1
               TERM= FF3(RHO,THETA,I)*FF3(RHO,THETA,J)
               ACCUM(III,JJ)=ACCUM(III,JJ)+(DWW4*TERM)
            END DO
         END DO
         COUNT=III
!
!       ALL VALUES CALCULATED FOR THE CURRENT "DATA"
!       STATEMENT.
!
         KK=KK+1
         WRITE(UNIT=64,REC=KK) DWW1,DWW2,DWW3,DWW4
      END IF
   END DO
!     KK IS THE NUMBER OF FITTED DATA ITEMS
   WRITE(UNIT=64,REC=1) KK
!
!       THE LARGE ARRAY WITH ACCUMULATED DATA IS
!       ACCUM. THE COLUMN VECTOR WITH FUNCTIONAL DATA
!       IS CCOL.
   MP=96
   NP2=96
   N=COUNT
   M=COUNT
   I=COUNT
   B(1:I)=CCOL(1:I)
   J=COUNT
   U(1:I,1:J)=ACCUM(1:I,1:J)
!       DO SINGULAR VALUE DECOMPOSITION
!
   SM=M
   SSN=N
   CALL SVDCMP(U,SM,SSN,MP,NP2,W,V)
   M=SM
   N=SSN
!
!       SOLVE LINEAR EQUATION
!
   SM=M
   SSN=N
   CALL SVBKSB(U,W,V,SM,SSN,MP,NP2,B,XXX)
   M=SM
   N=SSN
!
!       SIMPLE DATA FIT
   II=0
   DO I=1,37
      II=II+1
      CFTYPE(I)=XXX(II)
      X(I)=CFTYPE(I)
   END DO
   DO I=38,96
      II=II+1
      CFTYPE(I)=0.0D0
      X(I)=CFTYPE(I)
   END DO
   OPMAP=.TRUE.
   OUTLYNE=&
   &'FITTING COMPLETED'
   CALL SHOWIT(1)
   DEALLOCATE(U,W,V,XXX,B,STAT=ALLOERR)
   RETURN
END
! SUB WAVESLP1.FOR
SUBROUTINE WAVESLP1(DSPOTT,IITOT,JTYPE)
!
   use DATSP1
   use DATSPD
   use DATLEN
   use DATMAI
   use mod_surface, only: surf_clap_type, surf_clap_dim, surf_array_parity
   use mod_system, only: sys_units, sys_wavelength, sys_wl_weight
   IMPLICIT NONE
!
   EXTERNAL FF3
!
   INTEGER SSN,SM,NP2,MP,JTYPE,IITOT,COUNT,I,J,N,M,II,IIP,IWK,JJJ &
   &,ISS
!
   COMMON/PRSIZE/COUNT
!
!       THIS PASSES THE SOLUTION OF THE LEAST SQUARES PROBLEM
   COMMON/SOLU/X
   LOGICAL ERRR
!
   DOUBLE PRECISION ACCUM(1:96,1:96),REFHT,WW,TERM1,TERM2,&
   &WAVEX,CCOL(1:96),DSPOTT(35,IITOT),X(1:96),SUML2,SUML4,WEIS &
   &,DENOM,DZ1,DZ2,SS1,SS2,VALL,VALX,VALY,WEI(1:10),LAM(1:10)&
   &,PHI,PHIX,PHIY,SSS,COSX,COSY,SINX,SINY,SSSO,DLLZX,DLLZY,DLLZ0 &
   &,PHIO,NRR,ORR,TERM3
!
   COMMON/ACDATA/ACCUM,CCOL
!
   INTEGER III,JJ,ALLOERR
!
   DOUBLE PRECISION DWW1,DWW2,DWW3,DWW4,&
   &TERM,FF3,RHO,THETA
!
!
   DOUBLE PRECISION W,V,U,&
   &B,XXX,WV
!
   DIMENSION W(:),V(:,:),U(:,:),&
   &B(:),XXX(:)
!
   ALLOCATABLE :: W,V,U,B,XXX
   DEALLOCATE (W,V,U,B,XXX &
   &,STAT=ALLOERR)
   ALLOCATE (W(1:96),V(1:96,1:96),U(1:96,1:96),B(1:96),XXX(1:96)&
   &,STAT=ALLOERR)
!
!       DWW1=REF SURF Y-COORDINATE
!       DWW2=REF SURF X-COORDINATE
!       DWW3=OPD VALUE
!       DWW4=INTENSITY VALUE
!
   WAVEX=0.0D0
   DLLX=0.0D0
   DLLY=0.0D0
   DLLZ=0.0D0
   CCOL(1:96)=0.0D0
   CFTYPE(1:96)=0.0D0
   X(1:96)=0.0D0
   XXX(1:96)=0.0D0
   ACCUM(1:96,1:96)=0.0D0
!
!     DETERMINE THE REFERNCE APERTURE HEIGHT
!     OF THE REF SURFACE COORDINATES
   IF(surf_clap_type(NEWREF) >= 1.AND.surf_clap_type(NEWREF) <= 5.AND.surf_array_parity(NEWREF) == 0) THEN
      IF(surf_clap_type(NEWREF) == 1) THEN
         IF(surf_clap_dim(NEWREF, 1).LE.surf_clap_dim(NEWREF, 2)) THEN
            REFHT=surf_clap_dim(NEWREF, 1)
         ELSE
            REFHT=surf_clap_dim(NEWREF, 2)
         END IF
      END IF
!        RECT CLAP
!
      IF(surf_clap_type(NEWREF) == 2) THEN
         REFHT=DSQRT((surf_clap_dim(NEWREF, 1)**2)+(surf_clap_dim(NEWREF, 2)**2))
      END IF
!        ELIP CLAP
!
      IF(surf_clap_type(NEWREF) == 3) THEN
         IF(surf_clap_dim(NEWREF, 1).GE.surf_clap_dim(NEWREF, 2)) THEN
            REFHT=surf_clap_dim(NEWREF, 1)
         ELSE
            REFHT=surf_clap_dim(NEWREF, 2)
         END IF
      END IF
!        RCTK CLAP
!
      IF(surf_clap_type(NEWREF) == 4) THEN
         REFHT=DSQRT((surf_clap_dim(NEWREF, 1)**2)+(surf_clap_dim(NEWREF, 2)**2))
      END IF
!        POLY CLAP
!
      IF(surf_clap_type(NEWREF) == 5) THEN
         REFHT=DABS(surf_clap_dim(NEWREF, 1))
      END IF
!        IPOLY CLAP
!
      IF(surf_clap_type(NEWREF) == 6) THEN
         REFHT=DABS(surf_clap_dim(NEWREF, 5))
      END IF
!
   ELSE
!       NO CLAP ON REF SURF.
      IF(PXTRAY(1,NEWREF).GE.PXTRAX(1,NEWREF)) THEN
         REFHT=PXTRAY(1,NEWREF)
      ELSE
         REFHT=PXTRAX(1,NEWREF)
      END IF
   END IF
!
!     COMPUTE THE EFFECTIVE WAVELENGTH
   SUML2=0.0D0
   SUML4=0.0D0
   LAMAVE=0.0D0
   WEI(1)=sys_wl_weight(1)
   WEI(2)=sys_wl_weight(2)
   WEI(3)=sys_wl_weight(3)
   WEI(4)=sys_wl_weight(4)
   WEI(5)=sys_wl_weight(5)
   WEI(6)=sys_wl_weight(6)
   WEI(7)=sys_wl_weight(7)
   WEI(8)=sys_wl_weight(8)
   WEI(9)=sys_wl_weight(9)
   WEI(10)=sys_wl_weight(10)
   LAM(1)=sys_wavelength(1)
   LAM(2)=sys_wavelength(2)
   LAM(3)=sys_wavelength(3)
   LAM(4)=sys_wavelength(4)
   LAM(5)=sys_wavelength(5)
   LAM(6)=sys_wavelength(6)
   LAM(7)=sys_wavelength(7)
   LAM(8)=sys_wavelength(8)
   LAM(9)=sys_wavelength(9)
   LAM(10)=sys_wavelength(10)
   WEIS=0.0D0
   DO ISS=1,10
      WEIS=WEIS+WEI(ISS)
   END DO
   IF(WEIS.EQ.0.0D0) WEIS=1.0D0
   DO ISS=1,10
      WEI(ISS)=WEI(ISS)/WEIS
   END DO
   DO ISS=1,10
      IF(LAM(ISS).NE.0.0D0.AND.WEI(ISS).NE.0.0D0) THEN
         SUML2=SUML2+(WEI(ISS)/(LAM(ISS)**2))
         SUML4=SUML4+(WEI(ISS)/(LAM(ISS)**4))
      END IF
   END DO
   LAMAVE=DSQRT(SUML2/SUML4)
   IF(sys_units().EQ.1.0D0) THEN
      LAMAVE=LAMAVE*3.93700787402D-5
   END IF
   IF(sys_units().EQ.2.0D0) THEN
      LAMAVE=LAMAVE*1.0D-4
   END IF
   IF(sys_units().EQ.3.0D0) THEN
      LAMAVE=LAMAVE*1.0D-3
   END IF
   IF(sys_units().EQ.4.0D0) THEN
      LAMAVE=LAMAVE*1.0D-6
   END IF
   DO IIP=1,IITOT
