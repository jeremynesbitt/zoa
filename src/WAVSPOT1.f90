!       PROCEED WITH LISTING
!
!       WRITE THE HEADING THEN THE DATA
   WRITE(OUTLYNE,1106)
   CALL SHOWIT(0)
   WRITE(OUTLYNE,1105)
   CALL SHOWIT(0)
   WRITE(OUTLYNE,1106)
   CALL SHOWIT(0)
