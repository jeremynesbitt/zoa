   X(1:96)=0.0D0
   XXX(1:96)=0.0D0
   ACCUM(1:96,1:96)=0.0D0
!
!     DETERMINE THE REFERNCE APERTURE HEIGHT
!     OF THE REF SURFACE COORDINATES
   IF(surf_clap_type(NEWREF) >= 1.AND.surf_clap_type(NEWREF) <= 5.AND.surf_array_parity(NEWREF) == 0) THEN
      IF(surf_clap_type(NEWREF) == 1) THEN
         IF(surf_clap_dim(NEWREF, 1).LE.surf_clap_dim(NEWREF, 2)) THEN
            REFHT=surf_clap_dim(NEWREF, 1)
         ELSE
            REFHT=surf_clap_dim(NEWREF, 2)
         END IF
      END IF
!        RECT CLAP
!
      IF(surf_clap_type(NEWREF) == 2) THEN
         REFHT=DSQRT((surf_clap_dim(NEWREF, 1)**2)+(surf_clap_dim(NEWREF, 2)**2))
      END IF
!        ELIP CLAP
!
      IF(surf_clap_type(NEWREF) == 3) THEN
         IF(surf_clap_dim(NEWREF, 1).GE.surf_clap_dim(NEWREF, 2)) THEN
            REFHT=surf_clap_dim(NEWREF, 1)
         ELSE
            REFHT=surf_clap_dim(NEWREF, 2)
         END IF
      END IF
!        RCTK CLAP
!
      IF(surf_clap_type(NEWREF) == 4) THEN
         REFHT=DSQRT((surf_clap_dim(NEWREF, 1)**2)+(surf_clap_dim(NEWREF, 2)**2))
      END IF
!        POLY CLAP
!
      IF(surf_clap_type(NEWREF) == 5) THEN
         REFHT=DABS(surf_clap_dim(NEWREF, 1))
      END IF
!        IPOLY CLAP
!
      IF(surf_clap_type(NEWREF) == 6) THEN
         REFHT=DABS(surf_clap_dim(NEWREF, 5))
      END IF
!
   ELSE
!       NO CLAP ON REF SURF.
      IF(PXTRAY(1,NEWREF).GE.PXTRAX(1,NEWREF)) THEN
         REFHT=PXTRAY(1,NEWREF)
      ELSE
         REFHT=PXTRAX(1,NEWREF)
      END IF
   END IF
!
   call clear_file(trim(LIBSPO)//'OPDDAT.DAT')
!
!     OPEN OPDDAT.DAT
!
   OPEN(UNIT=64,ACCESS='DIRECT',FILE=trim(LIBSPO)//'OPDDAT.DAT',&
   &FORM='UNFORMATTED',RECL=(10*NRECL),STATUS='UNKNOWN')
   KK=1
   DO IIP=2,ITOT
!     LOAD DSPOT(*) WITH DSPOTT(*,ID)
      ID=IIP-1
      CALL SPOTIT(4)
!
      IF(DSPOT(16).EQ.WVNUMOP.AND.DSPOT(12).NE.0.0D0.OR.&
      &DSPOT(16).EQ.WVNUMOP.AND.DSPOT(12).EQ.0.0D0.AND.&
      &DSPOT(7).EQ.7.0D0) KK=KK+1
   END DO
   IF(KK.LT.37.0D0) THEN
!     NOT ENOUGH DATA FOR A FIT
      CALL MACFAL
      DEALLOCATE(U,W,V,XXX,B,STAT=ALLOERR)
      ERROP=.TRUE.
      RETURN
   END IF
!
   KK=1
   DO IIP=2,ITOT
!     LOAD DSPOT(*) WITH DSPOTT(*,ID)
      ID=IIP-1
      CALL SPOTIT(4)
!
      DWW1=DSPOT(6)/REFHT
      DWW2=DSPOT(5)/REFHT
      DWW3=DSPOT(4)/(TWOPII)
      DWW4=DSPOT(12)
!
      IF(DSPOT(16).EQ.WVNUMOP) THEN
!
!     DATA FOR DESIRED WAVELENGTH, PROCEED
!
!       PROCEED WITH FITTING A TYPE 3 TYPE/SURFACE
         RHO=DSQRT((DWW1**2)+(DWW2**2))
         IF(DABS(DWW1).GE.DABS(((1.0D35)*DWW2))) THEN
            IF(DWW1.GE.0.0D0) THETA=PII/2.0D0
            IF(DWW1.LT.0.0D0) THETA=(3.0D0*PII)/2.0D0
         ELSE
            IF(DABS(DWW1).EQ.0.0D0.AND.DABS(DWW2).EQ.0.0D0) THEN
               THETA=0.0D0
            ELSE
               THETA=DATAN2(DWW1,DWW2)
            END IF
            IF(THETA.LT.0.0D0) THETA=THETA+(TWOPII)
         END IF
!
         III=0
         DO I=1,37
!       FILL THE COLLUMN ARRAY
            III=III+1
!       CALCULATE COLUMN VECTOR
!       FUNCTION FF3 RETURNS THE CORRECT VALUE
!       FOR THE ITH ENTRY IN THE COLUMN VECTOR
!       WITHOUT THE GIVEN DEPENDENT VARIABLE VALUE
!       OF THE WEIGHT FUNCTION FACTORED IN. THIS IS
!       FOR THE STANDARD ZERNIKE. I RANGES FROM 1 TO 37
            TERM= FF3(RHO,THETA,I)
            TERM=TERM*DWW3*DWW4
            CCOL(III)=CCOL(III)+TERM
            JJ=0
            DO J=1,37
!       DO THE APPROPRIATE ROW FOR COLUMN I
               JJ=JJ+1
               TERM= FF3(RHO,THETA,I)*FF3(RHO,THETA,J)
               ACCUM(III,JJ)=ACCUM(III,JJ)+(DWW4*TERM)
            END DO
         END DO
         COUNT=III
!
!       ALL VALUES CALCULATED FOR THE CURRENT "DATA"
!       STATEMENT.
!
         KK=KK+1
         WRITE(UNIT=64,REC=KK) DWW1,DWW2,DWW3,DWW4
      END IF
   END DO
!     KK IS THE NUMBER OF FITTED DATA ITEMS
   WRITE(UNIT=64,REC=1) KK
!
!       THE LARGE ARRAY WITH ACCUMULATED DATA IS
!       ACCUM. THE COLUMN VECTOR WITH FUNCTIONAL DATA
!       IS CCOL.
   MP=96
   NP2=96
   N=COUNT
   M=COUNT
   I=COUNT
   B(1:I)=CCOL(1:I)
   J=COUNT
   U(1:I,1:J)=ACCUM(1:I,1:J)
!       DO SINGULAR VALUE DECOMPOSITION
!
   SM=M
   SSN=N
   CALL SVDCMP(U,SM,SSN,MP,NP2,W,V)
   M=SM
   N=SSN
!
!       SOLVE LINEAR EQUATION
!
   SM=M
   SSN=N
   CALL SVBKSB(U,W,V,SM,SSN,MP,NP2,B,XXX)
   M=SM
   N=SSN
!
!       SIMPLE DATA FIT
   II=0
   DO I=1,37
      II=II+1
      CFTYPE(I)=XXX(II)
      X(I)=CFTYPE(I)
   END DO
   DO I=38,96
      II=II+1
      CFTYPE(I)=0.0D0
      X(I)=CFTYPE(I)
   END DO
   OPMAP=.TRUE.
   DEALLOCATE(U,W,V,XXX,B,STAT=ALLOERR)
   ERROP=.FALSE.
   RETURN
END
