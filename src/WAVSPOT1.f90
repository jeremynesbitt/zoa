      CALL SHOWIT(0)
      WRITE(OUTLYNE,*)&
      &'"A" = ',ZMAX-(9.0D0*DL1),' TO ',ZMAX-(10.0D0*DL1),' WAVES'
      CALL SHOWIT(0)
      WRITE(OUTLYNE,*)&
      &'"B" = ',ZMAX-(10.0D0*DL1),' TO ',ZMAX-(11.0D0*DL1),' WAVES'
      CALL SHOWIT(0)
      WRITE(OUTLYNE,*)&
      &'"C" = ',ZMAX-(11.0D0*DL1),' TO ',ZMAX-(12.0D0*DL1),' WAVES'
      CALL SHOWIT(0)
      WRITE(OUTLYNE,*)&
      &'"D" = ',ZMAX-(12.0D0*DL1),' TO ',ZMAX-(13.0D0*DL1),' WAVES'
