!       FIRST FILE OF CAPFN/SPOT ROUTINES

! SUB OPDIN.FOR
SUBROUTINE OPDIN
!
   use DATSP1
   use DATSPD
   use DATLEN
   use DATMAI
   use mod_system, only: sys_units
   use command_utils, only: is_command_query
   IMPLICIT NONE
!
!     THIS ROUTINE INPUTS THE COMPEX APERTURE FUNCTION FROM CAPFNOUT.DAT
!     THEN IT CALCULATES THE PTOV AND RMS OPD
!
!
   INTEGER I,J,WWVN,K,ALLOERR,MEANCNT,CAPLEN,INUMPTS,IVAL3,IVAL4
!
   REAL*8 DS,REFHT,SPACEYY,SYSWV,PIT,PEAK,MEAN
!
   REAL*8 NUMPTS,CAPFNIN,&
   &WV,VAL1,VAL2,PTVOPD,MEAN2,ARG,VAL5
!
   DIMENSION CAPFNIN(:,:,:)
!
   ALLOCATABLE CAPFNIN
!
   LOGICAL EXIS73
   LOGICAL EXIS43
!
   CHARACTER CAPOUT*80,BCAPOUT*80
!
   SAVE CAPFNIN
!
   LOGICAL PLOTIT(1:10)
   COMMON/ITPLOT/PLOTIT
!
!
!
   IF(is_command_query()) THEN
      IF(WC.EQ.'CAPFNIN') OUTLYNE=&
      &'"CAPFNIN" INPUTS A CAPFN FROM AN ASCII FILE'
      IF(WC.EQ.'CAPFNIN') OUTLYNE=&
      &'"CAPFNCLR" DEALLOCATES THE CAPFNIN/CAPFNADD ARRAYS'
      IF(WC.EQ.'CAPFNADD') OUTLYNE=&
      &'"CAPFNADD" INPUTS AND ADDS A CAPFN FROM AN ASCII FILE'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(WC.EQ.'CAPFNCLR') THEN
      IF(SQ.EQ.1.OR.SN.EQ.1.OR.SST.EQ.1) THEN
         WRITE(OUTLYNE,*) WC,&
         &' TAKES NO ADDITIONAL INPUT'
         CALL SHOWIT(1)
         CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
         RETURN
      END IF
      DEALLOCATE (CAPFNIN,STAT=ALLOERR)
      CPFNEXT=.FALSE.
      PLOTIT(1)=.FALSE.
      PLOTIT(2)=.FALSE.
      PLOTIT(3)=.FALSE.
      PLOTIT(4)=.FALSE.
      PLOTIT(5)=.FALSE.
      PLOTIT(6)=.FALSE.
      PLOTIT(7)=.FALSE.
      PLOTIT(8)=.FALSE.
      PLOTIT(9)=.FALSE.
      PLOTIT(10)=.FALSE.
      ALLO=.FALSE.
      SUMMOR=.FALSE.
      ALLONUM=0
      RETURN
   END IF
   IF(SQ.EQ.1.OR.SN.EQ.1) THEN
      WRITE(OUTLYNE,*) WC,&
      &' TAKES NO NUMERIC OR QUALIFIER INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
   CAPOUT=TRIM(WS)//'.DAT'
   BCAPOUT=TRIM(WS)//'.BIN'
   IF(SST.EQ.0) CAPOUT='CAPFNOUT.DAT'
   IF(SST.EQ.0) BCAPOUT='CAPFNOUT.BIN'
   IF(WC.EQ.'CAPFNIN') CPFNEXT=.FALSE.
   IF(WC.EQ.'CAPFNIN') THEN
      DEALLOCATE (CAPFNIN,STAT=ALLOERR)
      ALLO=.FALSE.
      SUMMOR=.FALSE.
      ALLONUM=0
   END IF
   INQUIRE(FILE=CAPOUT,EXIST=EXIS73)
   INQUIRE(FILE=BCAPOUT,EXIST=EXIS43)
   IF(EXIS73) CPFNEXT=.TRUE.
   IF(.NOT.EXIS73) THEN
      OUTLYNE=&
      &'THE REQUESTED ASCII CAPFN FILE DOES NOT EXIST TO INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('NO ACTION TAKEN', 1)
      RETURN
   END IF
!
!     OPEN FILE
!
   OPEN(UNIT=73,FILE=CAPOUT,&
   &FORM='FORMATTED',STATUS='UNKNOWN')
   OPEN(UNIT=43,ACCESS='DIRECT',FILE=BCAPOUT,&
   &FORM='UNFORMATTED',RECL=(100*NRECL),STATUS='UNKNOWN')
   REWIND(UNIT=73)
!
   READ(73,*) NUMPTS,SPACEYY
   READ(73,*) REFHT,SYSWV
!
   WV=SYSWV
   IF(sys_units().EQ.1.0D0) THEN
      WV=WV*3.93700787402D-5
   END IF
   IF(sys_units().EQ.2.0D0) THEN
      WV=WV*1.0D-4
   END IF
   IF(sys_units().EQ.3.0D0) THEN
      WV=WV*1.0D-3
   END IF
   IF(sys_units().EQ.4.0D0) THEN
      WV=WV*1.0D-6
   END IF
   IF(WC.EQ.'CAPFNIN') THEN
      DEALLOCATE(CAPFNIN,STAT=ALLOERR)
      ALLOCATE(CAPFNIN(INT(NUMPTS),INT(NUMPTS),2),STAT=ALLOERR)
      ALLO=.TRUE.
      SUMMOR=.TRUE.
      ALLONUM=INT(NUMPTS)
   END IF
!
   IF(WC.EQ.'CAPFNIN') THEN
      J=INT(NUMPTS)
      I=INT(NUMPTS)
      CAPFNIN(1:I,1:J,1:2)=0.0D0
   END IF
!
   IF(WC.EQ.'CAPFNADD'.AND..NOT.ALLO) THEN
      OUTLYNE=&
      &'"CAPFNIN" MUST HAVE BEEN USED BEFORE "CAPFNADD" MAY BE USED'
      CALL SHOWIT(1)
      OUTLYNE='NO ACTION TAKEN'
      CALL SHOWIT(1)
      CALL CLOSE_FILE(73,1)
      CALL CLOSE_FILE(43,1)
      CALL MACFAL
      RETURN
   END IF
   IF(WC.EQ.'CAPFNADD'.AND.ALLONUM.NE.INT(NUMPTS)) THEN
      OUTLYNE=&
      &'"ADDED CAPFN IS NOT THE SAME DIMENSION AS CAPFN IN STORAGE'
      CALL SHOWIT(1)
      OUTLYNE='NO ACTION TAKEN'
      CALL SHOWIT(1)
      CALL CLOSE_FILE(73,1)
      CALL CLOSE_FILE(43,1)
      CALL MACFAL
      RETURN
   END IF
!
!     READ THE FILE AND ADD THE NON-ZERO ELEMENTS
   DO J=1,INT(NUMPTS)
      DO I=1,INT(NUMPTS)
         READ(73,*) VAL1,VAL2,IVAL3,IVAL4,VAL5
         IF(CAPFNIN(I,J,1).EQ.0.0D0.AND.VAL1.NE.0.0D0) THEN
            CAPFNIN(I,J,1)=CAPFNIN(I,J,1) + VAL1
            CAPFNIN(I,J,2)=CAPFNIN(I,J,2) + VAL2
         END IF
      END DO
   END DO
   INUMPTS=INT(NUMPTS)
!     NEXT REPLACE THE OLD OPD VALUE IN THE LAST READIN CAPFN FILE WITH
!     THE NEW CORRECTED VALUE BY READING IN UNIT 43, CHANGING DSPOT(4)
!     AND WRITING IT OUT AGAIN.
   PEAK=-1.0D+100
   PIT = 1.0D+100
   MEAN=0.0D0
   MEAN2=0.0D0
   RMSOPD=0.0D0
   PTVOPD=0.0D0
   MEANCNT=0
   DO J=1,INT(NUMPTS)
      DO I=1,INT(NUMPTS)
         IF(CAPFNIN(I,J,1).NE.0.0D0) THEN
            IF(CAPFNIN(I,J,2).GE.PEAK) PEAK=CAPFNIN(I,J,2)
            IF(CAPFNIN(I,J,2).LE.PIT)  PIT =CAPFNIN(I,J,2)
            MEAN=MEAN+((CAPFNIN(I,J,2))/(TWOPII))
            MEAN2=MEAN2+(((CAPFNIN(I,J,2))/(TWOPII))**2)
            MEANCNT=MEANCNT+1
         END IF
      END DO
   END DO
   PTVOPD=(PEAK-PIT)/(TWOPII)
   PTOVOPD(0)=PTVOPD
   PTOVOPD(1)=PTVOPD
   PTOVOPD(2)=PTVOPD
   PTOVOPD(3)=PTVOPD
   PTOVOPD(4)=PTVOPD
   PTOVOPD(5)=PTVOPD
   PTOVOPD(6)=PTVOPD
   PTOVOPD(7)=PTVOPD
   PTOVOPD(8)=PTVOPD
   PTOVOPD(9)=PTVOPD
   PTOVOPD(10)=PTVOPD

   ARG=((MEAN2-((MEAN**2)/&
   &DBLE(MEANCNT)))/DBLE(MEANCNT-1))
   IF(ARG.LT.0.0D0) THEN
      OUTLYNE=&
      &'RMSOPD ERROR OCCURED, CAPFNIN RMSOPD CALCULATION HAS FAILED'
      CALL SHOWIT(1)
      CALL MACFAL
      CALL CLOSE_FILE(73,1)
      CALL CLOSE_FILE(43,1)
      RETURN
   END IF
   RMSOPD=DSQRT(ARG)
   RMSOP(1)=DSQRT(ARG)
   RMSOP(2)=DSQRT(ARG)
   RMSOP(3)=DSQRT(ARG)
   RMSOP(4)=DSQRT(ARG)
   RMSOP(5)=DSQRT(ARG)
   RMSOP(6)=DSQRT(ARG)
   RMSOP(7)=DSQRT(ARG)
   RMSOP(8)=DSQRT(ARG)
   RMSOP(9)=DSQRT(ARG)
   RMSOP(10)=DSQRT(ARG)

!
   IF(WC.EQ.'CAPFNIN'.OR.WC.EQ.'CAPFNADD') THEN
!     SAVE THE SUMMED CAPFN
      ITOT=(INT(NUMPTS)**2)+1
      K=1
      DO J=2,ITOT
         READ(UNIT=43,REC=J) DSPOT(1),DSPOT(2),DSPOT(3),DSPOT(4)&
         &,DSPOT(5),DSPOT(6),DSPOT(7),DSPOT(8),DSPOT(9),DSPOT(10),&
         &DSPOT(11),DSPOT(12),DSPOT(13),DSPOT(14),DSPOT(15),DSPOT(16)&
         &,DSPOT(17)&
         &,DSPOT(18),DSPOT(19),DSPOT(20),DSPOT(21),DSPOT(22),DSPOT(23)&
         &,DSPOT(24)&
         &,DSPOT(25),DSPOT(26),DSPOT(27),DSPOT(28),DSPOT(29)&
         &,DSPOT(30),DSPOT(31),DSPOT(32),DSPOT(33),DSPOT(34),DSPOT(35)&
         &,DSPOT(36),DSPOT(37),DSPOT(38),DSPOT(39),DSPOT(40),DSPOT(41)&
         &,DSPOT(42),DSPOT(43),DSPOT(44),DSPOT(45),DSPOT(46),DSPOT(47)&
         &,DSPOT(48),DSPOT(49),DSPOT(50)
         IF(DSPOT(12).NE.0.0D0.AND.WC.EQ.'CAPFNADD'.OR.&
         &WC.EQ.'CAPFNIN') THEN
!     LOAD DSPOT(*) INTO DSPOTT(*,ID)
            ID=J-1
            CALL SPOTIT(3)
         END IF
      END DO
      NUMCOL=1
      READ(UNIT=43,REC=ITOT+1) WVSHORT,SHORT,NUMCOL,LFOB(1),LFOB(2)&
      &,LFOB(3),LFOB(4),LFOB(5),LFOB(6),LFOB(7),IW,DDEELL
      CALL CLOSE_FILE(43,1)
      CPFNEXT=.TRUE.
      PLOTIT(1)=.TRUE.
      PLOTIT(2)=.FALSE.
      PLOTIT(3)=.FALSE.
      PLOTIT(4)=.FALSE.
      PLOTIT(5)=.FALSE.
      PLOTIT(6)=.FALSE.
      PLOTIT(7)=.FALSE.
      PLOTIT(8)=.FALSE.
      PLOTIT(9)=.FALSE.
      PLOTIT(10)=.FALSE.
   END IF
!
   CALL CLOSE_FILE(73,1)
   CALL CLOSE_FILE(43,1)
   RETURN
END
! SUB CAPGRID.FOR
SUBROUTINE CAPGRID
!
   use DATSP1
   use DATSPD
   use DATLEN
   use DATMAI
   use mod_system, only: sys_units, sys_wavelength
   use command_utils, only: is_command_query
   IMPLICIT NONE
!
!     THIS ROUTINE OUTPUTS THE COMPEX APERTURE FUNCTION AS A PHASE
!     AND AN APODIZATION GRID FILE FOR LATER INPUT
!
   INTEGER I,J,K,WWVN,NUMPTS,WVN1
!
   CHARACTER UNIS*6
!
   LOGICAL READOK
!
   REAL*8 SPACING,WAV,MAXX,MAXY,MINX,MINY
!
!
   IF(is_command_query()) THEN
      OUTLYNE=&
      &'"CAPGRID" OUTPUTS THE CURRENT CAPFN TO AN ASCII FILES'
      CALL SHOWIT(1)
      OUTLYNE=&
      &'"GRIDAPD" AND "GRIDOPD'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(SST.EQ.1.OR.SQ.EQ.1) THEN
      OUTLYNE=&
      &'"CAPGRID" TAKES NO QUALIFIER OR STRING INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
      OUTLYNE=&
      &'"CAPGRID" TAKES NO NUMERIC WORD #2 THROUGH #5 INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(S1.EQ.0) THEN
      OUTLYNE=&
      &'"CAPGRID" REQUIRES EXPLICIT NUMERIC WORD #1 INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(.NOT.CPFNEXT) THEN
      OUTLYNE=&
      &'NO CAPFN EXISTS'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('NO ACTION TAKEN', 1)
      RETURN
   END IF
!     DEFAULT WAVELENGTH IS CONTROL WAVELENGTH
   IF(INT(W1).LT.1.OR.INT(W1).GT.10) THEN
      OUTLYNE=&
      &'NUMERIC WORD #1 MUST BE 1, 2, 3, 4, 5, 6, 7, 8, 9 OR 10'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
!
   OPEN(UNIT=45,FILE='GRIDAPD.DAT',&
   &FORM='FORMATTED',STATUS='UNKNOWN')
   CALL CLOSE_FILE(45,0)
   OPEN(UNIT=46,FILE='GRIDOPD.DAT',&
   &FORM='FORMATTED',STATUS='UNKNOWN')
   CALL CLOSE_FILE(46,0)
!
!     OPEN GRIDAPD AND GRIDOPD.DAT
!
   OPEN(UNIT=45,FILE='GRIDAPD.DAT',&
   &FORM='FORMATTED',STATUS='UNKNOWN')
   OPEN(UNIT=46,FILE='GRIDOPD.DAT',&
   &FORM='FORMATTED',STATUS='UNKNOWN')
!
   WVN1=INT(LFOB(4))
   WAV=sys_wavelength(WVN1)
   IF(sys_units().EQ.1.0D0) WAV=WAV*&
   &((1.0D-3)/(25.4D0))
   IF(sys_units().EQ.2.0D0) WAV=WAV*(1.0D-4)
   IF(sys_units().EQ.3.0D0) WAV=WAV*(1.0D-3)
   IF(sys_units().EQ.4.0D0) WAV=WAV*(1.0D-6)
   READOK=.FALSE.
   NUMPTS=INT((DSQRT((DBLE(ITOT-1)/DBLE(NUMCOL)))))
   MAXX=-1E30
   MAXY=-1E30
   MINX=1E30
   MINY=1E30
   K=1
   DO J=1,NUMPTS
      DO I=1,NUMPTS
         K=K+1
!     LOAD DSPOT(*) WITH DSPOTT(*,ID)
         ID=K-1
         CALL SPOTIT(4)
         IF(DSPOT(16).EQ.W1) THEN
            IF(DSPOT(5).GE.MAXX) MAXX=DSPOT(5)
            IF(DSPOT(6).GE.MAXY) MAXY=DSPOT(6)
            IF(DSPOT(5).LE.MINX) MINX=DSPOT(5)
            IF(DSPOT(6).LE.MINY) MINY=DSPOT(6)
            WRITE(45,15) I,J,DSPOT(12)
            WRITE(46,15) I,J,(DSPOT(4)/(TWOPII))*WAV
            READOK=.TRUE.
         END IF
      END DO
   END DO
15 FORMAT(I6,',',I6,',',D23.15)
!
   IF(READOK) THEN
      CALL CLOSE_FILE(45,1)
      CALL CLOSE_FILE(46,1)
      WRITE(OUTLYNE,*)&
      &'CAPFN DATA FOR WAVELENGTH NUMBER ',INT(W1)
      CALL SHOWIT(1)
      OUTLYNE='EXISTS. "GRIDAPD.DAT" AND "GRIDOPD.DAT" WERE WRITTEN'
      CALL SHOWIT(1)
      IF((MAXX-MINX).GE.(MAXY-MINY)) THEN
         SPACING=(MAXX-MINX)/(DBLE(NUMPTS)-1.0D0)
      ELSE
         SPACING=(MAXY-MINY)/(DBLE(NUMPTS)-1.0D0)
      END IF
      IF(sys_units().EQ.1.0D0) UNIS='INCHES'
      IF(sys_units().EQ.2.0D0) UNIS='CM    '
      IF(sys_units().EQ.3.0D0) UNIS='MM    '
      IF(sys_units().EQ.4.0D0) UNIS='METERS'
      REG(40)=REG(9)
      REG(9)=SPACING
      WRITE(OUTLYNE,20) SPACING,UNIS
      CALL SHOWIT(0)
20    FORMAT(&
      &'GRID SPACING AT THE CURRENT REFERENCE SURFACE = ',G13.6,&
      &1X,A6)
   ELSE
      CALL CLOSE_FILE(44,1)
      CALL CLOSE_FILE(43,1)
      WRITE(OUTLYNE,*)&
      &'CAPFN DATA FOR WAVELENGTH NUMBER ',INT(W1)
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL(&
      & 'DOES NOT EXIST. NO ASCII CAPFN FILE WAS WRITTEN'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
   END IF
   RETURN
END
! SUB OPDOUT.FOR
SUBROUTINE OPDOUT
!
   use DATSP1
   use DATSPD
   use DATLEN
   use DATMAI
   use mod_surface, only: surf_clap_type, surf_clap_dim, surf_array_parity
   use command_utils, only: is_command_query
   IMPLICIT NONE
!
!     THIS ROUTINE OUTPUTS THE COMPEX APERTURE FUNCTION TO AN ASCII FILE
!
!
   INTEGER I,KK,G,WWVN,CAPLEN
!
   REAL*8 NUMPTS,REFHT,SPACEYY
!
   CHARACTER CAPOUT*80,BCAPOUT*80
!
!
!
   IF(is_command_query()) THEN
      OUTLYNE=&
      &'"CAPFNOUT" OUTPUTS THE CURRENT CAPFN TO AN ASCII FILE'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(SQ.EQ.1.OR.SN.EQ.1) THEN
      OUTLYNE=&
      &'"CAPFNOUT" TAKES NO STRING OR QUALIFIER INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
   CAPOUT=TRIM(WS)//'.DAT'
   BCAPOUT=TRIM(WS)//'.BIN'
   IF(WS.EQ.'        ')  CAPOUT='CAPFNOUT.DAT'
   IF(WS.EQ.'        ') BCAPOUT='CAPFNOUT.BIN'
   IF(.NOT.CPFNEXT) THEN
      OUTLYNE=&
      &'NO CAPFN EXISTS'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('NO ACTION TAKEN', 1)
      RETURN
   END IF
! WINRER
!        CALL IOSDELETEFILE(CAPOUT)
!        CALL IOSDELETEFILE(BCAPOUT)
!
!     OPEN CAPFNOUT.DAT
!
   OPEN(UNIT=73,FILE=CAPOUT,&
   &FORM='FORMATTED',STATUS='UNKNOWN')
   OPEN(UNIT=43,ACCESS='DIRECT',FILE=BCAPOUT,&
   &FORM='UNFORMATTED',RECL=(100*NRECL),STATUS='UNKNOWN')
!
!     DETERMINE THE REFERNCE APERTURE HEIGHT
!     OF THE REF SURFACE COORDINATES
   IF(surf_clap_type(NEWREF) >= 1.AND.surf_clap_type(NEWREF) <= 5.AND.surf_array_parity(NEWREF) == 0) THEN
      IF(surf_clap_type(NEWREF) == 1) THEN
         IF(surf_clap_dim(NEWREF, 1).LE.surf_clap_dim(NEWREF, 2)) THEN
            REFHT=surf_clap_dim(NEWREF, 1)
         ELSE
            REFHT=surf_clap_dim(NEWREF, 2)
         END IF
      END IF
!        RECT CLAP
!
      IF(surf_clap_type(NEWREF) == 2) THEN
         REFHT=DSQRT((surf_clap_dim(NEWREF, 1)**2)+(surf_clap_dim(NEWREF, 2)**2))
      END IF
!        ELIP CLAP
!
      IF(surf_clap_type(NEWREF) == 3) THEN
         IF(surf_clap_dim(NEWREF, 1).GE.surf_clap_dim(NEWREF, 2)) THEN
            REFHT=surf_clap_dim(NEWREF, 1)
         ELSE
            REFHT=surf_clap_dim(NEWREF, 2)
         END IF
      END IF
!        RCTK CLAP
!
      IF(surf_clap_type(NEWREF) == 4) THEN
         REFHT=DSQRT((surf_clap_dim(NEWREF, 1)**2)+(surf_clap_dim(NEWREF, 2)**2))
      END IF
!        POLY CLAP
!
      IF(surf_clap_type(NEWREF) == 5) THEN
         REFHT=DABS(surf_clap_dim(NEWREF, 1))
      END IF
!
!       IPOLY CLAP
!
      IF(surf_clap_type(NEWREF) == 6) THEN
         REFHT=DABS(surf_clap_dim(NEWREF, 5))
      END IF
!
   ELSE
!       NO CLAP ON REF SURF.
      IF(PXTRAY(1,NEWREF).GE.PXTRAX(1,NEWREF)) THEN
         REFHT=PXTRAY(1,NEWREF)
      ELSE
         REFHT=PXTRAX(1,NEWREF)
      END IF
   END IF
!
   KK=1
   WRITE(UNIT=43,REC=1) ITOT
   G=ITOT+1
   WRITE(UNIT=43,REC=G) WVSHORT,SHORT,NUMCOL,LFOB(1),LFOB(2)&
   &,LFOB(3),LFOB(4),LFOB(5),LFOB(6),LFOB(7),IW,DDEELL
!
   WVN=INT(LFOB(4))
   IF(WVN.EQ.1) WWVN=1
   IF(WVN.EQ.2) WWVN=2
   IF(WVN.EQ.3) WWVN=3
   IF(WVN.EQ.4) WWVN=4
   IF(WVN.EQ.5) WWVN=5
   IF(WVN.EQ.6) WWVN=71
   IF(WVN.EQ.7) WWVN=72
   IF(WVN.EQ.8) WWVN=73
   IF(WVN.EQ.9) WWVN=74
   IF(WVN.EQ.10) WWVN=75
   REFHT=REFHT*DDEELL
   NUMPTS=(DSQRT((DBLE(ITOT-1)/DBLE(NUMCOL))))
   SPACEYY=REFHT/(NUMPTS-1.0D0)
   REWIND(UNIT=73)
   WRITE(73,15) NUMPTS,SPACEYY
   DO I=2,ITOT
!     LOAD DSPOT(*) WITH DSPOTT(*,ID)
      ID=I-1
      CALL SPOTIT(4)
      WRITE(UNIT=43,REC=I) DSPOT(1),DSPOT(2),DSPOT(3),DSPOT(4)&
      &,DSPOT(5),DSPOT(6),DSPOT(7),DSPOT(8),DSPOT(9),DSPOT(10),&
      &DSPOT(11),DSPOT(12),DSPOT(13),DSPOT(14),DSPOT(15),DSPOT(16)&
      &,DSPOT(17)&
      &,DSPOT(18),DSPOT(19),DSPOT(20),DSPOT(21),DSPOT(22),DSPOT(23)&
      &,DSPOT(24)&
      &,DSPOT(25),DSPOT(26),DSPOT(27),DSPOT(28),DSPOT(29)&
      &,DSPOT(30),DSPOT(31),DSPOT(32),DSPOT(33),DSPOT(34),DSPOT(35)&
      &,DSPOT(36),DSPOT(37),DSPOT(38),DSPOT(39),DSPOT(40),DSPOT(41)&
      &,DSPOT(42),DSPOT(43),DSPOT(44),DSPOT(45),DSPOT(46),DSPOT(47)&
      &,DSPOT(48),DSPOT(49),DSPOT(50)
      WRITE(73,19) DSPOT(12),DSPOT(4),INT(DSPOT(7))&
      &,INT(DSPOT(8)),DSPOT(35)
   END DO
   WRITE(73,17) REFRY(1,NEWOBJ),REFRY(2,NEWOBJ),REFRY(3,NEWOBJ)
   WRITE(73,17) REFRY(4,NEWOBJ),REFRY(5,NEWOBJ),REFRY(6,NEWOBJ)
   WRITE(73,18) REFRY(25,NEWOBJ)
15 FORMAT(D23.15,',',D23.15)
16 FORMAT(D23.15,',',D23.15,',',I3,',',I3)
17 FORMAT(D23.15,',',D23.15,',',D23.15)
18 FORMAT(D23.15)
19 FORMAT(D23.15,',',D23.15,',',I3,',',I3,',',D23.15)
!
   CALL CLOSE_FILE(73,1)
   CALL CLOSE_FILE(43,1)
   RETURN
END
! SUB WAMAP.FOR
SUBROUTINE WAMAP
!
   use DATSP1
   use DATSPD
   use DATLEN
   use DATMAI
   use mod_surface, only: surf_clap_type, surf_clap_dim, surf_array_parity
   use mod_system, only: sys_wl_ref
   use command_utils, only: is_command_query
   IMPLICIT NONE
!
   INTEGER KKK,KVAL,I,KKV,ALLOERR
!
   REAL*8 F,X,Y
   DIMENSION F(:,:),X(:),Y(:)
   ALLOCATABLE :: F,X,Y
!
   REAL*8 REFHT,WVAL
!
!
   IF(is_command_query()) THEN
      OUTLYNE=&
      &'"WAMAP" GENERATES A WAVEFRONT MAP USING CAPFN DATA'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(SQ.EQ.1.OR.SST.EQ.1) THEN
      OUTLYNE=&
      &'"WAMAP" TAKES NO QUALIFIER OR ALPHANUMERIC INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
!
!
   IF(S3.EQ.1.OR.S2.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
      OUTLYNE=&
      &'"WAMAP" ONLY TAKES NUMERIC WORD #1 INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
!
!     DEFAULT WAVELENGTH IS CONTROL WAVELENGTH
   IF(DF1.EQ.1) W1=sys_wl_ref()
   IF(INT(W1).LT.1.OR.INT(W1).GT.10) THEN
      OUTLYNE=&
      &'NUMERIC WORD #1 MUST BE 1, 2, 3, 4, 5, 6, 7, 8, 9 OR 10'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(.NOT.CPFNEXT) THEN
      OUTLYNE=&
      &'NO CAPFN EXISTS'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('NO ACTION TAKEN', 1)
      RETURN
   END IF
!
!     DETERMINE THE REFERNCE APERTURE HEIGHT
!     OF THE REF SURFACE COORDINATES
   IF(surf_clap_type(NEWREF) >= 1.AND.surf_clap_type(NEWREF) <= 5.AND.surf_array_parity(NEWREF) == 0) THEN
      IF(surf_clap_type(NEWREF) == 1) THEN
         IF(surf_clap_dim(NEWREF, 1).LE.surf_clap_dim(NEWREF, 2)) THEN
            REFHT=surf_clap_dim(NEWREF, 1)
         ELSE
            REFHT=surf_clap_dim(NEWREF, 2)
         END IF
      END IF
!        RECT CLAP
!
      IF(surf_clap_type(NEWREF) == 2) THEN
         REFHT=DSQRT((surf_clap_dim(NEWREF, 1)**2)+(surf_clap_dim(NEWREF, 2)**2))
      END IF
!        ELIP CLAP
!
      IF(surf_clap_type(NEWREF) == 3) THEN
         IF(surf_clap_dim(NEWREF, 1).GE.surf_clap_dim(NEWREF, 2)) THEN
            REFHT=surf_clap_dim(NEWREF, 1)
         ELSE
            REFHT=surf_clap_dim(NEWREF, 2)
         END IF
      END IF
!        RCTK CLAP
!
      IF(surf_clap_type(NEWREF) == 4) THEN
         REFHT=DSQRT((surf_clap_dim(NEWREF, 1)**2)+(surf_clap_dim(NEWREF, 2)**2))
      END IF
!        POLY CLAP
!
      IF(surf_clap_type(NEWREF) == 5) THEN
         REFHT=DABS(surf_clap_dim(NEWREF, 1))
      END IF
!        IPOLY CLAP
!
      IF(surf_clap_type(NEWREF) == 6) THEN
         REFHT=DABS(surf_clap_dim(NEWREF, 5))
      END IF
!
   ELSE
!       NO CLAP ON REF SURF.
      IF(PXTRAY(1,NEWREF).GE.PXTRAX(1,NEWREF)) THEN
         REFHT=(PXTRAY(1,NEWREF))
      ELSE
         REFHT=(PXTRAX(1,NEWREF))
      END IF
   END IF
!
   WVAL=(W1)
   KKV=(ITOT-1)/NUMCOL
!     KKV IS THE TOTAL NUMBER OF POINTS AT THE DESIRED COLOR
!     AND IT IS AN EXACT SQUARE
   DO I=2,ITOT
!     LOAD DSPOT(*) WITH DSPOTT(*,ID)
      ID=I-1
      CALL SPOTIT(4)
!
      IF((DSPOT(16)).EQ.WVAL) THEN
         KVAL=I
         GO TO 10
      END IF
!
   END DO
10 CONTINUE
!
   IF(WVAL.GT.NUMCOL) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & 'CAPFN DATA FOR THE WAVELENGTH VALUE (NUMERIC WORD 1)'//'\n'//&
      & 'DOES NOT EXIST'//'\n'//&
      & 'NO ACTION TAKEN', 1)
      RETURN
   END IF
   KKK=INT(SQRT(REAL(KKV)))
   DEALLOCATE(F,X,Y,STAT=ALLOERR)
   ALLOCATE(F(KKK,KKK),X(KKK),Y(KKK),STAT=ALLOERR)
!
   CALL WAPPLOT(KVAL,REFHT,WVAL,KKV,F,X,Y,KKK)
   DEALLOCATE(F,X,Y,STAT=ALLOERR)
!
   RETURN
END
