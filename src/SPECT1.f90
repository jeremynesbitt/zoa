! SUB BLCKBD.FOR
SUBROUTINE BLCKBD
!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDELS THE "BLACKBDY" SPECT COMMAND.
!       BLACK BODY RADIANT SPECTRAL EMMITTANCE OR
!       BLACK BODY RADIANT SPECTRAL PHOTON EMMITTANCE VALUES
!       ARE CALCULATED FOR THE VAVELENGTH VALUES IN THE CUMULATIVE
!       AREA AND THEN MULTIPLIED INTO THE FUNCTIONAL
!       AREA IN CUMULATIVE.
!
!                       DEFINE VARIABLES
!
   INTEGER I
!
   real(real64) CUMULT(1:1001,1:3),LAMB1,LAMB2,VAL,&
   &TEST
!
   INTEGER NTOTAL
!
   COMMON/CUM/CUMULT
!
   COMMON/WAVEL/LAMB1,LAMB2,NTOTAL
!
!
   IF(WQ.EQ.'SRE') THEN
      DO 10 I=1,NTOTAL
         IF(CUMULT(I,2).EQ.0.0D0) THEN
            CUMULT(I,3)=0.0D0
            GO TO 10
         ELSE
         END IF
!       CALCULATE BB VALUE
         TEST=1.43879D4/(CUMULT(I,2)*W1)
         IF(TEST.GE.10.0D0) THEN
            CUMULT(I,3)=CUMULT(I,3)*(DEXP(-TEST))*(3.7415D4)
            CUMULT(I,3)=CUMULT(I,3)/CUMULT(I,2)
            CUMULT(I,3)=CUMULT(I,3)/CUMULT(I,2)
            CUMULT(I,3)=CUMULT(I,3)/CUMULT(I,2)
            CUMULT(I,3)=CUMULT(I,3)/CUMULT(I,2)
            CUMULT(I,3)=CUMULT(I,3)/CUMULT(I,2)
         ELSE
            CUMULT(I,3)=CUMULT(I,3)*(3.7415D4/(CUMULT(I,2)**5))*&
            &(1.0D0/(DEXP(1.43879D4/(CUMULT(I,2)*W1))-1.0D0))
         END IF

10    CONTINUE
!       CUMULATIVE AREA HAS BEEM MULTIPLIED BY THE APPRORIATE
!       BLAKBODY VALUES.
!       PRINT MESSAGE AND RETURN
      WRITE(OUTLYNE,*)'FOR T = ',W1,' DEGREES KELVIN'
      CALL SHOWIT(1)
      OUTLYNE=&
      &'BLACK BODY SPECTRAL RADIANT EMMITTANCE VALUES HAVE BEEN'
      CALL SHOWIT(1)
      OUTLYNE='MULTIPLIED INTO THE CUMULATIVE AREA'
      CALL SHOWIT(1)
      OUTLYNE=&
      &'UNITS ARE: Watt/(cm2-micron)'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(WQ.EQ.'SRPE') THEN
      DO 20 I=1,NTOTAL
         IF(CUMULT(I,2).EQ.0.0D0) THEN
            CUMULT(I,3)=0.0D0
            GO TO 20
         ELSE
         END IF
         TEST=1.43879D4/(CUMULT(I,2)*W1)
         IF(TEST.GE.10.0D0) THEN
            VAL=(DEXP(-TEST))*(1.88356D3)
            VAL=VAL/CUMULT(I,2)
            VAL=VAL/CUMULT(I,2)
            VAL=VAL/CUMULT(I,2)
            VAL=VAL/CUMULT(I,2)
         ELSE
            VAL=(1.88356D3/(CUMULT(I,2)**4))*&
            &(1.0D0/(DEXP(1.43879D4/(CUMULT(I,2)*W1))-1.0D0))
         END IF
         CUMULT(I,3)=CUMULT(I,3)*VAL*(1D20)

20    CONTINUE
!       CUMULATIVE AREA HAS BEEM MULTIPLIED BY THE APPRORIATE
!       BLAKBODY VALUES.
!       PRINT MESSAGE AND RETURN
      WRITE(OUTLYNE,*)'FOR T = ',W1,' DEGREES KELVIN'
      CALL SHOWIT(1)
      OUTLYNE=&
      &'BLACK BODY SPECTRAL RADIANT PHOTON EMMITTANCE VALUES HAVE BEEN'
      CALL SHOWIT(1)
      OUTLYNE='MULTIPLIED INTO THE CUMULATIVE AREA'
      CALL SHOWIT(1)
      OUTLYNE=&
      &'UNITS ARE: Photon/(sec-cm2-micron)'
      CALL SHOWIT(1)
