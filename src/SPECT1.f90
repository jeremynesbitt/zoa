      DATA1=0.0D0
      DATA2=0.0D0
      OCC=0
      DDATE='         '
      TTIME='        '
      DDDAT='         '
      TTTIM='        '
      NM='        '
   END IF
!
!       FILE THE FILE IN THE APPROPRIATE SPOT
!       OPEN AND WRITE TO FILE
!       ***************************************************************
   EXISJK=.FALSE.
   INQUIRE(FILE=LIBTRA//'TRA.DAT',EXIST=EXISJK)
   IF(.NOT.EXISJK) THEN
      WRITE(OUTLYNE,*)'"TABLE FILES DO NOT YET EXIST'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'TO INITIALIZE IT, USE "ITF" AND "PROCEED"'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       ***************************************************************
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//'TRA.DAT',&
   &FORM='UNFORMATTED',RECL=(50*NRECL),STATUS='UNKNOWN')
   WRITE(UNIT=35,REC=NF) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
   CALL CLOSE_FILE(35,1)
   IF(OCC.EQ.0) THEN
      OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
      &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
      CALL CLOSE_FILE(35,0)
   END IF
!
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
   &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
   F19=1
   DO 220 J=1,POINTS
      WRITE(UNIT=35,REC=J) TABLE(J,1),TABLE(J,2),TABLE(J,3)
220 CONTINUE
   CALL CLOSE_FILE(35,1)
!       PRINT MESSAGE TO THAT EFFECT
   WRITE(OUTLYNE,*)'ENTRY # ',INT(W1),' DELETED FROM FILE ',WQ
   CALL SHOWIT(1)
   IF(OCC.EQ.0) THEN
      WRITE(OUTLYNE,*)'FILE ',WQ,' IS NOW EMPTY'
      CALL SHOWIT(1)
      F19=0
   END IF
!
   RETURN
!
END
! SUB DELETE.FOR
SUBROUTINE DELETE
!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDELS DELETING A SPECT DISK FILE
!
!                       DEFINE VARIABLES
!
   INTEGER NF,I
!
   CHARACTER NM*8,FN*10,DDATE*10,TTIME*8,DDDAT*10,TTTIM*8
!
   LOGICAL EXISJK
!
   INTEGER POINTS,OCC
!
   real(real64) DATA1,DATA2
!
   COMMON/STRNGR/DDDAT,TTTIM,NM,FN
!
!
!       TABLE DATA IN BINARY FORMAT. TRA.DAT IS UNIT 35
!
!       DATA STORED IN TRA.DAT IS:
!               FILE NAME (TNAME) CHARACTER *8
!               DATA1 STARTING WAVELENGTH (DOUBLE PRES.)
!               DATA2 ENDIG WAVLENGTH (DOUBLE PRES.)
!               POINTS - NUMBER OF DATA POINTS (INTEGER)
!               OCC- OCCUPANCY FLAG, 0=EMPTY, 1- FULL (INTEGER)
!               DDATE-DATE FILED
!               TTIME=TIME FILED
!       IF DF1=0, SPECIFIC NUMERIC FILE NUMBER TO BE DELETED
!
   IF(DF1.EQ.0) THEN
      WQ='        '
      SQ=1
      IF(W1.LE.0.0.OR.W1.GT.999.0) THEN
         WRITE(OUTLYNE,*)&
         &'FILE # REQUESTED FOR DELETION IS BEYOND DEFINED'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'FILE NUMBER RANGE. VALID RANGE IS 1 TO 999.'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
   ELSE
!       DF1=1, LOOK FOR A FILE BY NAME
      GO TO 100
   END IF
!       NOW TRY TO DELETE FILE INT(W1)
!
!       ***************************************************************
   EXISJK=.FALSE.
   INQUIRE(FILE=LIBTRA//'TRA.DAT',EXIST=EXISJK)
   IF(.NOT.EXISJK) THEN
      WRITE(OUTLYNE,*)'"TABLE FILES DO NOT YET EXIST'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'TO INITIALIZE IT, USE "ITF" AND "PROCEED"'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       ***************************************************************
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//'TRA.DAT',&
   &FORM='UNFORMATTED',RECL=(50*NRECL),STATUS='UNKNOWN')
!
   I=INT(W1)
   READ(UNIT=35,REC=I) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
   TTTIM=TTIME
   DDDAT=DDATE
   IF(OCC.EQ.0) THEN
!       FILE ALREADY EMPTY
      WRITE(OUTLYNE,*)'FILE # ',INT(W1),'DOES NOT EXIST'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   ELSE
!       FILE TO BE DELETED
      NM='        '
      DATA1=0.0D0
      DATA2=0.0D0
      POINTS=0
      OCC=0
      DDATE='         '
      TTIME='        '
      DDDAT='         '
      TTTIM='        '
      WRITE(UNIT=35,REC=I)NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
      CALL CLOSE_FILE(35,1)
      NF=I
      CALL TRAFIL(NF,FN)
      OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
      &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
      CALL CLOSE_FILE(35,0)
      WRITE(OUTLYNE,*)'FILE # ',INT(W1),' DELETED'
      CALL SHOWIT(1)
   END IF
   RETURN
!
100 CONTINUE
!
!       FILE FILE NAMED WQ
!
!       ***************************************************************
   EXISJK=.FALSE.
   INQUIRE(FILE=LIBTRA//'TRA.DAT',EXIST=EXISJK)
   IF(.NOT.EXISJK) THEN
      WRITE(OUTLYNE,*)'"TABLE FILES DO NOT YET EXIST'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'TO INITIALIZE IT, USE "ITF" AND "PROCEED"'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       ***************************************************************
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//'TRA.DAT',&
   &FORM='UNFORMATTED',RECL=(50*NRECL),STATUS='UNKNOWN')
!
   DO 200 I=1,999
!
      READ(UNIT=35,REC=I) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
      DDDAT=DDATE
      TTTIM=TTIME
!
      IF(NM.EQ.WQ) THEN
!       FOUND FILE TO DELETE
         NM='        '
         DATA1=0.0D0
         DATA2=0.0D0
         POINTS=0
         OCC=0
         DDATE='         '
         TTIME='        '
         DDDAT='         '
         TTTIM='        '
         WRITE(UNIT=35,REC=I)NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
         CALL CLOSE_FILE(35,1)
         NF=I
         CALL TRAFIL(NF,FN)
         OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
         &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
         CALL CLOSE_FILE(35,0)
         WRITE(OUTLYNE,*)'FILE NAMED ',WQ,' DELETED'
         CALL SHOWIT(1)
         RETURN
      END IF
200 CONTINUE
!       NEVER FOUND FILE
   WRITE(OUTLYNE,*)'FILE NAMED ',WQ,'DOES NOT EXIST'
   CALL SHOWIT(1)
   WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
   CALL SHOWIT(1)
   CALL MACFAL
   RETURN
!
END
! SUB GFILE.FOR
SUBROUTINE GFILE
!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDELS THE "GETFILE" SPECT COMMAND.
!
!                       DEFINE VARIABLES
!
   INTEGER I,J,JK,KI
!
   CHARACTER NM*8,FN*10,DDATE*10,TTIME*8,TTTIM*8,DDDAT*10
!
   LOGICAL EXISJK
!
   real(real64) WORK(1:1001,1:3),L1,L2,&
   &DATA1,DATA2,LAMB1,LAMB2,CUMULT(1:1001,1:3),DELLAM,WORKFC,&
   &SLOPE
!
   COMMON/STRNGR/DDDAT,TTTIM,NM,FN
!
   INTEGER OCC,POINTS,NTOTAL,NF
!
   COMMON/CUM/CUMULT
!
   COMMON/WRK/WORK
!
   COMMON/WAVEL/LAMB1,LAMB2,NTOTAL
!
!
!       TABLE DATA IN BINARY FORMAT. TRA.DAT IS UNIT 35
!
!       DATA STORED IN TRA.DAT IS:
!               FILE NAME (TNAME) CHARACTER *8
!               DATA1 STARTING WAVELENGTH (DOUBLE PRES.)
!               DATA2 ENDIG WAVLENGTH (DOUBLE PRES.)
!               POINTS - NUMBER OF DATA POINTS (INTEGER)
!               OCC- OCCUPANCY FLAG, 0=EMPTY, 1- FULL (INTEGER)
!               DDATE-DATE FILED
!               TTIME=TIME FILED
!       FIND THE DESIRED FILE
!       ***************************************************************
   EXISJK=.FALSE.
   INQUIRE(FILE=LIBTRA//'TRA.DAT',EXIST=EXISJK)
   IF(.NOT.EXISJK) THEN
      WRITE(OUTLYNE,*)'"TABLE FILES DO NOT YET EXIST'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'TO INITIALIZE IT, USE "ITF" AND "PROCEED"'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       ***************************************************************
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//'TRA.DAT',&
   &FORM='UNFORMATTED',RECL=(50*NRECL),STATUS='UNKNOWN')
!
   DO 200 I=1,999
      READ(UNIT=35,REC=I) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
      DDDAT=DDATE
      TTTIM=TTIME
      IF(NM.EQ.WQ) THEN
!       FOUND FILE
         GO TO 300
      END IF
200 CONTINUE
!       IF YOU GOT HERE, FILE DID NOT EXIST
   WRITE(OUTLYNE,*)'FILE ',WQ,' DOES NOT EXIST'
   CALL SHOWIT(1)
   WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
   CALL SHOWIT(1)
   CALL CLOSE_FILE(35,1)
   CALL MACFAL
   RETURN
300 CONTINUE
!
!       ESTABLISH NEW WAVELENGTH BOUNDARIES. NO WAVELENGTH
!       VALUE MAY BE LESS THAN 0.0 MICRONS
!
   L1=(LAMB1*W1)+W2
   IF(L1.LT.0.0D0) L1=0.0D0
   L2=(LAMB2*W1)+W2
!
   IF(L1.LT.0.0D0.OR.W2.LT.0.0D0) THEN
      WRITE(OUTLYNE,*)&
      &'A NEGATIVE WAVELENGTH VALUE IS NOT ALLOWED'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(L2.LE.0.0D0.OR.L2.LE.L1) THEN
!       NO WAVELENGTH RANGE EXISTS, NO INTERPOLATION POSSIBLE
      WRITE(OUTLYNE,*)'WAVELENGTH LIMITS ARE NOT REALISTIC'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   ELSE
!       REDEFINE LAMB1 AND LAMB2
      LAMB1=L1
      LAMB2=L2
   END IF
!
!       LOAD FILE INTO WORK ARRAY
!
   NF=I
   CALL TRAFIL(NF,FN)
!
!       OPEN AND READ FROM FILE
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
   &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
!
   DO 210 J=1,POINTS
      READ(UNIT=35,REC=J) WORK(J,1),WORK(J,2),WORK(J,3)
210 CONTINUE
   CALL CLOSE_FILE(35,1)
!
!
!       INTERPOLATE TO THE POINTS DEFINED IN THE CUMULATIVE
!       AREA BY LAMB1,LAMB2 AND NTOTAL.
!
!       THE DIFFERENCE IN WAVELENGTH BETWEEN ENTRIES IN THE NEW
!       DATA WILL BE
!       (LAMB2-LAMB1)/(NTOTAL-1)
!
   DELLAM=(LAMB2-LAMB1)/(DBLE(NTOTAL)-1.0D0)
!
!       SCALE THE CURRENT WORK DATA BY THE REQUESTED
!       TRANSFORMATION.
!
   DO 505 J=1,POINTS
      WORK(J,3)=(((WORK(J,3)*W3)+W4)**INT(DABS(W5)))
505 CONTINUE
   IF(INT(W5).LT.0.0D0) THEN
      DO 506 J=1,POINTS
         IF(WORK(J,3).NE.0.0D0) WORK(J,3)=1.0D0/WORK(J,3)
         IF(WORK(J,3).EQ.0.0D0) WORK(J,3)=0.0D0
506   CONTINUE
   END IF
!
!       THESE NEW FUNCTIONAL VALUES ARE FOR THE NEW WAVELENGTH
!       VALUES :
   DO 500 J=1,POINTS
      WORK(J,2)=(WORK(J,2)*W1)+W2
      IF(WORK(J,2).LT.0.0D0) WORK(J,2)=0.0D0
500 CONTINUE
!
   IF(WORK(POINTS,2).LE.WORK(1,2)) THEN
      WRITE(OUTLYNE,*)&
      &'NEW WAVELENGTHS IN WORK AREA ARE NOT IN ASCENDING ORDER AND'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'"GETFILE" WAS ABORTED.'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
!       THE NEW FUNCTIONAL VALUES ARE SPREAD OVER THE NEW LAMB1 TO
!       LAMB2 RANGE IN CUMULT, WITH SPACING DELLAM.
!
   CUMULT(1,2)=LAMB1
   CUMULT(NTOTAL,2)=LAMB2
   DO 10 J=2,(NTOTAL-1)
      CUMULT(J,2)=CUMULT((J-1),2)+DELLAM
10 CONTINUE
!
!       NOW INTERPOLATE THE WORK AREA AND MULTIPLY INTO THE
!       CUMULATIVE AREA FUNCTIONAL VALUES.
!
   SLOPE=0.0D0
   JK=1
   DO 20 J=1,POINTS-1
!
!       CALCULATE A SLOPE FROM THE WORK DATA
!
      SLOPE=(((WORK((J+1),3))-(WORK(J,3)))/&
      &((WORK((J+1),2))-(WORK(J,2))))
      KI=JK
!
!       RULES FOR INTERPOLATION (EXTRAPOLATION MAY OCCUR
!       AT END POINTS)
!
!       GET A WAVELENGTH VALUE FROM THE CUMULATIVE AREA
!       AS LONG AS THE WAVELENGTH VALUE FROM THE
!       CUMULATIVE AREA IS NOT GREATER THAN THE CURRENT
!       VALUE OF WORK((J+1),2), WE KEEP USING THE CURRENT SLOPE.
!
!       IF THE WAVELENGTH VALUE FROM CUMULATIVE AREA BECOMES
!       GREATER THAN THE CURRENT WORK((J+1),2), CALCULATE A NEW
!       SLOPE UNLESS J+1=POINTS. IF J+1=POINTS, KEEP USING
!       THE CURRENT SLOPE UNTIL THE CUMULATIVE AREA IS FILLED.
!
!       IF THE LAST WAVELENGTH VALUE IN WORK IS REACHED(J=POINTS),
!
      DO 30 I=KI,NTOTAL
         IF(CUMULT(I,2).LE.WORK((J+1),2).AND.(J+1).LE.POINTS &
         &.OR.CUMULT(I,2).GT.WORK((J+1),2).AND.(J+1).GE.POINTS) THEN
            WORKFC=(SLOPE*(CUMULT(I,2)-WORK(J,2)))+WORK(J,3)
            CUMULT(I,3)=CUMULT(I,3)*(WORKFC)
         ELSE
!       WE MUST
!       CALCULATE A NEW SLOPE. THE UNLESS OUT OF POINTS CASE WAS DONE
!       ABOVE.
            JK=I
            GO TO 20
         END IF
30    CONTINUE
!
20 CONTINUE
!
!       CUMULATIVE AREA HAS BEEM MULTIPLIED BY THE TRANSFORMED,
!       INTERPOLATED WORK AREA. PRINT MESSAGE AND RETURN
   WRITE(OUTLYNE,*)&
   &'SPECT FILE ( ',WQ,' ) READ INTO WORK AREA MEMORY'
   CALL SHOWIT(1)
   WRITE(OUTLYNE,*)'TRANSFORMED, INTERPOLATED AND'
   CALL SHOWIT(1)
   WRITE(OUTLYNE,*)&
   &'MULTIPLIED INTO THE CUMULATIVE MEMORY AREA'
   CALL SHOWIT(1)
   F19=1
!
   RETURN
!
END
