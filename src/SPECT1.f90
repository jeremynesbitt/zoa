! SUB SINSERT.FOR
SUBROUTINE SINSERT
!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDELS INSERTING OF A RECORD INTO
!       SPECT DISK FILE WQ
!
   INTEGER III,I,J
!
   CHARACTER DDATE*10,TTIME*8,FN*10,NM*8,TTTIM*8,DDDAT*10
!
   INTEGER POINTS,OCC,CTAB,NF
!
   LOGICAL EXISJK
!
   real(real64) DATA1,DATA2,TABLE(1:1001,1:3),&
   &LAMB1,LAMB2
!
   COMMON/STRNGR/DDDAT,TTTIM,NM,FN
!
   INTEGER NTOTAL
!
   COMMON/WAVEL/LAMB1,LAMB2,NTOTAL
!
   COMMON/TABL/TABLE,CTAB
!
!
   III=0
!
!       TABLE DATA IN BINARY FORMAT. TRA.DAT IS UNIT 35
!
!       DATA STORED IN TRA.DAT IS:
!               FILE NAME (TNAME) CHARACTER *8
!               DATA1 STARTING WAVELENGTH (DOUBLE PRES.)
!               DATA2 ENDIG WAVLENGTH (DOUBLE PRES.)
!               POINTS - NUMBER OF DATA POINTS (INTEGER)
!               OCC- OCCUPANCY FLAG, 0=EMPTY, 1- FULL (INTEGER)
!               DDATE-DATE FILED
!               TTIME=TIME FILED
!       FIND THE DESIRED FILE
!       ***************************************************************
   EXISJK=.FALSE.
   INQUIRE(FILE=LIBTRA//'TRA.DAT',EXIST=EXISJK)
   IF(.NOT.EXISJK) THEN
      WRITE(OUTLYNE,*)'"TABLE FILES DO NOT YET EXIST'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'TO INITIALIZE IT, USE "ITF" AND "PROCEED"'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       ***************************************************************
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//'TRA.DAT',&
   &FORM='UNFORMATTED',RECL=(50*NRECL),STATUS='UNKNOWN')
!
   DO 200 I=1,999
      READ(UNIT=35,REC=I) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
      DDDAT=DDATE
      TTTIM=TTIME
      IF(NM.EQ.WQ) THEN
!       FOUND FILE
         GO TO 300
      END IF
200 CONTINUE
!       IF YOU GOT HERE, FILE DID NOT EXIST
   WRITE(OUTLYNE,*)'FILE ',WQ,' DOES NOT EXIST'
   CALL SHOWIT(1)
   WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
   CALL SHOWIT(1)
   CALL CLOSE_FILE(35,1)
   CALL MACFAL
   RETURN
300 CONTINUE
!       LOAD FILE INTO TABLE ARRAY
!
   NF=I
   CALL TRAFIL(NF,FN)
!
!       OPEN AND READ FROM FILE
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
   &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
!
   DO 210 J=1,POINTS
      READ(UNIT=35,REC=J) TABLE(J,1),TABLE(J,2),TABLE(J,3)
210 CONTINUE
   CALL CLOSE_FILE(35,1)
   IF(W1.LT.LAMB1.OR.W1.GT.LAMB2) THEN
      WRITE(OUTLYNE,*)&
      &'NEW WAVELENGTH IS BEYOND CURRENTLY DEFINED RANGE'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
!       FIND A SPOT FOR THE NEW ENTRY
!

   DO 350 I=1,(POINTS-1)
      IF(TABLE(I,2).EQ.W1.OR.TABLE((I+1),2).EQ.W1) THEN
         WRITE(OUTLYNE,*)&
         &'NEW WAVELENGTH ENTRY EQUALS AN EXISTING WAVELENGTH VALUE'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(TABLE(I,2).LT.W1.AND.TABLE((I+1),2).GT.W1) THEN
!       NEW ENTRY GOES BETWEEN THE TWO TESTED RECORDS.
         III=I+1
         GO TO 360
      END IF
      IF(W1.LT.TABLE(1,2)) THEN
!       NEW ENTRY GOES AT BEGINNING OF LIST.
         III=1
         GO TO 360
      END IF
      IF(W1.GT.TABLE(POINTS,2)) THEN
!       NEW ENTRY GOES AT END OF LIST.
         III=POINTS+1
         GO TO 360
      END IF
350 CONTINUE
   WRITE(OUTLYNE,*)'NO INSERTION POINT FOUND'
   CALL SHOWIT(1)
   WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
   CALL SHOWIT(1)
   CALL MACFAL
   RETURN
360 CONTINUE
!
!       INCREMENT TOTAL NUMBER OF POINTS
!
   POINTS=POINTS+1
!
!       PERFORM INSERTION HERE
!
!       MOVE EVERYTHING DOWN ONE
!
   DO 370 I=POINTS,(III+1),-1
      TABLE(I,1)=TABLE((I-1),1)+1.0D0
      TABLE(I,2)=TABLE((I-1),2)
      TABLE(I,3)=TABLE((I-1),3)
370 CONTINUE
!
!       WRITE IN THE NEW DATA ITEM
!
   TABLE(III,1)=DBLE(III)
   TABLE(III,2)=W1
   TABLE(III,3)=W2
!
!       RESET PARAMETERS FOR DIRECTORY
!
   CTAB=POINTS
   CALL MYTIME(TTIME)
   CALL MYDATE(DDATE)
   DDDAT=DDATE
   TTTIM=TTIME
   IF(POINTS.EQ.0) THEN
      DATA1=0.0D0
      DATA2=0.0D0
      OCC=0
      DDATE='         '
      TTIME='        '
      DDDAT='         '
      TTTIM='        '
      NM='        '
   END IF
!
!       FILE THE FILE IN THE APPROPRIATE SPOT
!       OPEN AND WRITE TO FILE
!       ***************************************************************
   EXISJK=.FALSE.
   INQUIRE(FILE=LIBTRA//'TRA.DAT',EXIST=EXISJK)
   IF(.NOT.EXISJK) THEN
      WRITE(OUTLYNE,*)'"TABLE FILES DO NOT YET EXIST'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'TO INITIALIZE IT, USE "ITF" AND "PROCEED"'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       ***************************************************************
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//'TRA.DAT',&
   &FORM='UNFORMATTED',RECL=(50*NRECL),STATUS='UNKNOWN')
   WRITE(UNIT=35,REC=NF) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
   CALL CLOSE_FILE(35,1)
   IF(OCC.EQ.0) THEN
      OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
      &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
      CALL CLOSE_FILE(35,0)
   END IF
!
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
   &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
   F19=1
   DO 220 J=1,POINTS
      WRITE(UNIT=35,REC=J) TABLE(J,1),TABLE(J,2),TABLE(J,3)
220 CONTINUE
   CALL CLOSE_FILE(35,1)
!       PRINT MESSAGE TO THAT EFFECT
   WRITE(OUTLYNE,*)'ENTRY INSERTED INTO FILE ',WQ
   CALL SHOWIT(1)
   IF(OCC.EQ.0) THEN
      WRITE(OUTLYNE,*)'FILE ',WQ,' IS EMPTY'
      CALL SHOWIT(1)
      F19=0
   END IF
   RETURN
END
