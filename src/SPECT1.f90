!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS CALLED INTEGRATE THE CUMULATIVE
!       MEMORY AREA AND PLACE THE VALUE OF THE INTEGRAL
!       IN THE X- REGISTER
!
   INTEGER NTOTAL,I
!
   real(real64) VALUE,CUMULT(1:1001,1:3),LAMB1,LAMB2,&
   &IVAL
!
   COMMON/CUM/CUMULT
!
   COMMON/WAVEL/LAMB1,LAMB2,NTOTAL
!
!
!       THERE ARE NTOTAL POINTS IN THE CUMULATIVE AREA INLUDING
!       THE END POINTS. THEY ARE EQUALLY SPACED. EACH POINT HAS A
!       FUNCTIONAL VALUE ASSIGNED TO IT.
!
!       THE VAULE OF EACH INTERVAL IS EXACTLY
!
!               (LAMB2-LAMB1)/DBLE(NTOTAL-1)=IVAL
!
   IVAL=(LAMB2-LAMB1)/(DBLE(NTOTAL-1))
   VALUE=0.0D0
   DO 10 I=1,(NTOTAL-1)
      VALUE=VALUE+&
      &IVAL*((CUMULT(I,3)+CUMULT((I+1),3))/2.0D0)
!
10 CONTINUE
!
!       NOW SAVE THE VALUE IN THE X- REGISTER, PUSH THE STACK
!       AND UPDATE THE LASTX REGISTER.
   REG(40)=REG(9)
   REG(9)=VALUE
   WRITE(OUTLYNE,1000)
   CALL SHOWIT(0)
   WRITE(OUTLYNE,2000) VALUE
   CALL SHOWIT(0)
   WRITE(OUTLYNE,3000)
   CALL SHOWIT(0)
   WRITE(OUTLYNE,4000)
   CALL SHOWIT(0)
1000 FORMAT(&
   &'THE VALUE OF THE INTEGRAL OF THE "SPECT" CUMULATIVE')
2000 FORMAT('MEMORY AREA = ',G18.10)
3000 FORMAT(1X)
4000 FORMAT('THIS VALUE HAS BEEN STORED IN THE X-REGISTER')
   RETURN
END
