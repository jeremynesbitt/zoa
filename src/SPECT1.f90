! SUB WFACTOR.FOR
SUBROUTINE WFACTOR(NUM_JK)
!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS CALLED TO EXECUTE THE SPECT COMMAND "WFACTOR"
!       COMMAND "CUME" AT THE SPECTR LEVEL
!
   INTEGER I,NUM_JK,ALLOERR,J
!
   real(real64) CUMULT(1:1001,1:3),WVDELT,SUBSPACE &
   &,SPWTS,SPWV,CURVAL,TAREA,BIG1,WVLIM,TESTAREA,TEST2,TEST2OLD &
   &,STARTWV,STOPWV
!
   DIMENSION SPWTS(:),SPWV(:),BIG1(:),WVLIM(:)
!
   COMMON/CUM/CUMULT
!
   ALLOCATABLE :: SPWTS,SPWV,BIG1,WVLIM
   DEALLOCATE(BIG1,SPWTS,SPWV,WVLIM,STAT=ALLOERR)
   ALLOCATE(BIG1(1000),SPWTS(10),SPWV(10),WVLIM(11),STAT=ALLOERR)
!
   SPWTS(1:10)=0.0D0
   SPWV(1:10)=0.0D0
   WVDELT=DABS(CUMULT(1001,2)-CUMULT(1,2))/100.0D0
!
!     INTEGERATE THE FULL CUMULT AREA
   DO I=1,1000
      CURVAL=((CUMULT(I,3)+CUMULT(I+1,3))/2.0D0)*WVDELT
      BIG1(I)=CURVAL
   END DO
!     CALCULATE SUB-DIVISION SPACING
   SUBSPACE=DABS(CUMULT(1001,2)-CUMULT(1,2))/DBLE(NUM_JK)
!     CALCULATE ENDS OF SUB-DIVISIONS
   WVLIM(1)=CUMULT(1,2)
   DO I=2,NUM_JK+1
      WVLIM(I)=WVLIM(I-1)+SUBSPACE
   END DO
   DO I=1,1000
      DO J=1,NUM_JK
         IF((CUMULT(I,2)+CUMULT(I+1,2))/2.0D0.GE.WVLIM(J).AND.&
         &(CUMULT(I,2)+CUMULT(I+1,2))/2.0D0.LE.WVLIM(J+1))&
         &SPWTS(J)=SPWTS(J)+BIG1(I)
      END DO
   END DO
!     COMPUTE THE FULL CUMULT AREA AND NORMALIZE THE WEIGHTING FACTORS
   TAREA=0.0D0
   DO I=1,NUM_JK
      TAREA=TAREA+SPWTS(I)
   END DO
   DO I=1,NUM_JK
      SPWTS(I)=SPWTS(I)/TAREA
   END DO
!
!     NOW COMPUTE THE AREA CENTER WAVELENGTHS FOR EACH BAND
   DO I=1,NUM_JK
      STARTWV=WVLIM(I)
      STOPWV =WVLIM(I+1)
      TESTAREA=0.0D0
      DO J=1,1000
         IF((CUMULT(J,2)+CUMULT(J+1,2))/2.0D0.GE.STARTWV.AND.&
         &(CUMULT(J,2)+CUMULT(I+J,2))/2.0D0.LE.STOPWV) THEN
            TESTAREA=TESTAREA+BIG1(J)
         END IF
      END DO
      TEST2=0.0D0
      TEST2OLD=0.0D0
      DO J=1,1000
         IF((CUMULT(J,2)+CUMULT(J+1,2))/2.0D0.GE.STARTWV.AND.&
         &(CUMULT(J,2)+CUMULT(J+1,2))/2.0D0.LE.STOPWV) THEN
            TEST2OLD=TEST2
            TEST2=TEST2+BIG1(J)
            IF(TEST2OLD.LE.(TESTAREA/2.0D0).AND.TEST2.GE.(TESTAREA/2.0D0))&
            &THEN
!     SET WAVELENGTH AND GO TO 25
               SPWV(I)=(CUMULT(J,2)+CUMULT(J+1,2))/2.0D0
               GO TO 25
            END IF
         END IF
      END DO
!
25    CONTINUE
   END DO
   WRITE(OUTLYNE,202)
   CALL SHOWIT(0)
   WRITE(OUTLYNE,203)
   CALL SHOWIT(0)
!       PROCEED
   DO I=1,NUM_JK
      WRITE(OUTLYNE,100) SPWV(I),SPWTS(I)
      CALL SHOWIT(0)
   END DO
100 FORMAT(2X,G18.10,1X,6X,2X,G18.10)
200 FORMAT(&
   &'SPECTRAL WAVELENGTHS AND WEIGHTS')
201 FORMAT(1X)
202 FORMAT(' WAVLENGTH-(MICRONS) ',6X,&
   &'   SPECTRAL WEIGHT   ')
203 FORMAT(&
   &'--------------------------------------------------------')
   DEALLOCATE(BIG1,SPWTS,SPWV,WVLIM,STAT=ALLOERR)
   RETURN
END
