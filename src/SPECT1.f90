! SUB PFILE.FOR
SUBROUTINE PFILE
!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDELS FILING THE CONTENTS OF THE
!       CUMULATIVE MEMORY AREA TO A SPECT DISK FILE
!
!                       DEFINE VARIABLES
!
   CHARACTER NM*8,FN*10,DDATE*10,&
   &TTIME*8,TTTIM*8,DDDAT*10
!
   COMMON/STRNGR/DDDAT,TTTIM,NM,FN
!
   INTEGER POINTS,OCC,NTOTAL,NF,I,J,II
!
   real(real64) &
   &DATA1,DATA2,LAMB1,LAMB2
!
   real(real64) CUMULT(1:1001,1:3)
!
   LOGICAL EXISJK
!
   COMMON/CUM/CUMULT
!
   COMMON/WAVEL/LAMB1,LAMB2,NTOTAL
!
!
!       INITIALIZE II TO ZERO
   II=0
!
!       TABLE DATA IN BINARY FORMAT. TRA.DAT IS UNIT 35
!
!       DATA STORED IN TRA.DAT IS:
!               FILE NAME (WQ) CHARACTER *8
!               DATA1 STARTING WAVELENGTH (DOUBLE PRES.)
!               DATA2 ENDIG WAVLENGTH (DOUBLE PRES.)
!               POINTS - NUMBER OF DATA POINTS (INTEGER)
!               OCC- OCCUPANCY FLAG, 0=EMPTY, 1- FULL (INTEGER)
!               DDATE- CALENDER DATE FILE WAS STORED
!               TTIME-TIME FILED
!       WE MUST SEARCH FOR A FILE TO OVERWRITE
!       ***************************************************************
   EXISJK=.FALSE.
   INQUIRE(FILE=LIBTRA//'TRA.DAT',EXIST=EXISJK)
   IF(.NOT.EXISJK) THEN
      WRITE(OUTLYNE,*)'"TABLE FILES DO NOT YET EXIST'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'TO INITIALIZE IT, USE "ITF" AND "PROCEED"'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       ***************************************************************
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//'TRA.DAT',&
   &FORM='UNFORMATTED',RECL=(50*NRECL),STATUS='UNKNOWN')
   DO 9 I=1,200
      READ(UNIT=35,REC=I) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
      DDDAT=DDATE
      TTTIM=TTIME
      IF(NM.EQ.WQ) THEN
!       FOUND THE EARLIER VERSION OF THE TABLE FILE NAMED "WQ"
!       OVERWRITE IT.
         II=I
         GO TO 11
      END IF
!
9  CONTINUE
!
!       NO OVERWRITE TO DO, FIND A BLANK SPOT TO FILE IN
!
   DO 10 I=1,200
      READ(UNIT=35,REC=I) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
      DDDAT=DDATE
      TTTIM=TTIME
      IF(OCC.EQ.0) THEN
!       FOUND AN EMPTY SLOT
         II=I
         GO TO 11
      END IF
!
!       CHECK IF NM=WQ
!
10 CONTINUE
!       IF YOU GOT HERE, LIBRARY WAS FULL
   WRITE(OUTLYNE,*)&
   &'SPECT DISK FILE WAS FULL, CUMULATIVE DATA NOT FILED'
   CALL SHOWIT(1)
   WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
   CALL SHOWIT(1)
   CALL MACFAL
   RETURN
11 CONTINUE
!       PROCEED WITH STORAGE
!       FILE THE DIRECTORY DATA IN TRA.DAT
   DATA1=LAMB1
   DATA2=LAMB2
   POINTS=NTOTAL
   OCC=1
   CALL MYDATE(DDATE)
   CALL MYTIME(TTIME)
   DDDAT=DDATE
   TTTIM=TTIME
   WRITE(UNIT=35,REC=II) WQ,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
   CALL CLOSE_FILE(35,1)
   NF=II
   CALL TRAFIL(NF,FN)
!
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
   &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
   CALL CLOSE_FILE(35,0)
!       OPEN AND WRITE TO FILE
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
   &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
!
!       NOW OUTPUT TABLE DATA TO TRAXXX.DAT
!
   DO 210 J=1,NTOTAL
      WRITE(UNIT=35,REC=J) CUMULT(J,1),&
      &CUMULT(J,2),CUMULT(J,3)
210 CONTINUE
!
   CALL CLOSE_FILE(35,1)
   WRITE(OUTLYNE,*)&
   &'CUMULATIVE DATA STORED IN SPECT DISK FILE NO. ',II
   CALL SHOWIT(1)
   WRITE(OUTLYNE,*)'AS FILE ( ',WQ,' )'
   CALL SHOWIT(1)
   RETURN
!
END
! SUB TRFLEX.FOR
SUBROUTINE TRFLEX
!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE ERASES ALL EXISTING TRANSMISSION FILES
!       TRAXXX.FOR
!
   LOGICAL LXIST
!
   INTEGER NF
!
   CHARACTER FN*10
!
!
   DO NF=1,999
      CALL TRAFIL(NF,FN)
      LXIST=.FALSE.
      INQUIRE(FILE=LIBTRA//FN,EXIST=LXIST)
      IF(LXIST) THEN
         OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
         &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
         CALL CLOSE_FILE(35,1)
      ELSE
!       CONTINUE SEARCH
      END IF
   END DO
   RETURN
END
! SUB TRAFIL.FOR
SUBROUTINE TRAFIL(N,FN)
!
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS USED TO DETERMIN THE CORRECT FILE
!       NAME GIVEN A FILE NUMBER( FOR TRANSMISSION FILES USED
!       IN SPECT)
!
   INTEGER N
!
   CHARACTER FN*10,AN*3
!
   CALL CCOONN(N,AN)
!
   IF(N.GT.0.AND.N.LE.9) FN='TRA00'//AN(3:3)//'.DAT'
   IF(N.GT.9.AND.N.LE.99) FN='TRA0'//AN(2:3)//'.DAT'
   IF(N.GT.99.AND.N.LE.999) FN='TRA'//AN(1:3)//'.DAT'
   RETURN
END
! SUB CCOONN.FOR
SUBROUTINE CCOONN(N,AN)
!
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE USES WRITE AND READS TO AN INTERNAL
!       FILE TO CONVERT INTEGER NUMERIC WORDS
!       TO CHARACTER VARIABLES
!
   INTEGER N
!
   CHARACTER B*20,AN*3

   WRITE(B,100) N
   READ(B,200) AN
100 FORMAT(I3)
200 FORMAT(A3)
   RETURN
END
! SUB TFILE.FOR
SUBROUTINE TFILE(TNAME)
!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDELS FILING TABLE DATA TO DISK
!
!                       DEFINE VARIABLES
!
   CHARACTER TNAME*8,NM*8,FN*10,DDATE*10,&
   &TTIME*8,TTTIM*8,DDDAT*10
!
   LOGICAL EXISJK
!
   COMMON/STRNGR/DDDAT,TTTIM,NM,FN
!
   INTEGER CTAB,POINTS,OCC,NTOTAL,I,J,II
!
   real(real64) TABLE(1:1001,1:3),&
   &DATA1,DATA2,LAMB1,LAMB2
!
   INTEGER NF
!
   COMMON/TABL/TABLE,CTAB
!
   COMMON/WAVEL/LAMB1,LAMB2,NTOTAL
!
!
!       INITIALIZE II TO ZERO
   II=0
!
!       IF NUMBER OF POINTS IS LESS THAN 2, DON'T FILE THE
!       FILE, GIVE MESSAGE AND RETURN.
!
   IF(CTAB.LT.2) THEN
      WRITE(OUTLYNE,*)'LEAVING THE "TABLE" INPUT MODE'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)&
      &'NO TABLE DATA EXISTED TO FILE AND NO FILE WAS CREATED'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
!       TABLE DATA IN BINARY FORMAT. TRA.DAT IS UNIT 35
!
!       DATA STORED IN TRA.DAT IS:
!               FILE NAME (TNAME) CHARACTER *8
!               DATA1 STARTING WAVELENGTH (DOUBLE PRES.)
!               DATA2 ENDIG WAVLENGTH (DOUBLE PRES.)
!               POINTS - NUMBER OF DATA POINTS (INTEGER)
!               OCC- OCCUPANCY FLAG, 0=EMPTY, 1- FULL (INTEGER)
!               DDATE- CALENDER DATE FILE WAS STORED
!               TTIME-TIME FILED
!       WE MUST SEARCH FOR A FILE TO OVERWRITE
!       ***************************************************************
   EXISJK=.FALSE.
   INQUIRE(FILE=LIBTRA//'TRA.DAT',EXIST=EXISJK)
   IF(.NOT.EXISJK) THEN
      WRITE(OUTLYNE,*)'"TABLE FILES DO NOT YET EXIST'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'TO INITIALIZE IT, USE "ITF" AND "PROCEED"'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       ***************************************************************
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//'TRA.DAT',&
   &FORM='UNFORMATTED',RECL=(50*NRECL),STATUS='UNKNOWN')
   DO 9 I=1,999
      READ(UNIT=35,REC=I) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
      DDDAT=DDATE
      TTTIM=TTIME
      IF(NM.EQ.TNAME) THEN
!       FOUND THE EARLIER VERSION OF THE TABLE FILE NAMED "TNAME"
!       OVERWRITE IT.
         II=I
         GO TO 11
      END IF
!
9  CONTINUE
!
!       NO OVERWRITE TO DO, FIND A BLANK SPOT TO FILE IN
!
   DO 10 I=1,999
      READ(UNIT=35,REC=I) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
      DDDAT=DDATE
      TTTIM=TTIME
      IF(OCC.EQ.0) THEN
!       FOUND AN EMPTY SLOT
         II=I
         GO TO 11
      END IF
!
!       CHECK IF NM=TNAME
!
10 CONTINUE
!       IF YOU GOT HERE, LIBRARY WAS FULL
   WRITE(OUTLYNE,*)&
   &'SPECT DISK FILE WAS FULL, TABLE DATA NOT FILED'
   CALL SHOWIT(1)
   WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
   CALL SHOWIT(1)
   CALL MACFAL
   RETURN
11 CONTINUE
!       PROCEED WITH STORAGE
!       FILE THE DIRECTORY DATA IN TRA.DAT
   DATA1=LAMB1
   DATA2=LAMB2
   IF(TABLE(1,2).GT.0.0D0) DATA1=TABLE(1,2)
   IF(TABLE(CTAB,2).GT.0.0D0) DATA2=TABLE(CTAB,2)
   POINTS=CTAB
   OCC=1
   CALL MYDATE(DDATE)
   CALL MYTIME(TTIME)
   DDDAT=DDATE
   TTTIM=TTIME
   WRITE(UNIT=35,REC=II) TNAME,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
   CALL CLOSE_FILE(35,1)
!
   NF=II
   CALL TRAFIL(NF,FN)
!
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
   &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
   CALL CLOSE_FILE(35,0)
!       OPEN AND WRITE TO FILE
   OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//FN,&
   &FORM='UNFORMATTED',RECL=(80*NRECL),STATUS='UNKNOWN')
!
!       NOW OUTPUT TABLE DATA TO TRAXXX.DAT
!
   DO 210 J=1,POINTS
      WRITE(UNIT=35,REC=J) TABLE(J,1),TABLE(J,2),TABLE(J,3)
210 CONTINUE
!
   CALL CLOSE_FILE(35,1)
   WRITE(OUTLYNE,*)&
   &'TABLE ',TNAME,' STORED IN SPECT DISK FILE NO. ',II
   CALL SHOWIT(0)
   RETURN
!
END
! SUB TABDIR.FOR
SUBROUTINE TABDIR
!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
   INTEGER I
!
!       THIS SUBROUTINE HANDELS PRINTING THE DIRECTORY OF
!       SPECT DISK FILES
!
   CHARACTER NM*8,DDATE*10,TTIME*8,FN*10,TTTIM*8,DDDAT*10
!
   LOGICAL EXISJK
!
   COMMON/STRNGR/DDDAT,TTTIM,NM,FN
!
   INTEGER POINTS,OCC,OTPUT
!
   real(real64) DATA1,DATA2
!
!
!       TABLE DATA IN BINARY FORMAT. TRA.DAT IS UNIT 35
!
!       DATA STORED IN TRA.DAT IS:
!               FILE NAME (TNAME) CHARACTER *8
!               DATA1 STARTING WAVELENGTH (DOUBLE PRES.)
!               DATA2 ENDIG WAVLENGTH (DOUBLE PRES.)
!               POINTS - NUMBER OF DATA POINTS (INTEGER)
!               OCC- OCCUPANCY FLAG, 0=EMPTY, 1- FULL (INTEGER)
!               DDATE-DATE FILED
!               TTIME=TIME FILED
   IF(DF1.EQ.1) THEN
!       LIST AFF FILES
!
!       ***************************************************************
      EXISJK=.FALSE.
      INQUIRE(FILE=LIBTRA//'TRA.DAT',EXIST=EXISJK)
      IF(.NOT.EXISJK) THEN
         WRITE(OUTLYNE,*)'"TABLE FILES DO NOT YET EXIST'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'TO INITIALIZE IT, USE "ITF" AND "PROCEED"'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
!       ***************************************************************
      OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//'TRA.DAT',&
      &FORM='UNFORMATTED',RECL=(50*NRECL),STATUS='UNKNOWN')
!
      OTPUT=0
      DO 10 I=1,999
         READ(UNIT=35,REC=I)&
         &NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
         DDDAT=DDATE
         TTTIM=TTIME
         IF(OCC.EQ.1) THEN
            OTPUT=1
            GO TO 12
         END IF
!
10    CONTINUE
!
      IF(OTPUT.EQ.0) THEN
         WRITE(OUTLYNE,*)'SPECT DISK FILE IS EMPTY'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'NO DIRECTORY PRINTED'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
12    CONTINUE
      WRITE(OUTLYNE,100)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,101)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,106)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,102)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,101)
      CALL SHOWIT(0)
      DO 11 I=1,999
         READ(UNIT=35,REC=I) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
         DDDAT=DDATE
         TTTIM=TTIME
         IF(OCC.EQ.1) THEN
            WRITE(OUTLYNE,103)I,NM,DATA1,DATA2,POINTS,DDATE,TTIME
            CALL SHOWIT(0)
         END IF
!
!
11    CONTINUE
      CALL CLOSE_FILE(35,1)
   ELSE
!       JUST LIST ONE FILE IF IT EXISTS
!       ***************************************************************
      EXISJK=.FALSE.
      INQUIRE(FILE=LIBTRA//'TRA.DAT',EXIST=EXISJK)
      IF(.NOT.EXISJK) THEN
         WRITE(OUTLYNE,*)'"TABLE FILES DO NOT YET EXIST'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'TO INITIALIZE IT, USE "ITF" AND "PROCEED"'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
!       ***************************************************************
      OPEN(UNIT=35,ACCESS='DIRECT',FILE=LIBTRA//'TRA.DAT',&
      &FORM='UNFORMATTED',RECL=(50*NRECL),STATUS='UNKNOWN')
!
      I=INT(W1)
      READ(UNIT=35,REC=I) NM,DATA1,DATA2,POINTS,OCC,DDATE,TTIME
      DDDAT=DDATE
      TTTIM=TTIME
      CALL CLOSE_FILE(35,1)
      IF(OCC.EQ.1) THEN
!       OUTPUT
         WRITE(OUTLYNE,105) INT(W1),NM
         CALL SHOWIT(0)
         WRITE(OUTLYNE,101)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,106)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,102)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,101)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,103)I,NM,DATA1,DATA2,POINTS,DDATE,TTIME
         CALL SHOWIT(0)
         RETURN
      ELSE
!       PRINT MESSAGE
         WRITE(OUTLYNE,*)'FILE # ',INT(W1),' DOES NOT EXIST'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
      END IF
      RETURN
   END IF
100 FORMAT('DIRECTORY OF SPECT FILES STORED ON DISK')
105 FORMAT('DIRECTORY FOR SPECT FILE ',I3,1X,A8)
101 FORMAT(1X)
102 FORMAT(&
   &'           ',4X,'(MICRONS)',4X,'(MICRONS)')
106 FORMAT(&
   &'FILE #/NAME',4X,'LAMBDA 1',5X,'LAMBDA 2',3X,&
   &'NO. OF POINTS',2X,'DATE/TIME FILED')
103 FORMAT(I3,1X,A8,1X,G12.4,1X,G12.4,5X,I4,7X,A9,'/',A8)
   RETURN
!
END
! SUB SSTART.FOR
SUBROUTINE SSTART
!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS CALLED TO ENTER THE SPECT LEVEL COMMAND
!       "START"
!
   INTEGER NTOTAL,I
!
   real(real64) LAMB1,LAMB2,&
   &CUMULT(1:1001,1:3)
!
   COMMON/CUM/CUMULT
!
   COMMON/WAVEL/LAMB1,LAMB2,NTOTAL
!
!
   IF(NTOTAL.LT.0) NTOTAL=1001
   DO 5 I=1,NTOTAL
      CUMULT(I,1)=DBLE(I)
      IF(I.EQ.1) CUMULT(I,2)=LAMB1
      IF(I.NE.1) CUMULT(I,2)=CUMULT((I-1),2)+&
      &((LAMB2-LAMB1)/(DBLE(NTOTAL-1)))
5  CONTINUE
!       DEFINE AND SET THE CUMULATIVE AREA OF MEMORY
   DO 10 I=1,NTOTAL
      CUMULT(I,3)=1.0D0
10 CONTINUE
!
!                       RETURN
END
! SUB SPECTR.FOR
SUBROUTINE SPECTR
!
   use DATMAI
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS CALLED TO ENTER THE SPECT LEVEL
!       FROM THE CMD LEVEL
!
   INTEGER NTOTAL
!
   real(real64) LAMB1,LAMB2,&
   &CUMULT(1:1001,1:3)
!
   COMMON/CUM/CUMULT
!
   COMMON/WAVEL/LAMB1,LAMB2,NTOTAL
!
!
!       SPECT TAKES NO INPUT
   IF(SQ.EQ.1.OR.SN.EQ.1.OR.SST.EQ.1) THEN
      WRITE(OUTLYNE,*)&
      &'"SPECTR" TAKES NO EXPLICIT INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
!                       SET F17=1, LEAVE F1=1
!       SET F18 AND F19 TO 0
!       F18 TRACKS TABLE INPUT LEVEL
!       F19 TRACKS EXISTENCE OF TABLE DATA
   IF(F1.NE.1) F1=1
   F18=0
   F19=0
   NTOTAL=-99
   LAMB1=0.05
   LAMB2=20.0
   F17=1
   CALL SSTART
!                       RETURN
END
