SUBROUTINE ORIENT
!
   use DATLEN
   use DATMAI
   use command_utils, only: is_command_query
   IMPLICIT NONE
!
!       THIS ROUTINE DOES THE ORIENT COMMAND
!
   INTEGER I,ORSF
!
   COMMON/SFOR/ORSF
!
   REAL*8 LVAL,MVAL,NVAL,MAG,SINA,COSA,SINF,COSF
!
!
   IF(WC.EQ.'ORIENT') THEN
!
!
      IF(is_command_query()) THEN
         WRITE(OUTLYNE,800)
         CALL SHOWIT(1)
         WRITE(OUTLYNE,801)
         CALL SHOWIT(1)
         WRITE(OUTLYNE,802)
         CALL SHOWIT(1)
800      FORMAT('"ORIENT" SETS THE "LOOK VECTOR" PARALLEL')
801      FORMAT('"TO THE LOCAL Z-AXIS OF THE SURFACE DESIGNATED')
802      FORMAT('"BY NUMERIC WORD #1')
         RETURN
      ELSE
      END IF
!       CHECK SYNTAX
      IF(SQ.EQ.1.OR.SST.EQ.1) THEN
         OUTLYNE=&
         &'"ORIENT" TAKES NO QUALIFIER OR STRING INPUT'
         CALL SHOWIT(1)
         CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
         RETURN
      END IF
      IF(S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
         OUTLYNE=&
         &'"ORIENT" ONLY TAKES NUMERIC WORDS #1 AND #2 INPUT'
         CALL SHOWIT(1)
         CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
         RETURN
      END IF
      IF(DF1.EQ.1) THEN
         OUTLYNE=&
         &'"ORIENT" REQUIRES EXPLICIT NUMERIC WORD #1 INPUT'
         CALL SHOWIT(1)
         CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
         RETURN
      END IF
      IF(W1.LT.0.0D0.OR.W1.GT.SYSTEM(20)) THEN
         OUTLYNE=&
         &'NUMERIC WORD #1, SURFACE #, BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
         RETURN
      END IF
!
!     MAKE SURE YOU HAVE VERTEX DATA WITHOUT OFFSETS AND SET TO
!     THE FIRST SURFACE WITHOUT AN INFINITE THICKNESS
      GLSURF=-99
      DO I=NEWIMG,NEWOBJ,-1
         IF(DABS(ALENS(3,I)).LE.1.0D10) GLSURF=I
      END DO
      IF(GLSURF.EQ.-99) THEN
         GLOBE=.FALSE.
         CALL REPORT_ERROR_AND_FAIL(&
         & 'ALL SURFACES WERE OF INFINITE THICKNESS'//'\n'//&
         & '"ORIENT" DID NOT CHANGE THE "LOOK VECTOR"', 1)
         RETURN
      END IF
      GLOBE=.TRUE.
      OFFX=0.0D0
      OFFY=0.0D0
      OFFZ=0.0D0
      OFFA=0.0D0
      OFFB=0.0D0
      OFFC=0.0D0
!     RESET NEWIMG,NEWOBJ AND NEWREF
      CALL RESSUR
      CALL GLVERT
      CALL OLDSUR
      GLOBE=.FALSE.
!     VERTEX DATA EXISTS
!     NOW GET THE DIRECTION COSINES OF THE LOCAL Z-AXIS OF SURFACE
!     DESIGNATED BY W1
      ORSF=INT(W1)
      LVAL=VERTEX(10,INT(W1))
      MVAL=VERTEX(11,INT(W1))
      NVAL=VERTEX(12,INT(W1))
      MAG=DSQRT((LVAL**2)+(MVAL**2)+(NVAL**2))
      LOOKX=LVAL/MAG
      LOOKY=MVAL/MAG
      LOOKZ=NVAL/MAG
!       CALCULATE VIEALF AND VIEPHI
      SINA=LOOKY
      COSA=DSQRT((LOOKX**2)+(LOOKZ**2))
      IF(DABS(SINA).LE.1.0D-15.AND.DABS(COSA).LE.1.0D-15) THEN
         VIEALF=0.0D0
      ELSE
         VIEALF=DATAN2(SINA,COSA)
      END IF
      VIEALF=(180.0D0/PII)*VIEALF
      IF(VIEALF.GT.0.0D0) THEN
         IF(VIEALF.GT.90.0D0) VIEALF=180.0D0-VIEALF
         GO TO 1000
      ELSE
      END IF
      IF(VIEALF.LT.0.0D0) THEN
         IF(VIEALF.LT.-90.0D0) VIEALF=-180.0D0-VIEALF
      ELSE
      END IF
1000  CONTINUE
      IF(DABS(COSA).GT.1.0D-10) THEN
         SINF=LOOKX/COSA
         COSF=LOOKZ/COSA
         IF(DABS(SINF).LE.1.0D-15.AND.DABS(COSF).LE.1.0D-15) THEN
            VIEPHI=0.0D0
         ELSE
            VIEPHI=DATAN2(SINF,COSF)
         END IF
         VIEPHI=(180.0D0/PII)*VIEPHI
         IF(VIEPHI.LT.0.0D0) VIEPHI=360.0D0+VIEPHI
      ELSE
         VIEPHI=270.0D0
      END IF
      IF(DF2.EQ.0) THEN
         LORIENT=.TRUE.
         SORIENT=INT(W1)
      ELSE
         LORIENT=.FALSE.
      END IF
      RETURN
   END IF
   IF(WC.EQ.'NORIENT') THEN
!
!       CHECK SYNTAX
      IF(SQ.EQ.1.OR.SST.EQ.1) THEN
         OUTLYNE=&
         &'"NORIENT" TAKES NO QUALIFIER OR STRING INPUT'
         CALL SHOWIT(1)
         CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
         RETURN
      END IF
      IF(S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
         OUTLYNE=&
         &'"NORIENT" ONLY TAKES NUMERIC WORDS #1 AND #2 INPUT'
         CALL SHOWIT(1)
         CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
         RETURN
      END IF
      IF(DF1.EQ.1) THEN
         OUTLYNE=&
         &'"NORIENT" REQUIRES EXPLICIT NUMERIC WORD #1 INPUT'
         CALL SHOWIT(1)
         CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
         RETURN
      END IF
!
      IF(is_command_query()) THEN
         WRITE(OUTLYNE,1800)
         CALL SHOWIT(1)
         WRITE(OUTLYNE,1801)
         CALL SHOWIT(1)
1800     FORMAT('"NORIENT" SETS THE "LOOK VECTOR" ANTI-PARALLEL')
1801     FORMAT('"TO THE LOCAL Z-AXIS OF THE SURFACE DESIGNATED')
         RETURN
      ELSE
      END IF
!
!     MAKE SURE YOU HAVE VERTEX DATA WITHOUT OFFSETS AND SET TO
!     THE FIRST SURFACE WITHOUT AN INFINITE THICKNESS
      GLSURF=-99
      DO I=NEWIMG,NEWOBJ,-1
         IF(DABS(ALENS(3,I)).LE.1.0D10) GLSURF=I
      END DO
      IF(GLSURF.EQ.-99) THEN
         GLOBE=.FALSE.
         CALL REPORT_ERROR_AND_FAIL(&
         & 'ALL SURFACES WERE OF INFINITE THICKNESS'//'\n'//&
         & '"ORIENT" DID NOT CHANGE THE "LOOK VECTOR"', 1)
         RETURN
      END IF
      GLOBE=.TRUE.
      OFFX=0.0D0
      OFFY=0.0D0
      OFFZ=0.0D0
      OFFA=0.0D0
      OFFB=0.0D0
      OFFC=0.0D0
!     RESET NEWIMG,NEWOBJ AND NEWREF
      CALL RESSUR
      CALL GLVERT
      CALL OLDSUR
      GLOBE=.FALSE.
!     VERTEX DATA EXISTS
!     NOW GET THE DIRECTION COSINES OF THE LOCAL Z-AXIS OF SURFACE
!     DESIGNATED BY W1
      ORSF=INT(W1)
      LVAL=VERTEX(10,INT(W1))
      MVAL=VERTEX(11,INT(W1))
