   &RADTOL1=0.001D0/ALENS(1,DRAWSURF)
   IF(ALENS(1,DRAWSURF+1).NE.0.0D0)&
   &RADTOL2=0.001D0/ALENS(1,DRAWSURF+1)
   FRINTOL1=.FALSE.
   FRINTOL2=.FALSE.
!
!     IRREGULARITY FRINGES
   FRNG1=1.0D0
   FRNG2=1.0D0
