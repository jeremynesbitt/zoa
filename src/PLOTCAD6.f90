SUBROUTINE FIXUP1(IX,IY)
!       IX IS LESS THAN 0
   use DATHGR
   use DATMAI
   IMPLICIT NONE
   LOGICAL VERTICAL,HORIZONTAL
   INTEGER IX,IY
   REAL*8 DELX,DELY,SLOPE
   VERTICAL=.FALSE.
   HORIZONTAL=.FALSE.
   DELX=PCURX-POLDX
   DELY=PCURY-POLDY
   IF(DELY.EQ.0.0D0) HORIZONTAL=.TRUE.
   IF(DELX.EQ.0.0D0) VERTICAL=.TRUE.
   IF(VERTICAL) THEN
!       LINE WAS VERTICAL
      IF(PCURY.GT.7000.0D0) PNEWY=7000.0D0
      IF(PCURY.LT.0.0D0) PNEWY=0.0D0
      PNEWX=0.0D0
      IX=INT(PNEWX)
      IY=INT(PNEWY)
      RETURN
   END IF
   IF(HORIZONTAL) THEN
!       LINE WAS HORIZONTAL
      IF(PCURY.GT.7000.0D0) PNEWY=7000.0D0
      IF(PCURY.LT.0.0D0) PNEWY=0.0D0
      PNEWX=0.0D0
      IX=INT(PNEWX)
      IY=INT(PNEWY)
      RETURN
   END IF
   SLOPE=DELY/DELX
   PNEWX=0.0D0
   PNEWY=(SLOPE*(PNEWX-POLDX))+POLDY
   IX=INT(PNEWX)
   IY=INT(PNEWY)
   RETURN
END
