SUBROUTINE DRAW_INITIALIZE
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
   INTEGER I,J
!
   INCLUDE 'DATPTS.INC'
   PARTISDRAWING=.FALSE.
   TEST1=0.0D0
   TEST2=0.0D0
   PODA1=0.0D0
   PODA2=0.0D0
   PODB1=0.0D0
   PODB2=0.0D0
!
!     FIX DEFAULT VALUES
!
!     OD, THE OUTSIDE DIAMETER AND FLAT DIAMETER VALUES
!     FOR SURFACES DRAWSURF AND DRAWSURF+1
   DO I=1,2
      IF(I.EQ.1) J=DRAWSURF
      IF(I.EQ.2) J=DRAWSURF+1
      IF(ALENS(9,J).EQ.0.0D0.OR.&
      &ALENS(9,J).EQ.2.0D0.OR.&
      &ALENS(9,J).EQ.3.0D0.OR.&
      &ALENS(9,J).EQ.4.0D0.OR.&
      &ALENS(9,J).EQ.5.0D0) THEN
         TEST1=DABS(PXTRAY(1,J))+DABS(PXTRAY(5,J))
         TEST2=DABS(PXTRAX(1,J))+DABS(PXTRAX(5,J))
         IF(TEST1.GE.TEST2) THEN
            IF(I.EQ.1) THEN
               PODA1=TEST1
               PODA2=TEST1
            ELSE
               PODB1=TEST1
               PODB2=TEST1
            END IF
         ELSE
            IF(I.EQ.1) THEN
               PODA1=TEST2
               PODA2=TEST2
            ELSE
               PODB1=TEST2
               PODB2=TEST2
            END IF
         END IF
      ELSE
!     MUST BE CIRCULAR
         IF(I.EQ.1) THEN
            PODA1=DABS(ALENS(10,J))
            PODA2=DABS(ALENS(11,J))
         ELSE
            PODB1=DABS(ALENS(10,J))
            PODB2=DABS(ALENS(11,J))
         END IF
      END IF
   END DO
   IF(PODA1.GE.PODB1) POD1=PODA1
   IF(PODA1.LT.PODB1) POD1=PODB1
   IF(PODA2.GE.PODB2) POD2=PODA2
   IF(PODA2.LT.PODB2) POD2=PODB2
   POD1=2.0D0*POD1
   POD2=2.0D0*POD2
!
!     DIATOL
