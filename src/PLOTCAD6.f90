      MAG=DSQRT((LVAL**2)+(MVAL**2)+(NVAL**2))
      LOOKX=-LVAL/MAG
      LOOKY=-MVAL/MAG
      LOOKZ=-NVAL/MAG
!       CALCULATE VIEALF AND VIEPHI
      SINA=LOOKY
      COSA=DSQRT((LOOKX**2)+(LOOKZ**2))
      IF(DABS(SINA).LE.1.0D-15.AND.DABS(COSA).LE.1.0D-15) THEN
         VIEALF=0.0D0
      ELSE
         VIEALF=DATAN2(SINA,COSA)
      END IF
      VIEALF=(180.0D0/PII)*VIEALF
      IF(VIEALF.GT.0.0D0) THEN
         IF(VIEALF.GT.90.0D0) VIEALF=180.0D0-VIEALF
         GO TO 1001
      ELSE
      END IF
      IF(VIEALF.LT.0.0D0) THEN
         IF(VIEALF.LT.-90.0D0) VIEALF=-180.0D0-VIEALF
      ELSE
      END IF
1001  CONTINUE
      IF(DABS(COSA).GT.1.0D-10) THEN
         SINF=LOOKX/COSA
         COSF=LOOKZ/COSA
         IF(DABS(SINF).LE.1.0D-15.AND.DABS(COSF).LE.1.0D-15) THEN
            VIEPHI=0.0D0
         ELSE
            VIEPHI=DATAN2(SINF,COSF)
         END IF
         VIEPHI=(180.0D0/PII)*VIEPHI
         IF(VIEPHI.LT.0.0D0) VIEPHI=360.0D0+VIEPHI
      ELSE
         VIEPHI=270.0D0
      END IF
      IF(DF2.EQ.0) THEN
         LORIENT=.TRUE.
         SORIENT=INT(W1)
      ELSE
         LORIENT=.FALSE.
      END IF
      RETURN
   END IF
END
! SUB ORSHIFT.FOR
SUBROUTINE ORSHIFT
!
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
!     THIS DOES THE AUTOMATIC SHIFT CALCS FOR ORIENT
!
   REAL*8 X,Y,Z,XN,YN,ZN,ROT1X,ROT1Z,ROT2Y &
   &,ROT2Z,AX,AY,AZ,AALF,APHI,&
   &VIEPH,VIEAL
!
   INTEGER I
!
   REAL*8 XVAL,YVAL,ZVAL
!
!
!
!     LOOK.VIEW TRANSFORMS

   ROT1X(AX,AZ,APHI)=((AX*DCOS(APHI))-(AZ*DSIN(APHI)))
   ROT1Z(AX,AZ,APHI)=((AX*DSIN(APHI))+(AZ*DCOS(APHI)))
!
   ROT2Z(AZ,AY,AALF)=((AZ*DCOS(AALF))+(AY*DSIN(AALF)))
   ROT2Y(AZ,AY,AALF)=((-AZ*DSIN(AALF))+(AY*DCOS(AALF)))
!
   X=0.0D0
   Y=0.0D0
!
   VIEPH=(PII/180.0D0)*VIEPHI
   VIEAL=(PII/180.0D0)*VIEALF
!
!     MAKE SURE YOU HAVE VERTEX DATA WITHOUT OFFSETS AND SET TO
!     THE FIRST SURFACE WITHOUT AN INFINITE THICKNESS
   GLSURF=-99
   DO I=NEWIMG,NEWOBJ,-1
      IF(DABS(ALENS(3,I)).LE.1.0D10) GLSURF=I
   END DO
   IF(GLSURF.EQ.-99) THEN
      GLOBE=.FALSE.
      OUTLYNE='ALL SURFACES WERE OF INFINITE THICKNESS'
      CALL SHOWIT(1)
      OUTLYNE='NO AUTOMATIC SHIFT CALCULATIONS WERE MADE'
      CALL SHOWIT(1)
      LORIENT=.FALSE.
      CALL MACFAL
      RETURN
   END IF
   GLOBE=.TRUE.
   OFFX=0.0D0
   OFFY=0.0D0
   OFFZ=0.0D0
   OFFA=0.0D0
   OFFB=0.0D0
   OFFC=0.0D0
!     RESET NEWIMG,NEWOBJ AND NEWREF
   CALL RESSUR
   CALL GLVERT
   CALL OLDSUR
   GLOBE=.FALSE.
!     ORIGIN OF ORIENT SURFACE IS
   XVAL=VERTEX(1,SORIENT)+ALENS(13,SORIENT)
   YVAL=VERTEX(2,SORIENT)+ALENS(12,SORIENT)
   ZVAL=VERTEX(3,SORIENT)
!
   X=XVAL
   Y=YVAL
   Z=ZVAL
   X=X-XROT
   Y=Y-YROT
   Z=Z-ZROT
   XN=ROT1X(X,Z,VIEPH)
   YN=Y
   ZN=ROT1Z(X,Z,VIEPH)
   X=XN
   Y=YN
   Z=ZN
!
   ZN=ROT2Z(Z,Y,VIEAL)
   YN=ROT2Y(Z,Y,VIEAL)
   XN=X
   XVAL=XN
   YVAL=YN
   ZVAL=ZN
!
!
!     RIGHT NOW,COORDINATES ARE IN WORLD COORDINATES
!     CONVERT THEM TO DEVICE INDEPENDENT GRAPHICS COORDINATES IN
!     TWO STEPS.
!
!     THE WORLD X PLOTS TO THE PLOTTER X
!     THE WORLD Y PLOTS TO THE PLOTTER Y
!
!     STEP 1: CONVERT USING AN APPROPRIATE SCALE FACTOR
!               CALCULATING AN APPROPRIATE FACTOR IF NECESSARY
   XVAL=(XVAL/SCFAX)*1000.0D0
   YVAL=(YVAL/SCFAY)*1000.0D0
!     THESE ARE THE NEW SHIFTS
   PXSHFT= -INT(XVAL)
   PYSHFT= -INT(YVAL)
!
   RETURN
END
! SUB VIGCAL.FOR
SUBROUTINE VIGCAL(N,VLO,VHI,XY)
!
!     XY=1 FOR XZ
!     XY=2 FOR YZ
!     N IS NUMBER OF POINTS FROM -1 TO 1
!
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
   REAL*8 VHI,VLO
!
   INTEGER N,XY,ACOD,OACOD,I
!
!
!     FIRST LOWER VALUE
!     SET ACOD TO FAILURE
   ACOD=0
   DO I=-N,N,1
      IF(XY.EQ.1) THEN
         WW1=0.0D0
         WW2=DBLE(I)/DBLE(N)
      END IF
      IF(XY.EQ.2) THEN
         WW1=DBLE(I)/DBLE(N)
         WW2=0.0D0
      END IF
      WW3=SYSTEM(11)
      WVN=SYSTEM(11)
!               TRACE RAY
      MSG=.FALSE.
      CALL QTRA1(.FALSE.)
      OACOD=ACOD
      IF(RAYCOD(1).EQ.0.OR.RAYCOD(1).EQ.7)  ACOD=1
      IF(RAYCOD(1).NE.0.AND.RAYCOD(1).NE.7) ACOD=0
!
      IF(OACOD.EQ.0.AND.ACOD.EQ.1) THEN
         VLO=DBLE(I)/DBLE(N)
         GO TO 10
      END IF
   END DO
!     IF WE ARE HERE, THERE IS NO VIGNETTING OR CONPLETE
!     VIGNETTING AT THE LOWER END
   IF(ACOD.EQ.1) THEN
      VLO=-1.0D0
   END IF
   IF(ACOD.EQ.0) THEN
      VLO=0.0D0
   END IF
10 CONTINUE
!     NOW UPPER VALUE
   ACOD=0
   DO I=N,-N,-1
      IF(XY.EQ.1) THEN
         WW1=0.0D0
         WW2=DBLE(I)/DBLE(N)
      END IF
      IF(XY.EQ.2) THEN
         WW2=0.0D0
         WW1=DBLE(I)/DBLE(N)
      END IF
      WW3=SYSTEM(11)
      WVN=SYSTEM(11)
!               TRACE RAY
      MSG=.FALSE.
      CALL QTRA1(.FALSE.)
      OACOD=ACOD
      IF(RAYCOD(1).EQ.0.OR.RAYCOD(1).EQ.7)  ACOD=1
      IF(RAYCOD(1).NE.0.AND.RAYCOD(1).NE.7) ACOD=0
!
      IF(OACOD.EQ.0.AND.ACOD.EQ.1) THEN
         VHI=DBLE(I)/DBLE(N)
         GO TO 20
      END IF
   END DO
!     IF WE ARE HERE, THERE IS NO AT THE UPPER END OR COMPLETE
!     VIGNETTING
   IF(ACOD.EQ.1) THEN
      VHI=1.0D0
   END IF
   IF(ACOD.EQ.0) THEN
      VHI=0.0D0
   END IF
20 CONTINUE
   IF(VHI.LE.VLO) THEN
!       BAD SHIT HAPPENED, RESET TO DEFAULTS AND HOPE FOR THE BEST
      VHI=1.0D0
      VLO=-1.0D0
   ELSE
!       NO CORRECTION
   END IF

   RETURN
END
