   IF(SYSTEM(6).EQ.1.0D0) THITOL=0.002D0
   IF(SYSTEM(6).EQ.2.0D0) THITOL=0.002D0*2.54D0
   IF(SYSTEM(6).EQ.3.0D0) THITOL=0.002D0*25.4D0
