            CALL GNPR7(INT(W1),NWN1,NWN2,NWN3,NWN4,ERROR,0)
         END IF
         ERROR=0
         IF(WQ.EQ.'GPUCX') THEN
            NWN1=W2
            NWN2=W3
            NWN3=0.0D0
            NWN4=SYSTEM(11)
            CALL GNPR8(INT(W1),NWN1,NWN2,NWN3,NWN4,ERROR,0)
         END IF
!
         IF(ERROR.EQ.1) THEN
            WRITE(OUTLYNE,*)'RAY COULD NOT BE TRACED'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'GENERALIZED PARAXIAL RAY VALUES NOT CALCULABLE'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'NO ACTION TAKEN'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
         GPR=.FALSE.
         GO TO 200
      END IF
   END IF
!
   RETURN
!
200 CONTINUE
   NUM5=INT(W5)
   CALL GETA
   IF(SHOW) THEN
      SAVE_KDP(1)=SAVEINPT(1)
      INPUT='WRITE X '//REMWQ
      CALL PROCES
      REST_KDP(1)=RESTINPT(1)
   END IF
   RETURN
END
