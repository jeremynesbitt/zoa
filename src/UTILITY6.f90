FUNCTION EDGTHK(I,K)
!
   use DATLEN
   use mod_surface
   IMPLICIT NONE
!
   REAL*8 EDGTHK
!
   LOGICAL ETERROR
!
   INTEGER I,K
!
   EXTERNAL SAGIT
!
   REAL*8 CA1,CA2,CA,TH12,SAGIT,SAG1,SAG2
!
!
   ETERROR=.FALSE.
!
   IF(K.EQ.1) THEN
!     YZ PLANE CALCULATION FROM SURFACE I TO I+1
!     DETERMINE HEIGHTS AND THICKNESS FOR CALCULATION
      IF(surf_clap_type(I).EQ.0.0D0.AND.surf_clap_type(I+1).EQ.0.0D0) THEN
!     NO CLAPS
         CA1=DABS(PXTRAY(1,I))+DABS(PXTRAY(5,I))
         CA2=DABS(PXTRAY(1,I+1))+DABS(PXTRAY(5,I+1))
         TH12=surf_thickness(I)
      ELSE
!     SOME CLAPS
         IF(surf_clap_type(I).EQ.0.0D0) THEN
            CA1=DABS(PXTRAY(1,I))+DABS(PXTRAY(5,I))
         ELSE
            IF(surf_clap_type(I).EQ.1.0D0) THEN
               IF(surf_clap_dim(I, 1).LE.surf_clap_dim(I, 2))CA1=DABS(surf_clap_dim(I, 1))
               IF(surf_clap_dim(I, 1).GT.surf_clap_dim(I, 2))CA1=DABS(surf_clap_dim(I, 2))
            ELSE
               CA1=DABS(surf_clap_dim(I, 1))
            END IF
         END IF
         IF(surf_clap_type(I+1).EQ.0.0D0) THEN
            CA1=DABS(PXTRAY(1,I+1))+DABS(PXTRAY(5,I+1))
         ELSE
            IF(surf_clap_type(I+1).EQ.1.0D0) THEN
               IF(surf_clap_dim(I+1, 1).LE.surf_clap_dim(I+1, 2))CA1=DABS(surf_clap_dim(I+1, 1))
               IF(surf_clap_dim(I+1, 1).GT.surf_clap_dim(I+1, 2))CA1=DABS(surf_clap_dim(I+1, 2))
            ELSE
               CA1=DABS(surf_clap_dim(I+1, 1))
            END IF
         END IF
         TH12=surf_thickness(I)
      END IF
      IF(CA1.GT.CA2) CA=CA1
      IF(CA1.LE.CA2) CA=CA2
!     NOW CA AND TH12 HAVE BEEN DETERMINED
!     CALC SAG1 AND SAG2
      SAG1=SAGIT(I,CA,1,ETERROR)
      SAG2=SAGIT(I+1,CA,1,ETERROR)
      IF(ETERROR) THEN
         EDGTHK=0.0D0
      ELSE
         EDGTHK=-SAG1+SAG2+TH12
      END IF

      RETURN
   END IF
   IF(K.EQ.2) THEN
!     XZ PLANE CLACULATION
!     DETERMINE HEIGHTS AND THICKNESS FOR CALCULATION
      IF(surf_clap_type(I).EQ.0.0D0.AND.surf_clap_type(I+1).EQ.0.0D0) THEN
!     NO CLAPS
         CA1=DABS(PXTRAX(1,I))+DABS(PXTRAX(5,I))
         CA2=DABS(PXTRAX(1,I+1))+DABS(PXTRAX(5,I+1))
         TH12=surf_thickness(I)
      ELSE
!     SOME CLAPS
         IF(surf_clap_type(I).EQ.0.0D0) THEN
            CA1=DABS(PXTRAX(1,I))+DABS(PXTRAX(5,I))
         ELSE
            IF(surf_clap_type(I).EQ.1.0D0) CA1=DABS(surf_clap_dim(I, 1))
            IF(surf_clap_type(I).GT.1.0D0) CA1=DABS(surf_clap_dim(I, 2))
         END IF
         IF(surf_clap_type(I+1).EQ.0.0D0) THEN
            CA1=DABS(PXTRAX(1,I+1))+DABS(PXTRAX(5,I+1))
         ELSE
            IF(surf_clap_type(I+1).EQ.1.0D0) CA1=DABS(surf_clap_dim(I+1, 1))
            IF(surf_clap_type(I+1).GT.1.0D0) CA1=DABS(surf_clap_dim(I+1, 2))
         END IF
         TH12=surf_thickness(I)
      END IF
      IF(CA1.GT.CA2) CA=CA1
      IF(CA1.LE.CA2) CA=CA2
!     NOW CA AND TH12 HAVE BEEN DETERMINED
!     CALC SAG1 AND SAG2
      SAG1=SAGIT(I,CA,2,ETERROR)
      SAG2=SAGIT(I+1,CA,2,ETERROR)
      IF(ETERROR) THEN
         EDGTHK=0.0D0
      ELSE
         EDGTHK=-SAG1+SAG2+TH12
      END IF
      RETURN
   END IF
   RETURN
END
FUNCTION SAGIT(I,CA,K,ETERROR)
!
   use DATLEN
   use mod_surface
   IMPLICIT NONE
!
   INTEGER I,K
!
   LOGICAL ETERROR
!
   REAL*8 CA,SAGIT,SAG1,SAG2,SAG
!
!
   IF(K.EQ.1) THEN
!     YZ PLANE CALCULATION FROM SURFACE I TO I+1
!     DETERMINE SAG FOR SURF I AND I+1 IN YZ-PLANE
      CALL SAGITT(I,CA,K,SAG,ETERROR)
      SAGIT=SAG
      RETURN
   END IF
   IF(K.EQ.2) THEN
!     XZ PLANE CLACULATION
!     DETERMINE SAG FOR SURF I AND I+1 IN YZ-PLANE
      CALL SAGITT(I,CA,K,SAG,ETERROR)
      SAGIT=SAG
      RETURN
   END IF
   RETURN
END
