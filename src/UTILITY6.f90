SUBROUTINE SHOWREG
   use DATSUB
   use DATMAI
   IMPLICIT NONE
   INTEGER I
   IF(STI.EQ.1) THEN
      OUTLYNE='"SHOW" SHOWS A REGISTER VALUE'
      CALL SHOWIT(1)
      OUTLYNE='OR SHOWS A "GOTTEN" PARAMETER VALUE'
      CALL SHOWIT(1)
      OUTLYNE='IF A QUALIFIER WORD IS INCLUDED'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(S1.EQ.0) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & '"SHOW" WITHOUT A QUALIFIER REQUIRES EXPLICIT NUMERIC WORD #1'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & '"SHOW" WITHOUT A QUALIFIER ONLY TAKES NUMERIC WORD #1 AND #2'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(SST.EQ.1) THEN
      CALL REPORT_ERROR_AND_FAIL('"SHOW" TAKES NO ALPHANUMERIC INPUT'//'\n'//'RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(W1.LE.0.0D0.OR.INT(W1).GT.MAXREG) THEN
      OUTLYNE='FOR "SHOW" WITH NO QUALIFIER WORD INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'NUMERIC WORD #1 MUST LIE BETWEEN 1 AND ',MAXREG
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(S2.EQ.1.AND.W2.LE.0.0D0.OR.S2.EQ.1.AND.INT(W2).GT.MAXREG) THEN
      OUTLYNE='FOR "SHOW" WITH NO QUALIFIER WORD INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'NUMERIC WORD #2 MUST LIE BETWEEN 1 AND ',MAXREG
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
   IF(S2.EQ.1.AND.W2.LT.W1) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & 'FOR "SHOW" WITH NO QUALIFIER WORD INPUT'//'\n'//&
      & 'NUMERIC WORD #2 MUST BE GREATER THAN OR EQUAL TO NUMERIC WORD #1'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   END IF
!     INPUT OK
   IF(S2.EQ.0) W2=W1
!
10 FORMAT('GENERAL PURPOSE REGISTER # ',I6,' = ',D23.15)
   DO I=INT(W1),INT(W2)
      WRITE(OUTLYNE,10)I,GPREG(I)
      CALL SHOWIT(0)
   END DO
   RETURN
END
