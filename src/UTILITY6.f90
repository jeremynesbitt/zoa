SUBROUTINE CHADIR
!
   use DATMAI
   IMPLICIT NONE
!
   CHARACTER DIRNAMM*80
!
!       THIS IS SUBROUTINE CHADIR. IT IS USED TO CHANGE THE NAME
!       DIRECTORY OF THE LENS LIBRARY, MACRO LIBRARY, TRASMISSION FILES
!       AND PLOT LIBRARY.
!
!               COMANDS ARE LENDIR, MACDIR (CHGMAC), TRADIR AND PLTDIR
!
!               IF THE DIRECORIES DONT EXIST, THEY ARE CREATED
!               THE NEW NAMES ARE STORED IN THE VARIABLES
!
!                   LIBLEN,LIBMAC,LIBTRA AND LIBPLO
!
!
!               CHECK FOR PRESENCE OF QUALIFIER OR NUMERIC WORDS
!               PRINT ERROR AND RETURN IF DISCOVERED.
!
!               WE ARE AT LENS INPUT OR LENS UPDATE LEVEL
   IF(STI.EQ.1) THEN
      IF(WC.EQ.'LENDIR') THEN
         WRITE(OUTLYNE,10) LIBLEN
         CALL SHOWIT(0)
      ELSE
      END IF
      IF(WC.EQ.'MACDIR'.OR.WC.EQ.'CHGMAC') THEN
         WRITE(OUTLYNE,20) LIBMAC
         CALL SHOWIT(0)
      ELSE
      END IF
      IF(WC.EQ.'TRADIR') THEN
         WRITE(OUTLYNE,30) LIBTRA
         CALL SHOWIT(0)
      ELSE
      END IF
      IF(WC.EQ.'PLTDIR') THEN
         WRITE(OUTLYNE,40) LIBPLO
         CALL SHOWIT(0)
      ELSE
      END IF
      RETURN
   ELSE
   END IF
   IF(SST.EQ.1.OR.SN.EQ.1)THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & '"'//WC(1:6)//'" TAKES ONLY QUALIFIER INPUT'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   ELSE
   END IF
   IF(SQ.EQ.0) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & '"'//WC(1:6)//'" REQUIRES EXPLICIT QUALIFIER INPUT'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   ELSE
   END IF
   IF(WQ(1:1).EQ.' '.OR.WQ(2:2).EQ.' '.OR.WQ(3:3).EQ.' '.OR.WQ(4:4).EQ.' '.OR.WQ(5:5).EQ.' '.OR.WQ(6:6).EQ.' ') THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & 'NEW DIRECTORY NAME MUST BE EXACTLY 6 NON-BLANK CHARACTERS'//'\n'//&
      & 'IN LENGTH'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   ELSE
   END IF
   IF(WQ(7:7).NE.' '.OR.WQ(8:8).NE.' ') THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & 'NEW DIRECTORY NAME MAY BE ONLY 6 NON-BLANK CHARACTERS'//'\n'//&
      & 'IN LENGTH'//'\n'//&
      & 'RE-ENTER COMMAND', 1)
      RETURN
   ELSE
   END IF
!       INPUT OK
   IF(WC.EQ.'LENDIR') LIBLEN = WQ(1:6)//'\'
   IF(WC.EQ.'MACDIR'.OR.WC.EQ.'CHGMAC') LIBMAC = WQ(1:6)//'\'
   IF(WC.EQ.'TRADIR') LIBTRA = WQ(1:6)//'\'
   IF(WC.EQ.'PLTDIR') LIBPLO = WQ(1:6)//'\'
!     DO THE DIRECTORIES EXIST ? IF NOT CREATE THEM
!
   DIRNAMM=TRIM(WQ)
! WINTER
!        CALL MY_MKDIR(DIRNAMM)
   IF(WC.EQ.'LENDIR') THEN
      WRITE(OUTLYNE,10) LIBLEN
      CALL SHOWIT(0)
   ELSE
   END IF
   IF(WC.EQ.'MACDIR'.OR.WC.EQ.'CHGMAC') THEN
      WRITE(OUTLYNE,20) LIBMAC
      CALL SHOWIT(0)
   ELSE
   END IF
   IF(WC.EQ.'TRADIR') THEN
      WRITE(OUTLYNE,30) LIBTRA
      CALL SHOWIT(0)
   ELSE
   END IF
   IF(WC.EQ.'PLTDIR') THEN
      WRITE(OUTLYNE,40) LIBPLO
      CALL SHOWIT(0)
   ELSE
   END IF
!
   RETURN
10 FORMAT('CURRENT LENS LIBRARY DIRECTORY IS (',A7,')')
20 FORMAT('CURRENT MACRO LIBRARY DIRECTORY IS (',A7,')')
30 FORMAT('CURRENT TRANSMISSION FILE DIRECTORY IS (',A7,')')
40 FORMAT('CURRENT PLOT LIBRARY DIRECTORY IS (',A7,')')
END
