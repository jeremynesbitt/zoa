SUBROUTINE DODODO
!
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS CALLED TO DO THE "DO" STATEMENT
   CHARACTER AAW1*23,DOWQ*8
!
   INTEGER I
!
!
   IF(SST.EQ.1) THEN
      WRITE(OUTLYNE,*)'"DO" TAKES NO STRING INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
   IF(STI.EQ.1) THEN
      WRITE(OUTLYNE,*)'NO ADDITIONAL INFORMATION'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
   IF(S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
      WRITE(OUTLYNE,*)'"DO" TAKES NO NUMERIC WORD #1, #2 AND #3 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
   IF(SQ.EQ.0) THEN
      WRITE(OUTLYNE,*)'"DO" REQUIRES EXPLICIT QUALIFIER INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(S1.EQ.0) THEN
      WRITE(OUTLYNE,*)'"DO" REQUIRES EXPLICIT NUMERIC WORD #1 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(S2.EQ.0) THEN
      WRITE(OUTLYNE,*)'"DO" REQUIRES EXPLICIT NUMERIC WORD #2 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
! DO THE DO OPERATION
   DOWQ=WQ
!
   DO I=INT(W1),INT(W2)
!     NOTE:THIS WILL TAKE ANY QUALIFIER WORD
      CALL DONTOA(DBLE(I),AAW1)
      INPUT(1:80)=DOWQ(1:8)//','//AAW1(1:23)//',,,,,'
      CALL PROCES
   END DO
!
   RETURN
END
