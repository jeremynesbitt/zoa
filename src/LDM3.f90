!
!       FIRST PRINT SPIDER DATA.
!
!                       DO PRINTING NOW
!*****************************************************************
!
         WRITE(OUTLYNE,200) SURF,&
         &INT(ALENS(135,SURF)),ALENS(136,SURF),ALENS(137,SURF)
         CALL SHOWIT(0)
15       CONTINUE
      END DO
!
   END IF
!
200 FORMAT(I3,2X,I4,3X,G15.8,1X,G15.8)
!
