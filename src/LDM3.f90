!
      IF(ALENS(134,SURF).NE.0.0D0) THEN
         SPID=1
      ELSE
         SPID=0
      END IF
!
!       FIRST PRINT SPIDER DATA.
!
!               HANDEL NO DATA FOR A SURFACE
      IF(SPID.EQ.0) THEN
         WRITE(OUTLYNE,100) SURF
         CALL SHOWIT(0)
         RETURN
      END IF
!
!                       DO PRINTING NOW
!*****************************************************************
!
!
!
      IF(ALENS(134,SURF).NE.0.0D0) THEN
         IF(HEADIN) WRITE(OUTLYNE,3000)
