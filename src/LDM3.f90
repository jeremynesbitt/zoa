SUBROUTINE SPL23(AR,AX,AY,AZ,IFUNC)
!
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
!     MAIN CUBIC SPLINE SURFACE ROUTINE
!
   REAL*8 XV,XARAY(1:96),YARAY(1:96),&
   &AX,AY,AZ,SPARRAY,INCRM,ARHO
!
   INTEGER AR,IFUNC,ISURF,ALLOERR,I,NV
!
   DIMENSION SPARRAY(1:96)
!
!
   REAL*8 SPL23A
   DIMENSION SPL23A(:,:)
   ALLOCATABLE :: SPL23A
!
   NV=INT(FTFL01(1,AR))
   ISURF=AR
!
!     IF IFUNC = 1, JUST DEALLOCATE THE SPL23 ARRAYS. THIS HAPPENS
!     WHENEVER LENNS IS CALLED. THIS FREES MEMORY
!
!     IF IFUNC = 3, INTERPOLATE A SPLINE TO GET A SAG VALUE
!
   IF(IFUNC.EQ.1) THEN
      DEALLOCATE(SPL23A,STAT=ALLOERR)
      RETURN
   END IF
   IF(IFUNC.EQ.3) THEN
!     ALLOCATE THE SPL23 ARRAY FOR THIS SURFACE AND LOAD IT
      DEALLOCATE(SPL23A,STAT=ALLOERR)
      ALLOCATE(SPL23A(1:NV,1:3),STAT=ALLOERR)
      IF(FTFL01(2,AR).EQ.0.0D0) THEN
         INCRM=ALENS(76,ISURF/(DABS(FTFL01(1,ISURF))-1.0D0))
      ELSE
         INCRM=DABS(FTFL01(2,ISURF)/(DABS(FTFL01(1,ISURF))-1.0D0))
      END IF
      XV=0.0D0
      NV=INT(FTFL01(1,ISURF))
      DO I=1,NV
         XARAY(I)=XV
         YARAY(I)=FTFL01(I+2,ISURF)
         XV=XV+INCRM
      END DO
      CALL SPLINE(XARAY,YARAY,NV,1.0D35,1.0D35,SPARRAY)
      NV=INT(FTFL01(1,ISURF))
      ARHO=DSQRT((AX**2)+(AY**2))
      CALL SPLINT(XARAY,YARAY,SPARRAY,NV,ARHO,AZ)
   END IF
   DEALLOCATE(SPL23A,STAT=ALLOERR)
   RETURN
END
