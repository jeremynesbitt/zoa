!
   END IF
!
200 FORMAT(I3,2X,A5,3X,1X,G15.8,1X,G15.8,1X,G15.8)
!
100 FORMAT('SURF',1X,I3,1X,&
   &':NO USER-DEFINED PIVOT POINT DATA')
110 FORMAT('NO USER-DEFINED PIVOT POINT DATA')
1000 FORMAT(&
   &'USER DEFINED PIVOT POINT DATA')
