!       THIRD FILE FOR LENS DATABASE MANAGER FILES

! SUB STAD.FOR
SUBROUTINE STAD
!
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE STAD WHICH IMPLEMENTS THE TAD
!       COMMAND AT THE CMD LEVEL
!       THE ONLY VALID QUALIFIERS ARE (BLANK) AND "ALL","OB" AND "OBJ"
!       THE NUMERIC ENTRY W1, IS THE SURFACE DESIGNATOR
!       FOR PRINTOUT OF ONE SURFACE'S DATA ONLY
!
   CHARACTER TYPE1*5,TYPE2*3

!
   INTEGER TCNT,DCNT,TAD,DEC,TADCNT
!
!
!               THIS IS A CMD LEVEL COMMAND ONLY
!
!               CHECK FOR ADDITIONAL INPUT AND
!               PRINT ERROR AND RETURN IF DISCOVERED.
!
   IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
!
      OUTLYNE=&
      &'"TAD" ONLY TAKES QUALIFIER OR NUMERIC WORD #1 INPUT'
      CALL SHOWIT(1)
      OUTLYNE='RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(SQ.EQ.1.AND.S1.EQ.1) THEN
!
      OUTLYNE=&
      &'"TAD" TAKES EITHER QUALIFIER OR NUMERIC WORD #1 INPUT'
      CALL SHOWIT(1)
      OUTLYNE='BUT NOT BOTH'
      CALL SHOWIT(1)
      OUTLYNE='RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       WHAT IF NO SURFACES EXIST
   IF(SYSTEM(20).EQ.0.0D0) THEN
      OUTLYNE='NO TILTS OR DECENTERS EXIST'
      CALL SHOWIT(1)
      OUTLYNE='LENS SYSTEM HAS NO SURFACES'
      CALL SHOWIT(1)
      OUTLYNE='RE-ENTER COMMAND'
      CALL SHOWIT(1)
      RETURN
   END IF
!
!       W1 DESIGNATES THE SURFACE FOR WHICH THE TAD
!       DATA IS TO BE OUTPUT.
!       IF THE QUALIFIER "ALL" IS USED, THEN THE TILT/DEC DATA FOR
!       THE ENTIRE LENS IS PRINTED. IF THE QUALIFIER IS "OB" OR "OBJ"
!       THEN DATA FOR THE OBJECT SURFACE IS PRINTED.
!
   IF(WQ.EQ.'OB'.OR.WQ.EQ.'OBJ') THEN
!
!       THIS IS SAME AS TAD,0
      S1=1
      DF1=0
      SQ=0
      WQ='        '
      W1=0.0
   ELSE
!       WQ NOT OB OR OBJ
   END IF
   IF(SQ.EQ.1.AND.WQ.NE.'ALL') THEN
!
!       WE HAVE INVALID QUALIFIER
      OUTLYNE='INVALID QUALIFIER INPUT'
      CALL SHOWIT(1)
      OUTLYNE='RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(SQ.EQ.0) THEN
!       HANDEL AN INDIVIDUAL SURFACE INCLUDING "OB" AND "OBJ"
      IF(DF1.EQ.1) W1=DBLE(INT(SYSTEM(20)))
      SURF=INT(W1)
      IF(SURF.GT.(INT(SYSTEM(20))).OR.SURF.LT.0) THEN
         OUTLYNE='SURFACE NUMBER BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
! SET DEC AND TAD APPROPRIATELY
!
      IF(ALENS(25,SURF).NE.0.0D0) THEN
         TAD=1
      ELSE
         TAD=0
      END IF
      IF(ALENS(29,SURF).NE.0.0D0) THEN
         DEC=1
      ELSE
         DEC=0
      END IF
!
!       FIRST PRINT TAD DATA. ONLY ONE KIND OF TAD CAN EXITS
!       ON A SURFACE
!
!               HANDEL NO DATA FOR A SURFACE
      IF(TAD.EQ.0.AND.DEC.EQ.0) THEN
         WRITE(OUTLYNE,100) SURF
         CALL SHOWIT(0)
         RETURN
      END IF
!
!                       DO PRINTING NOW
!*****************************************************************
!
!
!
      IF(ALENS(25,SURF).NE.0.0D0) THEN
         IF(ALENS(25,SURF).EQ.1.0.AND.ALENS(77,SURF).EQ.0.0D0) THEN
            TYPE1 =' TILT'
            TYPE2 ='   '
         END IF
         IF(ALENS(25,SURF).EQ.-1.0) THEN
            TYPE1 ='RTILT'
            TYPE2 ='   '
         END IF
         IF(ALENS(25,SURF).EQ.2.0) THEN
            TYPE1 =' TILT'
            TYPE2 ='  A'
         END IF
         IF(ALENS(25,SURF).EQ.3.0) THEN
            TYPE1 =' TILT'
            TYPE2 =' AM'
         END IF
         IF(ALENS(25,SURF).EQ.4.0) THEN
            TYPE1 =' TILT'
            TYPE2 ='BEN'
         END IF
         IF(ALENS(25,SURF).EQ.5.0) THEN
            TYPE1 =' TILT'
            TYPE2 ='DAR'
         END IF
         IF(ALENS(25,SURF).EQ.7.0) THEN
            TYPE1 =' TILT'
            TYPE2 ='REV'
         END IF
         IF(ALENS(25,SURF).EQ.6.0.OR.ALENS(25,SURF).EQ.1.0D0.AND.&
         &ALENS(77,SURF).EQ.1.0D0) THEN
            TYPE1 =' TILT'
            TYPE2 ='RET'
         END IF
         IF(HEADIN) WRITE(OUTLYNE,2000)
         IF(HEADIN) CALL SHOWIT(0)
         IF(ALENS(25,SURF).EQ.6.0D0.OR.ALENS(25,SURF).EQ.1.0D0.AND.&
         &ALENS(77,SURF).EQ.1.0D0) THEN
            WRITE(OUTLYNE,2001) SURF,TYPE1,TYPE2,&
            &ALENS(26,SURF),ALENS(27,SURF),ALENS(28,SURF),&
            &INT(ALENS(70,SURF))
         ELSE
            WRITE(OUTLYNE,200) SURF,TYPE1,TYPE2,&
            &ALENS(26,SURF),ALENS(27,SURF),ALENS(28,SURF)
         END IF
         CALL SHOWIT(0)
      END IF
      IF(ALENS(29,SURF).NE.0.0D0) THEN
         TYPE1='  DEC'
         TYPE2='   '
!      WRITE(OUTLYNE,1500)
!      CALL SHOWIT(0)
         IF(HEADIN) WRITE(OUTLYNE,3000)
         IF(HEADIN) CALL SHOWIT(0)
         WRITE(OUTLYNE,200) SURF,TYPE1,TYPE2,ALENS(30,SURF),ALENS(31,SURF),&
         &ALENS(69,SURF)
         CALL SHOWIT(0)
      END IF
!     GLOBAL DECENTERS AND TILTS
      IF(ALENS(90,SURF).NE.0.0D0.OR.ALENS(91,SURF).NE.0.0D0.OR.&
      &ALENS(92,SURF).NE.0.0D0.OR.ALENS(93,SURF).NE.0.0D0.OR.&
      &ALENS(94,SURF).NE.0.0D0.OR.ALENS(95,SURF).NE.0.0D0) THEN
         WRITE(OUTLYNE,9000) SURF
         CALL SHOWIT(0)
         WRITE(OUTLYNE,9001) ALENS(90,SURF)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,9002) ALENS(91,SURF)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,9003) ALENS(92,SURF)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,9004) ALENS(93,SURF)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,9005) ALENS(94,SURF)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,9006) ALENS(95,SURF)
         CALL SHOWIT(0)
      END IF
      RETURN
   ELSE
!*********************************************************************
!       SQ=1 AND ONLY ALLOWED WQ BY NOW IS "ALL"
!       HANDEL THE WHOLE LENS
!       PRINT HEADING DATA
!
!       SET THE TILT COUNTER. IT IS USED TO DETERMINE
!       THE CASE OF NO TILTS OR DECENTERS IN A LENS
      TADCNT=0
      DCNT=0
      TCNT=0
      DO SURF=0,INT(SYSTEM(20))
         IF(ALENS(25,SURF).NE.0.0.AND.ALENS(29,SURF).NE.0.0 &
         &.OR.ALENS(25,SURF).NE.0.0.OR.ALENS(29,SURF)&
         &.NE.0.0D0) THEN
            IF(ALENS(25,SURF).NE.0.0D0) TCNT=TCNT+1
            IF(ALENS(29,SURF).NE.0.0D0) DCNT=DCNT+1
            TADCNT=TADCNT+1
         ELSE
         END IF
      END DO
      IF(TADCNT.EQ.0) THEN
         WRITE(OUTLYNE,110)
         CALL SHOWIT(0)
         RETURN
      END IF
      WRITE(OUTLYNE,1500)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2000)
      CALL SHOWIT(0)
      DO SURF=0,INT(SYSTEM(20))
!
! SET DEC AND TAD APPROPRIATELY
!
         IF(ALENS(25,SURF).NE.0.0D0) THEN
            TAD=1
         ELSE
            TAD=0
         END IF
         IF(TAD.EQ.0) GO TO 15
!
!       SET THE TYPE1 AND TYPE2 TYPES CORRECTLY
!
         IF(ALENS(25,SURF).EQ.1.0.AND.ALENS(77,SURF).EQ.0.0D0) THEN
            TYPE1 =' TILT'
            TYPE2 ='   '
         END IF
         IF(ALENS(25,SURF).EQ.-1.0) THEN
            TYPE1 ='RTILT'
            TYPE2 ='   '
         END IF
         IF(ALENS(25,SURF).EQ.2.0) THEN
            TYPE1 =' TILT'
            TYPE2 ='  A'
         END IF
         IF(ALENS(25,SURF).EQ.3.0) THEN
            TYPE1 =' TILT'
            TYPE2 =' AM'
         END IF
         IF(ALENS(25,SURF).EQ.4.0) THEN
            TYPE1 =' TILT'
            TYPE2 ='BEN'
         END IF
         IF(ALENS(25,SURF).EQ.5.0) THEN
            TYPE1 =' TILT'
            TYPE2 ='DAR'
         END IF
         IF(ALENS(25,SURF).EQ.7.0) THEN
            TYPE1 =' TILT'
            TYPE2 ='REV'
         END IF
         IF(ALENS(25,SURF).EQ.6.0.OR.ALENS(25,SURF).EQ.1.0D0.AND.&
         &ALENS(77,SURF).EQ.1.0D0) THEN
            TYPE1 =' TILT'
            TYPE2 ='RET'
         END IF
!
!       FIRST PRINT TAD DATA. ONLY ONE KIND OF TAD CAN EXITS
!       ON A SURFACE
!
!                       DO PRINTING NOW
!*****************************************************************
!
         IF(ALENS(25,SURF).EQ.6.0.OR.ALENS(25,SURF).EQ.1.0D0.AND.&
         &ALENS(77,SURF).EQ.1.0D0) THEN
            WRITE(OUTLYNE,2001) SURF,TYPE1,TYPE2 &
            &,ALENS(26,SURF),ALENS(27,SURF),ALENS(28,SURF),&
            &INT(ALENS(70,SURF))
         ELSE
            WRITE(OUTLYNE,200) SURF,TYPE1,TYPE2 &
            &,ALENS(26,SURF),ALENS(27,SURF),ALENS(28,SURF)
         END IF
         CALL SHOWIT(0)
15       CONTINUE
      END DO
!
!     DECENTERS
      IF(DCNT.NE.0) THEN
         WRITE(OUTLYNE,1500)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,3000)
         CALL SHOWIT(0)
!
         DO SURF=0,INT(SYSTEM(20))
!
! SET DEC
!
            IF(ALENS(29,SURF).NE.0.0D0) THEN
               DEC=1
            ELSE
               DEC=0
            END IF
!
!
            IF(DEC.EQ.1) THEN
!       YOU GOT HERE BECAUSE DEC=1 NOT BECAUSE TAD=1
               TYPE1='  DEC'
               TYPE2='   '
            END IF
!
!       FIRST PRINT TAD DATA. ONLY ONE KIND OF TAD CAN EXITS
!       ON A SURFACE
!
!               HANDEL NO DATA FOR A SURFACE
            IF(DEC.EQ.0) THEN
!       JUMP TO THE CONTINUE STATEMENT AND DO THE NEXT SURFACE
               GO TO 16
            ELSE
!       PROCEED
            END IF
!
!                       DO PRINTING NOW
!*****************************************************************
!
            WRITE(OUTLYNE,200) SURF,TYPE1,TYPE2 &
            &,ALENS(30,SURF),ALENS(31,SURF)&
            &,ALENS(69,SURF)
            CALL SHOWIT(0)
!
16          CONTINUE
         END DO
      END IF
      DO SURF=0,INT(SYSTEM(20))
!     GLOBAL DECENTERS AND TILTS
         IF(ALENS(90,SURF).NE.0.0D0.OR.ALENS(91,SURF).NE.0.0D0.OR.&
         &ALENS(92,SURF).NE.0.0D0.OR.ALENS(93,SURF).NE.0.0D0.OR.&
         &ALENS(94,SURF).NE.0.0D0.OR.ALENS(95,SURF).NE.0.0D0) THEN
            WRITE(OUTLYNE,9000) SURF
            CALL SHOWIT(0)
            WRITE(OUTLYNE,9001) ALENS(90,SURF)
            CALL SHOWIT(0)
            WRITE(OUTLYNE,9002) ALENS(91,SURF)
            CALL SHOWIT(0)
            WRITE(OUTLYNE,9003) ALENS(92,SURF)
            CALL SHOWIT(0)
            WRITE(OUTLYNE,9004) ALENS(93,SURF)
            CALL SHOWIT(0)
            WRITE(OUTLYNE,9005) ALENS(94,SURF)
            CALL SHOWIT(0)
            WRITE(OUTLYNE,9006) ALENS(95,SURF)
            CALL SHOWIT(0)
            WRITE(OUTLYNE,1500)
            CALL SHOWIT(0)
         END IF
      END DO
   END IF
!
200 FORMAT(I3,1X,A5,1X,A3,1X,G15.8,1X,G15.8,1X,G15.8)
2001 FORMAT(I3,1X,A5,1X,A3,1X,G15.8,1X,G15.8,1X,G15.8,5X,I3)
!
100 FORMAT('SURF',1X,I3,1X,&
   &':NO TILT AND DECENTER DATA')
110 FORMAT('NO TILT AND DECENTER DATA')
2000 FORMAT('SURF',1X,'TYPE',9X,'ALPHA',10X,'BETA',12X,'GAMMA'&
   &,9X,'RETURN SURF ')
3000 FORMAT('SURF',1X,'TYPE',8X,'  YD ',12X,' XD ',11X,'  ZD ')
1500 FORMAT(1X)
9000 FORMAT('SURFACE NUMBER = ',I3)
9001 FORMAT('GLOBAL X-DECENTRATION (GDX)    = ',G15.8)
9002 FORMAT('GLOBAL Y-DECENTRATION (GDY)    = ',G15.8)
9003 FORMAT('GLOBAL Z-DECENTRATION (GDZ)    = ',G15.8)
9004 FORMAT('GLOBAL ALPHA TILT     (GALPHA) = ',G15.8)
9005 FORMAT('GLOBAL BETA TILT      (GBETA)  = ',G15.8)
9006 FORMAT('GLOBAL GAMMA TILT     (GGAMMA) = ',G15.8)
   RETURN
END
