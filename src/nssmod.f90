!       CURRENT REAL INDEX VALUES
   real(real64) NSS_N,NSS_NP
!
!       OPL AND RAYLENGTH
   real(real64) NSS_OPL,NSS_RAYLENGTH
!
!       RAY HISTORY NUMBER TAG
   INTEGER RHISTN
!
!       STATS
   real(real64) NSSMEANX
   real(real64) NSSMEANY
   real(real64) NSSMEANR
   real(real64) NSSRMSX
   real(real64) NSSRMSY
   real(real64) NSSRMSR
!
!       SUPPORT FOR LINE CONNECTING OBJ CENTER AND REF CENTER
   real(real64) OMAG,OBJL,OBJM,OBJN,OALPHA,OBETA,OGAMMA
!
!       SUPPORT 200 LEVEL SURFACE POSITION NESTING
   INTEGER NSSNEST(0:200),INEST
!
   real(real64) XMEMCOR(1:4),YMEMCOR(1:4)
!
END MODULE NSSMOD
