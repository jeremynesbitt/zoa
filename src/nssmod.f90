   real(real64) GRRX,GRRY,GRRZ,GRRL,GRRM,GRRN
   real(real64) GXRRL,GXRRM,GXRRN
   real(real64) GYRRL,GYRRM,GYRRN
!     LOCAL RAY DATA BEFORE SURFACE SEARCH
   real(real64) LRRX,LRRY,LRRZ,LRRL,LRRM,LRRN
   real(real64) LXRRL,LXRRM,LXRRN
   real(real64) LYRRL,LYRRM,LYRRN
