   real(real64) RRX,RRY,RRZ,RRL,RRM,RRN,NSSLN,NSSMN,NSSNN
   real(real64) NLN,NMN,NNN
   real(real64) XRRL,XRRM,XRRN
   real(real64) YRRL,YRRM,YRRN
   LOGICAL NSSRAYFAIL
   INTEGER NSSRAYFAILCODE(1:2)
!       1 IS THE FAILED SURFACE
!       2 IS FAILURE CODE
!               1. INTERACTION FAILURE
