   real(real64) CURWAVELENGTH,CURINDEXR,CURINDEXI,PREVINDEXR,PREVINDEXI
!
!       RAY TRAVEL DIRECTION 1= FROM MEDIA1 TO 2, 2= MEDIA2 TO 1
   INTEGER TRAVEL
!
!       EVEN OR ODD GRIDS
   LOGICAL OBJODD,REFODD
!
!       SAG ERROR FLAG
   LOGICAL NSSSAGERR
!
!       NSS SCALE FACTOR
   real(real64) NSSSCFA,NSSPSIZP
   real(real64) NSSSCFAP,NSSPSIZ
   LOGICAL NSSPLSZ,NSSPLSC
