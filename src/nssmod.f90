   CHARACTER*13, TO_MEDA,TO_MEDB,FROM_MEDA,FROM_MEDB
!
   LOGICAL NSS_INTERSECT
!
   real(real64) NSSWAVL,NSSWVWT,REALINDEX1,REALINDEX2,IMAGINDEX1
   real(real64) IMAGINDEX2
   INTEGER NSSWAVNUM
!
   real(real64) RRX0,RRY0,RRZ0,RRL0,RRM0,RRN0
   real(real64) XRRL0,XRRM0,XRRN0
   real(real64) YRRL0,YRRM0,YRRN0
