   INTEGER NSSRAY_HITS
!
   LOGICAL NSSRAY_EXIST
!
!       SET TO 50 BUT ONLY USE 16 AS OF 4/2004
!       START IS THE ORIGINAL STARTING RAY DATA
   real(real64) START(1:50)
!
!     GRAY IS THE RAY DATA IN GLOBAL COORDINATES BEFORE SURFACE INTERACTION
   real(real64) NSS_GRAY(1:50)
!     LRAY IS THE RAY DATA IN LOCAL COORDINATES BEFORE SURFACE INTERACTION
   real(real64) NSS_LRAY(1:50)
!     GRAYP IS THE RAY DATA IN GLOBAL COORDINATES AFTER SURFACE INTERACTION
   real(real64) NSS_GRAYP(1:50)
!     LRAYP IS THE RAY DATA IN LOCAL COORDINATES AFTER SURFACE INTERACTION
   real(real64) NSS_LRAYP(1:50)
!
!     OGRAY IS THE PREVIOUS RAY DATA IN GLOBAL COORDINATES BEFORE SURFACE INTERACTION
   real(real64) NSS_OGRAY(1:50)
!     OLRAY IS THE PREVIOUS RAY DATA IN LOCAL COORDINATES BEFORE SURFACE INTERACTION
   real(real64) NSS_OLRAY(1:50)
!     OGRAYP IS THE PREVIOUS RAY DATA IN GLOBAL COORDINATES AFTER SURFACE INTERACTION
   real(real64) NSS_OGRAYP(1:50)
!     OLRAYP IS THE PREVIOUS RAY DATA IN LOCAL COORDINATES AFTER SURFACE INTERACTION
   real(real64) NSS_OLRAYP(1:50)
