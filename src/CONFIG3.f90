            RETURN
         END IF
!
!       HERE IS WERE THE CHANGES OCCUR
!       CHANGE TO CFG F12
!
!     NEXT COPY THE PERMANENT LENS TO THE ACTIVE LENS ARRAYS
!
         LI=LIP
         LLTYPE=LLTYPEP
         INNI=INNIP
         LIC=LICP
         SYSTEM=SYSP
         ALENS=ALENP
         PIKP=PIKUP
         SOLVE=SLVP
         FTFL01=FT01P
         GLANAM=GLANMP
!
         IF(F12.NE.1) THEN
!       NOW THE ORIGINAL PERMANENT LENS IS IN
!       WORKING LENS STORAGE
!
!       NOW THE MAGIC. FOR THE PARTICULAR CFG
!       DESIGNATED BY THE CURRENT VALE OF FLAG F12.
!       DURING THIS PROCESS, FLAG F15 IS SET TO 1
!       TO HELP THE UPDATE LENS AND UPDATE SPSRF
!       LEVELS KNOW HOW TO PROERLY RESPOND
            F15=1
!       READ THE CONTENTS OF
!               CONFIG(F12,I) FROM I=1 TO I=CFGCNT(F12)
!       AND ONE BY ONE RUN THE STRING VALUES FOUND BACK
!       THROUGH THE PROCESS SUBROUTINE
            CFCH=.TRUE.
            DO I=1,CFGCNT(F12)
               EE12=CONFG(F12,I)
               DUMPY=EE12
               CALL CFCHG1
            END DO
            CFCH=.FALSE.
!
!       PROCESS DONE, SET F15 TO 0
            F15=0
         END IF
      ELSE
         OUTLYNE='CONFIGURATION NUMBER BEYOND LEGAL RANGE'
         IF(.NOT.CFGQUIET) CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         IF(.NOT.CFGQUIET) CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
   END IF
   RETURN
END
! SUB DEZOOM.FOR
SUBROUTINE DEZOOM
!
   use DATCFG
   use DATLEN
   use mod_system
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDELS THE BEHAVIOR OF THE DEZOOM
!       COMMAND FROM THE CMD LEVEL
!
!       DEZOOM CHANGES THE DESIGNATED CONFIGURATION
!       INTO THE MAIN PERMANENT LENS AND THE DELETES
!       ALL CONFIGURATION DATA AND SETS CFG = 1
!
   CHARACTER DUMPY*140,BLANK*140
!
   COMMON/LUMPY/DUMPY
!
   COMMON/BLAAA/BLANK
!
   INTEGER I,KI,J,K
!
!
!       DEZOOM TAKES ONLY NUMERIC INPUT
!
   BLANK=AA//AA//AA//AA//AA//AA//AA
   IF(SST.EQ.1.OR.SQ.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1 .OR.S5.EQ.1) THEN
      WRITE(OUTLYNE,*)'"DEZOOM" ONLY TAKES NUMERIC WORD #1 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(WC.EQ.'DEZOOM'.AND.DF1.EQ.1) THEN
      WRITE(OUTLYNE,*)'"DEZOOM" REQUIRES EXPLICIT NUMERIC WORD #1 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(WC.EQ.'DEZOOM'.AND.S1.EQ.1) THEN
!
!       HERE GOES
!
      IF(W1.GE.1.0.AND.W1.LE.DBLE(MAXCFG)) THEN
!       VALID CFG NUMBER
!
         IF(CFGCNT(INT(W1)).EQ.0.AND.INT(W1).NE.1) THEN
!       CFG DESIGNATED HAS NO DATA, NODEZOOM
            WRITE(OUTLYNE,*)'NO CONFIGS DATA DEFINED FOR CFG ',INT(W1)
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'NO DEZOOMING PERFORMED'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
!
         IF(INT(W1).NE.1) THEN
            WRITE(OUTLYNE,*)'DEZOOMING FROM CFG NUMBER',INT(W1)
            CALL SHOWIT(1)
         END IF
!
         IF(INT(W1).EQ.1) THEN
            WRITE(OUTLYNE,*)'DEZOOMING FROM CFG #1 IS MEANINGLESS'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
!       FIRST COPY THE PERMANENT LENS TO THE ACTIVE LENS ARRAYS
!
         LI=LIP
         LLTYPE=LLTYPEP
         INNI=INNIP
         LIC=LICP
         SYSTEM=SYSP
         ALENP=ALENS
         PIKUP=PIKP
         SLVP=SOLVE
         FT01P=FTFL01
         GLANMP=GLANAM
         LBL=PLBL
!
!       NOW CHANGE TO THE CFG INDICATED BY INT(W1)
!
         F12=INT(W1)
!
!       NOW THE MAGIC. FOR THE PARTICULAR CFG
!       DESIGNATED BY THE CURRENT VALE OF FLAG F12.
!       DURING THIS PROCESS, FLAG F15 IS SET TO 1
!       TO HELP THE UPDATE LENS AND UPDATE SPSRF
!       LEVELS KNOW HOW TO PROERLY RESPOND
         F15=1
!       READ THE CONTENTS OF
!               CONFG(F12,I)
!       FROM I=1 TO I=CFGCNT(F12)
!       AND ONE BY ONE RUN THE STRING VALUES FOUND BACK
!       THROUGH THE PROCESS SUBROUTINE
         DO 14 I=1,CFGCNT(F12)
            EE12=CONFG(F12,I)
            DUMPY=EE12
            CALL CFCHG1
14       CONTINUE
!
!       PROCESS DONE, SET F15 TO 0
         F15=0
!
!       NOW SET F12 TO 1 AND DELETE ALL THE CONFIGS DATA
         F12=1
         CFGCNT(2:MAXCFG)=0
!
!       NOW COPY THIS INTO THE PERM DATA
!
         CALL CTOP
         LNSTYP=1
         CALL LNSEOS
         IF(F28.EQ.0) CALL VCHECK
!
      ELSE
         WRITE(OUTLYNE,*)'CONFIGURATION NUMBER BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
   END IF
   RETURN
END
! SUB DELCFG.FOR.FOR
SUBROUTINE DELCFG
!
   use DATCFG
   use DATLEN
   use mod_system
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS CALLED FROM THE CMD LEVEL
!       IT IS USED TO DELETE THE CONFIGURATION DATA
!       DESIGNATER BY THE CONFIGURATION NUMBER INT(W1)
!
!       CURRENT CONFIGURATION NUMBER IS NOT AFFECTED
!
   CHARACTER BLANK*140
!
   COMMON/BLAAA/BLANK
!
   INTEGER J
!
!
   IF(SQ.EQ.1.OR.SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1 .OR.S5.EQ.1) THEN
      WRITE(OUTLYNE,*)'"DELCFG" ONLY TAKES NUMERIC WORD #1 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       DEFAULT NUMERIC INPUT
   IF(DF1.EQ.1) THEN
      WRITE(OUTLYNE,*)'"DELCFG" MUST HAVE EXPLICIT NUMERIC INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       CFG OUT OF RANGE
   IF(INT(W1).LT.2.OR.INT(W1).GT.MAXCFG) THEN
      WRITE(OUTLYNE,*)'NUMERIC INPUT BEYOND LEGAL RANGE'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'VALID CFG NUMBERS ARE 2 TO ',MAXCFG
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(INT(W1).GE.2.AND.INT(W1).LE.MAXCFG) THEN
!       VALID ADDRESS
!       CASE OF EMPTY CFG
      IF(CFGCNT(INT(W1)).LT.1) THEN
         WRITE(OUTLYNE,*)'CFG',INT(W1),' :ALREADY EMPTY'
         CALL SHOWIT(1)
         RETURN
      ELSE
!       NOT EMPTY
         BLANK=AA//AA//AA//AA//AA//AA//AA
         CONFG(INT(W1),1:MAXCFG)=BLANK(1:140)
         CFGCNT(INT(W1))=0
         LNSTYP=1
         CALL LNSEOS
         IF(F28.EQ.0) CALL VCHECK
         WRITE(OUTLYNE,*)'CFG',INT(W1),' :CONFIG DATA DELETED'
         CALL SHOWIT(1)
      END IF
   END IF
   RETURN
END
