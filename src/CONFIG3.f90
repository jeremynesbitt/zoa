!
   IF(RET.EQ.1) THEN
      LEOS=0
      SPEOS=0
      RET=0
      WC='EOS'
   END IF
!
!       IN ORDER TO CLOSE THE CONFIGS OR UPDATE CONFIGS
!       ENTRY THE COMMAND
!                       (EOS) IS USED,BUT THERE IS
!       A PROBLEM. AS IT IS PERMISSIBLE TO ALSO USE
!       UPDATE LENS AND UPDATE SPSRF LEVELS INSIDE
!       CONFIGS OR UPDATE CONFIGS, WE MUST PROPERLY
!       HANDLE NESTING OF (EOS) COMMANDS
!       IF UPDATE LENS IS ENCOUNTERED, THE LEOS FLAG IS SET TO 1,
!       ALSO F13 IS SET TO 1 FOR USE BY QUERRY
!       THE NEXT EOS COMMAND ISSUED WILL BE USED TO CLOSE THAT LEVEL,
!       NOT TO EXIT FROM CONFIGS OR UPDATE CONFIGS.
!
!       FOR UPDATE SPSRF THE SPEOS FLAG WILL BE USED ANALOGOUSLY
!       ALSO F14 IS USED FOR QUERRY
!
!       FIRST CHECK IF THE CURRENT CONFIGURATION HAS A FULL
!       ARRAY
!
   IF(F12.NE.1.AND.CFGCNT(F12).EQ.2000) THEN
      WRITE(OUTLYNE,*)'CONFIGURATION',F12,' :CONFIGURATION STORAGE NOW FULL'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'NO ADDITIONAL ENTRIES ALLOWED'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       CURRENT ARRAY IS NOT FULL, WE CAN ACCEPT COMMANDS
!*******************************************************************
!       HANDEL QUERRY OF CURRENT CFG NUMBER
   IF(WC.EQ.'CFG'.AND.DF1.EQ.1.AND.DF2.EQ.1.AND.DF3.EQ.1 .AND.DF4.EQ.1.AND.DF5.EQ.1) THEN
