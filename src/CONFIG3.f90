   COMMON/BLAAA/BLANK
!
   INTEGER ULTEST,ALLOERR,SCRCNT,SPTEST,IL,I,J,K,JJ,JJJ,JK,UL,SP,UL1,SP1,UUL,IEND,STOP,REF,CFCN,TESTV1
!
!
   CHARACTER*140 SCRATH
   DIMENSION SCRATH(:)
   ALLOCATABLE :: SCRATH
   INTEGER NANA
   NANA=2000
   DEALLOCATE(SCRATH,STAT=ALLOERR)
   ALLOCATE(SCRATH(NANA),STAT=ALLOERR)
!
   BLANK=AA//AA//AA//AA//AA//AA//AA
   DO IL=1,2000
      SCRATH(IL)=BLANK
   END DO
!       IEND IS THE LAST NON-BLANK CONFIG
   IEND=INT(sys_high_cfg())
!
!       BLANK OUT ALL BUT THE LAST ASTOP AND REFS IN THE CONFIG
!
!
   DO 13 I=2,IEND
      CFCN=CFGCNT(I)
      STOP=0
      DO 11 J=CFGCNT(I),1,-1
         EE12=CONFG(I,J)
         HOLDER=EE12
         IF((HOLDER(1:5)).EQ.'ASTOP') THEN
            IF(STOP.EQ.0) THEN
!       SET STOP TO 1 AND PROCEED
               STOP=1
               GO TO 11
            END IF
            IF(STOP.EQ.1) THEN
               DO 14 K=J,(CFGCNT(I)-1)
                  EE12=CONFG(I,(K+1))
                  HOLDER=EE12
                  CONFG(I,K)=HOLDER(1:140)
14             CONTINUE
               CFCN=CFCN-1
            END IF
         END IF
11    CONTINUE
      REF=0
      DO 12 J=CFGCNT(I),1,-1
         EE12=CONFG(I,J)
         HOLDER=EE12
         IF(HOLDER(1:4).EQ.'REFS') THEN
            IF(REF.EQ.0) THEN
!       SET REF TO 1 AND PROCEED
               REF=1
               GO TO 12
            END IF
            IF(REF.EQ.1) THEN
               DO 16 K=J,(CFGCNT(I)-1)
                  EE12=CONFG(I,(K+1))
                  HOLDER=EE12
                  CONFG(I,K)=HOLDER(1:140)
16             CONTINUE
               CFCN=CFCN-1
               CONFG(I,J)=BLANK(1:140)
            END IF
         END IF
12    CONTINUE
      CFGCNT(I)=CFCN
13 CONTINUE
!
!
!       CHECK EACH CONFIG FOR WHICH THERE IS CONFIG DATA
!
   DO 10 I=2,IEND
      ULTEST=0
      SPTEST=0
      UL=0
      SP=0
!       COUNT THE NUMBER OF U L AND U SP IN THIS CONFIG
      DO 80 K=1,CFGCNT(I)
         EE12=CONFG(I,K)
         HOLDER=EE12
         IF((HOLDER(1:10)).EQ.'U        L'.OR.(HOLDER(1:13)).EQ.'UPDATE   LENS'.OR.(HOLDER(1:10)).EQ.'UPDATE   L'.OR.(HOLDER(1:13)).EQ.'U        LENS') UL=UL+1
         IF((HOLDER(1:11)).EQ.'U        SP'.OR.(HOLDER(1:14)).EQ.'UPDATE   SPSRF'.OR.(HOLDER(1:11)).EQ.'UPDATE  SP'.OR.(HOLDER(1:14)).EQ.'U        SPSRF') SP=SP+1
80    CONTINUE
!
!       INITIALIZE THE SCRATCH AREA FOR CLEANUP PROCESSING
      IF(CFGCNT(I).NE.0) THEN
!       THERE IS CONFIG DATA TO CHECK, DO IT HERE
!                       THE CLEANUP PROCESS
         SCRCNT=0
!       SCRATCH MEMORY INITIALIZED
!
!       IS THERE UPDATE SPSRF DATA TO BE PUT INTO THE SCRATCH
!       ARRAY?
!       IF SP NOT 0, THEN THERE MAY BE
!
!       SP NON-ZERO MEANS THERE ARE SP U SP/EOS PAIRS TO PROCESS
!       SP1 SET TO ZERO MEANS WE ARE IN AN U SP/EOS PAIR.
!
!       ANY COMMANDS WHICH LIE BETWEEN U SP OR UPDATE SPSRF
!       AND ANY FOLLOWING EOS SHOULD BE WRITTEN BETWEEN ONE
!       U SP/EOS PAIR IN THE SCRATH ARRAY
         IF(SP.EQ.0) GO TO 32
         SP1=1
         DO 30 J=1,CFGCNT(I)
            EE12=CONFG(I,J)
            HOLDER=EE12
            IF(HOLDER.EQ.BLANK) GO TO 30
!       SCAN THE CONFIG DATA, LOOK FOR THE FIRST
!       U SP OR UPDATE SPSRF
            IF(HOLDER(1:11).EQ.'U        SP'.OR.HOLDER(1:14).EQ.'UPDATE   SPSRF'.OR.HOLDER(1:11).EQ.'UPDATE   SP'.OR.HOLDER(1:14).EQ.'U        SPSRF') THEN
               SP1=0
!       IF THE NEXT ENTRY IS EOS, MAYBE THROW OUT THE PAIR
!       UNLESS THE EOS REPRESENTS THE LAST EOS
               EE12=CONFG(I,(J+1))
               HOLDER=EE12
               IF((HOLDER(1:3)).EQ.'EOS') THEN
!       DECREMENT SP BY 1
                  SP=SP-1
                  IF(SP.EQ.0) THEN
!       JUST THROW OUT THE U SP, NOT THE EOS
!       FOUND LAST 'EOS'
                     SCRCNT=SCRCNT+1
                     SCRATH(SCRCNT)=HOLDER
                     CONFG(I,(J+1))=BLANK(1:140)
!       JUMP OUT OF LOOP
                     GO TO 31
                  ELSE
!       SP NOT 0, DUMP THE PAIR.
                     CONFG(I,J)=BLANK(1:140)
                     CONFG(I,(J+1))=BLANK(1:140)
                     SP1=1
                     GO TO 30
                  END IF
               ELSE
                  SP1=0
!       DONT THROW OUT THE PAIR, NEXT ENTRY WAS
!       NOT 'EOS' PROCEED TO PROCESS
               END IF
!       CHECK IF U SP WAS FIRST ONE, IF SO WRITE IT.
               IF(SPTEST.EQ.0) THEN
!       WRITE THE U SP TO THE FILE AND SET SPTEST=1
                  SCRCNT=SCRCNT+1
                  EE12=CONFG(I,J)
                  HOLDER=EE12
                  SCRATH(SCRCNT)=HOLDER
                  SPTEST=1
!       DECREMENT THE U SP COUNTER
                  SP=SP-1
!
!       NOW PROCEED TO CHECK THE NEXT CONFIG ENTRY
                  GO TO 30
               ELSE
!       SPTEST=1, DON'T WRITE THE U SP, IT IS NOT THE FIRST ONE.
!       DECREMENT THE U SP COUNTER
                  SP=SP-1
                  CONFG(I,J)=BLANK(1:140)
!       NOW PROCEED TO CHECK THE NEXT CONFIG ENTRY
               END IF
               GO TO 30
            ELSE
!       THE CURRENT ENTRY IS NOT U SP. IF SP1=0, PROCEED TO PROCESS
!       IF SP1=1, GO TO 30 AND LOOK FOR U SP
               IF(SP1.EQ.1) GO TO 30
!       SP1 NOT 1
!       THE CONFIG ENTRY IS NOT U SP, PROCEED WITH SEARCH
!       THE CURRENT ENTRY IS EITHER NOT AN U SP COMMAND OR IT IS
!       IF SPTEST=1, IT IS AN SPSRF COMMAND, CHECK IT AND PROCESS IT.
!       ALSO SEE IF THE COMMAND FOLLOWING IT IS AN 'EOS' AND HANDEL IT.
               IF(SPTEST.EQ.1) THEN
!       THE CURRENT ENTRY IS AN U SP COMMAND, WRITE IT
                  SCRCNT=SCRCNT+1
                  EE12=CONFG(I,J)
                  HOLDER=EE12
                  SCRATH(SCRCNT)=HOLDER
                  CONFG(I,J)=BLANK(1:140)
!       IS THE COMMAND FOLLOWED BY AN 'EOS'
                  EE12=CONFG(I,(J+1))
                  HOLDER=EE12
                  IF(HOLDER(1:3).EQ.'EOS') THEN
!       NEXT COMMAND IS 'EOS'. IF SP=0, IT IS THE FINAL EOS,
!       IF SP NOT 0, SKIP IT, BLANK IT AND PROCEED
                     IF(SP.EQ.0) THEN
!       FOUND LAST 'EOS'
                        SCRCNT=SCRCNT+1
                        SCRATH(SCRCNT)=HOLDER
                        CONFG(I,(J+1))=BLANK(1:140)
!       JUMP OUT OF LOOP
                        GO TO 31
                     ELSE
!       SP NOT 0
!       THIS WAS NOT THE LAST EOS, SEARCH FOR ANOTHER U SP
                        SP1=1
                        CONFG(I,(J+1))=BLANK(1:140)
!
                     END IF
!       BLANK EOS AND PROCEED WITH SEARCH
                     GO TO 30
                  ELSE
!       NEXT COMMAND WAS NOT 'EOS', PROCEED
                  END IF
                  GO TO 30
               ELSE
!       THE CURRENT COMMAND WAS NOT AN U SP COMMAND
!       AND IT OCCURED WITH SP1=0
                  OUTLYNE='SERIOUS ERROR IN CFGCLN.FOR (1)'
                  CALL SHOWIT(1)
                  DEALLOCATE(SCRATH,STAT=ALLOERR)
                  RETURN
               END IF
            END IF
!
30       CONTINUE
31       CONTINUE
32       CONTINUE
!
!       NOW DO THE SAME THING FOR U L/EOS
!
!       IS THERE UPDATE LENS DATA TO BE PUT INTO THE SCRATCH
!       ARRAY?
!       ANY COMMANDS WHICH LIE BETWEEN U L OR UPDATE LENS
!       AND ANY FOLLOWING EOS SHOULD BE WRITTEN BETWEEN ONE
!       U L/EOS PAIR IN THE SCRATH ARRAY
!
!       AVOID THE SEARCH
         IF(UL.EQ.0) GO TO 50
         UL1=1
         DO 40 J=1,CFGCNT(I)
            EE12=CONFG(I,J)
!
            HOLDER=EE12
            IF(HOLDER.EQ.BLANK) GO TO 40
!       SCAN THE CONFIG DATA, LOOK FOR THE FIRST
!       U L OR UPDATE LENS
            IF(HOLDER(1:10).EQ.'U        L'.OR.HOLDER(1:13).EQ.'UPDATE   LENS'.OR.HOLDER(1:10).EQ.'UPDATE   L'.OR.HOLDER(1:13).EQ.'U        LENS') THEN
!       IF THE NEXT ENTRY IS EOS, THROW OUT THE PAIR
!       OR IF NEXT TWO COMMANDS ARE CHG AND EOS THROW OUT PAIR
               EE12=CONFG(I,(J+1))
               HOLDER=EE12
               IF((HOLDER(1:3)).EQ.'EOS') THEN
!       THROW OUT THE PAIR AND DECREMENT UL BY 1
                  UL=UL-1
                  CONFG(I,J)=BLANK(1:140)
                  CONFG(I,(J+1))=BLANK(1:140)
!       WAS THAT THE END OF THE CONFIG FILE?
!       IF SO ADD AN EOS TO THE END OF SCATCH FILE
                  IF((J+1).EQ.CFGCNT(I)) THEN
                     SCRCNT=SCRCNT+1
                     SCRATH(SCRCNT)='EOS'
                  ELSE
!       NOT END
                  END IF
                  GO TO 40
               END IF
               EE12=CONFG(I,(J+1))
               HOLDER=EE12
               EE12=CONFG(I,(J+2))
               HOLD2=EE12
               IF((HOLDER(1:3)).EQ.'CHG'.AND.(HOLD2(1:3)).EQ.'EOS') THEN
!       THROW OUT THE THREE AND DECREMENT UL BY 1
                  UL=UL-1
                  CONFG(I,J)=BLANK(1:140)
                  CONFG(I,(J+1))=BLANK(1:140)
                  CONFG(I,(J+2))=BLANK(1:140)
!       WAS THAT THE END OF THE CONFIG FILE?
!       IF SO ADD AN EOS TO THE END OF SCATCH FILE
                  IF((J+2).EQ.CFGCNT(I)) THEN
                     SCRCNT=SCRCNT+1
                     SCRATH(SCRCNT)='EOS'
                  ELSE
!       NOT END
                  END IF
                  GO TO 40
               ELSE
!       FOUND AN U L NOT FOLLOWED BY EOS
!       OR CHG AND EOS, SET UL1=0
                  UL1=0
!       DONT THROW OUT, NEXT ENTRY WAS
!       NOT 'EOS'  OR 'CHG' AND 'EOS' PROCEED TO PROCESS
               END IF
!       CHECK IF U L WAS FIRST ONE, IF SO WRITE IT.
               IF(UL.NE.0) THEN
                  IF(ULTEST.EQ.0) THEN
!       WRITE THE U L TO THE FILE AND SET ULTEST=1
                     SCRCNT=SCRCNT+1
                     EE12=CONFG(I,J)
                     HOLDER=EE12
                     SCRATH(SCRCNT)=HOLDER
                     ULTEST=1
! NOW LOAD IN A DEFAULT CHG,0
                     EE12=CONFG(I,(J+1))
                     HOLDER=EE12
                     IF((HOLDER(1:29)).NE.'CHG     ,  0.000000000000D+00')THEN
                        SCRCNT=SCRCNT+1
                        SCRATH(SCRCNT)='CHG     ,  0.000000000000D+00,,,,,'
                     ELSE
!       DON'T ADD CHG,0
                     END IF
!                       ULTEST=1
!       DECREMENT THE U L COUNTER
                     UL=UL-1
!       NOW PROCEED TO CHECK THE NEXT CONFIG ENTRY
                     GO TO 40
                  ELSE
!       ULTEST=1, DON'T WRITE THE U L, IT IS NOT THE FIRST ONE.
!       DECREMENT THE U L COUNTER
                     UL=UL-1
                     CONFG(I,J)=BLANK(1:140)
!       NOW PROCEED TO CHECK THE NEXT CONFIG ENTRY
                  END IF
               END IF
               GO TO 40
            ELSE
!       THE CURRENT ENTRY IS NOT U L. IF UL1=0, PROCEED TO PROCESS
!       IF UL1=1, GO TO 40 AND LOOK FOR U L
               IF(UL1.EQ.1) GO TO 40
!       UL1 NOT 1
!       THE CONFIG  ENTRY IS NOT U L, PROCEED WITH SEARCH
!       THE CURRENT ENTRY IS EITHER NOT AN U L COMMAND OR IT IS
!       IF ULTEST=1, IT IS AN LENS COMMAND, CHECK IT AND PROCESS IT.
!       ALSO SEE IF THE COMMAND FOLLOWING IT IS AN 'EOS' AND HANDEL IT.
               IF(ULTEST.EQ.1) THEN
!       THE CURRENT ENTRY IS AN U L COMMAND, WRITE IT
                  SCRCNT=SCRCNT+1
                  EE12=CONFG(I,J)
                  HOLDER=EE12
                  SCRATH(SCRCNT)=HOLDER
                  CONFG(I,J)=BLANK(1:140)
!       IS THE COMMAND FOLLOWED BY AN 'EOS'
                  EE12=CONFG(I,(J+1))
                  HOLDER=EE12
                  IF((HOLDER(1:3)).EQ.'EOS') THEN
!       NEXT COMMAND IS 'EOS'. IF UL=0, IT IS THE FINAL EOS,
!       IF UL NOT 0, SKIP IT, BLANK IT AND PROCEED
                     IF(UL.EQ.0) THEN
!       FOUND LAST 'EOS'
                        SCRCNT=SCRCNT+1
                        SCRATH(SCRCNT)=HOLDER
                        CONFG(I,(J+1))=BLANK(1:140)
!       JUMP OUT OF LOOP
                        GO TO 41
                     ELSE
!       UL NOT 0
!       THIS WAS NOT THE LAST EOS, SEARCH FOR ANOTHER U L
                        UL1=1
                        CONFG(I,(J+1))=BLANK(1:140)
!
                     END IF
!       BLANK EOS AND PROCEED WITH SEARCH
                     GO TO 40
                  ELSE
!       NEXT COMMAND WAS NOT 'EOS', PROCEED
                  END IF
                  GO TO 40
               ELSE
!       THE CURRENT COMMAND WAS NOT AN U L COMMAND
!       AND IT OCCURED WITH UL1=0
                  OUTLYNE= 'SERIOUS ERROR IN CFGCLN.FOR (2)'
                  CALL SHOWIT(1)
                  DEALLOCATE(SCRATH,STAT=ALLOERR)
                  RETURN
               END IF
            END IF
!
!
40       CONTINUE
41       CONTINUE
!       CODE WHICH ADDED A BLANK U L/EOS PAIR WAS REMOVED
!       FROM HERE ON 9/12/88 AND IS NOT NEEDED ANYMORE.
!
50       CONTINUE
!
!***************************************************************
!       START CHECKING CONFIGS FOR DATA
!       CHECK FOR NON- U L OR U SP COMMANDS.
!       IF FOUND WRITE THEM TO SCRATH(1:2000) AND INCREMENT
!       SCRCNT, THE SCRATCH COUNTER.
!       THE COMMANDS WE ARE LOOKING FOR ARE:
!       MODE (FOCAL,AFOCAL,UFOCAL OR UAFOCAL)
!       FNBY AND FNBX
!       ERY AND ERX
!       MAGY AND MAGX
!       SPTWT AND SPTWT2. (ALSO DO SPSRF ON AND SPSRF OFF WITH
!       SURFACE NUMBER ENTRIES)
!       START CHECKING FROM END OF DATA LIST
         DO 21 JJ=CFGCNT(I),1,-1
            EE12=CONFG(I,JJ)
            HOLDER=EE12
            IF(HOLDER(1:16).EQ.'MODE     FOCAL  '.OR.HOLDER(1:16).EQ.'MODE     AFOCAL '.OR.HOLDER(1:16).EQ.'MODE     UAFOCAL'.OR.HOLDER(1:16).EQ.'MODE     UFOCAL ') THEN
!       FOUND AND ENTRY FOR THE SCRATH ARRAY
               SCRCNT=SCRCNT+1
               SCRATH(SCRCNT)=HOLDER
               CONFG(I,JJ)=BLANK(1:140)
!       NOW IF THERE ARE ANY MODE COMMANDS BETWEEN THIS MODE
!       COMMAND AND THE START OF THE CONFIG DATA, REMOVE THEM
               DO 22 JJJ=(JJ-1),1,-1
                  EE12=CONFG(I,JJJ)
                  HOLDER=EE12
                  IF(HOLDER(1:16).EQ.'MODE     FOCAL  '.OR.HOLDER(1:16).EQ.'MODE     AFOCAL '.OR.HOLDER(1:16).EQ.'MODE     UAFOCAL'.OR.HOLDER(1:16).EQ.'MODE     UFOCAL ') THEN
                     CONFG(I,JJJ)=BLANK(1:140)
                  ELSE
!       PROCEED WITH CHECKING
                  END IF
22             CONTINUE
            ELSE
!       FIRST OCCURENCE OF MODE NOT FOUND, CHECK NEXT ENTRY
            END IF
21       CONTINUE
!
!       NOW ALL MODE COMMANDS ARE RE-WRITTEN TO SCRATH ARRAY
!       NOW CHECK 'MAGY' OR 'MAGX'
!
!       START CHECKING FROM END OF DATA LIST
         DO 23 JJ=CFGCNT(I),1,-1
            EE12=CONFG(I,JJ)
            HOLDER=EE12
            IF(HOLDER(1:4).EQ.'MAGY'.OR.HOLDER(1:4).EQ.'MAGX') THEN
!       FOUND AND ENTRY FOR THE SCRATH ARRAY
               SCRCNT=SCRCNT+1
               SCRATH(SCRCNT)=HOLDER
               CONFG(I,JJ)=BLANK(1:140)
!       NOW IF THERE ARE ANY MAGY OR MAGX
!       COMMANDS BETWEEN THIS MAGX OR MAGY
!       COMMAND AND THE START OF THE CONFIG DATA, REMOVE THEM
               DO 24 JJJ=(JJ-1),1,-1
                  EE12=CONFG(I,JJJ)
                  HOLDER=EE12
                  IF(HOLDER.EQ.'MAGY'.OR.HOLDER(1:4).EQ.'MAGX') THEN
                     CONFG(I,JJJ)=BLANK(1:140)
                  ELSE
!       PROCEED WITH CHECKING
                  END IF
24             CONTINUE
            ELSE
!       FIRST OCCURENCE OF MAGY OR MAGX NOT FOUND, CHECK NEXT ENTRY
            END IF
23       CONTINUE
!       NOW ALL MAGY/MAGX COMMANDS ARE RE-WRITTEN TO SCRATH ARRAY
!       NOW CHECK 'FNBY' OR 'ERY'. ALL FNBY AND ERY COMMANDS
!       TAKE PRECIDENCE OVER ALL OTHERS, ONLY ONE CAN BE
!       ALLOWED TO REMAIN!
!
!       START CHECKING FROM END OF DATA LIST
         DO 25 JJ=CFGCNT(I),1,-1
            EE12=CONFG(I,JJ)
            HOLDER=EE12
            IF(HOLDER(1:4).EQ.'FNBY'.OR.HOLDER(1:3).EQ.'ERY') THEN
!       FOUND AND ENTRY FOR THE SCRATH ARRAY
               SCRCNT=SCRCNT+1
               SCRATH(SCRCNT)=HOLDER
               CONFG(I,JJ)=BLANK(1:140)
!       NOW IF THERE ARE ANY FNBY OR ERY
!       COMMANDS BETWEEN THIS FNBY OR ERY
!       COMMAND AND THE START OF THE CONFIG DATA, REMOVE THEM
               DO 26 JJJ=(JJ-1),1,-1
                  EE12=CONFG(I,JJJ)
                  HOLDER=EE12
                  IF(HOLDER(1:4).EQ.'FNBY'.OR.HOLDER(1:3).EQ.'ERY') THEN
                     CONFG(I,JJJ)=BLANK(1:140)
                  ELSE
!       PROCEED WITH CHECKING
                  END IF
26             CONTINUE
            ELSE
!       FIRST OCCURENCE OF FNBY OR ERY NOT FOUND, CHECK NEXT ENTRY
            END IF
25       CONTINUE
!       NOW ALL FNBY/ERY COMMANDS ARE RE-WRITTEN TO SCRATH ARRAY
!       NOW CHECK 'FNBX' OR 'ERX'. ALL FNBX AND ERX COMMANDS
!       TAKE PRECIDENCE OVER ALL OTHERS, ONLY ONE CAN BE
!       ALLOWED TO REMAIN!
!
!       START CHECKING FROM END OF DATA LIST
         DO 27 JJ=CFGCNT(I),1,-1
            EE12=CONFG(I,JJ)
            HOLDER=EE12
            IF(HOLDER(1:4).EQ.'FNBX'.OR.HOLDER(1:3).EQ.'ERX') THEN
!       FOUND AND ENTRY FOR THE SCRATH ARRAY
               SCRCNT=SCRCNT+1
               SCRATH(SCRCNT)=HOLDER
               CONFG(I,JJ)=BLANK(1:140)
!       NOW IF THERE ARE ANY FNBX OR ERX
!       COMMANDS BETWEEN THIS FNBX OR ERX
!       COMMAND AND THE START OF THE CONFIG DATA, REMOVE THEM
               DO 28 JJJ=(JJ-1),1,-1
                  EE12=CONFG(I,JJJ)
                  HOLDER=EE12
                  IF(HOLDER(1:4).EQ.'FNBX'.OR.HOLDER(1:3).EQ.'ERX') THEN
                     CONFG(I,JJJ)=BLANK(1:140)
                  ELSE
!       PROCEED WITH CHECKING
                  END IF
28             CONTINUE
            ELSE
!       FIRST OCCURENCE OF FNBX OR ERX NOT FOUND, CHECK NEXT ENTRY
            END IF
27       CONTINUE
!       NOW SPTWT
!       NOW CHECK 'SPTWT'
!
!       START CHECKING FROM END OF DATA LIST
         DO 231 JJ=CFGCNT(I),1,-1
            EE12=CONFG(I,JJ)
            HOLDER=EE12
            IF(HOLDER(1:5).EQ.'SPTWT') THEN
!       FOUND AND ENTRY FOR THE SCRATH ARRAY
               SCRCNT=SCRCNT+1
               SCRATH(SCRCNT)=HOLDER
               CONFG(I,JJ)=BLANK(1:140)
!       NOW IF THERE ARE ANY SPTWT
!       COMMANDS BETWEEN THIS SPTWT
!       COMMAND AND THE START OF THE CONFIG DATA, REMOVE THEM
               DO 241 JJJ=(JJ-1),1,-1
                  EE12=CONFG(I,JJJ)
                  HOLDER=EE12
                  IF(HOLDER(1:5).EQ.'SPTWT') THEN
                     CONFG(I,JJJ)=BLANK(1:140)
                  ELSE
!       PROCEED WITH CHECKING
                  END IF
241            CONTINUE
            ELSE
!       FIRST OCCURENCE OF SPTWT NOT FOUND, CHECK NEXT ENTRY
            END IF
231      CONTINUE
!       NOW ALL SPTWT COMMANDS ARE RE-WRITTEN TO SCRATH ARRAY
!       SPTWT DONE
!       NOW SPTWT2
!       NOW CHECK 'SPTWT2'
!
!       START CHECKING FROM END OF DATA LIST
         DO JJ=CFGCNT(I),1,-1
            EE12=CONFG(I,JJ)
            HOLDER=EE12
            IF(HOLDER(1:6).EQ.'SPTWT2') THEN
!       FOUND AND ENTRY FOR THE SCRATH ARRAY
               SCRCNT=SCRCNT+1
               SCRATH(SCRCNT)=HOLDER
               CONFG(I,JJ)=BLANK(1:140)
!       NOW IF THERE ARE ANY SPTWT2
!       COMMANDS BETWEEN THIS SPTWT2
!       COMMAND AND THE START OF THE CONFIG DATA, REMOVE THEM
               DO JJJ=(JJ-1),1,-1
                  EE12=CONFG(I,JJJ)
                  HOLDER=EE12
                  IF(HOLDER(1:6).EQ.'SPTWT2') THEN
                     CONFG(I,JJJ)=BLANK(1:140)
                  ELSE
!       PROCEED WITH CHECKING
                  END IF
               END DO
            ELSE
!       FIRST OCCURENCE OF SPTWT2 NOT FOUND, CHECK NEXT ENTRY
            END IF
         END DO
!       NOW ALL SPTWT2 COMMANDS ARE RE-WRITTEN TO SCRATH ARRAY
!       SPTWT2 DONE
!       NOW THERM SHAPE
!       NOW CHECK 'THERM SHAPE'
!
!       START CHECKING FROM END OF DATA LIST
         DO JJ=CFGCNT(I),1,-1
            EE12=CONFG(I,JJ)
            HOLDER=EE12
            IF(HOLDER(1:14).EQ.'THERM    SHAPE') THEN
!       FOUND AND ENTRY FOR THE SCRATH ARRAY
               SCRCNT=SCRCNT+1
               SCRATH(SCRCNT)=HOLDER
               CONFG(I,JJ)=BLANK(1:140)
            ELSE
!       FIRST OCCURENCE OF THERM SHAPE NOT FOUND, CHECK NEXT ENTRY
            END IF
         END DO
!       NOW ALL THERM SHAPE COMMANDS ARE RE-WRITTEN TO SCRATH ARRAY
!       THERM SHAPE DONE
!       NOW THERM THICK
!       NOW CHECK 'THERM THICK'
!
!       START CHECKING FROM END OF DATA LIST
         DO JJ=CFGCNT(I),1,-1
            EE12=CONFG(I,JJ)
            HOLDER=EE12
            IF(HOLDER(1:14).EQ.'THERM    THICK') THEN
!       FOUND AND ENTRY FOR THE SCRATH ARRAY
               SCRCNT=SCRCNT+1
               SCRATH(SCRCNT)=HOLDER
               CONFG(I,JJ)=BLANK(1:140)
            ELSE
!       FIRST OCCURENCE OF THERM THICK NOT FOUND, CHECK NEXT ENTRY
            END IF
         END DO
!       NOW ALL THERM THICK COMMANDS ARE RE-WRITTEN TO SCRATH ARRAY
!       THERM THICK DONE
!       NOW THERM SPACE
!       NOW CHECK 'THERM SPACE'
!
!       START CHECKING FROM END OF DATA LIST
         DO JJ=CFGCNT(I),1,-1
            EE12=CONFG(I,JJ)
            HOLDER=EE12
            IF(HOLDER(1:14).EQ.'THERM    SPACE') THEN
!       FOUND AND ENTRY FOR THE SCRATH ARRAY
               SCRCNT=SCRCNT+1
               SCRATH(SCRCNT)=HOLDER
               CONFG(I,JJ)=BLANK(1:140)
            ELSE
!       FIRST OCCURENCE OF THERM SPACE NOT FOUND, CHECK NEXT ENTRY
            END IF
         END DO
!       NOW ALL THERM SPACE COMMANDS ARE RE-WRITTEN TO SCRATH ARRAY
!       THERM SPACE DONE
!       NOW THERM GLASS
!       NOW CHECK 'THERM GLASS'
!
!       START CHECKING FROM END OF DATA LIST
         DO JJ=CFGCNT(I),1,-1
            EE12=CONFG(I,JJ)
            HOLDER=EE12
            IF(HOLDER(1:14).EQ.'THERM    GLASS') THEN
!       FOUND AND ENTRY FOR THE SCRATH ARRAY
               SCRCNT=SCRCNT+1
               SCRATH(SCRCNT)=HOLDER
               CONFG(I,JJ)=BLANK(1:140)
            ELSE
!       FIRST OCCURENCE OF THERM GLASS NOT FOUND, CHECK NEXT ENTRY
