! SUB CFGIN.FOR
SUBROUTINE CFGIN
!
   use DATCFG
   use DATLEN
   use mod_system
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS CALLED TO CREATE THE
!       CONFIGURATION DATA USING THE COMMAND
!       (CONFIGS) FROM THE CMD LEVEL
!
   INTEGER I,J
!
!
!       CONFIGS TAKES NO INPUT
   IF(SQ.EQ.1.OR.SN.EQ.1.OR.SST.EQ.1) THEN
      OUTLYNE='"CONFIGS" TAKES NO EXPLICIT INPUT'
      CALL SHOWIT(1)
      OUTLYNE='RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       INITIALIZE REFERENCE RAY LOGICAL
   IF(RAYCLEAR) THEN
      FOBYES=.FALSE.
      REFEXT=.FALSE.
      NULL=.FALSE.
      RAYEXT=.FALSE.
      POLEXT=.FALSE.
      SPDEXT=.FALSE.
      GSPDEXT=.FALSE.
      FAIL=.TRUE.
   END IF

!       INITIALIZE sys_curr_cfg() AND SYSP(50) AND SYTEM(56) AND SYSP(56)
   sys_curr_cfg()=1.0D0
   SYSP(50)=1.0D0
   call set_sys_high_cfg(1.0D0)
   SYSP(56)=1.0D0
!
!       INITIALIZE THE ARRAY CONFIGS
!       INITIALIZE THE ARRAY CONFIGS
!                      DO I=2,MAXCFG
   CFGCNT(2:MAXCFG)=0
!                      END DO
!     INITIALIZE THE ARRAY THAT KEEPS TRACK OF SPECIAL SURFACES
   DO J=2,MAXCFG
      I=INT(sys_last_surf())
      SPECFF(J,0:I)=0
      SPECF2(J,0:I)=INT(DABS(ALENS(34,0:I)))
   END DO
!                       SET F1=0 AND F10=1
   F1=0
   F10=1
   F12=2
!                       RETURN
END
! SUB CFGCHG2.FOR
SUBROUTINE CFGCHG2
!
   use DATCFG
   use DATSUB
   use DATLEN
   use mod_system
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDELS THE BEHAVIOR OF THE CFG
!       COMMAND FROM INSIDE OPTIMIZATION
   CHARACTER DUMPY*140
   COMMON/LUMPY/DUMPY
!
   INTEGER IEND,I,J,K,IC
!
   COMMON/AUTOI/I
!
!
   IF(I.GE.1.AND.I.LE.MAXCFG) THEN
      IF(I.EQ.1) GO TO 7687
!
      IF(I.NE.1) THEN
!
!     UPPRLN COPIES THE CURRENT LENS TO THE PERMANENT LENS IF F12
!     IS 1, IF THE CURRENT LENS IS CFG 1
         CALL UPPRLN
         F12=I
      END IF

!       DECIDE IF THE INTEGER PART OF sys_high_cfg() SHOULD
!       BE INCREMENTED.
      IEND=INT(sys_high_cfg())
      IF(I.GT.IEND) THEN
!       CHANGE IEND AND FRAC sys_curr_cfg()
         call set_sys_high_cfg(sys_high_cfg()+1.0D0)
         SYSP(56)=sys_high_cfg()
      END IF
!
!       RETURN TO CFG 1
!
7687  IF(I.EQ.1) THEN
!
         IF(F12.NE.1) THEN
            CALL PTOC
!************************************************************
!               FINISHED RETURNING TO CFG 1
!
            F12=1
            F1=0
            F6=1
            F22=1
            LNSTYP=1
            CALL LNSEOS
         END IF
         RETURN
      END IF
!
!       HERE IS WERE THE CHANGES OCCUR
!       CHANGE TO CFG F12
!
!     NEXT COPY THE PERMANENT LENS TO THE ACTIVE LENS ARRAYS
!
      LI=LIP
      LLTYPE=LLTYPEP
      INNI=INNIP
      LIC=LICP
      SYSTEM=SYSP
      ALENS=ALENP
      PIKP=PIKUP
      SOLVE=SLVP
      FTFL01=FT01P
      GLANAM=GLANMP
!
      IF(F12.NE.1) THEN
!       NOW THE ORIGINAL PERMANENT LENS IS IN
!       WORKING LENS STORAGE
!
!       NOW THE MAGIC. FOR THE PARTICULAR CFG
!       DESIGNATED BY THE CURRENT VALUE OF FLAG F12.
!       DURING THIS PROCESS, FLAG F15 IS SET TO 1
!       TO HELP THE UPDATE LENS AND UPDATE SPSRF
!       LEVELS KNOW HOW TO PROERLY RESPOND
         F15=1
!       READ THE CONTENTS OF
!               CONFIG(F12,IC) FROM IC=1 TO IC=CFGCNT(F12)
!       AND ONE BY ONE RUN THE STRING VALUES FOUND BACK
!       THROUGH THE PROCESS SUBROUTINE
         CFCH=.TRUE.
         DO 14 IC=1,CFGCNT(F12)
            EE12=CONFG(F12,IC)
            DUMPY=EE12
            CALL CFCHG1
14       CONTINUE
         CFCH=.FALSE.
!
!       PROCESS DONE, SET F15 TO 0
         F15=0
      END IF
   END IF
   RETURN
END
