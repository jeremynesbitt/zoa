! SUB CFGUP.FOR
SUBROUTINE CFGUP
!
   use DATCFG
   use DATLEN
   use mod_system
   use DATMAI
   use mod_surface, only: surf_special_type
   IMPLICIT NONE
!
   INTEGER I,J
!
!       THIS SUBROUTINE IS CALLED TO UPDATE THE
!       CONFIGURATION DATA USING THE COMMAND
!       (UPDATE CONFIGS) FROM THE CMD LEVEL
!
!
!       CONFIGS TAKES NO INPUT
   IF(SN.EQ.1.OR.SST.EQ.1) THEN
      WRITE(OUTLYNE,*)'"UPDATE CONFIGS" TAKES NO STRING OR NUMERIC INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
!       INITIALIZE REFERENCE RAY LOGICAL
   IF(RAYCLEAR) THEN
      FOBYES=.FALSE.
      REFEXT=.FALSE.
      NULL = .FALSE.
      RAYEXT=.FALSE.
      POLEXT=.FALSE.
      SPDEXT=.FALSE.
      GSPDEXT=.FALSE.
      CPFNEXT=.FALSE.
      CALL DELPSF
      FAIL=.TRUE.
   END IF
!
!                       SET F1=0 AND F11=1
   F1=0
   F11=1
   F12=2
   DO J=2,MAXCFG
      DO I=0,INT(sys_last_surf())
         SPECF2(J,I)=abs(surf_special_type(I))
      END DO
   END DO
!       IF THIS CONFIG IS EMPTY, THE STARTING ADDRESS IS 0
!                       RETURN
END
! SUB CFGPRT.FOR.FOR
SUBROUTINE CFGPRT
!
   use DATCFG
   use DATLEN
   use mod_system
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS CALLED FROM THE CMD LEVEL
!       IT IS USED TO LIST THE CONFIGURATION DATA
!       DESIGNATED BY THE CONFIGURATION NUMBER INT(W1)
!       IF DF1 NOT 1. PRINT ALL THE NON-BLANK COFIGURATION
!       DATA WHEN DF1 = 1
!       CURRENT CONFIGURATION NUMBER IS NOT AFFECTED
!
   CHARACTER BL1*58
!
   INTEGER ALL,IEND,I,IC,J
!
!
   BL1=AA//AA//'                  '
!
!       IEND IS THE LAST NON-BLANK CONFIG
   IEND=INT(sys_high_cfg())
!
   IF(SQ.EQ.1.OR.SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1 .OR.S5.EQ.1) THEN
      WRITE(OUTLYNE,*)'"CF" ONLY TAKES NUMERIC WORD #1 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
!       DEFAULT NUMERIC INPUT
   IF(DF1.EQ.1) THEN
!       ALL IS A FLAG USED TO TELL THIS ROUTINE TO PRINT ALL
!       CONFIGS DATA
      ALL=1
      DF1=0
      GO TO 56
   END IF
!
!       CFG OUT OF RANGE
   IF(INT(W1).LT.2.OR.INT(W1).GT.MAXCFG) THEN
      WRITE(OUTLYNE,*)'NUMERIC INPUT BEYOND LEGAL RANGE'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'VALID CFG NUMBERS ARE 2 TO ',MAXCFG
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       MUST BE A VALID ADDRESS
!       ONLY ONE SURFACE TO BE PRINTED
!
!       CASE OF EMPTY CFG
   IF(CFGCNT(INT(W1)).EQ.0) THEN
      WRITE(OUTLYNE,100) INT(W1)
      CALL SHOWIT(0)
      RETURN
   ELSE
!       WRITE OUT CONFIGS DATA
      WRITE(OUTLYNE,1000) INT(W1)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1100)
      CALL SHOWIT(0)
      DO I=1,CFGCNT(INT(W1))
         EE12=CONFG(INT(W1),I)
         HOLDER=EE12
         IF(OUT.EQ.6.OR.OUT.EQ.7) THEN
            IF((HOLDER(75:140)).EQ.BL1) THEN
!     JUST DO ONE LINE OF OUTPUT
               WRITE(OUTLYNE,1200) I,HOLDER(1:74)
               CALL SHOWIT(0)
            ELSE
               WRITE(OUTLYNE,1200) I,HOLDER(1:74)
               CALL SHOWIT(0)
               WRITE(OUTLYNE,1201) HOLDER(75:140)
               CALL SHOWIT(0)
            END IF
         ELSE
!       OUT NOT 6 OR 7
            WRITE(OUTLYNE,1202) I,HOLDER(1:126)
            CALL SHOWIT(0)
         END IF
      END DO
   END IF
   RETURN
56 CONTINUE
   IF(ALL.EQ.1) THEN
!
!       PRINT ALL NON-BLANK DATA
!
      IC=0
      DO 20 I=2,IEND
         IF(CFGCNT(I).GT.0)  THEN
!       DISPLAY THIS CONFIG
            IC=IC+1
!       PRINT THIS CONFIG
            WRITE(OUTLYNE,1000) I
            CALL SHOWIT(0)
            WRITE(OUTLYNE,1100)
            CALL SHOWIT(0)
            DO 30 J=1,CFGCNT(I)
               EE12=CONFG(I,J)
               HOLDER=EE12
!       WRITE OUT CONFIGS DATA
               IF(OUT.EQ.6.OR.OUT.EQ.7) THEN
                  IF((HOLDER(75:140)).EQ.BL1) THEN
                     WRITE(OUTLYNE,1200) J,HOLDER(1:74)
                     CALL SHOWIT(0)
                  ELSE
                     WRITE(OUTLYNE,1200) J,HOLDER(1:74)
                     CALL SHOWIT(0)
                     WRITE(OUTLYNE,1201) HOLDER(75:140)
                     CALL SHOWIT(0)
                  END IF
               ELSE
!       OUT NOT 6,7
                  WRITE(OUTLYNE,1202) J,HOLDER(1:126)
                  CALL SHOWIT(0)
               END IF
30          CONTINUE
         END IF
20    CONTINUE
   END IF
   IF(IC.EQ.0) WRITE(OUTLYNE,110)
   CALL SHOWIT(0)
   RETURN
110 FORMAT('NO CONFIG DATA')
100 FORMAT('CFG NUMBER ',I3,' :NO CONFIG. DATA')
1000 FORMAT('CONFIG. DATA FOR CONFIG. NUMBER',I3)
1100 FORMAT(2X)
1200 FORMAT(I3,1X,A75)
1201 FORMAT(5X,A74)
1202 FORMAT(I3,1X,A127)
END
