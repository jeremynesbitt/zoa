            WRITE(OUTLYNE,*)'CONFIGURATION',F12,' :CONFIGURATION STORAGE FULL'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'ADDITIONAL DATA ENTRY NOT ALLOWED'
            CALL SHOWIT(1)
