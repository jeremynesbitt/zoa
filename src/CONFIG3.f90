!       WRITE 'EOS' TO THE CONFIGS DATA FILE THEN
         LEOS=0
         F13=0
         CFGCNT(F12)=CFGCNT(F12)+1
         HOLDER(1:140)=BLANK
         HOLDER(1:3)='EOS'
