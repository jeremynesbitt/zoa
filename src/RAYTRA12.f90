         IF(sys_rxim_fang_set().LT.0.0D0) THEN
            OLDCX=REFRY(1,NEWIMG)
            OLDCY=REFRY(2,NEWIMG)
         ELSE
            OLDCX=REFRY(11,NEWIMG)*180.0D0/PII
            OLDCY=REFRY(12,NEWIMG)*180.0D0/PII
         END IF
!     FIRST TIME
         OXSTRT=XSTRT
         OYSTRT=YSTRT

         XSTRT=XSTRT+CDDELX
         YSTRT=YSTRT+CDDELY
!     COMPUTE THE APROPRIATE ZSTRT VALUE
         IISURF=NEWOBJ
         CALL SAGRET(IISURF,XSTRT,YSTRT,ZSAG,SAGERR)
         IF(SAGERR) CALL HANDLESAGRETFAILURE(MSG, NEWOBJ)

         ZSTRT=ZSAG
         GO TO 9898
      ELSE
!     NOT FIRST TIME
!     SET DEPENDENT VARIABLE VALUES
!
!     PREVIOUS X AND Y VALUES ON IMAGE SURFACE
         CRXONE=OLDCX
         CRYONE=OLDCY
!     CURRENT X AND Y VALUES ON IMAGE SURFACE
         IF(sys_rxim_fang_set().EQ.-1.0D0)&
         &CRXLAST=REFRY(1,NEWIMG)
         IF(sys_rxim_fang_set().EQ.1.0D0)&
         &CRXLAST=REFRY(11,NEWIMG)*180.0D0/PII
         IF(sys_ryim_fang_set().EQ.-1.0D0)&
         &CRYLAST=REFRY(2,NEWIMG)
         IF(sys_ryim_fang_set().EQ.1.0D0)&
         &CRYLAST=REFRY(12,NEWIMG)*180.0D0/PII
         OLDCX=CRXLAST
         OLDCY=CRYLAST
!     SET INDEPENDENT VARIABLE VALUES
         CX1ONE=OXSTRT
         CY1ONE=OYSTRT
         CX1LAST=XSTRT
         CY1LAST=YSTRT
         CD11=0.0D0
         CD12=0.0D0
         CD21=0.0D0
         CD22=0.0D0
         CDET=0.0D0
         CALL CRAYDERIV(CX1LAST,CY1LAST,CX1ONE,CY1ONE &
         &,CRXONE,CRYONE,CRXLAST,CRYLAST,CD11,CD12,CD21,CD22)
      END IF
      IF(KKKK.GT.NRAITR) THEN
!     MAXIMUM CYCLE
!     NO REFERENCE RAY SOLUTION, REF RAY FAILURE
         STOPP=1
         RAYCOD(1)=8
         RAYCOD(2)=I
         OUTLYNE = "SETTING CHIEF RAY TO FALSE 1111"
         CALL SHOWIT(19)
         REFEXT=.FALSE.
         IF(MSG) THEN
            CALL RAY_FAILURE(NEWOBJ)
            WRITE(OUTLYNE,*)'AIMED CHIEF RAY COULD NOT HIT THE DEFINED TARGET'
            CALL SHOWIT(1)
            IF(.NOT.NULL) FOBYES=.FALSE.
            IF(.NOT.NULL.AND.F34.EQ.0) CALL MACFAL
         END IF
         RETURN
      END IF
!     REFINE THE XSTRT,YSTRT VALUE
      CDELFAIL=.FALSE.
!       CMF1=XCHIEF_TARGET-CRXLAST, CMF2=YCHIEF_TARGET-CRYLAST
!
      CMF1=XCHIEF_TARGET-CRXLAST
      CMF2=YCHIEF_TARGET-CRYLAST
      CALL CNEWDEL(CMF1,CMF2,CD11,CD12,CD21,CD22,CDELFAIL,&
      &CDDELX,CDDELY)
      IF(CDELFAIL) THEN
         GO TO 14
      ELSE
         GO TO 9898
      END IF
   ELSE
!     TARGET MET!
      !PRINT *, "TARGET MET FOR SURFACE ", NEWOBJ
      !PRINT *, "Alternate Object Surface ", NEWOBJ
      !PRINT *, "Alternate Image Surface ", NEWIMG

   END IF
   RETURN
END
