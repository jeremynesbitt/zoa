!      THIS IS THE TWELFTH FILE OF RAYTRACING ROUTINES
!
! SUB REFRAY.FOR
SUBROUTINE REFRAY(WPAS)
   USE GLOBALS
   use mod_lens_data_manager
   use real_ray_trace
   use type_utils, only: real2str, int2str
!
   use DATLEN
   use DATMAI
   use mod_surface
   use mod_system, only: sys_scx, sys_scy, sys_scy_fang, sys_scx_fang, &
      sys_ray_aiming, sys_telecentric, sys_aim_offset_x, sys_aim_offset_y, &
      sys_aim_offset_z, sys_scy_fang_set, sys_scx_fang_set, &
      sys_rxim, sys_rxim_fang_set, sys_ryim, sys_ryim_fang_set, &
      sys_set_rxim, sys_set_rxim_fang_set, sys_set_scx_fang_set, sys_set_scy_fang_set
   use iso_fortran_env, only: real64
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE REFRAY.FOR. THIS SUBROUTINE IMPLEMENTS
!       THE TRACING OF THE GUT OR REFERENCE RAY. THIS IS INITIATED
!       BY THE FOB COMMAND.
!
   integer :: jj
   INTEGER NO,KKK,ISYS20,I,KKKK,N_HITS

   INTEGER IISURF,CAERAS,COERAS,IJKTOTAL,IJK,IFINISH
!
   real(real64) X,Y,Z,L,M,N,SNINDX,LOLD,MOLD,NOLD,WPAS &
   &,D21,D22,LS,SNIND2,YL,YM,YN,OXSTRT,OYSTRT,OLDCX,OLDCY,&
   &D11,D12,A10,STX,STY,STZ,XAHT,YAHT,NXAHT,NYAHT,MAG,&
   &LSTART,MSTART,NSTART,YANG,XANG,X1LAST,LARGE,WHT1,WHT2,&
   &Y1LAST,X1ONE,Y1ONE,RXLAST,RYLAST,RXONE,RYONE,DDELX,DDELY,&
   &YYANG,XXANG,MF1,MF2,DET,TARX,TARY,XFOBB0,YFOBB0,ZFOBB0 &
   &,ANGJK1,ANGJK2,XC1,YC1,ZC1,WWWW1,WWWW2,WWWW3,XCHIEF_TARGET &
   &,ZSAG,SCLFACY,SCLFACX,AWW1,AWW2,YCHIEF_TARGET,RAT,CX,CY &
   &,CX1LAST,CY1LAST,CX1ONE,CY1ONE,CRXLAST,CRYLAST,CRXONE,CRYONE,&
   &CD11,CD12,CD21,CD22,CDET,CDDELX,CDDELY,CMF1,CMF2
!
   real(real64) XRAYER,YRAYER,ZRAYER,STEPL,STEPL1,ERRORVEC
   COMMON/RAYERPASS/XRAYER,YRAYER,ZRAYER
!
   COMMON/STRSTR/STX,STY,STZ
!
   COMMON/OHFOB/XFOBB0,YFOBB0,ZFOBB0
!
   COMMON/CACO/CAERAS,COERAS,LS
!
!       FOBB0=TRUE FOR ON AXIS AND FALSE FOR OFF AXIS
   LOGICAL SAGERR,FOBB0,FOBB0X,FOBB0Y,NOCALL,CDELFAIL,DELFAIL
!
   COMMON/FFOOBB/FOBB0,FOBB0X,FOBB0Y
!

   !call logger%logText("Reference Ray Routine Started")

   KKKK=0
   KKK=0
   YAIMOL=0.0D0
   XAIMOL=0.0D0
   ZAIMOL=0.0D0
   DDELX=0.001D0
   DDELY=0.001D0
   CDDELX=0.0001D0*sys_scx()
   CDDELY=0.0001D0*sys_scy()
   IF(WC.EQ.'FOB') THEN
      IF(sys_scy_fang().EQ.0.0D0) THEN
         SCLFACY=1.0D0
      ELSE
         SCLFACY=(1.0D0/sys_scy_fang())
      END IF
      IF(sys_scx_fang().EQ.0.0D0) THEN
         SCLFACX=1.0D0
      ELSE
         SCLFACX=(1.0D0/sys_scx_fang())
      END IF
      AWW1=WW1/SCLFACY
      AWW2=WW2/SCLFACX
   END IF
   IF(WC.EQ.'FOBH') THEN
      SCLFACY=1.0D0
      SCLFACX=1.0D0
      AWW1=WW1/SCLFACY
      AWW2=WW2/SCLFACX
   END IF
!
   WWWW1=WW1
   WWWW2=WW2
   WWWW3=WW3
!
!
!     PROPER INITIALIZE PROCEEDURE 3/3/96
   R_X=0.0D0
   R_Y=0.0D0
   R_Z=0.0D0
   XOLD=0.0D0
   YOLD=0.0D0
   ZOLD=0.0D0
   LOLD=0.0D0
   MOLD=0.0D0
   NOLD=0.0D0
!
!       DDELX AND DDELY ARE THE INITIAL INCREMENTORS FOR THE
!       DERIVATIVE CALCULATION IN NEWTON-RAPHSON SEARCH FOR
!       CHIEF RAY AIMING.
!
!     THE CHECK FOR TILTED AND DECENTERD OBJECT SURFACE HERE WAS REMOVED
!     ON 10/11/94
   LARGE=999999.99D0
   X1ONE=LARGE
   Y1ONE=LARGE
   X1LAST=LARGE
   Y1LAST=LARGE
   CX1ONE=LARGE
   CY1ONE=LARGE
   CX1LAST=LARGE
   CY1LAST=LARGE
!
!       SET INITIAL VALUES FOR RXONE, RYONE, RXLAST AND RYLAST
   RXONE=LARGE
   RYONE=LARGE
   RXLAST=LARGE
   RYLAST=LARGE
   CRXONE=LARGE
   CRYONE=LARGE
   CRXLAST=LARGE
   CRYLAST=LARGE
!
!       THE FIRST THING TO DO IS TO DETERMINE THE STARTING
!       COORDINATES OF THE RAY AT THE OBJECT SURFACE. FIRST
!       CONSIDER THAT WW5 IS ZERO AND THERE IS NO RE-ASSIGNMENT
!       OF OBJECT,REFERENCE OR IMAGE.
!
!       THE RELATIVE OBJECT HEIGHTS ARE TRANSLATED INTO REAL
!       X,Y AND Z POSITIONS WITH RESPECT TO THE OBJECT SURFACE HERE.
!
!       WW1 IS THE FRACTIONAL Y- OBJECT HEIGHT
!       WW2 IS THE FRACTIONAL X- OBJECT HEIGHT
!       WW3 IS THE FRACTIONAL OBJECT DISPLACEMENT FROM THE OBJECT SURFACE
!               PLANE. (ALLOWS FOR DEFINITION OF CURVED OBJECTS)
!       EXPRESSED AS THE WW3 FRACTION OF THE OBJECT SURFACE THICKNESS
!       WW4 IS THE WAVELENGTH NUMBER (1 TO 5)
!       WW5 IS CURRENTLY ASSUMED TO BE ZERO.
!
!       THE STARTING RAY COORDINATES FOR THE RAY TRACE ARE:
!       (AT THE OBJECT SURFACE)
!
   IF(WC.EQ.'FOBH') THEN
!       OBJ ANGLES CANT BE INPUT
      call sys_set_scy_fang_set(0.0D0)
      call sys_set_scx_fang_set(0.0D0)
   END IF
   IF(sys_scy_fang_set().EQ.1.AND.NEWOBJ.EQ.0.OR.&
   &sys_scx_fang_set().EQ.1.AND.NEWOBJ.EQ.0) THEN
!     OBJECT ANGLES INPUT
      ANGIN=.TRUE.
!     REF OBJ HT INPUT AS ANGLE
!     IN THIS CASE, FOB INDICATES FRACTIONS OF FIELD ANGLES
!     BUT ONLY IF THE NEWOBJ SURFACE IS SURFACE 0
!
!     THIS NEEDS MODIFICATION FOR FRACTIONS OF FIELD ANGLES
!
!     ANGLES IN RADIANS ARE:
      ANGJK1=AWW1
      ANGJK2=AWW2
      CALL ADJUSTANGLEIFCLOSETO90(ANGJK1)
      CALL ADJUSTANGLEIFCLOSETO90(ANGJK2)

!
      YYANG=ANGJK1*PII/180.0D0
      XXANG=ANGJK2*PII/180.0D0
!
      IF(DABS(ANGJK1).GT.90.0D0) YSTRT=DTAN(YYANG)*surf_thickness(NEWOBJ)
      IF(DABS(ANGJK1).LE.90.0D0) YSTRT=-DTAN(YYANG)*surf_thickness(NEWOBJ)
!
      IF(DABS(ANGJK2).GT.90.0D0) XSTRT=DTAN(XXANG)*surf_thickness(NEWOBJ)
      IF(DABS(ANGJK2).LE.90.0D0) XSTRT=-DTAN(XXANG)*surf_thickness(NEWOBJ)

      ZSTRT=2.0D0*surf_thickness(NEWOBJ)

      IF(ANGJK1.GT.90.0D0.AND.ANGJK1.LT.270.0D0.OR.&
      &ANGJK1.LT.-90.0D0.AND.ANGJK1.GT.-270.0D0) THEN
!
         IF(ANGJK2.GT.90.0D0.AND.ANGJK2.LT.270.0D0.OR.&
         &ANGJK2.LT.-90.0D0.AND.ANGJK2.GT.-270.0D0) THEN
            ZSTRT=0.0D0
         END IF
      END IF
      IF(ANGJK2.GT.90.0D0.AND.ANGJK2.LT.270.0D0.OR.&
      &ANGJK2.LT.-90.0D0.AND.ANGJK2.GT.-270.0D0) THEN
!
         IF(ANGJK1.GT.90.0D0.AND.ANGJK1.LT.270.0D0.OR.&
         &ANGJK1.LT.-90.0D0.AND.ANGJK1.GT.-270.0D0) THEN
            ZSTRT=0.0D0
         END IF
      END IF
      IF(ANGJK1.GE.0.0D0.AND.ANGJK1.LT.90.0D0.OR.&
      &ANGJK1.LE.0.0D0.AND.ANGJK1.GT.-90.0D0.OR.&
      &ANGJK1.GT.270.0D0.AND.ANGJK1.LE.0.0D0.OR.&
      &ANGJK1.LT.-270.0D0.AND.ANGJK1.GE.0.0D0) THEN
!
         IF(ANGJK2.GE.0.0D0.AND.ANGJK2.LT.90.0D0.OR.&
         &ANGJK2.LE.0.0D0.AND.ANGJK2.GT.-90.0D0.OR.&
         &ANGJK2.GT.270.0D0.AND.ANGJK2.LE.0.0D0.OR.&
         &ANGJK2.LT.-270.0D0.AND.ANGJK2.GE.0.0D0) THEN
            ZSTRT=0.0D0
         END IF
      END IF
      IF(ANGJK2.GE.0.0D0.AND.ANGJK2.LT.90.0D0.OR.&
      &ANGJK2.LE.0.0D0.AND.ANGJK2.GT.-90.0D0.OR.&
      &ANGJK2.GT.270.0D0.AND.ANGJK2.LE.0.0D0.OR.&
      &ANGJK2.LT.-270.0D0.AND.ANGJK2.GE.0.0D0) THEN
!
         IF(ANGJK1.GE.0.0D0.AND.ANGJK1.LT.90.0D0.OR.&
         &ANGJK1.LE.0.0D0.AND.ANGJK1.GT.-90.0D0.OR.&
         &ANGJK1.GT.270.0D0.AND.ANGJK1.LE.0.0D0.OR.&
         &ANGJK1.LT.-270.0D0.AND.ANGJK1.GE.0.0D0) THEN
            ZSTRT=0.0D0
         END IF
      END IF
      IF(sys_scy_fang_set().EQ.1.0D0.AND.sys_scy_fang().EQ.0.0D0) YSTRT=0.0D0
      IF(sys_scx_fang_set().EQ.1.0D0.AND.sys_scx_fang().EQ.0.0D0) XSTRT=0.0D0
      IF(XSTRT.EQ.0.0D0.AND.YSTRT.EQ.0.0D0) ZSTRT=0.0D0
   ELSE
!     OBJECT ANGLES NOT INPUT
      ANGIN=.FALSE.
!
      IF(surf_clap_type(NEWOBJ) == 0.OR.sys_rxim_fang_set().NE.0.0D0 &
      &.OR.sys_ryim_fang_set().NE.0.0D0.OR.surf_array_parity(NEWOBJ) /= 0) THEN
!     THE STARTING RAY COORDINATES ON THE OBJECT SURAFCE
!     ARE NOT DETERMINED BY THE CLAP ON THE OBJECT SURFACE
!     USING SCY AND SCX, FOB REPRESENTS FRACTIONS OF IMAGE HEIGHT
!     AND DEPTH.
         IF(sys_scx().NE.0.0D0) XSTRT=WW2*PXTRAX(5,NEWOBJ)
         IF(sys_scx().EQ.0.0D0) XSTRT=0.0D0
         IF(sys_scy().NE.0.0D0) YSTRT=WW1*PXTRAY(5,NEWOBJ)
         IF(sys_scy().EQ.0.0D0) YSTRT=0.0D0
         IF(sys_rxim_fang_set().NE.0.0D0.OR.sys_ryim_fang_set().NE.0.0D0) THEN
            XSTRT=XSTRT+XRAYER
            YSTRT=YSTRT+YRAYER
         END IF
         IISURF=NEWOBJ
         CALL SAGRET(IISURF,XSTRT,YSTRT,ZSAG,SAGERR)
         IF(SAGERR) CALL HANDLESAGRETFAILURE(MSG, NEWOBJ)

         ZSTRT=(WW3*surf_thickness(NEWOBJ))+ZSAG
      ELSE
!     USE Y HEIGHT OF CLAP WITH OFFSETS
         IF(surf_clap_type(NEWOBJ) == 1.AND.&
         &surf_array_parity(NEWOBJ) == 0) THEN
            IF(surf_clap_dim(NEWOBJ, 1).LE.surf_clap_dim(NEWOBJ, 2)) THEN
               XSTRT=WW2*(surf_clap_dim(NEWOBJ, 1)+surf_clap_dim(NEWOBJ, 4))
               YSTRT=WW1*(surf_clap_dim(NEWOBJ, 1)+surf_clap_dim(NEWOBJ, 3))
            ELSE
               XSTRT=WW2*(surf_clap_dim(NEWOBJ, 2)+surf_clap_dim(NEWOBJ, 4))
               YSTRT=WW1*(surf_clap_dim(NEWOBJ, 2)+surf_clap_dim(NEWOBJ, 3))
            END IF
            IISURF=NEWOBJ
            CALL SAGRET(IISURF,XSTRT,YSTRT,ZSAG,SAGERR)
            IF(SAGERR) CALL HANDLESAGRETFAILURE(MSG, NEWOBJ)

            ZSTRT=(WW3*surf_thickness(NEWOBJ))+ZSAG
         END IF
         IF(surf_clap_type(NEWOBJ) == 5.AND.surf_array_parity(NEWOBJ) == 0) THEN
            XSTRT=WW2*(surf_clap_dim(NEWOBJ, 1)+surf_clap_dim(NEWOBJ, 4))
            YSTRT=WW1*(surf_clap_dim(NEWOBJ, 1)+surf_clap_dim(NEWOBJ, 3))
            IISURF=NEWOBJ

            CALL SAGRET(IISURF,XSTRT,YSTRT,ZSAG,SAGERR)
            IF(SAGERR) CALL HANDLESAGRETFAILURE(MSG, NEWOBJ)

            ZSTRT=(WW3*surf_thickness(NEWOBJ))+ZSAG
         END IF
         IF(surf_clap_type(NEWOBJ) == 6.AND.surf_array_parity(NEWOBJ) == 0) THEN
            XSTRT=WW2*(surf_clap_dim(NEWOBJ, 2)+surf_clap_dim(NEWOBJ, 4))
            YSTRT=WW1*(surf_clap_dim(NEWOBJ, 2)+surf_clap_dim(NEWOBJ, 3))
            IISURF=NEWOBJ

            CALL SAGRET(IISURF,XSTRT,YSTRT,ZSAG,SAGERR)
            IF(SAGERR) CALL HANDLESAGRETFAILURE(MSG, NEWOBJ)

            ZSTRT=(WW3*surf_thickness(NEWOBJ))+ZSAG
         END IF
         IF(surf_clap_type(NEWOBJ) > 1.AND.surf_clap_type(NEWOBJ) <= 4.AND.&
         &surf_array_parity(NEWOBJ) == 0) THEN
            XSTRT=WW2*(surf_clap_dim(NEWOBJ, 2)+surf_clap_dim(NEWOBJ, 4))
            YSTRT=WW1*(surf_clap_dim(NEWOBJ, 1)+surf_clap_dim(NEWOBJ, 3))
            IISURF=NEWOBJ

            CALL SAGRET(IISURF,XSTRT,YSTRT,ZSAG,SAGERR)
            IF(SAGERR) CALL HANDLESAGRETFAILURE(MSG, NEWOBJ)

            ZSTRT=(WW3*surf_thickness(NEWOBJ))+ZSAG
         END IF
      END IF
   END IF
!
!               XSTRT,YSTRT AND ZSTRT HAVE BEEN INITIALLY SET
!
!               NOW CALCULATE THE TARGET FOR THE CHIEF RAY IF RXIM AND RYIM
!               ARE IN EFFECT.
!
!       THE WAVELENGTH NUMBER FOR THE REFERENCE RAY TRACE IS:
!       INT(WW4) OR INT(SYSTEM(11))
!     CALC AIM POINTS IF RYIM OR RXIM ARE IN EFFECT
   IF(sys_ryim_fang_set().NE.sys_rxim_fang_set())&
   &call sys_set_rxim_fang_set(sys_ryim_fang_set())
   IF(sys_ryim_fang_set().NE.sys_rxim_fang_set())&
   &call sys_set_rxim(sys_ryim())
   XCHIEF_TARGET=0.0D0
   YCHIEF_TARGET=0.0D0
   IF(sys_rxim_fang_set().NE.0.0D0) THEN
      XCHIEF_TARGET=(WW2*sys_rxim())
   END IF
   IF(sys_ryim_fang_set().NE.0.0D0) THEN
      YCHIEF_TARGET=(WW1*sys_ryim())
   END IF


!
!                       THE CHIEF RAY TARGETS HAVE BEEN COMPUTED
!
9898 CONTINUE
   KKKK=KKKK+1
   KKK=0

!     THE ABOVE CONTINUATION IS THE RE-ENTRY POINT FOR CHIEF RAY
!     ITERATION
!
!       NOW WHAT ARE THE STARTING DIRECTION COSINES AND HOW ARE
!       THEY DETERMINED?
!
!     STARTING POINTS AT SURFACE NEWOBJ+1 ARE THE PARAXIAL VALUES
!     MODULATED BY ANY DECENTER OR TILT ON NEWOBJ+1
!
   IF(sys_ray_aiming().EQ.0.0D0) THEN
!     RAY AIMING IS OFF
      IF(sys_telecentric().EQ.0.0D0) THEN
!     TEL OFF
         X1AIM=PXTRAX(5,(NEWOBJ+1))
         X1AIM=(X1AIM*WW2)-surf_decenter_x(NEWOBJ+1)+sys_aim_offset_x()
         Y1AIM=PXTRAY(5,(NEWOBJ+1))
         Y1AIM=(Y1AIM*WW1)-surf_decenter_y(NEWOBJ+1)+sys_aim_offset_y()
         Z1AIM=sys_aim_offset_z()
      ELSE
!     TEL ON,ALL RAYS ARE PARALLEL
         call logger%logText("Telecentric Ray Aiming On!")
         IF(sys_scx().NE.0.0D0) X1AIM=XSTRT
         IF(sys_scx().EQ.0.0D0) X1AIM=0.0D0
         IF(sys_scy().NE.0.0D0) Y1AIM=YSTRT
         IF(sys_scy().EQ.0.0D0) Y1AIM=0.0D0
         Z1AIM=0.0D0
      END IF
   ELSE
!     RAY AIMING IS ON
      !PRINT *, "TRYY = ", TRYY
      !PRINT *, "YFOBB0 = ", YFOBB0


      X1AIM=TRYX+XFOBB0
      Y1AIM=TRYY+YFOBB0
      Z1AIM=TRYZ+ZFOBB0
   END IF

   XC=X1AIM
   YC=Y1AIM
   ZC=Z1AIM
!     SAVE VERY FIRST X1AIM,Y1AIM, Z1AIM
   STX=X1AIM
   STY=Y1AIM
   STZ=Z1AIM
!     CALL BAKONE TO CONVERT TO NEWOBJ COORDINATES
   !IF (KKK.EQ.1) THEN
   !PRINT *, "YAIMOL LINE 445 is ", YAIMOL
   !PRINT *, "YC LINE 445 is ", YC
   !END IF

   XAIMOL=XC
   YAIMOL=YC
   ZAIMOL=ZC
   R_TX=X1AIM
   R_TY=Y1AIM
   R_TZ=Z1AIM
   CALL BAKONE
   X1AIM=R_TX
   Y1AIM=R_TY
   Z1AIM=R_TZ
!       NOW THE COORDINATES ARE IN THE NEWOBJ COORD SYSTEM
!
!       FOR NOW, IGNORE THE FOLLOWING:
!               1) CLAPS/COBS AND ERASES
!       NOW THE STARTING DIRECTION COSINES ARE RELATIVELY SIMPLE.
!
!       THEY ARE CALLED LSTART,MSTART AND NSTART
!
!       FIRST CONVERT THE SURFACE 0 AND 1 COORDINATES INTO YANG AND XANG
!       ANGULAR VALUES AS IN HEXAGON AND CODE V.
!       YANG AND XANG ANGLES IN RADIANS.
   STOPP=0
   REFEXT=.TRUE.
!
!       THE STARTING COORDINATES AND DIRECTION COSINES FOR THE VERY
!       FIRST PART OF THE CHIEF OR "GUT" RAY TRACE HAVE BEEN CALCULATED
!       BASED UPON THE RESULTS OF THE CURRENT PARAXIAL RAY TRACE AND
!       ASSUMING THAT SURFACE NEWOBJ+1 IS PLANO.
!
!       NEXT STEPS
!
!       1. TRANSLATE TO THE NEXT SURFACE
!       2. IF THAT SURFACE IS TILTED AND/OR DECENTERED,
!          TRANSFORM TO THAT SURFACES COORDINATE SYSTEM.
!       3. INTERSECT THE SURFACE
!       4. INTERACT WITH THAT SURFACE
!       5. REPEAT STEP 1.
!
!       1. TRANSFER TO NEXT SURFACES COORDINATE SYSTEM LOCATED
!       AT THE NEXT SURFACES VERTEX (THIS INCLUDES THE AXIAL THICKNESS).
!       AND TILTS AND DECENTERS.
!
!       CALL TRANSF
!
!       2. INTERSECT AND INTERACT
!
!       CALL HITSUR
!
!       3. REPEAT WITH A DO LOOP
!
!       STORE THE REFERENCE RAY DATA FOR SURFACE 0 IN THE ARRAY
!       REFRY(1:50,0:MAXSUR)
!
!       KKK COUNTS THE NUMBER OF TRIES TO GET A GOOD REFERENCE
!       SURFACE ZERO POINT INTERSECTION AND LABEL 9 IS THE RE-ENTRY POINT
!
!
!       THIS IS THE RE-ENTRY POINT FOR RAY AIMING. RE-ENTRY
!       IS PERFORMED AFTER NEW VALUES OF X1AIM AND Y1AIM WERE
!       CALCULATED JUST BEFORE ENCOUNTERING THE (GO TO 9)
!       STATEMENT NEAR THE END OF THIS ROUTINE.
9  CONTINUE
   RV=.FALSE.
   KKK=KKK+1

!       IF KKK EXCEEDS NRAITR TRIES, PRINT RAY ITERRATION ERROR
!       AND STOP SEARCHING.
   IF(KKK.GT.NRAITR) THEN
      !OUTLYNE = "GOING TO SEND REF RAY FAILURE 511"
      !CALL SHOWIT(19)
!                   PRINT *, "KKK is ", KKK
!                   WRITE(OUTLYNE, *), "ERROR VECTOR IS ",
!     1   DSQRT(((CX-XCHIEF_TARGET)**2)+((CY-YCHIEF_TARGET)**2))
!                   CALL SHOWIT(19)
      GO TO 14
   ELSE
      STOPP=0
      REFEXT=.TRUE.
!        PROCEED
   END IF
!
!     COMPUTE DIR COS DIRECTLY FROM POSITIONS
   MAG=DSQRT(((XSTRT-X1AIM)**2)+((YSTRT-Y1AIM)**2)&
   &+((ZSTRT-Z1AIM)**2))
   LSTART=(X1AIM-XSTRT)/MAG
   MSTART=(Y1AIM-YSTRT)/MAG
   NSTART=(Z1AIM-ZSTRT)/MAG
   NINTY=.FALSE.
   IF(NSTART.LT.0.0D0) NINTY=.TRUE.
   IF(NINTY) RVSTART=.TRUE.
   IF(NSTART.EQ.0.0D0) THEN
      YANG=PII/2.0D0
      XANG=PII/2.0D0
   ELSE
      IF(DABS(NSTART).EQ.0.0D0) THEN
         YANG=0.0D0
      ELSE
         YANG=DATAN2(MSTART,NSTART)
      END IF
      IF(DABS(NSTART).EQ.0.0D0) THEN
         XANG=0.0D0
      ELSE
         XANG=DATAN2(LSTART,NSTART)
      END IF
   END IF
   IF(NSTART.EQ.0.0D0) THEN
      YYANG=PII/2.0D0
      XXANG=PII/2.0D0
   ELSE
      IF(DABS(NSTART).EQ.0.0D0) THEN
         YYANG=0.0D0
      ELSE
         YYANG=DATAN2(MSTART,NSTART)
      END IF
      IF(DABS(NSTART).EQ.0.0D0) THEN
         XXANG=0.0D0
      ELSE
         XXANG=DATAN2(LSTART,NSTART)
      END IF
   END IF
!
!       FOR SURFACE NEWOBJ
!       REFRY(1,SURF)=X(LOCAL)- COORDINATE AT SURFACE I
!       REFRY(2,SURF)=Y(LOCAL)- COORDINATE AT SURFACE I
!       REFRY(3,SURF)=Z(LOCAL)- COORDINATE AT SURFACE I
!       REFRY(4,SURF)=L(LOCAL)- DIRECTION COSINE AT SURFACE I
!       REFRY(5,SURF)=M(LOCAL)- DIRECTION COSINE AT SURFACE I
!       REFRY(6,SURF)=N(LOCAL)- DIRECTION COSINE AT SURFACE I
!       REFRY(8,SURF)=LEN - PHYSICAL LENGTH ALONG RAY FROM SURF I-1 TO I
!       REFRY(7,SURF)=OPL - OPL FROM SURFACE I-1 TO I
!       REFRY(9,SURF)=COSINE(I)
!       REFRY(10,SURF)=COSINE(IP)
!       REFRY(11,SURF)=UX - (RADIAN) MARGINAL XZ SLOPE ANGLE
!       REFRY(12,SURF)=UY - (RADIAN) MARGINAL YZ SLOPE ANGLE
!       REFRY(13,SURF)=LN - SURFACE NORMAL DIRECTION COSINE
!       REFRY(14,SURF)=MN - SURFACE NORMAL DIRECTION COSINE
!       REFRY(15,SURF)=NN - SURFACE NORMAL DIRECTION COSINE
!       REFRY(16,SURF)=XOLD
!       REFRY(17,SURF)=YOLD
