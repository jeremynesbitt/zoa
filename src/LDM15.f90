      call set_surf_grating_order(I, 0.0D0)
      call set_surf_grating_spacing(I, 0.0D0)
      call set_surf_grating_vx(I, 0.0D0)
      call set_surf_grating_vy(I, 0.0D0)
      call set_surf_grating_vz(I, 0.0D0)
      call set_surf_spgr(I, 0.0D0)
      call set_surf_default_flag(I, 0)
      call set_surf_mtracei_nx(I, 0.0D0)
      call set_surf_mtracei_ny(I, 0.0D0)
      call set_surf_psfbin_data(I, 0.0D0)
      call set_surf_profit_data(I, 0.0D0)
      ALENS(108,I)=0.0D0
      ALENS(109,I)=0.0D0
      call set_surf_mirror_thickness(I, 0.0D0)
      call set_surf_tilt_flag(I, 0)
      call set_surf_alpha(I, 0.0D0)
      call set_surf_beta(I, 0.0D0)
      call set_surf_gamma(I, 0.0D0)
      call set_surf_alpha_deg(I, 0.0D0)
      call set_surf_beta_deg(I, 0.0D0)
      call set_surf_gamma_deg(I, 0.0D0)
   END IF
   I=INT(SYSTEM(20))-1
   IF(GLANAM(I,2).EQ.'PERFECT      ') THEN
      IF(SYSTEM(30).GE.3.0D0) THEN
         IF(SYSTEM(30).EQ.3.0D0) SYSTEM(30)=1.0D0
         IF(SYSTEM(30).EQ.4.0D0) SYSTEM(30)=2.0D0
         OUTLYNE='"PERFECT" SURFACE IN USE. LENS MODE HAS BEEN'
         CALL SHOWIT(1)
         IF(SYSTEM(30).EQ.1.0D0) OUTLYNE='RESET TO THE "FOCAL" MODE'
         IF(SYSTEM(30).EQ.2.0D0) OUTLYNE='RESET TO THE "UFOCAL" MODE'
         CALL SHOWIT(1)
      END IF
   END IF
!     RESOLVE CHARACTERISTICS OF IDEAL LENSES
   I=INT(SYSTEM(20))-1
   IF(GLANAM(I,2).EQ.'IDEAL        ') THEN
      ALENS(1:2,I)=0.0D0
      ALENS(4:9,I)=0.0D0
      call set_surf_multi_clap_flag(I, 0)
      ALENS(23:24,I)=0.0D0
      call set_surf_decenter_flag(I, 0)
      ALENS(30:43,I)=0.0D0
      ALENS(45,I)=0.0D0
      call set_surf_cobs_ape_type(I, 0)
      call set_surf_pivot_flag(I, 0)
      call set_surf_cobs_era_type(I, 0)
      call set_surf_dummy_val(I, 1)
      call set_surf_decenter_z(I, 0.0D0)
      call set_surf_ret_surf_num(I, 0)
      ALENS(77:110,I)=0.0D0
      ALENS(25:28,I)=0.0D0
      ALENS(114:120,I)=0.0D0
      I=INT(SYSTEM(20))
      ALENS(1:2,I)=0.0D0
      ALENS(4:9,I)=0.0D0
      call set_surf_multi_clap_flag(I, 0)
      ALENS(23:24,I)=0.0D0
      ALENS(29:30,I)=0.0D0
      ALENS(114:116,I)=0.0D0
      ALENS(31:43,I)=0.0D0
      ALENS(45,I)=0.0D0
      call set_surf_cobs_ape_type(I, 0)
      call set_surf_pivot_flag(I, 0)
      call set_surf_cobs_era_type(I, 0)
      call set_surf_dummy_val(I, 1)
      call set_surf_decenter_z(I, 0.0D0)
      call set_surf_ret_surf_num(I, 0)
      ALENS(77:110,I)=0.0D0
      ALENS(25:28,I)=0.0D0
      ALENS(118:120,I)=0.0D0
   END IF
   I=INT(SYSTEM(20))-1
   IF(GLANAM(I,2).EQ.'IDEAL        ') THEN
      IF(SYSTEM(30).GE.3.0D0) THEN
         IF(SYSTEM(30).EQ.3.0D0) SYSTEM(30)=1.0D0
         IF(SYSTEM(30).EQ.4.0D0) SYSTEM(30)=2.0D0
         OUTLYNE='"IDEAL" SURFACE IN USE. LENS MODE HAS BEEN'
         CALL SHOWIT(1)
         IF(SYSTEM(30).EQ.1.0D0) OUTLYNE='RESET TO THE "FOCAL" MODE'
         IF(SYSTEM(30).EQ.2.0D0) OUTLYNE='RESET TO THE "UFOCAL" MODE'
         CALL SHOWIT(1)
      END IF
   END IF
!
!
!     ADDED ON 3/17/93
!     SET UP THE FAST ACCESS CFG ARRAYS FOR OPTIMIZATION
!     IF THERE ARE CONFIGS

   IF(INT(SYSTEM(56)).GT.1.AND.LNSTYP.NE.3) CALL AUXCFG
   OUTLYNE = "JUST FINISHED AUXCFG CHECK"
   CALL SHOWIT(19)
!
!     INITIALIZE THE FRONT FOCAL LENGTH, BACK FOCAL LENGTH
!     AND ENTRANCE TO EXIT PUPIL MAG VARIABLES BASED ON
!     PARAXIAL DATA
!
   RFFLX=0.0D0
   RFFLY=0.0D0
   RBFLX=0.0D0
   RBFLY=0.0D0
   RMAGX=0.0D0
   RMAGY=0.0D0
   MAGXOR=0.0D0
   MAGYOR=0.0D0
!
   IF(PXTRAX(6,NEWREF).NE.0.0D0) THEN
      MAGXOR=PXTRAX(6,NEWOBJ)/PXTRAX(6,NEWREF)
   ELSE
      IF(PXTRAX(6,NEWOBJ).GE.0.0D0) MAGXOR=1.0D10
      IF(PXTRAX(6,NEWOBJ).LT.0.0D0) MAGXOR=-1.0D10
   END IF
   IF(PXTRAY(6,NEWREF).NE.0.0D0) THEN
      MAGYOR=PXTRAY(6,NEWOBJ)/PXTRAY(6,NEWREF)
   ELSE
      IF(PXTRAY(6,NEWOBJ).GE.0.0D0) MAGYOR=1.0D10
      IF(PXTRAY(6,NEWOBJ).LT.0.0D0) MAGYOR=-1.0D10
   END IF
!
!     RFFLX,RFFLY,RBFLX,RBFLY
!
   I=NEWOBJ
   J=NEWIMG-1
   IF(((PXTRAY(2,I)*PXTRAY(6,J))-(PXTRAY(6,I)*PXTRAY(2,J))).NE.0.0D0) THEN
      REFLY=-(((PXTRAY(2,I)*PXTRAY(5,I+1))-(PXTRAY(1,I+1)*PXTRAY(6,I )))/((PXTRAY(2,I)*PXTRAY(6,J))-(PXTRAY(6,I)*PXTRAY(2,J))))
   ELSE
      REFLY=1.0D20
   END IF
   IF(((PXTRAX(2,I)*PXTRAX(6,J))-(PXTRAX(6,I)*PXTRAX(2,J))).NE.0.0D0) THEN
      REFLX=-(((PXTRAX(2,I)*PXTRAX(5,I+1))-(PXTRAX(1,I+1)*PXTRAX(6,I )))/((PXTRAX(2,I)*PXTRAX(6,J))-(PXTRAX(6,I)*PXTRAX(2,J))))
   ELSE
      REFLX=1.0D20
   END IF
   IF(((PXTRAY(2,I)*PXTRAY(6,J))-(PXTRAY(6,I)*PXTRAY(2,J))).NE.0.0D0) THEN
      RBFLY=-(((PXTRAY(2,I)*PXTRAY(5,J))-(PXTRAY(6,I)*PXTRAY(1,J)))/((PXTRAY(2,I)*PXTRAY(6,J))-(PXTRAY(6,I)*PXTRAY(2,J))))
   ELSE
      RBFLY=1.0D20
   END IF
   IF(((PXTRAX(2,I)*PXTRAX(6,J))-(PXTRAX(6,I)*PXTRAX(2,J))).NE.0.0D0) THEN
      RBFLX=-(((PXTRAX(2,I)*PXTRAX(5,J))-(PXTRAX(6,I)*PXTRAX(1,J)))/((PXTRAX(2,I)*PXTRAX(6,J))-(PXTRAX(6,I)*PXTRAX(2,J))))
   ELSE
      RBFLX=1.0D20
   END IF
   IF(((PXTRAY(2,I)*PXTRAY(6,J))-(PXTRAY(6,I)*PXTRAY(2,J))).NE.0.0D0) THEN
      RFFLY=-(((PXTRAY(1,I+1)*PXTRAY(6,J))-(PXTRAY(2,J)*PXTRAY(5,I+1 )))/((PXTRAY(2,I)*PXTRAY(6,J))-(PXTRAY(6,I)*PXTRAY(2,J))))
   ELSE
      RFFLY=1.0D20
   END IF
   IF(((PXTRAX(2,I)*PXTRAX(6,J))-(PXTRAX(6,I)*PXTRAX(2,J))).NE.0.0D0) THEN
      RFFLX=-(((PXTRAX(1,I+1)*PXTRAX(6,J))-(PXTRAX(2,J)*PXTRAX(5,I+1 )))/((PXTRAX(2,I)*PXTRAX(6,J))-(PXTRAX(6,I)*PXTRAX(2,J))))
   ELSE
      RFFLX=1.0D20
   END IF
!
!     F-NUMBERS
!
   IF(PXTRAX(2,NEWOBJ).NE.0.0D0) THEN
      RFFNX=-0.5D0/PXTRAX(2,NEWOBJ)
   ELSE
      IF(RFFLX.GE.0.0D0) RFFNX=1.0D10
      IF(RFFLX.LT.0.0D0) RFFNX=-1.0D10
   END IF
   IF(PXTRAY(2,NEWOBJ).NE.0.0D0) THEN
      RFFNY=-0.5D0/PXTRAY(2,NEWOBJ)
   ELSE
      IF(RFFLY.GE.0.0D0) RFFNY=1.0D10
      IF(RFFLY.LT.0.0D0) RFFNY=-1.0D10
   END IF
   IF(PXTRAX(2,NEWIMG).NE.0.0D0) THEN
      RBFNX=-0.5D0/PXTRAX(2,NEWIMG)
   ELSE
      IF(RBFLX.GE.0.0D0) RBFNX=1.0D10
      IF(RBFLX.LT.0.0D0) RBFNX=-1.0D10
   END IF
   IF(PXTRAY(2,NEWIMG).NE.0.0D0) THEN
      RBFNY=-0.5D0/PXTRAY(2,NEWIMG)
   ELSE
      IF(RBFLY.GE.0.0D0) RBFNY=1.0D10
      IF(RBFLY.LT.0.0D0) RBFNY=-1.0D10
   END IF
!     RMAGX AND RMAGY
   IF(PXTRAX(6,NEWOBJ).NE.0.0D0) THEN
      RMAGX=PXTRAX(6,NEWIMG)/PXTRAX(6,NEWOBJ)
   ELSE
      IF(PXTRAX(6,NEWOBJ).GE.0.0D0) RMAGX=1.0D10
      IF(PXTRAX(6,NEWOBJ).LT.0.0D0) RMAGX=-1.0D10
   END IF
   IF(PXTRAY(6,NEWOBJ).NE.0.0D0) THEN
      RMAGY=PXTRAY(6,NEWIMG)/PXTRAY(6,NEWOBJ)
   ELSE
      IF(PXTRAY(6,NEWOBJ).GE.0.0D0) RMAGY=1.0D10
      IF(PXTRAY(6,NEWOBJ).LT.0.0D0) RMAGY=-1.0D10
   END IF
!     PARAXIAL DEFAULT PUPIL DATA
!     NOW THE ENTRANCE AND EXIT PUPIL POSITIONS
   IF(DABS(PXTRAY(6,1)).EQ.0.0) THEN
      ENPOSY=-(surf_thickness(0))
   ELSE
      !ENPOSY=(-PXTRAY(5,1)/PXTRAY(6,1))
      ! JN:  The entrance pupil position was not being
      ! calculated per standard convention for infinite
      ! object distance.  The chief ray angle that was
      ! being used was the angle after surface 1, not
      ! the angle at the object plane. Updated this to
      ! use the angle at the object plane (0 index)
      ENPOSY=(-PXTRAY(5,1)/PXTRAY(6,0))
   END IF
   IF(DABS(PXTRAX(6,1)).EQ.0.0D0) THEN
      ENPOSX=-(surf_thickness(0))
   ELSE
      !ENPOSX=(-PXTRAX(5,1)/PXTRAX(6,1))
      ENPOSX=(-PXTRAX(5,1)/PXTRAX(6,0))
   END IF
   ENPUX=0.0D0
   ENPUY=0.0D0
   ENPUZ=(ENPOSX+ENPOSY)/2.0D0
!     NOW THE EXIT PUPIL POSITIONS
   IF(DABS(PXTRAY(6,INT(SYSTEM(20)))).EQ.0.0D0) THEN
      EXPOSY=surf_thickness(0)
   ELSE
      EXPOSY=(-PXTRAY(5,INT(SYSTEM(20)))/PXTRAY(6,INT(SYSTEM(20))))
   END IF
   IF(DABS(PXTRAX(6,INT(SYSTEM(20)))).EQ.0.0D0) THEN
      EXPOSX=surf_thickness(0)
   ELSE
      EXPOSX=(-PXTRAX(5,INT(SYSTEM(20)))/PXTRAX(6,INT(SYSTEM(20))))
   END IF
   EXPUX=0.0D0
   EXPUY=0.0D0
   EXPUZ=(EXPOSX+EXPOSY)/2.0D0
!     NOW THE ENTRANCE AND EXIT PUPIL DIAMETERS
   ENDIAX=2.0D0*DABS(PXTRAX(1,1)+(ENPUZ*PXTRAX(2,0)))
   ENDIAY=2.0D0*DABS(PXTRAY(1,1)+(ENPUZ*PXTRAY(2,0)))
   EXDIAX=2.0D0*DABS(PXTRAX(1,INT(SYSTEM(20)))+(EXPUZ *PXTRAX(2,INT(SYSTEM(20)))))
   EXDIAY=2.0D0*DABS(PXTRAY(1,INT(SYSTEM(20)))+(EXPUZ *PXTRAY(2,INT(SYSTEM(20)))))

   !JN:  Now that we have the entrance and exit pupils, call
   ! RATRA one more time such that we can properly trace
   ! the marginal ray for finite conjugate systems
   call curr_par_ray_trace%calculateFirstOrderParameters(curr_lens_data)
   CALL PRTRA
!
!     NOW SET THE INR FOR PHASE SURFACE RAY TRACES
!     THE INR IS THE LARGER OF THE ABSOLUTE VALUE OF THE
!     X-CLAP PLUS X-CLAP OFFSET OR THE Y-CLAP PLUS Y-CLAP OFFSET
!     IF THERE IS A CLAP. IF NO CLAP, IT IS THE SUM OF THE ABSOLUTE
!     VALUES OF THE PY PLUS PCY AT THAT SURFACE
   DO I=0,INT(SYSTEM(20))
      IF(surf_inr_flag(I).EQ.0.0D0) THEN
!     MAKE A DEFAULT ASSIGNMENT
         IF(surf_clap_type(I) /= 0.AND.surf_multi_clap_flag(I) == 0) THEN
!     USE CLAP DATA
            IF(surf_clap_type(I) /= 5) THEN
               A1=DABS(surf_clap_dim(I, 1))+DABS(surf_clap_dim(I, 3))
               A2=DABS(surf_clap_dim(I, 2))+DABS(surf_clap_dim(I, 4))
            ELSE
               A1=DABS(surf_clap_dim(I, 1))+DABS(surf_clap_dim(I, 3))
               A2=DABS(surf_clap_dim(I, 1))+DABS(surf_clap_dim(I, 4))
            END IF
            IF(A1.GE.A2) THEN
               call set_surf_inr_value(I, A1)
            ELSE
               call set_surf_inr_value(I, A2)
            END IF
         ELSE
!     USE PARAX DATA
            A1=DABS(PXTRAX(1,I))+DABS(PXTRAX(5,I))
            A2=DABS(PXTRAY(1,I))+DABS(PXTRAY(5,I))
            IF(A1.GE.A2) THEN
               call set_surf_inr_value(I, A1)
            ELSE
               call set_surf_inr_value(I, A2)
            END IF
         END IF
!     NO ASSIGNMENT IS NECESSARY
      END IF
   END DO
!     11/04/96
!     RESOLVE ALL TILT RETURNS AND APPLY ALL TILT RETURN RULES
!     BY CALLING TILT_RETURN
   SYSTEM(90)=0.0D0
   DO I=0,INT(SYSTEM(20))
!     IF THE TILT RET FLAG RES WAS ON, SET THE TILT TYPE TO TILT RET
!     THEN SET RESOLUTION FLAG TO OFF OR ZERO
      IF(surf_tilt_return_flag(I) == 1) call set_surf_tilt_flag(I, 6)
      call set_surf_tilt_return_flag(I, 0)
   END DO
!     RESOLVE ALL PIVOTS WHICH BY THEIR NATURE DEFINE NEW DECENTERS
!     ON THE AFFECTED SURFACES
   DO I=0,INT(SYSTEM(20))
      IF(surf_tilt_flag(I) == 1.OR.surf_tilt_flag(I).EQ.-1.0D0.OR.surf_tilt_flag(I) == 5) THEN
         OUTLYNE = "ABOUT TO CHECK PIVDEC"
         CALL SHOWIT(19)
         IF(surf_pivot_flag(I).EQ.1.0D0) CALL PIVDEC(I)
         OUTLYNE = "JUST FINISHED PIVDEC CHECK"
         CALL SHOWIT(19)
      END IF
   END DO
   ERCODE=.FALSE.
   TRYES=.FALSE.
!     THIS CALL FINDS ILLEGAL TILT RETS AND TILT AUTOS WHICH PRECEED
!     TILT RETS AND FIXES THEM
   CALL TILT_RETURN(ERCODE,TRYES)
   IF(ERCODE) THEN
      OUTLYNE = "ERCODE IS TRUE in LNSEOS1"
      CALL SHOWIT(19)
!     THERE WERE ERRORS RESOLVING TILT RETURN, CALL MACFAL
!     AND SET ERCODE TO .FALSE.
      ERCODE=.FALSE.
!     STOP A MACRO IF ONE IS RUNNING, THEN PROCEED
      CALL MACFAL
   END IF
   IF(TRYES) THEN
!     RESOLVE TILT RETURNS INTO TILTS AND DECENTER VALUES
!     ON SURFACES
!
      CALL RETRES
      OUTLYNE = "RETRES ROUTINE FINISHED "
      CALL SHOWIT(19)
!
   END IF
!     FORCE IMAGE SURFACE TO HAVE ZERO THICKNESS
!       CHECK FOR LAST SURFACE THICKNESS NOT ZERO
!     THE FINAL SURFACE ALWAYS HAS ZERO THICKNESS
   ! JN:  test violating this
!       IF(surf_thickness(INT(SYSTEM(20))).NE.0.0D0) THEN
!       surf_thickness(INT(SYSTEM(20)))=0.0D0
!         OUTLYNE=
!      1  'WARNING: FINAL SURFACE THICKNESS WAS RESET TO ZERO'
!       CALL SHOWIT(1)
!                         END IF
   DO I=1,INT(SYSTEM(20))
      call set_surf_decenter_flag(I, 0)
      IF(surf_decenter_y(I).NE.0.0D0.OR.surf_decenter_x(I).NE.0.0D0.OR.surf_decenter_z(I).NE.0.0D0) call set_surf_decenter_flag(I, 1)
   END DO
   IF(.NOT.REDOLNSEOS) THEN
      IF(SYSTEM(94).NE.0.0D0.OR.SYSTEM(98).NE.0.0D0) THEN
         CALL REDOXOBJ(REDOLNSEOS)
         OUTLYNE = "REDOXOBJ FINISHED"
         CALL SHOWIT(19)
      END IF
      IF(SYSTEM(95).NE.0.0D0.OR.SYSTEM(99).NE.0.0D0) THEN
         CALL REDOYOBJ(REDOLNSEOS)
         OUTLYNE = "REDOYOBJ FINISHED"
         CALL SHOWIT(19)
      END IF
   ELSE
!     DONE
      RETURN
   END IF
   IF(REDOLNSEOS) THEN
      F1=0
      F6=1
      F22=0
      GO TO 999
   END IF
   ! Update the NEW data structures
   call check_clear_apertures(curr_lens_data)
   call curr_asph_data%updateAsphereTable(INT(SYSTEM(20))+1)
   call sysConfig%updateParameters()

   RETURN
END
SUBROUTINE REDOXOBJ(REDOLNSEOS)
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
   REAL*8 A,B,JK_FACTOR
   LOGICAL REDOLNSEOS
!
   JK_FACTOR=1.0D0
   IF(SYSTEM(94).EQ.1.0D0.OR.SYSTEM(98).EQ.1.0D0) THEN
!     IMAGE ANGLES SCALE
      B=PXTRAX(6,INT(SYSTEM(20)))
      IF(B.NE.0.0D0) THEN
         IF(SYSTEM(94).EQ.1.0D0)JK_FACTOR=DTAN(PII*SYSTEM(92)/180.0D0)/B
         IF(SYSTEM(98).EQ.1.0D0)JK_FACTOR=DTAN(PII*SYSTEM(96)/180.0D0)/B
      END IF
   ELSE
!     IMAGE HT SCALE
      B=PXTRAX(5,INT(SYSTEM(20)))
      IF(B.NE.0.0D0) THEN
         IF(SYSTEM(94).EQ.-1.0D0)JK_FACTOR=SYSTEM(92)/B
         IF(SYSTEM(98).EQ.-1.0D0)JK_FACTOR=SYSTEM(96)/B
      END IF
   END IF
!     SET SCX
   SYSTEM(16)=SYSTEM(16)*JK_FACTOR
   SYSTEM(17)=0.0D0
   SYSTEM(19)=0.0D0
   SYSTEM(52)=1.0D0
   SYSTEM(54)=0.0D0
   SYSTEM(61)=1.0D0
   REDOLNSEOS=.TRUE.
   RETURN
END
SUBROUTINE REDOYOBJ(REDOLNSEOS)
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
   REAL*8 A,B,JK_FACTOR
   LOGICAL REDOLNSEOS
!
   JK_FACTOR=1.0D0
   IF(SYSTEM(95).EQ.1.0D0.OR.SYSTEM(99).EQ.1.0D0) THEN
!     IMAGE ANGLES SCALE
      B=PXTRAY(6,INT(SYSTEM(20)))
      IF(B.NE.0.0D0) THEN
         IF(SYSTEM(95).EQ.1.0D0)JK_FACTOR=DTAN(PII*SYSTEM(93)/180.0D0)/B
         IF(SYSTEM(99).EQ.1.0D0)JK_FACTOR=DTAN(PII*SYSTEM(97)/180.0D0)/B
      END IF
   ELSE
!     IMAGE HT SCALE
      B=PXTRAY(5,INT(SYSTEM(20)))
      IF(B.NE.0.0D0) THEN
         IF(SYSTEM(95).EQ.-1.0D0)JK_FACTOR=SYSTEM(93)/B
         IF(SYSTEM(99).EQ.-1.0D0)JK_FACTOR=SYSTEM(97)/B
      END IF
   END IF
!     SET SCY
   SYSTEM(14)=SYSTEM(14)*JK_FACTOR
   SYSTEM(15)=0.0D0
   SYSTEM(18)=0.0D0
   SYSTEM(51)=1.0D0
   SYSTEM(53)=0.0D0
   SYSTEM(60)=1.0D0
   REDOLNSEOS=.TRUE.
   RETURN
END
