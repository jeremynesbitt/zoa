! SUB LNSEOS.FOR
SUBROUTINE LNSEOS
   USE GLOBALS
   use zoa_ui_callbacks, only: notify_replot, notify_refresh_status
!
!     CALLS LNSEOS1. THIS IS HERE TO SUPPORT AUTOFUNC
!
   use DATSUB
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
   LOGICAL SAV2,RES2,EXIS49
!
   INTEGER I,J,OF5,OF6,ALLOERR
   COMMON/OLDFVAL/OF5,OF6
!
   OF5=F5
   OF6=F6

   OUTLYNE = "LNSEOS ROUTINE STARTING! "
   CALL SHOWIT(19)

!
   IF(CFLDCNT.EQ.0) THEN
!       SET UP THREE FIELD OF VIEW POSITIONS AT 0.7 AND 1.0
      CFLDCNT=3
      CFLDS(1,1)=0.0D0
      CFLDS(1,2)=0.0D0
      CFLDS(1,3)=0.0D0
      CFLDS(2,1)=0.0D0
      CFLDS(2,2)=0.7D0
      CFLDS(2,3)=1.0D0
   END IF
!     RESTORE DECENTERS TO CONVER PIVOTS
   IF(SYSTEM(20).GT.DBLE(MAXSUR)) SYSTEM(20)=DBLE(MAXSUR)
   I=INT(SYSTEM(20))
   ALENS(29,1:I) = 0.0D0
   ALENS(30,1:I) = ALENS(114,1:I)
   ALENS(31,1:I) = ALENS(115,1:I)
   ALENS(69,1:I) = ALENS(116,1:I)
   ALENS(26,1:I) = ALENS(118,1:I)
   ALENS(27,1:I) = ALENS(119,1:I)
   ALENS(28,1:I) = ALENS(120,1:I)
   DO I=0,INT(SYSTEM(20))
      IF(surf_decenter_x(I).NE.0.0D0.OR.surf_decenter_y(I).NE.0.0D0.OR.surf_decenter_z(I).NE.0.0D0) call set_surf_decenter_flag(I, 1)
   END DO
!
   IF(INT(SYSTEM(91)).EQ.0) F57=0
!
   IF(F57.EQ.0) THEN
!       ALLOCATE THE MULTIHIT ARRAYS AND P1ARAY
      DEALLOCATE(GLOBAL_MULTIRAY_DATA,GLOBAL_MULTIREF_DATA,MULTIRAY_DATA,MULTIREF_DATA,MULTI_DATA,P1ARAY,STAT=ALLOERR)
      IF(.NOT.ALLOCATED(MULTIRAY_DATA)) THEN
         IF(SYSTEM(102).EQ.1.0D0) THEN
!       AN NSS TUBE SURFACE IS PRESENT, ALLOCATE MEMORY
            DEALLOCATE(GLOBAL_MULTIRAY_DATA,GLOBAL_MULTIREF_DATA,MULTIRAY_DATA,MULTIREF_DATA,MULTI_DATA,P1ARAY,STAT=ALLOERR)
            ALLOCATE(GLOBAL_MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS),GLOBAL_MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS),MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS),MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS),MULTI_DATA(1:50,0:MAXSUR,1:MRAYS),P1ARAY(0:360,1:3,1:MRAYS),STAT=ALLOERR)
            GLOBAL_MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
            GLOBAL_MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
            MULTI_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
            MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
            MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
         ELSE
            DEALLOCATE(P1ARAY,STAT=ALLOERR)
            ALLOCATE(P1ARAY(0:360,1:3,1:MRAYS),STAT=ALLOERR)
         END IF
      END IF
      CALL LNSEOS1
      OUTLYNE = "LNSEOS1 CALL FINISHED 81"
      CALL SHOWIT(19)
      IF(OF5.EQ.1) THEN
!     SET PARAXIAL FLAGS TO FALSE
         CALL PRTRB
         OUTLYNE = "DONE WITH PRTRB"
         CALL SHOWIT(19)
         OUTLYNE = "ABOUT TO CALL PRTRC"
         CALL SHOWIT(19)
         CALL PRTRC
         OUTLYNE = "DONE WITH PRTRC"
         CALL SHOWIT(19)
         CALL PRTRD
         OUTLYNE = "DONE WITH PRTRD"
         CALL SHOWIT(19)
      END IF
      ! JN:  Added this to force replot after lens update
      ! TODO:  There are multiple returns in this sub
      ! Not sure If I should do this in all exits
      call notify_replot()
      call notify_refresh_status()
      RETURN
   END IF
   IF(F57.EQ.1) THEN
      F57=0
!       ALLOCATE THE MULTIHIT ARRAYS AND P1ARAY
      DEALLOCATE(GLOBAL_MULTIRAY_DATA,GLOBAL_MULTIREF_DATA,MULTIRAY_DATA,MULTIREF_DATA,MULTI_DATA,P1ARAY,STAT=ALLOERR)
      IF(.NOT.ALLOCATED(MULTIRAY_DATA)) THEN
         IF(SYSTEM(102).EQ.1.0D0) THEN
!       AN NSS TUBE SURFACE IS PRESENT, ALLOCATE MEMORY
            DEALLOCATE(GLOBAL_MULTIRAY_DATA,GLOBAL_MULTIREF_DATA,MULTIRAY_DATA,MULTIREF_DATA,P1ARAY,MULTI_DATA,STAT=ALLOERR)
            ALLOCATE(GLOBAL_MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS),GLOBAL_MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS),MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS),MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS),MULTI_DATA(1:50,0:MAXSUR,1:MRAYS),P1ARAY(0:360,1:3,1:MRAYS),STAT=ALLOERR)
            GLOBAL_MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
            GLOBAL_MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
            MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
            MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
            MULTI_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
         ELSE
            DEALLOCATE(P1ARAY,STAT=ALLOERR)
            ALLOCATE(P1ARAY(0:360,1:3,1:MRAYS),STAT=ALLOERR)
         END IF
      END IF
      CALL LNSEOS1
      OUTLYNE = "LNSEOS1 CALL FINISHED 120"
      CALL SHOWIT(19)
      IF(OF5.EQ.1) THEN
!     SET PARAXIAL FLAGS TO FALSE
         CALL PRTRB
         CALL PRTRC
         CALL PRTRD
      END IF
      F57=1
      F1=0
      F6=1
      F22=1
      LNSTYP=2
   END IF
!
   IF(F6.EQ.1) THEN
      IF(F57.EQ.1) THEN
         F57=0
!       ALLOCATE THE MULTIHIT ARRAYS AND P1ARAY
         DEALLOCATE(GLOBAL_MULTIRAY_DATA,GLOBAL_MULTIREF_DATA,MULTIRAY_DATA,MULTIREF_DATA,MULTI_DATA,P1ARAY,STAT=ALLOERR)
         IF(.NOT.ALLOCATED(MULTIRAY_DATA)) THEN
            IF(SYSTEM(102).EQ.1.0D0) THEN
!       AN NSS TUBE SURFACE IS PRESENT, ALLOCATE MEMORY
               DEALLOCATE(GLOBAL_MULTIRAY_DATA,GLOBAL_MULTIREF_DATA,MULTIRAY_DATA,MULTIREF_DATA,P1ARAY,MULTI_DATA,STAT=ALLOERR)
               ALLOCATE(GLOBAL_MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS),GLOBAL_MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS),MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS),MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS),MULTI_DATA(1:50,0:MAXSUR,1:MRAYS),P1ARAY(0:360,1:3,1:MRAYS),STAT=ALLOERR)
               GLOBAL_MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
               GLOBAL_MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
               MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
               MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
               MULTI_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
            ELSE
               DEALLOCATE(P1ARAY,STAT=ALLOERR)
               ALLOCATE(P1ARAY(0:360,1:3,1:MRAYS),STAT=ALLOERR)
            END IF
         END IF
         CALL LNSEOS1
         OUTLYNE = "LNSEOS1 CALL FINISHED 165"
         CALL SHOWIT(19)
!       REFRESH 357 ABERRATIONS
         IF(OF5.EQ.1) THEN
!     SET PARAXIAL FLAGS TO FALSE
            CALL PRTRB
            CALL PRTRC
            CALL PRTRD
         END IF
!     AUTOFUNC (binary macro merit functions) removed
         OUTLYNE='AUTOFUNC REQUIRES BINARY MACRO FUNCTIONS (FUN01-FUN10)'
         CALL SHOWIT(1)
         OUTLYNE='BINARY MACRO SYSTEM REMOVED - AUTOFUNC NOT SUPPORTED'
         CALL SHOWIT(1)
         CALL MACFAL
         GO TO 5000
         F1=0
         F6=1
         F22=1
         LNSTYP=2
!       ALLOCATE THE MULTIHIT ARRAYS AND P1ARAY
         DEALLOCATE(GLOBAL_MULTIRAY_DATA,GLOBAL_MULTIREF_DATA,MULTIRAY_DATA,MULTIREF_DATA,MULTI_DATA,P1ARAY,STAT=ALLOERR)
!       DO WE NEED TO ALLOCATE THE MULTIHIT ARRAYS
         IF(.NOT.ALLOCATED(MULTIRAY_DATA)) THEN
            IF(SYSTEM(102).EQ.1.0D0) THEN
!       AN NSS TUBE SURFACE IS PRESENT, ALLOCATE MEMORY
               DEALLOCATE(GLOBAL_MULTIRAY_DATA,GLOBAL_MULTIREF_DATA,MULTIRAY_DATA,MULTIREF_DATA,MULTI_DATA,P1ARAY,STAT=ALLOERR)
               ALLOCATE(GLOBAL_MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS),GLOBAL_MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS),MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS),MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS),MULTI_DATA(1:50,0:MAXSUR,1:MRAYS),P1ARAY(0:360,1:3,1:MRAYS),STAT=ALLOERR)
               GLOBAL_MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
               GLOBAL_MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
               MULTIRAY_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
               MULTIREF_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
               MULTI_DATA(1:50,0:MAXSUR,1:MRAYS)=0.0D0
            ELSE
               DEALLOCATE(P1ARAY,STAT=ALLOERR)
               ALLOCATE(P1ARAY(0:360,1:3,1:MRAYS),STAT=ALLOERR)
            END IF
         END IF
         CALL LNSEOS1
         OUTLYNE = "LNSEOS1 CALL FINISHED 510"
         CALL SHOWIT(19)
         IF(OF5.EQ.1) THEN
!     SET PARAXIAL FLAGS TO FALSE
            CALL PRTRB
            CALL PRTRC
            CALL PRTRD
         END IF
         F57=1
      END IF
   END IF
5000 CONTINUE
   call LogTermFOR("After LNNSEOS1 I presume?")
   RETURN
END
SUBROUTINE TILT_RETURN(ERCODE,TRYES)
   use DATSUB
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
   INTEGER I,J
   LOGICAL ERCODE,TRYES
!     TEST FOR NESTED TILT RETS
   DO I=0,INT(SYSTEM(20))
      IF(surf_tilt_flag(I) == 6) THEN
         TRYES=.TRUE.
!     FOUND A TILT RET ON SURFACE I
!     THAT TILT RET MUST NOT REFER TO A SURFACE WITH A TILT RET ON IT
         J=INT(surf_ret_surf_num(I))
         IF(surf_tilt_flag(J) == 6) THEN
!     TILT RET REFERS TO A SURFACE WHICH HAS A TILT RET ON IT, DROP THE
!     TILT RETURN
            ALENS(25:28,I)=0.0D0
            ALENS(118:120,I)=0.0D0
            call set_surf_ret_surf_num(I, 0)
            ALENS(77:80,I)=0.0D0
            call set_surf_pivot_flag(I, 0)
            ALENS(90:95,I)=0.0D0
            ERCODE=.TRUE.
            WRITE(OUTLYNE,*)'"TILT RET" ON SURFACE ',I
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'REFERS TO SURFACE ',J,' WHICH ITSELF HAS'
            CALL SHOWIT(1)
            OUTLYNE='A "TILT RET" ASSIGNED TO IT'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'THE "TILT RET" ON SURFACE ',I,' IS BEING DELETED'
            CALL SHOWIT(1)
         END IF
      ELSE
!     NO TILT RET ON SURFACE I
      END IF
   END DO
   DO I=0,INT(SYSTEM(20))
      IF(surf_tilt_flag(I) == 6) THEN
!     TILT RET
         DO J=0,I
            IF(surf_tilt_flag(J) == 2.OR.surf_tilt_flag(J) == 3) THEN
!     PREVIOUS TILT AUTO FOUND
!     DELETE THE TILT RET ON I
               ALENS(25:28,I)=0.0D0
               ALENS(118:120,I)=0.0D0
               call set_surf_ret_surf_num(I, 0)
               ALENS(77:80,I)=0.0D0
               call set_surf_pivot_flag(I, 0)
               ALENS(90:95,I)=0.0D0
               ERCODE=.TRUE.
               WRITE(OUTLYNE,*)'"TILT RET" ON SURFACE ',I
               CALL SHOWIT(1)
               WRITE(OUTLYNE,*)'IS PRECEEDED BY A "TILT AUTO(M)" ON SURFACE',J
               CALL SHOWIT(1)
               WRITE(OUTLYNE,*)'THE "TILT RET" ON SURFACE ',I,' IS BEING DELETED'
               CALL SHOWIT(1)
            ELSE
!     TO PREV TILT AUTO FOUND
            END IF
         END DO
      ELSE
!     NO TILT RET
      END IF
   END DO
   DO I=0,INT(SYSTEM(20))
      IF(surf_tilt_flag(I) == 6) THEN
!     TILT RET
         IF(INT(surf_ret_surf_num(I)).GE.I) THEN
            ERCODE=.TRUE.
            WRITE(OUTLYNE,*)'"TILT RET" ON SURFACE ',I
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'REFERS TO A FOLLOWING SURFACE'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'THE "TILT RET" ON SURFACE ',I,' IS BEING DELETED'
            CALL SHOWIT(1)
         ELSE
!     REFERS TO EARLIER SURFACE
         END IF
      ELSE
!     NO TILT RET
      END IF
   END DO
   RETURN
END
! SUB LNSEOS1.FOR
SUBROUTINE LNSEOS1
   use global_widgets
   use kdp_data_types, only: check_clear_apertures
   use paraxial_ray_trace_test, only: PRTRA_NEW
   USE GLOBALS

!     NOTE ON 11/04/96 THIS ROUTINE ONLY HAS ONE SUCCESSFUL RETURN
!     AND IT IS AT THE BOTTOM OF THE ROUTINE
!
   use DATSUB
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE LNSEOS. THIS IS THE SUBROUTINE WHICH
!       HANDLES THE COMMAND EOS WHEN ISSUED FROM THE NEW LENS INPUT
!       OR LENS UPDATE MODE.
!
   LOGICAL REDOLNSEOS,JKOFF,BADGLS,ERCODE,TRYES,LPASS1
   REAL*8 RD,INDEX,VNUM
!
   COMMON/GLSBAD/BADGLS

!
   INTEGER KIL,IST,LST,ITYPEP,CFI,CFJ,CFF12,IPASS1,IPASS2 ,KKKK,I,II,III,SLVMRK,ITT,IFF,TESTIT,J,K,ZZTOP, JJ
!
   COMMON/DPIKER/CFI,CFJ,CFF12
!
   COMMON/PTYPER/ITYPEP
!
   REAL*8 YCLAP,XCLAP,XTERM,YTERM,A1,A2,JK_TEMP
!
   LOGICAL NOSTOP,NOREF,SKIP
!


   OUTLYNE = "LNSEOS1 Routine Starting!"
   CALL SHOWIT(19)
!       IF F28 OR F31 ARE 1, WE ARE IN OPTIM OR TOLER
!       AND SOME THINGS GET SKIPPED
   SKIP=.FALSE.
   IF(F28.EQ.1.OR.F31.EQ.1.OR.LNSTYP.NE.1) SKIP=.TRUE.
!
   SELECT CASE (SKIP)
    CASE (.TRUE.)
!       FIX SECOND NUMERIC WORD IN SCY/SCX/SCY FANG/SCX FANG
!       AND ASTOP ASSIGNMENT
      NOSTOP=.TRUE.
      NOREF=.TRUE.
      IF(INT(SYSTEM(25)).GE.0.AND.INT(SYSTEM(25)).LE.INT(SYSTEM(20)))NOREF=.FALSE.
      IF(INT(SYSTEM(26)).GE.0.AND.INT(SYSTEM(26)).LE.INT(SYSTEM(20)))NOSTOP=.FALSE.
!       IF NO REF SURF AND NO STOP, MAKE REF SURF = SURFACE 1
      IF(NOREF.AND.NOSTOP) THEN
         SYSTEM(25)=1.0D0
      END IF
!       IF NO REF SURF BUT THERE IS A STOP, MAKE REF SURF = STOP SURF
      IF(.NOT.NOSTOP.AND.NOREF) THEN
         SYSTEM(25)=SYSTEM(26)
      END IF
      IF(.NOT.NOSTOP) THEN
!       THERE IS A STOP SURFACE, ZERO OUT SECOND NUMERIC WORDS OF
!       SCY,SCX,SCY FANG AND SCX FANG
         SYSTEM(15)=0.0D0
         SYSTEM(17)=0.0D0
         SYSTEM(22)=0.0D0
         SYSTEM(24)=0.0D0
      END IF
!
   END SELECT
!
   REDOLNSEOS=.FALSE.



!       999 IS A RE-ENTRY POINT
999 CONTINUE
   SELECT CASE (SKIP)
    CASE (.TRUE.)
!       CHECK FOR OBJECT THICKNESS ZERO
      IF(DABS(surf_thickness(0)).EQ.0.0D0) THEN
         OUTLYNE='WARNING: OBJECT THICKNESS IS ZERO'
         CALL SHOWIT(1)
         OUTLYNE='IS CHANGED TO 1.0D20'
         CALL SHOWIT(1)
         call set_surf_thickness(0, 1.0D20)
      END IF
!       CHECK FOR OBJECT THICKNESS LESS THAN SAY OR SAX
      IF(DABS(surf_thickness(0)).LT.0.01D0) THEN
         OUTLYNE='WARNING: OBJECT THICKNESS IS LESS THAN 0.01 LENS UNITS.'
         CALL SHOWIT(1)
         OUTLYNE='INCREASE THE OBJECT THICKNESS IN ORDER TO AVOID OPD'
         CALL SHOWIT(1)
         OUTLYNE='INACCURACIES'
         CALL SHOWIT(1)
      END IF
!       CHECK FOR SURFACE THICKNESS MAG GREATER THAN 1E200
!     OR LESS THAN 1.0D-20
!       THIS HELPS OVERFLOWING ON MACHINES.
      DO KIL=0,INT(SYSTEM(20))
         IF(surf_thickness(KIL).GT.1.0D200)call set_surf_thickness(KIL, 1.0D200)
         IF(surf_thickness(KIL).LT.-1.0D200)call set_surf_thickness(KIL, -1.0D200)
         IF(DABS(surf_thickness(KIL)).LT.1.0D-20)call set_surf_thickness(KIL, 0.0D0)
      END DO
!       RESET NEWOBJ,NEWREF AND NEWIMG
      CALL RESSUR
      !OUTLYNE = "JUST FINISHED RESSUR"
      !CALL SHOWIT(19)
!
      IF(SYSTEM(18).EQ.1.0D0) SYSTEM(19)=1.0D0
      IF(SYSTEM(18).EQ.0.0D0) SYSTEM(19)=0.0D0
!
!       SET REFEXT TO FALSE AND NULL TO TRUE
      IF(RAYCLEAR) THEN
         FOBYES=.FALSE.
         NULL=.TRUE.
         FAIL=.TRUE.
         RAYEXT=.FALSE.
         POLEXT=.FALSE.
         REFEXT=.FALSE.
         SPDEXT=.FALSE.
         GSPDEXT=.FALSE.
         CPFNEXT=.FALSE.
         CALL DELPSF
         OUTLYNE = "JUST FINISHED DELPSF"
         CALL SHOWIT(19)
      END IF
   END SELECT
   II=INT(SYSTEM(20))
   ALENS(109,1:II)=0.0D0
   IF(F5.EQ.1) THEN
!       LENS INPUT, UNLOAD THE GRID FILES
      ALENS(88,1:II)=0.0D0
      IPASS1=1
      IPASS2=0
      LPASS1=.FALSE.
      GRIDSUNLOADED19(0:MAXSUR)=.TRUE.
      GRIDSUNLOADED20(0:MAXSUR)=.TRUE.
      GRIDSUNLOADED22(0:MAXSUR)=.TRUE.
      CALL GRIDS(1,0,LPASS1)
      OUTLYNE = "JUST FINISHED GRIDS"
      CALL SHOWIT(19)
   END IF
!
!     RESOLVE FICT GLASS TYPE IN OPTIM AND TOLER

   IF(LNSTYP.EQ.2.OR.LNSTYP.EQ.3) THEN
!       NOW RESOLVE THE CALCULATION OF REFRACTIVE INDICES VIA
!       GLASS CATALOG SEARCHES
!
!     FIX LEADING COLON IN GLASS NAME
      DO I=0,INT(SYSTEM(20))
         IF(GLANAM(I,2)(1:1).EQ.':') GLANAM(I,2)(1:13)=GLANAM(I,2)(2:13)
         IF(GLANMP(I,2)(1:1).EQ.':') GLANMP(I,2)(1:13)=GLANMP(I,2)(2:13)
      END DO
      call logger%logText('About to call GLSRES 780')
      CALL GLSRES
      OUTLYNE = "JUST FINISHED GLSRES"
      CALL SHOWIT(19)
   END IF
!
   IF(LNSTYP.EQ.1) THEN
!
!       AS VARIOUS FEATURES ARE ADDED TO THE LENS SUBFILE, THINGS
!       SUCH AS THE PARAXIAL SOLVES OR THE PIKUPS, THEY WILL
!       REQUIRE RESOLUTION PRIOR TO A RETURN TO THE CMD LEVEL.
!
!       NOW RESOLVE THE CALCULATION OF REFRACTIVE INDICES VIA
!       GLASS CATALOG SEARCHES IF F22=1. DONE FOR THE CURRENT
!       CONFIGURATION ONLY.
!
!       PROCEED TO CHECK CATALOGS
!
!     FIX LEADING COLON IN GLASS NAME
      DO I=0,INT(SYSTEM(20))
         IF(GLANAM(I,2)(1:1).EQ.':') GLANAM(I,2)(1:13)=GLANAM(I,2)(2:13)
         IF(GLANMP(I,2)(1:1).EQ.':') GLANMP(I,2)(1:13)=GLANMP(I,2)(2:13)
      END DO
!
      BADGLS=.FALSE.
      IF(F22.EQ.1) THEN
         !call logger%logText('About to call GLSRES 806')
         CALL GLSRES
         OUTLYNE = "JUST FINISHED GLSRES"
         CALL SHOWIT(19)
         IF(BADGLS) THEN
            OUTLYNE = "BADGLS IS TRUE"
            CALL SHOWIT(19)
!     DO THE MESSAGE
            OUTLYNE='WARNING: CURRENT LENS HAS GLASS INPUT ERRORS'
            CALL SHOWIT(1)
            OUTLYNE= 'LENS PRESCRIPTION SHOULD BE CHECKED'
            CALL SHOWIT(1)
         END IF
!       NO CATALOG SEARCH IS REQUIRED
      END IF
!
!
!       NOW AT LENS INPUT LEVEL, THE LAST SURFACE ENTERED INCREMENTED THE
!       SURFACE COUNT. TO CORRECT THIS, ONLY AT F5=1, DECREASE THE SURFACE COUNT
!       BY 1.0 AND ONLY IF THERE ARE NO SOLVES OR THICKNESS
!     PIKUPS ON THIS LAST SURFACE
      IF(F5.EQ.1) THEN
!     IS THERE A THICKNESS SOLVE OR PIKUP ON SURF INT(SYSTEM(20))-1?
         IF(surf_pickup_count(INT(SYSTEM(20))-1).NE.0.0D0.OR.surf_solve_flag(INT(SYSTEM(20))-1).NE.0.0D0.OR.surf_thickness(INT(SYSTEM(20))).NE.0.0D0) THEN
!     HAS SOLVES OR PIKUPS OR THICKNESS ON I-1, DON'T REDUCE THE COUNT
         ELSE
!     NO SOLVES, PIKUPS OR THICKNESS ON I-1, DELETE THE SURFACE I
            SYSTEM(20)=SYSTEM(20)-1.0D0
         END IF
      END IF
!
      NEWIMG=SYSTEM(20)
      IF(SYSTEM(20).LT.0.0D0) SYSTEM(20)=0.0D0
      NEWIMG=SYSTEM(20)
      IF(F5.EQ.1.AND.SYSTEM(20).EQ.0.0D0) THEN
         OUTLYNE='LENS SYSTEM HAS NO SURFACES'
         CALL SHOWIT(1)
         OUTLYNE='RETURNED TO CMD LEVEL'
         CALL SHOWIT(1)
         F1=1
         F5=0
         F22=0
         SYSTEM(25)=0.0D0
         NEWREF=SYSTEM(25)
         OUTLYNE = "ABOUT TO CALL MACFAL "
         CALL SHOWIT(19)
         CALL MACFAL
         RETURN
      END IF
      IF(F6.EQ.1.AND.SYSTEM(20).EQ.0.0D0) THEN
         OUTLYNE='LENS SYSTEM HAS NO SURFACES'
         CALL SHOWIT(1)
         OUTLYNE='RETURNED TO CMD LEVEL'
         CALL SHOWIT(1)
         F1=1
         F6=0
         F22=0
         SYSTEM(25)=0.0D0
         NEWREF=SYSTEM(25)
         OUTLYNE = "ABOUT TO CALL MACFAL "
         CALL SHOWIT(19)
         CALL MACFAL
         RETURN
      END IF
