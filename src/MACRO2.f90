      &'"MDEL" REQUIRES A NON-BLANK MACRO NAME QUALIFIER WORD'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   ELSE
   END IF
   NAME=WQ
   KEY=0.0D0
!       OPEN UNIT 20 FOR I/O
!
!       ***************************************************************
   call does_mac_dat_exist(EXISJK)
!       ***************************************************************
   fileID = open_mac_dat()
!
   DO 10 I=1,MAXMAC
      READ(fileID,REC=I,ERR=10) MACNAM,MACLOC,IMACEN,OCC,STAMP
!
!       MACNAN IS THE MACRO NAME, MACLOC IS THE MACRO LOCK,
!       IMACEN IS THE MACRO LENGTH IN LINES, AND OCC IS A FLAG
!       =0 IF THE RECORD REPRESENTS A BLANK MACRO AND =1
!       REPRESENTS A NON-BLANK MACRO.
!
!       TEST IF THE ITH MACRO DIRECTORY ENTRY MATCHES THE
!       MACRO TO BE DELETED. THE MATCH MUST BE MADE FOR
!       THE MACRO NAME AND THE MACRO LOCK.
!
      IF(MACNAM.EQ.NAME.AND.MACLOC.EQ.KEY) THEN
!       THE MACRO TO BE DELETED HAS BEEN FOUND
!       WRITE THE ITH RECORD OF THE DIRECTORY WITH:
!       MACRO NAME = 'BLANK',LOCK=0,LENGTH=0,AND,OCC=0
!       THEN FILL THE APPROPRIATE MAXLL RECORDS WITH APPRORIATE
!       ZEROS AND BLANKS.
!
         WRITE(UNIT=20,REC=I) 'BLANK',0,0,0
         KLI=0
         IF(MACNAM.EQ.'FUN01   ')  KLI=1
         IF(MACNAM.EQ.'FUN02   ')  KLI=2
         IF(MACNAM.EQ.'FUN03   ')  KLI=3
         IF(MACNAM.EQ.'FUN04   ')  KLI=4
         IF(MACNAM.EQ.'FUN05   ')  KLI=5
         IF(MACNAM.EQ.'FUN06   ')  KLI=6
         IF(MACNAM.EQ.'FUN07   ')  KLI=7
         IF(MACNAM.EQ.'FUN08   ')  KLI=8
         IF(MACNAM.EQ.'FUN09   ')  KLI=9
         IF(MACNAM.EQ.'FUN10   ')  KLI=10
!
         IF(KLI.NE.0) THEN
            FUNEXT(KLI)=.FALSE.
            FCDIR1(KLI)='BLANK'
            FCDIR2(1:3,KLI)=0
         ELSE
!       NOT A FUNCTION
         END IF
!
!
!       NOW THE FILE CONTAINING THE MACRO BODY IS DELETED
         NF=I
         CALL MACFIL
         EXST=.FALSE.
         CALL DOESMACROFILEEXIST(FILNAM, EXST)
         IF(.NOT.EXST) THEN
            CALL MACFAL
            RETURN
         END IF
         call clear_file(trim(LIBMAC)//FILNAM)
         WRITE(OUTLYNE,*)'MACRO ',NAME,' DELETED'
         CALL SHOWIT(1)
         CALL CLOSE_FILE(20,1)
         RETURN
      END IF
10 CONTINUE
   CALL CLOSE_FILE(20,1)
!
!       IF YOU GOT HERE YOU DID NOT FIND A MATCH.
   IF(IN.EQ.5.AND.OUT.EQ.6) THEN
      WRITE(OUTLYNE,*)&
      &'MACRO ',NAME,' NOT FOUND'
      CALL SHOWIT(1)
   END IF
   RETURN
END
! SUB MLO.FOR
SUBROUTINE MLO
!
   use DATMAI
   IMPLICIT NONE
!
!       THE FORMAT FOR THE PROGRAM COMMAND IS:
!
!               LO (C OR Q OR CQ OR COQ),: TARGET STRING
!       OR      LO (C OR Q OR CQ OR COQ) : TARGET STRING
!
!       THIS SUBROUTINE IS USED TO LOCATE A MACRO LINE
!       WITH A SPECIFIC COMMAND WORD AND OR QUALIFIER WORD
!       OR A COMMAND WORD OR A QUALIFIER WORD ALONE.
!       THE SEARCH IS PERFORMED FROM THE LINE JUST AFTER
!       THE CURRENT LINE
!       DOWN TO THE MACRO END. IF THE TARGET OF THE SEARCH
!       IS NOT FOUND, THE POINTER IS LEFT AT THE LAST
!       MACRO LINE AND A MESSAGE THAT THE TARGET WAS NOT
!       FOUND IS PRINTED.
!
   CHARACTER &
   &TARGET*80,TAR1*8,TAR2*8,WCN*8,WQN*8,WSN*80
!
   INTEGER MMIJ,QBVAL,L,I,J,K

   COMMON/MIJ/MMIJ
!
   COMMON/NXT/WCN,WQN,WSN
!
   INCLUDE 'DATMAC.INC'
!
!       SET UP FOR POSSIBLE USE OF SUBROUTINE (NEXT)
   WCN='LO'
   WQN=WQ
   WSN=WS
!
!       WHAT WAS TO QUALIFIER WORD FOR LO ?
!
!       WAS IT C OR Q OR CQ OR COQ.
!       IF IT WAS C SEARCH FOR TARGET = COMMAND WORD
!       IF IT WAS Q SEARCH FOR TARGET = QUALIFIER WORD
!       IF IT WAS COQ SEARCH FOR TARGET = COMMAND WORD OR
!                                       QUALIFIER WORD
!       IF IT WAS CQ SEARCH FOR FIRST PART OF TARGET
!                               = COMMAND WORD
!                               SECOND PART OF TARGET
!                               = QUALIFIER WORD.
!       THE TARGET STRING IS PASSED IN THE WS VARIABLE
!       HANDLE THE STRING AND DETERMINE IF IT HAS MORE THAN
!       ONE PART.
   TARGET=WS
!       ALL LEADING BLANKS ARE NOW TO BE REMOVED.
   L=0
1  QBVAL=ICHAR(TARGET(1:1))
   IF(QBVAL.EQ.32) THEN
      TARGET=TARGET(2:80)
      L=L+1
      IF(L.EQ.80) GO TO 20
      GO TO 1
   ELSE
!       FOUND A NON-BLANK, PROCEED, LEADING BLANKS REMOVED
   END IF
!       NOW SEARCH FOR EITHER THE NEXT BLANK OR 8 CHARACTERS.
!       BREAK OFF THE FIRST PART OF TARGET TO TAR1.
!
   QBVAL=0
   DO 2 I=1,8
      QBVAL=ICHAR(TARGET(I:I))
      IF(QBVAL.EQ.32) THEN
         TAR1=TARGET(1:(I-1))
         TARGET=TARGET(I:80)
         GO TO 3
      ELSE
!       CONTINUE SEARCH
      END IF
2  CONTINUE
!       IF YOU GOT HERE THEN TAR1 = FIRST EIGHT CHARACTERS
   TAR1=TARGET(1:8)
   TARGET=TARGET(9:80)
3  CONTINUE
!       NOW PROCESS OUT TAR2.
!       ARE THERE ANY NON-BLANKS TO BE REMOVED?
!       IF SO REMOVE THEM.
   L=0
   QBVAL=0
30 QBVAL=ICHAR(TARGET(1:1))
   IF(QBVAL.NE.32) THEN
      TARGET=TARGET(2:80)
      L=L+1
      IF(L.EQ.80) THEN
!       TAR2 DID NOT EXIST
         TAR2=' '
         GO TO 100
      ELSE
      END IF
      GO TO 30
   ELSE
!       FOUND A BLANK, PROCEED
   END IF
!       AGAIN WE MUST REMOVE LEADING BLANKS
   L=0
   QBVAL=0
40 QBVAL=ICHAR(TARGET(1:1))
   IF(QBVAL.EQ.32) THEN
      TARGET=TARGET(2:80)
      L=L+1
      IF(L.EQ.80) THEN
!       TAR2 WAS BLANK
         TAR2=' '
         GO TO 100
      ELSE
      END IF
      GO TO 40
   ELSE
!       ANOTHER NON-BLANK WAS FOUND
!       THIS IS THE START OF TAR2.
   END IF
   L=0
   QBVAL=0
   DO 50 I=1,8
      QBVAL=ICHAR(TARGET(I:I))
      IF(QBVAL.EQ.32) THEN
         TAR2=TARGET(1:(I-1))
         GO TO 100
      ELSE
      END IF
!               PROCEED
50 CONTINUE
!       IF YOU GOT HERE THEN
   TAR2=TARGET(1:8)
!       NOW TARGET HAS BEEN PROCESSED
!               NOW SEARCH FO APPROPRIATE TARGETS THROUGH THE MACRO
!
100 J=CURLIN+1
   DO 5000 I=J,(MCDIR2(2,(MMIJ))-2)
      IF(WQ.EQ.'CK') GO TO 1000
      IF(WQ.EQ.'Q') GO TO 2000
      IF(WQ.EQ.'CQ') GO TO 3000
      IF(WQ.EQ.'COQ') GO TO 4000
1000  IF(MACCW(I).EQ.TAR1) THEN
         K=I
         GO TO 6000
      ELSE
         GO TO 5000
      END IF
2000  IF(MACQW(I).EQ.TAR1) THEN
         K=I
         GO TO 6000
      ELSE
         GO TO 5000
      END IF
3000  IF(MACCW(I).EQ.TAR1.AND.MACQW(I).EQ.TAR2) THEN
         K=I
         GO TO 6000
      ELSE
         GO TO 5000
      END IF
4000  IF(MACCW(I).EQ.TAR1.OR.MACQW(I).EQ.TAR1) THEN
         K=I
         GO TO 6000
      ELSE
         GO TO 5000
      END IF
5000 CONTINUE
   CURLIN=(MCDIR2(2,(MMIJ))-2)
   OUTLYNE='SEARCH TARGET NOT FOUND'
   CALL SHOWIT(1)
   CALL MPR
   RETURN
6000 CURLIN=K
   CALL MPR
   RETURN
!
20 CONTINUE
   OUTLYNE='SEARCH TARGET WAS BLANK. NO SEARCH PERFORMED.'
   CALL SHOWIT(1)
   RETURN
END
