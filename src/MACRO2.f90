!       THE MINIMUM VALUE OF 1.
!       THE MAXIMUM VALUE OF EITHER:
!               MCDIR2(2,(MMIJ))-2
!               OR
!               MAXLL-2
!       WHICHEVER IS LESS.
!
      OCRLIN=CURLIN
      NCRLIN=INT(W1)
!
      IF(NCRLIN.LT.0) THEN
         RETURN
      ELSE
!       NEW POSITION NOT TOO SMALL
