         OUTLYNE=&
         &'"RE" REQUIRES NON-BLANK STRING INPUT'
         CALL SHOWIT(1)
         CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
         RETURN
      END IF
!
!       IF THE CURRENT LINE IS 0 THEN NO REPLACEMENT OCCURS
!       JUST PRINT LINE POINTER AT MACRO TOP
!
      IF(CURLIN.EQ.0) THEN
         CALL REPORT_ERROR_AND_FAIL('LINE POINTER AT MACRO TOP'//'\n'//'NO REPLACEMENT OCCURED', 1)
         RETURN
      ELSE
      END IF
!
!       THIS SUBROUTINE IS USED TO REPLACE THE CONTENTS OF THE
!       CURRENT MACRO LINE WITH THE NEW MACRO LINE REPRESENTED
!       BY THE ALPHANUMERIC STRING FOLLOWING RE OR RE,.
!
!       THEREFORE TO REPLACE THE CURRENT LINE IN A MACRO
!       WITH (SET A 2)
!       WE TYPE EITHER:
!               RE,:SET A 2
!       OR      RE :SET A 2©©
!
!       REMEMBER AN ALPHANUMERIC STRING MUST BE DELIMITED
!       BY A : UNLESS IT IS THE SPECIAL INTERROGATOR (?).
!
!       CURRENT INSRUCTION LINE'S WS STRING VALUE BECOMES
!       THE NEW MACRO LINE.
!       THIS SUBROUTINE PASSES WS BACK TO THE PROCESS
!       SUBROUTINE  AS THE NEW VALUE OF THE VARIABLE (INPUT)
!       FOR REPROCESSING.  FLAG F47 IS SET HERE
!       WITH FLAG 47 SET, SUBROUTINE PROCESS DOES NOT CALL
!       SUBROUTINE CONTROL BUT CALLS SUBROUTINE MREA
!       FOR FUTHER PROCESSING. AFTER REPLACEMENT
!       FLAG 47 IS CLEARED TO ZERO.
!
      F47=1
      INPUT=WS
      CALL PROCES
!       NOW DO THE REPLACEMENT AT THE CURRENT LINE.
!       THE REPLACEMENT IS DONE BY SUBROUTINE MREA
      RETURN
   ELSE
!       NO RE
   END IF
   IF(WC.EQ.'TP') THEN
      IF(SQ.EQ.1.OR.SN.EQ.1.OR.SST.EQ.1) THEN
         OUTLYNE=&
         &'"TP" TAKES NO EXPLICIT INPUT'
         CALL SHOWIT(1)
         CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
         RETURN
      END IF
!
!       TP IS A VERY SIMPLE ROUTINE WHICH
!       CHANGES THE CURRENT LINE (CURLIN) FROM
!       ITS OLD VALUE TO LINE 0 OR THE MACRO TOP.
!
      OCRLIN=CURLIN
      NCRLIN=0
!
      CURLIN=0
      OUTLYNE='LINE POINTER AT MACRO TOP'
      CALL SHOWIT(1)
   END IF
END
