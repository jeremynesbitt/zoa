! SUB LTH80.FOR
SUBROUTINE LTH80(IPT80,LENG)
!
   IMPLICIT NONE
!
!       THIS SUBROUTINE DETERMINES THE LAST POSITION IN
!       THE CHARACTER VARIABLE INPT80 WHICH IS NOT BLANK.
!       IT PASSES THIS LAST NON-BLANK POSITION BACK TO THE
!       CALLING ROUTINE IN THE INTEGER VARIABLE LENG
!
   CHARACTER IPT80*80
!
   INTEGER LENG,QBVAL,I
!
   LENG=80
   DO 10 I=80,1,-1
      QBVAL=ICHAR(IPT80(I:I))
      IF(QBVAL.EQ.32) THEN
         LENG=LENG-1
         GO TO 10
      ELSE
!       QBVAL IS NOT 32. FOUND END OF INPT80
         RETURN
      END IF
10 CONTINUE
!       IF YOU GOT HERE THEN INPT80 WAS ALL BLANK
   RETURN
END
