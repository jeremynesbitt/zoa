! SUB MMEDIT.FOR
SUBROUTINE MMEDIT
   use zoa_file_handler
!
   use DATMAI
   IMPLICIT NONE
!
   CHARACTER STAMP*20
!
   LOGICAL EXISJK
!
   INTEGER I,J,HAPPY
   CHARACTER HAPPYNAME*8
   SAVE HAPPY
   SAVE HAPPYNAME
!
!       THIS IS THE SUBROUTINE WHICH CONTROLS THE MACRO EDIT OR
!       LMEDIT OPERATION.
!       THE OPERATION HERE IS SIMILAR TO THE MACRO INPUT MODE BUT
!       ALSO HAS THE CAPABILITY TO MOVE AROUND INSIDE A MACRO
!       AND MAKE CHANGES TO THAT MACRO.
!
!       SUBROUTINE FLMAC IS THE ONLY WAY BACK TO THE
!       MAIN PROGRAM CMD LEVEL. WQ IS THE NEW MACRO NAME.
!       MACRO NAMES MUST NOT BE THE SAME AS PROGRAM COMMAND
!       NAMES AS IF THEY ARE, THE MACRO WILL NEVER BE ABLE
!       TO BE EXECUTED OR DELETED WITHOUT EXTERNAL INTERVENTION.
!
   CHARACTER MNAME*8
!
!       W1 IS USED TO PASS THE MACRO LOCK IF ONE IS DESIRED
!
   INTEGER NKEY, fileID &
   &,M1,M2,M3,NEXTM3,MMIJ,FLAGA

   COMMON/MIJ/MMIJ
!
   INCLUDE 'DATMAC.INC'
!
!       ***************************************************************
   call does_mac_dat_exist(EXISJK)
!       ***************************************************************
!       SET FLAGS
   F1=0
   F3=1
!
!       CHECK FOR NO MACRO NAME
!
   IF(WQ.EQ.' ') THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & 'LMEDIT MUST BE FOLLOWED BY A MACRO NAME'//'\n'//&
      & 'RETURNED TO CMD LEVEL', 1)
      F1=1
      F3=0
      RETURN
   END IF
!
!       OPEN UNIT 20 FOR I/O
!
   NKEY=0.0D0
!
   fileID = open_mac_dat()

   J=0
   M3=0
   FLAGA=0
   DO 10 I=1,MAXMAC
      READ(fileID,REC=I,ERR=10) MNAME,M1,M2,NEXTM3,STAMP
      M3=M3+NEXTM3
      IF(WQ.EQ.MNAME.AND.NKEY.EQ.M1) THEN
         J=1
         MMIJ=I
         FLAGA=1
      ELSE
         IF(WQ.EQ.MNAME.AND.NKEY.NE.M1) THEN
            J=1
            MMIJ=1
            FLAGA=0
         ELSE
         END IF
      END IF
10 CONTINUE
!               CLOSE UNIT 20
   CALL CLOSE_FILE(20,1)
!       CHECK FOR FULL DIRECTORY
!
   IF(M3.GE.MAXMAC) THEN
!       DIRECTORY FULL
      CALL REPORT_ERROR_AND_FAIL('MACRO DIRECTORY FULL. RETURNED TO CMD LEVEL', 1)
      F1=1
      F3=0
      RETURN
   END IF
!
!       CHECK FOR MACRO ALREADY ON FILE.
   HAPPY=0
!
   IF(J.EQ.0) THEN
      MMIJ=0
      HAPPY=1
      HAPPYNAME=WQ
   ELSE
!       WE ARE CHANGING AN EXISTING MACRO AND IT IS THE MMIJ TH
!       MACRO IN THE DIRECTORY.
   END IF
!
!       DURING MACRO EDITING, EACH PROGRAM INSTRUCTION
!       ENTERED IS PROCESSED BY SUBROUTINE PROCESS
!       THEN PASSED BY SUBROUTINE CONTROL TO SUBROUTINE
!       MACMOD WHERE EACH COMMAND IS EITHER ACTED UPON IF IT IS
!       A MEDIT SPECIFIC COMMAND OR IS ENTERED AS A NEW LINE IN THE
!       MACRO IF IT IS NOT A MEDIT SPECIFIC COMMAND.
!
!       WHEN FL IS ENTERED, THE MACRO STORAGE ARRAYS
!       ARE WRITTEN TO THE MACRO FILE MAC.DAT.
!       THE RECORDS ARE WRITTEN DIRECT AND UNFORMATTED.
!       A DIRECTORY OF MACRO NAMES IS WRITTEN IN THE FIRST
!       MAXMAC ENRTIES OF THIS MACRO FILE.
!
   CALL MACEDT
   RETURN
END
