!       THIS SUBROUTINE ABORTS THE MACRO EDIT PROCESS (MEDIT) AND
!       RETURNS TO THE CMD LEVEL WITHOUT FILING THE CURRENT
!       MACRO.
!               SET FLAG F1=1 AND F3=0
      F1=1
      F3=0
      IF(F46.EQ.1) F46=0
      OUTLYNE=&
      &'MACRO EDIT ABORTED. RETURNING TO CMD LEVEL'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(WC.EQ.'RE') THEN
      IF(SQ.EQ.1.OR.SN.EQ.1) THEN
         OUTLYNE=&
         &'"RE" TAKES NO QUALIFIER OR NUMERIC INPUT'
         CALL SHOWIT(1)
