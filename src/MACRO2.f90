! SUB MMFLN.FOR
SUBROUTINE MMFLN
   use zoa_file_handler
!
   use DATMAI
   IMPLICIT NONE
!
   INTEGER NKEY,LINCUR,LINBOT,I,J,M
!
   LOGICAL EXISJK
!
   REAL*8 KEY
!
   CHARACTER AASORT*80,COMM_ARRAY*40
!
   DIMENSION COMM_ARRAY(:)
   ALLOCATABLE :: COMM_ARRAY
!
   INTEGER AKK,ALLOERR, fileID
!
   COMMON/STAT13/LINCUR,LINBOT
!
   INCLUDE 'DATMAC.INC'
!
!       THIS SUBROUTINE IS USED TO LIST THE CONTENTS
!       OF THE MACRO DIRECTORY. THE MACRO NAMES ARE PRINTED
!       FOR EACH MACRO FOR WHICH THE MACRO KEY MATCHES THE MACRO LOCK.
!       IF THE SPECIAL KEY (-1) IS USED, ALL MACRO NAMES
!       ARE LISTED.
!
   PRINT *, "LIBMAC is ", LIBMAC
   IF(SST.EQ.1.OR.SN.EQ.1) THEN
      OUTLYNE=&
      &'"MFLC" ONLY TAKES QUALIFIER INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF
!
!
!       OPEN UNIT 20 FOR I/O
!
   KEY=0.0D0
   NKEY=INT(KEY)
!
!       ***************************************************************
