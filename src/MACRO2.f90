      END IF
   END IF
40 WRITE(OUTLYNE,500)I,LINE1
   CALL SHOWIT(3)
   IF(CURLIN.EQ.(MCDIR2(2,(MMIJ))-2)) THEN
      OUTLYNE='LINE POINTER AT MACRO BOTTOM'
