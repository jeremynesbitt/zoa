! SUB MACRUN.FOR
SUBROUTINE MACRUN(MACYES)
   use zoa_file_handler
!
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS THE MAIN CONTROLING SUBROUTINE FOR
!       MACRO EXECUTION. IT IS CALLED BY SUBROUTINE CONTROL.
!       UPON RETURN TO CONTROL, THE VALUE OF MACYES TELLS
!       CONTROL IF A MACRO WAS FOUND AND EXECUTED OR IF
!       NO MACRO WAS FOUND AND AND INVALID COMMAND HAD BEEN
!       ENTERED
!
   INTEGER FLG(0:20)&
   &,MACYES,NF,I,KLI
!
   LOGICAL EXISJK

   INTEGER ACCCNT,ACCSUB, fileID
!
   COMMON/ACCSB2/ACCSUB,ACCCNT
!
   COMMON/FFL/FLG
!
   COMMON/NEFER/NF
!
   LOGICAL FUN
!
   COMMON/COMFUN/FUN
!
   INCLUDE 'DATMAC.INC'
!
!       CHECK THE FUNCTION DIRECTORY IF REQUIRED
   KLI=0
   FUN=.FALSE.
   IF(WC.EQ.'FUN01   ') KLI=1
   IF(WC.EQ.'FUN02   ') KLI=2
   IF(WC.EQ.'FUN03   ') KLI=3
   IF(WC.EQ.'FUN04   ') KLI=4
   IF(WC.EQ.'FUN05   ') KLI=5
   IF(WC.EQ.'FUN06   ') KLI=6
   IF(WC.EQ.'FUN07   ') KLI=7
   IF(WC.EQ.'FUN08   ') KLI=8
   IF(WC.EQ.'FUN09   ') KLI=9
   IF(WC.EQ.'FUN10   ') KLI=10

   PRINT *, "MACRUN BEING EXECUTED!"
!     ALLOW FUN NAMES IN MERIT AND UPDATE MERIT
   IF(F27.EQ.1.OR.F27.EQ.2) KLI=0
   IF(KLI.NE.0.AND.FUNEXT(KLI)) THEN
      FUN=.TRUE.
!       WE WANT TO RUN A FUNCTION, NOT A MACRO
!**********************************************************************
!       WE FOUND THE FUNCTION CALLED FOR.
      F4=1
!       FLAG F4 = 1 INDICATES INSTRUCTIONS RUNNING IN A FUNCTION
!       BY SETTING FLAG F4 HERE, WE GUARANTEE THAT IT IS ALWAYS
!       SET DURING FUNCTION EXECUTION EVEN IF THE MACROS ARE NESTED.
      NESFUN(0)=.TRUE.
      NEST=0
      NESTI(0)=1
      NESTIJ(0)=KLI
      NF=KLI
      ACCSUB=0
      CALL MACEXC
      KLI=NF
      F4=0
!       SETTING F4 = 0 INDICATES ALL FUNCTIONS FINISHED RUNNING
      MACYES=1
      RETURN
!       MACEXC IS THE SUBROUTINE WHICH ACTUALLY RUNS THE
!       KLI TH FUNCTION.
!**********************************************************************
   ELSE
!       PROCEED WITH MACRO RUNNING
   END IF
!       READ THE CURRENT MACRO DIRECTORY
!
!       OPEN UNIT 20 FOR I/O
!
!       ***************************************************************
   call does_mac_dat_exist(EXISJK)
!       ***************************************************************
   fileID = open_mac_dat()
!
   DO 10 I=1,MAXMAC
      READ(fileID,REC=I,ERR=10)MCDIR1(I),MCDIR2(1,I),MCDIR2(2,I),&
      &MCDIR2(3,I),MCDIR3(I)
!
!       THE INITIAL VALUES STORED IN MAC.DAT ARE:
!
!               MCDIR1(I) IS THE MACRO NAME
!               MCDIR2(1,I) IS THE MACRO LOCK
!               MCDIR2(2,I) IS THE MACRO LENGTH
!               MCDIR2(3,I) IS THE OCCUPANCY FLAG
!                               = 0 FOR BLANK MACRO
!                               = 1 FOR NON-BLANK MACRO
!

10 CONTINUE
   CALL CLOSE_FILE(fileID,1)
!
!       SEARCHES OF THIS MACRO DIRECTORY ARE NOW MADE TO DETERMINE
!       IF THE MACRO REQUESTED IS ON FILE.
!
!       NOW DETERMINE A LOCATION OF THE MACRO.
!       SEARCH THE DIRECTORY FOR THE MACRO TO BE EXECUTED.
!
   DO 20 I=1,MAXMAC
!
!       TEST FOR A NAME MATCH
!
      IF(MCDIR1(I).EQ.WC) THEN
!
!       WE FOUND THE MACRO CALLED FOR. SET NF=I
         NF=I
         F4=1
!       FLAG F4 = 1 INDICATES INSTRUCTIONS RUNNING IN A MACRO
!       BY SETTING FLAG F4 HERE, WE GUARANTEE THAT IT IS ALWAYS
!       SET DURING MACRO EXECUTION EVEN IF THE MACROS ARE NESTED.
         NESFUN(0)=.FALSE.
         NEST=0
         NESTI(0)=1
         NESTIJ(0)=NF
         ACCSUB=0
         CALL MACEXC
         F4=0
!       SETTING F4 = 0 INDICATES ALL MACROS FINISHED RUNNING
         MACYES=1
         RETURN
!       MACEXC IS THE SUBROUTINE WHICH ACTUALLY RUNS THE
!       NF TH MACRO IN MAC.DAT.
!
      ELSE
!       CONTINUE THE SEARCH
      END IF
20 CONTINUE
!
!       IF YOU GOT HERE YOU COULD NOT FIND THE MACRO WITH THE CALLED
!       FOR LOCK.
!
   MACYES=0
   RETURN
!
END
! SUB MACMOD.FOR
SUBROUTINE MACMOD
!
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDLES THE MEDIT COMMANDS AND MEDIT MODE
!       MACRO INPUT
!
   CHARACTER WCN*8,WQN*8,WSN*80
!
   INTEGER MMIJ
!
   COMMON/MIJ/MMIJ
!
   COMMON/NXT/WCN,WQN,WSN
!
   INCLUDE 'DATMAC.INC'
!
!       FIRST CHECK THE COMMAND WORD WC FOR SPECIAL
!       MEDIT COMMANDS.
!       THEY ARE
!
!               PR -  PRINT LINE OR LINES
!               BT -  GO TO BOTTOM OF MACRO
!               DE -  DELETE  MACRO LINES
!               EX -  EXTRACT ANOTHER MACRO INTO THE
!                     CURRENT MACRO
!               FL -  STOP EDITING AND FILE THE MACRO
!               GO -  GO TO A SPECIFIED MACRO LINE
!             LO C -  LOCATE THE NEXT OCCURENCE OF A COMMAND
!                     WORD
!             LO Q -  LOCATE THE NEXT OCCURENCE OF A QUALIFIER
!                     WORD
!            LO CQ -  LOCATE THE NEXT OCCURENCE OF THE COMMAND
!                     AND QUALIFIER WORD.
!           LO COQ -  LOCATE THE NEXT OCCURENCE OT THE COMMAND
!                     OR QUALIFIER WORD.
!               NEXT- REPEAT SEARCH FURTHER DOWN MACRO.
!       QUIT OR QU -  QUIT WITHOUT CHANGING MACRO FILE(MAC.DAT)
!               RE -  REPLACE CURRENT LINE WITH WHAT FOLLOWS
!               TP -  GO TO TOP OF THE MACRO
!               PB -  PRINTS PAGES BACK IF SCROLLING (CMD LEVEL COM.)
!
!       THERE ARE THE SPECIAL COMMAND (BLANK WITH A RETURN
!       AND ?. A BLANK WITH A RETURN YIELDS THE MESSAGE.
!
!       MACRO (MACRO NAME),EDITING AT MACRO LINE # EXCEPT AT
!       LINE ZERO WHICH YIELDS A TOP OF MACRO MESSAGE.
!
!
!       THE BLANK FOLLOWED BY A RETURN YIELDS THE PROGRAM
!       LEVEL MESSAGE FROM SUBROUTINE BLANK
!
!       CHECK FOR BT,DE,EX,FL,GO,LO,QUIT,QU,RE,TP OR PR
!       AND BLANK OR ? AND NEXT
!
   IF(WC.EQ.'?') THEN
      CALL QUERRYY
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.' '.AND.F50.EQ.1) THEN
      CALL BLANK
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.' '.AND.F50.NE.1) THEN
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'PR'.AND.F46.EQ.0) THEN
      IF(SST.EQ.1.OR.SQ.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1 &
      &.OR.S5.EQ.1) THEN
         CALL REPORT_ERROR_AND_FAIL(&
         & '"PR" ONLY TAKES NUMERIC WORD #1 INPUT'//'\n'//&
         & 'RE-ENTER COMMAND', 1)
         RETURN
      END IF
      CALL MPR
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'PR'.AND.F46.EQ.1) THEN
      IF(SST.EQ.1.OR.SQ.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1 &
      &.OR.S5.EQ.1) THEN
         CALL REPORT_ERROR_AND_FAIL(&
         & '"PR" ONLY TAKES NUMERIC WORD #1 INPUT'//'\n'//&
         & 'RE-ENTER COMMAND', 1)
         RETURN
      END IF
      OUTLYNE='MACRO HAS NO LINES'
      CALL SHOWIT(1)
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'BT'.AND.F46.EQ.0) THEN
      CALL MOD2
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'BT'.AND.F46.EQ.1) THEN
      OUTLYNE='MACRO HAS NO LINES'
      CALL SHOWIT(1)
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'NEXT'.AND.F46.EQ.0) THEN
      CALL MOD2
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'NEXT'.AND.F46.EQ.1) THEN
      OUTLYNE='MACRO HAS NO LINES'
      CALL SHOWIT(1)
      GO TO 1000
   ELSE
   END IF
   IF(WC(1:2).EQ.'DE'.AND.F46.EQ.0) THEN
      CALL MOD2
      GO TO 1000
   ELSE
   END IF
   IF(WC(1:2).EQ.'DE'.AND.F46.EQ.1) THEN
      OUTLYNE='MACRO HAS NO LINES'
      CALL SHOWIT(1)
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'EX') THEN
      CALL MOD2
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'FL') THEN
      CALL MOD2
      GO TO 1000
   ELSE
   END IF
   IF(WC(1:2).EQ.'GO'.AND.F46.EQ.0) THEN
      CALL MOD2
      GO TO 1000
   ELSE
   END IF
   IF(WC(1:2).EQ.'GO'.AND.F46.EQ.1) THEN
      OUTLYNE='MACRO HAS NO LINES'
      CALL SHOWIT(1)
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'LO'.AND.F46.EQ.0) THEN
      CALL MLO
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'LO'.AND.F46.EQ.1) THEN
      OUTLYNE='MACRO HAS NO LINES'
      CALL SHOWIT(1)
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'QUIT'.OR.WC.EQ.'QU') THEN
      CALL MOD2
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'RE'.AND.F46.EQ.0) THEN
      CALL MOD2
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'RE'.AND.F46.EQ.1) THEN
      OUTLYNE='MACRO HAS NO LINES'
      CALL SHOWIT(1)
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'TP'.AND.F46.EQ.0) THEN
      CALL MOD2
      GO TO 1000
   ELSE
   END IF
   IF(WC.EQ.'TP'.AND.F46.EQ.1) THEN
      OUTLYNE='MACRO HAS NO LINES'
      CALL SHOWIT(1)
      GO TO 1000
   ELSE
!       THE INSTRUCTION LINE MUST BE AN INPUT LINE. IF IT IS NOT
!       BLANK, INSERT IT INTO THE MACRO AT THE CURRENT POSITION
!
!**********************************************************************
!
!       HERE IS WHERE MACRO SPECIFIC OPERATING COMMANDS HAVE SOME
!       OR THEIR SYNTAX CHECKED.
!
      IF(WC.EQ.'QSUB'.OR.WC.EQ.'SSUB') THEN
         IF(SN.EQ.1) THEN
            CALL REPORT_ERROR_AND_FAIL(&
            & '"'//WC(1:4)//'" TAKES NO NUMERIC INPUT'//'\n'//&
            & 'RE-ENTER COMMAND', 1)
            RETURN
         END IF
      END IF
      IF(WC.EQ.'QRSUB') THEN
         IF(SN.EQ.1) THEN
            CALL REPORT_ERROR_AND_FAIL(&
            & '"'//WC(1:5)//'" TAKES NO NUMERIC INPUT'//'\n'//&
            & 'RE-ENTER COMMAND', 1)
            RETURN
         END IF
      END IF
      IF(WC.EQ.'ACCSUB') THEN
         IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
            OUTLYNE=&
            &'"ACCSUB" ONLY TAKES QUALIFIER AND NUMERIC WORD #1 INPUT'
            CALL SHOWIT(1)
            CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
            RETURN
         END IF
         IF(DF1.EQ.1) THEN
            OUTLYNE=&
            &'"ACCSUB" REQUIRES EXPLICIT NUMERIC WORD #1 INPUT'
            CALL SHOWIT(1)
            CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
            RETURN
         ELSE
         END IF
      ELSE
!       NOT ACCSUB
      END IF
      IF(WC.EQ.'CSUB') THEN
         IF(SST.EQ.1) THEN
            CALL REPORT_ERROR_AND_FAIL(&
            & '"'//WC(1:4)//'" TAKES NO EXPLICIT INPUT'//'\n'//&
            & 'RE-ENTER COMMAND', 1)
            RETURN
         END IF
      END IF
      IF(WC.EQ.'CRSUB') THEN
         IF(SST.EQ.1) THEN
            CALL REPORT_ERROR_AND_FAIL(&
            & '"'//WC(1:5)//'" TAKES NO EXPLICIT INPUT'//'\n'//&
            & 'RE-ENTER COMMAND', 1)
            RETURN
         END IF
      END IF
      IF(WC.EQ.'SAVE') THEN
         IF(SST.EQ.1) THEN
            CALL REPORT_ERROR_AND_FAIL(&
            & '"SAVE" TAKES NO EXPLICIT INPUT'//'\n'//&
            & 'RE-ENTER COMMAND', 1)
            RETURN
         END IF
      END IF
      IF(WC.EQ.'RELOAD') THEN
         IF(SST.EQ.1) THEN
            CALL REPORT_ERROR_AND_FAIL(&
            & '"RELOAD" TAKES NO EXPLICIT INPUT'//'\n'//&
            & 'RE-ENTER COMMAND', 1)
            RETURN
         END IF
      END IF
      IF(WC.EQ.'NSUB') THEN
         IF(SST.EQ.1) THEN
            CALL REPORT_ERROR_AND_FAIL(&
            & '"'//WC(1:4)//'" TAKES NO STRING INPUT'//'\n'//&
            & 'RE-ENTER COMMAND', 1)
            RETURN
         END IF
