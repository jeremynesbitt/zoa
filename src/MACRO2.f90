   IF(.NOT.EXISJK) THEN
      CALL REPORT_ERROR_AND_FAIL(&
      & '"MACRO FILES DO NOT YET EXIST'//'\n'//&
      & 'TO INITIALIZE IT, USE "IMF" AND "PROCEED"', 1)
      RETURN
   END IF
!       ***************************************************************
   PRINT *, "MMFLN File Path is ", LIBMAC//'MAC.DAT'
   fileID = open_mac_dat()
!
!       INITIALIZE ASORT TO ALL BLANKS
!
   ASORT(1:MAXMAC)='        '
!
   J=0
!       J COUNTS THE NUMBER OF NON-BLANK ENTRIES IN THE DIRECTORY.
   DO 10 I=1,MAXMAC
      READ(fileID,REC=I,ERR=10)MCDIR1(I),MCDIR2(1,I),MCDIR2(2,I),&
      &MCDIR2(3,I),MCDIR3(I)
      IF(MCDIR2(3,(I)).EQ.1) J=J+1
10 CONTINUE
!
!       CLOSE UNIT 20 TO I/O
!
   CALL CLOSE_FILE(20,1)
!
!       LOOK FOR MCDIR2(3,(I))=1 AND MATCHING LOCKS OR LOCK = -1
!     NOW GET THE LEADING COMMENT IN EVERY MACRO AND STORE THEM IN
!     THE COMMENT ARRAY COM_ARRAY
   ALLOCATE(COMM_ARRAY(1:MAXMAC),STAT=ALLOERR)
!
   AKK=0
   DO 20 I=1,MAXMAC
      IF(MCDIR2(3,(I)).EQ.1.OR.&
      &MCDIR2(3,(I)).EQ.1) THEN
         AKK=AKK+1
         ASORT(AKK)=MCDIR1(I)
         SAVE_KDP(18)=SAVEINPT(18)
         WQ=TRIM(ASORT(AKK))
         CALL MMFLC1
         REST_KDP(18)=RESTINPT(18)
         AISORT(AKK)=' '//MCDIR3(I)//' '//MAC_COM1
      END IF
20 CONTINUE
!       NOW SORT THE MACRO NANES
   CALL SORTA(AKK)
!       NOW PRINT THEM
   WRITE(OUTLYNE,50)
   CALL SHOWIT(0)
50 FORMAT('MACRO FILE CONTENTS')
   WRITE(OUTLYNE,100)
   CALL SHOWIT(0)
100 FORMAT('NAME      TIME AND DATE        DESCRIPTION')
   DO I=1,AKK
      WRITE(OUTLYNE,200) ASORT(I)(1:8),AISORT(I)(1:70)
      CALL SHOWIT(0)
   END DO
200 FORMAT(A8,1X,A70)
   DEALLOCATE(COMM_ARRAY,STAT=ALLOERR)
   RETURN
END
