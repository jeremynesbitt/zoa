!
   use DATMAI
   IMPLICIT NONE
!
!
!       THIS SUBROUTINE IS CALLED TO CALCULATE THE MODULO FUNCTION
!       USING THE ARGUMENTS W1 AND W2.
!
!       THE RESULT IS LEFT IN THE ACCUMULATOR.
!
   IF(SQ.EQ.1.OR.SST.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1 &
   &.OR.S5.EQ.1) THEN
