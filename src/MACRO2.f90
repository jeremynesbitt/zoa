! SUB MPR.FOR
SUBROUTINE MPR
!
   use DATMAI
   IMPLICIT NONE
!
!       THIS MACRO CONTROLS PRINTING OF LINES DURING THE MEDIT
!       MODE
!
   CHARACTER ALN1*80
!
   INTEGER LENG,NUMLIN,COUNT,MWW1
!
   INTEGER MACLOC,IMACEN,&
   &MMIJ,NF,I,LINBOT,LINCUR

   CHARACTER &
   &BN1*10,BN2*10,BN3*10,BN4*10,BN5*10,&
   &BBN1*1,BBN2*1,BBN3*1,BBN4*1,BBN5*1,&
   &LINE1*80,MACNAM*8
!
   COMMON/MIJ/MMIJ
!
   COMMON/MACID/MACNAM
!
   COMMON/MACID2/MACLOC,IMACEN
!
   COMMON/STAT13/LINCUR,LINBOT
!
   INCLUDE 'DATMAC.INC'
!
!               HANDEL TOP AND BOTTOM
!
   IF(CURLIN.EQ.0) THEN
      CURLIN=0
   ELSE
   END IF
!
!               TOP AND BOTTOM HANDLED
!
!       EACH TIME A SINGLE LINE IS PRINTED, THE CURRENT
!       LINE IS INCREMENTED BY 1 UNLESS ONLY ONE LINE WAS
!       REQUESTED. PR OR PR,-1 WILL REQUEST THIS.
!
!       THE PR COMMAND IN MEDIT ONLY ALLOWS OUTPUT TO THE
!       TERMINAL OR SCREEN (OUT=6) AND THE PRINTER FILE
!       (OUT=7). AND OTHER OUT VALUE YIELDS AN ERROR MESSAGE
!       AND AN AUTOMATIC RETURN TO OUT=6.
!
!       HOW MANY LINES ARE TO BE PRINTED?
!               W1 LINES
!
   COUNT=1
   NUMLIN=1
   MWW1=0
   IF(W1.LE.0.0D0) THEN
      W1=1.0D0
      MWW1=1
   ELSE
   END IF
   NUMLIN=INT(W1)
   COUNT=NUMLIN
41 IF(COUNT.EQ.0) GO TO 2000
   IF(MWW1.EQ.1.AND.CURLIN.EQ.0) CURLIN=1
   IF(COUNT.LE.NUMLIN.AND.MWW1.NE.1) CURLIN=CURLIN+1
   IF(CURLIN.GT.(MCDIR2(2,(MMIJ))-2))&
   &CURLIN=MCDIR2(2,(MMIJ))-2
   I=CURLIN
!       CHECK FOR PRESECE OF A QUALIFIER
   IF(MACSTA(5,I).EQ.1) THEN
!       THERE IS A QUALIFIER WORD
!       NOW CHECK FOR PRESENCE OF A STRING
      IF(MACSTA(6,I).EQ.1) THEN
!       THERE IS A QUALIFIER WORD AND A STRING  AND
!       NO NUMERIC WORDS SINCE STRINGS AND NUMERIC WORDS
!       ARE MUTUALLY EXCLUSIVE.
!       PRODUCE OUTPUT
!
!       HERE OUT ALWAYS = 6 SO WRITE COMPACT 80 COLUMN OUTPUT.
!
         LINE1=MACCW(I)//CHAR(32)//MACQW(I)//CHAR(32)//MACSTR(I)
         GO TO 40
      ELSE
!       THERE IS NO STRING,THERE ARE NUMERIC WORDS AND A QUALIFIER.
!       BEFORE WRITING THE NUMERIC DATA,IT MUST BE CONVERTED
!       INTO A STRING VARIABLE. OTHERWISE HANDELING THE DEFAULT
!       VALUES BECOMES AN S.O.B..
!       CONVERT THE NUMERIC VALES TO STRING VALUES WITH
!       SUBROUTINE NTOA2.  THE CALL IS :
!
!               CALL NTOA2(N1,N2,N3,N4,N5,BN1,BN2,BN3,BN4,BN5)
!
         CALL NTOA2(MACNW(1,I),MACNW(2,I),MACNW(3,I),MACNW(4,I)&
         &,MACNW(5,I),BN1,BN2,BN3,BN4,BN5)
!
!       DEAL WITH THE DEFAULT VALUES
         BBN1=','
         BBN2=','
         BBN3=','
         BBN4=','
         BBN5=','
!
         LINE1=MACCW(I)//CHAR(32)//MACQW(I)//','
         IF(MACSTA(12,I).EQ.1) THEN
            CALL LTH80(LINE1,LENG)
            LINE1=LINE1(1:LENG)//BBN1
         ELSE
            CALL LTH80(LINE1,LENG)
            LINE1=LINE1(1:LENG)//BN1//','
         END IF
         IF(MACSTA(13,I).EQ.1) THEN
            CALL LTH80(LINE1,LENG)
            LINE1=LINE1(1:LENG)//BBN2
         ELSE
            CALL LTH80(LINE1,LENG)
            LINE1=LINE1(1:LENG)//BN2//','
         END IF
         IF(MACSTA(14,I).EQ.1) THEN
            CALL LTH80(LINE1,LENG)
            LINE1=LINE1(1:LENG)//BBN3
         ELSE
            CALL LTH80(LINE1,LENG)
            LINE1=LINE1(1:LENG)//BN3//','
         END IF
         IF(MACSTA(15,I).EQ.1) THEN
            CALL LTH80(LINE1,LENG)
            LINE1=LINE1(1:LENG)//BBN4
         ELSE
            CALL LTH80(LINE1,LENG)
            LINE1=LINE1(1:LENG)//BN4//','
         END IF
         IF(MACSTA(16,I).EQ.1) THEN
            CALL LTH80(LINE1,LENG)
            LINE1=LINE1(1:LENG)//BBN5
         ELSE
            CALL LTH80(LINE1,LENG)
            LINE1=LINE1(1:LENG)//BN5//','
         END IF
      END IF
      GO TO 40
   ELSE
!       NO QUALIFIER WORD
!       CHECK FOR THE PRESENCE OF A STRING
      IF(MACSTA(6,I).EQ.1) THEN
!       THERE IS A STRING WITHOUT A QUALIFIER AND NO NUMERIC INPUT
!       PRODUCE OUTPUT
!
         LINE1=MACCW(I)//CHAR(32)//MACSTR(I)
         GO TO 40
      ELSE
!       THERE IS NO QUALIFIER AND NO STRING
!       THERE IS NUMERIC INPUT.
!       BEFORE WRITING THE NUMERIC DATA,IT MUST BE CONVERTED
!       INTO A STRING VARIABLE. OTHERWISE HANDELING THE DEFAULT
!       VALUES BECOMES AN S.O.B..
