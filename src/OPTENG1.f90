FUNCTION RETVAL(T,LAMBDA,RADUNI)
   IMPLICIT NONE
   REAL*8 T,LAMBDA,C1,C2,C1P,SUMOLD,RETVAL,SUM ,A
   INTEGER RADUNI,K
   C1=37415.0D0
   C1P=1.88365D23
   C2=14387.9D0
   IF(RADUNI.EQ.1) THEN
!     WATT UNITS
      SUM=0.0D0
      DO K=1,1000000
         SUMOLD=SUM
         A=DEXP((-C2*DBLE(K))/(LAMBDA*T))/DBLE(K)
         SUM=SUM+(((1.0D0/LAMBDA**3)+((3.0D0*T)/(DBLE(K)*C2*(LAMBDA**2)))+((6.0D0*(T**2))/(DBLE((K**2))*(C2**2)*LAMBDA))+((6.0D0*(T**3))/(DBLE((K**3))*(C2**3))))*A)
         IF(DABS(SUM-SUMOLD).LE.1.0D-10) GO TO 999
      END DO
999   RETVAL=((T*C1)/C2)*SUM
      RETURN
   END IF
   IF(RADUNI.EQ.2) THEN
!     PHOTON UNITS
      SUM=0.0D0
      DO K=1,1000000
         SUMOLD=SUM
         A=DEXP((-C2*DBLE(K))/(LAMBDA*T))/DBLE(K)
         SUM=SUM+(((1.0D0/(LAMBDA**2))+((2.0D0*T)/(DBLE(K)*C2*LAMBDA))+((2.0D0*(T**2))/(DBLE((K**2))*(C2**2))))*A)
         IF(DABS(SUM-SUMOLD).LE.1.0D-10) GO TO 888
      END DO
888   RETVAL=((T*C1P)/C2)*SUM
      RETURN
   END IF
   RETURN
END
