! SUB XXF.FOR
SUBROUTINE XXF
!
   use DATMAI
   IMPLICIT NONE
!
!     XXF IS A SUBROUTINE WHICH CALCULATES OBJECT OR IMAGE
!     DISTACE OR FOCAL LENGTH GIVEN ANY TWO OF THE THREE VALUES
!     USING THE NEWTONIAN EQUATION:
!
!     f**2=-X*X'
!
!       XXF, NW1 NW2 NW2
!       WHERE NW1 IS THE DISTANCE X
!       WHERE NW2 IS THE DISTANCE X'
!       WHERE NW3 IS THE FOCAL LENGTH
!
   REAL*8 VALUE
!
!
   IF(SST.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
      WRITE(OUTLYNE,*)'"XXF (P)" ONLY TAKES NUMERIC WORD #1, #2 AND #3 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
   IF(SQ.EQ.1.AND.WQ.NE.'P') THEN
      WRITE(OUTLYNE,*)'"P" IS THE ONLY OPTIONAL QUALIFIER WORD FOR "XXF"'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(DF1.EQ.0.AND.W1.EQ.0.0D0.OR.DF2.EQ.0.AND.W2.EQ.0.0D0 .OR.DF3.EQ.0.AND.W3.EQ.0.0D0) THEN
      WRITE(OUTLYNE,*)'"XXF (P)" DOES NOT ACCEPT 0.0 FOR ANY OF ITS INPUT VALUES'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(W1.GT.0.0D0.AND.W2.GT.0.0D0.OR.W1.LT.0.0D0.AND.W2.LT.0.0D0)THEN
      WRITE(OUTLYNE,*)'"(X) AND (X`) MAY NOT BE BOTH POSITIVE OF BOTH NEGATIVE'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
   IF(DF1.EQ.1.AND.DF2.EQ.0.AND.DF3.EQ.0.OR.DF1.EQ.0.AND.DF2.EQ.1.AND.DF3.EQ.0.OR.DF1.EQ.0.AND.DF2.EQ.0.AND.DF3.EQ.1) THEN
!     PROCEED

      IF(DF1.EQ.1) VALUE=-(W3**2)/(W2)
      IF(DF2.EQ.1) VALUE=-(W3**2)/(W1)
      IF(DF3.EQ.1) VALUE=DSQRT(-(W1*W2))
!       UPDATE LASTX
      REG(40)=REG(9)
      REG(9) = VALUE
!
      IF(WQ.EQ.'P') THEN
         IF(DF1.EQ.1) WRITE(OUTLYNE,100) VALUE
         IF(DF1.EQ.1) CALL SHOWIT(0)
         IF(DF1.EQ.1) WRITE(OUTLYNE,20) W2
         IF(DF1.EQ.1) CALL SHOWIT(0)
         IF(DF1.EQ.1) WRITE(OUTLYNE,30) W3
         IF(DF1.EQ.1) CALL SHOWIT(0)
         IF(DF2.EQ.1) WRITE(OUTLYNE,200) VALUE
         IF(DF2.EQ.1) CALL SHOWIT(0)
         IF(DF2.EQ.1) WRITE(OUTLYNE,10) W1
         IF(DF2.EQ.1) CALL SHOWIT(0)
         IF(DF2.EQ.1) WRITE(OUTLYNE,30) W3
         IF(DF2.EQ.1) CALL SHOWIT(0)
         IF(DF3.EQ.1) WRITE(OUTLYNE,300) VALUE
         IF(DF3.EQ.1) CALL SHOWIT(0)
         IF(DF3.EQ.1) WRITE(OUTLYNE,10) W1
         IF(DF3.EQ.1) CALL SHOWIT(0)
         IF(DF3.EQ.1) WRITE(OUTLYNE,20) W2
         IF(DF4.EQ.1) CALL SHOWIT(0)
10       FORMAT('  INPUT (X) DISTANCE WAS = ',G13.6)
20       FORMAT(' INPUT (X`) DISTANCE WAS = ',G13.6)
30       FORMAT('  INPUT FOCAL LENGTH WAS = ',G13.6)
100      FORMAT(' THE NEW (X) DISTANCE IS = ',G13.6)
200      FORMAT('THE NEW (X`) DISTANCE IS = ',G13.6)
300      FORMAT(' THE NEW FOCAL LENGTH IS = ',G13.6)
      END IF
      RETURN
   ELSE
      WRITE(OUTLYNE,*)'"XXF (P)" REQUIRES TWO EXPLICIT NUMERIC VALUES'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
   END IF
   RETURN
END
! SUB XXFF.FOR
SUBROUTINE XXFF
!
   use DATMAI
   IMPLICIT NONE
!
!
!     XXFF IS A SUBROUTINE WHICH CALCULATES OBJECT OR IMAGE
!     DISTACE OR FOCAL LENGTH GIVEN ANY THREE OF THE FOUR VALUES
!     USING THE NEWTONIAN EQUATION:
!
!     ff'=-X*X'
!
!       XXF, NW1 NW2 NW2
!       WHERE NW1 IS THE DISTANCE X
!       WHERE NW2 IS THE DISTANCE X'
!       WHERE NW3 IS THE FOCAL LENGTH f
!       WHERE NW3 IS THE FOCAL LENGTH f'
!
   REAL*8 VALUE
!
!
   IF(SST.EQ.1.OR.S5.EQ.1) THEN
      WRITE(OUTLYNE,*)'"XXFF (P)" ONLY TAKES NUMERIC WORD #1, #2, #3 AND #4 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
   IF(SQ.EQ.1.AND.WQ.NE.'P') THEN
      WRITE(OUTLYNE,*)'"P" IS THE ONLY OPTIONAL QUALIFIER WORD FOR "XXFF"'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(DF1.EQ.0.AND.W1.EQ.0.0D0.OR.DF2.EQ.0.AND.W2.EQ.0.0D0 .OR.DF3.EQ.0.AND.W3.EQ.0.0D0.OR.DF4.EQ.0.AND.W4.EQ.0.0D0) THEN
      WRITE(OUTLYNE,*)'"XXFF (P)" DOES NOT ACCEPT 0.0 FOR ANY OF ITS INPUT VALUES'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
   IF(DF1.EQ.1.AND.DF2.EQ.0.AND.DF3.EQ.0.AND.DF4.EQ.1.OR.DF1.EQ.0.AND.DF2.EQ.1.AND.DF3.EQ.0.AND.DF4.EQ.0.OR.DF1.EQ.0.AND.DF2.EQ.0.AND.DF3.EQ.1.AND.DF4.EQ.0.OR.DF1.EQ.0.AND.DF2.EQ.0.AND.DF3.EQ.0.AND.DF4.EQ.1) THEN
!     PROCEED

      IF(DF1.EQ.1) VALUE=-(W3*W4)/(W2)
      IF(DF2.EQ.1) VALUE=-(W3*W4)/(W1)
      IF(DF3.EQ.1) VALUE=(-(W1*W2))/W4
      IF(DF4.EQ.1) VALUE=(-(W1*W2))/W3
!       UPDATE LASTX
      REG(40)=REG(9)
      REG(9) =VALUE
!
      IF(WQ.EQ.'P') THEN
         IF(DF1.EQ.1) WRITE(OUTLYNE,100) VALUE
         IF(DF1.EQ.1) CALL SHOWIT(0)
         IF(DF1.EQ.1) WRITE(OUTLYNE,20) W2
         IF(DF1.EQ.1) CALL SHOWIT(0)
         IF(DF1.EQ.1) WRITE(OUTLYNE,30) W3
         IF(DF1.EQ.1) CALL SHOWIT(0)
         IF(DF1.EQ.1) WRITE(OUTLYNE,40) W4
         IF(DF1.EQ.1) CALL SHOWIT(0)
         IF(DF2.EQ.1) WRITE(OUTLYNE,200) VALUE
         IF(DF2.EQ.1) CALL SHOWIT(0)
         IF(DF2.EQ.1) WRITE(OUTLYNE,10) W1
         IF(DF2.EQ.1) CALL SHOWIT(0)
         IF(DF2.EQ.1) WRITE(OUTLYNE,30) W3
         IF(DF2.EQ.1) CALL SHOWIT(0)
         IF(DF2.EQ.1) WRITE(OUTLYNE,40) W4
         IF(DF2.EQ.1) CALL SHOWIT(0)
         IF(DF3.EQ.1) WRITE(OUTLYNE,300) VALUE
         IF(DF3.EQ.1) CALL SHOWIT(0)
         IF(DF3.EQ.1) WRITE(OUTLYNE,10) W1
         IF(DF3.EQ.1) CALL SHOWIT(0)
         IF(DF3.EQ.1) WRITE(OUTLYNE,20) W2
         IF(DF3.EQ.1) CALL SHOWIT(0)
         IF(DF3.EQ.1) WRITE(OUTLYNE,40) W4
         IF(DF3.EQ.1) CALL SHOWIT(0)
         IF(DF4.EQ.1) WRITE(OUTLYNE,400) VALUE
         IF(DF4.EQ.1) CALL SHOWIT(0)
         IF(DF4.EQ.1) WRITE(OUTLYNE,10) W1
         IF(DF4.EQ.1) CALL SHOWIT(0)
         IF(DF4.EQ.1) WRITE(OUTLYNE,20) W2
         IF(DF4.EQ.1) CALL SHOWIT(0)
         IF(DF4.EQ.1) WRITE(OUTLYNE,30) W3
         IF(DF4.EQ.1) CALL SHOWIT(0)
10       FORMAT('  INPUT (X) DISTANCE WAS = ',G13.6)
20       FORMAT(' INPUT (X`) DISTANCE WAS = ',G13.6)
30       FORMAT('           INPUT (F) WAS = ',G13.6)
40       FORMAT('           INPUT (F`) WAS = ',G13.6)
100      FORMAT(' THE NEW (X) DISTANCE IS = ',G13.6)
200      FORMAT('THE NEW (X`) DISTANCE IS = ',G13.6)
300      FORMAT('          THE NEW (F) IS = ',G13.6)
400      FORMAT('         THE NEW (F`) IS = ',G13.6)
      END IF
      RETURN
   ELSE
      WRITE(OUTLYNE,*)'"XXFF (P)" REQUIRES TWO EXPLICIT NUMERIC VALUES'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
   END IF
   RETURN
END
! SUB OUTFLT.FOR
SUBROUTINE OUTFLT
!
   use DATLEN
   use mod_system
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE OUTFLT. THIS SUBROUTINE IMPLEMENTS THE
!       CURVATURE FOR M FRINGES OUT OF FLAT CALCULATION AND
!     OUTPUTS THE CURVATURE CORRESPONDING
!     TO AN OUT OF FLAT CONDITION MEASURES IN FRINGES AT AN ARBITRARY
!     WAVELENGTH
!       NW1=NUMBER FOR FRINGES OR FRACTION OF FRINGES (REQUIRED)
!       NW2=PART DIAMETER IN CURRENT LENS UNITS (REQUIRED)
!       NW3=WAVELENGTH IN MICRONS (DEFAULT = 0.5461)
!         NW4 AND NW5 NOT USED
!         WQ NOT USED
!         WS NOT USED
!
   CHARACTER UN*12
!
   REAL*8 CURVE,WAVE
!
!
   IF(sys_units().EQ.1.0) UN='/INCHES     '
   IF(sys_units().EQ.2.0) UN='/CENTIMETERS'
   IF(sys_units().EQ.3.0) UN='/MILLIMETERS'
   IF(sys_units().EQ.4.0) UN='/METERS'
!
!
   IF(SST.EQ.1) THEN
      WRITE(OUTLYNE,*)'"OUTFLT" TAKES NO ALPHANUMERIC STRING INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(S4.EQ.1.OR.S5.EQ.1) THEN
      WRITE(OUTLYNE,*)'"OUTFLT" TAKES NO NUMERIC WORD #4 OR #5 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(SQ.EQ.1.AND.WQ.NE.'ACC') THEN
      WRITE(OUTLYNE,*)'INVALID QUALIFIER WORD USED WITH "OUTFLT"'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       HANDLE DEFAULT VALUES FOR NW1, NW2 AND NW3
!
   IF(DF1.EQ.1.OR.DF2.EQ.1) THEN
      WRITE(OUTLYNE,*)'"OUTFLT" REQUIRES EXPLICIT NUMERIC WORD #1 AND #2 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(DF3.EQ.1) W3=0.5461D0
   IF(W1.EQ.0.0D0) THEN
      WRITE(OUTLYNE,*)'NUMERIC WORD #1 MUST BE NON-ZERO'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(W2.EQ.0.0D0) THEN
      WRITE(OUTLYNE,*)'NUMERIC WORD #2 MUST BE NON-ZERO'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(W3.EQ.0.0D0) THEN
      WRITE(OUTLYNE,*)'NUMERIC WORD #3 MUST BE NON-ZERO'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(sys_units().EQ.1.0) WAVE=(W3*1.0D-3)/(25.4D0)
   IF(sys_units().EQ.2.0) WAVE=W3*1.0D-4
   IF(sys_units().EQ.3.0) WAVE=W3*1.0D-3
   IF(sys_units().EQ.4.0) WAVE=W3*1.0D-6
   CURVE=1.0D0/DABS((W2**2)/(4.0D0*W1*WAVE))
   IF(WQ.EQ.'ACC') THEN
      REG(40)=REG(9)
      REG(9)=CURVE
      RETURN
   END IF
   IF(SQ.EQ.0) THEN
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2010) W1
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2001) W2,UN(2:12)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2002) W3
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2003) CURVE,UN
      CALL SHOWIT(0)
      RETURN
   END IF
1000 FORMAT(1X)
2000 FORMAT('    CURVATURE FOR M FRINGES OUT OF FLAT')
2010 FORMAT('    NUMBER OF FRINGES OUT OF FLAT = ',G12.4)
2001 FORMAT('                    PART DIAMETER = ',G15.6,1X,A11)
2002 FORMAT ('                       WAVELENGTH = ',G12.4,' MICRONS')
2003 FORMAT('   CORRESPONDING SURFCE CURVATURE = ',G15.6,1X,A12)
   RETURN
END
! SUB SCHWARTZ.FOR
SUBROUTINE SCHWARTZ
!       MAY THE SCHWARTZ BE WITH YOU
!
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDELS THE "RADUNITS", "WIEN", "PLANK"
!       AND "STEFBOLT" CMD LEVEL COMMANDS
!
!                       DEFINE VARIABLES
!
!       "MAY THE SCHWARTZ BE WITH YOU!"
!
!
   REAL*8 T,C1,C1P,C2,VAL ,VAL1,VAL2,SIG,SIGP,RETVAL
!
   EXTERNAL RETVAL
!
!
   C1=37415.0D0
   C1P=1.88365D23
   C2=14387.9D0
   SIG=5.6697D-12
   SIGP=1.52041D+11
!
!     WC IS RADUNITS
   IF(WC.EQ.'RADUNITS') THEN
      IF(STI.EQ.1) THEN
         IF(RADUNI.EQ.1) WRITE(OUTLYNE,10)
         IF(RADUNI.EQ.2) WRITE(OUTLYNE,20)
         CALL SHOWIT(0)
         RETURN
      END IF
      IF(SQ.EQ.1.AND.WQ.NE.'WATTS'.AND.WQ.NE.'PHOTONS') THEN
         WRITE(OUTLYNE,*)'INVALID QUALIFIER WORD USED WITH "RADUNITS"'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(SQ.EQ.0..OR.SST.EQ.1) THEN
         IF(RADUNI.EQ.1) WRITE(OUTLYNE,10)
         IF(RADUNI.EQ.2) WRITE(OUTLYNE,20)
         CALL SHOWIT(0)
10       FORMAT('"RADUNITS" IS SET TO "WATTS"')
20       FORMAT('"RADUNITS" IS SET TO "PHOTONS"')
         RETURN
      END IF
      IF(WQ.EQ.'WATTS') RADUNI=1
      IF(WQ.EQ.'PHOTONS') RADUNI=2
   END IF
!
!
!     WC IS WIEN
   IF(WC.EQ.'WIEN') THEN
      IF(SQ.EQ.1.AND.WQ.NE.'P') THEN
         WRITE(OUTLYNE,*)'INVALID QUALIFIER WORD USED WITH "WIEN"'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(SST.EQ.1) THEN
         WRITE(OUTLYNE,*) 'NO ADDITIONAL INFORMATION AVAILABLE'
         CALL SHOWIT(1)
         RETURN
      END IF
      IF(DF1.EQ.1) THEN
         WRITE(OUTLYNE,*) '"WIEN" REQUIRES EXPLICIT NUMERIC WORD #1 INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(W1.LE.0.0D0) THEN
         WRITE(OUTLYNE,*) '"WIEN" REQUIRES (T) GREATER THAN 0'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(RADUNI.EQ.1) THEN
         VAL=2897.8D0/W1
      END IF
      IF(RADUNI.EQ.2) THEN
         VAL=3669.73D0/W1
      END IF
      IF(WQ.EQ.'P') THEN
         IF(RADUNI.EQ.1) WRITE(OUTLYNE,30) VAL
30       FORMAT('WAVELENGTH FOR PEAK RADIANT EMITTANCE = ',G23.15,' MICRON(S)')
         IF(RADUNI.EQ.2) WRITE(OUTLYNE,40) VAL
40       FORMAT('WAVELENGTH FOR PEAK RADIANT PHOTON EMITTANCE = ',G23.15,' MICRON(S)')
         CALL SHOWIT(0)
      END IF
      REG(40)=REG(9)
      REG(9)=VAL
   END IF
!
!
!     WC IS PLANK
   IF(WC.EQ.'PLANK') THEN
      IF(SQ.EQ.1.AND.WQ.NE.'P') THEN
         WRITE(OUTLYNE,*)'INVALID QUALIFIER WORD USED WITH "PLANK"'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(SST.EQ.1) THEN
         WRITE(OUTLYNE,*) 'NO ADDITIONAL INFORMATION AVAILABLE'
         CALL SHOWIT(1)
         RETURN
      END IF
      IF(DF1.EQ.1.OR.DF2.EQ.1) THEN
         WRITE(OUTLYNE,*)'"PLANK" REQUIRES EXPLICIT NUMERIC WORD #1 AND #2 INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(W1.LE.0.0D0) THEN
         WRITE(OUTLYNE,*) '"PLANK" REQUIRES (T) GREATER THAN 0'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(W2.LE.0.0D0) THEN
         WRITE(OUTLYNE,*) '"PLANK" REQUIRES (LAMBDA) GREATER THAN 0'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(RADUNI.EQ.1) THEN
         VAL=(C1/(W2**5))/(DEXP(C2/(W1*W2))-1.0D0)
      END IF
      IF(RADUNI.EQ.2) THEN
         VAL=(C1P/(W2**4))/(DEXP(C2/(W1*W2))-1.0D0)
      END IF
      IF(WQ.EQ.'P') THEN
         IF(RADUNI.EQ.1) WRITE(OUTLYNE,35) VAL
35       FORMAT('RADIANT EMITTANCE = ',G23.15,' WATTS/CM2-MICRON')

         IF(RADUNI.EQ.2) WRITE(OUTLYNE,45) VAL
45       FORMAT('RADIANT PHOTON EMITTANCE = ',G23.15,' PHOTONS/SEC-CM2-MICRON')
         CALL SHOWIT(0)
      END IF
      REG(40)=REG(9)
      REG(9)=VAL
   END IF
!
!
!     WC IS STEFBOLT
   IF(WC.EQ.'STEFBOLT') THEN
      IF(SQ.EQ.1.AND.WQ.NE.'P') THEN
         WRITE(OUTLYNE,*)'INVALID QUALIFIER WORD USED WITH "STEFBOLT"'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(SST.EQ.1) THEN
         WRITE(OUTLYNE,*) 'NO ADDITIONAL INFORMATION AVAILABLE'
         CALL SHOWIT(1)
         RETURN
      END IF
      IF(DF1.EQ.1) THEN
         WRITE(OUTLYNE,*)'"STEFBOLT" REQUIRES EXPLICIT NUMERIC WORD #1'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(W1.LE.0.0D0) THEN
         WRITE(OUTLYNE,*) '"STEFBOLT" REQUIRES (T) GREATER THAN 0'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(DF2.EQ.1) W2=1.0D20
      IF(DF3.EQ.1) W3=0.0D0
      IF(W2.LE.0.0D0) THEN
         WRITE(OUTLYNE,*)'"STEFBOLT" REQUIRES (LAMBDA UPPER) GREATER THAN 0'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(W3.LT.0.0D0) THEN
         WRITE(OUTLYNE,*)'"STEFBOLT" REQUIRES (LAMBDA LOWER) GREATER THAN OR EQUAL TO 0'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(W3.GE.W2) THEN
         WRITE(OUTLYNE,*) '"STEFBOLT" REQUIRES (LAMBDA UPPER)'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'GREATER THAN (LAMBDA LOWER)'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(RADUNI.EQ.1) THEN
!     CASE OF TOTAL
         IF(W2.EQ.1.0D20.AND.W3.EQ.0.0D0) THEN
            VAL=SIG*(W1**4)
            GO TO 200
         END IF
!     CASE OF ONLY LAMBDA UPPER
         IF(W2.LT.1.0D20.AND.W3.EQ.0.0D0) THEN
            VAL=RETVAL(W1,W2,RADUNI)
            GO TO 200
         END IF
!     CASE OF RANGE
         IF(W2.LT.1.0D20.AND.W3.GT.0.0D0) THEN
            VAL1=RETVAL(W1,W2,RADUNI)
            VAL2=RETVAL(W1,W3,RADUNI)
            VAL=VAL1-VAL2
            GO TO 200
         END IF
      END IF
      IF(RADUNI.EQ.2) THEN
!     CASE OF TOTAL
         IF(W2.EQ.1.0D20.AND.W3.EQ.0.0D0) THEN
            VAL=SIGP*(W1**3)
            GO TO 200
         END IF
!     CASE OF ONLY LAMBDA UPPER
         IF(W2.LT.1.0D20.AND.W3.EQ.0.0D0) THEN
            VAL=RETVAL(W1,W2,RADUNI)
            GO TO 200
         END IF
!     CASE OF RANGE
         IF(W2.LT.1.0D20.AND.W3.GT.0.0D0) THEN
