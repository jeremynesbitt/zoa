               RHOA=DABS(PXTRAY(1,I))+DABS(PXTRAY(5,I))
            END IF
            IF(surf_curvature(I).EQ.0.0D0) THEN
               COR1=0.0D0
            ELSE
               RD=DABS(1.0D0/surf_curvature(I))
               C=surf_curvature(I)
               C2=surf_curvature(I)**2
               RHO2=RHOA**2
               SAG1=((C*RHO2)/(1.0D0+(DSQRT(1.0D0-(C2*RHO2)))))
               IF(SAG1.GE.0.0D0) SIGNIT1=1.0D0
               IF(SAG1.LT.0.0D0) SIGNIT1=-1.0D0
               SAG1=DABS(SAG1)
               COR1=(1.0D0/3.0D0)*PII*(SAG1**2)*((3.0D0*RD)-SAG1)
               COR1=DABS(COR1)
               IF(SIGNIT1.LT.0.0D0) THEN
                  COR1=(PII*RHO2*SAG1)-COR1
               END IF
            END IF
!     ADJUSTMENT FOR R2
            IF(surf_clap_type(I+1).EQ.1.0D0.AND.surf_multi_clap_flag(I+1).EQ.0.0D0) THEN
               RHOB=DABS(surf_clap_dim(I+1, 1))
            ELSE
               RHOB=DABS(PXTRAY(1,I+1))+DABS(PXTRAY(5,I+1))
            END IF
            IF(surf_curvature(I+1).EQ.0.0D0) THEN
               COR2=0.0D0
            ELSE
               RD=DABS(1.0D0/surf_curvature(I+1))
               C=surf_curvature(I+1)
               C2=surf_curvature(I+1)**2
               RHO2=RHOB**2
               SAG2=((C*RHO2)/(1.0D0+(DSQRT(1.0D0-(C2*RHO2)))))
               IF(SAG2.GE.0.0D0) SIGNIT2=1.0D0
               IF(SAG2.LT.0.0D0) SIGNIT2=-1.0D0
               SAG2=DABS(SAG2)
               COR2=(1.0D0/3.0D0)*PII*(SAG2**2)*((3.0D0*RD)-SAG2)
               COR2=DABS(COR2)
               IF(SIGNIT2.GT.0.0D0) THEN
                  COR2=(PII*RHO2*SAG2)-COR2
               END IF
            END IF
!     WEIGHT OF PLUG
            H=DABS(surf_thickness(I))
            IF(SIGNIT1.GT.0.0D0) H=H-SAG1
            IF(SIGNIT2.LT.0.0D0) H=H-SAG2
            PLUG=(1.0D0/3.0D0)*PII*H*((RHOA**2)+(RHOA*RHOB)+(RHOB**2))
            MYWEIGHT=(((PLUG+COR1+COR2)*CFAC)*surf_spgr(I))
            IF(MYWEIGHT.LT.0.0D0) MYWEIGHT=0.0D0
            TOTALW=TOTALW+MYWEIGHT
            MYWEIGHT=0.0D0
         END IF
      END DO
   END IF
   DO I=INT(W1),INT(W2)
      IF(surf_mirror_thickness(I).NE.0.0D0) THEN
!     MIRROR THICKNESS
!     ADJUSTMENT FOR R1
         IF(surf_clap_type(I).EQ.1.0D0.AND.surf_multi_clap_flag(I).EQ.0.0D0) THEN
            RHOA=DABS(surf_clap_dim(I, 1))
         ELSE
            RHOA=DABS(PXTRAY(1,I))+DABS(PXTRAY(5,I))
         END IF
         IF(surf_curvature(I).EQ.0.0D0) THEN
            COR1=0.0D0
         ELSE
            RD=DABS(1.0D0/surf_curvature(I))
            C=surf_curvature(I)
            C2=surf_curvature(I)**2
            RHO2=RHOA**2
            SAG1=((C*RHO2)/(1.0D0+(DSQRT(1.0D0-(C2*RHO2)))))
            IF(SAG1.GE.0.0D0) SIGNIT1=1.0D0
            IF(SAG1.LT.0.0D0) SIGNIT1=-1.0D0
            SAG1=DABS(SAG1)
            COR1=(1.0D0/3.0D0)*PII*(SAG1**2)*((3.0D0*RD)-SAG1)
            COR1=DABS(COR1)
            IF(SIGNIT1.LT.0.0D0) THEN
               COR1=(PII*RHO2*SAG1)-COR1
            END IF
         END IF
!     ADJUSTMENT FOR BACK OF MIRROR
         IF(surf_clap_type(I).EQ.1.0D0.AND.surf_multi_clap_flag(I).EQ.0.0D0) THEN
            RHOB=DABS(surf_clap_dim(I, 1))
         ELSE
            RHOB=DABS(PXTRAY(1,I))+DABS(PXTRAY(5,I))
         END IF
         IF(surf_curvature(I).EQ.0.0D0) THEN
            COR2=0.0D0
         ELSE
            IF(surf_refractive_index(I, 1).LT.0.0D0) THEN
               RD=(1.0D0/surf_curvature(I))-DABS(surf_mirror_thickness(I))
            ELSE
               RD=(1.0D0/surf_curvature(I))+DABS(surf_mirror_thickness(I))
            END IF
            C=1.0D0/RD
            C2=C**2
            RHO2=RHOB**2
            SAG2=((C*RHO2)/(1.0D0+(DSQRT(1.0D0-(C2*RHO2)))))
            IF(SAG2.GE.0.0D0) SIGNIT2=1.0D0
            IF(SAG2.LT.0.0D0) SIGNIT2=-1.0D0
            SAG2=DABS(SAG2)
            COR2=(1.0D0/3.0D0)*PII*(SAG2**2)*((3.0D0*RD)-SAG2)
            COR2=DABS(COR2)
            IF(SIGNIT2.GT.0.0D0) THEN
               COR2=(PII*RHO2*SAG2)-COR2
            END IF
         END IF
!     WEIGHT OF PLUG
         H=DABS(surf_mirror_thickness(I))
         IF(SIGNIT1.GT.0.0D0) H=H-SAG1
         IF(SIGNIT2.LT.0.0D0) H=H-SAG2
         PLUG=(1.0D0/3.0D0)*PII*H*((RHOA**2)+(RHOA*RHOB)+(RHOB**2))
         THMFACTOR=(((PLUG+COR1+COR2)*CFAC)*surf_spgr(I))
         IF(THMFACTOR.LT.0.0D0) THMFACTOR=0.0D0
         MYWEIGHT=MYWEIGHT+THMFACTOR
         TOTALW=TOTALW+((MYWEIGHT/1000.0D0)*surf_price(I))
      END IF
   END DO
   MYWEIGHT=0.0D0
   THMFACTOR=0.0D0
   WEIGT=TOTALW
   TOTALW=0.0D0
!
   IF(WQ.EQ.'ACC') THEN
      REG(40)=REG(9)
      REG(9)=WEIGT
      RETURN
   END IF
   IF(SQ.EQ.0) THEN
      REG(40)=REG(9)
      REG(9)=WEIGT
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2010) INT(W1)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2020) INT(W2)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2002) WEIGT
      CALL SHOWIT(0)
      RETURN
   END IF
1000 FORMAT(1X)
2000 FORMAT('    WEIGHT CALCULATION')
2010 FORMAT('    FROM SURFACE # = ',I3)
2020 FORMAT('      TO SURFACE # = ',I3)
2002 FORMAT('              COST = ',G15.6,1X,'Cost Units')
   RETURN
END
