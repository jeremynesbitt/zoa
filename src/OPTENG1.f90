! SUB ZZEEOO.FOR
SUBROUTINE ZZEEOO
!
   use DATMAI
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE ZZEEOO. THIS SUBROUTINE IMPLEMENTS
!     THE CMD LEVEL "Z0" COMMAND WHICH CALCULTES THE Z POSITIONS OF
!     OF CONIC GIVEN THE CURVATURE CV, THE CONIC CONSTANT CC
!     AND A RHO HEIGHT.
!
!       FORM OF THE FOCI COMMAND IS:
!
!       Z0 (R or C) ,(radius or curvature) , (conic constant) , RHO
!
   REAL*8 RHO,CEE,Z1,Z2,AC,KAPPA
!
!
   IF(STI.EQ.1) THEN
      WRITE(OUTLYNE,*)'"Z0" COMPUTES AND DISPLAYS CONIC SECTION Z POSITIONS'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'GIVEN RADII OR CURVATURE CONIC CONSTANT AND RHO AS INPUTS'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(SST.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
      WRITE(OUTLYNE,*)'"Z0" ONLY TAKES QUALIFIER AND NUMERIC WORD #1, #2 AND #3 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       HANDLE DEFAULT VALUES FOR NW1 AND NW2 AND NW3
!
   IF(DF1.EQ.1.OR.DF2.EQ.1.OR.DF3.EQ.1) THEN
      WRITE(OUTLYNE,*)'"Z0" REQUIRES EXPLICIT NUMERIC WORD #1, #2 AND #3 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(SQ.EQ.0) THEN
      SQ=1
      WQ='R       '
   END IF
   IF(WQ(1:1).NE.'R'.AND.WQ(1:1).NE.'C') THEN
      WRITE(OUTLYNE,*)'"Z0" ONLY TAKES "R" (FOR RADIUS)'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'               OR "C" (FOR CURVATURE) AS QUALIFIER WORDS'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
!     FLAT SURFACES
   IF(W1.EQ.0.0D0.OR.WQ.EQ.'R'.AND.DABS(W1).GT.1.0D20) THEN
      WRITE(OUTLYNE,*)'"Z0" REQUIRES A NON-PLANO SURFACE'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!
   Z1=0.0D0
   Z2=0.0D0
   IF(WQ(1:1).EQ.'R') CEE=1/W1
   IF(WQ(1:1).EQ.'C') CEE=W1
   KAPPA=W2
   RHO=W3
   AC=4.0D0-(4.0D0*(KAPPA+1.0D0)*(RHO**2)*(CEE**2))
   IF(AC.LT.0.0D0) THEN
      WRITE(OUTLYNE,*)'"Z0" CAN NOT BE CALCULATED WITH CURRENT INPUT VALUES'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*) 'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF((KAPPA+1.0D0).NE.0.0D0) THEN
      Z1=(2.0D0+DSQRT(AC))/(2.0D0*(KAPPA+1.0D0)*CEE)
      Z2=(2.0D0-DSQRT(AC))/(2.0D0*(KAPPA+1.0D0)*CEE)
   ELSE
!     PARABOLA
      Z1=((RHO**2)*CEE)/2.0D0
   END IF
   IF((KAPPA+1.0D0).EQ.0.0D0) THEN
!     ONE Z VALUE
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,5000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,5001)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1001) CEE,(1.0D0/CEE)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1002) KAPPA
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1003) RHO
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2000) Z1
      CALL SHOWIT(0)
      REG(40)=REG(9)
      REG(9)=(Z1)
   ELSE
!     TWO ZEE VALUES
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,5000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,5002)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1001) CEE,(1.0D0/CEE)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1002) KAPPA
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1003) RHO
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2001) Z1
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2002) Z2
      CALL SHOWIT(0)
      REG(40)=REG(9)
      REG(10)=Z1
      REG(9)=Z2
   END IF
!
1000 FORMAT(1X)
5000 FORMAT('FOR THE INPUT VALUES OF:')
5001 FORMAT('PARABOLA WITH ONE Z VALUE')
5002 FORMAT('NON-PARABOLA WITH TWO Z VALUES')
1001 FORMAT('     CURVATURE = ',D23.15,' (RADIUS = ',D23.15,')')
1002 FORMAT('CONIC CONSTANT = ',D23.15)
1003 FORMAT('           RHO = ',D23.15)
2000 FORMAT('  THE NEW Z IS = ',D23.15)
2001 FORMAT(' THE NEW Z1 IS = ',D23.15)
2002 FORMAT(' THE NEW Z2 IS = ',D23.15)
   RETURN
END
