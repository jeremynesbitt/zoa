   END IF
!
   IF(W2.EQ.-1.0D0) THEN
!     PARABOLA
      IF(WQ(1:1).EQ.'C') W11=W1
      IF(WQ(1:1).EQ.'R') W11=1.0D0/W1
      FC=1.0D0/(W11*2.0D0)
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1001)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1002) FC
      CALL SHOWIT(0)
      REG(40)=REG(9)
      REG(9)=FC
      RETURN
   END IF
   IF(W2.LT.-1.0D0) THEN
!     HYPERBOLA
      IF(WQ(1:1).EQ.'C') W11=W1
      IF(WQ(1:1).EQ.'R') W11=1.0D0/W1
      B=-1.0D0/(W11*(W2+1.0D0))
      FC1=B*(DSQRT(-W2)-1.0D0)
      FC2=B*(-DSQRT(-W2)-1.0D0)
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2001)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2002) FC1
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2003) FC2
      CALL SHOWIT(0)
      REG(40)=REG(9)
      REG(10)=FC1
      REG(9)=FC2
      RETURN
   END IF
   IF(W2.LT.0.0D0.AND.W2.GT.-1.0D0) THEN
!     PROLATE SPHERIOD
      IF(WQ(1:1).EQ.'C') W11=W1
      IF(WQ(1:1).EQ.'R') W11=1.0D0/W1
      B=(1.0D0/(W11*(W2+1.0D0)))
      FC1=B*(1.0D0-DSQRT(DABS(W2)))
      FC2=B*(1.0D0+DSQRT(DABS(W2)))
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,3001)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,3002) FC1
      CALL SHOWIT(0)
      WRITE(OUTLYNE,3003) FC2
      CALL SHOWIT(0)
      REG(40)=REG(9)
      REG(10)=FC1
      REG(9)=FC2
      RETURN
   END IF
1000 FORMAT(1X)
1001 FORMAT('SURFACE TYPE = PARABOLA')
1002 FORMAT('SURFACE VERTEX TO FOCUS DISTANCE IS = ',D23.15)
2001 FORMAT('SURFACE TYPE = HYPERBOLA')
2002 FORMAT('SURFACE VERTEX TO NEAR FOCUS DISTANCE IS = ',D23.15)
2003 FORMAT('SURFACE VERTEX TO FAR  FOCUS DISTANCE IS = ',D23.15)
3001 FORMAT('SURFACE TYPE = PROLATE SPHERIOD')
3002 FORMAT('SURFACE VERTEX TO NEAR FOCUS DISTANCE IS = ',D23.15)
3003 FORMAT('SURFACE VERTEX TO FAR  FOCUS DISTANCE IS = ',D23.15)
   RETURN
END
! SUB APOVERT.FOR
SUBROUTINE APOVERT
!
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDELS THE "K0" AND "CVG"
!       CMD LEVEL COMMANDS
!
!                       DEFINE VARIABLES
!
   REAL*8 CURVE,ZZERO
!
!
   ZZERO=0.0D0
   IF(SQ.EQ.1.OR.SST.EQ.1) THEN
      WRITE(OUTLYNE,*)'"K0" AND "CVG" TAKE NO QUALIFIER OR STRING INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(WC.EQ.'K0'.AND.STI.EQ.1) THEN
      WRITE(OUTLYNE,*)'"K0" DOES A K0, APO-VERTEXT CALCULATION'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(WC.EQ.'CVG'.AND.STI.EQ.1) THEN
      WRITE(OUTLYNE,*)'"CVG" DOES A CURVATURE, APO-VERTEXT CALCULATION'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(WC.EQ.'K0') THEN
      IF(S4.EQ.1.OR.S5.EQ.1) THEN
         WRITE(OUTLYNE,*)'"K0" TAKES NO NUMERIC WORD #4 OR #5 INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         RETURN
      END IF
      IF(DF1.EQ.1.OR.DF2.EQ.1.OR.DF3.EQ.1) THEN
         WRITE(OUTLYNE,*)'"K0" REQUIRES EXPLICIT NUMERIC WORD #1, #2 AND #3 INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         RETURN
      END IF
      IF(W1.EQ.0.0D0) THEN
         WRITE(OUTLYNE,*)'"K0" REQUIRES A NON-ZERO NUMERIC WORD #1 INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         RETURN
      END IF
      ZZERO=(1.0D0/W1)+((1.0D0+W2)*W3)
10    FORMAT('THE "Z0" VALUE IS : ',D23.15,' LENS UNITS')
      WRITE(OUTLYNE,10) ZZERO
      CALL SHOWIT(0)
      REG(40)=REG(9)
      REG(9)=ZZERO
      RETURN
   END IF
   IF(WC.EQ.'CVG') THEN
      IF(S4.EQ.1.OR.S5.EQ.1) THEN
         WRITE(OUTLYNE,*)'"CVG" TAKES NO NUMERIC WORD #4 OR #5 INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         RETURN
      END IF
      IF(DF1.EQ.1.OR.DF2.EQ.1.OR.DF3.EQ.1) THEN
         WRITE(OUTLYNE,*)'"CVG" REQUIRES EXPLICIT NUMERIC WORD #1, #2 AND #3 INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         RETURN
      END IF
      CURVE=(W1)+((1.0D0+W2)*W3)
      IF(CURVE.EQ.0.0D0) THEN
         WRITE(OUTLYNE,*)'THE "CVR" VALUE WAS INFINITE. CHECK YOUR INPUT VALUES'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         REG(40)=REG(9)
         REG(9)=0.0D0
         CALL MACFAL
         RETURN
      END IF
20    FORMAT('THE "CVR" VALUE IS : ',D23.15,' INVERSE LENS UNITS')
      WRITE(OUTLYNE,20) (1.0D0/CURVE)
      CALL SHOWIT(0)
      REG(40)=REG(9)
      REG(9)=(1.0D0/CURVE)
      RETURN
   END IF
!
END
