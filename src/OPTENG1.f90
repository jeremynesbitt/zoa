SUBROUTINE DLRPFR
!
   use DATLEN
   use mod_system
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE DLRPFR. THIS SUBROUTINE IMPLEMENTS THE
!       DELTA RADIUS PER FRINGE CALCULATION
!       NW1=RADIUS OF CURVATURE IN CURRENT LENS UNITS (REQUIRED)
!       NW2=PART DIAMETER IN CURRENT LENS UNITS (REQUIRED)
!       NW3=WAVELENGTH IN MICRONS (DEFAULT = 0.5461)
!         NW4 AND NW5 NOT USED
!         WQ NOT USED
!         WS NOT USED
!
   CHARACTER UN*11
!
   REAL*8 DR,WAVE,ARG1
!
!
   IF(sys_units().EQ.1.0) UN='INCHES     '
   IF(sys_units().EQ.2.0) UN='CENTIMETERS'
   IF(sys_units().EQ.3.0) UN='MILLIMETERS'
   IF(sys_units().EQ.4.0) UN='METERS'
!
!
   IF(SST.EQ.1) THEN
      WRITE(OUTLYNE,*)'"DR/FR" TAKES NO ALPHANUMERIC STRING INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(S4.EQ.1.OR.S5.EQ.1) THEN
      WRITE(OUTLYNE,*)'"DR/FR" TAKES NO NUMERIC WORD #4 OR #5 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(SQ.EQ.1.AND.WQ.NE.'ACC') THEN
      WRITE(OUTLYNE,*)'INVALID QUALIFIER WORD USED WITH "DR/FR"'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!       HANDLE DEFAULT VALUES FOR NW1, NW2 AND NW3
!
   IF(DF1.EQ.1.OR.DF2.EQ.1) THEN
      WRITE(OUTLYNE,*)'"DR/FR" REQUIRES EXPLICIT NUMERIC WORD #1 AND #2 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(DF3.EQ.1) W3=0.5461D0
   IF(W1.EQ.0.0D0) THEN
      WRITE(OUTLYNE,*)'NUMERIC WORD #1 MUST BE NON-ZERO'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(W2.EQ.0.0D0) THEN
      WRITE(OUTLYNE,*)'NUMERIC WORD #2 MUST BE NON-ZERO'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(W3.EQ.0.0D0) THEN
      WRITE(OUTLYNE,*)'NUMERIC WORD #3 MUST BE NON-ZERO'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   ARG1=(W1**2)-((W2/2.0D0)**2)
   IF(ARG1.LT.0.0D0) THEN
      WRITE(OUTLYNE,*)'DR/FR CAN NOT BE CALCULATED FOR CURRENT INPUT VALUES'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(sys_units().EQ.1.0) WAVE=(W3*1.0D-3)/(25.4D0)
   IF(sys_units().EQ.2.0) WAVE=W3*1.0D-4
   IF(sys_units().EQ.3.0) WAVE=W3*1.0D-3
   IF(sys_units().EQ.4.0) WAVE=W3*1.0D-6
   DR=DABS((WAVE*DSQRT(ARG1))/(2.0D0*(DSQRT(ARG1)-DABS(W1))))
   IF(WQ.EQ.'ACC') THEN
      REG(40)=REG(9)
      REG(9)=DR
      RETURN
   END IF
   IF(SQ.EQ.0) THEN
      WRITE(OUTLYNE,1000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2010) W1,UN
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2001) W2,UN
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2002) W3
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2003) DR,UN
      CALL SHOWIT(0)
      RETURN
   END IF
1000 FORMAT(1X)
2000 FORMAT('    DELTA RADIUS PER FRINGE CALCULATION')
2010 FORMAT('    RADIUS OF CURVATURE = ',G15.6,1X,A11)
2001 FORMAT('          PART DIAMETER = ',G15.6,1X,A11)
2002 FORMAT('             WAVELENGTH = ',G12.4,' MICRONS')
2003 FORMAT('DELTA RADIUS PER FRINGE = ',G15.6,1X,A11)
   RETURN
END
! SUB DTOR.FOR
SUBROUTINE DTOR
!
   use DATMAI
