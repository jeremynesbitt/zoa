            IF(VTYPE.EQ.20) ALTYPE=115
            IF(VTYPE.EQ.21) ALTYPE=46
            IF(VTYPE.EQ.22) ALTYPE=47
            IF(VTYPE.EQ.23) ALTYPE=48
