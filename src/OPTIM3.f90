!     NOW UPDATE THE CONFIG ARRAY
            CONFG(CFADD(VADD,3),CFADD(VADD,9))(CFADD(VADD,6):CFADD(VADD,7))=AV1(1:23)
         ELSE
            CFVAL(VADD,1)=V1
            CFCHAR(VADD,1)=AV1
!     NOW UPDATE THE CONFIG ARRAY
            CONFG(CFADD(VADD,3),CFADD(VADD,9))(CFADD(VADD,4):CFADD(VADD,5))=AV1(1:23)
         END IF
!     NOW LOOK UP WHERE THIS CHARACTER REPRESENTATION OF THE NEW VALUE
!     SHOULD BE STUFFED INTO THE CONFIG ARRAYS CONFG AND
!     STUFF IT THERE.
!
!     NOW LOOP BACK AND REPEAT FOR THE NEXT VARIABLE.
!     FINISHED WITH A CONFIG VARIABLE
      END IF
!     LOOP TO NEXT VARIABL
   END DO
!     UPDATE THE LENS OR THE PERMANENT LENS WILL BE ******!
   CALL FIXDEFORMFILE
   F6=1
   F1=0
   F22=0
   LNSTYP=2
   CALL LNSEOS
!**********************************************************************
!     EVALUATE OPREANDS AND FMT
   OPCALC_TYPE=3
