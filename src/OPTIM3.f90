   SAVE_KDP(4)=SAVEINPT(4)
   F28=1
   MSG=.FALSE.
   OPTMES=.FALSE.
   WC='FMT'
   WQ='        '
   SQ=0
   SN=0
   SST=0
   WS=' '
   DF1=1
   S1=0
   S2=0
   S3=0
   S4=0
   S5=0
   DF2=1
   DF3=1
   DF4=1
   DF5=1
   STI=0
   CALL FMT2
   REST_KDP(4)=RESTINPT(4)
   DEALLOCATE(OLDX,DELOLDX,VSAVE,STAT=ALLOERR)
   RETURN
!       ALL DONE
END
! SUB SOLVIT.FOR
SUBROUTINE SOLVIT(IID,JJD,MDERIV,SILENT)
   use type_utils
!
   use DATCFG
   use DATSUB
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
   CHARACTER OOLDWQ*8
!
   LOGICAL ITDER,SILENT
!
   INTEGER SSN,SM,NP2,MP,N,J,I,L,M,VTYPE,ALTYPE,VADD,VCFG ,VN1,MAXCNT,IV1,VN,ALLOERR,IID,JJD, II
!
   INTEGER ISURF
!
   REAL*8 NEWDEFVAL,PFACSCL
!
   COMMON/DEFVALCOM/NEWDEFVAL
!
   REAL*8 X(1:100000),WT,V1,MAX,VTEMP,OLDCUR,NEWCUR
!
   DIMENSION WT(:)
!
   ALLOCATABLE :: WT
!
   LOGICAL LVAL
!
   CHARACTER AV1*23

   COMMON/SVD1/SM,SSN,MP,NP2
!
   COMMON/SVD2/X
!
   COMMON/CAUX1/V1,AV1
!
   REAL*8 MDERIV
!
   DIMENSION MDERIV(1:IID,1:JJD)
!
   LOGICAL ERR1,ERR2
!
!
   REAL*8 W,V,BTB,BTG,DIAGSUM,WMAX,WMIN
   DIMENSION W(:),V(:,:),BTB(:,:),BTG(:)
   ALLOCATABLE :: W,V,BTB,BTG
   VN=IID
   VN1=IID
   DEALLOCATE(WT,W,V,BTB,BTG,STAT=ALLOERR)
   ALLOCATE (W(VN),V(VN,VN),WT(VN1),BTB(VN1,VN1),BTG(VN1),STAT=ALLOERR)
   W(1:VN)=0.0D0
   WT(1:VN)=0.0D0
   BTG(1:VN)=0.0D0
   V(1:VN,1:VN)=0.0D0
   BTB(1:VN,1:VN)=0.0D0
   OOLDWQ='        '
   ITDER=.FALSE.
   !call LogTermDebug("Starting SOLVIT")
   ! Hack because it seems damping is off.
   DMP = 6
!
   IF(WQ.NE.'D'.AND.WQ.NE.'DIR') THEN
!
!                JUST ITER OR ITER,1
!       THIS IS SUBROUTINE SOLVIT. THIS IS THE SUBROUTINE WHICH
!       HANDLES THE SOLUTION TO THE DAMPED LEAST SQUARES PROBLEM.
!     FORM (BTB+KC)
!     FIRST JUST BTB
!     THIS IS THE MATRIX PRODUCT OF THE TRANSPOSE OF THE DERIV MATRIX
!     TIMES THE DERIV MATRIX ITSELF
!     L IS THE FIRST INDEX OF BTB AND M IS THE SECOND
!     BTB IS SQUARE WITH DIMENSION VBCNT
!
      IF(VBCNT.GE.OPCNT) MAXCNT=VBCNT
      IF(VBCNT.LT.OPCNT) MAXCNT=OPCNT
!
!       ZERO OUT TOO SMALL DERIVATIVES
!
      DO I=1,MAXCNT
         DO M=1,MAXCNT
            IF(DABS(MDERIV(I,M)).LT.ONTOL) MDERIV(I,M)=0.0D0
         END DO
      END DO
!
      DO L=1,MAXCNT
         DO M=1,MAXCNT
            DO I=1,MAXCNT
               BTB(L,M)=BTB(L,M)+(MDERIV(I,L)*MDERIV(I,M))
            END DO
         END DO
      END DO
!     NOW KC IS A DIAGONAL TERM ADDED TO THE DIAGONAL ELEMENTS OF
!     BTB
!     NOTE IF THE VARIABLE WEIGHTS ARE ALL 1.0D0 (DEFAULT)
!     KC BECOMES KI, THE P FACTOR TIMES THE IDENTITY MATRIX.
      DO I=1,VBCNT
         IF(VARABL(I,7).NE.0.0D0) WT(I)=1.0D0/VARABL(I,7)
         IF(VARABL(I,7).EQ.0.0D0) WT(I)=0.0D0
      END DO
      IF(DMP.EQ.6) THEN
         DIAGSUM=0.0D0
         DO I=1,MAXCNT
            DIAGSUM=DIAGSUM+DABS(BTB(I,I))
         END DO
         IF(DIAGSUM.EQ.0.0D0) THEN
            WRITE(OUTLYNE,*) 'THE DIAGONAL OF BTB IS ZERO'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*) 'DAMPING WILL HAVE NO EFFECT'
            CALL SHOWIT(1)
         END IF
      END IF

      !call LogTermDebug("PFAC is "//real2str(PFAC))
      DO I=1,MAXCNT
         IF(DMP.EQ.6) then

            !call LogTermDebug("**************************")
            !I believe there is a bug here in the use of the damping factor
            !It should not be multiplied by the derivative transpose *
            !derivative, it should be added to it
            !Eg see Handbook of Optical Systems, VOl 3 Section 32.4.4
            !While I am at it I got rid of the factor of 1000 to make things
            !A bit cleßaner (time will tell whether I regret it)
            !BTB(I,I)=BTB(I,I)*(1.0D0+(1000.0D0*PFAC*WT(I)))
            BTB(I,I)=BTB(I,I)+PFAC*WT(I)
            !call LogTermDebug("**************************")
         end if
      END DO
!     NOW FORM BTG
      DO L=1,VBCNT
         DO I=1,OPCNT
            BTG(L)=BTG(L)+(MDERIV(I,L)*(-OPERND(I,14)))
         END DO
      END DO
!
!     NOW SOLVE THE PROBLEM WITH SVD AND THEN APPLY THE SOLUTION TO
!     THE LENS
!
      MP=MAXCNT
      NP2=MAXCNT
      N=MAXCNT
      M=MAXCNT
!       DO SINGULAR VALUE DECOMPOSITION
      SM=M
      SSN=N

      CALL SVDCMPA(W,V,VN,VN1,BTB,BTG)
      M=SM
      N=SSN
      WMAX=-1.0D300
      DO J=1,N
         IF(W(J).GT.WMAX) WMAX=W(J)
      END DO
      WMIN=WMAX*SINGTOL
      DO J=1,N
         IF(W(J).LT.WMIN) W(J)=0.0D0
      END DO
!
!       SOLVE LINEAR EQUATION
!
      SM=M
      SSN=N
      ! THis method is updating the solution vector
      ! Original numerical recipes code has X as a return value
      ! Not sure why that wasn't done here
      ! THis is supposed to sove A x = B
      ! A is solution of SVD (why W is used here)
      ! BTG = B
      CALL SVBKSBA(W,V,VN,VN1,BTB,BTG)
      M=SM
      N=SSN

      !call LogTermDebug("about tu update variables in SOLVIT!")
      !call LogTermDebug("VBCNT IS "//int2str(VBCNT))
      !call LogTermDebug("VAR 1 is "//real2str(VARABL(1,4)))
      !call LogTermDebug("X(I) is "//real2str(X(1)))
!
!
!     NOW APPLY THE SOLUTION VECTOR X(I) TO THE VARABL ARRAY AND TO THE LENS
!     THE ITH SOLUTION VECTOR COMPONENT APPLIES TO THE ITH VARIABLE
      LCVLCV=0.0D0
      DO I=1,VBCNT
!
!     THE CURRENT VARIABLE VALUE BECOMES THE PREVIOUS VARIABLE VALUE
         VARABL(I,5)=VARABL(I,4)
!
!     THE NEW SOLUTION VECTOR ADDED TO THE PREVIOS VARIABLE VALUE
!     BECOMES THE NEW SOLUTION VECTOR
!     VALUE
         LCVLCV=LCVLCV+DABS(((X(I)))/(DINMUL*VARABL(I,8)))
         VARABL(I,4)=VARABL(I,4)+(X(I))
!
         IF(VARABL(I,1).EQ.1.0D0) THEN
            IF(VARABL(I,9).LT.THMINLIM) VARABL(I,9)=THMINLIM
            IF(VARABL(I,10).GT.THMAXLIM) VARABL(I,10)=THMAXLIM
         END IF
         IF(VARABL(I,1).EQ.2.0D0.OR.VARABL(I,1).EQ.10.0D0) THEN
            IF(RDNEGLIM.EQ.0.0D0) RDNEGLIM=-1.0D-20
            IF(RDPOSLIM.EQ.0.0D0) RDPOSLIM=1.0D-20
            IF(VARABL(I,9).LT.(1.0D0/RDNEGLIM)) VARABL(I,9)=1.0D0/RDNEGLIM
            IF(VARABL(I,10).GT.(1.0D0/RDPOSLIM)) VARABL(I,10)=1.0D0/RDPOSLIM
         END IF
!     BOUNDS CHECKER
         IF(VARABL(I,4).LT.VARABL(I,9)) THEN
            VARABL(I,4)=VARABL(I,9)
            WRITE(OUTLYNE,*)'WARNING: '
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'VARIABLE # ',I,' HAS REACHED ITS LOWER BOUNDARY VALUE'
            CALL SHOWIT(1)
         END IF
         IF(VARABL(I,4).GT.VARABL(I,10)) THEN
            VARABL(I,4)=VARABL(I,10)
            WRITE(OUTLYNE,*)'WARNING: '
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'VARIABLE # ',I,' HAS REACHED ITS UPPER BOUNDARY VALUE'
            CALL SHOWIT(1)
         END IF
!
!
!     THE VARIABLE CHANGE IS (THE NEW CURRENT VALUE-OLD LAST CURRENT VALUE)
         VARABL(I,6)=VARABL(I,4)-VARABL(I,5)
!
!     DINCRS ARE ONLY CHANGED BY HAND AND BY THE ITER ADJUST COMMAND
!
      END DO
      LCVLCV=(LCVLCV)/DBLE(VBCNT)
!
!     NOW APPLY THE CHANGE VECTOR TO THE LENS
!**********************************************************************
      DO I=1,VBCNT
!     FIRST, IF THE VARIABLE IS A CONFIG 1 VARIABLE
!     NEXT IF THE VARIABLE IS IN A CONFIG OTHER THAN 1
         IF(VARABL(I,2).EQ.1.0D0) THEN
!     NON-CONFIGS VARIABLE MEANING CONFIG 1
!     THIS IS A LENS LEVEL VARIABLE CHANGE
!     GET THE DATA TYPE NUMBER OF THE VARIABLE
            VTYPE=INT(VARABL(I,1))
!                          CURVATURE
            IF(VTYPE.EQ.2.OR.VTYPE.EQ.1) THEN
!     SURFACE CURVATURE
!     NEW VALUE IS:
               V1=VARABL(I,4)
               call set_surf_curvature(INT(VARABL(I,3)), V1)
!                       CURVATURE DONE
            END IF
            IF(VTYPE.EQ.10.OR.VTYPE.EQ.9) THEN
!     SURFACE TORIC CURVATURE
!     NEW VALUE IS:
               V1=VARABL(I,4)
               call set_surf_toric_curvature(INT(VARABL(I,3)), V1)
!                       TORIC CURVATURE DONE
            END IF
            IF(VTYPE.GE.3.AND.VTYPE.LE.8) THEN
               IF(VTYPE.EQ.3) ALTYPE=3
               IF(VTYPE.EQ.4) ALTYPE=2
               IF(VTYPE.EQ.5) ALTYPE=4
               IF(VTYPE.EQ.6) ALTYPE=5
               IF(VTYPE.EQ.7) ALTYPE=6
               IF(VTYPE.EQ.8) ALTYPE=7
!     NEW VALUE IS:
               V1=VARABL(I,4)
               ALENS(ALTYPE,INT(VARABL(I,3)))=V1
!                       THESE VARIABLES DONE
            END IF
            IF(VTYPE.GE.11.AND.VTYPE.LE.25.OR.VTYPE.EQ.75.OR.VTYPE.GE.124.AND.VTYPE.LE.149) THEN
               IF(VTYPE.EQ.11) ALTYPE=41
               IF(VTYPE.EQ.12) ALTYPE=37
               IF(VTYPE.EQ.13) ALTYPE=38
