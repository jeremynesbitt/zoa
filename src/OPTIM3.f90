   FMT1=0.0D0
   DO I=1,OPCNT
      IF(OPERND(I,19).EQ.0.0D0) FMT1=FMT1+(OPERND(I,14)**2)
   END DO
!     NOW IS THE NEW FMT LESS THAN THE OLD FMT
   IF(FMT1.LE.OROBFMT) THEN
!     WE IMPROVED THE LENS, LETS TRY IT AGAIN
      OROBFMT=FMT1
      GO TO 1
   ELSE
!     RESTORE THE LENS TO THE LAST SET OF VARIABLES
   END IF
   DO I=1,VBCNT
!     GO BACK TO THE LAST VARIABLE VALUES
      VARABL(I,4)=VSAVE(I)
!
!     BOUNDS CHECKER
      IF(VARABL(I,4).LT.VARABL(I,9)) THEN
         VARABL(I,4)=VARABL(I,9)
         WRITE(OUTLYNE,*)'WARNING: '
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'VARIABLE # ',I,' HAS REACHED ITS LOWER BOUNDARY VALUE'
         CALL SHOWIT(1)
