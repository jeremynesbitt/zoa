SUBROUTINE SOLVIT_POWELL(IID,JJD,MDERIV,SILENT)
   USE GLOBALS
!
   use DATCFG
   use DATSUB
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
   CHARACTER OOLDWQ*8
!
   LOGICAL ITDER,SILENT
!
   INTEGER SSN,SM,NP2,MP,N,J,I,L,M,VTYPE,ALTYPE,VADD,VCFG ,VN1,MAXCNT,IV1,VN,ALLOERR,IID,JJD
!
   INTEGER ISURF,IVB,IVBB
!
   REAL*8 NEWDEFVAL,PFACSCL
!
   COMMON/DEFVALCOM/NEWDEFVAL
!
   REAL*8 X(1:100000),WT,V1,MAX,VTEMP,OLDCUR,NEWCUR
!
   DIMENSION WT(:)
!
   ALLOCATABLE :: WT
!
   LOGICAL LVAL,OLDSILENT
!
   CHARACTER AV1*23

   COMMON/SVD1/SM,SSN,MP,NP2
!
   COMMON/SVD2/X
!
   COMMON/CAUX1/V1,AV1
!
   REAL*8 MDERIV
!
   DIMENSION MDERIV(1:IID,1:JJD)
!
   LOGICAL ERR1,ERR2
!
!
   REAL*8 W,V,BTB,BTG,DIAGSUM,WMAX,WMIN
   DIMENSION W(:),V(:,:),BTB(:,:),BTG(:)
   ALLOCATABLE :: W,V,BTB,BTG
   VN=IID
   VN1=IID
   DEALLOCATE(WT,W,V,BTB,BTG,STAT=ALLOERR)
   ALLOCATE (W(VN),V(VN,VN),WT(VN1),BTB(VN1,VN1),BTG(VN1),STAT=ALLOERR)
   W(1:VN)=0.0D0
   WT(1:VN)=0.0D0
   BTG(1:VN)=0.0D0
   V(1:VN,1:VN)=0.0D0
   BTB(1:VN,1:VN)=0.0D0
   OOLDWQ='        '
   ITDER=.FALSE.
!
!       THIS IS SUBROUTINE SOLVIT_POWELL. THIS IS THE SUBROUTINE WHICH
!       HANDLES THE SOLUTION TO THE ITER POWELL PROBLEM
!     FORM (BTB+KC)
!     FIRST JUST BTB
!     THIS IS THE MATRIX PRODUCT OF THE TRANSPOSE OF THE DERIV MATRIX
!     TIMES THE DERIV MATRIX ITSELF
!     L IS THE FIRST INDEX OF BTB AND M IS THE SECOND
!     BTB IS SQUARE WITH DIMENSION VBCNT
!
!     MAKE OUR SHUFFLE LIST OF RANDOMLY CHOSEN VARIABLE NUMBERS
   N=VBCNT
   DEALLOCATE (DARR,STAT=ALLOERR)
   ALLOCATE (DARR(1:N,1:2),STAT=ALLOERR)
   CALL SHUFFLE(N,DARR)
!
   IF(VBCNT.GE.OPCNT) MAXCNT=VBCNT
   IF(VBCNT.LT.OPCNT) MAXCNT=OPCNT
!
!       ZERO OUT TOO SMALL DERIVATIVES
!
   DO I=1,MAXCNT
      DO M=1,MAXCNT
         IF(DABS(MDERIV(I,M)).LT.ONTOL) MDERIV(I,M)=0.0D0
      END DO
   END DO
!
   DO L=1,MAXCNT
      DO M=1,MAXCNT
         DO I=1,MAXCNT
            BTB(L,M)=BTB(L,M)+(MDERIV(I,L)*MDERIV(I,M))
         END DO
      END DO
   END DO
!
!                       WE DO THIS ONCE FOR EACH VARIABLE
   DO IVBB=1,VBCNT
      IVB=INT(DARR(IVBB,2))
!
!     NO DAMPING IS USED IN ITER POWELL

!     NOW FORM BTG
      L=IVB
      DO I=1,OPCNT
         BTG(L)=BTG(L)+(MDERIV(I,L)*(-OPERND(I,14)))
      END DO
!
!     NOW SOLVE THE PROBLEM WITH SVD AND THEN APPLY THE SOLUTION TO
!     THE LENS
!
      MP=MAXCNT
      NP2=MAXCNT
      N=MAXCNT
      M=MAXCNT
!       DO SINGULAR VALUE DECOMPOSITION
      SM=M
      SSN=N
      CALL SVDCMPA(W,V,VN,VN1,BTB,BTG)
      M=SM
      N=SSN
      WMAX=-1.0D300
      DO J=1,N
         IF(W(J).GT.WMAX) WMAX=W(J)
      END DO
      WMIN=WMAX*SINGTOL
      DO J=1,N
         IF(W(J).LT.WMIN) W(J)=0.0D0
      END DO
!
!       SOLVE LINEAR EQUATION
!
      SM=M
      SSN=N
      CALL SVBKSBA(W,V,VN,VN1,BTB,BTG)
      M=SM
      N=SSN
!
!
!     NOW APPLY THE SOLUTION VECTOR X(I) TO THE VARABL ARRAY AND TO THE LENS
!     THE ITH SOLUTION VECTOR COMPONENT APPLIES TO THE ITH VARIABLE
      LCVLCV=0.0D0
      I=IVB
!
!     THE CURRENT VARIABLE VALUE BECOMES THE PREVIOUS VARIABLE VALUE
      VARABL(I,5)=VARABL(I,4)
!
!     THE NEW SOLUTION VECTOR ADDED TO THE PREVIOS VARIABLE VALUE
!     BECOMES THE NEW SOLUTION VECTOR
!     VALUE
      LCVLCV=LCVLCV+DABS(((X(I)))/(DINMUL*VARABL(I,8)))
      VARABL(I,4)=VARABL(I,4)+(X(I))
!
      IF(VARABL(I,1).EQ.1.0D0) THEN
         IF(VARABL(I,9).LT.THMINLIM) VARABL(I,9)=THMINLIM
         IF(VARABL(I,10).GT.THMAXLIM) VARABL(I,10)=THMAXLIM
      END IF
      IF(VARABL(I,1).EQ.2.0D0.OR.VARABL(I,1).EQ.10.0D0) THEN
         IF(RDNEGLIM.EQ.0.0D0) RDNEGLIM=-1.0D-20
         IF(RDPOSLIM.EQ.0.0D0) RDPOSLIM=1.0D-20
         IF(VARABL(I,9).LT.(1.0D0/RDNEGLIM)) VARABL(I,9)=1.0D0/RDNEGLIM
         IF(VARABL(I,10).GT.(1.0D0/RDPOSLIM)) VARABL(I,10)=1.0D0/RDPOSLIM
      END IF
!     BOUNDS CHECKER
      IF(VARABL(I,4).LT.VARABL(I,9)) THEN
         VARABL(I,4)=VARABL(I,9)
         WRITE(OUTLYNE,*)'WARNING: '
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'VARIABLE # ',I,' HAS REACHED ITS LOWER BOUNDARY VALUE'
         CALL SHOWIT(1)
      END IF
      IF(VARABL(I,4).GT.VARABL(I,10)) THEN
         VARABL(I,4)=VARABL(I,10)
         WRITE(OUTLYNE,*)'WARNING: '
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'VARIABLE # ',I,' HAS REACHED ITS UPPER BOUNDARY VALUE'
         CALL SHOWIT(1)
      END IF
!
!
!     THE VARIABLE CHANGE IS (THE NEW CURRENT VALUE-OLD LAST CURRENT VALUE)
      VARABL(I,6)=VARABL(I,4)-VARABL(I,5)
!
!     DINCRS ARE ONLY CHANGED BY HAND AND BY THE ITER ADJUST COMMAND
!
      LCVLCV=(LCVLCV)/DBLE(VBCNT)
!
!     NOW APPLY THE CHANGE VECTOR TO THE LENS
!**********************************************************************
      I=IVB
!     FIRST, IF THE VARIABLE IS A CONFIG 1 VARIABLE
!     NEXT IF THE VARIABLE IS IN A CONFIG OTHER THAN 1
      IF(VARABL(I,2).EQ.1.0D0) THEN
!     NON-CONFIGS VARIABLE MEANING CONFIG 1
!     THIS IS A LENS LEVEL VARIABLE CHANGE
!     GET THE DATA TYPE NUMBER OF THE VARIABLE
         VTYPE=INT(VARABL(I,1))
!                          CURVATURE
         IF(VTYPE.EQ.2.OR.VTYPE.EQ.1) THEN
!     SURFACE CURVATURE
!     NEW VALUE IS:
            V1=VARABL(I,4)
            call set_surf_curvature(INT(VARABL(I,3)), V1)
!                       CURVATURE DONE
         END IF
         IF(VTYPE.EQ.10.OR.VTYPE.EQ.9) THEN
!     SURFACE TORIC CURVATURE
!     NEW VALUE IS:
            V1=VARABL(I,4)
            call set_surf_toric_curvature(INT(VARABL(I,3)), V1)
!                       TORIC CURVATURE DONE
         END IF
         IF(VTYPE.GE.3.AND.VTYPE.LE.8) THEN
            IF(VTYPE.EQ.3) ALTYPE=3
            IF(VTYPE.EQ.4) ALTYPE=2
            IF(VTYPE.EQ.5) ALTYPE=4
            IF(VTYPE.EQ.6) ALTYPE=5
            IF(VTYPE.EQ.7) ALTYPE=6
            IF(VTYPE.EQ.8) ALTYPE=7
!     NEW VALUE IS:
            V1=VARABL(I,4)
            ALENS(ALTYPE,INT(VARABL(I,3)))=V1
!                       THESE VARIABLES DONE
         END IF
         IF(VTYPE.GE.11.AND.VTYPE.LE.25.OR.VTYPE.EQ.75.OR.VTYPE.GE.124.AND.VTYPE.LE.149) THEN
            IF(VTYPE.EQ.11) ALTYPE=41
            IF(VTYPE.EQ.12) ALTYPE=37
            IF(VTYPE.EQ.13) ALTYPE=38
            IF(VTYPE.EQ.14) ALTYPE=39
            IF(VTYPE.EQ.15) ALTYPE=40
            IF(VTYPE.EQ.16) ALTYPE=118
            IF(VTYPE.EQ.17) ALTYPE=119
            IF(VTYPE.EQ.18) ALTYPE=120
            IF(VTYPE.EQ.19) ALTYPE=114
            IF(VTYPE.EQ.20) ALTYPE=115
            IF(VTYPE.EQ.21) ALTYPE=46
            IF(VTYPE.EQ.22) ALTYPE=47
            IF(VTYPE.EQ.23) ALTYPE=48
            IF(VTYPE.EQ.24) ALTYPE=49
            IF(VTYPE.EQ.25) ALTYPE=50
            IF(VTYPE.EQ.75) ALTYPE=43
            IF(VTYPE.EQ.124) ALTYPE=71
            IF(VTYPE.EQ.125) ALTYPE=72
            IF(VTYPE.EQ.126) ALTYPE=73
            IF(VTYPE.EQ.127) ALTYPE=74
            IF(VTYPE.EQ.128) ALTYPE=75
            IF(VTYPE.EQ.129) ALTYPE=81
            IF(VTYPE.EQ.130) ALTYPE=82
            IF(VTYPE.EQ.131) ALTYPE=83
            IF(VTYPE.EQ.132) ALTYPE=84
            IF(VTYPE.EQ.133) ALTYPE=85
            IF(VTYPE.EQ.134) ALTYPE=116
            IF(VTYPE.EQ.135) ALTYPE=86
            IF(VTYPE.EQ.136) ALTYPE=87
            IF(VTYPE.EQ.137) ALTYPE=78
            IF(VTYPE.EQ.138) ALTYPE=79
            IF(VTYPE.EQ.139) ALTYPE=80
            IF(VTYPE.EQ.140) ALTYPE=89
            IF(VTYPE.EQ.141) ALTYPE=11
            IF(VTYPE.EQ.142) ALTYPE=10
            IF(VTYPE.EQ.143) ALTYPE=90
            IF(VTYPE.EQ.144) ALTYPE=91
            IF(VTYPE.EQ.145) ALTYPE=92
            IF(VTYPE.EQ.146) ALTYPE=93
            IF(VTYPE.EQ.147) ALTYPE=94
            IF(VTYPE.EQ.148) ALTYPE=95
            IF(VTYPE.EQ.149) ALTYPE=98
!     NEW VALUE IS:
            V1=VARABL(I,4)
            ALENS(ALTYPE,INT(VARABL(I,3)))=V1
         END IF
         IF(VTYPE.EQ.150) THEN
!     NEW VALUE IS:
            V1=VARABL(I,4)
            GPREG(INT(VARABL(I,3)))=V1
         END IF
         IF(VTYPE.GE.250.AND.VTYPE.LE.4218) THEN
            ALTYPE=VTYPE-249
!     NEW VALUE IS:
            V1=VARABL(I,4)
!     RESET THE APPRORIATE ARRAY VALUE IN THE DEFORMABLE SURFACE
            ISURF=INT(VARABL(I,3))
            DEFGR1=surf_default_flag(ISURF)
            DEFGR2=surf_mtracei_nx(ISURF)
            DEFGR3=surf_mtracei_ny(ISURF)
            DEFGR4=surf_psfbin_data(ISURF)
            DEFGR5=surf_profit_data(ISURF)
            DEFGR6=0.0D0
            DEFGR7=ALENS(109,ISURF)
            DEFGR8=0.0D0
            ACTNUM=ALTYPE
            NEWDEFVAL=V1
            ERR1=.FALSE.
            ERR2=.FALSE.
            CALL DEFGRIDS(6,ISURF,ERR1,ERR2)
         END IF
         IF(VTYPE.GE.27.AND.VTYPE.LE.74) THEN
!     SPECIAL SURFACE COEFFICIENTS
!     NEW VALUE IS:
            V1=VARABL(I,4)
            FTFL01((VTYPE-26),INT(VARABL(I,3)))=V1
!                       SPECIAL SURFACE COEFICIENTS DONE
         END IF
         IF(VTYPE.GE.76.AND.VTYPE.LE.123) THEN
!     SPECIAL SURFACE COEFFICIENTS
!     NEW VALUE IS:
            V1=VARABL(I,4)
            FTFL01((VTYPE-27),INT(VARABL(I,3)))=V1
!                       SPECIAL SURFACE COEFICIENTS DONE
         END IF
!
      ELSE
!     CONFIGS VARIABLE
!     VCFG IS THE CONFIG NUMBER
         VCFG=INT(VARABL(I,2))
!     VTYPE IS THE VARIABLE TYPE NUMBER AS USED IN THE VARIABLES ARRAYS
         VTYPE=INT(VARABL(I,1))
!
!     FOR VARIABLE I, APPLY THE SPECIFIED CHANGE TO THE SPECIFIED
!     CONFIG
!
!     THE NEW VARAIBLE VALUE IS JUST
         V1=VARABL(I,4)
!     CONVERT THIS VALUE TO A CHARACTER*23 CHARACTER VARIABLE
         VADD=INT(VARABL(I,14))
         IF(CFADD(VADD,1).EQ.1.AND.V1.NE.0.0D0) V1=1.0D0/V1
         IF(CFADD(VADD,1).EQ.9.AND.V1.NE.0.0D0) V1=1.0D0/V1
         CALL AUXNTA
!
!     THE POSITION IN THE CFADD,CFVAL AND CFCHAR ARRAYS WHERE THIS
!     VARIABLE IS FOUND IS:
!
         IF(CFADD(VADD,1).GE.27.AND.CFADD(VADD,1).LE.74.OR.CFADD(VADD,1).GE.76.AND.CFADD(VADD,1).LE.123 .OR.CFADD(VADD,1).EQ.141) THEN
            CFVAL(VADD,2)=V1
            CFCHAR(VADD,2)=AV1
!     NOW UPDATE THE CONFIG ARRAY
            CONFG(CFADD(VADD,3),CFADD(VADD,9))(CFADD(VADD,6):CFADD(VADD,7))=AV1(1:23)
         ELSE
            CFVAL(VADD,1)=V1
            CFCHAR(VADD,1)=AV1
!     NOW UPDATE THE CONFIG ARRAY
            CONFG(CFADD(VADD,3),CFADD(VADD,9))(CFADD(VADD,4):CFADD(VADD,5))=AV1(1:23)
         END IF
!     NOW LOOK UP WHERE THIS CHARACTER REPRESENTATION OF THE NEW VALUE
!     SHOULD BE STUFFED INTO THE CONFIG ARRAYS CONFG AND
!     STUFF IT THERE.
!
!     NOW LOOP BACK AND REPEAT FOR THE NEXT VARIABLE.
!     FINISHED WITH A CONFIG VARIABLE
      END IF
!     LOOP TO NEXT VARIABL
!     WRITE(OUTLYNE,*)'VARIABLE ',IVB
!     CALL SHOWIT(1)
   END DO
!     UPDATE THE LENS OR THE PERMANENT LENS WILL BE ******!
   CALL FIXDEFORMFILE
   F6=1
   F1=0
   F22=0
   LNSTYP=2
   CALL LNSEOS
!**********************************************************************
!     AND RE-EVALUATE THE OPERANDS AND DISPLAY
!     THE NEW FIGURE OF MERIT, THE OLD FIGURE OF MERIT  AND ITS CHANGE
   SAVE_KDP(4)=SAVEINPT(4)
   F28=1
   MSG=.FALSE.
   OPTMES=.FALSE.
   WC='FMT'
   WQ='        '
   SQ=0
   SN=0
   SST=0
   WS=' '
   DF1=1
   DF2=1
   DF3=1
   DF4=1
   DF5=1
   STI=0
   SN=0
   S1=0
   S2=0
   S3=0
   S4=0
   S5=0
   W1=0.0D0
   CALL FMT3
   SOLEXT=.TRUE.
   REST_KDP(4)=RESTINPT(4)
   DEALLOCATE(WT,W,V,BTB,BTG,STAT=ALLOERR)
   RETURN
