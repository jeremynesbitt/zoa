            IF(VTYPE.EQ.144) ALTYPE=91
            IF(VTYPE.EQ.145) ALTYPE=92
            IF(VTYPE.EQ.146) ALTYPE=93
            IF(VTYPE.EQ.147) ALTYPE=94
            IF(VTYPE.EQ.148) ALTYPE=95
            IF(VTYPE.EQ.149) ALTYPE=98
!     NEW VALUE IS:
            V1=VARABL(I,4)
            ALENS(ALTYPE,INT(VARABL(I,3)))=V1
