SUBROUTINE SPLINT(XA,YA,Y2A,N,X,Y)
!     THIS IS A MODIFIED VERSION FROM NUMERICAL RECIPIES
   use DATMAI
   IMPLICIT NONE
   REAL*8 XA,YA,Y2A,X,Y,H,A,B
   INTEGER KLO,KHI,N,K
   DIMENSION XA(N),YA(N),Y2A(N)
   KLO=1
   KHI=N
1  IF (KHI-KLO.GT.1) THEN
      K=(KHI+KLO)/2
      IF(XA(K).GT.X)THEN
         KHI=K
      ELSE
         KLO=K
      ENDIF
      GOTO 1
   ENDIF
   H=XA(KHI)-XA(KLO)
   IF (H.EQ.0.0D0) THEN
      OUTLYNE= 'SERIOUS ERROR IN CALL TO SPLINE INTERPOLATION'
      CALL SHOWIT(1)
      OUTLYNE= 'REPORT THIS IMMEDIATELY'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   A=(XA(KHI)-X)/H
   B=(X-XA(KLO))/H
   Y=A*YA(KLO)+B*YA(KHI)+((A**3-A)*Y2A(KLO)+(B**3-B)*Y2A(KHI))*(H**2)/6.0D0
   RETURN
END
SUBROUTINE SPLINE(X,Y,N,YP1,YPN,Y2)
!     THIS IS A MODIFIED VERSION FROM NUMERICAL RECIPIES
   IMPLICIT NONE
   REAL*8 X,Y,Y2,U,SIG,P,YPN,QN,UN,YP1
   INTEGER NMAX,I,N,K
   PARAMETER (NMAX=250)
   DIMENSION X(N),Y(N),Y2(N),U(NMAX)
   PRINT *, "N is ", N
   PRINT *, "Y(N)) is ", Y(N)
   IF (YP1.GT..99D30) THEN
      Y2(1)=0.0D0
      U(1)=0.0D0
   ELSE
      Y2(1)=-0.5D0
      U(1)=(3.0D0/(X(2)-X(1)))*((Y(2)-Y(1))/(X(2)-X(1))-YP1)
   ENDIF
   DO 11 I=2,N-1
      SIG=(X(I)-X(I-1))/(X(I+1)-X(I-1))
      P=SIG*Y2(I-1)+2.0D0
      Y2(I)=(SIG-1.0D0)/P
      U(I)=(6.0D0*((Y(I+1)-Y(I))/(X(I+1)-X(I))-(Y(I)-Y(I-1))/(X(I)-X(I-1)))/(X(I+1)-X(I-1))-SIG*U(I-1))/P
11 CONTINUE
   IF (YPN.GT..99D30) THEN
      QN=0.0D0
      UN=0.0D0
   ELSE
      QN=0.5D0
      UN=(3.0D0/(X(N)-X(N-1)))*(YPN-(Y(N)-Y(N-1))/(X(N)-X(N-1)))
   ENDIF
   Y2(N)=(UN-QN*U(N-1))/(QN*Y2(N-1)+1.0D0)
   DO 12 K=N-1,1,-1
      Y2(K)=Y2(K)*Y2(K+1)+U(K)
12 CONTINUE
   RETURN
END
! SUB ROBB.FOR
SUBROUTINE ROBB
!
   use DATCFG
   use DATSUB
   use DATLEN
   use mod_surface
   use DATMAI
   IMPLICIT NONE
!
   INTEGER IROBB,RMAX,I,J,VTYPE,K,ALTYPE,VADD,VCFG,KMIN
!
   CHARACTER AV1*23
!
   INTEGER VCN,ALLOERR
!
   REAL*8 ROBBB,ROBDD,OLDX,VSAVE,OROBFMT ,V1,FMT1,DELOLDX
!
   INTEGER ISURF
!
   REAL*8 NEWDEFVAL
!
   COMMON/DEFVALCOM/NEWDEFVAL
!
   DIMENSION OLDX(:),DELOLDX(:),VSAVE(:)
!
   ALLOCATABLE :: OLDX,DELOLDX,VSAVE
!
   COMMON/CAUX1/V1,AV1
!
   LOGICAL ERR1,ERR2
!
   VCN=VBCNT
   DEALLOCATE(OLDX,DELOLDX,VSAVE,STAT=ALLOERR)
   ALLOCATE(OLDX(VCN),DELOLDX(VCN),VSAVE(VCN),STAT=ALLOERR)
!
!       THIS IS SUBROUTINE ROBB.FOR THIS IS THE SUBROUTINE WHICH
!       HANDLES CMD LEVEL COMMANDS "ROBB"
!
!       "ROBB" EITHER TAKES OPTINAL NW1 AND NW2
   IF(STI.EQ.1) THEN
      WRITE(OUTLYNE,*)'"ROBB" PERFORMS CONVERGENCE ACCELERATION'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      DEALLOCATE(OLDX,DELOLDX,VSAVE,STAT=ALLOERR)
      RETURN
   END IF
   IF(SQ.EQ.1.OR.SST.EQ.1) THEN
      WRITE(OUTLYNE,*)'"ROBB" TAKES NO QUALIFIER OR STRING INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      DEALLOCATE(OLDX,DELOLDX,VSAVE,STAT=ALLOERR)
      RETURN
   END IF
   IF(S4.EQ.1.OR.S5.EQ.1) THEN
      WRITE(OUTLYNE,*)'"ROBB" TAKES NO NUMERIC WORD #4 OR #5 INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      DEALLOCATE(OLDX,DELOLDX,VSAVE,STAT=ALLOERR)
      RETURN
   END IF
   IF(DF3.EQ.0.AND.W3.LT.5.0D0) THEN
      WRITE(OUTLYNE,*)'"ROBB" REQUIRES "rmax" TO BE GREATER THAN OR EQUAL TO 5'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      DEALLOCATE(OLDX,DELOLDX,VSAVE,STAT=ALLOERR)
      RETURN
   END IF
   IF(DF3.EQ.1) W3=100.0D0
   RMAX=INT(W3)
!
   IF(KILOPT) THEN
      OUTLYNE='SOME OPERANDS ARE NOT CALCULABLE'
      CALL SHOWIT(1)
      OUTLYNE='NO ACCELERATION IS POSSIBLE'
      CALL SHOWIT(1)
      DEALLOCATE(OLDX,DELOLDX,VSAVE,STAT=ALLOERR)
      RETURN
   END IF
!
   IF(DF1.EQ.1) W1=1.0D0
   IF(DF2.EQ.1) W2=1.1D0
   ROBBB=W1
   ROBDD=W2
!
!       PROCEED WITH ACTION FOR COMMAND
   IF(.NOT.SOLEXT) THEN
      WRITE(OUTLYNE,*)'NO SOLUTION VECTOR EXISTS'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'NO ACTION TAKEN'
      CALL SHOWIT(1)
      CALL MACFAL
      DEALLOCATE(OLDX,DELOLDX,VSAVE,STAT=ALLOERR)
      RETURN
   END IF
!       PROCEED WITH ACTION FOR COMMAND
   IF(VBCNT.EQ.0) THEN
      WRITE(OUTLYNE,*)'THE VARIABLE SUBFILE IS EMPTY'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'NO VARIABLE DATA VALUES EXISTS TO RESTORE'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'NO ACTION TAKEN'
      CALL SHOWIT(1)
      CALL MACFAL
      DEALLOCATE(OLDX,DELOLDX,VSAVE,STAT=ALLOERR)
      RETURN
   END IF
   IF(OPCNT.EQ.0) THEN
      WRITE(OUTLYNE,*)'THE MERIT SUBFILE IS EMPTY'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'NO OPERAND DATA VALUES EXISTS'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'NO ACTION TAKEN'
      CALL SHOWIT(1)
      CALL MACFAL
      DEALLOCATE(OLDX,DELOLDX,VSAVE,STAT=ALLOERR)
      RETURN
   END IF

!     THERE IS A SOLUTION TO ACCELERATE, DO IT
!
!     SAVE THE CURRENT FMT
   OROBFMT=FMTFMT
!
!     CALCULATE AND STORE THE LAST OLD SOLUTION VECTOR
   DO I=1,VBCNT
      OLDX(I)=VARABL(I,4)-VARABL(I,5)
   END DO
!     NOW START SCALING AND APPLYING THE CHANGE TO THE SOLUTION VECTOR
!     TO THE CURRENT LENS WHICH ALREADY HAS THE EXISTING SOLUTION
!     VECTOR IN IT.
   IROBB=0

30 FORMAT('  I  SCALE FACTOR   CURRENT FMT')
   WRITE(OUTLYNE,30)
   CALL SHOWIT(0)
1  CONTINUE
   IROBB=IROBB+1
   IF(IROBB.GT.RMAX) THEN
      WRITE(OUTLYNE,98) RMAX
      CALL SHOWIT(1)
98    FORMAT('MAXIMUM NUMBER OF "ROBB" CYCLES, ',I5,' ,HAS BEEN REACHED')
      RETURN
   END IF
   ROBBB=ROBBB*ROBDD
   DO I=1,VBCNT
      DELOLDX(I)=(ROBBB*OLDX(I))-OLDX(I)
   END DO
   DO I=1,VBCNT
!     REMEMBER THE CURRENT VARIABLE VALUE BEFORE SCALING
      VSAVE(I)=VARABL(I,4)
!     CALCULATE THE NEW VARIABLE VALUE AFTER SCALING
      VARABL(I,4)=VSAVE(I)+DELOLDX(I)
!
!     BOUNDS CHECKER
      IF(VARABL(I,4).LT.VARABL(I,9)) THEN
         VARABL(I,4)=VARABL(I,9)
         WRITE(OUTLYNE,*)'WARNING: '
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'VARIABLE # ',I,' HAS REACHED ITS LOWER BOUNDARY VALUE'
         CALL SHOWIT(1)
      END IF
      IF(VARABL(I,4).GT.VARABL(I,10)) THEN
         VARABL(I,4)=VARABL(I,10)
         WRITE(OUTLYNE,*)'WARNING: '
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'VARIABLE # ',I,' HAS REACHED ITS UPPER BOUNDARY VALUE'
         CALL SHOWIT(1)
      END IF
      VARABL(I,6)=VARABL(I,4)-VARABL(I,5)
!
   END DO
!
!     CHANGE THE LENS
!**********************************************************************
!     NOW APPLY THE CHANGE VECTOR TO THE LENS
   DO I=1,VBCNT
!     FIRST, IF THE VARIABLE IS A CONFIG 1 VARIABLE
!     NEXT IF THE VARIABLE IS IN A CONFIG OTHER THAN 1
      IF(VARABL(I,2).EQ.1.0D0) THEN
!     NON-CONFIGS VARIABLE MEANING CONFIG 1
!     THIS IS A LENS LEVEL VARIABLE CHANGE
!     GET THE DATA TYPE NUMBER OF THE VARIABLE
         VTYPE=INT(VARABL(I,1))
!                          CURVATURE
         IF(VTYPE.EQ.2.OR.VTYPE.EQ.1) THEN
!     SURFACE CURVATURE
!     NEW VALUE IS:
            V1=VARABL(I,4)
            call set_surf_curvature(INT(VARABL(I,3)), V1)
!                       CURVATURE DONE
         END IF
         IF(VTYPE.EQ.10.OR.VTYPE.EQ.9) THEN
!     SURFACE TORIC CURVATURE
!     NEW VALUE IS:
            V1=VARABL(I,4)
            call set_surf_toric_curvature(INT(VARABL(I,3)), V1)
!                       TORIC CURVATURE DONE
         END IF
         IF(VTYPE.GE.3.AND.VTYPE.LE.8) THEN
            IF(VTYPE.EQ.3) ALTYPE=3
            IF(VTYPE.EQ.4) ALTYPE=2
            IF(VTYPE.EQ.5) ALTYPE=4
