         SCFAX=DABS(MAXI-MINI)/6.0D0
         !SCFAY=DABS(MAXI-MINI)/10D0
         !SCFAX=DABS(MAXI-MINI)/10D0
         PSIZY=1.0D0/SCFAY
         PSIZX=1.0D0/SCFAX
         IF(SYSTEM(6).EQ.1.0D0) SCFAYP=SCFAY
         IF(SYSTEM(6).EQ.1.0D0) SCFAXP=SCFAX
         IF(SYSTEM(6).EQ.2.0D0) SCFAYP=SCFAY/2.54D0
         IF(SYSTEM(6).EQ.2.0D0) SCFAXP=SCFAX/2.54D0
         IF(SYSTEM(6).EQ.3.0D0) SCFAYP=SCFAY/25.4D0
         IF(SYSTEM(6).EQ.3.0D0) SCFAXP=SCFAX/25.4D0
         IF(SYSTEM(6).EQ.4.0D0) SCFAYP=SCFAY/0.0254D0
         IF(SYSTEM(6).EQ.4.0D0) SCFAXP=SCFAX/0.0254D0
         PSIZYP=1.0D0/SCFAYP
         PSIZXP=1.0D0/SCFAXP
         IF(SCFAY.EQ.0.0D0.OR.SCFAX.EQ.0.0D0) THEN
            OUTLYNE=&
            &'ERROR: AUTOMATIC SCALE FACTOR IS ZERO'
            CALL SHOWIT(1)
            OUTLYNE=&
            &'EXPLICITLY ISSUE A "PLOT SCALE" OR "PLOT SIZE" COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
!     SET FLAGS TO LOCK IN THESE VALUES
!     DON'T DO ANOTHER PLOT SCALE
         AUTSL=.FALSE.
!     AUTOSCALE HAS BEEN CALCULATED
         ASCFLG=.TRUE.
      ELSE
!     AUTO SCALE FACTOR HAS ALREADY BEEN CALCULATED, KEEP GOING
      END IF
   ELSE
!     A NON-AUTO SCALE OR SIZE FACTOR EXISTS, USE IT
      IF(RCL.EQ.1.OR.RCL.EQ.2.OR.RCL.EQ.3) THEN
!     DO THIS TO CALCULATE RANGE FOR PLOT JUSTIFICATION
         DO I=STASUR,STPSUR
            X=VERTEX(1,I)
            Y=VERTEX(2,I)
            Z=VERTEX(3,I)
            X=X-XROT
            Y=Y-YROT
            Z=Z-ZROT
            XN=ROT1X(X,Z,VIEPH)
            YN=Y
            ZN=ROT1Z(X,Z,VIEPH)
            X=XN
            Y=YN
            Z=ZN
!
            ZN=ROT2Z(Z,Y,VIEAL)
            YN=ROT2Y(Z,Y,VIEAL)
            XN=X
            X=XN
            Y=YN
            Z=ZN
            IF(I.EQ.STASUR) THEN
               YMAXI=Y
               YMINI=Y
               XMAXI=X
               XMINI=X
            ELSE
!     NOT FIRST CYCLE, DO SOME TESTING
               IF(Y.GT.YMAXI) YMAXI=Y
               IF(Y.LT.YMINI) YMINI=Y
               IF(X.GT.XMAXI) XMAXI=X
               IF(X.LT.XMINI) XMINI=X
!                        DONE WITH THE TESTING
!
            END IF
         END DO
      ELSE
!     DON'T NEED TO CALCULATE VALUES FOR RANGE
      END IF
   END IF
!
   RETURN
END
