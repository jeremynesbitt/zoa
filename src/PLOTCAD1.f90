                  YMINI=Y
                  XMAXI=X
                  XMINI=X
               ELSE
!     NOT FIRST CYCLE, DO SOME TESTING
                  IF(Y.GT.YMAXI) YMAXI=Y
                  IF(Y.LT.YMINI) YMINI=Y
