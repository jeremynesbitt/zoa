SUBROUTINE PLT_SAGFILE
!
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
!       THIS PROGRAM CONTROLS THE PLOT SAGFILE COMMAND
!
   INTEGER COLPAS
!
   INTEGER I,ALLOERR,N,NNN,ISURF,NSTAR2
!
   REAL*8 SAGARRAY1,SAGARRAY2,SAGARRAY3
!
   REAL*8 XMAX,XMIN,ZMAX,ZMIN,X,Y,SAG
!
   DIMENSION SAGARRAY1(:),SAGARRAY2(:),SAGARRAY3(:)
!
   ALLOCATABLE :: SAGARRAY1,SAGARRAY2,SAGARRAY3
!
   LOGICAL EXIS90,OPEN90
!
   LOGICAL ROTSAGFL
!
   COMMON/SAGFLROT/ROTSAGFL
!
!
   DEALLOCATE(SAGARRAY1,SAGARRAY2,SAGARRAY3,STAT=ALLOERR)
!
   IF(STI.EQ.1) THEN
      OUTLYNE= '"PLOT SAGFILE" PLOTS THE EXISTING SAG.DAT FILE'
      CALL SHOWIT(1)
      OUTLYNE= 'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(SST.EQ.1) THEN
      OUTLYNE= '"PLOT SAGFILE" TAKES NO STRING INPUT'
      CALL SHOWIT(1)
      OUTLYNE= 'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
      OUTLYNE= '"PLOT SAGFILE" ONLY TAKES NUMERIC WORD #1'
      CALL SHOWIT(1)
      OUTLYNE= 'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(DF1.EQ.1) W1=0.0D0
!
!     OPEN AND LOAD THE APPROPRIATE SAG DATA AND THEN PLOT IT
   EXIS90=.FALSE.
   INQUIRE(FILE='SAG.DAT',EXIST=EXIS90)
   IF(.NOT.EXIS90) THEN
      OUTLYNE=&
      &'NO SAG.DAT FILE EXISTS TO PLOT'
      CALL SHOWIT(1)
      OUTLYNE='NO ACTION TAKEN'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
!     FILE EXISTS
   CALL CLOSE_FILE(90,1)
   OPEN(UNIT=90,FILE='SAG.DAT',STATUS='UNKNOWN')
   READ(90,END=777,FMT=*,ERR=778) ISURF,NSTAR2
   DO I=1,99999
      READ(90,END=777,FMT=*,ERR=778) X,Y,SAG
!     N IS THE NUMBER OF GOOD DATA POINTS
      N=I
   END DO
   GO TO 777
778 CONTINUE
   OUTLYNE=&
   &'SAG.DAT FILE CONTAINS ERRORS AND CAN NOT BE PLOTTED'
   CALL SHOWIT(1)
   OUTLYNE='NO ACTION TAKEN'
   CALL SHOWIT(1)
   CALL MACFAL
   RETURN
777 CONTINUE
   REWIND (UNIT=90)
   DEALLOCATE(SAGARRAY1,SAGARRAY2,SAGARRAY3 &
   &,STAT=ALLOERR)
   ALLOCATE(SAGARRAY1(1:N),SAGARRAY2(1:N),SAGARRAY3(1:N)&
   &,STAT=ALLOERR)
   XMAX=-1.0D30
   XMIN=1.0D30
   READ(90,*) ISURF
   DO I=1,N
      READ(90,*) SAGARRAY1(I),SAGARRAY2(I),SAGARRAY3(I)

      IF(SAGARRAY1(I).GT.XMAX) XMAX=SAGARRAY1(I)
      IF(SAGARRAY2(I).GT.XMAX) XMAX=SAGARRAY2(I)
      IF(SAGARRAY1(I).LT.XMIN) XMIN=SAGARRAY1(I)
      IF(SAGARRAY2(I).LT.XMIN) XMIN=SAGARRAY2(I)
   END DO

   ZMAX=-1.0D30
   ZMIN=1.0D30
   DO I=1,N
      IF(SAGARRAY3(I).GT.ZMAX) ZMAX=SAGARRAY3(I)
      IF(SAGARRAY3(I).LT.ZMIN) ZMIN=SAGARRAY3(I)
   END DO
   DO I=1,N
      SAGARRAY3(I)=SAGARRAY3(I)-ZMIN
   END DO
!
!     NOW ARRAYS ARE LOADED AND RANGES ARE COMPUTED, CONTINUE
!
   NNN=INT(DSQRT(DBLE(N)))
!
   CALL SAGPLOT(SAGARRAY1,SAGARRAY2,&
   &SAGARRAY3,XMIN,XMAX,ZMIN,ZMAX,NNN,N,ISURF)
   RETURN
END
! SUB SAGPLOT.FOR
SUBROUTINE SAGPLOT(SAGARRAY1,SAGARRAY2,SAGARRAY3,XMIN &
&,XMAX,ZMIN,ZMAX,NNN,N,ISURF)
!
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
   LOGICAL ITSFLAT
!
   INTEGER I,N,NNN,II,J,JJ,ISURF
   INTEGER ALLOERR
!
   REAL*8 SAGARRAY1,SAGARRAY2,SAGARRAY3,XMIN,XMAX &
   &,ZMIN,ZMAX,XNORM,ZNORM
!
   REAL XPLT,YPLT,FPLT
!
   DIMENSION FPLT(:,:),XPLT(:),YPLT(:)
   DIMENSION SAGARRAY1(1:N),SAGARRAY2(1:N),SAGARRAY3(1:N)
!
   ALLOCATABLE :: FPLT,YPLT,XPLT
!
   DEALLOCATE(XPLT,YPLT,FPLT,STAT=ALLOERR)
   ALLOCATE(XPLT(NNN),YPLT(NNN),FPLT(NNN,NNN),&
   &STAT=ALLOERR)
   FPLT(1:NNN,1:NNN)=0.0
   XNORM=0.5D0
   IF((XMAX-XMIN).NE.0.0D0) XNORM=(XMAX-XMIN)/2.0D0
   ZNORM=1.0D0
   IF((ZMAX-ZMIN).NE.0.0D0) ZNORM=ZMAX-ZMIN
   NNN=INT(DSQRT(DBLE(N)))
   DO I=1,NNN
      XPLT(I)=SNGL(SAGARRAY1(I)/(XNORM))
   END DO
   II=1
   JJ=1
   DO I=1,NNN
      YPLT(II)=SNGL(SAGARRAY2(JJ)/(XNORM))
      II=II+1
      JJ=JJ+NNN
   END DO
   II=1
   DO J=1,NNN
      DO I=1,NNN
         FPLT(I,J)=SNGL(SAGARRAY3(II)/(ZNORM))
         II=II+1
      END DO
   END DO
!
   CALL FFT4(NNN,XPLT,YPLT,FPLT,XMIN,XMAX,ZMIN,ZMAX,ISURF)
   DEALLOCATE(XPLT,YPLT,FPLT,STAT=ALLOERR)
   RETURN
END
SUBROUTINE FFT4(NNN,XPLT,YPLT,FPLT,XMIN,XMAX,ZMIN,ZMAX,ISURF)
   use DATHGR
   use DATLEN
   use DATMAI
   IMPLICIT NONE
   REAL SPACER
   INTEGER ROT,I,J,ALLOERR,NNN,ISURF
!
   REAL*8 XMIN,XMAX,ZMIN,ZMAX
!
   REAL FPLT,XPLT,YPLT,FTF1
!
   LOGICAL ROTSAGFL
!
   COMMON/SAGFLROT/ROTSAGFL
!
   DIMENSION FTF1(:,:),FPLT(NNN,NNN),XPLT(NNN),YPLT(NNN)
   ALLOCATABLE :: FTF1
!
   DEALLOCATE(FTF1,STAT=ALLOERR)
   ALLOCATE(FTF1(NNN,NNN),STAT=ALLOERR)
   ROT=0
   IF(ROTSAGFL) ROT=90
!
   DO I=1,NNN
      DO J=1,NNN
         FTF1(I,J)=FPLT(NNN+1-I,J)
      END DO
   END DO
   DO J=1,NNN
      DO I=1,NNN
         FPLT(I,J)=FTF1(I,J)
      END DO
   END DO
!     WE NEED TO REFLECT IN THE Y-DIRECTION
   DO I=1,NNN
      DO J=1,NNN
         FTF1(I,J)=FPLT(I,NNN+1-J)
      END DO
   END DO
   DO J=1,NNN
      DO I=1,NNN
         FPLT(I,J)=FTF1(I,J)
      END DO
   END DO
   IF(ROT.EQ.90) THEN
      DO J=1,NNN
         DO I=1,NNN
            FTF1(I,J)=FPLT(NNN+1-J,I)
         END DO
      END DO
      DO J=1,NNN
         DO I=1,NNN
            FPLT(I,J)=FTF1(I,J)
         END DO
      END DO
   END IF
   DO I=1,NNN
      DO J=1,NNN
         FPLT(I,J)=(FPLT(I,J))
      END DO
   END DO
   DO I=1,NNN
      DO J=1,NNN
         FPLT(I,J)=FPLT(I,J)
      END DO
   END DO
   DO I=1,NNN
      XPLT(I)=((XPLT(I))*1500.0)+1500.0
      YPLT(I)=((YPLT(I))*1500.0)+1500.0
   END DO
   DO I=1,NNN
      DO J=1,NNN
         FPLT(I,J)=(FPLT(I,J))*2000.0
      END DO
   END DO
!
!     NOW FUNCTION GOES FROM 0 TO 2000 AND XPLT AND YPLT GO FROM
!     0 TO +2000 EACH
!
!     THE PLOT WILL BE SEEN IN ORTHOGRAPHIC PROJECTION, XPLT ACROSS THE SCREEN,
!     YPLT INTO THE SCREEN AT 45 DEG EL AND AZ AND FPLT UP ON THE SCREEN
!
   CALL PLOTSAGFL(NNN,XPLT,YPLT,FPLT,FTF1,ROT &
   &,XMIN,XMAX,ZMIN,ZMAX,ISURF)
!
   DEALLOCATE(FTF1,STAT=ALLOERR)
   RETURN
END
