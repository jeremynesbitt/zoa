!       FIRST FILE OF PLOT/CAD ROUTINES

! SUB PPLOTT.FOR
SUBROUTINE PPLOTT
   USE NSSMOD
!
   use DATHGR
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
!       THIS PROGRAM CONTROLS THE ALL PLOTTING PROCEDURES.
!       ALL THE PLOTTING COMMAND ARE EXECUTED FROM HERE.
!

   LOGICAL PLQ,FANEXT,EXIS94,OPEN94,EXIS92,PLC,EXIS103,OPEN103
!
   INTEGER CACOCHVIE
!
   REAL UPPER,LOWER
!
   REAL*8 SFI,MDX,MDY,GAMGAM,V1,V2
!
   CHARACTER NAME*40
!
   INTEGER COLPAS,N,I,J
!
   COMMON/FANEXI/FANEXT
!
!
   IF(WC.EQ.'PLOT') THEN
      PLQ=.FALSE.
      IF(WC.EQ.'PNOTE')       PLQ=.TRUE.
      IF(WQ.EQ.'UXAXIS')      PLQ=.TRUE.
      IF(WQ.EQ.'UYAXIS')      PLQ=.TRUE.
      IF(WQ.EQ.'UXAXRNG')     PLQ=.TRUE.
      IF(WQ.EQ.'UYAXRNG')     PLQ=.TRUE.
      IF(WQ.EQ.'UXAXISLB')    PLQ=.TRUE.
      IF(WQ.EQ.'UYAXISLB')    PLQ=.TRUE.
      IF(WQ.EQ.'UPLOT')       PLQ=.TRUE.
      IF(WQ.EQ.'UYLINE')      PLQ=.TRUE.
      IF(WQ.EQ.'UXLINE')      PLQ=.TRUE.
      IF(WQ.EQ.'END')         PLQ=.TRUE.
      IF(WQ.EQ.'LI')          PLQ=.TRUE.
      IF(WQ.EQ.'LBL')         PLQ=.TRUE.
      IF(WQ.EQ.'NOLBL')       PLQ=.TRUE.
      IF(WQ.EQ.'FIGURE')      PLQ=.TRUE.
      IF(WQ.EQ.'NOLI')        PLQ=.TRUE.
      IF(WQ.EQ.'AXIS')        PLQ=.TRUE.
      IF(WQ.EQ.'NSSSCALE')    PLQ=.TRUE.
      IF(WQ.EQ.'SCALE')       PLQ=.TRUE.
      IF(WQ.EQ.'NOSCALE')     PLQ=.TRUE.
      IF(WQ.EQ.'YESCALE')     PLQ=.TRUE.
      IF(WQ.EQ.'SIZE')        PLQ=.TRUE.
      IF(WQ.EQ.'NOSIZE')      PLQ=.TRUE.
      IF(WQ.EQ.'YESIZE')      PLQ=.TRUE.
      IF(WQ.EQ.'ORIGIN')      PLQ=.TRUE.
      IF(WQ.EQ.'LSTYLE')      PLQ=.TRUE.
      IF(WQ.EQ.'SYMBOL')      PLQ=.TRUE.
      IF(WQ.EQ.'NEW')         PLQ=.TRUE.
      IF(WQ.EQ.'NAME')        PLQ=.TRUE.
      IF(WQ.EQ.'PEN')         PLQ=.TRUE.
      IF(WC.EQ.'LISTDRAW')    PLQ=.TRUE.
      IF(WC.EQ.'DRAWFAN')     PLQ=.TRUE.
      IF(WC.EQ.'PLT_FAN')     PLQ=.TRUE.
      IF(WC.EQ.'GRAOUT')      PLQ=.TRUE.
      IF(WQ.EQ.'LOOK')        PLQ=.TRUE.
      IF(WQ.EQ.'YESLOOK')     PLQ=.TRUE.
      IF(WQ.EQ.'NOLOOK')      PLQ=.TRUE.
      IF(WQ.EQ.'VIEW')        PLQ=.TRUE.
      IF(WQ.EQ.'YESVIEW')     PLQ=.TRUE.
      IF(WQ.EQ.'NOVIEW')      PLQ=.TRUE.
      IF(WQ.EQ.'DASH')        PLQ=.TRUE.
      IF(WQ.EQ.'NODASH')      PLQ=.TRUE.
      IF(WQ.EQ.'CHNOTE')      PLQ=.TRUE.
      IF(WQ.EQ.'NOTE')        PLQ=.TRUE.
      IF(WQ.EQ.'ACC')         PLQ=.TRUE.
      IF(WQ.EQ.'RAY')         PLQ=.TRUE.
      IF(WQ.EQ.'RAYS')        PLQ=.TRUE.
      IF(WQ.EQ.'RAYSCO')      PLQ=.TRUE.
      IF(WQ.EQ.'PROF')        PLQ=.TRUE.
      IF(WQ.EQ.'PROFX')       PLQ=.TRUE.
      IF(WQ.EQ.'PROFY')       PLQ=.TRUE.
      IF(WQ.EQ.'EDGEX')       PLQ=.TRUE.
      IF(WQ.EQ.'EDGEY')       PLQ=.TRUE.
      IF(WQ.EQ.'CLAP')        PLQ=.TRUE.
      IF(WQ.EQ.'COBS')        PLQ=.TRUE.
      IF(WQ.EQ.'MIRROR')      PLQ=.TRUE.
      IF(WQ.EQ.'LEFT')        PLQ=.TRUE.
      IF(WQ.EQ.'RIGHT')       PLQ=.TRUE.
      IF(WQ.EQ.'CENTER')      PLQ=.TRUE.
      IF(WQ.EQ.'GAMMA')       PLQ=.TRUE.
      IF(WQ.EQ.'XSHIFT')      PLQ=.TRUE.
      IF(WQ.EQ.'YSHIFT')      PLQ=.TRUE.
      IF(WC.EQ.'PLTXFAN')     PLQ=.TRUE.
      IF(WC.EQ.'PLTYFAN')     PLQ=.TRUE.
      IF(WC.EQ.'PLTNFAN')     PLQ=.TRUE.
      IF(WC.EQ.'PLTPFAN')     PLQ=.TRUE.
      IF(WC.EQ.'PLTXYFAN')    PLQ=.TRUE.
      IF(WC.EQ.'PLTYXFAN')    PLQ=.TRUE.
      IF(WC.EQ.'PLOTFANS')    PLQ=.TRUE.
      IF(WC.EQ.'ORIENT')      PLQ=.TRUE.
      IF(WC.EQ.'NORIENT')     PLQ=.TRUE.
      IF(WQ.EQ.'FRAME')       PLQ=.TRUE.
      IF(WQ.EQ.'FOOT')        PLQ=.TRUE.
      IF(WQ.EQ.'RHFOOT')      PLQ=.TRUE.
      IF(WQ.EQ.'VERTLINE')    PLQ=.TRUE.
      IF(WC.EQ.'PFANAXIS')    PLQ=.TRUE.
      IF(WC.EQ.'PFANLBL')     PLQ=.TRUE.
      IF(WC.EQ.'PFANCOMP')    PLQ=.TRUE.
      IF(WC.EQ.'PFANCAP')     PLQ=.TRUE.
      IF(WC.EQ.'PFANSSI')     PLQ=.TRUE.
      IF(WQ.EQ.'NSSSURFS')    PLQ=.TRUE.
      IF(WQ.EQ.'NSSSURF')     PLQ=.TRUE.
      IF(WQ.EQ.'NSSRAYS')     PLQ=.TRUE.
      IF(WQ.EQ.'NSSSPOT')     PLQ=.TRUE.
      IF(WQ.EQ.'LINE')        PLQ=.TRUE.
      IF(WQ.EQ.'PMRAYX')        PLQ=.TRUE.
      IF(WQ.EQ.'PMRAYY')        PLQ=.TRUE.
      IF(WQ.EQ.'PCRAYX')        PLQ=.TRUE.
      IF(WQ.EQ.'PCRAYY')        PLQ=.TRUE.
      IF(WQ.EQ.'FILL')        PLQ=.TRUE.
      IF(.NOT.PLQ) THEN
         OUTLYNE='INVALID QUALIFIER USED WITH "PLOT"'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
   END IF
!
!       PLOT NEW
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'NEW') THEN
!       INITIALIZE PLOTTING
!       CHECK SYNTAX
      IF(SN.EQ.1 &
      &.OR.SST.EQ.1.AND.STI.NE.1) THEN
         OUTLYNE='"PLOT NEW" TAKES NO ADDITIONAL INPUT'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(STI.EQ.1) THEN
         OUTLYNE='"PLOT NEW ?" HAS NO MEANING'
         CALL SHOWIT(1)
         RETURN
      END IF
      CALL PLTDEV
      RETURN
   END IF
   IF(WC.EQ.'DRAWFAN') GO TO 654
   IF(WC.EQ.'PLT_FAN') GO TO 654
   IF(WQ.NE.'NEW'&
   &.OR.WC.EQ.'ORIENT'.OR.WC.EQ.'NORIENT'.OR.WC.EQ.'PNOTE') THEN
      IF(DEVTYP.NE.1) THEN
         OUTLYNE=&
         &'"PLOT NEW" MUST BE ISSUED BEFORE PLOTTING CAN PROCEED'
         CALL SHOWIT(1)
         OUTLYNE=&
         &'NO ACTION TAKEN'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
   END IF
654 CONTINUE
!
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'UXAXIS') THEN
!       UXAXIS
      LOWER=REAL(W1)
      UPPER=REAL(W2)
      IF(DF3.EQ.0) UUDX=REAL(W3)
      IF(UUDX.EQ.0.0) UUDX=5.0
      CALL PLOTUXAXIS(LOWER,UPPER)
   END IF
!
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'UYAXIS') THEN
!       UYAXIS
      LOWER=REAL(W1)
      UPPER=REAL(W2)
      IF(DF1.EQ.0) UUDY=REAL(W3)
      IF(UUDY.EQ.0.0) UUDY=10.0
      CALL PLOTUYAXIS(LOWER,UPPER)
   END IF
!
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'UXAXRNG') THEN
!       UXAXIS
      LOWER=REAL(W1)
      UPPER=REAL(W2)
      CALL PLOTUXAXRNG(LOWER,UPPER)
   END IF
!
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'UYAXRNG') THEN
!       UYAXIS
      LOWER=REAL(W1)
      UPPER=REAL(W2)
      CALL PLOTUYAXRNG(LOWER,UPPER)
   END IF
!
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'UXAXISLB') THEN
!       UXAXISLB
      NAME(1:40)=WS(1:40)
      DO I=40,1,-1
         IF(NAME(I:I).NE.' ') THEN
            N=I
            GO TO 10
         END IF
      END DO
10    CONTINUE

      CALL PLOTUXAXISLB(NAME,N)
   END IF
!
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'UYAXISLB') THEN
!       UYAXISLB
      NAME(1:40)=WS(1:40)
      DO I=40,1,-1
         IF(NAME(I:I).NE.' ') THEN
            N=I
            GO TO 11
         END IF
      END DO
11    CONTINUE

      CALL PLOTUYAXISLB(NAME,N)
   END IF
!
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'UPLOT') THEN
      CALL PLOTUFUNC
   END IF
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'UYLINE') THEN
      CALL PLOTUFUNCY
   END IF
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'UXLINE') THEN
      CALL PLOTUFUNCX
   END IF
!
!       PLI
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'NAME') CALL PLTNAM
!
!       PLOT FRAME
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'FRAME') CALL PLTFRM
!
!       PLOT END
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'END') CALL PLTSTP
!
!       LISTDRAW
   IF(WC.EQ.'LISTDRAW') CALL LDRAW


!       DRAWFAN
   IF(WC.EQ.'DRAWFAN') CALL DRAWFAN
!       PLT_FAN
   IF(WC.EQ.'PLT_FAN') CALL PLT_FAN
!
!       GRAOUT
   IF(WC.EQ.'GRAOUT') THEN
      CALL GRAOUT
   END IF
!
!       PLOT LI
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'LI') CALL PLTLI
!
!       PNOTE
   IF(WC.EQ.'PNOTE') CALL MAKEPNOTE
!
!       PLOT LBL
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'LBL') CALL PLTLBLL
!
!       PLOT NOLBL
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'NOLBL') CALL PLTLBLL
!
!       PLOT FIGURE
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'FIGURE') CALL PLTFIG
!
!       PLOT NOLI
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'NOLI') CALL PLTLI
!
!       PLOT AXIS
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'AXIS') CALL PLTAX
!
!       PLOT SCALE, NOSCALE AND YESCALE
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'SCALE') CALL PLTSZ
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'NOSCALE') CALL PLTSZ
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'YESCALE') CALL PLTSZ
!
!       PLOT NSSSCALE
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'NSSSCALE') CALL NSSPLTSZ
!
!       PLOT SIZE, NOSIZE AND YESIZE
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'SIZE') CALL PLTSCL
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'NOSIZE') CALL PLTSCL
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'YESIZE') CALL PLTSCL
!
!       PLOT ORIGIN
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'ORIGIN') CALL PLTORG
!
!       PLOT SYMBOL
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'SYMBOL') CALL PLTSYM
!
!       LSTYLE
   IF(WQ.EQ.'LSTYLE') CALL PLTSTL
!
!       LOOK
   IF(WQ.EQ.'LOOK'.OR.WQ.EQ.'YESLOOK'.OR.WQ.EQ.'NOLOOK')&
   &CALL PLTLOK
!
!       VIEW
   IF(WQ.EQ.'VIEW'.OR.WQ.EQ.'YESVIEW'.OR.WQ.EQ.'NOVIEW')&
   &CALL PLTVIE
!
!       PEN
   IF(WQ.EQ.'PEN') CALL PLTPEN
!
!       DASH
   IF(WQ.EQ.'DASH') CALL PLTDSH
!
!       NODASH
   IF(WQ.EQ.'NODASH') CALL PLTDSH
!
!       NOTE
   IF(WQ.EQ.'NOTE') CALL PLNOTE
!
!       NOTE
   IF(WQ.EQ.'CHNOTE') CALL CHSIZE
!
!       ACC
   IF(WQ.EQ.'ACC') CALL PLTACC
!
!       RAY
   IF(WQ.EQ.'RAY') CALL PLTRAE
!
!       RAYS
   IF(WQ.EQ.'RAYS') THEN
      CACOCHVIE=0
      IF(.NOT.VIGOFF) CACOCHVIE=1
      F34=1
      MSG=.FALSE.
      CALL PLTRAYS(CACOCHVIE)
      F34=0
      MSG=.TRUE.
      CACOCH=0
      CACOCHVIE=0
      RETURN
   END IF
!       RAYSCO
   IF(WQ.EQ.'RAYSCO') THEN
      VIGOFF=.TRUE.
      CACOCH=1
      CACOCHVIE=1
      F34=1
      MSG=.FALSE.
      CALL PLTRAYS(CACOCHVIE)
      F34=0
      MSG=.TRUE.
      CACOCH=0
      CACOCHVIE=0
      RETURN
   END IF
!
!       PMRAYX
   IF(WQ.EQ.'PMRAYX') THEN
      SAVE_KDP(1)=SAVEINPT(1)
      INPUT='OUT NULL'
      CALL PROCES
      INPUT='GPXTY ALL'
      CALL PROCES
      INPUT='OUT TP'
      CALL PROCES
      REST_KDP(1)=RESTINPT(1)
      IF(GPRAYEXT) THEN
         CALL PLTPRAE(1)
         CALL PLTPRAE(2)
      ELSE
         WRITE(OUTLYNE,*) 'NO GENERALIZED PARAXIAL RAYS COULD BE TRACED'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'NO GENERALIZED PARAXIAL RAYS EXISTS TO PLOT'
         CALL SHOWIT(1)
      END IF
   END IF
!
!       PMRAYY
   IF(WQ.EQ.'PMRAYY') THEN
      SAVE_KDP(1)=SAVEINPT(1)
      INPUT='OUT NULL'
      CALL PROCES
      INPUT='GPXTY ALL'
      CALL PROCES
      INPUT='OUT TP'
      CALL PROCES
      REST_KDP(1)=RESTINPT(1)
      IF(GPRAYEXT) THEN
         CALL PLTPRAE(3)
         CALL PLTPRAE(4)
      ELSE
         WRITE(OUTLYNE,*) 'NO GENERALIZED PARAXIAL RAYS COULD BE TRACED'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'NO GENERALIZED PARAXIAL RAYS EXISTS TO PLOT'
         CALL SHOWIT(1)
      END IF
   END IF
!
!       PCRAYX
   IF(WQ.EQ.'PCRAYX') THEN
      SAVE_KDP(1)=SAVEINPT(1)
      INPUT='OUT NULL'
      CALL PROCES
      INPUT='GPXTY ALL'
      CALL PROCES
      INPUT='OUT TP'
      CALL PROCES
      REST_KDP(1)=RESTINPT(1)
      IF(GPRAYEXT) THEN
         CALL PLTPRAE(5)
         CALL PLTPRAE(6)
      ELSE
         WRITE(OUTLYNE,*) 'NO GENERALIZED PARAXIAL RAYS COULD BE TRACED'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'NO GENERALIZED PARAXIAL RAYS EXISTS TO PLOT'
         CALL SHOWIT(1)
      END IF
   END IF
!
!       PCRAYY
   IF(WQ.EQ.'PCRAYY') THEN
      SAVE_KDP(1)=SAVEINPT(1)
      INPUT='OUT NULL'
      CALL PROCES
      INPUT='GPXTY ALL'
      CALL PROCES
      INPUT='OUT TP'
      CALL PROCES
      REST_KDP(1)=RESTINPT(1)
      IF(GPRAYEXT) THEN
         CALL PLTPRAE(7)
         CALL PLTPRAE(8)
      ELSE
         WRITE(OUTLYNE,*) 'NO GENERALIZED PARAXIAL RAYS COULD BE TRACED'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*) 'NO GENERALIZED PARAXIAL RAYS EXISTS TO PLOT'
         CALL SHOWIT(1)
      END IF
   END IF
   IF(WC.EQ.'PLOT'.AND.WQ.EQ.'FILL') CALL PLTFILL

!
!       LINE
   IF(WQ.EQ.'LINE') THEN
      IF(NORAYPLOT) THEN
         OUTLYNE=&
         &'AT LEAST ONE RAY MUST BE PLOTTED BEFORE 3D-LINES MAY BE PLOTTED'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
         CALL PLTLINE
      END IF
   END IF
!
!       VERTLINE
   IF(WQ.EQ.'VERTLINE') CALL VERTLINE
!
!       FOOT
   IF(WQ.EQ.'FOOT') THEN
!
      OPEN94=.FALSE.
      EXIS94=.FALSE.
      INQUIRE(FILE='FOOT1.DAT',OPENED=OPEN94)
      INQUIRE(FILE='FOOT1.DAT',EXIST=EXIS94)
      IF(.NOT.EXIS94) THEN
         OUTLYNE=&
         &'NO FOOT PRINT DATA EXISTS TO PLOT WITH "PLOT FOOT"'
         CALL SHOWIT(1)
         OUTLYNE='NO ACTION TAKEN'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(OPEN94) CALL CLOSE_FILE(94,1)
!     MAKE SURE YOU HAVE VERTEX DATA WITHOUT OFFSETS AND SET TO
!     THE FIRST SURFACE WITHOUT AN INFINITE THICKNESS
      GLSURF=-99
      DO I=NEWIMG,0,-1
         IF(DABS(ALENS(3,I)).LE.1.0D10) GLSURF=I
      END DO
      IF(GLSURF.EQ.-99) THEN
         GLOBE=.FALSE.
         OUTLYNE='ALL SURFACES WERE OF INFINITE THICKNESS'
         CALL SHOWIT(1)
         OUTLYNE='NO OPTICAL SYSTEM PLOT COULD BE MADE'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      CALL PLTFOOT
      RETURN
   END IF
!       RHFOOT
   IF(WQ.EQ.'RHFOOT') THEN
!
      OPEN103=.FALSE.
      EXIS103=.FALSE.
      INQUIRE(FILE='RHFOOT.DAT',OPENED=OPEN103)
      INQUIRE(FILE='RHFOOT.DAT',EXIST=EXIS103)
      IF(.NOT.EXIS103) THEN
         OUTLYNE=&
         &'NO RHFOOT PRINT DATA EXISTS TO PLOT WITH "PLOT RHFOOT"'
         CALL SHOWIT(1)
         OUTLYNE='NO ACTION TAKEN'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(OPEN103) CALL CLOSE_FILE(103,1)
!     MAKE SURE YOU HAVE VERTEX DATA WITHOUT OFFSETS AND SET TO
!     THE FIRST SURFACE WITHOUT AN INFINITE THICKNESS
      CALL PLTRHFOOT
      RETURN
   END IF
!
!       NSSSURFS
   IF(WQ.EQ.'NSSSURFS') THEN
      IF(NEXISTN) THEN
         CALL PLTNSSSFS
         RETURN
      ELSE
         OUTLYNE=&
         &'NO NSS DATABASE EXISTS, NO NSS SURFACES CAN BE PLOTTED'
         CALL MACFAL
         RETURN
      END IF
   END IF
!
!       NSSSURF
   IF(WQ.EQ.'NSSSURF') THEN
      IF(NEXISTN) THEN
         CALL PLTNSSSF
         RETURN
      ELSE
         OUTLYNE=&
         &'NO NSS DATABASE EXISTS, NO NSS SURFACES CAN BE PLOTTED'
         CALL MACFAL
         RETURN
      END IF
   END IF
!
!       NSSRAYS
   IF(WQ.EQ.'NSSRAYS') THEN
      EXIS92=.FALSE.
      INQUIRE(FILE='NSSHT.DAT',EXIST=EXIS92)
      IF(NEXISTN.AND.EXIS92) THEN
         CALL PLTNSSRY
         CALL CLOSE_FILE(92,1)
         RETURN
      ELSE
         IF(.NOT.NEXISTN) THEN
            OUTLYNE=&
            &'NO NSS DATABASE EXISTS, NO NSS RAYS CAN BE PLOTTED'
            CALL SHOWIT(1)
         END IF
         IF(.NOT.EXIS92) THEN
            OUTLYNE=&
            &'NO NSS RAY HISTORY FILE EXISTS, NO NSS RAYS CAN BE PLOTTED'
            CALL SHOWIT(1)
         END IF
         CALL MACFAL
         RETURN
      END IF
   END IF
!
!       NSSSPOT
   IF(WQ.EQ.'NSSSPOT') THEN
      IF(.NOT.NSSSPOTEXIST) THEN
         OUTLYNE=&
         &'NO NSS SPOT DIAGRAM EXISTS, NO NSS SPOTS CAN BE PLOTTED'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      EXIS92=.FALSE.
      INQUIRE(FILE='NSSHT.DAT',EXIST=EXIS92)
      V1=DBLE(NSSSPOTSURF)
      V2=W1
      IF(DF1.EQ.1) V2=1.0D0
      V2=W1
      IF(INT(V2).LT.1) V2=1.0D0
      CALL PLTNSSSPOTS(V1,V2)
      CALL CLOSE_FILE(92,1)
      RETURN
   END IF
!
!       PROFX
   IF(WQ.EQ.'PROFX') THEN
      DF3=0
      S3=1
      W3=0.0D0
      WQ='PROF'
   END IF
!
!       PROFY
   IF(WQ.EQ.'PROFY') THEN
      DF3=0
      S3=1
      W3=90.0D0
      WQ='PROF'
   END IF
!
!       PROF
   IF(WQ.EQ.'PROF') THEN
      CALL PLTPRO1
      RETURN
   END IF
!
!       EDGEX OR EDGEY
   IF(WQ.EQ.'EDGEX'.OR.WQ.EQ.'EDGEY') CALL PLTEDG
!
!       CLAP

   IF(WQ.EQ.'CLAP') THEN
!
!       CHECK SYNTAX
      IF(SST.EQ.1) THEN
         OUTLYNE=&
         &'"PLOT CLAP" TAKES NO STRING INPUT'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(S4.EQ.1) THEN
         OUTLYNE=&
         &'"PLOT CLAP" ONLY TAKES NUMERIC WORDS #1, #2, #3 AND #5 INPUT'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(DF3.EQ.0.AND.W3.LE.0.0D0.OR.&
      &DF3.EQ.0.AND.W3.GT.1.0D0) THEN
         OUTLYNE=&
         &'THE SCALING FACTOR IN NUMERIC WORD #3 MUST BE GREATER'
         CALL SHOWIT(1)
         OUTLYNE=&
         &'THAN ZERO AND LESS THAN OR EQUAL TO 1.0'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(STI.EQ.1) THEN
         WRITE(OUTLYNE,800)
         CALL SHOWIT(1)
800      FORMAT('QUERRY (?) HAS NO MEANING WITH "PLOT CLAP"')
         RETURN
      ELSE
      END IF

      IF(DF1.EQ.0.AND.W1.LT.0.0D0) W1=SYSTEM(20)+W1
      IF(DF2.EQ.0.AND.W2.LT.0.0D0) W2=SYSTEM(20)+W2
      IF(DF3.EQ.1) W3=1.0D0
!       DEFAULT VALUES
      IF(DF1.EQ.1) THEN
         IF(DABS(ALENS(3,0)).GT.1.0D10) THEN
            W1=DBLE(1)
         ELSE
            W1=DBLE(0)
         END IF
      ELSE
!       DF1 NOT 1, W1 EXPLICITLY ENTERED
      END IF
      STASUR=INT(W1)
      IF(DF2.EQ.1) THEN
         W2=DBLE(NEWIMG)
      ELSE
!       DF2 NOT 1, W2 EXPLICITLY ENTERED
      END IF
      STPSUR=INT(W2)
      IF(INT(W1).LT.0) THEN
!       INVALID NUMERIC WORD #1
         OUTLYNE=&
         &'SURFACE NUMBER (NUMERIC WORD #1) IS BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(INT(W2).GT.NEWIMG) THEN
