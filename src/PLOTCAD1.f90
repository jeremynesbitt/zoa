!     AUTOSCALING IS THE DEFAULT, DOES IT NEED TO BE CALCULATED ?
         IF(.NOT.ASCFLG) THEN
!     AUTOSCALE FACTOR HAS NOT BEEN CALCULATED
!     PROCEED
!     CALCULATE AN AUTO SCALE FACTOR BASED ON THE CURRENTLY
!     REQUESTED RAY PLOTTING DATA SO NO DATA RANGE EXCEEDS
!     6000 DEVICE INDEPENDENT UNITS. THIS KEEPS A GAMMA ROTATION
!     AT THE SCREEN FROM THE FIFTH NUMERIC WORD OF PLOT LOOK OR
