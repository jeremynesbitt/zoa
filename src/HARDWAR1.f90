         IF(S1.EQ.0.OR.SQ.EQ.0) THEN
            OUTLYNE=&
            &'"COLORSET" REQUIRES EXPLICIT QUALIFIER AND'
            CALL SHOWIT(1)
            OUTLYNE=&
            &'EXPLICIT NUMERIC WORD #1 INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         ELSE
         END IF
      ELSE
!     WQ WAS RESET
      END IF
      IF(WQ.EQ.'RESET') THEN
         IF(SN.EQ.1) THEN
            OUTLYNE=&
            &'"COLORSET RESET" TAKES NO ADDITIONAL INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         ELSE
         END IF
      ELSE
!     WQ WAS NOT RESET
      END IF
      IF(WQ.EQ.'RESET') THEN
!
         CALL MY_INIPLT
!
!     RESETTING PROGRAM COLORS TO DEFAULTS
!
         COLDEF=15
         COLRAY=15
         COLCLP=3
         COLCOB=9
         COLEDG=1
         COLPRO=1
         COLAXS=15
         COLBAC=0
!
         COLR1=15
         COLR2=12
         COLR3=2
         COLR4=3
         COLR5=4
         COLR6=5
         COLR7=6
         COLR8=7
         COLR9=8
         COLR10=9
!
         COLFRM=15
         COLLBL=15
         COLSPE=15
         COLPEN=15
         COLFILL = 2

         RETURN
      ELSE
      END IF
      IF(WQ.NE.'RAYS'.AND.WQ.NE.'CLAP'.AND.WQ.NE.'COBS'.AND.&
      &WQ.NE.'EDGE'.AND.WQ.NE.'PROF'.AND.WQ.NE.'AXIS'.AND.&
      &WQ.NE.'GBAC'.AND.WQ.NE.'WAV1'.AND.WQ.NE.'WAV2'.AND.&
      &WQ.NE.'WAV3'.AND.WQ.NE.'WAV4'.AND.WQ.NE.'WAV5'.AND.&
      &WQ.NE.'FRAM'.AND.WQ.NE.'LABL'.AND.&
      &WQ.NE.'SPEC'.AND.WQ.NE.'PEN'.AND.&
      &WQ.NE.'WAV6'.AND.WQ.NE.'WAV7'.AND.WQ.NE.'WAV8'.AND.&
      &WQ.NE.'WAV9'.AND.WQ.NE.'WAV10'.AND.WQ.NE.'FILL')THEN
         OUTLYNE=&
         &'INVALID QUALIFIER USED WITH "COLORSET"'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
      END IF
      IF(INT(W1).LT.-1.OR.INT(W1).GT.16) THEN
         OUTLYNE=&
         &'NUMERIC COLOR VALUES MUST RANGE FROM -1 TO 15'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
      END IF
!
!     PROCEED WITH SETTING OF COLORS
      IF(WQ.EQ.'RAYS') COLRAY=INT(W1)
      IF(WQ.EQ.'CLAP') COLCLP=INT(W1)
      IF(WQ.EQ.'COBS') COLCOB=INT(W1)
      IF(WQ.EQ.'EDGE') COLEDG=INT(W1)
      IF(WQ.EQ.'PROF') COLPRO=INT(W1)
      IF(WQ.EQ.'AXIS') COLAXS=INT(W1)
      IF(WQ.EQ.'GBAC') COLBAC=INT(W1)
      IF(WQ.EQ.'WAV1') COLR1=INT(W1)
      IF(WQ.EQ.'WAV2') COLR2=INT(W1)
      IF(WQ.EQ.'WAV3') COLR3=INT(W1)
      IF(WQ.EQ.'WAV4') COLR4=INT(W1)
      IF(WQ.EQ.'WAV5') COLR5=INT(W1)
      IF(WQ.EQ.'WAV6') COLR6=INT(W1)
      IF(WQ.EQ.'WAV7') COLR7=INT(W1)
      IF(WQ.EQ.'WAV8') COLR8=INT(W1)
      IF(WQ.EQ.'WAV9') COLR9=INT(W1)
      IF(WQ.EQ.'WAV10') COLR10=INT(W1)
