SUBROUTINE PLTSYM
!
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
!       THIS ROUTINE DOES THE PLOT SYMBOL COMMAND AT THE CMD LEVEL
!
   CHARACTER SYM*15
!
   INTEGER COLPAS,IXPPEE,IYPPEE
!
!
!       PLOT SYMBOL
!
   IF(WQ.EQ.'SYMBOL') THEN
!       CHECK SYNTAX
      IF(SST.EQ.1) THEN
         OUTLYNE=&
         &'"PLOT SYMBOL" TAKES NO STRING INPUT'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(S5.EQ.1) THEN
         OUTLYNE=&
         &'"PLOT SYMBOL" TAKES NO NUMERIC WORD #5 INPUT'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(STI.EQ.1) THEN
         IF(SYMB.EQ.1)  SYM='+'
         IF(SYMB.EQ.2)  SYM='x'
         IF(SYMB.EQ.3)  SYM='SQUARE'
         IF(SYMB.EQ.4)  SYM='TRIANGLE'
         IF(SYMB.EQ.5)  SYM='INVERTED TRIANGLE'
         IF(SYMB.EQ.6)  SYM='QUARTERED SQUARE'
         IF(SYMB.EQ.7)  SYM='CROSSED TRIANGLE'
         IF(SYMB.EQ.8)  SYM='CROSSED INVERTED TRIANGLE'
         IF(SYMB.EQ.9)  SYM='SQUARE WITH X'
         IF(SYMB.EQ.10) SYM='TRIANGLE PLUS INVERTED TRIANGLE'
         WRITE(OUTLYNE,874) SYM,SYMB
         CALL SHOWIT(1)
874      FORMAT('CURRENT SYMBOL IS ',A15,1X,'SYMBOL NUMBER = ',I1)
         RETURN
      ELSE
      END IF
      IF(INT(W1).LT.1.OR.INT(W1).GT.10) THEN
         OUTLYNE=&
         &'VALID SYMBOL NUMBERS RANGE FROM 1 TO 10'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(INT(W2).LT.1.OR.INT(W2).GT.9) THEN
         OUTLYNE=&
         &'VALID SYMBOL SIZES RANGE FROM 1 TO 9'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(DF3.EQ.1.OR.DF4.EQ.1) THEN
         OUTLYNE=&
         &'X AND Y-COORDINATES MUST BE ENTERED IN NUMERIC WORDS 3 AND 4'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
      END IF
      IF(DF1.EQ.1) W1=9.0D0
      IF(DF2.EQ.1) W2=1.0D0
      SYMB=INT(W1)
      SYMSIZ=INT(W2)
      IXPPEE=INT(W3)
      IYPPEE=INT(W4)
      COLPAS=COLLBL
      CALL MY_COLTYP(COLPAS)
      IF(.NOT.PLEXIS) PLEXIS=.TRUE.
      CALL MY_MARKER(SYMB,SYMSIZ,IXPPEE,IYPPEE)
      RETURN
   ELSE
!       NOT PLOT SYMBOL
   END IF
   RETURN
END

