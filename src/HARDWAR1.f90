SUBROUTINE PLSTAT
!
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE RETURNS THE NUMBER OF PLOTS
!       CURRENTLY ON FILE IN THE PLOT LIBRARAY
!
!
   INTEGER I,J,K,II
!
   LOGICAL EXISJK
!
   CHARACTER DATA*80
!
   COMMON/DATA/I,J,K,II
!
!
!       OPEN UNIT 20 FOR I/O
!
!       ***************************************************************
   EXISJK=.FALSE.
   INQUIRE(FILE=LIBPLO//'PLIB.DAT',EXIST=EXISJK)
   IF(.NOT.EXISJK) THEN
      OUTLYNE='"PLOT LIBRARY DOES NOT YET EXIST'
