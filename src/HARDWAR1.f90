         IF(W1.LT.-1.0D0.OR.W1.GT.15.0D0) THEN
            OUTLYNE=&
            &'"COLORSET GBAC" ONLY USES COLOR NUMBERS -1 TO 15'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         ELSE
!     INPUT OK
         END IF
      ELSE
!     NOT GBAC
      END IF
      IF(WQ.EQ.'SPEC') COLSPE=INT(W1)
      IF(WQ.EQ.'PEN') COLPEN=INT(W1)
!
      RETURN
!
   ELSE
!     QUERRY IS IMPLEMENTED HERE
!     CASE OF "COLORSET ?"
      IF(SQ.EQ.0) THEN
         OUTLYNE='"COLORSET" QUERRY REQUIRES A VALID QALIFIER WORD'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
!     SQ NOT 0
      END IF
!     IS SQ=1 ?
      IF(SQ.EQ.1) THEN
         IF(WQ.NE.'RAYS'.AND.WQ.NE.'CLAP'.AND.WQ.NE.'COBS'.AND.&
         &WQ.NE.'EDGE'.AND.WQ.NE.'PROF'.AND.WQ.NE.'AXIS'.AND.&
         &WQ.NE.'GBAC'.AND.WQ.NE.'WAV1'.AND.WQ.NE.'WAV2'.AND.&
         &WQ.NE.'WAV3'.AND.WQ.NE.'WAV4'.AND.WQ.NE.'WAV5'.AND.&
         &WQ.NE.'FRAM'.AND.WQ.NE.'LABL'.AND.&
         &WQ.NE.'WAV6'.AND.WQ.NE.'WAV7'.AND.WQ.NE.'WAV8'.AND.&
         &WQ.NE.'WAV9'.AND.WQ.NE.'WAV10'.AND.&
         &WQ.NE.'SPEC'.AND.WQ.NE.'PEN'.AND.WQ.NE.'FILL')THEN
            OUTLYNE=&
            &'INVALID QUALIFIER USED WITH "COLORSET" QUERRY'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         ELSE
         END IF
!     QUALIFIER OK
         IF(WQ.EQ.'RAYS') COLANS=COLRAY
         IF(WQ.EQ.'CLAP') COLANS=COLCLP
         IF(WQ.EQ.'COBS') COLANS=COLCOB
         IF(WQ.EQ.'EDGE') COLANS=COLEDG
         IF(WQ.EQ.'PROF') COLANS=COLPRO
         IF(WQ.EQ.'AXIS') COLANS=COLAXS
         IF(WQ.EQ.'GBAC') COLANS=COLBAC
         IF(WQ.EQ.'WAV1') COLANS=COLR1
         IF(WQ.EQ.'WAV2') COLANS=COLR2
         IF(WQ.EQ.'WAV3') COLANS=COLR3
         IF(WQ.EQ.'WAV4') COLANS=COLR4
         IF(WQ.EQ.'WAV5') COLANS=COLR5
         IF(WQ.EQ.'WAV6') COLANS=COLR6
         IF(WQ.EQ.'WAV7') COLANS=COLR7
         IF(WQ.EQ.'WAV8') COLANS=COLR8
         IF(WQ.EQ.'WAV9') COLANS=COLR9
         IF(WQ.EQ.'WAV10') COLANS=COLR10
         IF(WQ.EQ.'FRAM') COLANS=COLFRM
         IF(WQ.EQ.'LABL') COLANS=COLLBL
         IF(WQ.EQ.'SPEC') COLANS=COLSPE
         IF(WQ.EQ.'PEN') COLANS=COLPEN
         IF(WQ.EQ.'FILL') COLANS=COLFILL

100      FORMAT('"',A4,'"',' IS CURRENTLY SET TO COLOR NUMBER ',I3)
         WRITE(OUTLYNE,100)WQ(1:4),COLANS
         CALL SHOWIT(1)
         RETURN
      ELSE
!     SQ NOT 1
      END IF
   END IF
   RETURN
!
END
SUBROUTINE MY_DEL_NEUT
   USE GLOBALS
   use DATMAI
   IMPLICIT NONE
   LOGICAL EXISNEUT,OPENNEUT
   INTEGER ALLOERR, fNeut
   INQUIRE(UNIT=28,EXIST=EXISNEUT)
   INQUIRE(UNIT=28,OPENED=OPENNEUT)
   IF(EXISNEUT.AND..NOT.OPENNEUT) THEN
      CALL OPEN_NEUTRAL_DAT(fNeut)
      CALL CLOSE_FILE(fNeut,0)
   END IF
   IF(EXISNEUT.AND.OPENNEUT) THEN
      CALL CLOSE_FILE(28,0)
   END IF
   DEALLOCATE(NEUTARRAY,STAT=ALLOERR)
   RETURN
END
SUBROUTINE RESIZE_NEUT
!     THIS ROUTINE DOUBLES THE SIZE OF NEUTARRAY IF NEEDED
   USE GLOBALS
   use DATHGR
   IMPLICIT NONE
   CHARACTER*80 NEUTTEMP
   INTEGER ALLOERR,I
   DIMENSION NEUTTEMP(:)
   ALLOCATABLE :: NEUTTEMP
   MAXNEUTRAL=MAXNEUTRAL*2
   ALLOCATE (NEUTTEMP(MAXNEUTRAL),STAT=ALLOERR)
   DO I=1,MAXNEUTRAL/2
      NEUTTEMP(I)=NEUTARRAY(I)
   END DO
   DEALLOCATE(NEUTARRAY,STAT=ALLOERR)
   ALLOCATE(NEUTARRAY(MAXNEUTRAL),STAT=ALLOERR)
   DO I=1,MAXNEUTRAL/2
      NEUTARRAY(I)=NEUTTEMP(I)
   END DO
   DEALLOCATE(NEUTTEMP,STAT=ALLOERR)
   RETURN
END
SUBROUTINE PLIBRY
   USE GLOBALS
   use DATHGR
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE HANDELS PLOT LIBRARY COMMANDS
!       PLIB P, PLIB PUT, PLIB DEL AND PLIB GET.
!                       DEFINE VARIABLES
!
   CHARACTER DATA*80,WSS1*80,PPPLI*60,STRINGER*1,&
   &LLIP*80,DDATE*10,TTIME*8,AN*3,NEUTLINE*42 &
   &,FN*10,NM*8,WSS*80,TTTIM*8,DDDAT*10 &
   &,DRWNAM*11,JK_Fromfile*80,JK_Tofile*80
!
   COMMON/DRWTAG/DRWNAM
!
   CHARACTER C*1,PFILENAME*17,C1A*20,C1B*20,C1C*20,C1D*20
!
   COMMON/STRNGR/DDDAT,TTTIM,NM,FN
!
   LOGICAL EXISJK,REOPEN,OPPLT
!
   REAL JJ1,JJ2
!
   INTEGER SING,LCNT,I,II,J,N,NIN,PFCOUNTER,I1,I2,I3,I4,I5,I6,I7,&
   &I8,ALLOERR,JJ, fNeut
!
   INTEGER DUMTOT,FLG(0:20),ALL
!
   COMMON/FFL/FLG
!
!
!       SET ALL=0
   ALL=0
!       SET SING=0
   SING=0

   PRINT *, "PLIBRY CALLED"
!
!
!*************************************************************
!       SPECIFIC CHECKS FOR PLIB P
!
!       LIB P CAN TAKE NUMERIC INPUT FROM NW1 AND NW2
!       NW1 CAN BE 1 TO 999 OR BLANK
!       NW2 CAN BE 2 TO 999 OR BLANK
!       NW1 MUST BE LESS THAN NW2
!
   IF(WQ.EQ.'P') THEN
      IF(SST.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
         OUTLYNE=&
         &'"PLIB P" ONLY TAKES NUMERIC WORD #1 AND #2 INPUT'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
!       INPUT OK
      END IF
      IF(W1.LT.1.0.AND.DF1.NE.1.OR.&
      &W1.GT.999.0.AND.DF1.NE.1) THEN
         OUTLYNE='NUMERIC WORD 1 BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
      END IF
      IF(W2.LT.2.0.AND.DF2.NE.1.OR.&
      &W2.GT.999.0.AND.DF2.NE.1) THEN
         OUTLYNE='NUMERIC WORD 2 BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
      END IF
      IF(S2.EQ.1) THEN
!
         IF(W1.GE.W2) THEN
            OUTLYNE=&
            &'NUMERIC WORD 1 MUST BE LESS THAN NUMERIC WORD 2'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         ELSE
         END IF
      ELSE
!       NW2 IS BLANK
!       SET SING=1
         SING=1
      END IF
!       IS NW1 AND NW2 DEFAULT
      IF(DF1.EQ.1.AND.DF2.EQ.1) THEN
!       NO NUMERIC INPUT, SET THE ALL VARIABLE TO 1
         ALL=1
      ELSE
      END IF
   ELSE
!       NOT PLIB P
   END IF
!**********************************************************
!       SPECIFIC CHECKS FOR PLIB PUT
!
!       LIB PUT CAN TAKE NUMERIC INPUT FROM NW1 ONLY
!       NW1 CAN BE 1 TO 999 OR BLANK
!
!       PLIB PUT NEEDS THE FILE NEUTRAL.DAT TO EXIST AND FOR
!       IT TO BE CLOSED
   IF(WQ.EQ.'PUT') THEN
      EXIS28=.FALSE.
      OPEN28=.FALSE.
      INQUIRE(FILE=trim(basePath)//'NEUTRAL.DAT',EXIST=EXIS28)
      INQUIRE(FILE=trim(basePath)//'NEUTRAL.DAT',OPENED=OPEN28)
      IF(.NOT.OPEN28.AND.EXIS28) THEN
!       PROCEED WITH PLIB PUT, ELSE RETURN WITH A MESSAGE
         GO TO 89
      ELSE
      END IF
!
!     FILE DOES NOT EXIST
      IF(.NOT.EXIS28) THEN
         OUTLYNE='NO PLOTFILE EXISTS TO BE FILED'
         CALL SHOWIT(1)
         OUTLYNE='NO "LIB PUT" CAN BE DONE'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
      END IF
89    CONTINUE
   ELSE
!     WQ.NOT.'PUT'
   END IF
   IF(WQ.EQ.'PUT') THEN
      IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
         OUTLYNE=&
         &'"PLIB PUT" ONLY TAKES NUMERIC WORD #1 INPUT'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
!       INPUT OK
      END IF
      IF(W1.LT.1.0.AND.DF1.NE.1.OR.&
      &W1.GT.999.0.AND.DF1.NE.1) THEN
         OUTLYNE='NUMERIC WORD 1 BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
      END IF
!       IS NW1 DEFAULT
      IF(DF1.EQ.1) THEN
!       NO NUMERIC INPUT, SET THE ALL VARIABLE TO 1
         ALL=1
      ELSE
      END IF
   ELSE
!       NOT PLIB PUT
   END IF

!**********************************************************
!       SPECIFIC CHECKS FOR PLIB GET
!
!       LIB GET CAN TAKE NUMERIC INPUT FROM NW1 ONLY
!       NW1 CAN BE 1 TO 999 OR BLANK
!
   IF(WQ.EQ.'GET') THEN
      IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
         OUTLYNE=&
         &'"PLIB GET" ONLY TAKES NUMERIC WORD #1 INPUT'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
!       INPUT OK
      END IF
      IF(W1.LT.1.0.AND.DF1.NE.1.OR.&
      &W1.GT.999.0.AND.DF1.NE.1) THEN
         OUTLYNE='NUMERIC WORD 1 BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
      END IF
!       IS NW1 DEFAULT
      IF(DF1.EQ.1) THEN
         OUTLYNE='"PLIB GET" REQUIRES EXPLICIT NUMERIC INPUT'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
      END IF
   ELSE
!       NOT PLIB GET
   END IF
!*************************************************************
!       SPECIFIC CHECKS FOR PLIB DEL
!
!       PLIB DEL CAN TAKE NUMERIC INPUT FROM NW1 AND NW2
!       NW1 CAN BE 1 TO 999
!       NW2 CAN BE 2 TO 999
!       NW1 MUST BE LESS THAN NW2 IF NW2 NOT DEFAULT
!
   IF(WQ.EQ.'DELK') THEN
      IF(SST.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
         OUTLYNE=&
         &'"PLIB DEL" ONLY TAKES NUMERIC WORD #1 AND #2 INPUT'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
!       INPUT OK
      END IF
      IF(W1.LT.1.0.AND.DF1.NE.1.OR.&
      &W1.GT.999.0.AND.DF1.NE.1) THEN
         OUTLYNE='NUMERIC WORD 1 BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
      END IF
      IF(W2.LT.2.0.AND.DF2.NE.1.OR.&
      &W2.GT.999.0.AND.DF2.NE.1) THEN
         OUTLYNE='NUMERIC WORD 2 BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
      END IF
      IF(S2.EQ.1) THEN
!
         IF(W1.GE.W2) THEN
            OUTLYNE=&
            &'NUMERIC WORD 1 MUST BE LESS THAN NUMERIC WORD 2'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         ELSE
         END IF
      ELSE
!       NW2 IS BLANK
!       SET SING=1
         SING=1
      END IF
!       IS NW1 AND NW2 DEFAULT
      IF(DF1.EQ.1.AND.DF2.EQ.1) THEN
         OUTLYNE='"PLIB DEL" REQUIRES EXPLICIT NUMERIC INPUT'
         CALL SHOWIT(1)
         OUTLYNE='RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
      END IF
   ELSE
!       NOT PLIB DEL
   END IF
!***********************************************************
!       IF YOU GOT HERE, INPUT WAS OK
!       PROCEED WITH PROCESSING
!
   IF(WQ.EQ.'PUT') THEN
!       FIRST PLIB PUT
!       WHEN PLIB PUT IS USED, AN ENTRY IS MADE INTO AN AVALIABLE
!       SLOT IN PLIB.DAT (THE PLOT LIBRARY DIRECTORY)
!       DEPENDING ON WHICH SLOT WAS USED, A FILE
!       PLIB001.DAT TO PLIB999.DAT IS USED TO THEN STORE THE
!       PLOT DATA IN BINARY FORMAT. PLIB.DAT IS UNIT 24
!
!       IF ALL=1 THEN WE MUST SEARCH FOR AN AVALIABLE SLOT
!       ***************************************************************
      EXISJK=.FALSE.
      INQUIRE(FILE=LIBPLO//'PLIB.DAT',EXIST=EXISJK)
      IF(.NOT.EXISJK) THEN
         OUTLYNE='"PLOT LIBRARY DOES NOT YET EXIST'
         CALL SHOWIT(1)
         OUTLYNE='TO INITIALIZE IT, USE "IPF" AND "PROCEED"'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
!       PROCEED
      END IF
!       ***************************************************************
      OPEN(UNIT=24,ACCESS='DIRECT',FILE=LIBPLO//'PLIB.DAT',&
      &FORM='UNFORMATTED',RECL=(90*NRECL),STATUS='UNKNOWN')
      IF(ALL.EQ.1) THEN
         DO 10 I=1,999
            READ(UNIT=24,REC=I) II,DATA,DUMTOT
            IF(II.EQ.0) THEN
!       FOUND AN EMPTY SLOT
               II=I
               GO TO 11
            ELSE
!       CONTINUE SEARCH
            END IF
10       CONTINUE
!       IF YOU GOT HERE, LIBRARY WAS FULL
         OUTLYNE='PLOT LIBRARY FULL, PLOT NOT FILED'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
11       CONTINUE
!       PROCEED WITH STORAGE
      ELSE
!       ALL NOT 1 SET II=INT(W1)
         II=INT(W1)
         READ(UNIT=24,REC=II) J,DATA,DUMTOT
         IF(J.NE.0) THEN
!       SLOT OCCUPIED
            WRITE(OUTLYNE,100) J
100         FORMAT('LIBRARY FILE NO. ',I3,' OCCUPIED')
            CALL SHOWIT(1)
            OUTLYNE='PLOT NOT FILED'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         ELSE
!       PROCEED, SPOT EMPTY
         END IF
      END IF
!       FILE THE NUMBER AND NAME IN THE LIBRARY
      CALL MYDATE(DDATE)
      CALL MYTIME(TTIME)
      TTTIM=TTIME
      DDDAT=DDATE
      IF(PPLI(1:30).NE.'                              ') THEN
         LLIP=DDATE//' '//TTIME//' '//PPLI(1:59)
      ELSE
         LLIP=DDATE//' '//TTIME//' '//LI(1:59)
      END IF
      WRITE(UNIT=24,REC=II) II,LLIP,NEUTTOTAL+1
      CLOSE(24 )
!
!     OPEN, CLOSE AND REMOVE FILE 28
      CALL OPEN_NEUTRAL_DAT(fNeut)
      CALL CLOSE_FILE(fNeut,0)
!     OPEN A NEW ONE
      CALL OPEN_NEUTRAL_DAT(fNeut)
      IF(NEUTFILE) THEN
         DO I=1,NEUTTOTAL+1
            WRITE(UNIT=fNeut,REC=I) NEUTARRAY(I)
         END DO
      END IF
      CALL CLOSE_FILE(fNeut,1)
!       NOW DETERMINE WHICH OF THE 999 PLOT LIBRARY FILES
!       TO USE
!
      N=II
      CALL CCOONN(N,AN)
      IF(N.GT.0.AND.N.LE.9) FN='PLT00'//AN(3:3)//'.DAT'
      IF(N.GT.9.AND.N.LE.99) FN='PLT0'//AN(2:3)//'.DAT'
      IF(N.GT.99.AND.N.LE.999) FN='PLT'//AN(1:3)//'.DAT'
      CLOSE(28 )
      PFILENAME=LIBPLO//FN
      PFCOUNTER=NEUTTOTAL
! WINTER
!      CALL MY_COPYFILE('NEUTRAL.DAT',PFILENAME)
!
      WRITE(OUTLYNE,470) II
470   FORMAT('PLOT STORED IN LIBRARY FILE NO. ',I3)
      CALL SHOWIT(1)
      RETURN
   END IF
!
!       NOW PLIB GET
!
!
   IF(WQ.EQ.'GET') THEN
