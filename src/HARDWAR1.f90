SUBROUTINE CLOSE_FILE(UNITT,STAT)
!
!     THIS SUBROUTINE IS CALLED TO CLOSE OPEN FILES
!
   use DATMAI
   IMPLICIT NONE
!
   INTEGER UNITT,STAT
!
   LOGICAL UNITOPEN
!
   CHARACTER*80 FILE_NAME
!
!
!     STAT = 1 MEANS 'KEEP'
!     STAT = 0 MEANS 'DELETE'
!
   ALLSTOP=.FALSE.
!
   FILE_NAME='UNNAMED'
   INQUIRE(UNIT=UNITT,NAME=FILE_NAME)

   UNITOPEN=.FALSE.
   INQUIRE(UNIT=UNITT,OPENED=UNITOPEN)
   IF(UNITOPEN) THEN
      IF(STAT.EQ.0) THEN
         CLOSE(UNITT,STATUS='DELETE',ERR=666)
      END IF
      IF(STAT.EQ.1) THEN
         CLOSE(UNITT,STATUS='KEEP',ERR=777)
      END IF
   END IF
   RETURN
666 CONTINUE
   WRITE(OUTLYNE,*)&
   &'An error was encountered in closing the file:',&
   &TRIM(FILE_NAME),' with status = "DELETE"'
   CALL SHOWIT(1)
   WRITE(OUTLYNE,*)&
   &'Try deleting the file from the operating system level'&
   &,' before proceeding.'
   CALL SHOWIT(1)
   CALL MACFAL
   RETURN
777 CONTINUE
   WRITE(OUTLYNE,*)&
   &'An error was encountered in closing the file:',&
   &TRIM(FILE_NAME),' with status = "KEEP"'
   CALL SHOWIT(1)
   WRITE(OUTLYNE,*)&
   &'Try deleting the file from the operating system level'&
   &,' before proceeding.'
   CALL SHOWIT(1)
   CALL MACFAL
   RETURN
   RETURN
END
