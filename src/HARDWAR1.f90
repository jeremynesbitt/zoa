SUBROUTINE SHOWIT(II)
!     THIS REPLACES CALLES TO "WRITE" EVERYWHERE IN THE PROGRAM
!     II=0 MEANS THE OUTPUT IS TO UNIT=OUT (79 COL OUTPUT)
!     II=2 MEANS THE OUTPUT IS TO UNIT=OUT (139 COL OUTPUT)
!     II=1 MEANS THE OUTPUT IS A MESSAGE TO 6 UNLESS OUT IS NULL
!     II=4 MEANS USE $ EDITING
!     II=5 MEANS OUTPUT TO UNIT 6 ONLY
!     II=10 MEANS II=0 BUT STRIP ALL BLANKS INFRONT OF COMMAS AND OTHER STUFF
!     OPERATIONS, USED ONLY IN LENO
!     II=11 MEANS II=10 BUT NO STRIP OPERATIONS, USED ONLY IN LENO
!     II = 19 DEBUG
!
   use global_widgets, only: debug_messages, ioConfig
   use zoa_output


   use DATMAI
   IMPLICIT NONE
!
   CHARACTER JK_J1*20,BFRM*3,JK_BLLIN*139,OLYNE*140,A1*1,A2*2,A3*3
!
   CHARACTER FFORM*10
!
   CHARACTER*140 STRIPOUT
   CHARACTER*20 txtColor
!
   INTEGER OI,II,I,OUTY,FLEN,LL,MY_LEN,IPASS1
!
   LOGICAL OPENOUT
!
   COMMON/SHOWME/FLEN,BFRM
!
   EXTERNAL MY_LEN
!
   I=II

   !PRINT *, "SHOW IT SUBROUTINE"

   !PRINT *, OUTLYNE

!    TODO:  Abstract this so it works with different outputs.
!           for now hack to print to terminal
!      WRITE (*, '(a)') 'Call to SHOWIT'
   IF (II.LT.2) THEN
      !PRINT *, OUTLYNE
!      Respect PROCESSILENT suppression: when dumpText is TRUE, ioConfig has
!      redirected output to the KDPDUMP sink (suppress mode). Skip zoa_emit so
!      that headless mode matches GUI behaviour (updateTerminalLog also skips
!      when textView is c_null_ptr, i.e. the KDPDUMP sink).
      if (.not. ioConfig%dumpText) then
         call zoa_emit(trim(OUTLYNE),&
         &"black")
      end if
      !call pending_events()
   END IF

   IF (II.EQ.19) THEN

      if (debug_messages) THEN

         txtColor = "red"
         !PRINT *, OUTLYNE
         call zoa_emit(OUTLYNE, txtColor)
      else
         ! Flush OUTLYNE b/c even with this check some debug messages were
         ! getting through on some futue SHOWIT call.  It means there are bad
         ! SHOWit calls in the code, but too hard to find so this is a band aid
         OUTLYNE = ""
      end IF


      !call pending_events()
      RETURN

   END IF


!       ADDED 1/20/2004
   IF(OUTLYNE(1:1).EQ.' ') OUTLYNE(1:139)=OUTLYNE(2:139)//' '
   OLYNE=TRIM(OUTLYNE)
   LL=MY_LEN(OLYNE)
   IF(LL.LT.10) THEN
      WRITE(A1,FMT='(I1)') LL
      FFORM=TRIM('A'//A1)
   END IF
   IF(LL.GT.9.AND.LL.LT.100) THEN
      WRITE(A2,FMT='(I2)') LL
      FFORM=TRIM('A'//A2)
   END IF
   IF(LL.GT.99.AND.LL.LT.1000) THEN
      WRITE(A3,'(I3)') LL
      FFORM=TRIM('A'//A3)
   END IF
   FFORM='('//TRIM(FFORM)//')'
   !PRINT *, "FFORM is ", FFORM
!
!     NO OUTPUT CAN EVER BE DONE TO 0 OR 1 WHEN OUTPUT NULL IS IN EFFECT
   IF(I.EQ.1.AND.OUT.EQ.98.OR.I.EQ.0.AND.OUT.EQ.98) RETURN
!
   IF(I.EQ.3) I=1
!
   IF(F15.EQ.1) THEN
!     CHANGING CONFIGS, NO SCREEN OUTPUT
      IF(I.EQ.0.AND.OUT.EQ.6.OR.&
      &I.EQ.2.AND.OUT.EQ.6.OR.&
      &I.EQ.0.AND.OUT.EQ.69.OR.&
      &I.EQ.2.AND.OUT.EQ.69.OR.&
      &I.EQ.1.OR.&
      &I.EQ.4.OR.&
      &I.EQ.5.OR.I.EQ.10.AND.OUT.EQ.6 &
      &.OR.I.EQ.10.AND.OUT.EQ.6) RETURN
   END IF
!
   IF(I.EQ.20) THEN
!       OUTPUT INTENDED FOR UNIT=OUT (256 COL), UNIT 97
!       USED ONLY IN COMMA DELIMITED TABLE OUTPUT
      PRINT *, "IN RIGHT Block of SHOWIT"
      PRINT *, "OUTLYNE_LONG IS ", OUTLYNE_LONG
      call zoa_emit(trim(OUTLYNE_LONG), "black")

      WRITE(97,2000) OUTLYNE_LONG(1:256)
2000  FORMAT(A256)
   END IF
!
   IF(I.EQ.10) THEN
      !PRINT *, "LENO Output Activated!"
      I=0
      STRIPOUT=OUTLYNE
      IPASS1=139
      CALL NO_ZEROS(STRIPOUT,IPASS1)
      OUTLYNE=STRIPOUT
      STRIPOUT=OUTLYNE
      IPASS1=139
      CALL ONE_BLANK(STRIPOUT,IPASS1)
      OUTLYNE=STRIPOUT
      !call updateTerminalLog(OUTLYNE, "black")
   END IF
   IF(I.EQ.11) THEN
      I=0
   END IF
!
!     IF SHOWIT GETS CALLED WHILE READING IN A LENS, DON'T DO
!     ANYTHING, JUST RETURN
   IF(IN.EQ.99.AND.OUT.EQ.99) RETURN
!
   JK_J1='                    '
   JK_BLLIN=JK_J1//JK_J1//JK_J1//JK_J1//JK_J1//JK_J1//JK_J1
!
   IF(OUT.EQ.69) WRITE(97,FMT=FFORM) OLYNE
!
   IF(I.EQ.3) OUTY=6
   IF(I.NE.3) OUTY=OUT
   OPENOUT=.FALSE.
   IF(OUTY.NE.6) INQUIRE(UNIT=OUTY,OPENED=OPENOUT)
   IF(.NOT.OPENOUT) OUTY=6
!     RESETTING I TO 0 OR 2
   IF(I.NE.1.AND.I.NE.4.AND.I.NE.5) THEN
!     NOT A SCREEN MESSAGE
      IF(OUTY.EQ.6.OR.OUTY.EQ.7) THEN
!     79 COL OUTPUT
         I=0
      ELSE
!     139 COL OUTPUT
         I=2
      END IF
   END IF
!
   IF(I.EQ.0) THEN
!       OUTPUT INTENDED FOR UNIT=OUT (79 COL)
      IF(OUT.EQ.6.OR.OUT.EQ.69.OR.OUT.EQ.7) THEN
         IF(OUT.EQ.6.OR.OUT.EQ.69) THEN
            IPASS1=79
! WINTER
!        Call MY_IWinOutStringXYSCROLL(OUTLYNE(1:IPASS1),IPASS1)
         ELSE
!     OUT IS NOT 6
            WRITE(OUTY,FMT=FFORM) OLYNE
            IF(OUTY.EQ.6) THEN
               IPASS1=79
! WINTER
!        Call MY_IWinOutStringXYSCROLL(OUTLYNE(1:IPASS1),IPASS1)
            END IF
         END IF
      ELSE
         IF(I.NE.5) THEN
            WRITE(OUTY,FMT=FFORM) OLYNE
            IF(OUTY.EQ.6) THEN
               IPASS1=79
            END IF
         END IF
      END IF
      OUTLYNE=JK_BLLIN
      RETURN
   END IF
   IF(I.EQ.2) THEN
!       OUTPUT INTENDED FOR UNIT=OUT (139 COL)
      IF(OUT.EQ.6.OR.OUT.EQ.69.OR.OUT.EQ.7) THEN
         LL=MY_LEN(OUTLYNE)
         LL=131
         IF(OUT.EQ.6.OR.OUT.EQ.69) THEN
! WINTER
!        Call MY_IWinOutStringXYSCROLL(OUTLYNE(1:LL),LL)
         ELSE
            LL=MY_LEN(OUTLYNE)
            WRITE(OUTY,FFORM) OLYNE
            IF(OUTY.EQ.6) THEN
               IPASS1=79
! WINTER
!        Call MY_IWinOutStringXYSCROLL(OUTLYNE(1:IPASS1),IPASS1)
            END IF
         END IF
      ELSE
         IF(I.NE.5) THEN
            LL=MY_LEN(OUTLYNE)
            IF (LL.NE.0) WRITE(OUTY,FMT=FFORM) OLYNE
            IF(OUTY.EQ.6) THEN
               IPASS1=79
! WINTER
!        Call MY_IWinOutStringXYSCROLL(OUTLYNE(1:IPASS1),IPASS1)
            END IF
            LL=MY_LEN(OUTLYNE)
         END IF
      END IF
      OUTLYNE=JK_BLLIN
      RETURN
   END IF
!
   IF(I.EQ.5) THEN
!       A MESSAGE INTENDED FOR THE SCREEN ONLY (UNIT 6 EXCLUSIVELY)
      IPASS1=79
! WINTER
!        Call MY_IWinOutStringXYSCROLL(OUTLYNE(1:IPASS1),IPASS1)
      OUTLYNE=JK_BLLIN
      RETURN
   END IF
!
   IF(I.EQ.1) THEN
!       A MESSAGE INTENDED FOR THE SCREEN ONLY
      IPASS1=79
! WINTER
!        Call MY_IWinOutStringXYSCROLL(OUTLYNE(1:IPASS1),IPASS1)
      OUTLYNE=JK_BLLIN
      RETURN
   END IF
   IF(I.EQ.4) THEN
!       A MESSAGE INTENDED FOR THE SCREEN WITHOUT A CARRIAGE CONTROL/LF
! WINTER
!        Call MY_IWinOutStringXYSCROLL(OUTLYNE(1:FLEN),FLEN)
      OUTLYNE=JK_BLLIN
      RETURN
   END IF
   IF(I.NE.5) OUTLYNE=JK_BLLIN
   RETURN
END
