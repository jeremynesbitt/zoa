SUBROUTINE COLORS
!
!               COLORS AND THEIR INTEGER CODES
!
!               0 = WHITE
!               1 = LIGHT YELLOW
!               2 = LIGHT MAGENTA
!               3 = LIGHT RED
!               4 = LIGHT CYAN
!               5 = LIGHT GREEN
!               6 = LIGHT BLUE
!               7 = DARK GREY
!               8 = LIGHT GREY
!               9 = DARK YELLOW
!              10 = DARK MAGENTA
!              11 = DARK RED
!              12 = DARK CYAN
!              13 = DARK GREEN
!              14 = DARK BLUE
!              15 = BLACK
!
   use DATHGR
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE COLORS. THIS IS THE SUBROUTINE WHICH
!       SETS THE PROGRAM COLORS.
!
   INTEGER COLANS,JK_ICOL,N
!
   CHARACTER JK_AICOL*2,COLCMD*11
!
!
   IF(STI.EQ.0) THEN
