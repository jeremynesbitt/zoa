   OPEN(UNIT=24,ACCESS='DIRECT',FILE=LIBPLO//'PLIB.DAT',&
   &FORM='UNFORMATTED',RECL=(90*NRECL),STATUS='UNKNOWN')
   J=0
   K=0
   DO 10 I=1,999
      READ(UNIT=24,REC=I) II,DATA
