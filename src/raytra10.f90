! SUB GLPRY.FOR
SUBROUTINE GLPRY
   USE GLOBALS
!
   use DATLEN
   use DATMAI
   use mod_surface, only: surf_thickness, surf_special_type
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE GLPRY.FOR. THIS SUBROUTINE IMPLEMENTS
!       CREATION OF GLOBAL RAY DATA FOR THE LAST RAY TRACED.
!       FOR RAY PLOTTING IT IS CALLED BY RAYTRA.FOR
!
   INTEGER JK,I,N_HITS
!
   REAL*8 GRX,GRY,GRZ,GRL,GRM,GRN,GRLP,GRMP,GRNP,&
   &X1,Y1,Z1,L1,M1,N1,X00,Y00,Z0,LX0,LY0,LZ0,&
   &MX0,MY0,MZ0,NX0,NY0,NZ0,LO1,MO1,NO1
!
!
   IF(DABS(surf_thickness(NEWOBJ)).LE.1.0D10) THEN
!       FINITE OBJECT THICKNESS
      JK=NEWOBJ
   ELSE
!       INFINITE OBJECT THICKNESS
      JK=NEWOBJ+1
   END IF
!
   DO 10 I=JK,NEWIMG
!
!       NOW TRANSFORM TO THE GLOBAL COORDINATE SYSTEM.
!       THE GLOBAL COORDINATES OF EACH LOCAL COORDINATE
!       SYSTEM ARE:
!
      X00=VERTEX(1,I)
      Y00=VERTEX(2,I)
      Z0=VERTEX(3,I)
      LX0=VERTEX(4,I)
      MX0=VERTEX(5,I)
      NX0=VERTEX(6,I)
      LY0=VERTEX(7,I)
      MY0=VERTEX(8,I)
      NY0=VERTEX(9,I)
      LZ0=VERTEX(10,I)
      MZ0=VERTEX(11,I)
      NZ0=VERTEX(12,I)
!
      X1=X00+((LX0*(RAYRAY(1,I)))+(LY0*(RAYRAY(2,I)))&
      &+(LZ0*(RAYRAY(3,I))))
      Y1=Y00+((MX0*(RAYRAY(1,I)))+(MY0*(RAYRAY(2,I)))&
      &+(MZ0*(RAYRAY(3,I))))
      Z1=Z0+((NX0*(RAYRAY(1,I)))+(NY0*(RAYRAY(2,I)))&
      &+(NZ0*(RAYRAY(3,I))))
!
      L1=((LX0*(RAYRAY(4,I)))+(LY0*(RAYRAY(5,I)))&
      &+(LZ0*(RAYRAY(6,I))))
      M1=((MX0*(RAYRAY(4,I)))+(MY0*(RAYRAY(5,I)))&
      &+(MZ0*(RAYRAY(6,I))))
      N1=((NX0*(RAYRAY(4,I)))+(NY0*(RAYRAY(5,I)))&
      &+(NZ0*(RAYRAY(6,I))))
!
      LO1=((LX0*(RAYRAY(19,I)))+(LY0*(RAYRAY(20,I)))&
      &+(LZ0*(RAYRAY(21,I))))
      MO1=((MX0*(RAYRAY(19,I)))+(MY0*(RAYRAY(20,I)))&
      &+(MZ0*(RAYRAY(21,I))))
      NO1=((NX0*(RAYRAY(19,I)))+(NY0*(RAYRAY(20,I)))&
      &+(NZ0*(RAYRAY(21,I))))
!
      GLPRAY(1,I)=X1
      GLPRAY(2,I)=Y1
      GLPRAY(3,I)=Z1
      GLPRAY(4,I)=L1
      GLPRAY(5,I)=M1
      GLPRAY(6,I)=N1
      GLPRAY(7,I)=LO1
      GLPRAY(8,I)=MO1
      GLPRAY(9,I)=NO1
      GLVIRT(I)=.FALSE.
!     IF THE CURRENT SURFACE IS A TRUE DUMMY
      IF(DUM(I).AND.surf_special_type(I).EQ.0 &
      &.AND.I.NE.NEWIMG) GLVIRT(I)=.TRUE.
      IF(I.EQ.NEWIMG) GLVIRT(I)=.FALSE.

!       THE CURRENT ENTRY FOR GLPRY REPRESENTS THE RAY AT THE BEGINNING
!       OF AN NSS OR GRIN ENVIRONMENT. NOW POPULATE THE ARRAY
!       GLOBAL_MULTIRAY_DATA STORED AT I TO REPRESENT THE INTERMEDIATE
!       RAY COORDINATES BETWEEN I AND I+1
      IF(I.GE.1) THEN
         IF(NUMHITS(I-1).GT.1) THEN
            DO N_HITS=1,NUMHITS(I-1)
               X00=VERTEX(1,I-1)
               Y00=VERTEX(2,I-1)
               Z0=VERTEX(3,I-1)
               LX0=VERTEX(4,I-1)
               MX0=VERTEX(5,I-1)
               NX0=VERTEX(6,I-1)
               LY0=VERTEX(7,I-1)
               MY0=VERTEX(8,I-1)
               NY0=VERTEX(9,I-1)
               LZ0=VERTEX(10,I-1)
               MZ0=VERTEX(11,I-1)
               NZ0=VERTEX(12,I-1)
               GRX=MULTI_DATA(1,I-1,N_HITS)
               GRY=MULTI_DATA(2,I-1,N_HITS)
               GRZ=MULTI_DATA(3,I-1,N_HITS)
               GRL=MULTI_DATA(4,I-1,N_HITS)
               GRM=MULTI_DATA(5,I-1,N_HITS)
               GRN=MULTI_DATA(6,I-1,N_HITS)
               GRLP=MULTI_DATA(19,I-1,N_HITS)
               GRMP=MULTI_DATA(20,I-1,N_HITS)
               GRNP=MULTI_DATA(21,I-1,N_HITS)
               X1=X00+((LX0*(GRX))+(LY0*(GRY))&
               &+(LZ0*(GRZ)))
               Y1=Y00+((MX0*(GRX))+(MY0*(GRY))&
               &+(MZ0*(GRZ)))
               Z1=Z0+((NX0*(GRX))+(NY0*(GRY))&
               &+(NZ0*(GRZ)))
!
               L1=((LX0*(GRL))+(LY0*(GRM))&
               &+(LZ0*(GRN)))
               M1=((MX0*(GRL))+(MY0*(GRM))&
               &+(MZ0*(GRN)))
               N1=((NX0*(GRL))+(NY0*(GRM))&
               &+(NZ0*(GRN)))
!
               LO1=((LX0*(GRLP))+(LY0*(GRMP))&
               &+(LZ0*(GRNP)))
               MO1=((MX0*(GRLP))+(MY0*(GRMP))&
               &+(MZ0*(GRNP)))
               NO1=((NX0*(GRLP))+(NY0*(GRMP))&
               &+(NZ0*(GRNP)))
!
               GLOBAL_MULTIRAY_DATA(1,I-1,N_HITS)=X1
               GLOBAL_MULTIRAY_DATA(2,I-1,N_HITS)=Y1
               GLOBAL_MULTIRAY_DATA(3,I-1,N_HITS)=Z1
               GLOBAL_MULTIRAY_DATA(4,I-1,N_HITS)=L1
               GLOBAL_MULTIRAY_DATA(5,I-1,N_HITS)=M1
               GLOBAL_MULTIRAY_DATA(6,I-1,N_HITS)=N1
               GLOBAL_MULTIRAY_DATA(19,I-1,N_HITS)=LO1
               GLOBAL_MULTIRAY_DATA(20,I-1,N_HITS)=MO1
               GLOBAL_MULTIRAY_DATA(21,I-1,N_HITS)=NO1
               GLOBAL_MULTIREF_DATA(1,I-1,N_HITS)=X1
               GLOBAL_MULTIREF_DATA(2,I-1,N_HITS)=Y1
               GLOBAL_MULTIREF_DATA(3,I-1,N_HITS)=Z1
               GLOBAL_MULTIREF_DATA(4,I-1,N_HITS)=L1
               GLOBAL_MULTIREF_DATA(5,I-1,N_HITS)=M1
               GLOBAL_MULTIREF_DATA(6,I-1,N_HITS)=N1
               GLOBAL_MULTIREF_DATA(19,I-1,N_HITS)=LO1
               GLOBAL_MULTIREF_DATA(20,I-1,N_HITS)=MO1
               GLOBAL_MULTIREF_DATA(21,I-1,N_HITS)=NO1
            END DO
         END IF
      END IF

10 CONTINUE
!       ALL GLOBAL TRANSFORMATIONS ARE COMPLETE
!
   RETURN
END
! SUB GLOBAL.FOR
SUBROUTINE GLOBAL
!
   use DATLEN
   use DATMAI
   use mod_surface, only: surf_thickness
   use mod_system, only: sys_units, sys_last_surf
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE GLOBAL. THIS SUBROUTINE IMPLEMENTS
!       THE CMD COMMANDS "GLOBAL", "OFFSET" AND "VERTEX".
!
   CHARACTER UNIT*11
!
   INTEGER SF,I,JK
!
!
   IF(sys_units().EQ.1.0D0) UNIT='INCHES'
   IF(sys_units().EQ.2.0D0) UNIT='CENTIMETERS'
   IF(sys_units().EQ.3.0D0) UNIT='MILLIMETERS'
   IF(sys_units().EQ.4.0D0) UNIT='METERS'
!
   IF(WC.EQ.'GLOBAL') THEN
!
      IF(SQ.EQ.1.AND.S1.EQ.1) THEN
         WRITE(OUTLYNE,*)&
         &'"',WC(1:6),&
         &'" TAKES EITHER QUALIFIER OR NUMERIC WORD #1 INPUT NOT BOTH'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(SQ.EQ.1.AND.WQ.NE.'OFF') THEN
         WRITE(OUTLYNE,*)'INVALID QUALIFIER WORD USED WITH "',&
         &WC(1:6),'" COMMAND'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
         WRITE(OUTLYNE,*)'"',WC(1:6),&
         &'" ONLY TAKES QUALIFIER OR NUMERIC WORD #1 INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(S1.EQ.1.AND.INT(W1).LT.NEWOBJ.OR.S1.EQ.1.AND.&
      &INT(W1).GT.NEWIMG) THEN
         WRITE(OUTLYNE,*)'SURFACE NUMBER BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(DF1.EQ.1.AND.DF2.EQ.1.AND.DF3.EQ.1.AND.SQ.EQ.0.AND.&
      &DF4.EQ.1.AND.DF5.EQ.1) THEN
!       PRINT CURRENT GLOBAL STATUS
         IF(.NOT.GLOBE) THEN
            WRITE(OUTLYNE,100)
            CALL SHOWIT(0)
100         FORMAT('"GLOBAL" IS CURRENTLY SET TO "OFF"')
!                       RETURN
         END IF
         IF(GLOBE) THEN
            WRITE(OUTLYNE,101) GLSURF
            CALL SHOWIT(0)
101         FORMAT(&
            &'"GLOBAL" IS CURRENTLY "ON", REFERENCED TO SURFACE '&
            &,I3)
            WRITE(OUTLYNE,102) OFFX,UNIT
            CALL SHOWIT(0)
            WRITE(OUTLYNE,103) OFFY,UNIT
            CALL SHOWIT(0)
            WRITE(OUTLYNE,104) OFFZ,UNIT
            CALL SHOWIT(0)
            WRITE(OUTLYNE,105) OFFA
            CALL SHOWIT(0)
            WRITE(OUTLYNE,106) OFFB
            CALL SHOWIT(0)
            WRITE(OUTLYNE,107) OFFC
            CALL SHOWIT(0)
102         FORMAT('WITH X-OFFSET   = ',G18.10,1X,A11)
103         FORMAT('WITH Y-OFFSET   = ',G18.10,1X,A11)
104         FORMAT('WITH Z-OFFSET   = ',G18.10,1X,A11)
105         FORMAT('WITH ALPHA TILT = ',G18.10,' DEGREES')
106         FORMAT('WITH BETA  TILT = ',G18.10,' DEGREES')
107         FORMAT('WITH GAMMA TILT = ',G18.10,' DEGREES')
!                       RETURN
         END IF
      END IF
      IF(WQ.EQ.'OFF') THEN
         IF(GLOBE) THEN
!       SET REFERENCE AND OTHER RAY STATUS TO FALSE
            FOBYES=.FALSE.
            REFEXT=.FALSE.
            SPDEXT=.FALSE.
            GSPDEXT=.FALSE.
            CPFNEXT=.FALSE.
            CALL DELPSF
            RAYEXT=.FALSE.
            GLOBE=.FALSE.
            GLSURF=0
            OFFX=0.0D0
            OFFY=0.0D0
            OFFZ=0.0D0
            OFFA=0.0D0
            OFFB=0.0D0
            OFFC=0.0D0
            RETURN
         END IF
         IF(.NOT.GLOBE) THEN
            RETURN
         END IF
      END IF
      IF(S1.EQ.1) THEN
         IF(DABS(surf_thickness(INT(W1))).GT.1.0D10.AND.INT(W1)&
         &.NE.NEWOBJ) THEN
            WRITE(OUTLYNE,*)&
            &'THE GLOBAL SURFACE SPECIFIED ,SURFACE NUMBER ',INT(W1)
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)&
            &'HAS AN INFINITE THICKNESS ( MAGNITUDE GREATER THAN 1.0D+10)'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)&
            &'"GLOBAL" WILL YIELD UNCERTAIN RESULTS IN THIS CASE'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)&
            &'ADJUST THICKNESSES AND RE-ENTER THE "GLOBAL" COMMAND'
            CALL SHOWIT(1)
            REFEXT=.FALSE.
            SPDEXT=.FALSE.
            GSPDEXT=.FALSE.
            CPFNEXT=.FALSE.
            CALL DELPSF
            FOBYES=.FALSE.
            RAYEXT=.FALSE.
            GLOBE=.FALSE.
            CALL MACFAL
            RETURN
         END IF
         IF(DABS(surf_thickness(INT(W1))).GT.1.0D10.AND.INT(W1)&
         &.EQ.NEWOBJ) THEN
            WRITE(OUTLYNE,*)&
            &'GLOBAL SURFACE SELECTED WAS THE CURRENT OBJECT SURFACE'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)&
            &'IT HAS AN INFINITE THICKNESS ( MAGNITUDE GREATER THAN 1.0D+10)'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)&
            &'"GLOBAL" WILL YIELD UNCERTAIN RESULTS IN THIS CASE'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)&
            &'RE-ENTER "GLOBAL" WITH A DIFFERENT SURFACE NUMBER'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)&
            &'OR A REDUCED OBJECT THICKNESS'
            CALL SHOWIT(1)
            REFEXT=.FALSE.
            SPDEXT=.FALSE.
            GSPDEXT=.FALSE.
            CPFNEXT=.FALSE.
            CALL DELPSF
            FOBYES=.FALSE.
            RAYEXT=.FALSE.
            GLOBE=.FALSE.
            CALL MACFAL
            RETURN
         END IF
!       CHECK FOR AND THICKNESSES GT 1.0D10
         DO 1010 I=NEWOBJ+1,NEWIMG
            IF(DABS(surf_thickness(I)).GT.1.0D10) THEN
               WRITE(OUTLYNE,*)&
               &'SURFACE NUMBER ',I
               CALL SHOWIT(1)
               WRITE(OUTLYNE,*)&
               &'HAS AN INFINITE THICKNESS ( MAGNITUDE GREATER THAN 1.0D+10)'
               CALL SHOWIT(1)
               WRITE(OUTLYNE,*)&
               &'"GLOBAL" WILL YIELD UNCERTAIN RESULTS IN THIS CASE'
               CALL SHOWIT(1)
               WRITE(OUTLYNE,*)&
               &'ADJUST THICKNESSES AND RE-ENTER THE "GLOBAL" COMMAND'
               CALL SHOWIT(1)
               REFEXT=.FALSE.
               SPDEXT=.FALSE.
               GSPDEXT=.FALSE.
               CPFNEXT=.FALSE.
               CALL DELPSF
               FOBYES=.FALSE.
               RAYEXT=.FALSE.
               GLOBE=.FALSE.
               CALL MACFAL
               RETURN
            END IF
1010     CONTINUE
         REFEXT=.FALSE.
         SPDEXT=.FALSE.
         GSPDEXT=.FALSE.
         CPFNEXT=.FALSE.
         CALL DELPSF
         FOBYES=.FALSE.
         RAYEXT=.FALSE.
         GLOBE=.TRUE.
         GLSURF=INT(W1)
         OFFX=0.0D0
         OFFY=0.0D0
         OFFZ=0.0D0
         OFFA=0.0D0
         OFFB=0.0D0
         OFFC=0.0D0
         CALL GLVERT
         RETURN
      END IF
   END IF
!
   IF(WC.EQ.'OFFSET') THEN
!
      IF(.NOT.GLOBE) THEN
         WRITE(OUTLYNE,198)
         CALL SHOWIT(0)
199      FORMAT('"GLOBAL" IS CURRENTLY SET TO "OFF"')
         WRITE(OUTLYNE,199)WC,WQ
         CALL SHOWIT(0)
198      FORMAT(A6,' ',A4,&
         &' IS NOT A VALID COMMAND  WHEN "GLOBAL" IS SET TO "OFF"')
         RETURN
      END IF
      IF(WQ.NE.'DEC'.AND.WQ.NE.'TILT') THEN
         WRITE(OUTLYNE,*)'INVALID QUALIFIER WORD USED WITH "',&
         &WC(1:6),'" COMMAND'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(S4.EQ.1.OR.S5.EQ.1.OR.SST.EQ.1) THEN
         WRITE(OUTLYNE,*)'"',WC(1:6),' ',WQ(1:4),&
         &'" TAKES NO STRING OR NUMERIC WORD 4 OR 5 INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(DF1.EQ.1.AND.DF2.EQ.1.AND.DF3.EQ.1.AND.SQ.EQ.0.AND.&
      &DF4.EQ.1.AND.DF5.EQ.1) THEN
!       PRINT CURRENT OFFSET STATUS
         IF(.NOT.GLOBE) THEN
            WRITE(OUTLYNE,200)
            CALL SHOWIT(0)
200         FORMAT(&
            &'"GLOBAL" IS CURRENTLY SET TO "OFF", NO OFFSETS CAN EXIST')
            RETURN
         END IF
         IF(GLOBE) THEN
            WRITE(OUTLYNE,201) GLSURF
            CALL SHOWIT(0)
201         FORMAT(&
            &'"GLOBAL" IS CURRENTLY "ON", REFERENCED TO SURFACE '&
            &,I3)
            WRITE(OUTLYNE,220)
            CALL SHOWIT(0)
220         FORMAT('CURRENT OFFSET STATUS IS:')
            WRITE(OUTLYNE,202) OFFX,UNIT
            CALL SHOWIT(0)
            WRITE(OUTLYNE,203) OFFY,UNIT
            CALL SHOWIT(0)
            WRITE(OUTLYNE,204) OFFZ,UNIT
            CALL SHOWIT(0)
            WRITE(OUTLYNE,205) OFFA
            CALL SHOWIT(0)
            WRITE(OUTLYNE,206) OFFB
            CALL SHOWIT(0)
            WRITE(OUTLYNE,207) OFFC
            CALL SHOWIT(0)
202         FORMAT('WITH X-OFFSET   = ',G18.10,1X,A11)
203         FORMAT('WITH Y-OFFSET   = ',G18.10,1X,A11)
204         FORMAT('WITH Z-OFFSET   = ',G18.10,1X,A11)
205         FORMAT('WITH ALPHA TILT = ',G18.10,' DEGREES')
206         FORMAT('WITH BETA  TILT = ',G18.10,' DEGREES')
207         FORMAT('WITH GAMMA TILT = ',G18.10,' DEGREES')
!                       RETURN
         END IF
      END IF
      IF(DF1.EQ.1.AND.DF2.EQ.1.AND.DF3.EQ.1.AND.SQ.EQ.0.AND.&
      &DF4.EQ.1.AND.DF5.EQ.1) THEN
!      OFFSET REQUIRES A QUALIFIER WITH NUMERIC INPUT
         WRITE(OUTLYNE,*)&
         &'"OFFSET" REQUIRES A QUALIFIER WITH NUMERIC INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(WQ.EQ.'DEC') THEN
         RAYEXT=.FALSE.
         REFEXT=.FALSE.
         SPDEXT=.FALSE.
         GSPDEXT=.FALSE.
         CPFNEXT=.FALSE.
         CALL DELPSF
         FOBYES=.FALSE.
         IF(DF1.EQ.1) W1=0.0D0
         IF(DF2.EQ.1) W2=0.0D0
         IF(DF3.EQ.1) W3=0.0D0
         OFFX=W1
         OFFY=W2
         OFFZ=W3
!       GLVERT.FOR CALCULATES THE X,Y,Z OF EACH SURFACE
!       VERTEX IN THE COORDINATE SYSTEM OF THE GLOBAL REFERENCE
!       COORDINATE SYSTEM AND ALSO THE L,M AND N OF THE X,Y AND Z
!       AXES AT EACH SURFACE VERTEX IN THE COORDINATE SYSTEM
!       OF THE GLOBAL REFERENCE POINT.
         CALL GLVERT
         RETURN
      END IF
      IF(WQ.EQ.'TILT') THEN
         RAYEXT=.FALSE.
         REFEXT=.FALSE.
         SPDEXT=.FALSE.
         GSPDEXT=.FALSE.
         CPFNEXT=.FALSE.
         CALL DELPSF
         FOBYES=.FALSE.
         IF(DF1.EQ.1) W1=0.0D0
         IF(DF2.EQ.1) W2=0.0D0
         IF(DF3.EQ.1) W3=0.0D0
         OFFA=W1
         OFFB=W2
         OFFC=W3
!       GLVERT.FOR CALCULATES THE X,Y,Z OF EACH SURFACE
!       VERTEX IN THE COORDINATE SYSTEM OF THE GLOBAL REFERENCE
!       COORDINATE SYSTEM AND ALSO THE L,M AND N OF THE X,Y AND Z
!       AXES AT EACH SURFACE VERTEX IN THE COORDINATE SYSTEM
!       OF THE GLOBAL REFERENCE POINT.
         CALL GLVERT
         RETURN
      END IF
!
   END IF

   IF(WC.EQ.'VERTEX') THEN
      IF(.NOT.GLOBE) THEN
!       NO VERTEX DATA TO OUTPUT
         WRITE(OUTLYNE,*)&
         &'NO CURRENT GLOBAL VERTEX DATA EXISTS TO OUTPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
!
      IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
         WRITE(OUTLYNE,*)&
         &'"VERTEX" ONLY TAKES QUALIFIER OR NUMERIC WORD #1 INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(SQ.EQ.1.AND.S1.EQ.1) THEN
         WRITE(OUTLYNE,*)&
         &'"VERTEX" TAKES EITHER QUALIFIER OR NUMERIC WORD #1 INPUT'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'BUT NOT BOTH'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(sys_last_surf().EQ.0.0D0) THEN
         WRITE(OUTLYNE,*)'LENS SYSTEM HAS NO SURFACES'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'NO GLOBAL VERTEX DATA CAN EXIST'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(SQ.EQ.1.AND.WQ.EQ.'ALL') THEN
!       PRINT WARNING FOR OBJECT THICKNESS INFINITE
         IF(DABS(surf_thickness(NEWOBJ)).GT.1.0D10)THEN
            WRITE(OUTLYNE,*)'WARNING:'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)&
            &'INFINITE OBJECT THICKNESS (MAGNITUDE GREATER THAN 1.0D+10'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)&
            &'OBJECT THICKNESS WAS IGNORED IN ALL GLOBAL CALCULATIONS'
            CALL SHOWIT(1)
            JK=NEWOBJ+1
         ELSE
            JK=NEWOBJ
         END IF
         SF=NEWIMG
         WRITE(OUTLYNE,5001) INT(F12)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,5002) GLSURF
         CALL SHOWIT(0)
         WRITE(OUTLYNE,102) OFFX,UNIT
         CALL SHOWIT(0)
         WRITE(OUTLYNE,103) OFFY,UNIT
         CALL SHOWIT(0)
         WRITE(OUTLYNE,104) OFFZ,UNIT
         CALL SHOWIT(0)
         WRITE(OUTLYNE,105) OFFA
         CALL SHOWIT(0)
         WRITE(OUTLYNE,106) OFFB
         CALL SHOWIT(0)
         WRITE(OUTLYNE,107) OFFC
         CALL SHOWIT(0)
         WRITE(OUTLYNE,2501)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,5000)
         CALL SHOWIT(0)
         DO 10 I=JK,SF
            WRITE(OUTLYNE,2000)I,VERTEX(1,I),VERTEX(2,I),VERTEX(3,I)
            CALL SHOWIT(0)
10       CONTINUE
         WRITE(OUTLYNE,2501)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,5003)
         CALL SHOWIT(0)
         DO 11 I=JK,SF
            WRITE(OUTLYNE,2000)I,VERTEX(4,I),VERTEX(5,I),VERTEX(6,I)
            CALL SHOWIT(0)
11       CONTINUE
         WRITE(OUTLYNE,2501)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,5004)
         CALL SHOWIT(0)
         DO 12 I=JK,SF
            WRITE(OUTLYNE,2000)I,VERTEX(7,I),VERTEX(8,I),VERTEX(9,I)
            CALL SHOWIT(0)
12       CONTINUE
         WRITE(OUTLYNE,2501)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,5005)
         CALL SHOWIT(0)
         DO 13 I=JK,SF
            WRITE(OUTLYNE,2000)I,VERTEX(10,I),VERTEX(11,I),VERTEX(12,I)
            CALL SHOWIT(0)
13       CONTINUE
         RETURN
      END IF
      IF(SQ.EQ.1.AND.WQ.EQ.'OBJ'.OR.SQ.EQ.1.AND.WQ.EQ.'OB') THEN
         SF=NEWOBJ
         IF(DABS(surf_thickness(SF)).GT.1.0D10) THEN
            WRITE(OUTLYNE,*)&
            &'NO GLOBAL RAY DATA EXISTS FOR THE OBJECT SURFACE'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)&
            &'WHICH HAS INFINTE THICKNESS (GREATER THAN 1.0D10)'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
         IF(HEADIN) THEN
            WRITE(OUTLYNE,5001) INT(F12)
            CALL SHOWIT(0)
            WRITE(OUTLYNE,5002) GLSURF
            CALL SHOWIT(0)
            WRITE(OUTLYNE,102) OFFX,UNIT
            CALL SHOWIT(0)
            WRITE(OUTLYNE,103) OFFY,UNIT
            CALL SHOWIT(0)
            WRITE(OUTLYNE,104) OFFZ,UNIT
            CALL SHOWIT(0)
            WRITE(OUTLYNE,105) OFFA
            CALL SHOWIT(0)
            WRITE(OUTLYNE,106) OFFB
            CALL SHOWIT(0)
            WRITE(OUTLYNE,107) OFFC
            CALL SHOWIT(0)
         END IF
         WRITE(OUTLYNE,2501)
         CALL SHOWIT(0)
         IF(HEADIN) WRITE(OUTLYNE,5000)
         IF(HEADIN) CALL SHOWIT(0)
         WRITE(OUTLYNE,1500)SF,VERTEX(1,SF),VERTEX(2,SF),VERTEX(3,SF)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,2501)
         CALL SHOWIT(0)
         IF(HEADIN) WRITE(OUTLYNE,5003)
         IF(HEADIN) CALL SHOWIT(0)
         WRITE(OUTLYNE,1500)SF,VERTEX(4,SF),VERTEX(5,SF),VERTEX(6,SF)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,2501)
         CALL SHOWIT(0)
         IF(HEADIN) WRITE(OUTLYNE,5004)
         IF(HEADIN) CALL SHOWIT(0)
         WRITE(OUTLYNE,1500)SF,VERTEX(7,SF),VERTEX(8,SF),VERTEX(9,SF)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,2501)
         CALL SHOWIT(0)
         IF(HEADIN) WRITE(OUTLYNE,5005)
         IF(HEADIN) CALL SHOWIT(0)
         WRITE(OUTLYNE,1500)SF,VERTEX(10,SF),VERTEX(11,SF),VERTEX(12,SF)
         CALL SHOWIT(0)
         RETURN
      END IF
      IF(SQ.EQ.1.AND.WQ.NE.'OBJ'.OR.SQ.EQ.1.AND.&
      &WQ.NE.'ALL'.OR.SQ.EQ.1.AND.WQ.NE.'OB') THEN
         WRITE(OUTLYNE,*)'INVALID QUALIFIER WORD'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      IF(SQ.EQ.0.AND.DF1.EQ.1) THEN
!       OUTPUT IMAGE SURFACE
         SF=NEWIMG
         IF(HEADIN) THEN
            WRITE(OUTLYNE,5001) INT(F12)
