         IF(surf_thickness(NEWIMG-1).EQ.0.0D0.OR.surf_thickness(NEWIMG-1).NE.&
         &0.0D0.AND..NOT.LDIF2) THEN
!       DIST FROM I-1 TO I IS ZERO OF LDIF2 IS FALSE AND DIST NOT 0
!       DO AN INTERNAL "ASTOP EX" ADJUSTMENT
            IF(DABS(PXTRAY(5,NEWIMG)*1.0D-15).LT.&
            &DABS(PXTRAY(6,NEWIMG))) THEN
               RAD=(PXTRAY(5,NEWIMG)/PXTRAY(6,NEWIMG))
            END IF
            IF(DABS(PXTRAY(5,NEWIMG)*1.0D-15).GE.&
            &DABS(PXTRAY(6,NEWIMG))) THEN
!       REF RAY IS TELECENTRIC, SET RAD = 1.0D20
               RAD=1.0D20
            END IF
!       USE EXISTING THICKNESS OF NEWIMG-1 AS RAD
         ELSE
            RAD=surf_thickness(NEWIMG-1)
         END IF
!
!       NOW WE HAVE LOCATION OF REFERENCE SPHERE CENTER
!       AND ITS RADIUS
!
!       THE CHIEF RAY INTERSECTS THE REFERENCE SPHERE
!       THE LENGTH FROM THIS INTERSECTION TO THE INTERSECTION
!       OF THE REFERENCE RAY WITH THE FINAL SURFACE IS RCOR=RAD
!       REFERENCE RAY INTERSECTION WITH THE TANGENT PLANE IS:
!
         RCOR=RAD
!
!       THE LENGTH FROM THE REFERENCE SPHERE
!       TO THE FINAL SURFACE ALONG THE "OTHER" RAY IS:
!
         AL=RAYRAY(1,NEWIMG)-XREFI
         BE=RAYRAY(2,NEWIMG)-YREFI
         GA=RAYRAY(3,NEWIMG)-ZREFI
         C=(AL**2)+(BE**2)+(GA**2)-(RAD**2)
         B=2.0D0*((RAYRAY(19,NEWIMG)*AL)+&
         &(RAYRAY(20,NEWIMG)*BE)+&
         &(RAYRAY(21,NEWIMG)*GA))
         A=1.0D0
!       SOLVE FOR LEN
         IF(B.NE.0.0D0) SIGNB=((DABS(B))/(B))
         IF(B.EQ.0.0D0) SIGNB=1.0D0
         IF(A.EQ.0.0D0) THEN
            LEN=-(C/B)
         ELSE
            ARG=((B**2)-(4.0D0*A*C))
            IF(ARG.LT.0.0D0) THEN
               OUTLYNE='RAY(S) COULD NOT INTERSECT REFERENCE SPHERE'
               CALL SHOWIT(1)
               OUTLYNE=&
               &'TRY USING AN "ASTOP EX" OR ASTOP "EN/EX" ADJUSTMENT'
               CALL SHOWIT(1)
               OUTLYNE=&
               &'OPD VALUES MAY BE SUSPECT'
               CALL SHOWIT(1)
               OCOR=0.0D0
               RCOR=0.0D0
               RETURN
!       REFERENCE SPHERE INTERSECTED
            END IF
            Q=(-0.5D0*(B+(SIGNB*(DSQRT((B**2)-(4.0D0*A*C))))))
            LEN1=C/Q
            LEN2=Q/A
            IF(REFRY(6,NEWIMG).GT.0.0D0) THEN
               IF(RAD.GT.0.0D0) THEN
!     THEN THE INTERSECTION TO USE IS THE NEGATIVE INTERSECTION
                  IF(LEN1.LT.0.0D0) LEN=LEN1
                  IF(LEN2.LT.0.0D0) LEN=LEN2
               ELSE
!       RAD NEG
!     THEN THE INTERSECTION TO USE IS THE POSITIVE INTERSECTION
                  IF(LEN1.GT.0.0D0) LEN=LEN1
                  IF(LEN2.GT.0.0D0) LEN=LEN2
               END IF
            END IF
            IF(REFRY(6,NEWIMG).LT.0.0D0) THEN
               IF(RAD.GT.0.0D0) THEN
!     THEN THE INTERSECTION TO USE IS THE NEGATIVE INTERSECTION
                  IF(LEN1.LT.0.0D0) LEN=-LEN2
                  IF(LEN2.LT.0.0D0) LEN=-LEN1
               ELSE
!       RAD NEG
!     THEN THE INTERSECTION TO USE IS THE POSITIVE INTERSECTION
                  IF(LEN1.GT.0.0D0) LEN=-LEN2
                  IF(LEN2.GT.0.0D0) LEN=-LEN1
               END IF
            END IF
         END IF
!       NEGATIVE SIGN SINCE LEN1 AND LEN2 MEASURES DIRECTED
!       DISTANCE FROM IMAGE TO SPHERE INTERSECTION AND
!       LEN IS DIRECTED FROM SPHERE INTERSECTION TO IMAGE

         OCOR=-LEN
         GO TO 900
!     EXPAUT NOT FALSE
      END IF
      IF(EXPAUT) THEN
!       THE REFERENCE SPHERE IS CENTERED WHERE THE REAL CHIEF
!       RAY CROSSES THE FINAL SURFACE.
!       THE RADIUS IS EQUAL
!       TO THE DISTANCE FROM POINT TO WHERE THE CHIEF RAY CROSSES THE REAL
!     EXIT PUPIL
!       THIS IS JUST AS IN CODE-V AND IS THE DEFAULT WHEN THE PROGRAM
!     BEGINS
!
!       COORDINATES OF THE REAL CHIEF RAY AT THE IMAGE PLANE ARE:
!       THIS IS THE REFERENCE SPHERE CENTER LOCATION.
         XREFI=REFRY(1,NEWIMG)
         YREFI=REFRY(2,NEWIMG)
         ZREFI=REFRY(3,NEWIMG)
!
!       DO A REAL RAY INTERNAL EXIT PUPIL ADJUSTMENT
!
         XO=REFRY(1,NEWIMG)
         YO=REFRY(2,NEWIMG)
         ZO=REFRY(3,NEWIMG)
         LO=REFRY(4,NEWIMG)
         MO=REFRY(5,NEWIMG)
         NO=REFRY(6,NEWIMG)
         XOOX=RFDIFF(1,NEWIMG)
         YOOX=RFDIFF(2,NEWIMG)
         ZOOX=RFDIFF(3,NEWIMG)
         LOOX=RFDIFF(4,NEWIMG)
         MOOX=RFDIFF(5,NEWIMG)
         NOOX=RFDIFF(6,NEWIMG)
         XOOY=RFDIFF(7,NEWIMG)
         YOOY=RFDIFF(8,NEWIMG)
         ZOOY=RFDIFF(9,NEWIMG)
         LOOY=RFDIFF(10,NEWIMG)
         MOOY=RFDIFF(11,NEWIMG)
         NOOY=RFDIFF(12,NEWIMG)
         THXNUM=((XO-XOOX)*(LO-LOOX))+((YO-YOOX)*(MO-MOOX))&
         &+((ZO-ZOOX)*(NO-NOOX))
         THYNUM=((XO-XOOY)*(LO-LOOY))+((YO-YOOY)*(MO-MOOY))&
         &+((ZO-ZOOY)*(NO-NOOY))
         THXDEN=((LO-LOOX)**2)+((MO-MOOX)**2)&
         &+((NO-NOOX)**2)
         THYDEN=((LO-LOOY)**2)+((MO-MOOY)**2)&
         &+((NO-NOOY)**2)
         IF(DABS(THXNUM*1.0D-15).LT.DABS(THXDEN).AND.&
         &DABS(THYNUM*1.0D-15).LT.&
         &DABS(THYDEN)) THEN
            RAD=-((-THXNUM/THXDEN)+(-THYNUM/THYDEN))/2.0D0
!     THE EXIT PUPIL IS LOCATED AT:
            XA=XO+(-RAD*LO)
            YA=YO+(-RAD*MO)
            ZA=ZO+(-RAD*NO)
            RAD=DSQRT(&
            &((XA-XREFI)**2)+((YA-YREFI)**2)+((ZA-ZREFI)**2)&
            &)
            IF(ZA.LE.ZO) THEN
!     PUPIL LIES ON THE NEGATIVE SIDE OF NEWIMG SO RAD IS POS
               RAD=RAD
            ELSE
               RAD=-RAD
            END IF
         END IF
         IF(DABS(THXNUM*1.0D-15).GE.DABS(THXDEN).OR.&
         &DABS(THYNUM*1.0D-15).GE.&
         &DABS(THYDEN)) THEN
!       REF RAY IS TELECENTRIC, SET RAD =1.0D20
            RAD=1.0D20
         END IF
!
!       NOW WE HAVE LOCATION OF REFERENCE SPHERE CENTER
!       AND ITS RADIUS MAGNITUDE
!
!       THE CHIEF RAY INTERSECTS THE REFERENCE SPHERE
!       THE LENGTH FROM THIS INTERSECTION TO THE INTERSECTION
!       OF THE REFERENCE RAY WITH THE FINAL SURFACE IS RCOR=RAD
!       REFERENCE RAY INTERSECTION WITH THE TANGENT PLANE IS:
!
         RCOR=RAD
!
!       THE LENGTH FROM THE REFERENCE SPHERE
!       TO THE FINAL SURFACE ALONG THE "OTHER" RAY IS:
!
         LLL=RAYRAY(19,NEWIMG)
         MMM=RAYRAY(20,NEWIMG)
         NNN=RAYRAY(21,NEWIMG)
         AL=RAYRAY(1,NEWIMG)-XREFI
         BE=RAYRAY(2,NEWIMG)-YREFI
         GA=RAYRAY(3,NEWIMG)-ZREFI
         C=(AL**2)+(BE**2)+(GA**2)-(RAD**2)
         B=2.0D0*((LLL*AL)+&
         &(MMM*BE)+&
         &(NNN*GA))
         A=1.0D0
!       SOLVE FOR LEN
         IF(B.NE.0.0D0) SIGNB=((DABS(B))/(B))
         IF(B.EQ.0.0D0) SIGNB=1.0D0
         IF(A.EQ.0.0D0) THEN
            LEN=-(C/B)
         ELSE
            ARG=((B**2)-(4.0D0*A*C))
            IF(ARG.LT.0.0D0) THEN
               OUTLYNE='RAY(S) COULD NOT INTERSECT REFERENCE SPHERE'
               CALL SHOWIT(1)
               OUTLYNE=&
               &'THE CURRENT SYSTEM MAY HAVE EXCESSIVE ABERRATIONS'
               CALL SHOWIT(1)
               OUTLYNE=&
               &'OPD VALUES MAY BE SUSPECT'
               CALL SHOWIT(1)
               OCOR=0.0D0
               RCOR=0.0D0
               RETURN
!       REFERENCE SPHERE INTERSECTED
            END IF
            Q=(-0.5D0*(B+(SIGNB*(DSQRT((B**2)-(4.0D0*A*C))))))
            LEN1=C/Q
            LEN2=Q/A
            IF(REFRY(6,NEWIMG).GT.0.0D0) THEN
               IF(RAD.GT.0.0D0) THEN
!     THEN THE INTERSECTION TO USE IS THE NEGATIVE INTERSECTION
                  IF(LEN1.LT.0.0D0) LEN=LEN1
                  IF(LEN2.LT.0.0D0) LEN=LEN2
               ELSE
!       RAD NEG
!     THEN THE INTERSECTION TO USE IS THE POSITIVE INTERSECTION
                  IF(LEN1.GT.0.0D0) LEN=LEN1
                  IF(LEN2.GT.0.0D0) LEN=LEN2
               END IF
            END IF
            IF(REFRY(6,NEWIMG).LT.0.0D0) THEN
               IF(RAD.GT.0.0D0) THEN
!     THEN THE INTERSECTION TO USE IS THE NEGATIVE INTERSECTION
                  IF(LEN1.LT.0.0D0) LEN=-LEN2
                  IF(LEN2.LT.0.0D0) LEN=-LEN1
               ELSE
!       RAD NEG
!     THEN THE INTERSECTION TO USE IS THE POSITIVE INTERSECTION
                  IF(LEN1.GT.0.0D0) LEN=-LEN2
                  IF(LEN2.GT.0.0D0) LEN=-LEN1
               END IF
            END IF
         END IF
!       NEGATIVE SIGN SINCE LEN1 AND LEN2 MEASURES DIRECTED
!       DISTANCE FROM IMAGE TO SPHERE INTERSECTION AND
!       LEN IS DIRECTED FROM SPHERE INTERSECTION TO IMAGE
         OCOR=-LEN
         GO TO 900
!     EXPAUT NOT TRUE
      END IF
      GO TO 900
!     NOT FOCAL
   END IF
900 CONTINUE
   IF(DABS(RCOR).GT.1.0D10.OR.DABS(OCOR).GT.1.0D10) THEN
!     INFINITE REFERENCE SPHERE
      RCOR=0.0D0
!     OCOR IS THE DISTANCE ALONG THE RAY BETWEEN AN INTERSECTION
!     WITH A PLANE PERPENDICULAR TO A PARALLEL RAY WHICH PASSES
!     THROUGH A POINT ON THE IMAGE SURFACE WHERE THE CHIEF RAY
!     HITS
      RCOR=0.0D0
      RX0=REFRY(1,NEWIMG-1)
      RY0=REFRY(2,NEWIMG-1)
      RZ0=REFRY(3,NEWIMG-1)
      RL0=RAYRAY(19,NEWIMG-1)
      RM0=RAYRAY(20,NEWIMG-1)
      RN0=RAYRAY(21,NEWIMG-1)
!       NOW THE OTHER RAY:
      RRX0=RAYRAY(1,NEWIMG-1)
      RRY0=RAYRAY(2,NEWIMG-1)
      RRZ0=RAYRAY(3,NEWIMG-1)
      L0=RAYRAY(19,NEWIMG-1)
      M0=RAYRAY(20,NEWIMG-1)
      N0=RAYRAY(21,NEWIMG-1)
!       THE LINE DIRECTION NUMBERS ARE:
!
!       THE PLANE WHICH HAS ITS ORIGIN WHERE THE
!       REFERENCE RAY INTERSECTS THE IMAGE PLANE
!       AND IS PERPENDICULAR TO THE REGULAR RAY HAS THE FORM:
!
!       (RL0*(X-RX0))+(RM0*(Y-RY0))+(RN0*(Z-RZ0))=0
!       X,Y AND Z ARE ANY POINTS ON THE PLANE.
!
!       NOW DETERMINE THE INTERSECTION OF AN "OTHER" RAY
!       WITH THIS REFERENCE PLANE.
!
!       THE EQUATION OF THE OTHER RAY IS
!               XA=RRX0+(T*L0)
!               YA=RRY0+(T*M0)
!               ZA=RRZ0+(T*N0)
! THE PARAMETER T IS DETERMINED BY SUBSTITUTING THIS LINE
!       EQUATION INTO THE REFERENCE SURAFCE EQUATION
!       AND SOLVING FOR T.
!
      T=((RL0*(RX0-RRX0))+(RM0*(RY0-RRY0))+(RN0*(RZ0-RRZ0)))/&
      &((RL0*L0)+(RM0*M0)+(RN0*N0))
!
!       NOW X,Y AND Z ARE:
      XA=RRX0+(T*L0)
      YA=RRY0+(T*M0)
      ZA=RRZ0+(T*N0)
      OCOR=DSQRT(&
      &((RAYRAY(1,NEWIMG-1)-XA)**2)+&
      &((RAYRAY(2,NEWIMG-1)-YA)**2)+&
      &((RAYRAY(3,NEWIMG-1)-ZA)**2))
!     IF T IS LESS THAN ZERO, THE RAY INTERSECTION WITH THE
!     REFERENCE PLANE LIES ON THE NEGATIVE SIDE OF THE IMAGE SURFACE
!     AND ZA LT RAYRAY(3,NEWIMG-1) AND OCOR MUST BE POSITIVE AND VICE VERSA.
!       THE CORRECT SIGN IS:
      IF(ZA.GT.RAYRAY(3,NEWIMG-1)) OCOR=-OCOR
      IF(ZA.LE.RAYRAY(3,NEWIMG-1)) OCOR=OCOR
   ELSE
   END IF
   RETURN
END
! SUB GPXT.FOR
SUBROUTINE GPXT
!
   use DATLEN
   use DATMAI
   use command_utils, only: is_command_query
   use mod_system, only: sys_wl_ref, sys_last_surf
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE GPXT. THIS SUBROUTINE IMPLEMENTS
!       THE GENERALIZED PARAXIAL RAY TRACE PRINTOUT COMMANDS
!       "GPXTX" AND "GPXTY"
!
   INTEGER SF,I,ERROR
!
   REAL*8 PX,PY,PCX,PCY,PUX,PUY,PUCX,PUCY,DWORD2,DWORD3 &
   &,DWORD4,DWORD5
!
!
   GPRAYEXT=.FALSE.
   IF(DF2.EQ.1) W2=0.0D0
   IF(DF3.EQ.1) W3=0.0D0
   IF(DF4.EQ.1) W4=0.0D0
   IF(DF5.EQ.1) W5=sys_wl_ref()
   DWORD2=W2
   DWORD3=W3
   DWORD4=W4
   DWORD5=W5
!
   IF(is_command_query()) THEN
      WRITE(OUTLYNE,*)&
      &'"GPXT(XorY)" OUTPUT GENERALIZED PARAXIAL DATA'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(SST.EQ.1) THEN
      WRITE(OUTLYNE,*)&
      &'GENERALIZED PARAXIAL RAYTRACE OUTPUT COMMANDS TAKE'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)&
      &'NO STRING INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(SQ.EQ.1.AND.S1.EQ.1) THEN
      WRITE(OUTLYNE,*)&
      &'GENERALIZED PARAXIAL RAYTRACE OUTPUT COMMANDS TAKE'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)&
      &'QUALIFIER OR NUMERIC WORD #1 INPUT BUT NOT BOTH'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(DWORD5.LT.1.0D0.OR.DWORD5.GT.10.0D0) THEN
      WRITE(OUTLYNE,*)'INVALID WAVELENGTH NUMBER ISSUED'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'NO GENERALIZED PARAXIAL DATA EXISTS'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(sys_last_surf().EQ.0.0) THEN
      WRITE(OUTLYNE,*)'LENS SYSTEM HAS NO SURFACES'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'NO GENERALIZED PARAXIAL DATA EXISTS'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(SQ.EQ.1.AND.WQ.EQ.'ALL') THEN
      CALL GNPRTGEN(0,PY,PX,PUY,PUX,PCY,PCX,PUCY,PUCX,ERROR &
      &,DWORD2,DWORD3,DWORD4,DWORD5,0)
      IF(ERROR.EQ.1) THEN
         WRITE(OUTLYNE,*)'RAY COULD NOT BE TRACED'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'DIFFERENTIAL RAY VALUES NOT CALCULABLE'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'NO ACTION TAKEN'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
         GPRAYEXT=.TRUE.
      END IF
      SF=INT(sys_last_surf())
      IF(WC.EQ.'GPXTX') WRITE(OUTLYNE,6001) INT(F12)
      IF(WC.EQ.'GPXTY') WRITE(OUTLYNE,6004) INT(F12)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6006) INT(DWORD5)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6007) DWORD2
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6008) DWORD3
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6009) DWORD4
      CALL SHOWIT(0)
      IF(WC.EQ.'GPXTX') WRITE(OUTLYNE,6002)
      IF(WC.EQ.'GPXTY') WRITE(OUTLYNE,6005)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,2501)
      CALL SHOWIT(0)
      IF(WC.EQ.'GPXTX') WRITE(OUTLYNE,6000)
      IF(WC.EQ.'GPXTY') WRITE(OUTLYNE,6003)
      CALL SHOWIT(0)
      DO 10 I=0,SF
         CALL GNPRTGEN(I,PY,PX,PUY,PUX,PCY,PCX,PUCY,PUCX,ERROR &
         &,DWORD2,DWORD3,DWORD4,DWORD5,0)
         IF(ERROR.EQ.1) THEN
            WRITE(OUTLYNE,*)'RAY COULD NOT BE TRACED'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'DIFFERENTIAL RAY VALUES NOT CALCULABLE'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)'NO ACTION TAKEN'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         ELSE
            GPRAYEXT=.TRUE.
         END IF
         GPXY(1,I)=PX
         GPXY(2,I)=PY
         GPXY(3,I)=PCX
         GPXY(4,I)=PCY
         IF(WC.EQ.'GPXTX') WRITE(OUTLYNE,1500)I,PX,PUX,PCX,PUCX
         IF(WC.EQ.'GPXTY') WRITE(OUTLYNE,1500)I,PY,PUY,PCY,PUCY
         CALL SHOWIT(0)
10    CONTINUE
      RETURN
   END IF
   IF(SQ.EQ.1.AND.WQ.EQ.'OBJ'.OR.SQ.EQ.1.AND.WQ.EQ.'OB') THEN
      SF=0
      CALL GNPRTGEN(SF,PY,PX,PUY,PUX,PCY,PCX,PUCY,PUCX,ERROR &
      &,DWORD2,DWORD3,DWORD4,DWORD5,0)
      IF(ERROR.EQ.1) THEN
         WRITE(OUTLYNE,*)'RAY COULD NOT BE TRACED'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'DIFFERENTIAL RAY VALUES NOT CALCULABLE'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'NO ACTION TAKEN'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
         GPRAYEXT=.TRUE.
      END IF
      IF(WC.EQ.'GPXTX')THEN
         IF(HEADIN) WRITE(OUTLYNE,6000)
         IF(HEADIN) CALL SHOWIT(0)
         WRITE(OUTLYNE,1500) SF,PX,PUX,PCX,PUCX
         CALL SHOWIT(0)
      END IF
      IF(WC.EQ.'GPXTY')THEN
         IF(HEADIN) WRITE(OUTLYNE,6003)
         IF(HEADIN) CALL SHOWIT(0)
         WRITE(OUTLYNE,1500) SF,PY,PUY,PCY,PUCY
         CALL SHOWIT(0)
      END IF
      RETURN
   END IF
   IF(SQ.EQ.1.AND.WQ.NE.'OBJ'.OR.SQ.EQ.1.AND.&
   &WQ.NE.'ALL'.OR.SQ.EQ.1.AND.WQ.NE.'OB') THEN
      WRITE(OUTLYNE,*)'INVALID QUALIFIER WORD'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(SQ.EQ.0.AND.DF1.EQ.1) THEN
!       OUTPUT IMAGE SURFACE
      SF=INT(sys_last_surf())
      CALL GNPRTGEN(SF,PY,PX,PUY,PUX,PCY,PCX,PUCY,PUCX,ERROR &
      &,DWORD2,DWORD3,DWORD4,DWORD5,0)
      IF(ERROR.EQ.1) THEN
         WRITE(OUTLYNE,*)'RAY COULD NOT BE TRACED'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'DIFFERENTIAL RAY VALUES NOT CALCULABLE'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'NO ACTION TAKEN'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
         GPRAYEXT=.TRUE.
      END IF
      IF(WC.EQ.'GPXTX')THEN
         IF(HEADIN) WRITE(OUTLYNE,6000)
         IF(HEADIN) CALL SHOWIT(0)
         WRITE(OUTLYNE,1500) SF,PX,PUX,PCX,PUCX
         CALL SHOWIT(0)
      END IF
      IF(WC.EQ.'GPXTY')THEN
         IF(HEADIN) WRITE(OUTLYNE,6003)
         IF(HEADIN) CALL SHOWIT(0)
         WRITE(OUTLYNE,1500) SF,PY,PUY,PCY,PUCY
         CALL SHOWIT(0)
      END IF
      RETURN
   END IF
   IF(SQ.EQ.0.AND.DF1.NE.1) THEN
      I=INT(W1)
      SF=INT(sys_last_surf())
      IF(I.GT.SF.OR.I.LT.0) THEN
         WRITE(OUTLYNE,*)'SURFACE NUMBER BEYOND LEGAL RANGE'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      END IF
      CALL GNPRTGEN(I,PY,PX,PUY,PUX,PCY,PCX,PUCY,PUCX,ERROR &
      &,DWORD2,DWORD3,DWORD4,DWORD5,0)
      IF(ERROR.EQ.1) THEN
         WRITE(OUTLYNE,*)'RAY COULD NOT BE TRACED'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'DIFFERENTIAL RAY VALUES NOT CALCULABLE'
         CALL SHOWIT(1)
         WRITE(OUTLYNE,*)'NO ACTION TAKEN'
         CALL SHOWIT(1)
         CALL MACFAL
         RETURN
      ELSE
         GPRAYEXT=.TRUE.
      END IF
      IF(WC.EQ.'GPXTX') THEN
         IF(HEADIN) WRITE(OUTLYNE,6000)
         IF(HEADIN) CALL SHOWIT(0)
         WRITE(OUTLYNE,1500)I,PX,PUX,PCX,PUCX
         CALL SHOWIT(0)
      END IF
      IF(WC.EQ.'GPXTY') THEN
         IF(HEADIN) WRITE(OUTLYNE,6003)
         IF(HEADIN) CALL SHOWIT(0)
         WRITE(OUTLYNE,1500)I,PY,PUY,PCY,PUCY
         CALL SHOWIT(0)
      END IF
   END IF
1500 FORMAT(I3,2X,G13.6,2X,G13.6,2X,G13.6,2X,G13.6)
!
!       GPXTX
!
6000 FORMAT('SURF',6X,'PX',12X,'PUX',13X,'PCX',11X,'PUCX')
6001 FORMAT('GENERALIZED PARAXIAL RAYTRACE DATA (XZ-PLANE)',&
   &' - (CFG #',I2,')')
6002 FORMAT(&
   &'(PUX AND PUCX) MEASURED WITH RESPECT TO THE Z-AXIS')
!
!       GPXTY
!
6003 FORMAT('SURF',6X,'PY',12X,'PUY',13X,'PCY',11X,'PUCY')
6004 FORMAT('GENERALIZED PARAXIAL RAYTRACE DATA (YZ-PLANE)',&
   &' - (CFG #',I2,')')
6005 FORMAT(&
   &'(PUY AND PUCY) MEASURED WITH RESPECT TO THE Z-AXIS')
2501 FORMAT(1X)
