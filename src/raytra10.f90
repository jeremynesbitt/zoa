      WRITE(OUTLYNE,2501)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6003)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6006) INT(DWORD5)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6007) DWORD2
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6008) DWORD3
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6009) DWORD4
      CALL SHOWIT(0)
