! SUB GLBRAY.FOR
SUBROUTINE GLBRAY
!
   use DATLEN
   use DATMAI
   use mod_surface, only: surf_thickness
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE GLBRAY.FOR. THIS SUBROUTINE IMPLEMENTS
!       CREATION OF GLOBAL RAY DATA FOR THE LAST RAY TRACED.
!       IT IS CALLED BY RAYTRA.FOR
!
   INTEGER JK,I
!
   REAL*8 &
   &X1,Y1,Z1,L1,M1,N1,X00,Y00,Z0,LX0,LY0,LZ0,&
   &MX0,MY0,MZ0,NX0,NY0,NZ0,LO1,MO1,NO1,LN1,MN1,NN1
!
!
   IF(DABS(surf_thickness(0)).LE.1.0D10) THEN
!       FINITE OBJECT THICKNESS
      JK=0
   ELSE
!       INFINITE OBJECT THICKNESS
      JK=1
   END IF
!
   DO 10 I=JK,NEWIMG
!
!       NOW TRANSFORM TO THE GLOBAL COORDINATE SYSTEM.
!       THE GLOBAL COORDINATES OF EACH LOCAL COORDINATE
!       SYSTEM ARE:
!
      X00=VERTEX(1,I)
      Y00=VERTEX(2,I)
      Z0=VERTEX(3,I)
      LX0=VERTEX(4,I)
      MX0=VERTEX(5,I)
      NX0=VERTEX(6,I)
      LY0=VERTEX(7,I)
      MY0=VERTEX(8,I)
      NY0=VERTEX(9,I)
      LZ0=VERTEX(10,I)
      MZ0=VERTEX(11,I)
      NZ0=VERTEX(12,I)
!
      X1=X00+((LX0*(RAYRAY(1,I)))+(LY0*(RAYRAY(2,I)))&
      &+(LZ0*(RAYRAY(3,I))))
      Y1=Y00+((MX0*(RAYRAY(1,I)))+(MY0*(RAYRAY(2,I)))&
      &+(MZ0*(RAYRAY(3,I))))
      Z1=Z0+((NX0*(RAYRAY(1,I)))+(NY0*(RAYRAY(2,I)))&
      &+(NZ0*(RAYRAY(3,I))))
!
      L1=((LX0*(RAYRAY(4,I)))+(LY0*(RAYRAY(5,I)))&
      &+(LZ0*(RAYRAY(6,I))))
      M1=((MX0*(RAYRAY(4,I)))+(MY0*(RAYRAY(5,I)))&
      &+(MZ0*(RAYRAY(6,I))))
      N1=((NX0*(RAYRAY(4,I)))+(NY0*(RAYRAY(5,I)))&
      &+(NZ0*(RAYRAY(6,I))))
!
      LO1=((LX0*(RAYRAY(19,I)))+(LY0*(RAYRAY(20,I)))&
      &+(LZ0*(RAYRAY(21,I))))
      MO1=((MX0*(RAYRAY(19,I)))+(MY0*(RAYRAY(20,I)))&
      &+(MZ0*(RAYRAY(21,I))))
      NO1=((NX0*(RAYRAY(19,I)))+(NY0*(RAYRAY(20,I)))&
      &+(NZ0*(RAYRAY(21,I))))
!
      LN1=((LX0*(RAYRAY(13,I)))+(LY0*(RAYRAY(14,I)))&
      &+(LZ0*(RAYRAY(15,I))))
      MN1=((MX0*(RAYRAY(13,I)))+(MY0*(RAYRAY(14,I)))&
      &+(MZ0*(RAYRAY(15,I))))
      NN1=((NX0*(RAYRAY(13,I)))+(NY0*(RAYRAY(14,I)))&
      &+(NZ0*(RAYRAY(15,I))))
!
      GLRAY(1,I)=X1
      GLRAY(2,I)=Y1
      GLRAY(3,I)=Z1
      GLRAY(4,I)=L1
      GLRAY(5,I)=M1
      GLRAY(6,I)=N1
      GLRAY(7,I)=LO1
      GLRAY(8,I)=MO1
      GLRAY(9,I)=NO1
      GLRAY(10,I)=LN1
      GLRAY(11,I)=MN1
      GLRAY(12,I)=NN1
10 CONTINUE
!       ALL GLOBAL TRANSFORMATIONS ARE COMPLETE
!
   RETURN
END
! SUB GETOPD.FOR
SUBROUTINE GETOPD(LEN,LENW,OPDERROR)
   use type_utils, only: real2str
!
   use DATLEN
   use DATMAI
   use mod_surface, only: surf_thickness
   use mod_system, only: sys_units, sys_mode, sys_last_surf, sys_wavelength
   use mod_lens_data_manager, only: ldm
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE GETOPD WHICH IMPLEMENTS THE GET OPD
!       AND GET OPDW
!       COMMANDS AT THE CMD LEVEL.
!
   INTEGER J,JJ
!
   REAL*8 LEN,LENW,WW,WAVE
!
   LOGICAL OPDERROR
!
   INTEGER WWVN
   COMMON/WVPASS/WWVN
   IF(INT(CURLAM).EQ.1) WWVN=46
   IF(INT(CURLAM).EQ.2) WWVN=47
   IF(INT(CURLAM).EQ.3) WWVN=48
   IF(INT(CURLAM).EQ.4) WWVN=49
   IF(INT(CURLAM).EQ.5) WWVN=50
   IF(INT(CURLAM).EQ.6) WWVN=71
   IF(INT(CURLAM).EQ.7) WWVN=72
   IF(INT(CURLAM).EQ.8) WWVN=73
   IF(INT(CURLAM).EQ.9) WWVN=74
   IF(INT(CURLAM).EQ.10) WWVN=75
!
!               CHECK FOR PRESENCE OF STRING,QUALIFIER,OR NUMERIC
!               INPUT AND
!               PRINT ERROR AND RETURN IF DISCOVERED.
!
   IF(RAYEXT.AND.REFEXT) THEN
      LEN=0.0D0
      RCOR=0.0D0
      OCOR=0.0D0
      IF(DABS(surf_thickness(NEWOBJ)).GE.1.0D10) JJ=NEWOBJ+2
      IF(DABS(surf_thickness(NEWOBJ)).LT.1.0D10) JJ=NEWOBJ+1
      DO J=JJ,NEWIMG
         LEN=LEN+RAYRAY(7,J)&
         &-(REFRY(7,J)*(ldm%getSurfIndex(J-1,INT(CURLAM))/ldm%getSurfIndex(J-1,INT(LFOB(4)))))
         ! call LogTermFOR("RAYRAY(7) "//real2str(RAYRAY(7,J)))
         ! call LogTermFOR("REFRY(7) "//real2str(REFRY(7,J)))
         ! call LogTermFOR("LEN "//real2str(LEN))


      END DO
      IF(sys_mode().EQ.1.0D0.OR.sys_mode().EQ.2.0D0) THEN
!       MODE FOCAL
         RCOR=0.0D0
         OCOR=0.0D0
         CALL FOPD
         ! call LogTermFOR("RCOR="//real2str(RCOR))
         ! call LogTermFOR("OCOR="//real2str(OCOR))
         ! call LogTermFOR("LEN="//real2str(LEN))

         LEN=LEN-(OCOR*(ldm%getSurfIndex(NEWOBJ,INT(CURLAM))))&
         &+(RCOR*(ldm%getSurfIndex(NEWOBJ,INT(CURLAM))))
         RCOR=0.0D0
         OCOR=0.0D0
         CENCEN=.FALSE.
         CALL LOPD
         ! call LogTermFOR("RCOR="//real2str(RCOR))
         ! call LogTermFOR("OCOR="//real2str(OCOR))
         ! call LogTermFOR("LEN="//real2str(LEN))

         LEN=LEN-(OCOR*(ldm%getSurfIndex(NEWIMG-1,INT(CURLAM))))&
         &+(RCOR*(ldm%getSurfIndex(NEWIMG-1,INT(CURLAM))))
         ! call LogTermFOR("LENPC="//real2str(LEN))
      ELSE
!       MODE AFOCAL
         RCOR=0.0D0
         OCOR=0.0D0
         CALL FOPD
         LEN=LEN-((OCOR*ldm%getSurfIndex(NEWOBJ,INT(CURLAM))))&
         &+(RCOR*(ldm%getSurfIndex(NEWOBJ,INT(CURLAM))))
         RCOR=0.0D0
         OCOR=0.0D0
         CENCEN=.FALSE.
         CALL LOPD
         ! call LogTermFOR("RCOR="//real2str(RCOR))
         ! call LogTermFOR("OCOR="//real2str(OCOR))
         LEN=LEN-(OCOR*(ldm%getSurfIndex(NEWIMG-1,INT(CURLAM))))&
         &+(RCOR*(ldm%getSurfIndex(NEWIMG-1,INT(CURLAM))))
      END IF
!     CALCULATE LEN IN WAVES AT THE REFERENCE RAY WAVELENGTH
      IF(INT(CURLAM).GE.1.AND.INT(CURLAM).LE.10) THEN
         WW=sys_wavelength(INT(CURLAM))
      END IF
      IF(sys_units().EQ.1.0) WAVE=(WW*1.0D-3)/(25.4D0)
      IF(sys_units().EQ.2.0) WAVE=WW*1.0D-4
      IF(sys_units().EQ.3.0) WAVE=WW*1.0D-3
      IF(sys_units().EQ.4.0) WAVE=WW*1.0D-6
      LEN=-LEN
      IF(REVSTR) LEN=-LEN
      LENW=LEN/WAVE
      OPDERROR=.FALSE.
      RETURN
   ELSE
!       NO RAY EXITS OR NO REF RAY
      IF(.NOT.REFEXT) THEN
         OUTLYNE=&
         &'NO REFERENCE RAY DATA EXISTS, "OPD" CAN NOT BE CALCULATED'
         CALL SHOWIT(1)
         CALL MACFAL
         OPDERROR=.TRUE.
      END IF
      IF(.NOT.RAYEXT) THEN
         OUTLYNE=&
         &'NO RAY DATA EXISTS, "OPD" CAN NOT BE CALCULATED'
         CALL SHOWIT(1)
         CALL MACFAL
         OPDERROR=.TRUE.
      END IF
   END IF
   RETURN
END
