      CALL SHOWIT(0)
      WRITE(OUTLYNE,6011) WRY
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6012) BDX
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6013) BDY
      CALL SHOWIT(0)
!
!     FIRST XZ-PLANE
!
