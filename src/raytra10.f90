         RETURN
      END IF
      V1=DSQRT((PX**2)+(PCX**2))
      IF(REAL(V1).EQ.0.0) THEN
         VALVA=1.0D20
      ELSE
         VALVA=((PX*PUX)+(PCX*PUCX))/V1
      END IF
      IF(REAL(VALVA).EQ.0.0) THEN
         V2=0.0D0
