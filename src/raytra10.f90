      WRITE(OUTLYNE,6001) INT(F12)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1500) SF,V1,V2,V3,V4
      CALL SHOWIT(0)
!     YZ-PLANE
      WRITE(OUTLYNE,2501)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6002) INT(F12)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,6000)
      CALL SHOWIT(0)
      WRITE(OUTLYNE,1500) SF,V5,V6,V7,V8
      CALL SHOWIT(0)
      call sys_set_ref_surf(DBLE(OLDREF2))
      call sys_set_astop(DBLE(OLDSTOP))
      RETURN
   END IF
   IF(SQ.EQ.1.AND.WQ.NE.'OBJ'.OR.SQ.EQ.1.AND.&
   &WQ.NE.'ALL'.OR.SQ.EQ.1.AND.WQ.NE.'OB') THEN
      WRITE(OUTLYNE,*)'INVALID QUALIFIER WORD'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      call sys_set_ref_surf(DBLE(OLDREF2))
      call sys_set_astop(DBLE(OLDSTOP))
      RETURN
   END IF
   IF(SQ.EQ.0.AND.DF1.EQ.1) THEN
!       OUTPUT IMAGE SURFACE
      SF=INT(sys_last_surf())
      CALL GNPRTGEN(SF,PY,PX,PUY,PUX,PCY,PCX,PUCY,PUCX,ERROR &
      &,DWORD2,DWORD3,DWORD4,DWORD5,1)
      IF(ERROR.EQ.1) THEN
