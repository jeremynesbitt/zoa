! SUB PGLPRY.FOR
SUBROUTINE PGLPRY
!
   use DATLEN
   use DATMAI
   use mod_surface, only: surf_thickness
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE GLPRAY.FOR. THIS SUBROUTINE IMPLEMENTS
!       CREATION OF GLOBAL RAY DATA FOR THE LAST RAY TRACED.
!       FOR RAY PLOTTING IT IS CALLED BY RAYTRA.FOR
!
   INTEGER JK,I
!
   REAL*8 X,Y,Z,&
   &X1,Y1,Z1,L1,M1,N1,X00,Y00,Z0,LX0,LY0,LZ0,&
   &MX0,MY0,MZ0,NX0,NY0,NZ0,LO1,MO1,NO1
!
!
   IF(DABS(surf_thickness(NEWOBJ)).LE.1.0D10) THEN
!       FINITE OBJECT THICKNESS
      JK=NEWOBJ
   ELSE
!       INFINITE OBJECT THICKNESS
      JK=NEWOBJ+1
   END IF
!
   DO 10 I=JK,NEWIMG
!
!       NOW TRANSFORM TO THE GLOBAL COORDINATE SYSTEM.
!       THE GLOBAL COORDINATES OF EACH LOCAL COORDINATE
!       SYSTEM ARE:
!
      X00=VERTEX(1,I)
      Y00=VERTEX(2,I)
      Z0=VERTEX(3,I)
      LX0=VERTEX(4,I)
      MX0=VERTEX(5,I)
      NX0=VERTEX(6,I)
      LY0=VERTEX(7,I)
      MY0=VERTEX(8,I)
      NY0=VERTEX(9,I)
      LZ0=VERTEX(10,I)
      MZ0=VERTEX(11,I)
      NZ0=VERTEX(12,I)
!
      X=GPXY(1,I)
      Y=0.0D0
      Z=0.0D0
      X1=X00+((LX0*(X))+(LY0*(Y))&
      &+(LZ0*(X)))
      Y1=Y00+((MX0*(X))+(MY0*(Y))&
      &+(MZ0*(Z)))
      Z1=Z0+((NX0*(X))+(NY0*(Y))&
      &+(NZ0*(Z)))
      PGLPRAY(1,I)=X1
      PGLPRAY(2,I)=Y1
      PGLPRAY(3,I)=Z1
      X=0.0D0
      Y=GPXY(2,I)
      Z=0.0D0
      X1=X00+((LX0*(X))+(LY0*(Y))&
      &+(LZ0*(X)))
      Y1=Y00+((MX0*(X))+(MY0*(Y))&
      &+(MZ0*(Z)))
      Z1=Z0+((NX0*(X))+(NY0*(Y))&
      &+(NZ0*(Z)))
      PGLPRAY(4,I)=X1
      PGLPRAY(5,I)=Y1
      PGLPRAY(6,I)=Z1
      X=GPXY(3,I)
      Y=0.0D0
      Z=0.0D0
      X1=X00+((LX0*(X))+(LY0*(Y))&
      &+(LZ0*(X)))
      Y1=Y00+((MX0*(X))+(MY0*(Y))&
      &+(MZ0*(Z)))
      Z1=Z0+((NX0*(X))+(NY0*(Y))&
      &+(NZ0*(Z)))
      PGLPRAY(7,I)=X1
      PGLPRAY(8,I)=Y1
      PGLPRAY(9,I)=Z1
      X=0.0D0
      Y=GPXY(4,I)
      Z=0.0D0
      X1=X00+((LX0*(X))+(LY0*(Y))&
      &+(LZ0*(X)))
      Y1=Y00+((MX0*(X))+(MY0*(Y))&
      &+(MZ0*(Z)))
      Z1=Z0+((NX0*(X))+(NY0*(Y))&
      &+(NZ0*(Z)))
      PGLPRAY(10,I)=X1
      PGLPRAY(11,I)=Y1
      PGLPRAY(12,I)=Z1
10 CONTINUE
!       ALL GLOBAL TRANSFORMATIONS ARE COMPLETE
   RETURN
END
