      &'"BEA" OR "BEAM" OUTPUTS GAUSSIAN BEAM PROPERTIES'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(SST.EQ.1) THEN
      WRITE(OUTLYNE,*)&
      &'GAUSSIAN BEAM OUTPUT COMMANDS TAKE NO STRING INPUT'
      CALL SHOWIT(1)
      WRITE(OUTLYNE,*)'RE-ENTER COMMAND'
      CALL SHOWIT(1)
      CALL MACFAL
      RETURN
   END IF
   IF(SQ.EQ.1.AND.S1.EQ.1) THEN
