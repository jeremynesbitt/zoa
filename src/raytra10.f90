         RETURN
      END IF
      CALL GNPRTGEN(I,PY,PX,PUY,PUX,PCY,PCX,PUCY,PUCX,ERROR &
      &,DWORD2,DWORD3,DWORD4,DWORD5,1)
      IF(ERROR.EQ.1) THEN
