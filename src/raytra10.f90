      END IF
      IF(REAL((PUY**2)+(PUCY**2)).EQ.0.0) THEN
         V7=1.0D20
      ELSE
         V7=-((PY*PUY)+(PCY*PUCY))/((PUY**2)+(PUCY**2))
      END IF
      VAL1=PY+(PUY*V7)
      VAL2=PCY+(PUCY*V7)
