! SUB MACLOD.FOR
SUBROUTINE MACLOD
!
   use DATMAI
   IMPLICIT NONE
!
   LOGICAL EXST
!
!       THIS IS CALLED BY MACEXC TO RUN A MACRO OR FUNCTION
!       INSIDE A MACRO OR FUNCTION.
!       THE FUNCTION OF THIS SUBROUTINE IS TO LOAD INTO THE
!       MACRO ARRAYS THE TARGET MACRO OR FUNCTION DESIGNATED BY ITS ADDRESS
!       IN THE MACRO OR FUNCTION DIRECTORY.
!
   CHARACTER FILNAM*10
!
   INTEGER NF,L,K
   INTEGER FLG(0:20)
!
   COMMON/NEFER/NF
!
   COMMON/COMNAM/FILNAM
!
   COMMON/FFL/FLG
!
   INCLUDE 'DATMAC.INC'

   PRINT *, "MACLOD function called! "
!
   IF(.NOT.NESFUN(NEST)) THEN
      CALL MACFIL

      EXST=.FALSE.
      CALL DOESMACROFILEEXIST(FILNAM, EXST)
      IF(.NOT.EXST) THEN
         CALL MACFAL
         RETURN
      END IF

      CALL READMACROFILE(trim(LIBMAC)//FILNAM)

!
   ELSE
!       THE CALLED ITEM WAS A FUNCTION, LOAD IT
!
!       NOW LETS LOAD THE MACRO ARRAYS WITH THE CONTENTS OF
!       THE NF TH FUNCTION.
!       INITIALIZE MACRO FUNCTIONS
      MCDIR1(NF)=FCDIR1(NF)
      MCDIR2(1,NF)=FCDIR2(1,NF)
      MCDIR2(2,NF)=FCDIR2(2,NF)
      MCDIR2(3,NF)=FCDIR2(3,NF)
      MCDIR3(NF)=FCDIR3(NF)
!
      DO K=1,(MCDIR2(2,NF))
         L=K-1
         MACCW(L)=FUNCW(NF,L)
         MACQW(L)=FUNQW(NF,L)
         MACSTR(L)=FUNSTR(NF,L)
         MACNW(1,L)=FUNNW(NF,1,L)
         MACNW(2,L)=FUNNW(NF,2,L)
         MACNW(3,L)=FUNNW(NF,3,L)
         MACNW(4,L)=FUNNW(NF,4,L)
         MACNW(5,L)=FUNNW(NF,5,L)
         MACSTA(1,L)=FUNSTA(NF,1,L)
         MACSTA(2,L)=FUNSTA(NF,2,L)
         MACSTA(3,L)=FUNSTA(NF,3,L)
         MACSTA(4,L)=FUNSTA(NF,4,L)
         MACSTA(5,L)=FUNSTA(NF,5,L)
         MACSTA(6,L)=FUNSTA(NF,6,L)
         MACSTA(7,L)=FUNSTA(NF,7,L)
         MACSTA(8,L)=FUNSTA(NF,8,L)
         MACSTA(9,L)=FUNSTA(NF,9,L)
         MACSTA(10,L)=FUNSTA(NF,10,L)
         MACSTA(11,L)=FUNSTA(NF,11,L)
         MACSTA(12,L)=FUNSTA(NF,12,L)
         MACSTA(13,L)=FUNSTA(NF,13,L)
         MACSTA(14,L)=FUNSTA(NF,14,L)
         MACSTA(15,L)=FUNSTA(NF,15,L)
         MACSTA(16,L)=FUNSTA(NF,16,L)
         MACSTA(17,L)=FUNSTA(NF,17,L)
         MACSTA(18,L)=FUNSTA(NF,18,L)
         MACSTA(19,L)=FUNSTA(NF,19,L)
         MACSTA(20,L)=FUNSTA(NF,20,L)
      END DO
!       FUNCTION LOADED INTO MAC ARRAYS
   END IF
   RETURN
END
! SUB MACIN.FOR
SUBROUTINE MACIN(WWW1)
!
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS ONLY CALLED WHEN FLAG F2=1 (MACRO
!       CREATION MODE. HEREIN ALL COMMANDS ARE RESOLVED CORRECTLY
!       FOR THE MACRO CREATION MODE OF THE PROGRAM.
!
!       THE EOM COMMAND IS THE ONLY WAY OUT OF THE MACRO CREATION
!       MODE. THIS SUBROUTINE PROCESSES THE EOM COMMAND BY CALLING
!       SUBROUTINE EEOM. SUBROUTINE EEOM CLOSES OFF MACRO CREATION,
!       SETS FLAG F2=0 AND F1=1 (REENABLING THE CMD LEVEL,
!       AND APPROPRIATELY FILING THE MACRO INTO THE MACRO LIBRARY
!       FILE MAC.DAT.
!
   INTEGER IMAC,MAXLL
!
   CHARACTER MACNAM*8
!
!       MACCW,MACQW,AND MACSTR HOLD COMMWD,QUALWD AND STRINGS
!       FOR MAXLL MACRO LINES. MACSTA HOLDS VARIOUS STATUS INDICATORS.
!       MACNW HOLDS NUMERIC WORDS.
!
   DOUBLE PRECISION WWW1
!
   INTEGER MACLOC,IMACEN
!
!       IMAC TRACKS THE CURRENT MACRO LINE NUMBER.
!       MAXIMUM VALUE ALLOWED FOR IMAC IS MAXLL-1.
!
   COMMON/IMAC/IMAC
!
   COMMON/MACID/MACNAM
!
   COMMON/MACID2/MACLOC,IMACEN
!
   INCLUDE 'DATMAC.INC'
!
!       TEST FOR MACRO INPUT COMMANDS. IF THERE ARE NONE, GO TO
!       CREATION PHASE IE COMMANDS WHICH EXECUTE IMMEDIATELY AND
!       ARE NOT STORED IN THE MACRO. THE COMMAND ARE '?' AND ' '.
!
   IF(WC.EQ.'?'.OR.WC.EQ.' ') THEN
      IF(WC.EQ.'?') CALL QUERRYY
      IF(WC.EQ.' '.AND.F50.EQ.1) CALL BLANK
   ELSE
!
!       HERE IS WHERE ALL OF THE PASSED COMMAND WORDS,QUALIFIER WORDS,
!       AND OTHER INPUT ARE STORED IN THE MACRO ARRAYS.  THE FIRST LINE OF
!       EACH MACRO WILL BE AUTOMATICALLY WRITTEN AS:
!
!       MACRO (NAME),(LOCK)
!
!       THE LAST LINE (MAXLL-1) CAN ONLY CONTAIN 'EOM'.
!       (END OF MACRO). WHEN EOM IS ISSUED FROM THE KEYBOARD OR
!       READ IN FROM FILES EDITTEXT,CARDTEXT OR PUNCH
!       IT CAUSES 'EOM' TO BE WRITTEN IN THE CURRENT MACRO LINE
!       AND IT ENDS MACRO CREATION. IF 'EOM' IS ENCOUNTERED IN A MACRO
!       DURING MACRO EXECUTION THEN
!       IT SIMPLY INDICATES THE END OF THE MACRO.
!
!       AFTER EACH STORAGE
!       OPERATION, IMAC  THE COUNTER IS INCREMENTED. WHEN IMAC REACHES
!       MAXLL-2 A MESSAGE ' THAT WAS THE MAXLL-2 TH LINE,
!       THE LINE MAXLL TH LINE
!       IS RESERVED
!       FOR THE AUTOMATIC EOM COMMAND.'
!       IF THE MACRO IS LESS THAN MAXLL LINES LONG, THE LAST LINE OF
!       THE MACRO WILL AUTOMATICALLY BE WRITTEN AS EOM PRIOR TO STORAGE.
!
!       TEST FOR THE FIRST LINE OF THE MACRO
      IF(IMAC.EQ.0) THEN
         MACCW(IMAC)='MACRO'
!       TEST FOR A BLANK MACRO NAME (BLANK NAMES ARE NOT ALLOWED)
         IF(WQ.EQ.'        ') THEN
            OUTLYNE=&
            &'A BLANK MACRO NAME IS NOT ALLOWED.'
            CALL SHOWIT(1)
            OUTLYNE=&
            &'RETURNED TO CMD LEVEL'
            CALL SHOWIT(1)
            F2=0
            F1=1
            CALL MACFAL
            RETURN
         ELSE
         END IF
!       THE MACRO NAME IS NOT BLANK, CONTINUE PROCESSING.
!
!       STORE THE MACRO NAME AND THE MACRO LOCK IN ARRAYS AND IN SPECIAL
!       VARIABLES.
!
         MACNAM=WQ
         MACLOC=INT(WWW1)
         MACCW(IMAC)=WC
         MACQW(IMAC)=WQ
         MACSTR(IMAC)=WS
         MACNW(1,IMAC)=W1
         MACNW(2,IMAC)=W2
         MACNW(3,IMAC)=W3
         MACNW(4,IMAC)=W4
         MACNW(5,IMAC)=W5
         MACSTA(1,IMAC)=SB1
         MACSTA(2,IMAC)=SB2
         MACSTA(3,IMAC)=SC1
         MACSTA(4,IMAC)=SC2
         MACSTA(5,IMAC)=SQ
         MACSTA(6,IMAC)=SST
         MACSTA(7,IMAC)=S1
         MACSTA(8,IMAC)=S2
         MACSTA(9,IMAC)=S3
         MACSTA(10,IMAC)=S4
         MACSTA(11,IMAC)=S5
         MACSTA(12,IMAC)=DF1
         MACSTA(13,IMAC)=DF2
         MACSTA(14,IMAC)=DF3
         MACSTA(15,IMAC)=DF4
         MACSTA(16,IMAC)=DF5
         MACSTA(17,IMAC)=SN
         MACSTA(18,IMAC)=STI
!       MACSTA(19,IMAC) AND MACSTA(20,IMAC) NOT YET USED
         MACSTA(19,IMAC)=0
         MACSTA(20,IMAC)=0
!       INCREMENT THE COUNTER
         IMAC=IMAC+1
         RETURN
      ELSE
!       IMAC NOT EQUAL TO 0
      END IF
!       PROCEED WITH PROCESSING
!       ARE WE AT LINE MAXLL-2?
      CALL MAXLNN(MACNAM,MAXLL,MAXLIN)
      IF(IMAC.EQ.(MAXLL-2)) THEN
         WRITE(OUTLYNE,*)&
         &'THAT WAS LINE ',MAXLL,'. LINE ',&
         &MAXLL,' IS RESERVED FOR (EOM)'
         CALL SHOWIT(1)
!       INCREMENT THE COUTER
         IMAC=IMAC+1
         CALL MACFAL
         RETURN
      END IF
!       CONTINUE PROCESSING
!       ARE WE AT MAXLL-1 WITH NON- (EOM) INPUT?
      CALL MAXLNN(MACNAM,MAXLL,MAXLIN)
      IF(IMAC.EQ.(MAXLL-1).AND.WC.NE.'EOM') THEN
         OUTLYNE=&
         &'NO INPUT OTHER THAN (EOM) IS ALLOWED'
         CALL SHOWIT(1)
!       IMAC NOT INCREMENTED HERE
         CALL MACFAL
         RETURN
      END IF
!       IS THE NEXT INPUT (EOM)?
!
      IF(WC.EQ.'EOM') THEN
         MACCW(IMAC)=WC
         MACQW(IMAC)=WQ
         MACSTR(IMAC)=WS
         MACNW(1,IMAC)=W1
         MACNW(2,IMAC)=W2
         MACNW(3,IMAC)=W3
         MACNW(4,IMAC)=W4
         MACNW(5,IMAC)=W5
         MACSTA(1,IMAC)=SB1
         MACSTA(2,IMAC)=SB2
         MACSTA(3,IMAC)=SC1
         MACSTA(4,IMAC)=SC2
         MACSTA(5,IMAC)=SQ
         MACSTA(6,IMAC)=SST
         MACSTA(7,IMAC)=S1
         MACSTA(8,IMAC)=S2
         MACSTA(9,IMAC)=S3
         MACSTA(10,IMAC)=S4
         MACSTA(11,IMAC)=S5
         MACSTA(12,IMAC)=DF1
         MACSTA(13,IMAC)=DF2
         MACSTA(14,IMAC)=DF3
         MACSTA(15,IMAC)=DF4
         MACSTA(16,IMAC)=DF5
         MACSTA(17,IMAC)=SN
         MACSTA(18,IMAC)=STI
!       MACSTA(19,IMAC) AND MACSTA(20,IMAC) NOT YET USED
         MACSTA(19,IMAC)=0
         MACSTA(20,IMAC)=0
!       VARIABLE IMACEN KEEPS TRACK OF THE NUMBER OF LINES IN EACH
!       FINISHED MACRO
!
         IMACEN=IMAC+1
!       IMAC NOT INCREMENTED HERE. NOW CALL EEOM TO FILE THE MACRO
!       IN MAC.DAT
         CALL EEOM
!       WE ARE AT THE CMD LEVEL NOW SO JUST RETURN
         RETURN
      ELSE
      END IF
!       IMAC .NE. MAXLL-2 OR MAXLL-1 AND WC .NE. 'EOM'
!       PROCEED WITH PROCESSING WHEN IMAC NE 0,MAXLL-2,OR
!       MAXLL-1.
!
!       BEFORE PUTING ING THE LINE IN THE MACRO, CHECK SOME SYNTAX
!       JUST AS IN MACMOD.FOR
!**********************************************************************
!
!       HERE IS WHERE MACRO SPECIFIC OPERATING COMMANDS HAVE SOME
!       OR THEIR SYNTAX CHECKED.
!
      IF(WC.EQ.'QSUB'.OR.WC.EQ.'SSUB') THEN
         IF(SN.EQ.1) THEN
            OUTLYNE='"'//WC(1:4)//'" TAKES NO NUMERIC INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'QRSUB') THEN
         IF(SN.EQ.1) THEN
            OUTLYNE='"'//WC(1:5)//'" TAKES NO NUMERIC INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'ACCSUB') THEN
         IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
            OUTLYNE=&
            &'"ACCSUB" ONLY TAKES QUALIFIER AND NUMERIC WORD #1 INPUT'
