!       OPEN UNIT 20 FOR I/O
!
!       ***************************************************************
   call does_mac_dat_exist(EXISJK)
!       ***************************************************************
   uLIB = open_mac_dat()
!
   READ(UNIT=uLIB,REC=NF,ERR=10) MCNAM1,MCLOC1,IMCEN1,OCC,STAMP

!
!       MCNAM1 IS THE MACRO NAME, MCLOC1 IS THE MACRO LOCK,
!       IMCEN1 IS THE MACRO LENGTH IN LINES, AND OCC IS A FLAG
!       =0 IF THE RECORD REPRESENTS A BLANK MACRO AND =1
!       FOR A NON-BLANK MACRO.
!
!       MACRO RECORDS (MAXIMUM OF MAXLL NON-BLANK RECL=55*NRECL RECORDS)
