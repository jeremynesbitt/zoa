            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
         IF(DF1.EQ.1) THEN
            OUTLYNE=&
            &'"ACCSUB" REQUIRES EXPLICIT NUMERIC WORD #1 INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'CSUB') THEN
         IF(SST.EQ.1) THEN
            OUTLYNE='"'//WC(1:4)//'" TAKES NO EXPLICIT INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'CRSUB') THEN
         IF(SST.EQ.1) THEN
            OUTLYNE='"'//WC(1:5)//'" TAKES NO EXPLICIT INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'SAVE') THEN
         IF(SST.EQ.1) THEN
            OUTLYNE='"SAVE" TAKES NO EXPLICIT INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'RELOAD') THEN
         IF(SST.EQ.1) THEN
            OUTLYNE='"RELOAD" TAKES NO EXPLICIT INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'NSUB') THEN
         IF(SST.EQ.1) THEN
            OUTLYNE='"'//WC(1:4)//'" TAKES NO STRING INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'BPOS'.OR.WC.EQ.'BNEG'.OR.WC.EQ.'BRDQ') THEN
         IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:4)//&
            &'" ONLY TAKES QUALIFIER AND NUMERIC WORD #1 INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
         IF(SQ.EQ.1.AND.S1.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:4)//'" TAKES EITHER QUALIFIER OR'
            CALL SHOWIT(1)
            OUTLYNE='NUMERIC WORD #1 INPUT, BUT NOT BOTH'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'BRDF1'.OR.WC.EQ.'BRDF2'.OR.WC.EQ.'BRDF3'.OR.&
      &WC.EQ.'BRDF4'.OR.WC.EQ.'BRDF5') THEN
         IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:5)//&
            &'" ONLY TAKES QUALIFIER AND NUMERIC WORD #1 INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
         IF(SQ.EQ.1.AND.S1.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:5)//'" TAKES EITHER QUALIFIER OR'
            CALL SHOWIT(1)
            OUTLYNE='NUMERIC WORD #1 INPUT, BUT NOT BOTH'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'BRT'.OR.WC.EQ.'BRF') THEN
         IF(SST.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:3)//'" TAKES NO STRING INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'TRACE') THEN
         IF(SST.EQ.1.OR.SN.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:5)//'" ONLY TAKES QUALIFIER WORD INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'SSTEP') THEN
         IF(SST.EQ.1.OR.SN.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:5)//'" ONLY TAKES QUALIFIER WORD INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'PAUSE') THEN
         IF(SST.EQ.1.OR.SN.EQ.1.OR.SQ.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:5)//'" TAKES NO ADDITIONAL INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'BZE'.OR.WC.EQ.'BRI'.OR.WC.EQ.'BRJ'.OR.&
      &WC.EQ.'BRU'.OR.WC.EQ.'BRK'.OR.WC.EQ.'BRL'.OR.WC.EQ.'BRM'&
      &.OR.WC.EQ.'BRN') THEN
         IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:3)//&
            &'" ONLY TAKES QUALIFIER AND NUMERIC WORD #1 INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
         IF(SQ.EQ.1.AND.S1.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:3)//'" TAKES EITHER QUALIFIER OR'
            CALL SHOWIT(1)
            OUTLYNE='NUMERIC WORD #1 INPUT, BUT NOT BOTH'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'IF(X>0)'.OR.WC.EQ.'IF(X<0)'.OR.WC.EQ.'IF(X=0)'.OR.&
      &WC.EQ.'IF(X=Y)'.OR.WC.EQ.'IF(X<Y)'.OR.WC.EQ.'IF(X>Y)') THEN
         IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:6)//&
            &'" ONLY TAKES QUALIFIER AND NUMERIC WORD #1 INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
         IF(SQ.EQ.1.AND.S1.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:6)//'" TAKES EITHER QUALIFIER OR'
            CALL SHOWIT(1)
            OUTLYNE='NUMERIC WORD #1 INPUT, BUT NOT BOTH'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'BRERR') THEN
         IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:5)//&
            &'" ONLY TAKES QUALIFIER AND NUMERIC WORD #1 INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
         IF(SQ.EQ.1.AND.S1.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:5)//'" TAKES EITHER QUALIFIER OR'
            CALL SHOWIT(1)
            OUTLYNE='NUMERIC WORD #1 INPUT, BUT NOT BOTH'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         ELSE
         END IF
      ELSE
!       NOT BREER
      END IF
      IF(WC.EQ.'PUTR') THEN
         IF(SST.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
            OUTLYNE=&
            &'"PUTR" ONLY TAKES QUALIFIER AND NUMERIC WORD #1 INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'BRANCH') THEN
         IF(SST.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
            OUTLYNE=&
            &'"BRANCH" ONLY TAKES QUALIFIER AND '
            CALL SHOWIT(1)
            OUTLYNE='NUMERIC WORD #1, #2 AND #3 INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
         IF(SQ.EQ.1.AND.S3.EQ.1) THEN
            OUTLYNE=&
            &'"BRANCH" TAKES EITHER QUALIFIER OR'
            CALL SHOWIT(1)
            OUTLYNE='NUMERIC WORD #3 INPUT, BUT NOT BOTH'
            CALL SHOWIT(1)
