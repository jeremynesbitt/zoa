      MACSTA(16,IMAC)=DF5
      MACSTA(17,IMAC)=SN
      MACSTA(18,IMAC)=STI
!       MACSTA(19,IMAC) AND MACSTA(20,IMAC) NOT YET USED
      MACSTA(19,IMAC)=0
      MACSTA(20,IMAC)=0
      IMAC=IMAC+1
   END IF
   RETURN
END
