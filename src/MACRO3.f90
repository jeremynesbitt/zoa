!
!       NOW DETERMINE THE LOCATION OF THE MACRO BODY.
!
   CALL MACFIL
   EXST=.FALSE.
   CALL DOESMACROFILEEXIST(FILNAM, EXST)
   IF(.NOT.EXST) THEN
      CALL MACFAL
      RETURN
   END IF
   CALL READMACROFILE(trim(LIBMAC)//FILNAM)
!

   CALL CLOSE_FILE(uLIB,1)
!
!       THE MACRO ARRAYS ARE NOW LOADED. NOW REFORMAT AND OUTPUT.
!
!       THE OUTPUT MAY HAVE UP TO IMCEN1 LINES IN IT.
!
