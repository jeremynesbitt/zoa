            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         ELSE
         END IF
         IF(INT(W1).LT.1.OR.INT(W1).GT.5) THEN
            OUTLYNE=&
            &'"MOVE NW" REQUIRES 1, 2, 3, 4 OR 5 FOR NUMERIC INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         ELSE
         END IF
      ELSE
      END IF
      IF(WC.EQ.'BP') THEN
         IF(SST.EQ.1.OR.SN.EQ.1) THEN
            OUTLYNE=&
            &'"BP" ONLY TAKES QUALIFIER WORD INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'BRQ') THEN
         IF(SN.EQ.1) THEN
            OUTLYNE=&
            &'"BRQ" ONLY TAKES QUALIFIER AND STRING INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'STORE') THEN
         IF(SST.EQ.1.OR.SN.EQ.1) THEN
            OUTLYNE=&
            &'"STORE" ONLY TAKES QUALIFIER WORD INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
      IF(WC.EQ.'M') THEN
         IF(SQ.EQ.1.OR.S2.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
            OUTLYNE=&
            &'"'//WC(1:1)//'&
            &" ONLY TAKES STRING AND NUMERIC WORD #1 INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
