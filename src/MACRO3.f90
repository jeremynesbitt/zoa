!
   DO 40 I=0,IMCEN1
!       ONLY LINES WHICH BEGIN WITH A COMMAND WORD (C)
!       WILL BE PRINTED UNLESS MFLC IS ISSUED WITHOUT A MACRO NAME.
!       IF THAT HAPPENS, THE MACRO NAME WILL BE PRINTED.
      IF(I.EQ.0) THEN
         GO TO 40
      ELSE
         GO TO 41
      END IF
!
!       IF OUT = 6 (SCREEN) OR OUT = 7 (PRINTER)
!       THEN WRITE COMPACT 80 COLUMN OUTPUT.
!
!       FOR ED,CR,AND PU WRITE EXTENDED COMPLETE OUTPUT
!
41    IF(MACCW(I)(1:2).EQ.'C '.OR.MACCW(I)(1:2).EQ.'C,') THEN
!
         IF(OUT.EQ.8.OR.OUT.EQ.9.OR.OUT.EQ.10) THEN
            WRITE(OUTLYNE,100) MACCW(I),(MACSTR(I))
            CALL SHOWIT(0)
         ELSE
            WRITE(OUTLYNE,200) MACCW(I),(MACSTR(I))
            CALL SHOWIT(0)
         END IF
         GO TO 40
      ELSE
         WRITE(OUTLYNE,51)
         CALL SHOWIT(0)
         RETURN
      END IF
!
40 CONTINUE
!
50 FORMAT(A8,1X,A8)
51 FORMAT('END OF LEADING COMMENTS')
100 FORMAT(A8,1X,A80)
200 FORMAT(A8,1X,A61)
!
10 RETURN
END
