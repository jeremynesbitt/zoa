   use zoa_file_handler
!
   use DATMAI
   IMPLICIT NONE
!
   LOGICAL EXST,EXISJK
!
!       THIS SUBROUTINE IS CALLED BY MMFLC AND
!       CAUSES THE ITH MACRO'S LEADING COMMENTS TO BE OUTPUT
!       TO THE DEFAULT OUTPUT DEVICE.
!
   INTEGER OCC,MCLOC1,IMCEN1 &
   &,NF,K,L,I, uLIB
!
   CHARACTER MCNAM1*8,FILNAM*10,STAMP*20

!
   COMMON/NEFER/NF
