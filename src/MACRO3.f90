            OUTLYNE='"EOM" TAKES NO EXPLICIT INPUT'
            CALL SHOWIT(1)
            OUTLYNE='RE-ENTER COMMAND'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END IF
!
!**********************************************************************
!
      MACCW(IMAC)=WC
      MACQW(IMAC)=WQ
      MACSTR(IMAC)=WS
      MACNW(1,IMAC)=W1
      MACNW(2,IMAC)=W2
      MACNW(3,IMAC)=W3
      MACNW(4,IMAC)=W4
      MACNW(5,IMAC)=W5
      MACSTA(1,IMAC)=SB1
      MACSTA(2,IMAC)=SB2
