! SUB MACEDT.FOR
SUBROUTINE MACEDT
   use zoa_file_handler
!
   use DATMAI
   IMPLICIT NONE

   INTEGER I,K,J,L,MAXLL
!
   LOGICAL EXST,EXISJK
!
   INTEGER MMIJ,NF, uLIB
!
   CHARACTER FILNAM*10
!
   COMMON/MIJ/MMIJ
!
   COMMON/NEFER/NF
!
   COMMON/COMNAM/FILNAM
!
   INCLUDE 'DATMAC.INC'
!
!       THIS SUBROUTINE IS CALLED INITIALLY BY MMEDIT
!       THE VARIABLE MMIJ TRACKS THE LOCATIONS WHERE THE FINAL
!       STORAGE WILL OCCUR. AFTER THIS SUBROUTINE LOADS
!       THE MACRO STORAGE. FURTHER WORK ON THE MACRO WILL
!       OCCUR THROUGH CALLS FROM SUBROUTINE CONTOL THROUGH
!       SUBROUTINE MACMOD.
!       IF MMIJ = 0 DESIGNATES A NEW MACRO. IF MMIJ LIES BETWEEN
!       1 AND MAXMAC IT DESIGNATES THE LOCATION IN THE DIRECTORY
!       WHERE THE MEDITED MACRO IS AND IS TO BE STORED.
!
!       FL,QU AND QUIT ARE THE ONLY WAYS OUT OF THE MACRO EDIT
!       MODE. THIS SUBROUTINE PROCESSES THE FL COMMAND BY CALLING
!       SUBROUTINE FLMAC. SUBROUTINE FLMAC CLOSES OFF MACRO EDITING,
!       SETS FLAG F3=0 AND F1=1 (REENABLING THE CMD LEVEL,
!       AND APPROPRIATELY FILING THE MACRO INTO THE MACRO LIBRARY
!       FILE MAC.DAT. QUIT OR QU GETS OUT WITHOUT FILING.
!
!               INITIALIZE OLDIJ
   OLDIJ=MAXMAC+1
!
!       NOW LOAD THE CURRENT MACRO DIRECTORY INTO MEMORY
!
!       OPEN UNIT 20 FOR I/O
!
!       ***************************************************************
   call does_mac_dat_exist(EXISJK)
!       ***************************************************************
   uLIB = open_mac_dat()
!
   DO 10 I=1,MAXMAC
      READ(UNIT=uLIB,REC=I,ERR=10)MCDIR1(I),MCDIR2(1,I),MCDIR2(2,I)&
      &,MCDIR2(3,I),MCDIR3(I)
10 CONTINUE
   CALL CLOSE_FILE(uLIB,1)
!
!       IF MMIJ = 0 THEN FIND A BLANK SPOT IN THE DIRECTORY
!       ELSE MMIJ DESIGNATES THE MACRO TO BE MEDITED.
!
   IF(MMIJ.EQ.0) THEN
      OLDIJ=0
      DO 11 I=1,MAXMAC
         IF(MCDIR2(3,(I)).EQ.0) THEN
            MMIJ=I
            GO TO 12
         ELSE
         END IF
11    CONTINUE
   ELSE
!       MMIJ NOT = 0, PROCEED
   END IF
!
!       NOW WE HAVE A NON-ZERO MMIJ- A REAL PLACE TO GET AND STORE
!       A MACRO. IF OLDIJ = 0 DON'T BOTHER TO RETREIVE A MACRO FROM
!       POSITION MMIJ. IF OLDIJ = MAXMAC+1, THEN RETRIEVE A MACRO
!       NOW DEFINE THE ARRAYS WHICH HOLD THE MACRO.
12 CONTINUE
!       WAS THERE AN EXISTING MACRO TO EDIT?
!
   IF(OLDIJ.EQ.(MAXMAC+1)) THEN
!
!               YES THERE WAS
!
      NF=MMIJ
      CALL MACFIL
      EXST=.FALSE.
      CALL DOESMACROFILEEXIST(FILNAM, EXST)
      IF(.NOT.EXST) THEN
         CALL MACFAL
         RETURN
      END IF

      CALL READMACROFILE(trim(LIBMAC)//FILNAM)

!               SET CURRENT LINE
      CURLIN=0
   ELSE
!
!       THERE WAS NO EXISTING MACRO TO EDIT SO DON'T TRY
!       TO READ IN A MACRO.
!       THE MACRO DIRECTORY AND MACRO MEMORY ARE THEREFORE INITIALIZED.
      MCDIR1(MMIJ)=WQ
      MCDIR2(1,(MMIJ))=INT(W1)
!       LEAVE MCDIR2(3,(MMIJ))=0 TILL MACRO IS SUCCESFULLY STORED.
      MCDIR2(3,(MMIJ))=1
!
      MCDIR2(2,(MMIJ))=3
!       THE MACRO STORAGE IS NOW INITIALIZED.
      CALL MAXLNN(MCDIR1(MMIJ),MAXLL,MAXLIN)
      I=(MAXLL-1)
      MACCW(0:I)=' '
      MACQW(0:I)=' '
      MACSTR(0:I)=' '
      MACNW(1:5,0:I)=0.0D0
      MACSTA(1:20,0:I)=0
!       SET UP THE FIRST LINE OF THE NEW MACRO
      MACCW(0)='MACRO'
      MACQW(0)=MCDIR1(MMIJ)
      MACSTA(4,0)=1
      MACSTA(5,0)=1
      MACSTA(12,0)=1
      MACSTA(13,0)=1
      MACSTA(14,0)=1
      MACSTA(15,0)=1
      MACSTA(16,0)=1
!
!
!       SET CURRENT LINE
      CURLIN=1
!       SET FLAG F46 = 1 TO INDICATE A BLANK MACRO
      F46=1
   END IF
   RETURN
END
