!       FOURTH COLLECTION OF NSS FILES
!
SUBROUTINE NSSNR4(J)
   USE NSSMOD
!
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
   LOGICAL XODD,YODD,EXISTMEM,OPENMEN,NSSLPERROR,PERR,RAYERR
!
!       THIS IS SUBROUTINE NSSNR4.INC. THIS SUBROUTINE IMPLEMENTS
!       INTERSECTION CALCULATION TO A MEM SURFACE
!
   INTEGER I,J,K,ALLOERR,NX,NY,MEMNUM,XSTATE,YSTATE
!
   INTEGER XADD,YADD,IX,IY
!
   CHARACTER AMEMNUM*3,MEMFILE*10
!
   REAL*8 FNXP,FNYP,FNZP,MAG,XP,YP,ZP,NSSDELX,NSSDELY
!
   REAL*8 CENTERX,CENTERY,CENTERZ
!
   REAL*8 XCORR,YCORR,NEWRRX,NEWRRY,NEWRRZ,XERROR,YERROR
!
!
!       ARE THE MEM DIMENSIONS ODD OR EVEN
!       ODD OR EVEN
   NX=INT(NSSALENS(3,J))
   NY=INT(NSSALENS(4,J))
   XODD=.FALSE.
   IF((DBLE(NX)/2.0D0)-DBLE(NX/2).NE.0.0D0) XODD=.TRUE.
   YODD=.FALSE.
   IF((DBLE(NY)/2.0D0)-DBLE(NY/2).NE.0.0D0) YODD=.TRUE.
!
!       WHAT ARE THE FULL EXTENTS OF THE MEM PIXEL?
   NSSDELX=NSSALENS(6,J)
   NSSDELY=NSSALENS(7,J)
!
!       ALLOCATE THE ARRAYS THAT STORE THE MEM PIXEL CENTER LOCATIONS
!       AND INITIALIZE THEM TO ZERO
   ALLOCATE(MEMX(1:NX,1:NY),MEMY(1:NX,1:NY),STAT=ALLOERR)
   MEMX(1:NX,1:NY)=0.0D0
   MEMY(1:NX,1:NY)=0.0D0
!
!       ESTABLISH THE MEM PIXEL CENTER LOCATION VALUES
   IF(XODD) THEN
      DO I=0,NX-1
         DO K=1,NY
            MEMX(I+1,K)=-(NSSDELX*DBLE((NX-1)/2))+(DBLE(I)*NSSDELX)
         END DO
      END DO
   END IF
   IF(.NOT.XODD) THEN
      DO I=1,NX
         DO K=1,NY
            MEMX(I,K)=-(NSSDELX*(DBLE(NX+1)/2.0D0))+(DBLE(I)*NSSDELX)
         END DO
      END DO
   END IF
   IF(YODD) THEN
      DO I=1,NX
         DO K=0,NY-1
            MEMY(I,K+1)=-(NSSDELY*DBLE((NY-1)/2))+(DBLE(K)*NSSDELY)
         END DO
      END DO
   END IF
   IF(.NOT.YODD) THEN
      DO I=1,NX
         DO K=1,NY
            MEMY(I,K)=-(NSSDELY*(DBLE(NY+1)/2.0D0))+(DBLE(K)*NSSDELY)
         END DO
      END DO
   END IF
!
!       OPEN THE EXISTING MEMxxx.DAT FILE OR CREATE A NEW ONE.
!
!       MEM FILE NAME
   MEMNUM=INT(NSSALENS(5,J))
   IF(MEMNUM.LT.1.OR.MEMNUM.GT.999) THEN
      WRITE(OUTLYNE,*) 'MEM FILE NUMBER BEYOND LEGAL RANGE'
      CALL SHOWIT(1)
      NSS_INTERSECT=.FALSE.
      RETURN
   END IF
   CALL ITOAA(MEMNUM,AMEMNUM)
   MEMFILE='MEM'//AMEMNUM//'.DAT'
   EXISTMEM=.FALSE.
   INQUIRE(FILE=MEMFILE,EXIST=EXISTMEM)
!
!       ALLOCATE AND INITIALIZE MEM STATE AND MEM ERROR ARRAYS
   ALLOCATE (MEMXSTATE(1:NX,1:NY),MEMYSTATE(1:NX,1:NY)&
   &,STAT=ALLOERR)
   ALLOCATE (MEMXERROR(1:NX,1:NY),MEMYERROR(1:NX,1:NY)&
   &,STAT=ALLOERR)
   MEMXSTATE(1:NX,1:NY)=0
   MEMYSTATE(1:NX,1:NY)=0
   MEMXERROR(1:NX,1:NY)=0.0D0
   MEMYERROR(1:NX,1:NY)=0.0D0
!
   IF(EXISTMEM) THEN
      OPEN(UNIT=82,ACCESS='SEQUENTIAL',BLANK='NULL',&
      &FORM='FORMATTED',FILE=MEMFILE,&
      &STATUS='UNKNOWN')
      DO I=1,(NX*NY)
         READ(82,*) XADD,YADD,XSTATE,YSTATE,XERROR,YERROR
         MEMXSTATE(XADD,YADD)=XSTATE
         MEMYSTATE(XADD,YADD)=YSTATE
         MEMXERROR(XADD,YADD)=XERROR
         MEMYERROR(XADD,YADD)=YERROR
         IF(MEMXSTATE(XADD,YADD).NE.0.AND.MEMYSTATE(XADD,YADD).NE.0) THEN
            NSS_INTERSECT=.FALSE.
            WRITE(OUTLYNE,*)&
            &'EITHER THE X OR THE Y MEM STATE MAY BE NON-ZERO, BUT NOT BOTH'
            CALL SHOWIT(1)
            WRITE(OUTLYNE,*)&
            &'RAY TRACE STOPPED'
            CALL SHOWIT(1)
            CALL MACFAL
            RETURN
         END IF
      END DO
!       CLOSE AND SAVE THE MEMFILE
      CALL CLOSE_FILE(82,1)
   ELSE
!       CREATE AND LOAD FILE
      OPEN(UNIT=82,ACCESS='SEQUENTIAL',BLANK='NULL',&
      &FORM='FORMATTED',FILE=MEMFILE,&
      &STATUS='UNKNOWN')
      DO I=1,NY
         DO K=1,NX
!       THE DEFAULT OPTICAL STATE IS REFLECT
10          FORMAT(I5,I5,I2,I2,D23.15,D23.15)
            WRITE(82,10) K,I,0,0,0.0D0,0.0D0
            MEMXSTATE(K,I)=0
            MEMYSTATE(K,I)=0
            MEMXERROR(K,I)=0.0D0
            MEMYERROR(K,I)=0.0D0
         END DO
      END DO
!       CLOSE AND SAVE THE MEMFILE
      CALL CLOSE_FILE(82,1)
   END IF
!
!       WHAT CELL IS HIT BY THE RAY WITH COORDINATES RRX,RRY
   XCORR=MEMX(1,1)
   YCORR=MEMY(1,1)
   IX=NINT(((RRX-XCORR)/NSSDELX))+1
   IY=NINT(((RRY-YCORR)/NSSDELY))+1
   IF(IX.LT.1.OR.IX.GT.NX.OR.IY.LT.1.OR.IY.GT.NY) THEN
      NSS_INTERSECT=.FALSE.
      RETURN
   END IF
!
!       IS THAT CELL TILTED
   IF(MEMXSTATE(IX,IY).EQ.0.AND.MEMYSTATE(IX,IY).EQ.0.AND.&
   &MEMXERROR(IX,IY).EQ.0.0D0.AND.MEMYERROR(IX,IY).EQ.0.0D0) THEN
!       NO TILT
      RRZ=0.0D0
      CALL MEMPOWER(DBLE(IX),DBLE(IY),J,PERR)
      IF(PERR) THEN
         NSS_INTERSECT=.FALSE.
         RETURN
      END IF
      NSSLN=0.0D0
      NSSMN=0.0D0
      NSSNN=1.0D0
      NSS_INTERSECT=.TRUE.
   ELSE
!       MEM TILTED
      NSSLN=DSIN(&
      &((DBLE(MEMXSTATE(IX,IY))*NSSALENS(201,J))+MEMXERROR(IX,IY))&
      &*PII/180.0D0)
!
      NSSMN=-DSIN(&
      &((DBLE(MEMYSTATE(IX,IY))*NSSALENS(202,J))+MEMYERROR(IX,IY))&
      &*PII/180.0D0)
!
      NSSNN=DSQRT(1.0D0-(NSSLN**2)-(NSSMN**2))
!
!       IN THE PLANE OF THE UNTILTED PLANE, THE RAY COORDINATES
!       THE RAY HAS INITIAL COORDINATES RRX,RRY AND RRX AND DIRECTION COSINES
!       RRL,RRM AND RRN FOR A PLANE GOING THROUGH THE CENTER OF THE IX, IY PIXEL WITH DIRECTION COSINES
!       0,0 1. COMPUTE THE INTERSECTION WITH A PLANE GOING THROUGH THE CENTER OF THE IX, IY PIXEL WITH
!       NEW TILTED DIRECTION COSINES
!       PIXEL CENTER IS AT CENTERX,CENTERY AND CENTERZ
      CENTERX=XCORR+(DBLE(IX-1)*NSSDELX)
      CENTERY=YCORR+(DBLE(IY-1)*NSSDELY)
      CENTERZ=0.0D0
      RRZ=0.0D0
      CALL LINE_TO_PLANE &
      &(NEWRRX,NEWRRY,NEWRRZ,RRX,RRY,RRZ,RRL,RRM,RRN,&
      &CENTERX,CENTERY,CENTERZ &
      &,NSSLN,NSSMN,NSSNN,NSSLPERROR)
      IF(.NOT.NSSLPERROR) THEN
         RRX=NEWRRX
         RRY=NEWRRY
         RRZ=NEWRRZ
         PERR=.FALSE.
         CALL MEMPOWER(NEWRRX,NEWRRY,J,PERR)
         IF(PERR) THEN
            NSS_INTERSECT=.FALSE.
            RETURN
         END IF
         NSS_INTERSECT=.TRUE.
         IF(NSSALENS(210,J).NE.0.0D0) THEN
!       CODE GOES HERE TO KILL RAY IF IT IS OUTSIDE THE REFLECTIVE PART OF THE MEM
!       MEM PIXEL CENTER IS AT CENTERX,CENTERY,CENTERZ
!       MEM PIXEL REFLECTING EDGES ARE AT XMEMCOR(1:4),YMEMCOR(1:4)
!       RAY IS AT RRX,RRY,RRZ
            RAYERR=.FALSE.
            NSS_INTERSECT=.TRUE.
            CENTERX=XCORR+(DBLE(IX-1)*NSSDELX)
            CENTERY=YCORR+(DBLE(IY-1)*NSSDELY)
            XMEMCOR(1)=CENTERX-DABS(NSSALENS(208,J)/2.0D0)
            XMEMCOR(2)=CENTERX-DABS(NSSALENS(208,J)/2.0D0)
            XMEMCOR(3)=CENTERX+DABS(NSSALENS(208,J)/2.0D0)
            XMEMCOR(4)=CENTERX+DABS(NSSALENS(208,J)/2.0D0)
            YMEMCOR(1)=CENTERY+DABS(NSSALENS(209,J)/2.0D0)
            YMEMCOR(2)=CENTERY-DABS(NSSALENS(209,J)/2.0D0)
            YMEMCOR(3)=CENTERY-DABS(NSSALENS(209,J)/2.0D0)
            YMEMCOR(4)=CENTERY+DABS(NSSALENS(209,J)/2.0D0)
            CALL CHECKCLAP(RAYERR,CENTERX,CENTERY)
            IF(RAYERR) THEN
               NSS_INTERSECT=.FALSE.
            END IF
         END IF
         RETURN
      ELSE
         NSS_INTERSECT=.FALSE.
         RETURN
      END IF
   END IF
   RETURN
END
SUBROUTINE MEMPOWER(X,Y,J,PERR)
!       THIS APPLIES A SURFACE SLOPE ERROR TO THE MEM PIXEL WITH FUNCTIONAL FORM
!       R**2, R**4 R**6 R**8 AND R**10 BUT ASSUMES THE SURFACE DEFLECTION IS SMALL AND CAN BE IGNORED
!       IN ORDER TO KEEP RAY TRACING SPEED FAST. ADDED 4/22/2004
   USE NSSMOD
   use DATMAI
   IMPLICIT NONE
   REAL*8 X,Y,A2,A4,A6,A8,A10,LN,MN,MM,NX,NY,SGN,Q
   INTEGER J
   LOGICAL PERR
!       COMPUTE NEW SURFACE SLOPE BASED ON MEMX AND MEMY MEASURED FROM THE CENTER
!       OF THE MEM
!       F(X,Y,Z)=A2*(X**2+Y**2)
!       +A4*(X**2+Y**2)**2
!       +A6*(X**2+Y**2)**3
!       +A8*(X**2+Y**2)**4
!       +A10*(X**2+Y**2)**5
!
!       SURFACE X DERIVATIVE FUNCTION
   NX(X,Y,A2,A4,A6,A8,A10)=(&
   &((2.0D0*X)*A2)&
   &+((2.0D0*(((X**2)+(Y**2))**2)*2.0D0*X)*A4)&
   &+((3.0D0*(((X**2)+(Y**2))**2)*2.0D0*X)*A6)&
   &+((4.0D0*(((X**2)+(Y**2))**3)*2.0D0*X)*A8)&
   &+((5.0D0*(((X**2)+(Y**2))**4)*2.0D0*X)*A10)&
   &)
!       SURFACE Y DERIVATIVE FUNCTION
   NY(X,Y,A2,A4,A6,A8,A10)=(&
   &((2.0D0*Y)*A2)&
   &+((2.0D0*(((X**2)+(Y**2))**2)*2.0D0*Y)*A4)&
   &+((3.0D0*(((X**2)+(Y**2))**2)*2.0D0*Y)*A6)&
   &+((4.0D0*(((X**2)+(Y**2))**3)*2.0D0*Y)*A8)&
   &+((5.0D0*(((X**2)+(Y**2))**4)*2.0D0*Y)*A10)&
   &)
   PERR=.FALSE.
   A2=NSSALENS(203,J)
   A4=NSSALENS(204,J)
   A6=NSSALENS(205,J)
   A8=NSSALENS(206,J)
   A10=NSSALENS(207,J)
   IF(A2.NE.0.0D0.OR.A4.NE.0.0D0.OR.A6.NE.0.0D0.OR.A8.NE.0.0D0 &
   &.OR.A10.NE.0.0D0) THEN
      LN=-NX(X,Y,A2,A4,A6,A8,A10)
      MN=-NY(X,Y,A2,A4,A6,A8,A10)
   ELSE
      RETURN
   END IF
   NSSLN=NSSLN+LN
   NSSMN=NSSMN+MN
   IF(NSSNN.EQ.0.0D0) THEN
      SGN=1.0D0
   ELSE
      SGN=NSSNN/DABS(NSSNN)
   END IF
   Q=1.0D0-(NSSLN**2)-(NSSMN**2)
   IF(Q.LT.0.0D0) THEN
      PERR=.TRUE.
      RETURN
   ELSE
      NSSNN=SGN*DSQRT(Q)
   END IF
   RETURN
END

SUBROUTINE NSSSPOTSETUP
   USE NSSMOD
   USE GLOBALS
   use DATMAI
   IMPLICIT NONE
!
!       CREATE A NEW SPOT DISGRAM FILE OR PREPARE AN EXISTING FILE FOR
!       ADDITIONAL INPUT
   IF(NSSSPOTTYPE.EQ.0) THEN
      NSPOT=0
!       ERASE SPOT DIAGRAM FILE
      OPEN(UNIT=100,BLANK='NULL'&
      &,FORM='FORMATTED',FILE='NSSSPOT.DAT'&
      &,STATUS='UNKNOWN')
      CALL CLOSE_FILE(100,0)
      RETURN
   END IF
   IF(NSSSPOTTYPE.EQ.1) THEN
      NSPOT=0
!       ERASE AND CREATE A NEW SPOT DIAGRAM FILE
      OPEN(UNIT=100,BLANK='NULL'&
      &,FORM='FORMATTED',FILE='NSSSPOT.DAT'&
      &,STATUS='UNKNOWN')
      CALL CLOSE_FILE(100,0)
      OPEN(UNIT=100,ACCESS='SEQUENTIAL',BLANK='NULL'&
      &,FORM='FORMATTED',FILE='NSSSPOT.DAT'&
      &,STATUS='UNKNOWN')
      RETURN
   END IF
   IF(NSSSPOTTYPE.EQ.2) THEN
      OPEN(UNIT=100,BLANK='NULL'&
      &,FORM='FORMATTED',FILE='NSSSPOT.DAT'&
      &,STATUS='UNKNOWN')
      CALL CLOSE_FILE(100,0)
!       OPEN EXISTING SPOT DIAGRAM FILE SPOT DIAGRAM FILE
      OPEN(UNIT=100,ACCESS='APPEND',BLANK='NULL'&
      &,FORM='FORMATTED',FILE='NSSSPOT.DAT'&
      &,STATUS='UNKNOWN')
      RETURN
   END IF
END
