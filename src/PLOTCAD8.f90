SUBROUTINE FILCOB
   USE GLOBALS
!
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
!       THIS ROUTINE DOES COBS PATTERN FILLING
!
   INTEGER IK,IX(0:360),IY(0:360)
!
   REAL XWORLD,YWORLD
!

   PRINT *, "FILCOB Called"
!
   DO IK=0,360
!     FILL THE POLYGON WITH A CROSS HASHED PATTERN
      IX(IK)=(P1ARAY(IK,1,1))
      IY(IK)=(P1ARAY(IK,2,1))
   END DO
!     NOW DRAW THE FILL LINES
   CALL PENMV1(IX(1),IY(1),0)
   DO IK=1,180
      CALL PENMV1(IX(IK),IY(IK),1)
      CALL PENMV1(IX(360-IK),IY(360-IK),1)
   END DO
   CALL PENMV1(IX(90),IY(90),0)
   DO IK=90,0,-1
      CALL PENMV1(IX(IK),IY(IK),1)
      CALL PENMV1(IX(180-IK),IY(180-IK),1)
   END DO
   CALL PENMV1(IX(270),IY(270),0)
   DO IK=270,360
      CALL PENMV1(IX(IK),IY(IK),1)
      CALL PENMV1(IX(540-IK),IY(540-IK),1)
   END DO
   RETURN
END
