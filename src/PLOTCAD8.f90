! SUB PLTFIG.FOR
SUBROUTINE PLTFIG
!
   use DATHGR
   use DATLEN
   use DATMAI
   use command_utils, only: is_command_query
   IMPLICIT NONE
!
!       THIS ROUTINE DOES THE PLOTTING OF THE FIG
!
   CHARACTER TMY*8,DTY*10,BL20*20,BLNOTE*80,NNTT1*80
!
   INTEGER I,IB,COLPAS,NT1ANG,NT1SIZ,IIX,IIY,IIUD
!
!
   BL20='                    '
   BLNOTE=BL20//BL20//BL20//BL20
!
!     MOVE TO THE STARTING POSITION FOR THE FIG
!
   IF(is_command_query()) THEN
      OUTLYNE=&
      &'"PLOT FIGURE" PLOTS A FIGURE NAME'
      CALL SHOWIT(1)
      OUTLYNE='RE-ENTER COMMAND'
      CALL SHOWIT(1)
      RETURN
   END IF
   IF(SST.EQ.1.OR.S3.EQ.1.OR.S4.EQ.1.OR.S5.EQ.1) THEN
      OUTLYNE=&
      &'"PLOT FIGURE" ONLY TAKES OPTIONAL NUMERIC WORDS #1 AND #2'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   END IF


   IF(DF1.EQ.1) IIX=100
   IF(DF2.EQ.1) IIY=900
   IF(DF1.EQ.0) IIX=INT(W1)
   IF(DF2.EQ.0) IIY=INT(W2)
   IIUD=0
!     DO THE PLOTTING OF THE FIG
   NT1SIZ=1
   NT1ANG=0
   NNTT1=FIGTITLE
   IB=1
   DO I=80,1,-1
      IF(NNTT1(I:I).NE.' ') THEN
         IB=I
         GO TO 100
      END IF
   END DO
100 CONTINUE
   IF(NNTT1.NE.BLNOTE) THEN
      CALL MY_SETCHARASPECT(1.5,1.5)
      COLPAS=COLLBL
      CALL MY_COLTYP(COLPAS)
      IF(.NOT.PLEXIS) PLEXIS=.TRUE.
      CALL MY_JUSTSTRING(IIX,IIY,NNTT1(1:IB),NT1ANG,NT1SIZ,3,3)
   ELSE
!     FIGTITLE BLANK, NOT ACTION
   END IF
!
   RETURN
END
