      CS5='DN (N-FAN)'
      CS6='DP (N-FAN)'
!
      CS7='DNA (N-FAN)'
      CS8='DPA (N-FAN)'
   END IF
   IF(FANTYP.EQ.4) THEN
      CS5='DN (P-FAN)'
      CS6='DP (P-FAN)'
!
      CS7='DNA (P-FAN)'
      CS8='DPA (P-FAN)'
   END IF
   IF(FANTYP.EQ.5) THEN
      IF(QALTYP.NE.1) THEN
         CS5='DX (X-FAN)'
         CS6='DY (Y-FAN)'
!
         CS7='DXA (X-FAN)'
         CS8='DYA (Y-FAN)'
      ELSE
         CS5='OPD (X-FAN)'
         CS6='OPD (Y-FAN)'
!
         CS7='OPD (X-FAN)'
         CS8='OPD (Y-FAN)'
      END IF
   END IF
   IF(FANTYP.EQ.6) THEN
      IF(QALTYP.NE.1) THEN
         CS5='DY (X-FAN)'
         CS6='DX (Y-FAN)'
!
         CS7='DYA (X-FAN)'
         CS8='DXA (Y-FAN)'
      ELSE
         CS5='OPD (X-FAN)'
         CS6='OPD (Y-FAN)'
!
         CS7='OPD (X-FAN)'
         CS8='OPD (Y-FAN)'
      END IF
   END IF
!
   IF(FANNUM.EQ.1) THEN
      XFOB2=0.0D0
      YFOB2=0.0D0
      XFOB3=0.0D0
      YFOB3=0.0D0
   ELSE
   END IF
   IF(FANNUM.EQ.2) THEN
      XFOB3=0.0D0
      YFOB3=0.0D0
   ELSE
   END IF
!
!     DETERMINE FOB AND FIELD OF VIEW NAMES
   WRITE(B,180) REAL(XFOB1)
   READ(B,200) VALUE1
   XFOBN1='X-FOB = '//VALUE1
   WRITE(B,180) REAL(YFOB1)
   READ(B,200) VALUE1
   YFOBN1='Y-FOB = '//VALUE1
!
   WRITE(B,180) REAL(XFOB2)
   READ(B,200) VALUE1
   XFOBN2='X-FOB = '//VALUE1
   WRITE(B,180) REAL(YFOB2)
   READ(B,200) VALUE1
   YFOBN2='Y-FOB = '//VALUE1
!
   WRITE(B,180) REAL(XFOB3)
   READ(B,200) VALUE1
   XFOBN3='X-FOB = '//VALUE1
   WRITE(B,180) REAL(YFOB3)
   READ(B,200) VALUE1
   YFOBN3='Y-FOB = '//VALUE1
!
   IF(abs(surf_thickness(NEWOBJ)) >= 1.0D10) THEN
!     FIELD ANGLE
      VAA=XFOB1*sys_scy_fang()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      XN1='X-F.O.V. (DEG) = '//VALUE1
      VAA=YFOB1*sys_scy_fang()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      YN1='Y-F.O.V. (DEG) = '//VALUE1
!
      VAA=XFOB2*sys_scy_fang()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      XN2='X-F.O.V. (DEG) = '//VALUE1
      VAA=YFOB2*sys_scy_fang()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      YN2='Y-F.O.V. (DEG) = '//VALUE1
!
      VAA=XFOB3*sys_scy_fang()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      XN3='X-F.O.V. (DEG) = '//VALUE1
      VAA=YFOB3*sys_scy_fang()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      YN3='Y-F.O.V. (DEG) = '//VALUE1
!
   ELSE
!     FIELD HEIGHT
      VAA=XFOB1*sys_scy()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      IF(sys_units().EQ.1.0D0) XN1='X-OBJ. HT. (IN) = '//VALUE1
      IF(sys_units().EQ.2.0D0) XN1='X-OBJ. HT. (CM) = '//VALUE1
      IF(sys_units().EQ.3.0D0) XN1='X-OBJ. HT. (MM) = '//VALUE1
      IF(sys_units().EQ.4.0D0) XN1='X-OBJ. HT. (M)  = '//VALUE1
      VAA=YFOB1*sys_scy()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      IF(sys_units().EQ.1.0D0) YN1='Y-OBJ. HT. (IN) = '//VALUE1
      IF(sys_units().EQ.2.0D0) YN1='Y-OBJ. HT. (CM) = '//VALUE1
      IF(sys_units().EQ.3.0D0) YN1='Y-OBJ. HT. (MM) = '//VALUE1
      IF(sys_units().EQ.4.0D0) YN1='Y-OBJ. HT. (M)  = '//VALUE1
!
      VAA=XFOB2*sys_scy()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      IF(sys_units().EQ.1.0D0) XN2='X-OBJ. HT. (IN) = '//VALUE1
      IF(sys_units().EQ.2.0D0) XN2='X-OBJ. HT. (CM) = '//VALUE1
      IF(sys_units().EQ.3.0D0) XN2='X-OBJ. HT. (MM) = '//VALUE1
      IF(sys_units().EQ.4.0D0) XN2='X-OBJ. HT. (M)  = '//VALUE1
      VAA=YFOB2*sys_scy()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      IF(sys_units().EQ.1.0D0) YN2='Y-OBJ. HT. (IN) = '//VALUE1
      IF(sys_units().EQ.2.0D0) YN2='Y-OBJ. HT. (CM) = '//VALUE1
      IF(sys_units().EQ.3.0D0) YN2='Y-OBJ. HT. (MM) = '//VALUE1
      IF(sys_units().EQ.4.0D0) YN2='Y-OBJ. HT. (M)  = '//VALUE1
!
      VAA=XFOB3*sys_scy()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      IF(sys_units().EQ.1.0D0) XN3='X-OBJ. HT. (IN) = '//VALUE1
      IF(sys_units().EQ.2.0D0) XN3='X-OBJ. HT. (CM) = '//VALUE1
      IF(sys_units().EQ.3.0D0) XN3='X-OBJ. HT. (MM) = '//VALUE1
      IF(sys_units().EQ.4.0D0) XN3='X-OBJ. HT. (M)  = '//VALUE1
      VAA=YFOB3*sys_scy()
      WRITE(B,180)REAL(VAA)
      READ(B,200) VALUE1
      IF(sys_units().EQ.1.0D0) YN3='Y-OBJ. HT. (IN) = '//VALUE1
      IF(sys_units().EQ.2.0D0) YN3='Y-OBJ. HT. (CM) = '//VALUE1
      IF(sys_units().EQ.3.0D0) YN3='Y-OBJ. HT. (MM) = '//VALUE1
      IF(sys_units().EQ.4.0D0) YN3='Y-OBJ. HT. (M)  = '//VALUE1
!
   END IF

!     SET UP LABEL FOR TOP OF ABERRATION AXES
   IF(FANTYP.LE.4.AND.QALTYP.EQ.1) THEN
      XTOPLB=CS3
      YTOPLB=CS4
   ELSE
!     NOT OPD SINGLE FAN
      IF(sys_mode().EQ.1.0D0.OR.sys_mode().EQ.2.0D0) THEN
!     FOCAL
         XTOPLB=CS5
         YTOPLB=CS6
      ELSE
!     AFOCAL
         XTOPLB=CS7
         YTOPLB=CS8
      END IF
   END IF
!
!     SET LETTER SIZE AND ANGLE
   NT1SIZ=1
   NT1ANG=0
   CALL MY_SETCHARASPECT(1.5,1.5)
!
!     LIFT PEN, MOVE TO BOX START
   COLPAS=COLFRM
   CALL MY_COLTYP(COLPAS)
   CALL MY_PLOT(0,0,0,0,-10,10010,-10,10010)
!     DROP PEN, DRAW BOX
