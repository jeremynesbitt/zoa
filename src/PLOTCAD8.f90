!     NOW DRAW THE FAN AXES
!     THE Y COORDINATES ARE ALWAYS AT 3250 AND 5750
!     UNLESS IT IS AN OPD PLOT THEN THE Y COORDINATE IS 4500
!     THE Y EXTENT IS A TOTAL OF 2000 UNITS
!     THE X EXTENT IS A TOTAL OF 2500 UNITS
!     THE X CENTER IS AT 5000
!
      IF(FANTYP.LE.4.AND.QALTYP.NE.1.OR.FANTYP.GT.4) THEN
!     LEFT FAN AXES
!     DRAW X-AXES
         COLPAS=COLFRM
