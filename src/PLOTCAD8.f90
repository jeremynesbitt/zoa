! SUB DOAX.FOR
SUBROUTINE DOAX
!
   use DATHGR
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
!       THIS ROUTINE DOES THE PLOTTING OF THE AXES IN LENS PLOTTING
!
   CHARACTER XXX*1,YYY*1,ZZZ*1
!
   REAL*8 A0,XO,YO,ZO,XX,YX,ZX,XY,YY,ZY,XZ,YZ,ZZ &
   &,ROT1X,ROT1Z,ROT2Z,ROT2Y,AX,AY,AZ,APHI,AALF,XN,YN,ZN &
   &,X,Y,Z,VIEPH,VIEAL,ZETA,DELXX,DELYY,AA1
!
   INTEGER COLPAS,IIX,IIY,IIUD,NT1SZ
!
!
!     LOOK.VIEW TRANSFORMS

   ROT1X(AX,AZ,APHI)=((AX*DCOS(APHI))-(AZ*DSIN(APHI)))
   ROT1Z(AX,AZ,APHI)=((AX*DSIN(APHI))+(AZ*DCOS(APHI)))
!
   ROT2Z(AZ,AY,AALF)=((AZ*DCOS(AALF))+(AY*DSIN(AALF)))
   ROT2Y(AZ,AY,AALF)=((-AZ*DSIN(AALF))+(AY*DCOS(AALF)))
!
   COLPAS=COLAXS
   CALL MY_COLTYP(COLPAS)
!
   VIEPH=(PII/180.0D0)*VIEPHI
   VIEAL=(PII/180.0D0)*VIEALF
!
   NT1SZ=1
   CALL MY_SETCHARASPECT(1.5,1.5)
!
   XXX='X'
   YYY='Y'
   ZZZ='Z'
!
!     MOVE TO THE STARTING POSITION FOR THE ORIGIN OF THE AXES
!
!     THE LENGTH OF THE AXES ARE 500 UNITS
   A0=500.0D0
!     BEFORE AND AFTER ROTATION, THE ORIGIN IS ALWAYS AT X=8900,Y=1100
!     THE COORDINATE OF THE POSITIVE END OF THE X AXIS IS AT:
!     X=9400,Y=1100,Z=0
!     THE COORDINATE OF THE POSITIVE END OF THE Y AXIS IS AT:
!     X=9400,Y=1600,Z=0
!     THE COORDINATE OF THE POSITIVE END OF THE Z AXIS IS AT:
!     X=8900,Y=1100,Z=500
!
   XO=8900.0D0
   YO=1100.0D0
   ZO=0.0D0
!
   XX=XO+A0
   YX=YO
   ZX=ZO
!
   XY=XO
   YY=YO+A0
   ZY=ZO
!
   XZ=XO
   YZ=YO
   ZZ=ZO+A0
!
!     NOW APPLY THE ROTATIONS FROM PLOT LOOK
!     THE ORIGIN NEVER MOVES
   X=XX-XO
   Y=YX-YO
   Z=ZX-ZO
   XN=ROT1X(X,Z,VIEPH)
   YN=Y
   ZN=ROT1Z(X,Z,VIEPH)
   X=XN
   Y=YN
   Z=ZN
   XN=X
   YN=ROT2Y(Z,Y,VIEAL)
   ZN=ROT2Z(Z,Y,VIEAL)
   XX=XN+XO
   YX=YN+YO
   ZX=ZN+ZO
!
   X=XY-XO
   Y=YY-YO
   Z=ZY-ZO
   XN=ROT1X(X,Z,VIEPH)
   YN=Y
   ZN=ROT1Z(X,Z,VIEPH)
   X=XN
   Y=YN
   Z=ZN
   XN=X
   YN=ROT2Y(Z,Y,VIEAL)
   ZN=ROT2Z(Z,Y,VIEAL)
   XY=XN+XO
   YY=YN+YO
   ZY=ZN+ZO
!
   X=XZ-XO
   Y=YZ-YO
   Z=ZZ-ZO
   XN=ROT1X(X,Z,VIEPH)
   YN=Y
   ZN=ROT1Z(X,Z,VIEPH)
   X=XN
   Y=YN
   Z=ZN
   XN=X
   YN=ROT2Y(Z,Y,VIEAL)
   ZN=ROT2Z(Z,Y,VIEAL)
   XZ=XN+XO
   YZ=YN+YO
   ZZ=ZN+ZO
!
!
!     NOW CONVERT TO INTEGERS AND DRAW THE 3 LINES WITH EACH LINE TERMINATING
!     WITH THE APPROPRIATE AXIS LABEL
!
!     MOVE TO THE STARTING POSITION FOR THE ORIGIN OF THE AXES
   IIX=8900
   IIY=1100
   IIUD=0
   CALL MY_PLOT(IIX,IIY,IIUD,0,-10,10010,-10,7010)
!     DRAW TO THE TIP OF THE X-AXIS
   IIX=INT(XX)
   IIY=INT(YX)
   IIUD=1
   CALL MY_PLOT(IIX,IIY,IIUD,0,-10,10010,-10,7010)
!
!     LABEL THE AXIS
!
   AA1=88.0D0
   IF(YX.EQ.1100.AND.XX.EQ.8900) THEN
!     DON'T WRITE AN AXIS LABEL
   ELSE
!     WRITE LABEL
      IF(DABS(YX-1100.0D0).LE.1.0D-15.AND.&
      &DABS(XX-8900.0D0).LE.1.0D-15) THEN
         ZETA=0.0D0
      ELSE
         ZETA=DATAN2((YX-1100.0D0),(XX-8900.0D0))
      END IF
      DELYY=AA1*DSIN(ZETA)
      DELXX=AA1*DCOS(ZETA)
      YX=YX+DELYY
      XX=XX+DELXX
      COLPAS=COLLBL
      CALL MY_COLTYP(COLPAS)
      CALL MY_JUSTSTRING(INT(XX)-40,INT(YX)-40,XXX,0,NT1SZ,3,3)
      COLPAS=COLAXS
      CALL MY_COLTYP(COLPAS)
   END IF
!
!     MOVE TO THE STARTING POSITION FOR THE ORIGIN OF THE AXES
   IIX=8900
   IIY=1100
   IIUD=0
   CALL MY_PLOT(IIX,IIY,IIUD,0,-10,10010,-10,7010)
!     DRAW TO THE TIP OF THE Y-AXIS
   IIX=INT(XY)
   IIY=INT(YY)
   IIUD=1
   CALL MY_PLOT(IIX,IIY,IIUD,0,-10,10010,-10,7010)
!
   IF(YY.EQ.1100.AND.XY.EQ.8900) THEN
!     DON'T WRITE AN AXIS LABEL
   ELSE
!     WRITE LABEL
      IF(DABS(YY-1100.0D0).LE.1.0D-15.AND.&
      &DABS(XY-8900.0D0).LE.1.0D-15) THEN
         ZETA=0.0D0
      ELSE
         ZETA=DATAN2((YY-1100.0D0),(XY-8900.0D0))
      END IF
      DELYY=AA1*DSIN(ZETA)
      DELXX=AA1*DCOS(ZETA)
      YY=YY+DELYY
      XY=XY+DELXX
      COLPAS=COLLBL
      CALL MY_COLTYP(COLPAS)
      CALL MY_JUSTSTRING(INT(XY)-40,INT(YY)-40,YYY,0,NT1SZ,3,3)
      COLPAS=COLAXS
      CALL MY_COLTYP(COLPAS)
   END IF
!
!     MOVE TO THE STARTING POSITION FOR THE ORIGIN OF THE AXES
   IIX=8900
   IIY=1100
   IIUD=0
   CALL MY_PLOT(IIX,IIY,IIUD,0,-10,10010,-10,7010)
!     DRAW TO THE TIP OF THE Z-AXIS
   IIX=INT(XZ)
   IIY=INT(YZ)
   IIUD=1
   CALL MY_PLOT(IIX,IIY,IIUD,0,-10,10010,-10,7010)
!
   IF(YZ.EQ.1100.AND.XZ.EQ.8900) THEN
!     DON'T WRITE AN AXIS LABEL
   ELSE
!     WRITE LABEL
      IF(DABS(YZ-1100.0D0).LE.1.0D-15.AND.&
      &DABS(XZ-8900.0D0).LE.1.0D-15) THEN
         ZETA=0.0D0
      ELSE
         ZETA=DATAN2((YZ-1100.0D0),(XZ-8900.0D0))
      END IF
      DELYY=AA1*DSIN(ZETA)
      DELXX=AA1*DCOS(ZETA)
      YZ=YZ+DELYY
      XZ=XZ+DELXX
      COLPAS=COLLBL
      CALL MY_COLTYP(COLPAS)
      CALL MY_JUSTSTRING(INT(XZ)-40,INT(YZ)-40,ZZZ,0,NT1SZ,3,3)
      COLPAS=COLAXS
      CALL MY_COLTYP(COLPAS)
   END IF
   RETURN
END
