! SUB FRMBOX
SUBROUTINE FRMBOX
!
   use DATHGR
   use DATLEN
   use DATMAI
   use mod_surface, only: surf_thickness
   use mod_system, only: sys_units, sys_scy, sys_scy_fang, sys_mode
   IMPLICIT NONE
!
!       THIS ROUTINE DRAWS THE BOX FRAMING FOR ALL RAY FAN
!       PLOTTING
!
   INTEGER IJKX,IJKY
!
   REAL*8 VAA
!
   CHARACTER CS1*4,CS2*4,CS3*11,CS4*11,CS5*11,CS6*11,CS7*11,CS8*11 &
   &,XTOPLB*11,XN1*28,XN2*28,XN3*28,YN1*28,YN2*28,YN3*28,YFOBN3*18,&
   &YTOPLB*11,XFOBN1*18,XFOBN2*18,XFOBN3*18,YFOBN1*18,YFOBN2*18,&
   &B*140,VALUE1*8
!
   INTEGER COLPAS,NT1ANG,NT1SIZ
!
!
   CS1='+1.0'
   CS2='-1.0'
!
   IF(FANTYP.EQ.1) THEN
      CS3='OPD (Y-FAN)'
      CS4='OPD (Y-FAN)'
   END IF
   IF(FANTYP.EQ.2) THEN
      CS3='OPD (X-FAN)'
      CS4='OPD (X-FAN)'
   END IF
   IF(FANTYP.EQ.3) THEN
      CS3='OPD (N-FAN)'
      CS4='OPD (N-FAN)'
   END IF
   IF(FANTYP.EQ.4) THEN
      CS3='OPD (P-FAN)'
      CS4='OPD (P-FAN)'
   END IF
!
   IF(FANTYP.EQ.1) THEN
      CS5='DX (Y-FAN)'
      CS6='DY (Y-FAN)'
!
      CS7='DXA (Y-FAN)'
      CS8='DYA (Y-FAN)'
   END IF
   IF(FANTYP.EQ.2) THEN
      CS5='DX (X-FAN)'
      CS6='DY (X-FAN)'
!
      CS7='DXA (X-FAN)'
      CS8='DYA (X-FAN)'
   END IF
