         CALL SHOWIT(0)
         WRITE(OUTLYNE,118) I,REFRY(19,I)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,119) I,REFRY(20,I)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,120) I,REFRY(21,I)
         CALL SHOWIT(0)
         WRITE(OUTLYNE,121) I,REFRY(22,I)
         CALL SHOWIT(0)
