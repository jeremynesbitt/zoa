! SUB DEGRAD.FOR
SUBROUTINE DEGRAD
!
   use DATMAI
   IMPLICIT NONE
!
!       THIS SUBROUTINE IS CALLED TO SET RAYTRACE MODE TO
!       DEGRRES, RADIANS OR TANGENTS
!
   LOGICAL RAD,DEG,TANG
!
   COMMON/ANGMOD/DEG,RAD,TANG
!
!
   IF(SQ.EQ.1.OR.SST.EQ.1.OR.SN.EQ.1) THEN
      OUTLYNE='"DEG","RAD","TANGENT" AND "ANGMODE" COMMANDS'
      CALL SHOWIT(1)
      OUTLYNE=&
      &'TAKE NO EXPLICIT INPUT'
      CALL SHOWIT(1)
      CALL REPORT_ERROR_AND_FAIL('RE-ENTER COMMAND', 1)
      RETURN
   ELSE
   END IF
   IF(WC.EQ.'RAD') THEN
      RAD=.TRUE.
      DEG=.FALSE.
      TANG=.FALSE.
      RETURN
   ELSE
   END IF
   IF(WC.EQ.'DEG') THEN
      DEG=.TRUE.
      RAD=.FALSE.
      TANG=.FALSE.
      RETURN
   ELSE
   END IF
   IF(WC.EQ.'TANGENT') THEN
      TANG=.TRUE.
      DEG=.FALSE.
      RAD=.FALSE.
      RETURN
   ELSE
   END IF
   IF(WC.EQ.'ANGMODE') THEN
      IF(DEG) THEN
         OUTLYNE='RAYTRACE ANGULAR MODE SET TO "DEGREES"'
         CALL SHOWIT(1)
         RETURN
      ELSE
      END IF
      IF(RAD) THEN
         OUTLYNE='RAYTRACE ANGULAR MODE SET TO "RADIANS"'
         CALL SHOWIT(1)
         RETURN
      ELSE
      END IF
      IF(TANG) THEN
         OUTLYNE='RAYTRACE ANGULAR MODE SET TO "TANGENT"'
         CALL SHOWIT(1)
         RETURN
      END IF
   END IF
   RETURN
END
! SUB FOPD.FOR
SUBROUTINE FOPD
!
   use DATLEN
   use DATMAI
   IMPLICIT NONE
!
!       THIS IS SUBROUTINE FOPD.FOR. THIS SUBROUTINE IS
!       CALLED BY FANS AND AND OTHER ROUTINES. IT CALCULATES
!       ADJUSTMENTS TO THE
!       OPD DUE TO THE OBJECT REFERENCE SPHERE.
!     OCOR IS ALWAYS ADDED WHERE AS RCOR IS ALWAYS SUBTRACTED
!
   REAL*8 ROBJ,X1,Y1,Z1,RX0,RY0,RZ0,RLOLD,RMOLD,&
   &RNOLD,L,M,N,T,D,X2A,X2B,Y2A,Y2B,Z2A,Z2B,DA,DB,X2,Y2,Z2 &
   &,X3,Y3,Z3
!
!
   RCOR=0.0D0
   OCOR=0.0D0
!
!       IS THE BEGINNING REFERENCE SPHERE A SHERE OR A FLAT?
   D=DSQRT(((REFRY(16,NEWOBJ+1)-REFRY(1,NEWOBJ+1))**2)&
   &+((REFRY(17,NEWOBJ+1)-REFRY(2,NEWOBJ+1))**2)&
   &+((REFRY(18,NEWOBJ+1)-REFRY(3,NEWOBJ+1))**2))
!
!     D IS THE MAGNITUDE OF THE REFERENCE SPHERE RADIUS
!
   IF(D.GE.1.0D10) THEN
!       SURFACE IS FLAT, CORRECTION FROM NEWOBJ TO THE PLANE IS ZERO.
!       THE REFERENCE PLANE IS PERPENDICULAR TO THE REFERENCE RAY
!       AND HAS ITS ORIGIN WHERE THE CHIEF RAY INTERSECTS THE SURFACE
!       NEWOBJ+1
!       THE EQUATION OF THIS PLANE IS:
!       (X-RX0)*RLOLD + (Y-RY0)*RMOLD +(Z-RZ0)*RNOLD = 0
!       WHERE:
      RX0=REFRY(1,NEWOBJ+1)
      RY0=REFRY(2,NEWOBJ+1)
      RZ0=REFRY(3,NEWOBJ+1)
      RLOLD=REFRY(19,NEWOBJ+1)
      RMOLD=REFRY(20,NEWOBJ+1)
      RNOLD=REFRY(21,NEWOBJ+1)
!
!       WITH THIS DEFINITION, RCOR IS ALWAYS 0.0D0
      RCOR=0.0D0
!       OCOR IS THE SIGNED LENGTH ALONG THE "OTHER" RAY
!       BETWEEN THE INTERSECTION WITH THIS PLANE AND THE INTERSECTION
!       WITH THE SURFACE NEWOBJ+1.
!       THE "OTHER" RAY HITS SURFACE NEWOBJ+1 AT
      X1=RAYRAY(1,NEWOBJ+1)
      Y1=RAYRAY(2,NEWOBJ+1)
      Z1=RAYRAY(3,NEWOBJ+1)
!       ITS DIRECTION COSINES ARE
      L=RAYRAY(19,NEWOBJ+1)
      M=RAYRAY(20,NEWOBJ+1)
      N=RAYRAY(21,NEWOBJ+1)
!
!       THE OTHER RAY CAN BE REPRESENTED AS:
!
!       X=X1+(L*T)
!       Y=Y1+(M*T)
!       Z=Y1+(N*T)
!       T IS THE DISTANCE FROM THE NEWOBJ+1 SURFACE INTERSECTION TO THE
!       PLANE INTERSECTION AND IS:
!
      T=((RLOLD*(RX0-X1))+(RMOLD*(RY0-Y1))+(RNOLD*(RZ0-Z1)))/&
      &((L*RLOLD)+(M*RMOLD)+(N*RNOLD))
!       THE OTHER RAY INTERSECTS THE PLANE AT:
!
      X2=X1+(L*T)
      Y2=Y1+(M*T)
      Z2=Z1+(N*T)
!       THE CORRECTION TERM IS JUST:
      OCOR=DSQRT(((X2-X1)**2)+((Y2-Y1)**2)+((Z2-Z1)**2))
      IF(.NOT.REVSTR) THEN
!     FORWARD RAY
!     IF IT HITS THE PLANE BEFORE IT HITS NEWOBJ+1
         IF(Z1.GE.Z2) OCOR=-OCOR
!     IF IT HITS THE PLANE AFTER IT HITS NEWOBJ+1
         IF(Z1.LT.Z2) OCOR=OCOR
      ELSE
!     REVERSING RAY, FLIP SIGN
!     IF IT HITS THE PLANE BEFORE IT HITS NEWOBJ+1
         IF(Z1.GE.Z2) OCOR=OCOR
!     IF IT HITS THE PLANE AFTER IT HITS NEWOBJ+1
         IF(Z1.LT.Z2) OCOR=-OCOR
      END IF
      IF(REFRY(6,NEWOBJ).LT.0.0D0.AND..NOT.REVSTR.OR.&
      &REFRY(6,NEWOBJ).GT.0.0D0.AND.REVSTR) THEN
         OCOR=-OCOR
         RCOR=-RCOR
      END IF
      GO TO 900
   ELSE
!       NOT FLAT, RADIUS IS ROBJ AND CENTER IS AT
!       COORDINATES OF REFERENCE RAY AT NEWOBJ.
!
      RCOR=D
      OCOR=D
!     NOW FOR THE SIGN OF RCOR AND OCOR
!
      IF(.NOT.REVSTR) THEN
         OCOR=OCOR
         RCOR=RCOR
      ELSE
!     FLIP SIGN
         OCOR=-OCOR
         RCOR=-RCOR
      END IF
   END IF
   IF(REFRY(6,NEWOBJ).LT.0.0D0.AND..NOT.REVSTR.OR.&
   &REFRY(6,NEWOBJ).GT.0.0D0.AND.REVSTR) THEN
      OCOR=-OCOR
      RCOR=-RCOR
   END IF
900 CONTINUE
   IF(DABS(RCOR).GT.1.0D10.OR.DABS(OCOR).GT.1.0D10) THEN
!     INFINITE REFERENCE SPHERE
   ELSE
   END IF
   RETURN
END
